class I4DRFV3(bt.Indicator):
    lines = ('color', 'value')
    params = dict(period=11)

    def __init__(self):
        self.addminperiod(int(self.p.period) + 2)

    def next(self):
        total = 0.0
        period = int(self.p.period)
        for i in range(period):
            high_diff = float(self.data.high[-i]) - float(self.data.high[-(i + 1)])
            low_diff = float(self.data.low[-i]) - float(self.data.low[-(i + 1)])
            if high_diff > 0:
                total += 1.0
            if low_diff < 0:
                total -= 1.0
        value = total / float(period) * 100.0
        self.lines.value[0] = value
        self.lines.color[0] = 1.0 if value > 0 else 0.0


class ExpI4DRFV3Strategy(bt.Strategy):
    params = dict(
        period=11,
        signal_bar=1,
        trend_mode='DIRECT',
        stop_loss_points=1000,
        take_profit_points=2000,
        fixed_lot=0.1,
        point=0.01,
        buy_pos_open=True,
        sell_pos_open=True,
        buy_pos_close=True,
        sell_pos_close=True,
        indicator_minutes=240,
    )

    def __init__(self):
        self.base = self.datas[0]
        self.signal_data = self.datas[1]
        self.ind = I4DRFV3(self.signal_data, period=self.p.period)
        self.signal_count = 0
        self.trade_count = 0
        self.buy_count = 0
        self.sell_count = 0
        self.win_count = 0
        self.loss_count = 0
        self.bar_num = 0
        self._position_was_open = False
        self._last_signal_len = 0

    def _check_exit_levels(self):
        if not self.position: return False
        cp = float(self.base.close[0]); pv = float(self.p.point); ep = float(self.position.price)
        sd = self.p.stop_loss_points * pv; td = self.p.take_profit_points * pv
        if self.position.size > 0 and (cp <= ep - sd or cp >= ep + td): self.close(); return True
        if self.position.size < 0 and (cp >= ep + sd or cp <= ep - td): self.close(); return True
        return False

    def next(self):
        self.bar_num += 1
        if self._check_exit_levels(): return
        sb = max(int(self.p.signal_bar) - 1, 0)
        if len(self.signal_data) < int(self.p.period) + sb + 3: return
        if len(self.signal_data) == self._last_signal_len: return
        self._last_signal_len = len(self.signal_data)
        c0 = float(self.ind.color[-sb]) if sb else float(self.ind.color[0])
        c1 = float(self.ind.color[-(sb + 1)])
        direct = str(self.p.trend_mode).upper() == 'DIRECT'
        if direct:
            buy_open = c1 == 1.0 and c0 < 1.0 and self.p.buy_pos_open
            sell_open = c1 == 0.0 and c0 > 0.0 and self.p.sell_pos_open
        else:
            buy_open = c1 == 0.0 and c0 > 0.0 and self.p.buy_pos_open
            sell_open = c1 == 1.0 and c0 < 1.0 and self.p.sell_pos_open
        buy_close = sell_open and self.p.buy_pos_close
        sell_close = buy_open and self.p.sell_pos_close
        if sell_close and self.position.size < 0: self.close()
        if buy_close and self.position.size > 0: self.close()
        if buy_open and self.position.size <= 0:
            self.signal_count += 1
            self.buy(size=float(self.p.fixed_lot))
        if sell_open and self.position.size >= 0:
            self.signal_count += 1
            self.sell(size=float(self.p.fixed_lot))

    def notify_trade(self, trade):
        if trade.isopen and not self._position_was_open:
            self._position_was_open = True
            if trade.size > 0: self.buy_count += 1
            elif trade.size < 0: self.sell_count += 1
            return
        if not trade.isclosed: return
        self.trade_count += 1
        if trade.pnlcomm >= 0: self.win_count += 1
        else: self.loss_count += 1
        self._position_was_open = False



