def resample_frame(df, rule):
    """Resample an OHLCV DataFrame to a coarser timeframe.

    Args:
        df: Source DataFrame indexed by datetime with OHLCV and volume columns.
        rule: Pandas offset alias defining the target bar size.

    Returns:
        A resampled DataFrame aggregated with first/max/min/last/sum rules, with
        rows lacking OHLC data dropped and openinterest gaps filled with zero.
    """
    out = df.resample(rule, label='right', closed='right').agg({
        'open': 'first',
        'high': 'max',
        'low': 'min',
        'close': 'last',
        'tick_volume': 'sum',
        'real_volume': 'sum',
        'openinterest': 'last',
    })
    out = out.dropna(subset=['open', 'high', 'low', 'close'])
    out['openinterest'] = out['openinterest'].fillna(0)
    return out


def resolve_price(frame, price_mode):
    """Select an applied-price series from an OHLC frame.

    Args:
        frame: DataFrame with open, high, low, and close columns.
        price_mode: One of the ``PRICE_*`` mode constants selecting how the
            applied price is derived.

    Returns:
        A Series of applied prices for the requested mode, defaulting to close.
    """
    if price_mode == PRICE_OPEN:
        return frame['open']
    if price_mode == PRICE_HIGH:
        return frame['high']
    if price_mode == PRICE_LOW:
        return frame['low']
    if price_mode == PRICE_MEDIAN:
        return (frame['high'] + frame['low']) / 2.0
    if price_mode == PRICE_TYPICAL:
        return (frame['high'] + frame['low'] + frame['close']) / 3.0
    if price_mode == PRICE_WEIGHTED:
        return (frame['high'] + frame['low'] + frame['close'] + frame['close']) / 4.0
    if price_mode == PRICE_SIMPL:
        return (frame['open'] + frame['close']) / 2.0
    if price_mode == PRICE_QUARTER:
        return (frame['high'] + frame['low'] + frame['open'] + frame['close']) / 4.0
    if price_mode == PRICE_TRENDFOLLOW0:
        return (frame['high'] + frame['low'] + frame['close']) / 3.0 + (frame['close'] - frame['open'])
    if price_mode == PRICE_TRENDFOLLOW1:
        return (frame['high'] + frame['low'] + frame['close']) / 3.0 + (frame['open'] - frame['close'])
    if price_mode == PRICE_DEMARK:
        x = frame['close'] * 2 + frame['low'] + frame['high']
        x = np.where(frame['close'] < frame['open'], frame['high'] + frame['low'] + frame['close'] * 2, x)
        x = np.where(frame['close'] > frame['open'], frame['high'] + frame['low'] + frame['high'] + frame['low'], x)
        return pd.Series(x / 4.0, index=frame.index)
    return frame['close']


def smooth_series(series, period, method='MODE_EMA'):
    """Smooth a series using the requested moving-average method.

    Args:
        series: Series of input values.
        period: Window length; values below 1 are clamped to 1.
        method: Moving-average mode (``MODE_SMA``, ``MODE_SMMA``, ``MODE_LWMA``);
            anything else falls back to an bt.indicators.EMA.

    Returns:
        A Series smoothed with the selected method.
    """
    period = max(int(period), 1)
    if method == 'MODE_SMA':
        return series.rolling(period, min_periods=period).mean()
    if method == 'MODE_SMMA':
        return series.ewm(alpha=1.0 / period, adjust=False).mean()
    if method == 'MODE_LWMA':
        weights = np.arange(1, period + 1, dtype=float)
        return series.rolling(period, min_periods=period).apply(lambda x: np.dot(x, weights) / weights.sum(), raw=True)
    return series.ewm(span=period, adjust=False).mean()


def compute_xpvt(frame, volume_type='VOLUME_TICK', xma_method='MODE_EMA', xlength=5, ipc=PRICE_CLOSE):
    """Compute the XPVT indicator and its signal line for a frame.

    Args:
        frame: Resampled OHLCV DataFrame with tick_volume/real_volume columns.
        volume_type: ``VOLUME_TICK`` to use tick volume, otherwise real volume.
        xma_method: Moving-average method used to smooth the PVT into a signal.
        xlength: Smoothing window length for the signal line.
        ipc: Applied-price mode passed to ``resolve_price``.

    Returns:
        A copy of ``frame`` with ``ind`` (cumulative PVT) and ``sign`` (smoothed
        signal) columns added, dropping rows where either is undefined.
    """
    out = frame.copy()
    price = resolve_price(out, ipc)
    volume = out['tick_volume'] if volume_type == 'VOLUME_TICK' else out['real_volume']
    pvt = pd.Series(index=out.index, dtype=float)
    if len(out) == 0:
        out['ind'] = pvt
        out['sign'] = pvt
        return out
    pvt.iloc[0] = float(volume.iloc[0])
    for i in range(1, len(out)):
        prev_price = float(price.iloc[i - 1])
        curr_price = float(price.iloc[i])
        vol = float(volume.iloc[i])
        delta = 0.0 if prev_price == 0 else vol * (curr_price - prev_price) / prev_price
        pvt.iloc[i] = pvt.iloc[i - 1] + delta
    sign = smooth_series(pvt, xlength, xma_method)
    out['ind'] = pvt
    out['sign'] = sign
    out = out.dropna(subset=['ind', 'sign'])
    return out


