def _applied_price(data, price_type, ago=0):
    o = float(data.open[-ago])
    h = float(data.high[-ago])
    l = float(data.low[-ago])
    c = float(data.close[-ago])
    if price_type == 0:
        return c
    if price_type == 1:
        return o
    if price_type == 2:
        return h
    if price_type == 3:
        return l
    if price_type == 4:
        return (h + l) / 2.0
    if price_type == 5:
        return (h + l + c) / 3.0
    if price_type == 6:
        return (h + l + c + c) / 4.0
    return c


class VolumeWeightedMAIndicator(bt.Indicator):
    """Volume-weighted moving average of an applied price.

    Averages the applied price (selected by ``ipc``) over ``length`` bars,
    weighting each bar by its tick volume (or open interest when
    ``use_tick_volume`` is False); falls back to the plain applied price when
    the total weight is zero.
    """

    lines = ('vwma',)
    params = dict(length=12, ipc=0, use_tick_volume=True)

    def __init__(self):
        """Set the minimum period to cover the averaging window."""
        self.addminperiod(int(self.p.length) + 2)

    def next(self):
        """Compute the volume-weighted average price for the current bar."""
        length = int(self.p.length)
        weights = []
        total = 0.0
        for i in range(length):
            vol = float(self.data.volume[-i]) if self.p.use_tick_volume else float(self.data.openinterest[-i])
            if vol < 0:
                vol = 0.0
            weights.append(vol)
            total += vol
        if total == 0.0:
            self.lines.vwma[0] = _applied_price(self.data, int(self.p.ipc), 0)
            return
        value = 0.0
        for i in range(length):
            value += _applied_price(self.data, int(self.p.ipc), i) * (weights[i] / total)
        self.lines.vwma[0] = value


class ExpVolumeWeightedMAStrategy(bt.Strategy):
    """Port of the MT5 ``Exp_Volume_Weighted_MA`` slope-following strategy.

    Reads the VWMA slope from the H4 signal feed and opens or closes positions
    on slope turns, executing on the M15 feed with fixed stop-loss and
    take-profit distances in points.

    Args:
        length: VWMA averaging window length.
        ipc: Applied-price selector passed to the VWMA indicator.
        use_tick_volume: Weight by tick volume when True, else open interest.
        signal_bar: Which completed signal bar to read the VWMA from.
        stop_loss_points: Stop-loss distance in points (0 disables).
        take_profit_points: Take-profit distance in points (0 disables).
        fixed_lot: Fixed order size in lots.
        point: Price value of one point for stop/target distances.
        buy_pos_open: Whether long entries are permitted.
        sell_pos_open: Whether short entries are permitted.
        buy_pos_close: Whether long positions may be closed on signal.
        sell_pos_close: Whether short positions may be closed on signal.
        indicator_minutes: Signal timeframe size in minutes.
    """

    params = dict(
        length=12,
        ipc=0,
        use_tick_volume=True,
        signal_bar=1,
        stop_loss_points=1000,
        take_profit_points=2000,
        fixed_lot=0.1,
        point=0.01,
        buy_pos_open=True,
        sell_pos_open=True,
        buy_pos_close=True,
        sell_pos_close=True,
        indicator_minutes=240,
    )

    def __init__(self):
        """Bind the execution and signal feeds and reset counters.

        Captures the M15 execution feed and the H4 signal feed, builds the VWMA
        indicator on the signal feed, and clears bar/signal/trade counters and
        position-tracking state.
        """
        self.base = self.datas[0]
        self.signal_data = self.datas[1]
        self.indicator = VolumeWeightedMAIndicator(self.signal_data, length=self.p.length, ipc=self.p.ipc, use_tick_volume=self.p.use_tick_volume)
        self.bar_num = 0
        self.signal_count = 0
        self.buy_count = 0
        self.sell_count = 0
        self.trade_count = 0
        self.win_count = 0
        self.loss_count = 0
        self._position_was_open = False
        self._last_signal_len = 0

    def log(self, text):
        """Print a timestamped log line for the current execution bar.

        Args:
            text: The message to log alongside the current bar datetime.
        """
        print(f'{bt.num2date(self.base.datetime[0]).isoformat()}, {text}')

    def _check_exit_levels(self):
        if not self.position:
            return False
        cp = float(self.base.close[0])
        pv = float(self.p.point)
        sd = self.p.stop_loss_points * pv if self.p.stop_loss_points > 0 else None
        td = self.p.take_profit_points * pv if self.p.take_profit_points > 0 else None
        ep = float(self.position.price)
        if self.position.size > 0:
            if sd and cp <= ep - sd:
                self.log(f'close long SL {cp:.2f}')
                self.close()
                return True
            if td and cp >= ep + td:
                self.log(f'close long TP {cp:.2f}')
                self.close()
                return True
        else:
            if sd and cp >= ep + sd:
                self.log(f'close short SL {cp:.2f}')
                self.close()
                return True
            if td and cp <= ep - td:
                self.log(f'close short TP {cp:.2f}')
                self.close()
                return True
        return False

    def _val(self, line, offset):
        v = float(line[-offset]) if offset else float(line[0])
        return 0.0 if math.isnan(v) else v

    def next(self):
        """Enforce exits, then open/close on a VWMA slope turn each signal bar.

        Increments the bar counter, applies stop-loss/take-profit exits, and
        once per new signal bar reads three consecutive VWMA values to detect a
        slope turn, then closes opposing positions and opens a fixed-lot long or
        short per the configured permissions.
        """
        self.bar_num += 1
        if len(self.base) < 2:
            return
        if self._check_exit_levels():
            return
        signal_bar = max(int(self.p.signal_bar) - 1, 0)
        min_needed = int(self.p.length) + signal_bar + 4
        if len(self.signal_data) < min_needed:
            return
        current_signal_len = len(self.signal_data)
        if current_signal_len == self._last_signal_len:
            return
        self._last_signal_len = current_signal_len

        v0 = self._val(self.indicator.vwma, signal_bar)
        v1 = self._val(self.indicator.vwma, signal_bar + 1)
        v2 = self._val(self.indicator.vwma, signal_bar + 2)
        buy_open = False
        sell_open = False
        buy_close = False
        sell_close = False
        if v1 < v2:
            if self.p.buy_pos_open and v0 > v1:
                buy_open = True
            if self.p.sell_pos_close:
                sell_close = True
        if v1 > v2:
            if self.p.sell_pos_open and v0 < v1:
                sell_open = True
            if self.p.buy_pos_close:
                buy_close = True
        cp = float(self.base.close[0])
        sz = float(self.p.fixed_lot)
        if sz <= 0:
            return
        if sell_close and self.position.size < 0:
            self.log(f'close short signal {cp:.2f}')
            self.close()
        if buy_close and self.position.size > 0:
            self.log(f'close long signal {cp:.2f}')
            self.close()
        if buy_open:
            self.signal_count += 1
            self.log(f'buy signal {cp:.2f}')
            if self.position.size <= 0:
                self.buy(size=sz)
        if sell_open:
            self.signal_count += 1
            self.log(f'sell signal {cp:.2f}')
            if self.position.size >= 0:
                self.sell(size=sz)

    def notify_trade(self, trade):
        """Count entries when a trade opens and win/loss when it closes.

        Args:
            trade: The trade whose status changed; opening increments the
                buy/sell entry counter and closing updates win/loss counts.
        """
        if trade.isopen and not self._position_was_open:
            if trade.size > 0:
                self.buy_count += 1
            elif trade.size < 0:
                self.sell_count += 1
            self._position_was_open = True
            return
        if not trade.isclosed:
            return
        self.trade_count += 1
        if trade.pnlcomm >= 0:
            self.win_count += 1
        else:
            self.loss_count += 1
        self._position_was_open = False
        self.log(f'trade closed pnl={trade.pnlcomm:.2f}')



