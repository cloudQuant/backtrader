def prepare_carry_proxy_data(price_map, params):
    """Align commodities and compute each one's carry-proxy line.

    Intersects all asset indices to a common set of dates and builds a carry
    proxy per commodity from the difference between its short- and long-window
    returns, annualized.

    Args:
        price_map: Mapping of commodity symbol to its loaded OHLCV DataFrame.
        params: Strategy parameter dict supplying the short/long windows and the
            annualization factor.

    Returns:
        A tuple ``(prepared, score_df)`` where ``prepared`` maps each symbol to
        its date-aligned OHLCV frame with an added ``carry_proxy`` column and
        ``score_df`` collects the carry proxies across commodities.
    """
    aligned_index = None
    prepared = {}
    short_window = int(params.get('short_window', 21))
    long_window = int(params.get('long_window', 126))
    annualize_factor = float(params.get('annualize_factor', 12))
    for symbol, frame in price_map.items():
        aligned_index = frame.index if aligned_index is None else aligned_index.intersection(frame.index)
    aligned_index = aligned_index.sort_values()
    for symbol, frame in price_map.items():
        px = frame.loc[aligned_index].copy()
        near_return = px['close'].pct_change(short_window)
        far_return = px['close'].pct_change(long_window)
        carry_proxy = ((near_return - far_return / max(long_window / short_window, 1.0)) * annualize_factor).astype(float)
        prepared[symbol] = px[['open', 'high', 'low', 'close', 'volume', 'openinterest']].copy()
        prepared[symbol]['carry_proxy'] = carry_proxy
    score_df = pd.DataFrame({symbol: frame['carry_proxy'] for symbol, frame in prepared.items()}, index=aligned_index)
    return prepared, score_df.dropna(how='all')


class CarryProxyFeed(bt.feeds.PandasData):
    """PandasData feed exposing OHLCV plus the per-commodity carry-proxy line."""

    lines = ('carry_proxy',)
    params = (
        ('datetime', None), ('open', 0), ('high', 1), ('low', 2), ('close', 3), ('volume', 4), ('openinterest', 5),
        ('carry_proxy', 6),
    )


class CommodityCarryStrategy(bt.Strategy):
    """Cross-sectional carry factor that longs high-carry and shorts low-carry commodities.

    On the rebalance cadence the strategy ranks commodities by their carry proxy
    and issues order_target_size orders to hold equal-weight long legs on the top
    names and short legs on the bottom names, capped per leg.
    """

    params = dict(
        n_long=2,
        n_short=2,
        rebalance_interval_days=21,
        max_leg_notional_pct=0.25,
        short_window=21,
        long_window=126,
        annualize_factor=12,
        commission_pct=0.0005,
    )

    def __init__(self):
        """Initialize order tracking, counters, and long/short book tallies."""
        self.order_refs = set()
        self.bar_num = 0
        self.buy_count = 0
        self.sell_count = 0
        self.trade_count = 0
        self.win_count = 0
        self.loss_count = 0
        self.broker_value_series = []
        self.long_books = 0
        self.short_books = 0

    def _submit(self, order):
        if order is not None:
            self.order_refs.add(order.ref)

    def _target_size(self, data, target_pct):
        broker_value = float(self.broker.getvalue())
        price = float(data.close[0])
        if broker_value <= 0 or price <= 0:
            return 0.0
        comminfo = self.broker.getcommissioninfo(data)
        multiplier = float(getattr(comminfo.p, 'mult', 1.0) or 1.0)
        if multiplier <= 0:
            return 0.0
        size = broker_value * abs(float(target_pct)) / (price * multiplier)
        size = max(0.01, round(size, 2))
        return size if target_pct >= 0 else -size

    def next(self):
        """Record broker value and rebalance the carry legs on the cadence.

        On the rebalance interval, ranks commodities by carry proxy, assigns
        equal-weight long legs to the top names and short legs to the bottom
        names, and issues order_target_size orders, counting buys/sells and
        long/short book rebalances.
        """
        self.bar_num += 1
        self.broker_value_series.append((bt.num2date(self.datas[0].datetime[0]), float(self.broker.getvalue())))
        if self.order_refs:
            return
        if self.bar_num > 1 and (self.bar_num - 1) % max(1, int(self.p.rebalance_interval_days)) != 0:
            return
        carries = []
        for data in self.datas:
            carry_value = float(data.carry_proxy[0]) if data.carry_proxy[0] == data.carry_proxy[0] else None
            if carry_value is None:
                continue
            carries.append((data, carry_value))
        if len(carries) < max(int(self.p.n_long), int(self.p.n_short)):
            return
        ranked = sorted(carries, key=lambda item: item[1], reverse=True)
        long_group = ranked[:int(self.p.n_long)]
        short_group = ranked[-int(self.p.n_short):]
        target_map = {data._name: 0.0 for data in self.datas}
        long_weight = float(self.p.max_leg_notional_pct) / max(1, int(self.p.n_long))
        short_weight = -float(self.p.max_leg_notional_pct) / max(1, int(self.p.n_short))
        for data, _ in long_group:
            target_map[data._name] = long_weight
        for data, _ in short_group:
            target_map[data._name] = short_weight
        self.long_books += 1
        self.short_books += 1
        for data in self.datas:
            target_pct = target_map[data._name]
            current_pos = float(self.getposition(data).size)
            target_size = self._target_size(data, target_pct)
            if abs(target_size - current_pos) < 0.01:
                continue
            if target_size > current_pos:
                self.buy_count += 1
            elif target_size < current_pos:
                self.sell_count += 1
            self._submit(self.order_target_size(data=data, target=target_size))

    def notify_order(self, order):
        """Drop completed/cancelled orders from the pending-refs set.

        Args:
            order: The order whose status changed; submitted/accepted orders are
                ignored and any other terminal status clears its ref.
        """
        if order.status in (order.Submitted, order.Accepted):
            return
        self.order_refs.discard(order.ref)

    def notify_trade(self, trade):
        """Tally closed trades into win/loss buckets.

        Args:
            trade: The trade being notified; only closed trades update counts.
        """
        if not trade.isclosed:
            return
        self.trade_count += 1
        if trade.pnlcomm >= 0:
            self.win_count += 1
        else:
            self.loss_count += 1


