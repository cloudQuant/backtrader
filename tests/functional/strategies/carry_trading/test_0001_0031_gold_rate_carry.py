def finite_or_none(value):
    """Return the value when it is finite, otherwise ``None``."""
    return value if value is not None and math.isfinite(value) else None


def calculate_ulcer_index(values):
    """Compute the Ulcer Index from a series of portfolio values.

    Args:
        values: Sequence of broker values over time.

    Returns:
        The Ulcer Index (root-mean-square of percentage drawdowns), or 0.0
        when fewer than two values are provided.
    """
    if len(values) < 2:
        return 0.0
    peak = values[0]
    squared = 0.0
    for value in values:
        peak = max(peak, value)
        drawdown = (peak - value) / peak * 100.0 if peak > 0 else 0.0
        squared += drawdown ** 2
    return math.sqrt(squared / len(values))



