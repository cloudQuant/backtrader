def resample_frame(df, rule):
    """Resample OHLCV data to a new timeframe and normalize missing values.

    Args:
        df: Source OHLCV DataFrame indexed by datetime.
        rule: Pandas resampling rule such as `"5min"`.

    Returns:
        pd.DataFrame: Aggregated bars with first open, max high, min low, last close,
        summed volume, and normalized open-interest.
    """
    out = df.resample(rule, label='right', closed='right').agg({
        'open': 'first',
        'high': 'max',
        'low': 'min',
        'close': 'last',
        'volume': 'sum',
        'openinterest': 'last',
    })
    out = out.dropna(subset=['open', 'high', 'low', 'close'])
    out['openinterest'] = out['openinterest'].fillna(0)
    return out


