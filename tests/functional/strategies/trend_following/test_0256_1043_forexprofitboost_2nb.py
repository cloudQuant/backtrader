def resample_frame(df, rule):
    """Resample a minute-indexed DataFrame to a target bar size.

    Args:
        df: Source DataFrame with datetime index and OHLCV columns.
        rule: Resample rule string accepted by ``pandas.DataFrame.resample``.

    Returns:
        pandas.DataFrame: Resampled and cleaned OHLCV bars.
    """
    out = df.resample(rule, label='right', closed='right').agg({
        'open': 'first', 'high': 'max', 'low': 'min', 'close': 'last', 'volume': 'sum', 'openinterest': 'last',
    })
    out = out.dropna(subset=['open', 'high', 'low', 'close'])
    out['openinterest'] = out['openinterest'].fillna(0)
    return out


def moving_average(series, period, ma_type):
    """Compute a moving average by requested method.

    Args:
        series: Input price series.
        period: Window length.
        ma_type: ``EMA`` or ``SMA``.

    Returns:
        pandas.Series: The corresponding moving average series.
    """
    ma_type = str(ma_type).upper()
    period = int(period)
    if ma_type == 'EMA':
        return series.ewm(span=period, adjust=False).mean()
    if ma_type == 'SMA':
        return series.rolling(period).mean()
    raise ValueError('Unsupported MA type: {0}'.format(ma_type))


def bollinger_bands(series, period, deviation):
    """Calculate Bollinger upper and lower bands.

    Args:
        series: Source closing-price series.
        period: Rolling window length.
        deviation: Standard-deviation multiplier.

    Returns:
        tuple[pandas.Series, pandas.Series]: ``(upper, lower)`` band series.
    """
    mid = series.rolling(int(period)).mean()
    std = series.rolling(int(period)).std(ddof=0)
    upper = mid + float(deviation) * std
    lower = mid - float(deviation) * std
    return upper, lower


def compute_forexprofitboost_2nb(frame, ma_period1=7, ma_type1='EMA', ma_price1='CLOSE', ma_period2=21, ma_type2='SMA', ma_price2='CLOSE', bb_period=15, bb_deviation=1.0, bb_shift=1):
    """Build signal frame used by the strategy.

    Args:
        frame: Base DataFrame with at least ``close``.
        ma_period1: First MA period.
        ma_type1: First MA type (``EMA`` or ``SMA``).
        ma_price1: Price input for first MA (supports ``CLOSE`` only in this migration).
        ma_period2: Second MA period.
        ma_type2: Second MA type (``EMA`` or ``SMA``).
        ma_price2: Price input for second MA (supports ``CLOSE`` only in this migration).
        bb_period: Bollinger band rolling period.
        bb_deviation: Bollinger band deviation multiplier.
        bb_shift: Optional shift for BB bands.

    Returns:
        pandas.DataFrame: Input frame augmented with MA/Bollinger and state columns.
    """
    if str(ma_price1).upper() != 'CLOSE' or str(ma_price2).upper() != 'CLOSE':
        raise ValueError('This migration currently reconstructs CLOSE-based MA inputs only')
    close = frame['close']
    ma1 = moving_average(close, ma_period1, ma_type1)
    ma2 = moving_average(close, ma_period2, ma_type2)
    upper, lower = bollinger_bands(close, bb_period, bb_deviation)
    if int(bb_shift) != 0:
        upper = upper.shift(int(bb_shift))
        lower = lower.shift(int(bb_shift))
    up_state = ma1 > ma2
    down_state = ~up_state
    out = frame.copy()
    out['ma1'] = ma1
    out['ma2'] = ma2
    out['bb_upper'] = upper
    out['bb_lower'] = lower
    out['up_state'] = up_state.astype(float)
    out['down_state'] = down_state.astype(float)
    return out.dropna(subset=['ma1', 'ma2', 'bb_upper', 'bb_lower'])


