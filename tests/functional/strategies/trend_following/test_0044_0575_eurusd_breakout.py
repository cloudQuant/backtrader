class EurUsdBreakoutStrategy(bt.Strategy):
    """Session-range breakout strategy for the XAUUSD M15 regression data.

    Computes the European session range and, when that range is compact,
    arms long/short breakout orders inside the US-session window with fixed
    point-based stop-loss and take-profit distances.
    """

    params = dict(
        start_hour_eu_session=5,
        start_hour_us_session=2,
        end_hour_us_session=16,
        small_eu_session_pips=72,
        trade_on_monday=False,
        lots=1.0,
        stop_loss=12,
        take_profit=15,
        point=0.01,
        price_digits=2,
    )

    def __init__(self):
        """Initialize bar/order/trade counters and per-day session state."""
        self.bar_num = 0
        self.signal_count = 0
        self.buy_count = 0
        self.sell_count = 0
        self.trade_count = 0
        self.win_count = 0
        self.loss_count = 0
        self.completed_order_count = 0
        self.rejected_order_count = 0
        self.order = None
        self.pending_direction = None
        self.stop_price = None
        self.take_profit_price = None
        self.day_key = None
        self.top_range = None
        self.low_range = None
        self.bought = False
        self.sold = False
        self.small_session = False
        self.session_found = False

    def _point(self):
        return float(self.p.point)

    def _round(self, value):
        return round(float(value), int(self.p.price_digits))

    def _reset_day(self, day_key):
        self.day_key = day_key
        self.top_range = None
        self.low_range = None
        self.bought = False
        self.sold = False
        self.small_session = False
        self.session_found = False

    def _calc_eu_range(self):
        if len(self) < 25:
            return None, None
        highs = [float(self.data.high[-i]) for i in range(1, 25)]
        lows = [float(self.data.low[-i]) for i in range(1, 25)]
        return max(highs), min(lows)

    def _arm(self, direction, price):
        sl = float(self.p.stop_loss) * self._point()
        tp = float(self.p.take_profit) * self._point()
        if direction == 'buy':
            self.stop_price = self._round(price - sl) if sl > 0 else None
            self.take_profit_price = self._round(price + tp) if tp > 0 else None
            self.signal_count += 1
            self.order = self.buy(size=float(self.p.lots))
        else:
            self.stop_price = self._round(price + sl) if sl > 0 else None
            self.take_profit_price = self._round(price - tp) if tp > 0 else None
            self.signal_count += 1
            self.order = self.sell(size=float(self.p.lots))

    def _check_exit(self):
        if not self.position or self.order is not None:
            return
        high = float(self.data.high[0])
        low = float(self.data.low[0])
        if self.position.size > 0:
            if self.take_profit_price is not None and high >= float(self.take_profit_price):
                self.order = self.close(); return
            if self.stop_price is not None and low <= float(self.stop_price):
                self.order = self.close(); return
        else:
            if self.take_profit_price is not None and low <= float(self.take_profit_price):
                self.order = self.close(); return
            if self.stop_price is not None and high >= float(self.stop_price):
                self.order = self.close(); return

    def next(self):
        """Advance one bar: manage day state, exits, and breakout entries.

        Refreshes the per-day session state, skips weekends and (optionally)
        Mondays, processes any pending direction flip, checks open positions
        against stop/target, computes the European range at the US-session
        start, and arms long/short breakout orders when prior-bar prices clear
        the session range by the buffer.
        """
        self.bar_num += 1
        dt = self.data.datetime.datetime(0)
        if self.day_key != dt.date().isoformat():
            self._reset_day(dt.date().isoformat())

        if dt.weekday() >= 5:
            return
        if dt.weekday() == 0 and not self.p.trade_on_monday:
            return
        if self.order is not None:
            return

        if not self.position and self.pending_direction is not None:
            self._arm(self.pending_direction, float(self.data.close[0]))
            self.pending_direction = None
            return

        if self.position:
            self._check_exit()
            if self.order is not None:
                return

        if not self.session_found and dt.hour == int(self.p.start_hour_us_session):
            top, low = self._calc_eu_range()
            if top is None or low is None:
                return
            self.top_range = top
            self.low_range = low
            self.small_session = (top - low) <= float(self.p.small_eu_session_pips) * self._point()
            self.session_found = True

        if not self.session_found or not self.small_session:
            return
        if not (int(self.p.start_hour_us_session) <= dt.hour < int(self.p.end_hour_us_session)):
            return
        if not (dt.hour > int(self.p.start_hour_eu_session) + 5 and dt.hour < int(self.p.start_hour_eu_session) + 10):
            return
        if len(self) < 2:
            return

        low_prev = float(self.data.low[-1])
        high_prev = float(self.data.high[-1])
        if (not self.bought) and self.top_range is not None and low_prev > self.top_range + 3 * self._point():
            if self.position.size < 0:
                self.pending_direction = 'buy'
                self.order = self.close()
            elif not self.position:
                self._arm('buy', float(self.data.close[0]))
            self.bought = True
            return
        if (not self.sold) and self.low_range is not None and high_prev < self.low_range - 3 * self._point():
            if self.position.size > 0:
                self.pending_direction = 'sell'
                self.order = self.close()
            elif not self.position:
                self._arm('sell', float(self.data.close[0]))
            self.sold = True

    def notify_order(self, order):
        """Track completed and rejected orders and clear the pending order.

        Args:
            order: The backtrader order whose status changed. Completed orders
                increment fill counters (or clear stop/target on a flat close),
                while canceled/margin/rejected/expired orders bump the rejected
                counter. The tracked ``self.order`` is released once it reaches
                a terminal state.
        """
        if order.status in [bt.Order.Submitted, bt.Order.Accepted]:
            return
        if order.status == bt.Order.Completed:
            self.completed_order_count += 1
            if self.position:
                if order.executed.size > 0:
                    self.buy_count += 1
                elif order.executed.size < 0:
                    self.sell_count += 1
            else:
                self.stop_price = None
                self.take_profit_price = None
        elif order.status in [bt.Order.Canceled, bt.Order.Margin, bt.Order.Rejected, bt.Order.Expired]:
            self.rejected_order_count += 1
        if self.order is not None and order.ref == self.order.ref and order.status not in [bt.Order.Submitted, bt.Order.Accepted]:
            self.order = None

    def notify_trade(self, trade):
        """Tally closed trades into win/loss counters.

        Args:
            trade: The backtrader trade being notified. Only closed trades are
                counted; a non-negative ``pnlcomm`` increments the win count,
                otherwise the loss count.
        """
        if not trade.isclosed:
            return
        self.trade_count += 1
        if trade.pnlcomm >= 0:
            self.win_count += 1
        else:
            self.loss_count += 1



