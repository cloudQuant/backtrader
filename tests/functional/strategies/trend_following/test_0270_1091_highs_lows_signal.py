def resample_frame(df, rule):
    """Resample an intraday OHLCV frame to a coarser timeframe.

    Args:
        df: Datetime-indexed OHLCV frame at the base (M15) timeframe.
        rule: Pandas offset alias for the target timeframe (e.g. ``'240min'``).

    Returns:
        pandas.DataFrame: Right-labelled, right-closed resampled frame with
        first/max/min/last aggregation for OHLC, summed volume, and forward open
        interest; rows with missing OHLC are dropped.
    """
    out = df.resample(rule, label='right', closed='right').agg({
        'open': 'first',
        'high': 'max',
        'low': 'min',
        'close': 'last',
        'volume': 'sum',
        'openinterest': 'last',
    })
    out = out.dropna(subset=['open', 'high', 'low', 'close'])
    out['openinterest'] = out['openinterest'].fillna(0)
    return out


def compute_highs_lows_signal(frame, how_many_candles=3, atr_period=15):
    """Compute ATR-offset buy/sell signals from consecutive higher/lower extremes.

    Flags a buy when the last ``how_many_candles`` bars all make higher highs and
    higher lows, and a sell when they all make lower highs and lower lows. The
    signal level is offset from the bar low/high by 3/8 of the current bt.indicators.ATR.

    Args:
        frame: Datetime-indexed OHLCV frame (typically the resampled H4 frame).
        how_many_candles: Number of consecutive bars required to confirm a run.
        atr_period: Smoothing period for the ATR used to offset signal levels.

    Returns:
        pandas.DataFrame: Copy of ``frame`` with added ``sell_signal`` and
        ``buy_signal`` columns (0.0 where no signal fires).
    """
    count = max(int(how_many_candles), 1)
    high = frame['high'].astype(float)
    low = frame['low'].astype(float)
    close = frame['close'].astype(float)
    prev_close = close.shift(1)
    tr = pd.concat([
        high - low,
        (high - prev_close).abs(),
        (low - prev_close).abs(),
    ], axis=1).max(axis=1)
    atr = tr.ewm(alpha=1.0 / atr_period, adjust=False).mean()
    buy = np.zeros(len(frame), dtype=float)
    sell = np.zeros(len(frame), dtype=float)
    highs = high.to_numpy(dtype=float)
    lows = low.to_numpy(dtype=float)
    atr_vals = atr.to_numpy(dtype=float)

    for i in range(count, len(frame)):
        higher_highs = 0
        higher_lows = 0
        lower_highs = 0
        lower_lows = 0
        for k in range(count):
            if highs[i - k] > highs[i - k - 1]:
                higher_highs += 1
            if lows[i - k] > lows[i - k - 1]:
                higher_lows += 1
            if highs[i - k] < highs[i - k - 1]:
                lower_highs += 1
            if lows[i - k] < lows[i - k - 1]:
                lower_lows += 1
        if higher_highs == count and higher_lows == count:
            buy[i] = lows[i] - atr_vals[i] * 3.0 / 8.0
        elif lower_highs == count and lower_lows == count:
            sell[i] = highs[i] + atr_vals[i] * 3.0 / 8.0

    out = frame.copy()
    out['sell_signal'] = sell
    out['buy_signal'] = buy
    return out


