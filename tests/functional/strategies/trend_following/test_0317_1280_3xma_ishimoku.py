def resolve_ma_class(name):
    """Map a moving-average name to its backtrader indicator class.

    Args:
        name: MA type name (e.g. ``sma``, ``ema``, ``smma`` or MT5-style
            ``mode_*`` variants).

    Returns:
        The matching backtrader moving-average indicator class, defaulting to
        the weighted moving average for unrecognized names.
    """
    mode = str(name).lower()
    if mode in {'sma', 'mode_sma'}:
        return bt.indicators.SMA
    if mode in {'ema', 'mode_ema'}:
        return bt.indicators.EMA
    if mode in {'smma', 'mode_smma'}:
        return bt.indicators.SmoothedMovingAverage
    return bt.indicators.WeightedMovingAverage


class ThreeXMAIshimokuStrategy(bt.Strategy):
    """Port of the MT5 ``Exp_3XMA_Ishimoku`` triple-line strategy.

    Builds fast, medium and slow XMAIshimokuLine indicators and trades the fast
    line relative to the medium/slow band: a pullback from above the upper band
    is a long signal and a pullback from below the lower band is a short signal,
    entering on the cross and reversing on the opposite signal with a fixed lot.

    Args:
        up_period1/dn_period1: High/low lookback for the fast line.
        up_period2/dn_period2: High/low lookback for the medium line.
        up_period3/dn_period3: High/low lookback for the slow line.
        xma1_method/xma2_method/xma3_method: MA types for the three lines.
        xlength1/xlength2/xlength3: Smoothing lengths for the three lines.
        xphase: Phase parameter retained from the source EA (unused here).
        signal_bar: Which completed bar to evaluate signals on.
        lot: Fixed order size in lots.
    """

    params = dict(
        up_period1=3,
        dn_period1=3,
        up_period2=6,
        dn_period2=6,
        up_period3=9,
        dn_period3=9,
        xma1_method='sma',
        xma2_method='sma',
        xma3_method='sma',
        xlength1=8,
        xlength2=25,
        xlength3=80,
        xphase=15,
        signal_bar=1,
        lot=0.1,
    )

    def __init__(self):
        """Build the three XMAIshimoku lines and reset bar/trade counters."""
        self.fast = bt.indicators.XMAIshimokuLine(
            self.data,
            up_period=self.p.up_period1,
            dn_period=self.p.dn_period1,
            xma_method=self.p.xma1_method,
            xlength=self.p.xlength1,
            xphase=self.p.xphase,
        )
        self.mid = bt.indicators.XMAIshimokuLine(
            self.data,
            up_period=self.p.up_period2,
            dn_period=self.p.dn_period2,
            xma_method=self.p.xma2_method,
            xlength=self.p.xlength2,
            xphase=self.p.xphase,
        )
        self.slow = bt.indicators.XMAIshimokuLine(
            self.data,
            up_period=self.p.up_period3,
            dn_period=self.p.dn_period3,
            xma_method=self.p.xma3_method,
            xlength=self.p.xlength3,
            xphase=self.p.xphase,
        )
        self.bar_num = 0
        self.buy_count = 0
        self.sell_count = 0
        self.trade_count = 0
        self.win_count = 0
        self.loss_count = 0
        self._position_was_open = False

    def log(self, text):
        """Print a timestamped log line for the current bar.

        Args:
            text: The message to log alongside the current bar datetime.
        """
        dt = bt.num2date(self.data.datetime[0])
        print(f'{dt.isoformat()}, {text}')

    def _signals(self):
        shift = max(1, int(self.p.signal_bar))
        fast_prev = float(self.fast.xma[-shift])
        fast_cur = float(self.fast.xma[-shift + 1]) if shift > 1 else float(self.fast.xma[0])
        up_prev = max(float(self.mid.xma[-shift]), float(self.slow.xma[-shift]))
        up_cur = max(
            float(self.mid.xma[-shift + 1]) if shift > 1 else float(self.mid.xma[0]),
            float(self.slow.xma[-shift + 1]) if shift > 1 else float(self.slow.xma[0]),
        )
        dn_prev = min(float(self.mid.xma[-shift]), float(self.slow.xma[-shift]))
        dn_cur = min(
            float(self.mid.xma[-shift + 1]) if shift > 1 else float(self.mid.xma[0]),
            float(self.slow.xma[-shift + 1]) if shift > 1 else float(self.slow.xma[0]),
        )
        buy_open = False
        sell_open = False
        buy_close = False
        sell_close = False
        if fast_prev > up_prev:
            if fast_cur <= up_cur:
                buy_open = True
            sell_close = True
        if fast_prev < dn_prev:
            if fast_cur >= dn_cur:
                sell_open = True
            buy_close = True
        return buy_open, sell_open, buy_close, sell_close

    def next(self):
        """Open, close, or reverse on fast-line vs band signals each bar.

        Increments the bar counter, skips during warm-up, derives open/close
        signals from the fast line relative to the medium/slow band at the
        configured signal bar, and acts: when flat it opens a long or short; when
        in a position it closes on the close signal or closes and reverses on the
        opposite open signal.
        """
        self.bar_num += 1
        warmup = max(int(self.p.xlength1), int(self.p.xlength2), int(self.p.xlength3)) + int(self.p.signal_bar) + 10
        if len(self.data) < warmup:
            return
        buy_open, sell_open, buy_close, sell_close = self._signals()
        fast = float(self.fast.xma[0])
        upper = max(float(self.mid.xma[0]), float(self.slow.xma[0]))
        lower = min(float(self.mid.xma[0]), float(self.slow.xma[0]))
        if self.position:
            if self.position.size > 0:
                if buy_close and not sell_open:
                    self.log(f'close long fast={fast:.4f} upper={upper:.4f} lower={lower:.4f}')
                    self.close()
                    return
                if sell_open:
                    self.log(f'close long & sell fast={fast:.4f} upper={upper:.4f} lower={lower:.4f}')
                    self.close()
                    self.sell(size=self.p.lot)
                    return
            if self.position.size < 0:
                if sell_close and not buy_open:
                    self.log(f'close short fast={fast:.4f} upper={upper:.4f} lower={lower:.4f}')
                    self.close()
                    return
                if buy_open:
                    self.log(f'close short & buy fast={fast:.4f} upper={upper:.4f} lower={lower:.4f}')
                    self.close()
                    self.buy(size=self.p.lot)
                    return
        else:
            if buy_open:
                self.log(f'buy fast={fast:.4f} upper={upper:.4f} lower={lower:.4f}')
                self.buy(size=self.p.lot)
                return
            if sell_open:
                self.log(f'sell fast={fast:.4f} upper={upper:.4f} lower={lower:.4f}')
                self.sell(size=self.p.lot)
                return

    def notify_trade(self, trade):
        """Count entries when a trade opens and win/loss when it closes.

        Args:
            trade: The trade whose status changed; opening increments the
                buy/sell entry counter and closing updates win/loss counts.
        """
        if trade.isopen and not self._position_was_open:
            if trade.size > 0:
                self.buy_count += 1
            elif trade.size < 0:
                self.sell_count += 1
            self._position_was_open = True
            return
        if not trade.isclosed:
            return
        self.trade_count += 1
        if trade.pnlcomm >= 0:
            self.win_count += 1
        else:
            self.loss_count += 1
        self._position_was_open = False
        self.log(f'trade closed pnl={trade.pnlcomm:.2f}')


