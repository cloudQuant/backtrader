class VortexIndicatorSystemStrategy(bt.Strategy):
    """Manage Vortex crossover setups, entries, and reversal order flow."""
    params = dict(
        vi_length=14,
        lots=0.1,
        point=0.01,
        price_digits=2,
    )

    def __init__(self):
        """Initialize indicator, state flags, and all tracking counters."""
        self.vortex = bt.indicators.VortexSystemIndicator(self.data, period=int(self.p.vi_length))
        self.bar_num = 0
        self.signal_count = 0
        self.buy_count = 0
        self.sell_count = 0
        self.trade_count = 0
        self.win_count = 0
        self.loss_count = 0
        self.completed_order_count = 0
        self.rejected_order_count = 0
        self.order = None
        self.buy_setup = False
        self.sell_setup = False
        self.entry_trigger = None

    def next(self):
        """Evaluate Vortex crossover and manage setup, entry, reversal, and orders."""
        self.bar_num += 1
        if len(self) < int(self.p.vi_length) + 3 or self.order is not None:
            return

        plus1 = float(self.vortex.plus_vi[-1])
        plus2 = float(self.vortex.plus_vi[-2])
        minus1 = float(self.vortex.minus_vi[-1])
        minus2 = float(self.vortex.minus_vi[-2])

        if plus2 <= minus2 and plus1 > minus1:
            if self.position.size < 0:
                self.order = self.close()
                self.buy_setup = True
                self.sell_setup = False
                self.entry_trigger = float(self.data.high[-1])
                return
            self.buy_setup = True
            self.sell_setup = False
            self.entry_trigger = float(self.data.high[-1])
        elif minus2 <= plus2 and minus1 > plus1:
            if self.position.size > 0:
                self.order = self.close()
                self.sell_setup = True
                self.buy_setup = False
                self.entry_trigger = float(self.data.low[-1])
                return
            self.sell_setup = True
            self.buy_setup = False
            self.entry_trigger = float(self.data.low[-1])

        if self.position:
            return

        if self.buy_setup and self.entry_trigger is not None and float(self.data.high[0]) > float(self.entry_trigger):
            self.signal_count += 1
            self.order = self.buy(size=float(self.p.lots))
            self.buy_setup = False
            return
        if self.sell_setup and self.entry_trigger is not None and float(self.data.low[0]) < float(self.entry_trigger):
            self.signal_count += 1
            self.order = self.sell(size=float(self.p.lots))
            self.sell_setup = False

    def notify_order(self, order):
        """Track completed and rejected orders, including buy/sell counters."""
        if order.status in [bt.Order.Submitted, bt.Order.Accepted]:
            return
        if order.status == bt.Order.Completed:
            self.completed_order_count += 1
            if self.position:
                if order.executed.size > 0:
                    self.buy_count += 1
                elif order.executed.size < 0:
                    self.sell_count += 1
        elif order.status in [bt.Order.Canceled, bt.Order.Margin, bt.Order.Rejected, bt.Order.Expired]:
            self.rejected_order_count += 1
        if self.order is not None and order.ref == self.order.ref and order.status not in [bt.Order.Submitted, bt.Order.Accepted]:
            self.order = None

    def notify_trade(self, trade):
        """Track closed trade outcomes for win/loss counters."""
        if not trade.isclosed:
            return
        self.trade_count += 1
        if trade.pnlcomm >= 0:
            self.win_count += 1
        else:
            self.loss_count += 1


