def load_mt5_csv(filepath, fromdate=None, todate=None, bar_shift_minutes=0):
    """Load an MT5 CSV export into a time-indexed OHLCV DataFrame.

    Parameters
    ----------
    filepath : str
        Path to the tab-separated MT5 export file.
    fromdate : datetime, optional
        Inclusive lower bound on the datetime index.
    todate : datetime, optional
        Inclusive upper bound on the datetime index.
    bar_shift_minutes : int, optional
        Minutes to shift every bar timestamp forward.

    Returns
    -------
    pd.DataFrame
        Indexed by datetime with OHLCV/openinterest columns.
    """
    with open(filepath, 'r', encoding='utf-8') as f:
        lines = f.read().strip().split('\n')
    cleaned = '\n'.join(line.strip().strip('"') for line in lines)
    df = pd.read_csv(io.StringIO(cleaned), sep='\t')
    df['datetime'] = pd.to_datetime(df['<DATE>'] + ' ' + df['<TIME>'], format='%Y.%m.%d %H:%M:%S')
    df = df.rename(columns={
        '<OPEN>': 'open',
        '<HIGH>': 'high',
        '<LOW>': 'low',
        '<CLOSE>': 'close',
        '<TICKVOL>': 'volume',
        '<VOL>': 'openinterest',
    })
    df = df[['datetime', 'open', 'high', 'low', 'close', 'volume', 'openinterest']]
    df = df.set_index('datetime')
    if bar_shift_minutes:
        df.index = df.index + pd.Timedelta(minutes=bar_shift_minutes)
    if fromdate is not None:
        df = df[df.index >= fromdate]
    if todate is not None:
        df = df[df.index <= todate]
    return df


def resample_frame(df, rule):
    """Resample bars using the given pandas frequency rule.

    Parameters
    ----------
    df : pd.DataFrame
        Source minute bars with OHLCV data.
    rule : str
        Resample rule string such as ``240min``.

    Returns
    -------
    pd.DataFrame
        Resampled frame with first/high/low/last aggregations and cleaned rows.
    """
    out = df.resample(rule, label='right', closed='right').agg({
        'open': 'first',
        'high': 'max',
        'low': 'min',
        'close': 'last',
        'volume': 'sum',
        'openinterest': 'last',
    })
    out = out.dropna(subset=['open', 'high', 'low', 'close'])
    out['openinterest'] = out['openinterest'].fillna(0)
    return out


def compute_cci(frame, period=50):
    """Compute Commodity Channel Index values from a frame.

    Parameters
    ----------
    frame : pd.DataFrame
        Dataframe containing high/low/close columns.
    period : int, optional
        Rolling period for typical price SMA and MAD.

    Returns
    -------
    pd.Series
        CCI time series.
    """
    typical = (frame['high'] + frame['low'] + frame['close']) / 3.0
    sma = typical.rolling(int(period)).mean()
    mad = typical.rolling(int(period)).apply(lambda x: np.mean(np.abs(x - np.mean(x))), raw=True)
    cci = (typical - sma) / (0.015 * mad)
    return cci


def compute_atr(frame, period=5):
    """Compute Average True Range for ATR-based stop line updates.

    Parameters
    ----------
    frame : pd.DataFrame
        OHLC frame.
    period : int, optional
        Rolling window length for ATR.

    Returns
    -------
    pd.Series
        ATR time series.
    """
    prev_close = frame['close'].shift(1)
    tr = pd.concat([
        frame['high'] - frame['low'],
        (frame['high'] - prev_close).abs(),
        (frame['low'] - prev_close).abs(),
    ], axis=1).max(axis=1)
    return tr.rolling(int(period)).mean()


def compute_trendmagic(frame, cci_period=50, atr_period=5, price_shift=0.0):
    """Build TrendMagic line and regime color arrays from CCI/ATR signals.

    Parameters
    ----------
    frame : pd.DataFrame
        Input frame with OHL prices.
    cci_period : int, optional
        CCI calculation period.
    atr_period : int, optional
        ATR calculation period.
    price_shift : float, optional
        Optional offset for trendmagic line.

    Returns
    -------
    pd.DataFrame
        Input frame extended with ``trendmagic`` and ``color`` columns.
    """
    cci = compute_cci(frame, cci_period)
    atr = compute_atr(frame, atr_period)
    line = np.full(len(frame), np.nan, dtype=float)
    color = np.full(len(frame), np.nan, dtype=float)
    high = frame['high'].to_numpy(dtype=float)
    low = frame['low'].to_numpy(dtype=float)
    cci_values = cci.to_numpy(dtype=float)
    atr_values = atr.to_numpy(dtype=float)
    for idx in range(len(frame) - 1, -1, -1):
        if np.isnan(cci_values[idx]) or np.isnan(atr_values[idx]):
            continue
        next_idx = idx + 1
        next_line = line[next_idx] if next_idx < len(frame) and not np.isnan(line[next_idx]) else np.nan
        if cci_values[idx] >= 0.0:
            curr = low[idx] - atr_values[idx] + price_shift
            if not np.isnan(next_line) and curr < next_line:
                curr = next_line
            line[idx] = curr
            color[idx] = 0.0
        else:
            curr = high[idx] + atr_values[idx] + price_shift
            if not np.isnan(next_line) and curr > next_line:
                curr = next_line
            line[idx] = curr
            color[idx] = 1.0
    out = frame.copy()
    out['trendmagic'] = line
    out['color'] = color
    return out.dropna(subset=['trendmagic', 'color'])


