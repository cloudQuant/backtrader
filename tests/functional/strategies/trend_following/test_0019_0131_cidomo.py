def resample_ohlcv(df, rule):
    """Resample OHLCV bars with standard aggregation and right-aligned labels."""
    agg = {
        'open': 'first',
        'high': 'max',
        'low': 'min',
        'close': 'last',
        'volume': 'sum',
        'openinterest': 'last',
        'spread': 'last',
    }
    out = df.resample(rule, label='right', closed='right').agg(agg).dropna()
    return out


