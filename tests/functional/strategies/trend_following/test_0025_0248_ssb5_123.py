def resolve_data_path(filename):
    """Resolve a dataset filename relative to the test script directory.

    Args:
        filename: Relative path to a CSV file from ``tests/datas``.

    Returns:
        Absolute path to the dataset file.
    """
    path = (BASE_DIR / filename).resolve()
    if not path.exists():
        raise FileNotFoundError(f'Data file not found: {path}')
    return path


def parse_dt(value):
    """Parse a datetime-like string into a ``datetime`` object.

    Args:
        value: Datetime string in ISO format, or a falsy value.

    Returns:
        Parsed ``datetime`` object when value is truthy, otherwise ``None``.
    """
    if not value:
        return None
    return datetime.datetime.fromisoformat(value)


