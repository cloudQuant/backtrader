def resample_frame(df, rule):
    """Resample an OHLCV DataFrame to a coarser timeframe.

    Args:
        df: Source DataFrame indexed by datetime with OHLCV columns.
        rule: Pandas offset alias defining the target bar size.

    Returns:
        A resampled DataFrame aggregated with first/max/min/last/sum rules and
        with rows lacking OHLC data dropped.
    """
    out = df.resample(rule, label='right', closed='right').agg({
        'open': 'first',
        'high': 'max',
        'low': 'min',
        'close': 'last',
        'volume': 'sum',
        'real_volume': 'sum',
        'openinterest': 'last',
    })
    out = out.dropna(subset=['open', 'high', 'low', 'close'])
    out['openinterest'] = out['openinterest'].fillna(0)
    return out


def weighted_moving_average(series, period):
    """Compute a linearly weighted moving average of a series.

    Args:
        series: Series of input values.
        period: Window length; values below 1 are clamped to 1.

    Returns:
        A Series of weighted moving averages, with leading entries NaN until the
        window is filled.
    """
    period = int(max(period, 1))
    weights = np.arange(1, period + 1, dtype=float)
    return series.rolling(period).apply(lambda x: np.dot(x, weights) / weights.sum(), raw=True)


def smma(series, period):
    """Compute a smoothed (Wilder) moving average of a series.

    Args:
        series: Series of input values.
        period: Smoothing window length; values below 1 are clamped to 1.

    Returns:
        A Series seeded with the mean of the first ``period`` values and then
        smoothed recursively, with leading entries NaN.
    """
    period = int(max(period, 1))
    values = series.to_numpy(dtype=float)
    out = np.full(len(values), np.nan, dtype=float)
    if len(values) < period:
        return pd.Series(out, index=series.index)
    seed = np.nanmean(values[:period])
    out[period - 1] = seed
    for idx in range(period, len(values)):
        prev = out[idx - 1]
        curr = values[idx]
        if np.isnan(prev) or np.isnan(curr):
            continue
        out[idx] = (prev * (period - 1) + curr) / period
    return pd.Series(out, index=series.index)


def smooth_series(series, method, period):
    """Smooth a series using the requested moving-average method.

    Args:
        series: Series of input values.
        method: Moving-average mode (e.g. ``MODE_SMA``, ``MODE_EMA``,
            ``MODE_SMMA``); anything else falls back to a weighted MA.
        period: Window length; values below 1 are clamped to 1.

    Returns:
        A Series smoothed with the selected method.
    """
    method = str(method or 'MODE_T3').upper()
    period = int(max(period, 1))
    if method in ('MODE_SMA', 'MODE_SMA_'):
        return series.rolling(period).mean()
    if method in ('MODE_EMA', 'MODE_EMA_'):
        return series.ewm(span=period, adjust=False).mean()
    if method in ('MODE_SMMA', 'MODE_SMMA_'):
        return smma(series, period)
    return weighted_moving_average(series, period)


def compute_bnb(frame, xma_method='MODE_T3', xlength=14, xphase=15, volume_type='VOLUME_TICK'):
    """Compute the BnB bull/bear buffers for a resampled OHLCV frame.

    Args:
        frame: DataFrame with open, high, low, close, volume, and real_volume
            columns.
        xma_method: Moving-average method used to smooth the bull/bear pressure.
        xlength: Smoothing window length for the buffers.
        xphase: Phase parameter retained for signature compatibility.
        volume_type: ``VOLUME_TICK`` to use tick volume, otherwise real volume.

    Returns:
        A copy of ``frame`` with ``up_buffer`` and ``dn_buffer`` columns added,
        dropping rows where either buffer is undefined.
    """
    use_tick = str(volume_type or 'VOLUME_TICK').upper() == 'VOLUME_TICK'
    vol = frame['volume'] if use_tick else frame['real_volume']
    vol = vol.replace(0, np.nan).fillna(1.0)
    tic = (frame['high'] - frame['low']) / vol
    tic = tic.replace(0, np.nan).fillna(1.0)
    diff = pd.Series(0.0, index=frame.index)
    open_gt_close = frame['open'] > frame['close']
    open_lt_close = frame['open'] < frame['close']
    diff.loc[open_gt_close] = ((frame.loc[open_gt_close, 'high'] - frame.loc[open_gt_close, 'low']) - (frame.loc[open_gt_close, 'open'] - frame.loc[open_gt_close, 'close'])) / (2.0 * tic.loc[open_gt_close])
    diff.loc[open_lt_close] = ((frame.loc[open_lt_close, 'high'] - frame.loc[open_lt_close, 'low']) - (frame.loc[open_lt_close, 'close'] - frame.loc[open_lt_close, 'open'])) / (2.0 * tic.loc[open_lt_close])
    # pandas 2.x can share the underlying data when constructing a Series from
    # another Series with the same index.  The bull and bear buffers must be
    # independent or the later assignments make both buffers identical and all
    # crossover signals disappear.
    bulls = pd.Series(diff, index=frame.index).copy()
    bears = pd.Series(diff, index=frame.index).copy()
    bulls.loc[open_gt_close] = (frame.loc[open_gt_close, 'open'] - frame.loc[open_gt_close, 'close']) / tic.loc[open_gt_close] + diff.loc[open_gt_close]
    bears.loc[open_lt_close] = (frame.loc[open_lt_close, 'close'] - frame.loc[open_lt_close, 'open']) / tic.loc[open_lt_close] + diff.loc[open_lt_close]
    up = smooth_series(bulls, xma_method, xlength)
    dn = smooth_series(bears, xma_method, xlength)
    out = frame.copy()
    out['up_buffer'] = up
    out['dn_buffer'] = dn
    return out.dropna(subset=['up_buffer', 'dn_buffer'])


