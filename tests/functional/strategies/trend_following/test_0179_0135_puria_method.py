def load_mt5_csv(filepath, fromdate=None, todate=None, bar_shift_minutes=0):
    """Load a MetaTrader 5 tab-separated OHLCV export into a backtrader-ready frame.

    Args:
        filepath: Path to the MT5 CSV file (tab-separated, ``<DATE>``/``<TIME>``
            headers, optionally wrapped in double quotes per line).
        fromdate: Optional inclusive lower datetime bound; earlier rows are dropped.
        todate: Optional inclusive upper datetime bound; later rows are dropped.
        bar_shift_minutes: Minutes added to each bar timestamp so the index marks
            bar-close time rather than bar-open time.

    Returns:
        pandas.DataFrame: Datetime-indexed, sorted frame with ``open``, ``high``,
        ``low``, ``close``, ``volume``, ``openinterest`` and ``spread`` columns.
    """
    with open(filepath, 'r', encoding='utf-8') as f:
        lines = f.read().strip().split('\n')
    cleaned = '\n'.join(line.strip().strip('"') for line in lines if line.strip())
    df = pd.read_csv(io.StringIO(cleaned), sep='\t')
    df['datetime'] = pd.to_datetime(df['<DATE>'] + ' ' + df['<TIME>'], format='%Y.%m.%d %H:%M:%S')
    df = df.rename(columns={
        '<OPEN>': 'open',
        '<HIGH>': 'high',
        '<LOW>': 'low',
        '<CLOSE>': 'close',
        '<TICKVOL>': 'volume',
        '<VOL>': 'openinterest',
        '<SPREAD>': 'spread',
    })
    df = df[['datetime', 'open', 'high', 'low', 'close', 'volume', 'openinterest', 'spread']]
    df = df.set_index('datetime').sort_index()
    if bar_shift_minutes:
        df.index = df.index + pd.Timedelta(minutes=bar_shift_minutes)
    if fromdate is not None:
        df = df[df.index >= fromdate]
    if todate is not None:
        df = df[df.index <= todate]
    return df



def resample_ohlcv(df, rule):
    """Resample an intraday OHLCV+spread frame to a coarser timeframe.

    Args:
        df: Datetime-indexed frame with OHLCV, ``openinterest`` and ``spread``.
        rule: Pandas offset alias for the target timeframe (e.g. ``'30min'``).

    Returns:
        pandas.DataFrame: Right-labelled, right-closed resampled frame using
        first/max/min/last aggregation for OHLC, summed volume, and last open
        interest/spread, with incomplete rows dropped.
    """
    agg = {
        'open': 'first',
        'high': 'max',
        'low': 'min',
        'close': 'last',
        'volume': 'sum',
        'openinterest': 'last',
        'spread': 'last',
    }
    out = df.resample(rule, label='right', closed='right').agg(agg).dropna()
    return out


