class CryptoTrendFollowingStrategy(bt.Strategy):
    """Volatility-targeted MA-regime + Donchian breakout trend follower."""

    params = dict(
        rebalance_tolerance=0.05,
        fast_ma=50,
        slow_ma=200,
        breakout_period=50,
        atr_period=14,
        atr_multiplier=2.5,
        risk_per_trade=0.02,
        max_target_percent=1.0,
        commission_pct=0.0005,
        lot=0.1,
    )

    def __init__(self):
        """Initialize bar/trade counters, the pending-order guard, and series buffers."""
        self.bar_num = 0
        self.buy_count = 0
        self.sell_count = 0
        self.trade_count = 0
        self.win_count = 0
        self.loss_count = 0
        self.pending_order = None
        self.broker_value_series = []
        self.long_signal_days = 0
        self.short_signal_days = 0
        self.neutral_signal_days = 0

    def _current_exposure(self):
        broker_value = float(self.broker.getvalue())
        price = float(self.data.close[0])
        comminfo = self.broker.getcommissioninfo(self.data)
        multiplier = float(getattr(comminfo.p, 'mult', 1.0) or 1.0)
        if broker_value <= 0 or price <= 0 or multiplier <= 0:
            return 0.0
        return float(self.position.size) * price * multiplier / broker_value

    def next(self):
        """Record value, tally the signal regime, and rebalance toward the target percent."""
        self.bar_num += 1
        self.broker_value_series.append((bt.num2date(self.data.datetime[0]), float(self.broker.getvalue())))
        signal_value = float(self.data.signal[0])
        if signal_value > 0.5:
            self.long_signal_days += 1
        elif signal_value < -0.5:
            self.short_signal_days += 1
        else:
            self.neutral_signal_days += 1
        if self.pending_order is not None:
            return
        target_percent = float(self.data.target_percent[0])
        current_exposure = self._current_exposure()
        if abs(target_percent - current_exposure) <= float(self.p.rebalance_tolerance):
            return
        if target_percent > current_exposure:
            self.buy_count += 1
        elif target_percent < current_exposure:
            self.sell_count += 1
        self.pending_order = self.order_target_percent(target=target_percent)

    def notify_order(self, order):
        """Clear the pending-order guard once an order leaves the live state.

        Args:
            order: The order whose status changed; submitted/accepted orders are
                ignored, any other terminal status releases the guard.
        """
        if order.status in (order.Submitted, order.Accepted):
            return
        self.pending_order = None

    def notify_trade(self, trade):
        """Count each closed trade as a win or loss by sign of commissioned PnL.

        Args:
            trade: The trade whose status changed; only closed trades bump the
                trade counter and the win/loss tally.
        """
        if not trade.isclosed:
            return
        self.trade_count += 1
        if trade.pnlcomm >= 0:
            self.win_count += 1
        else:
            self.loss_count += 1


