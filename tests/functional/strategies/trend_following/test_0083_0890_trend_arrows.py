    cerebro.adddata(Mt5PandasFeed(dataname=sf, timeframe=bt.TimeFrame.Minutes, compression=im), name=f"{cfg['data']['symbol']}_{cfg['data']['indicator_timeframe']}")
    sp = dict(p); sp.pop('indicator_minutes', None)
