class BrakeExpIndicator(bt.Indicator):
    """Exponential trailing-stop indicator with trend and flip lines.

    Maintains an exponential-curve stop that rises while long (and falls while
    short) from a begin price; when price breaks the stop the direction flips.
    Exposes the active stop on ``up_trend``/``down_trend`` lines and
    direction-flip cues on ``buy_signal``/``sell_signal`` lines.
    """

    lines = ('up_trend', 'down_trend', 'buy_signal', 'sell_signal')
    params = dict(a=3.0, b=1.0)

    def __init__(self):
        """Set the minimum period and initialize the exponential-stop state."""
        self.addminperiod(5)
        self._is_long = True
        self._max_price = float('-inf')
        self._min_price = float('inf')
        self._begin_bar = 0
        self._begin_price = None

    def next(self):
        """Advance the exponential stop and emit trend and flip lines.

        Extends the stop along the exponential curve, flips direction (resetting
        the begin price and extremes) when price breaks the stop, and sets the
        ``up_trend``/``down_trend`` lines plus ``buy_signal``/``sell_signal`` flip
        cues for the bar.
        """
        if self._begin_price is None:
            self._begin_price = float(self.data.low[0])
        self._max_price = max(self._max_price, float(self.data.high[0]))
        self._min_price = min(self._min_price, float(self.data.low[0]))
        bars_since_begin = max(0, len(self.data) - 1 - self._begin_bar)
        a = float(self.p.a) * 0.1
        b = float(self.p.b) * 0.00001
        exp_val = (math.exp(bars_since_begin * a) - 1.0) * b
        value = self._begin_price + exp_val if self._is_long else self._begin_price - exp_val
        if self._is_long and value > float(self.data.low[0]):
            self._is_long = False
            self._begin_price = self._max_price
            self._begin_bar = len(self.data) - 1
            value = self._begin_price
            self._max_price = float('-inf')
            self._min_price = float('inf')
        elif (not self._is_long) and value < float(self.data.high[0]):
            self._is_long = True
            self._begin_price = self._min_price
            self._begin_bar = len(self.data) - 1
            value = self._begin_price
            self._max_price = float('-inf')
            self._min_price = float('inf')
        prev_up = float(self.lines.up_trend[-1]) if len(self) > 0 else 0.0
        prev_dn = float(self.lines.down_trend[-1]) if len(self) > 0 else 0.0
        if self._is_long:
            self.lines.up_trend[0] = value
            self.lines.down_trend[0] = 0.0
        else:
            self.lines.up_trend[0] = 0.0
            self.lines.down_trend[0] = value
        self.lines.buy_signal[0] = self.lines.down_trend[0] if prev_up > 0.0 and float(self.lines.down_trend[0]) > 0.0 else 0.0
        self.lines.sell_signal[0] = self.lines.up_trend[0] if prev_dn > 0.0 and float(self.lines.up_trend[0]) > 0.0 else 0.0


class BrakeMAStrategy(bt.Strategy):
    """Port of the MT5 ``Exp_BrakeMA`` exponential-stop flip strategy.

    Follows the BrakeExp stop flips: goes long on a down-to-up flip and short on
    an up-to-down flip, entering on the flip and reversing on the opposite flip
    with a fixed lot.

    Args:
        a: Exponential-curve growth coefficient for the stop.
        b: Scale coefficient for the stop offset.
        signal_bar: Which completed bar to read flip cues from.
        lot: Fixed order size in lots.
    """

    params = dict(a=3.0, b=1.0, signal_bar=1, lot=0.1)

    def __init__(self):
        """Build the BrakeExp indicator and reset bar/trade counters."""
        self.indicator = BrakeExpIndicator(self.data, a=self.p.a, b=self.p.b)
        self.bar_num = 0
        self.buy_count = 0
        self.sell_count = 0
        self.trade_count = 0
        self.win_count = 0
        self.loss_count = 0
        self._position_was_open = False

    def log(self, text):
        """Print a timestamped log line for the current bar.

        Args:
            text: The message to log alongside the current bar datetime.
        """
        dt = bt.num2date(self.data.datetime[0])
        print(f'{dt.isoformat()}, {text}')

    def _signals(self):
        shift = max(1, int(self.p.signal_bar))
        up_signal = float(self.indicator.buy_signal[-shift])
        dn_signal = float(self.indicator.sell_signal[-shift])
        up_trend = float(self.indicator.up_trend[-shift])
        dn_trend = float(self.indicator.down_trend[-shift])
        buy_open = up_signal != 0.0
        sell_open = dn_signal != 0.0
        sell_close = buy_open or (up_signal == 0.0 and up_trend != 0.0)
        buy_close = sell_open or (dn_signal == 0.0 and dn_trend != 0.0)
        return buy_open, sell_open, buy_close, sell_close

    def next(self):
        """Open, close, or reverse on BrakeExp flip signals each bar.

        Increments the bar counter, skips during warm-up, derives open/close
        signals from the buy/sell flip cues at the configured signal bar, and
        acts: when flat it opens a long or short; when in a position it closes on
        the close signal or closes and reverses on the opposite open signal.
        """
        self.bar_num += 1
        if len(self.data) < 6:
            return
        buy_open, sell_open, buy_close, sell_close = self._signals()
        up = float(self.indicator.up_trend[0])
        down = float(self.indicator.down_trend[0])
        if self.position:
            if self.position.size > 0:
                if buy_close and not sell_open:
                    self.log(f'close long up={up:.5f} down={down:.5f}')
                    self.close()
                    return
                if sell_open:
                    self.log(f'close long & sell up={up:.5f} down={down:.5f}')
                    self.close()
                    self.sell(size=self.p.lot)
                    return
            if self.position.size < 0:
                if sell_close and not buy_open:
                    self.log(f'close short up={up:.5f} down={down:.5f}')
                    self.close()
                    return
                if buy_open:
                    self.log(f'close short & buy up={up:.5f} down={down:.5f}')
                    self.close()
                    self.buy(size=self.p.lot)
                    return
        else:
            if buy_open:
                self.log(f'buy up={up:.5f} down={down:.5f}')
                self.buy(size=self.p.lot)
                return
            if sell_open:
                self.log(f'sell up={up:.5f} down={down:.5f}')
                self.sell(size=self.p.lot)
                return

    def notify_trade(self, trade):
        """Count entries when a trade opens and win/loss when it closes.

        Args:
            trade: The trade whose status changed; opening increments the
                buy/sell entry counter and closing updates win/loss counts.
        """
        if trade.isopen and not self._position_was_open:
            if trade.size > 0:
                self.buy_count += 1
            elif trade.size < 0:
                self.sell_count += 1
            self._position_was_open = True
            return
        if not trade.isclosed:
            return
        self.trade_count += 1
        if trade.pnlcomm >= 0:
            self.win_count += 1
        else:
            self.loss_count += 1
        self._position_was_open = False
        self.log(f'trade closed pnl={trade.pnlcomm:.2f}')



