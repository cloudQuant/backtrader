def resolve_ma_class(name):
    """Map a moving-average name to its backtrader indicator class.

    Args:
        name: MA type name (e.g. ``sma``, ``ema``, ``smma`` or MT5-style
            ``mode_*`` variants); several smoothing variants map to bt.indicators.EMA.

    Returns:
        The matching backtrader moving-average indicator class, defaulting to
        the weighted moving average for unrecognized names.
    """
    mode = str(name).lower()
    if mode in {'mode_sma', 'sma'}:
        return bt.indicators.SimpleMovingAverage
    if mode in {'mode_ema', 'ema', 'mode_jjma', 'jjma', 'mode_jurx', 'jurx', 'mode_parma', 'parma', 'mode_t3', 't3', 'mode_vidya', 'vidya', 'mode_ama', 'ama'}:
        return bt.indicators.ExponentialMovingAverage
    if mode in {'mode_smma', 'smma'}:
        return bt.indicators.SmoothedMovingAverage
    return bt.indicators.WeightedMovingAverage


def resolve_price_line(data, mode):
    """Return the applied-price line for a data feed given a price mode.

    Args:
        data: The data feed providing OHLC lines.
        mode: Applied-price selector (e.g. ``price_close``, ``price_typical``,
            ``price_weighted`` or their short forms).

    Returns:
        A line expression for the selected applied price, defaulting to the
        typical price for unrecognized modes.
    """
    price_mode = str(mode).lower()
    if price_mode in {'price_close', 'close'}:
        return data.close
    if price_mode in {'price_open', 'open'}:
        return data.open
    if price_mode in {'price_high', 'high'}:
        return data.high
    if price_mode in {'price_low', 'low'}:
        return data.low
    if price_mode in {'price_median', 'median'}:
        return (data.high + data.low) / 2.0
    if price_mode in {'price_typical', 'typical'}:
        return (data.high + data.low + data.close) / 3.0
    if price_mode in {'price_weighted', 'weighted'}:
        return (2.0 * data.close + data.high + data.low) / 4.0
    if price_mode in {'price_simpl', 'simpl'}:
        return (data.open + data.close) / 2.0
    if price_mode in {'price_quarter', 'quarter'}:
        return (data.high + data.low + data.open + data.close) / 4.0
    return (data.high + data.low + data.close) / 3.0


class Color3rdGenXMAStrategy(bt.Strategy):
    """Port of the MT5 ``Exp_Color3rdGenXMA`` slope-color, time-gated strategy.

    Opens longs/shorts based on the 3rd-gen MA slope color but only at a
    configured session start time, and force-closes positions after a maximum
    holding time, using a fixed lot size.

    Args:
        start_hour: Hour at which entries are allowed.
        start_minute: Minute at which entries are allowed.
        time_min: Maximum holding time in minutes before a forced close.
        xma_method: Moving-average type for the indicator.
        xlength: Base length for the 3rd-gen moving average.
        xphase: Phase parameter retained from the source EA (unused here).
        ipc: Applied-price selector for the indicator input.
        shift: Bar shift retained from the source EA (unused here).
        price_shift: Price offset applied to the indicator value.
        signal_bar: Which completed bar to read the color from.
        lot: Fixed order size in lots.
    """

    params = dict(
        start_hour=8,
        start_minute=0,
        time_min=720,
        xma_method='ema',
        xlength=50,
        xphase=15,
        ipc='price_typical',
        shift=0,
        price_shift=0,
        signal_bar=1,
        lot=0.1,
    )

    def __init__(self):
        """Build the Color3rdGenXMA indicator and reset bar/trade counters."""
        self.indicator = bt.indicators.Color3rdGenXMAIndicator(
            self.data,
            xma_method=self.p.xma_method,
            xlength=self.p.xlength,
            xphase=self.p.xphase,
            ipc=self.p.ipc,
            price_shift=self.p.price_shift,
        )
        self.bar_num = 0
        self.buy_count = 0
        self.sell_count = 0
        self.trade_count = 0
        self.win_count = 0
        self.loss_count = 0
        self._position_was_open = False
        self._entry_num = None

    def log(self, text):
        """Print a timestamped log line for the current bar.

        Args:
            text: The message to log alongside the current bar datetime.
        """
        dt = bt.num2date(self.data.datetime[0])
        print(f'{dt.isoformat()}, {text}')

    def _held_minutes(self):
        if not self.position:
            return 0.0
        entry_num = self._entry_num
        if entry_num is None:
            return 0.0
        current_num = self.data.datetime[0]
        return (current_num - entry_num) * 24.0 * 60.0

    def _indicator_signals(self):
        shift = max(1, int(self.p.signal_bar))
        color = int(self.indicator.color[-shift])
        buy_open1 = color == 2
        sell_open1 = color == 0
        buy_close = False
        sell_close = False
        return buy_open1, sell_open1, buy_close, sell_close

    def next(self):
        """Open at the session start on slope color and time-close positions.

        Increments the bar counter, skips during warm-up, reads the indicator
        color at the configured signal bar, opens a long/short only when the
        current bar matches the configured session start time, and force-closes
        any position once held for ``time_min`` minutes.
        """
        self.bar_num += 1
        warmup = int(self.p.xlength) * 3 + int(self.p.signal_bar) + 5
        if len(self.data) < warmup:
            return
        buy_open1, sell_open1, buy_close, sell_close = self._indicator_signals()
        dt = bt.num2date(self.data.datetime[0])
        buy_open = buy_open1 and dt.hour == int(self.p.start_hour) and dt.minute == int(self.p.start_minute)
        sell_open = sell_open1 and dt.hour == int(self.p.start_hour) and dt.minute == int(self.p.start_minute)
        if self.position and self._held_minutes() >= float(self.p.time_min):
            buy_close = True
            sell_close = True
        color = int(self.indicator.color[0])
        value = float(self.indicator.value[0])
        if self.position:
            if self.position.size > 0:
                if buy_close:
                    self.log(f'close long color={color} value={value:.5f}')
                    self.close()
                    return
            if self.position.size < 0:
                if sell_close:
                    self.log(f'close short color={color} value={value:.5f}')
                    self.close()
                    return
        else:
            if buy_open1 and buy_open:
                self.log(f'buy color={color} value={value:.5f}')
                self.buy(size=self.p.lot)
                return
            if sell_open1 and sell_open:
                self.log(f'sell color={color} value={value:.5f}')
                self.sell(size=self.p.lot)
                return

    def notify_trade(self, trade):
        """Record entry time, count entries on open and win/loss on close.

        Args:
            trade: The trade whose status changed; opening stores the entry
                timestamp and increments the buy/sell counter, while closing
                updates the win/loss counts and clears the entry timestamp.
        """
        if trade.isopen and not self._position_was_open:
            self._entry_num = self.data.datetime[0]
            if trade.size > 0:
                self.buy_count += 1
            elif trade.size < 0:
                self.sell_count += 1
            self._position_was_open = True
            return
        if not trade.isclosed:
            return
        self.trade_count += 1
        if trade.pnlcomm >= 0:
            self.win_count += 1
        else:
            self.loss_count += 1
        self._position_was_open = False
        self._entry_num = None
        self.log(f'trade closed pnl={trade.pnlcomm:.2f}')


