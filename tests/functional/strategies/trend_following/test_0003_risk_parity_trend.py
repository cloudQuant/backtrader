def finite_or_none(x):
    """Return ``x`` when it is a finite, truthy number, else ``None``.

    Args:
        x: The numeric value to validate.

    Returns:
        The original value if it is truthy and finite, otherwise ``None``.
    """
    return x if x and math.isfinite(x) else None


def calculate_ulcer_index(values):
    """Compute the Ulcer Index of an equity-value series.

    The Ulcer Index is the root-mean-square of percentage drawdowns from the
    running peak, emphasizing deep and sustained declines.

    Args:
        values: Sequence of portfolio values ordered in time.

    Returns:
        float: The Ulcer Index, or ``0.0`` when fewer than two values exist.
    """
    if len(values) < 2:
        return 0.0
    max_value = values[0]
    sum_squared = 0.0
    for v in values:
        if v > max_value:
            max_value = v
        drawdown = (max_value - v) / max_value * 100.0 if max_value > 0 else 0.0
        sum_squared += drawdown ** 2
    return math.sqrt(sum_squared / len(values))


def get_sharpe_analyzer_kwargs(config):
    """Build runtime kwargs for the Sharpe analyzer from strategy config.

    Args:
        config: Strategy configuration dictionary.

    Returns:
        Dict[str, object]: Keyword arguments for ``bt.analyzers.SharpeRatio``.
    """
    return dict(timeframe=bt.TimeFrame.Days, compression=1, factor=252, annualize=True, riskfreerate=0)


def load_data(config):
    """Load aligned OHLCV frames for each risk-parity basket asset.

    Args:
        config: Full configuration including data source paths and date bounds.

    Returns:
        Dict[str, object]: Prepared inputs and date bounds for cerebro wiring.
    """
    data_cfg = config['data']
    fromdate = datetime.fromisoformat(data_cfg['fromdate'])
    todate = datetime.fromisoformat(data_cfg['todate'])
    asset_frames = {
        symbol: load_mt5_csv((BASE_DIR / path).resolve(), fromdate=fromdate, todate=todate)
        for symbol, path in data_cfg['assets'].items()
    }
    inputs = prepare_risk_parity_inputs(asset_frames, config.get('params', {}))
    return {'inputs': inputs, 'fromdate': fromdate, 'todate': todate}


def build_cerebro(frame, config):
    """Create the Backtrader engine with signal feed, strategy feeds, and analyzers.

    Args:
        frame: Prepared input frame from :func:`load_data`.
        config: Fully resolved configuration object.

    Returns:
        bt.Cerebro: Configured backtesting engine.
    """
    cerebro = bt.Cerebro(stdstats=False)
    bt_cfg = config['backtest']
    cerebro.broker.setcash(float(bt_cfg['initial_cash']))
    comminfo = ETFCommissionInfo(commission=float(bt_cfg.get('commission', 0.001)))
    signal_feed = RiskParitySignalFeed(dataname=frame['inputs']['signal_df'], timeframe=bt.TimeFrame.Days, compression=1)
    cerebro.adddata(signal_feed, name='SIGNAL')
    for symbol in ASSET_ORDER:
        asset_feed = bt.feeds.PandasData(dataname=frame['inputs']['asset_frames'][symbol], timeframe=bt.TimeFrame.Days, compression=1)
        cerebro.adddata(asset_feed, name=symbol)
        cerebro.broker.addcommissioninfo(comminfo, name=symbol)
    cerebro.addstrategy(RiskParityTrendStrategy, **config.get('params', {}))
    cerebro.addanalyzer(bt.analyzers.SharpeRatio, _name='sharpe', **get_sharpe_analyzer_kwargs(config))
    cerebro.addanalyzer(bt.analyzers.TradeAnalyzer, _name='trades')
    cerebro.addanalyzer(bt.analyzers.DrawDown, _name='drawdown')
    cerebro.addanalyzer(bt.analyzers.Returns, _name='returns', tann=252)
    cerebro.addanalyzer(bt.analyzers.SQN, _name='sqn')
    return cerebro


def extract_metrics(strat, cerebro, frame, config):
    """Collect and normalize strategy/analyzer outputs into a metrics payload.

    Args:
        strat: Strategy instance returned by ``cerebro.run()``.
        cerebro: Executed Backtrader engine.
        frame: Data payload from :func:`load_data`.
        config: Configuration used for execution.

    Returns:
        Dict[str, object]: Metrics dictionary consumed by regression checks.
    """
    sharpe = strat.analyzers.sharpe.get_analysis().get('sharperatio')
    drawdown = strat.analyzers.drawdown.get_analysis()
    trades = strat.analyzers.trades.get_analysis()
    returns = strat.analyzers.returns.get_analysis()
    sqn = strat.analyzers.sqn.get_analysis().get('sqn')
    initial_cash = float(config['backtest']['initial_cash'])
    final_value = float(cerebro.broker.getvalue())
    equity_values = [value for _, value in getattr(strat, 'broker_value_series', [])] or [initial_cash, final_value]
    total_trades = trades.get('total', {}).get('closed', 0)
    won = trades.get('won', {}).get('total', 0)
    lost = trades.get('lost', {}).get('total', 0)
    gross_profit = trades.get('won', {}).get('pnl', {}).get('total', 0.0) or 0.0
    gross_loss = abs(trades.get('lost', {}).get('pnl', {}).get('total', 0.0) or 0.0)
    profit_factor = gross_profit / gross_loss if gross_loss > 0 else None
    win_rate = won / total_trades * 100.0 if total_trades else 0.0
    if total_trades == 0:
        total_trades = int(getattr(strat, 'rebalance_count', 0))
    total_return_pct = (final_value / initial_cash - 1.0) * 100.0
    annual_return_pct = (returns.get('rnorm') or 0.0) * 100.0
    max_dd = drawdown.get('max', {}).get('drawdown', 0.0) or 0.0
    return {
        'strategy_name': config['strategy']['name'],
        'fromdate': frame['fromdate'].isoformat(),
        'todate': frame['todate'].isoformat(),
        'bars': len(frame['inputs']['signal_df']),
        'bar_num': strat.bar_num,
        'rebalance_count': strat.rebalance_count,
        'total_trades': total_trades,
        'won': won,
        'lost': lost,
        'win_rate': win_rate,
        'profit_factor': profit_factor,
        'final_value': final_value,
        'net_pnl': final_value - initial_cash,
        'total_return_pct': total_return_pct,
        'annual_return_pct': annual_return_pct,
        'max_drawdown': max_dd,
        'sharpe_ratio': finite_or_none(sharpe),
        'sqn': finite_or_none(sqn),
        'ulcer_index': calculate_ulcer_index(equity_values),
    }


def main():
    """Run the full strategy backtest and produce the extracted metrics."""
    config = _bt_load_config(_CONFIG, repo=_REPO)
    frame = load_data(config)
    cerebro = build_cerebro(frame, config)
    results = cerebro.run()
    strat = results[0]
    metrics = extract_metrics(strat, cerebro, frame, config)


def _close(actual, expected, *, tol, key):
    """Assert ``actual`` is finite and within ``tol`` of ``expected``."""
    assert actual is not None, f"{key}: expected={expected}, got=None"
    a = float(actual)
    assert math.isfinite(a), f"{key}: expected={expected}, got non-finite {actual}"
    assert abs(a - float(expected)) <= tol, (
        f"{key}: expected={expected}, got={a} (tol={tol})"
    )


def _invoke_strategy_main():
    """Call main() or run() depending on what the original script defined."""
    import sys as _sys
    _mod = _sys.modules[__name__]
    if hasattr(_mod, "main") and callable(_mod.main):
        return _mod.main()
    if hasattr(_mod, "run") and callable(_mod.run):
        return _mod.run()
    raise RuntimeError("Neither main() nor run() found in inlined module")


