def parse_dt(value):
    """Parse an ISO 8601 string into a datetime, returning None for empty input.

    Args:
        value: ISO formatted datetime string, or a falsy value.

    Returns:
        datetime.datetime | None: The parsed datetime, or None when ``value`` is empty.
    """
    if not value:
        return None
    return datetime.datetime.fromisoformat(value)



def resample_ohlcv(df, rule):
    """Resample an OHLCV frame to a coarser timeframe using right-closed bars.

    Args:
        df: A datetime-indexed OHLCV frame including a ``spread`` column.
        rule: A pandas offset alias (e.g. ``'4h'``) for the target timeframe.

    Returns:
        pandas.DataFrame: The resampled frame with incomplete rows dropped.
    """
    agg = {
        'open': 'first',
        'high': 'max',
        'low': 'min',
        'close': 'last',
        'volume': 'sum',
        'openinterest': 'last',
        'spread': 'last',
    }
    out = df.resample(rule, label='right', closed='right').agg(agg).dropna()
    return out



def load_backtest_frames(config):
    """Load the M15 work frame and the resampled H4 signal frame.

    Args:
        config: Resolved config mapping whose ``data`` section names the file,
            date window, bar-shift and signal-resample rule.

    Returns:
        dict: Mapping with the ``work`` (M15) and ``h4`` (signal) DataFrames.

    Raises:
        ValueError: If either resulting frame is empty.
    """
    data_cfg = config['data']
    fromdate = parse_dt(data_cfg.get('fromdate'))
    todate = parse_dt(data_cfg.get('todate'))
    df = load_mt5_csv(
        resolve_data_path(data_cfg['file']),
        fromdate=fromdate,
        todate=todate,
        bar_shift_minutes=data_cfg.get('bar_shift_minutes', 0),
    )
    h4 = resample_ohlcv(df, data_cfg.get('signal_rule', '4h'))
    if df.empty or h4.empty:
        raise ValueError('Loaded data frame is empty')
    print(f'Loaded work bars {len(df)}: {df.index[0]} -> {df.index[-1]}')
    print(f'Loaded signal bars {len(h4)}: {h4.index[0]} -> {h4.index[-1]}')
    return {'work': df, 'h4': h4}



def build_cerebro(config, frames):
    """Assemble a cerebro with the broker, M15+H4 feeds, strategy and analyzers.

    Args:
        config: Resolved config mapping providing ``backtest``, ``data`` and ``params``.
        frames: Frame mapping returned by :func:`load_backtest_frames`.

    Returns:
        bt.Cerebro: The fully wired engine ready to run.
    """
    bt_cfg = config['backtest']
    data_cfg = config['data']
    cerebro = bt.Cerebro(stdstats=True)
    cerebro.broker.setcash(bt_cfg['initial_cash'])
    comm_type = bt.CommInfoBase.COMM_FIXED if bt_cfg.get('commission_type', 'fixed') == 'fixed' else bt.CommInfoBase.COMM_PERC
    cerebro.broker.setcommission(
        commission=bt_cfg['commission'],
        margin=bt_cfg['margin'],
        mult=bt_cfg['multiplier'],
        commtype=comm_type,
        stocklike=bt_cfg.get('stocklike', False),
    )
    work_feed = Mt5PandasFeed(dataname=frames['work'], timeframe=bt.TimeFrame.Minutes, compression=data_cfg.get('execution_compression_minutes', 15))
    h4_feed = Mt5PandasFeed(dataname=frames['h4'], timeframe=bt.TimeFrame.Minutes, compression=240)
    cerebro.adddata(work_feed, name=f"{data_cfg['symbol']}_{data_cfg['execution_timeframe']}")
    cerebro.adddata(h4_feed, name=f"{data_cfg['symbol']}_H4")
    cerebro.addstrategy(TDSGlobalStrategy, **config['params'])
    cerebro.addanalyzer(bt.analyzers.SharpeRatio, _name='sharpe', timeframe=bt.TimeFrame.Minutes, factor=MINUTES_PER_TRADING_YEAR, annualize=True, riskfreerate=0)
    cerebro.addanalyzer(bt.analyzers.Returns, _name='returns', timeframe=bt.TimeFrame.Minutes, compression=data_cfg.get('execution_compression_minutes', 15), tann=MINUTES_PER_TRADING_YEAR)
    cerebro.addanalyzer(bt.analyzers.DrawDown, _name='drawdown')
    cerebro.addanalyzer(bt.analyzers.TradeAnalyzer, _name='trades')
    cerebro.addanalyzer(bt.analyzers.SQN, _name='sqn')
    return cerebro



def finite_or_none(value):
    """Return the value unless it is non-finite, in which case return ``None``."""
    if value is None:
        return None
    if isinstance(value, (int, float)) and not math.isfinite(value):
        return None
    return value



def extract_metrics(results, start_value):
    """Collect summary metrics from the strategy, broker and analyzers.

    Args:
        results: Cerebro run result list.
        start_value: Starting account value.

    Returns:
        dict: Trade counts, equity figures and risk/return ratios keyed for the
        regression assertions.
    """
    strat = results[0]
    end_value = strat.broker.getvalue()
    drawdown = strat.analyzers.drawdown.get_analysis()
    returns = strat.analyzers.returns.get_analysis()
    sharpe = strat.analyzers.sharpe.get_analysis()
    sqn = strat.analyzers.sqn.get_analysis()
    trades = strat.analyzers.trades.get_analysis()
    total_closed = trades.get('total', {}).get('closed', 0)
    won = trades.get('won', {}).get('total', 0)
    lost = trades.get('lost', {}).get('total', 0)
    gross_won = trades.get('won', {}).get('pnl', {}).get('total', 0) or 0
    gross_lost = abs(trades.get('lost', {}).get('pnl', {}).get('total', 0) or 0)
    return {
        'bar_num': strat.bar_num,
        'buy_count': strat.buy_count,
        'sell_count': strat.sell_count,
        'trade_count': strat.trade_count,
        'initial_cash': start_value,
        'final_value': end_value,
        'net_pnl': end_value - start_value,
        'total_return_pct': (end_value / start_value - 1.0) * 100.0,
        'total_trades': total_closed,
        'won': won,
        'lost': lost,
        'win_rate': (won / total_closed * 100.0) if total_closed else 0.0,
        'profit_factor': (gross_won / gross_lost) if gross_lost else None,
        'max_drawdown': drawdown.get('max', {}).get('drawdown', 0.0),
        'sharpe_ratio': finite_or_none(sharpe.get('sharperatio')),
        'annual_return_pct': (returns.get('rnorm') or 0.0) * 100.0,
        'sqn': finite_or_none(sqn.get('sqn')),
    }





def run(plot=False):
    """Execute the full backtest and return results, metrics and the engine.

    Args:
        plot: When True, render the cerebro chart after the run.

    Returns:
        tuple: ``(results, metrics, cerebro)`` from the completed backtest.
    """
    config = load_config()
    frames = load_backtest_frames(config)
    cerebro = build_cerebro(config, frames)
    start_value = cerebro.broker.getvalue()
    print('\nStarting backtest...')
    results = cerebro.run(runonce=False)
    metrics = extract_metrics(results, start_value)

    if plot:
        cerebro.plot(style='candlestick')
    return results, metrics, cerebro


def _close(actual, expected, *, tol, key):
    """Assert ``actual`` is finite and within ``tol`` of ``expected``."""
    assert actual is not None, f"{key}: expected={expected}, got=None"
    a = float(actual)
    assert math.isfinite(a), f"{key}: expected={expected}, got non-finite {actual}"
    assert abs(a - float(expected)) <= tol, (
        f"{key}: expected={expected}, got={a} (tol={tol})"
    )


def _invoke_strategy_main():
    """Call main() or run() depending on what the original script defined."""
    import sys as _sys
    _mod = _sys.modules[__name__]
    if hasattr(_mod, "main") and callable(_mod.main):
        return _mod.main()
    if hasattr(_mod, "run") and callable(_mod.run):
        return _mod.run()
    raise RuntimeError("Neither main() nor run() found in inlined module")


