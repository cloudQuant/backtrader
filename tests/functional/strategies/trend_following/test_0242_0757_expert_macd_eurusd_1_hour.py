class ExpertMacdEurusd1HourStrategy(bt.Strategy):
    """Strategy class implementing the MACD crossover pattern.

    Attributes:
        params (dict): Configured strategy parameters.
    """
    params = dict(
        trailing=25,
        risk=0.01,
        macd_fast=5,
        macd_slow=15,
        macd_signal=3,
        point=0.01,
        digits_adjust=10,
        price_digits=2,
        min_lot=0.01,
    )

    def __init__(self):
        """Initialize indicators, backtest tracking metrics, and state variables."""
        self.macd = bt.indicators.MACD(self.data.close, period_me1=self.p.macd_fast, period_me2=self.p.macd_slow, period_signal=self.p.macd_signal)
        self.bar_num = 0
        self.signal_count = 0
        self.buy_count = 0
        self.sell_count = 0
        self.trade_count = 0
        self.win_count = 0
        self.loss_count = 0
        self.completed_order_count = 0
        self.rejected_order_count = 0

        self.order = None
        self.stop_price = None

    def log(self, text):
        """Log the given message with a timestamp prefix.

        Args:
            text (str): Message to log.
        """
        dt = bt.num2date(self.data.datetime[0])
        print(f'{dt.isoformat()}, {text}')

    def _unit(self):
        """Calculate the price unit value based on point size and digit adjustments.

        Returns:
            float: Calculated price step unit.
        """
        return float(self.p.point) * float(self.p.digits_adjust)

    def _lots_optimized(self):
        """Calculate risk-optimized position size.

        Returns:
            float: Risk-optimized lot size.
        """
        volume = round(float(self.broker.getcash()) * float(self.p.risk) / 1000.0, 2)
        return max(volume, float(self.p.min_lot))

    def _mac(self, line, index):
        """Safely fetch historical line values relative to current bar.

        Args:
            line (bt.LineSeries): Target indicator line.
            index (int): Lookback offset.

        Returns:
            float: Historical indicator value.
        """
        return float(line[-index]) if index > 0 else float(line[0])

    def _buy_signal(self):
        """Evaluate MACD buy entry pattern rules.

        Returns:
            bool: True if buy signal is triggered, otherwise False.
        """
        mac1 = self._mac(self.macd.macd, 0)
        mac2 = self._mac(self.macd.macd, 1)
        mac3 = self._mac(self.macd.macd, 2)
        mac4 = self._mac(self.macd.macd, 3)
        sig1 = self._mac(self.macd.signal, 0)
        sig2 = self._mac(self.macd.signal, 1)
        sig3 = self._mac(self.macd.signal, 2)
        sig4 = self._mac(self.macd.signal, 3)
        return sig4 > sig3 and sig3 > sig2 and sig2 < sig1 and mac4 > mac3 and mac3 < mac2 and mac2 < mac1 and mac2 < -0.00020 and mac4 < 0 and mac1 > 0.00020

    def _sell_signal(self):
        """Evaluate MACD sell entry pattern rules.

        Returns:
            bool: True if sell signal is triggered, otherwise False.
        """
        mac1 = self._mac(self.macd.macd, 0)
        mac2 = self._mac(self.macd.macd, 1)
        mac3 = self._mac(self.macd.macd, 2)
        mac4 = self._mac(self.macd.macd, 3)
        sig1 = self._mac(self.macd.signal, 0)
        sig2 = self._mac(self.macd.signal, 1)
        sig3 = self._mac(self.macd.signal, 2)
        sig4 = self._mac(self.macd.signal, 3)
        return sig4 < sig3 and sig3 < sig2 and sig2 > sig1 and mac4 < mac3 and mac3 > mac2 and mac2 > mac1 and mac2 > 0.00020 and mac4 > 0 and mac1 < -0.00035

    def _manage_position(self):
        """Manage active trade risk, checking trailing stop limits and reverse crossovers.

        Returns:
            bool: True if an exit order was successfully placed, otherwise False.
        """
        if not self.position or self.order is not None:
            return False
        mac1 = self._mac(self.macd.macd, 0)
        mac2 = self._mac(self.macd.macd, 1)
        close_price = float(self.data.close[0])
        unit = self._unit()
        if self.position.size > 0:
            if mac1 < mac2:
                self.order = self.close()
                return True
            if self.p.trailing > 0 and close_price - self.position.price > unit * float(self.p.trailing):
                new_stop = round(close_price - unit * float(self.p.trailing), int(self.p.price_digits))
                if self.stop_price is None or new_stop > self.stop_price:
                    self.stop_price = new_stop
            if self.stop_price is not None and float(self.data.low[0]) <= self.stop_price:
                self.order = self.close()
                return True
        else:
            if mac1 > mac2:
                self.order = self.close()
                return True
            if self.p.trailing > 0 and self.position.price - close_price > unit * float(self.p.trailing):
                new_stop = round(close_price + unit * float(self.p.trailing), int(self.p.price_digits))
                if self.stop_price is None or new_stop < self.stop_price:
                    self.stop_price = new_stop
            if self.stop_price is not None and float(self.data.high[0]) >= self.stop_price:
                self.order = self.close()
                return True
        return False

    def next(self):
        """Execute the strategy decision logic on each new bar."""
        self.bar_num += 1
        if len(self) < 10:
            return
        if self.order is not None:
            return
        if self.position:
            if self._manage_position():
                return
            return
        if self._buy_signal():
            self.signal_count += 1
            volume = self._lots_optimized()
            self.order = self.buy(size=volume)
            self.stop_price = None
            self.log(f'buy signal volume={volume:.2f}')
            return
        if self._sell_signal():
            self.signal_count += 1
            volume = self._lots_optimized()
            self.order = self.sell(size=volume)
            self.stop_price = None
            self.log(f'sell signal volume={volume:.2f}')

    def notify_order(self, order):
        """Callback to handle order status updates.

        Args:
            order (bt.Order): The updated order instance.
        """
        if order.status in [bt.Order.Submitted, bt.Order.Accepted]:
            return
        if order.status == bt.Order.Completed:
            self.completed_order_count += 1
            if self.position:
                if order.executed.size > 0:
                    self.buy_count += 1
                elif order.executed.size < 0:
                    self.sell_count += 1
            else:
                self.stop_price = None
        elif order.status in [bt.Order.Canceled, bt.Order.Margin, bt.Order.Rejected, bt.Order.Expired]:
            self.rejected_order_count += 1
        if self.order is not None and order.ref == self.order.ref and order.status not in [bt.Order.Submitted, bt.Order.Accepted]:
            self.order = None

    def notify_trade(self, trade):
        """Callback to handle closed trades and manage win/loss counts.

        Args:
            trade (bt.Trade): The closed trade instance.
        """
        if not trade.isclosed:
            return
        self.trade_count += 1
        if trade.pnlcomm >= 0:
            self.win_count += 1
        else:
            self.loss_count += 1



