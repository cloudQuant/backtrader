def build_resampled_frame(df, indicator_minutes):
    rule = f'{int(indicator_minutes)}min'
    signal_df = df.resample(rule, label='right', closed='right').agg({
        'open': 'first',
        'high': 'max',
        'low': 'min',
        'close': 'last',
        'volume': 'sum',
        'openinterest': 'last',
    })
    signal_df = signal_df.dropna(subset=['open', 'high', 'low', 'close']).copy()
    signal_df['openinterest'] = signal_df['openinterest'].fillna(0)
    return signal_df


def ema_mql_like(values, period):
    period = int(period)
    values = list(values)
    result = [math.nan] * len(values)
    if not values:
        return result
    alpha = 2.0 / (period + 1.0)
    prev = None
    for idx, value in enumerate(values):
        if not math.isfinite(value):
            continue
        if prev is None:
            prev = value
        else:
            prev = alpha * value + (1.0 - alpha) * prev
        result[idx] = prev
    return result


def build_color_lemantrend_frame(df, indicator_minutes, min_period, middle_period, max_period, ema_period):
    signal_df = build_resampled_frame(df, indicator_minutes)
    n = len(signal_df)
    highs = signal_df['high'].astype(float).to_numpy()
    lows = signal_df['low'].astype(float).to_numpy()
    start = max(int(min_period), int(middle_period), int(max_period))
    hh_values = [math.nan] * n
    ll_values = [math.nan] * n

    for idx in range(n):
        if idx < start:
            continue
        current_high = highs[idx]
        max_min = max(highs[idx - int(min_period):idx])
        max_middle = max(highs[idx - int(middle_period):idx])
        max_max = max(highs[idx - int(max_period):idx])
        high_sum = max_min + max_middle + max_max
        hh_values[idx] = 3.0 * current_high - high_sum

        current_low = lows[idx]
        min_min = min(lows[idx - int(min_period):idx])
        min_middle = min(lows[idx - int(middle_period):idx])
        min_max = min(lows[idx - int(max_period):idx])
        low_sum = min_min + min_middle + min_max
        ll_values[idx] = low_sum - 3.0 * current_low

    bulls = ema_mql_like(hh_values, ema_period)
    bears = ema_mql_like(ll_values, ema_period)

    out = signal_df.copy()
    out['bulls'] = bulls
    out['bears'] = bears
    return out


class ColorLeManTrendStrategy(bt.Strategy):
    params = dict(
        signal_bar=1,
        stop_loss_points=1000,
        take_profit_points=2000,
        mm=-0.1,
        point=0.01,
        buy_pos_open=True,
        sell_pos_open=True,
        buy_pos_close=True,
        sell_pos_close=True,
        indicator_minutes=240,
        min_period=13,
        middle_period=21,
        max_period=34,
        ema_period=3,
    )

    def __init__(self):
        self.base = self.datas[0]
        self.signal = self.datas[1]
        self.bar_num = 0
        self.signal_count = 0
        self.buy_count = 0
        self.sell_count = 0
        self.trade_count = 0
        self.win_count = 0
        self.loss_count = 0
        self._position_was_open = False
        self._last_signal_len = 0

    def log(self, text):
        dt = bt.num2date(self.base.datetime[0])
        print(f'{dt.isoformat()}, {text}')

    def _line_value(self, line, ago):
        return float(line[-ago]) if ago else float(line[0])

    def _position_size(self, price):
        if self.p.mm < 0:
            return abs(float(self.p.mm))
        if price <= 0:
            return 0.0
        cash = self.broker.getcash()
        return round((cash * float(self.p.mm)) / price, 4)

    def _check_exit_levels(self):
        if not self.position:
            return False
        close_price = float(self.base.close[0])
        point_value = float(self.p.point)
        stop_distance = self.p.stop_loss_points * point_value if self.p.stop_loss_points > 0 else None
        take_distance = self.p.take_profit_points * point_value if self.p.take_profit_points > 0 else None
        entry_price = float(self.position.price)

        if self.position.size > 0:
            if stop_distance is not None and close_price <= entry_price - stop_distance:
                self.log(f'close long by stop loss close={close_price:.2f} entry={entry_price:.2f}')
                self.close()
                return True
            if take_distance is not None and close_price >= entry_price + take_distance:
                self.log(f'close long by take profit close={close_price:.2f} entry={entry_price:.2f}')
                self.close()
                return True
        elif self.position.size < 0:
            if stop_distance is not None and close_price >= entry_price + stop_distance:
                self.log(f'close short by stop loss close={close_price:.2f} entry={entry_price:.2f}')
                self.close()
                return True
            if take_distance is not None and close_price <= entry_price - take_distance:
                self.log(f'close short by take profit close={close_price:.2f} entry={entry_price:.2f}')
                self.close()
                return True
        return False

    def next(self):
        self.bar_num += 1
        if len(self.base) < 2:
            return

        if self._check_exit_levels():
            return

        signal_bar = max(int(self.p.signal_bar), 1)
        if len(self.signal) < signal_bar + 1:
            return

        current_signal_len = len(self.signal)
        if current_signal_len == self._last_signal_len:
            return
        self._last_signal_len = current_signal_len

        recent_ago = signal_bar - 1
        prev_ago = signal_bar
        bulls_recent = self._line_value(self.signal.bulls, recent_ago)
        bears_recent = self._line_value(self.signal.bears, recent_ago)
        bulls_prev = self._line_value(self.signal.bulls, prev_ago)
        bears_prev = self._line_value(self.signal.bears, prev_ago)
        if not all(math.isfinite(v) for v in [bulls_recent, bears_recent, bulls_prev, bears_prev]):
            return

        buy_signal = bulls_recent > bears_recent and bulls_prev <= bears_prev
        sell_signal = bulls_recent < bears_recent and bulls_prev >= bears_prev
        if not buy_signal and not sell_signal:
            return

        close_price = float(self.base.close[0])
        size = self._position_size(close_price)
        if size <= 0:
            return

        if buy_signal:
            self.signal_count += 1
            self.log(f'buy signal close={close_price:.2f} bulls={bulls_prev:.2f} bears={bears_prev:.2f}')
            if self.position.size < 0 and self.p.sell_pos_close:
                self.close()
            if self.position.size <= 0 and self.p.buy_pos_open:
                self.buy(size=size)
            return

        if sell_signal:
            self.signal_count += 1
            self.log(f'sell signal close={close_price:.2f} bulls={bulls_prev:.2f} bears={bears_prev:.2f}')
            if self.position.size > 0 and self.p.buy_pos_close:
                self.close()
            if self.position.size >= 0 and self.p.sell_pos_open:
                self.sell(size=size)

    def notify_trade(self, trade):
        if trade.isopen and not self._position_was_open:
            if trade.size > 0:
                self.buy_count += 1
            elif trade.size < 0:
                self.sell_count += 1
            self._position_was_open = True
            return
        if not trade.isclosed:
            return
        self.trade_count += 1
        if trade.pnlcomm >= 0:
            self.win_count += 1
        else:
            self.loss_count += 1
        self._position_was_open = False
        self.log(f'trade closed pnl={trade.pnlcomm:.2f}')



