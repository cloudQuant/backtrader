def load_mt5_csv(filepath, fromdate=None, todate=None, bar_shift_minutes=0):
    """Load a MetaTrader 5 tab-separated CSV export into an OHLCV frame.

    Args:
        filepath: Path to the MT5 CSV export file.
        fromdate: Optional inclusive lower bound on the datetime index.
        todate: Optional inclusive upper bound on the datetime index.
        bar_shift_minutes: Minutes to add to each timestamp (e.g. to mark bar
            close rather than bar open).

    Returns:
        A pandas DataFrame indexed by datetime with open, high, low, close,
        volume, and openinterest columns.
    """
    with open(filepath, 'r', encoding='utf-8') as f:
        lines = f.read().strip().split('\n')
    cleaned = '\n'.join(line.strip().strip('"') for line in lines)
    df = pd.read_csv(io.StringIO(cleaned), sep='\t')
    df['datetime'] = pd.to_datetime(df['<DATE>'] + ' ' + df['<TIME>'], format='%Y.%m.%d %H:%M:%S')
    df = df.rename(columns={
        '<OPEN>': 'open',
        '<HIGH>': 'high',
        '<LOW>': 'low',
        '<CLOSE>': 'close',
        '<TICKVOL>': 'volume',
        '<VOL>': 'openinterest',
    })
    df = df[['datetime', 'open', 'high', 'low', 'close', 'volume', 'openinterest']]
    df = df.set_index('datetime')
    if bar_shift_minutes:
        df.index = df.index + pd.Timedelta(minutes=bar_shift_minutes)
    if fromdate is not None:
        df = df[df.index >= fromdate]
    if todate is not None:
        df = df[df.index <= todate]
    return df


def resample_frame(df, rule):
    """Resample an OHLCV frame to a coarser timeframe using right-closed bars.

    Args:
        df: Source OHLCV DataFrame indexed by datetime.
        rule: A pandas resample rule string (e.g. ``'240min'``).

    Returns:
        A resampled DataFrame with open/high/low/close/volume aggregated and
        rows lacking price data dropped.
    """
    out = df.resample(rule, label='right', closed='right').agg({
        'open': 'first',
        'high': 'max',
        'low': 'min',
        'close': 'last',
        'volume': 'sum',
        'openinterest': 'last',
    })
    out = out.dropna(subset=['open', 'high', 'low', 'close'])
    out['openinterest'] = out['openinterest'].fillna(0)
    return out


def weighted_moving_average(series, period):
    """Compute a linearly weighted moving average of a series.

    Args:
        series: Input pandas Series.
        period: Window length (coerced to at least 1).

    Returns:
        A pandas Series with the weighted moving average.
    """
    period = int(max(period, 1))
    weights = np.arange(1, period + 1, dtype=float)
    return series.rolling(period).apply(lambda x: np.dot(x, weights) / weights.sum(), raw=True)


def smma(series, period):
    """Compute a smoothed (Wilder) moving average of a series.

    Args:
        series: Input pandas Series.
        period: Smoothing period (coerced to at least 1).

    Returns:
        A pandas Series with the smoothed moving average, NaN before warmup.
    """
    period = int(max(period, 1))
    values = series.to_numpy(dtype=float)
    out = np.full(len(values), np.nan, dtype=float)
    if len(values) < period:
        return pd.Series(out, index=series.index)
    seed = np.nanmean(values[:period])
    out[period - 1] = seed
    for idx in range(period, len(values)):
        prev = out[idx - 1]
        curr = values[idx]
        if np.isnan(prev) or np.isnan(curr):
            continue
        out[idx] = (prev * (period - 1) + curr) / period
    return pd.Series(out, index=series.index)


def smooth_series(series, method, period):
    """Smooth a series using the requested MT5 moving-average method.

    Args:
        series: Input pandas Series.
        method: MT5 MA method (SMA, EMA, SMMA, or weighted by default).
        period: Smoothing period (coerced to at least 1).

    Returns:
        A pandas Series smoothed by the selected method.
    """
    method = str(method or 'MODE_EMA').upper()
    period = int(max(period, 1))
    if method in ('MODE_SMA', 'MODE_SMA_'):
        return series.rolling(period).mean()
    if method in ('MODE_EMA', 'MODE_EMA_'):
        return series.ewm(span=period, adjust=False).mean()
    if method in ('MODE_SMMA', 'MODE_SMMA_'):
        return smma(series, period)
    return weighted_moving_average(series, period)


def price_series(frame, applied_price):
    """Derive an applied-price series from OHLC columns per MT5 price modes.

    Args:
        frame: OHLCV DataFrame providing open, high, low, and close columns.
        applied_price: MT5 applied-price mode (e.g. ``'PRICE_CLOSE_'``,
            ``'PRICE_DEMARK_'``).

    Returns:
        A pandas Series of the selected applied price, defaulting to close.
    """
    key = str(applied_price or 'PRICE_CLOSE_').upper()
    if key in ('PRICE_CLOSE', 'PRICE_CLOSE_'):
        return frame['close']
    if key in ('PRICE_OPEN', 'PRICE_OPEN_'):
        return frame['open']
    if key in ('PRICE_HIGH', 'PRICE_HIGH_'):
        return frame['high']
    if key in ('PRICE_LOW', 'PRICE_LOW_'):
        return frame['low']
    if key in ('PRICE_MEDIAN', 'PRICE_MEDIAN_'):
        return (frame['high'] + frame['low']) / 2.0
    if key in ('PRICE_TYPICAL', 'PRICE_TYPICAL_'):
        return (frame['high'] + frame['low'] + frame['close']) / 3.0
    if key in ('PRICE_WEIGHTED', 'PRICE_WEIGHTED_'):
        return (frame['high'] + frame['low'] + 2.0 * frame['close']) / 4.0
    if key in ('PRICE_SIMPL', 'PRICE_SIMPL_'):
        return (frame['open'] + frame['close']) / 2.0
    if key in ('PRICE_QUARTER', 'PRICE_QUARTER_'):
        return (frame['high'] + frame['low'] + frame['open'] + frame['close']) / 4.0
    if key in ('PRICE_TRENDFOLLOW0', 'PRICE_TRENDFOLLOW0_'):
        return pd.Series(np.where(frame['close'] >= frame['open'], frame['high'], frame['low']), index=frame.index)
    if key in ('PRICE_TRENDFOLLOW1', 'PRICE_TRENDFOLLOW1_'):
        return pd.Series(np.where(frame['close'] >= frame['open'], frame['low'], frame['high']), index=frame.index)
    if key in ('PRICE_DEMARK', 'PRICE_DEMARK_'):
        cond_up = frame['close'] > frame['open']
        cond_dn = frame['close'] < frame['open']
        return pd.Series(
            np.where(
                cond_up,
                frame['high'] * 2 + frame['low'] + frame['close'],
                np.where(cond_dn, frame['high'] + frame['low'] * 2 + frame['close'], frame['high'] + frame['low'] + frame['close'] * 2),
            ) / 4.0,
            index=frame.index,
        )
    return frame['close']


def compute_tsi_macd(frame, xma_method='MODE_EMA', xfast=8, xslow=21, mom_period=1, xlength1=5, xlength2=8, xlength3=5, xphase=15, ipc='PRICE_CLOSE_'):
    """Compute the TSI-of-MACD oscillator and its smoothed signal line.

    Args:
        frame: OHLCV DataFrame for the signal timeframe.
        xma_method: Moving-average method used for all smoothing steps.
        xfast: Fast MA period for the MACD line.
        xslow: Slow MA period for the MACD line.
        mom_period: Momentum lag applied to the MACD line.
        xlength1: First TSI double-smoothing period.
        xlength2: Second TSI double-smoothing period.
        xlength3: Smoothing period for the signal line.
        xphase: Phase parameter retained for MT5 compatibility (unused here).
        ipc: MT5 applied-price mode for the source price.

    Returns:
        A copy of the frame with ``ind`` (oscillator) and ``sign`` (signal line)
        columns, dropping warmup rows.
    """
    price = pd.Series(price_series(frame, ipc), index=frame.index, dtype=float)
    fast = smooth_series(price, xma_method, xfast)
    slow = smooth_series(price, xma_method, xslow)
    macd = fast - slow
    mtm = macd - macd.shift(int(max(mom_period, 1)))
    absmtm = mtm.abs()
    xmtm = smooth_series(mtm, xma_method, xlength1)
    xabsmtm = smooth_series(absmtm, xma_method, xlength1)
    xxmtm = smooth_series(xmtm, xma_method, xlength2)
    xxabsmtm = smooth_series(xabsmtm, xma_method, xlength2)
    tsi = np.where(xxabsmtm != 0, 100.0 * xxmtm / xxabsmtm, 0.0)
    tsi = pd.Series(tsi, index=frame.index).replace(0.0, 1e-9)
    xtsi = smooth_series(tsi, xma_method, xlength3).replace(0.0, 1e-9)
    out = frame.copy()
    out['ind'] = tsi
    out['sign'] = xtsi
    return out.dropna(subset=['ind', 'sign'])


