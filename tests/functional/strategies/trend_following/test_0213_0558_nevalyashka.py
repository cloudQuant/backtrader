class NevalyashkaStrategy(bt.Strategy):
    """Alternating entry strategy with fixed lots and SL/TP exit policy."""
    params = dict(
        number_minimum_lots=1,
        stop_loss=50,
        take_profit=50,
        point=0.01,
        price_digits=2,
    )

    def __init__(self):
        """Initialize counters and alternating-direction trade state."""
        self.bar_num = 0
        self.signal_count = 0
        self.buy_count = 0
        self.sell_count = 0
        self.trade_count = 0
        self.win_count = 0
        self.loss_count = 0
        self.completed_order_count = 0
        self.rejected_order_count = 0
        self.order = None
        self.stop_price = None
        self.take_profit_price = None
        self.first_start = True
        self.next_direction = None
        self.last_direction = 'sell'

    def _point(self):
        return float(self.p.point)

    def _round(self, value):
        return round(float(value), int(self.p.price_digits))

    def _lot(self):
        return max(1.0, float(self.p.number_minimum_lots))

    def _arm(self, direction, price):
        sl = float(self.p.stop_loss) * self._point()
        tp = float(self.p.take_profit) * self._point()
        if direction == 'buy':
            self.stop_price = self._round(price - sl) if sl > 0 else None
            self.take_profit_price = self._round(price + tp) if tp > 0 else None
            self.signal_count += 1
            self.last_direction = 'buy'
            self.order = self.buy(size=self._lot())
        else:
            self.stop_price = self._round(price + sl) if sl > 0 else None
            self.take_profit_price = self._round(price - tp) if tp > 0 else None
            self.signal_count += 1
            self.last_direction = 'sell'
            self.order = self.sell(size=self._lot())

    def _check_exit(self):
        if not self.position or self.order is not None:
            return
        high = float(self.data.high[0])
        low = float(self.data.low[0])
        if self.position.size > 0:
            if self.take_profit_price is not None and high >= float(self.take_profit_price):
                self.order = self.close(); return
            if self.stop_price is not None and low <= float(self.stop_price):
                self.order = self.close(); return
        else:
            if self.take_profit_price is not None and low <= float(self.take_profit_price):
                self.order = self.close(); return
            if self.stop_price is not None and high >= float(self.stop_price):
                self.order = self.close(); return

    def next(self):
        """Evaluate setup state for each bar.

        On first bar, initializes with an initial short order.
        On subsequent bars, alternates direction when the previous trade closed,
        unless a manually requested next direction is already queued.
        """
        self.bar_num += 1
        if self.order is not None:
            return
        if self.position:
            self._check_exit()
            return

        if self.first_start:
            self.first_start = False
            self._arm('sell', float(self.data.close[0]))
            return

        if self.next_direction is not None:
            direction = self.next_direction
            self.next_direction = None
            self._arm(direction, float(self.data.close[0]))
            return

        if self.last_direction == 'buy':
            self._arm('sell', float(self.data.close[0]))
        else:
            self._arm('buy', float(self.data.close[0]))

    def notify_order(self, order):
        """Track completed/cancelled orders and reset pending order pointers."""
        if order.status in [bt.Order.Submitted, bt.Order.Accepted]:
            return
        if order.status == bt.Order.Completed:
            self.completed_order_count += 1
            if self.position:
                if order.executed.size > 0:
                    self.buy_count += 1
                elif order.executed.size < 0:
                    self.sell_count += 1
            else:
                self.stop_price = None
                self.take_profit_price = None
        elif order.status in [bt.Order.Canceled, bt.Order.Margin, bt.Order.Rejected, bt.Order.Expired]:
            self.rejected_order_count += 1
        if self.order is not None and order.ref == self.order.ref and order.status not in [bt.Order.Submitted, bt.Order.Accepted]:
            self.order = None

    def notify_trade(self, trade):
        """Update trade counters and prepare the direction flip for next cycle."""
        if not trade.isclosed:
            return
        self.trade_count += 1
        self.next_direction = 'sell' if self.last_direction == 'buy' else 'buy'
        if trade.pnlcomm >= 0:
            self.win_count += 1
        else:
            self.loss_count += 1


