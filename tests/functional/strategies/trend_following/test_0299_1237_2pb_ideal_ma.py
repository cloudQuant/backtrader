def build_resampled_frame(df, indicator_minutes):
    """Resample an M15 OHLCV frame to a coarser signal timeframe.

    Args:
        df: Source M15 OHLCV DataFrame indexed by datetime.
        indicator_minutes: Target bar size in minutes for the signal frame.

    Returns:
        A resampled DataFrame with open/high/low/close/volume aggregated and
        rows lacking price data dropped.
    """
    rule = f'{int(indicator_minutes)}min'
    signal_df = df.resample(rule, label='right', closed='right').agg({
        'open': 'first',
        'high': 'max',
        'low': 'min',
        'close': 'last',
        'volume': 'sum',
        'openinterest': 'last',
    })
    signal_df = signal_df.dropna(subset=['open', 'high', 'low', 'close']).copy()
    signal_df['openinterest'] = signal_df['openinterest'].fillna(0)
    return signal_df


def ideal_ma_smooth(weight_1, weight_2, series_1, series_0, result_1):
    """Advance one step of the IdealMA non-linear adaptive smoothing recurrence.

    Args:
        weight_1: Primary smoothing weight (1/period).
        weight_2: Secondary weight scaling the squared-change responsiveness term.
        series_1: Previous input value.
        series_0: Current input value.
        result_1: Previous IdealMA output.

    Returns:
        The updated IdealMA value for the current step; returns the previous
        result when the denominator degenerates to ~0.
    """
    dseries = series_0 - series_1
    dseries2 = dseries * dseries - 1.0
    denominator = 1.0 + weight_2 * dseries2
    if abs(denominator) <= 1e-12:
        return result_1
    return (
        weight_1 * (series_0 - result_1)
        + result_1
        + weight_2 * result_1 * dseries2
    ) / denominator


def build_fast_ideal_ma(price_series, period1, period2):
    """Compute the single-pass fast IdealMA line over a price series.

    Args:
        price_series: Input price series for the signal timeframe.
        period1: Primary IdealMA period (sets the primary weight).
        period2: Secondary IdealMA period (sets the responsiveness weight).

    Returns:
        A list of fast IdealMA values aligned to the input, seeded at index 0.
    """
    weight_1 = 1.0 / max(1, int(period1))
    weight_2 = 1.0 / max(1, int(period2))
    values = price_series.astype(float).tolist()
    result = [math.nan] * len(values)
    if not values:
        return result
    result[0] = values[0]
    for idx in range(1, len(values)):
        result[idx] = ideal_ma_smooth(weight_1, weight_2, values[idx - 1], values[idx], result[idx - 1])
    return result


def build_slow_ideal_ma(price_series, periodx1, periodx2, periody1, periody2, periodz1, periodz2):
    """Compute the triple-cascaded slow IdealMA line over a price series.

    Chains three IdealMA passes (x, y, z stages) so the slow line is heavily
    smoothed relative to the fast line.

    Args:
        price_series: Input price series for the signal timeframe.
        periodx1: Primary period of the first (x) IdealMA stage.
        periodx2: Responsiveness period of the first (x) IdealMA stage.
        periody1: Primary period of the second (y) IdealMA stage.
        periody2: Responsiveness period of the second (y) IdealMA stage.
        periodz1: Primary period of the third (z) IdealMA stage.
        periodz2: Responsiveness period of the third (z) IdealMA stage.

    Returns:
        A list of slow IdealMA values aligned to the input, seeded at index 0.
    """
    wx1 = 1.0 / max(1, int(periodx1))
    wx2 = 1.0 / max(1, int(periodx2))
    wy1 = 1.0 / max(1, int(periody1))
    wy2 = 1.0 / max(1, int(periody2))
    wz1 = 1.0 / max(1, int(periodz1))
    wz2 = 1.0 / max(1, int(periodz2))
    values = price_series.astype(float).tolist()
    result = [math.nan] * len(values)
    if not values:
        return result

    result[0] = values[0]
    moving01 = values[0]
    moving11 = values[0]
    moving21 = values[0]
    for idx in range(1, len(values)):
        moving00 = ideal_ma_smooth(wx1, wx2, values[idx - 1], values[idx], moving01)
        moving10 = ideal_ma_smooth(wy1, wy2, moving01, moving00, moving11)
        moving20 = ideal_ma_smooth(wz1, wz2, moving11, moving10, moving21)
        moving01 = moving00
        moving11 = moving10
        moving21 = moving20
        result[idx] = moving20
    return result


def build_2pb_ideal_ma_frame(df, indicator_minutes, period1, period2, periodx1, periodx2, periody1, periody2, periodz1, periodz2):
    """Build the resampled signal frame with fast and slow IdealMA lines.

    Args:
        df: Source M15 OHLCV DataFrame.
        indicator_minutes: Signal timeframe bar size in minutes.
        period1: Primary period for the fast IdealMA.
        period2: Responsiveness period for the fast IdealMA.
        periodx1: Primary period of the slow IdealMA first stage.
        periodx2: Responsiveness period of the slow IdealMA first stage.
        periody1: Primary period of the slow IdealMA second stage.
        periody2: Responsiveness period of the slow IdealMA second stage.
        periodz1: Primary period of the slow IdealMA third stage.
        periodz2: Responsiveness period of the slow IdealMA third stage.

    Returns:
        The resampled signal DataFrame with added ``fast_ma`` and ``slow_ma``
        columns.
    """
    signal_df = build_resampled_frame(df, indicator_minutes)
    price_series = signal_df['close'].astype(float)
    signal_df = signal_df.copy()
    signal_df['fast_ma'] = build_fast_ideal_ma(price_series, period1, period2)
    signal_df['slow_ma'] = build_slow_ideal_ma(price_series, periodx1, periodx2, periody1, periody2, periodz1, periodz2)
    return signal_df


class TwoPbIdealMAStrategy(bt.Strategy):
    """Dual-timeframe strategy trading fast/slow IdealMA crossovers.

    Reads the precomputed fast and slow IdealMA lines from the resampled signal
    feed and trades the M15 execution feed, opening on a crossover, optionally
    closing an opposite position, with fixed point-based stop-loss and
    take-profit exits.
    """

    params = dict(
        signal_bar=1,
        stop_loss_points=1000,
        take_profit_points=2000,
        mm=-0.1,
        point=0.01,
        buy_pos_open=True,
        sell_pos_open=True,
        buy_pos_close=True,
        sell_pos_close=True,
        indicator_minutes=240,
        period1=10,
        period2=10,
        periodx1=10,
        periodx2=10,
        periody1=10,
        periody2=10,
        periodz1=10,
        periodz2=10,
    )

    def __init__(self):
        """Bind execution and signal feeds and initialize counters and state."""
        self.base = self.datas[0]
        self.signal = self.datas[1]
        self.bar_num = 0
        self.signal_count = 0
        self.buy_count = 0
        self.sell_count = 0
        self.trade_count = 0
        self.win_count = 0
        self.loss_count = 0
        self._position_was_open = False
        self._last_signal_len = 0

    def log(self, text):
        """Print a timestamped log line using the base feed's current datetime.

        Args:
            text: Message to log alongside the current bar timestamp.
        """
        dt = bt.num2date(self.base.datetime[0])
        print(f'{dt.isoformat()}, {text}')

    def _line_value(self, line, ago):
        return float(line[-ago]) if ago else float(line[0])

    def _position_size(self, price):
        if self.p.mm < 0:
            return abs(float(self.p.mm))
        if price <= 0:
            return 0.0
        cash = self.broker.getcash()
        return round((cash * float(self.p.mm)) / price, 4)

    def _check_exit_levels(self):
        if not self.position:
            return False
        close_price = float(self.base.close[0])
        point_value = float(self.p.point)
        stop_distance = self.p.stop_loss_points * point_value if self.p.stop_loss_points > 0 else None
        take_distance = self.p.take_profit_points * point_value if self.p.take_profit_points > 0 else None
        entry_price = float(self.position.price)

        if self.position.size > 0:
            if stop_distance is not None and close_price <= entry_price - stop_distance:
                self.log(f'close long by stop loss close={close_price:.2f} entry={entry_price:.2f}')
                self.close()
                return True
            if take_distance is not None and close_price >= entry_price + take_distance:
                self.log(f'close long by take profit close={close_price:.2f} entry={entry_price:.2f}')
                self.close()
                return True
        elif self.position.size < 0:
            if stop_distance is not None and close_price >= entry_price + stop_distance:
                self.log(f'close short by stop loss close={close_price:.2f} entry={entry_price:.2f}')
                self.close()
                return True
            if take_distance is not None and close_price <= entry_price - take_distance:
                self.log(f'close short by take profit close={close_price:.2f} entry={entry_price:.2f}')
                self.close()
                return True
        return False

    def next(self):
        """Process each new signal bar to open or reverse on IdealMA crossovers."""
        self.bar_num += 1
        if len(self.base) < 2:
            return

        if self._check_exit_levels():
            return

        signal_bar = max(int(self.p.signal_bar), 1)
        if len(self.signal) < signal_bar + 1:
            return

        current_signal_len = len(self.signal)
        if current_signal_len == self._last_signal_len:
            return
        self._last_signal_len = current_signal_len

        recent_ago = signal_bar - 1
        prev_ago = signal_bar
        fast_recent = self._line_value(self.signal.fast_ma, recent_ago)
        slow_recent = self._line_value(self.signal.slow_ma, recent_ago)
        fast_prev = self._line_value(self.signal.fast_ma, prev_ago)
        slow_prev = self._line_value(self.signal.slow_ma, prev_ago)
        if not all(math.isfinite(v) for v in [fast_recent, slow_recent, fast_prev, slow_prev]):
            return

        buy_signal = fast_recent > slow_recent and fast_prev < slow_prev
        sell_signal = fast_recent < slow_recent and fast_prev > slow_prev
        if not buy_signal and not sell_signal:
            return

        close_price = float(self.base.close[0])
        size = self._position_size(close_price)
        if size <= 0:
            return

        if buy_signal:
            self.signal_count += 1
            self.log(f'buy signal close={close_price:.2f} fast={fast_recent:.2f} slow={slow_recent:.2f}')
            if self.position.size < 0 and self.p.sell_pos_close:
                self.close()
            if self.position.size <= 0 and self.p.buy_pos_open:
                self.buy(size=size)
            return

        if sell_signal:
            self.signal_count += 1
            self.log(f'sell signal close={close_price:.2f} fast={fast_recent:.2f} slow={slow_recent:.2f}')
            if self.position.size > 0 and self.p.buy_pos_close:
                self.close()
            if self.position.size >= 0 and self.p.sell_pos_open:
                self.sell(size=size)

    def notify_trade(self, trade):
        """Count directional entries on open and tally closed trades by outcome.

        Args:
            trade: The backtrader trade being notified.
        """
        if trade.isopen and not self._position_was_open:
            if trade.size > 0:
                self.buy_count += 1
            elif trade.size < 0:
                self.sell_count += 1
            self._position_was_open = True
            return
        if not trade.isclosed:
            return
        self.trade_count += 1
        if trade.pnlcomm >= 0:
            self.win_count += 1
        else:
            self.loss_count += 1
        self._position_was_open = False
        self.log(f'trade closed pnl={trade.pnlcomm:.2f}')


