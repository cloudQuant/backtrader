class Mt5PandasFeed(bt.feeds.PandasData):
    """PandasData feed mapping the standard OHLCV columns by position."""

    params = (
        ('datetime', None), ('open', 0), ('high', 1), ('low', 2),
        ('close', 3), ('volume', 4), ('openinterest', 5),
    )

class VortexStrategy(bt.Strategy):
    """
    Vortex Indicator strategy.
    Buy: VI+ crosses above VI- (bullish cloud color change)
    Sell: VI- crosses above VI+ (bearish cloud color change)
    """
    params = dict(
        vortex_period=14,
        lot=0.1,
        point=0.01,
        price_digits=2,
    )

    def __init__(self):
        """Attach the Vortex indicator and zero the trade/position counters."""
        self.vortex = bt.indicators.VortexIndicator(self.data, period=self.p.vortex_period)
        self.bar_num = 0
        self.buy_count = 0
        self.sell_count = 0
        self.trade_count = 0
        self.win_count = 0
        self.loss_count = 0
        self._position_was_open = False

    def log(self, text):
        """Print a timestamped log line for the current bar.

        Args:
            text: The message to print alongside the current bar datetime.
        """
        dt = bt.num2date(self.data.datetime[0])
        print(f'{dt.isoformat()}, {text}')

    def next(self):
        """Enter or reverse on VI+/VI- crossovers once warm-up has elapsed."""
        self.bar_num += 1
        if len(self.data) < self.p.vortex_period + 5:
            return

        vip0 = float(self.vortex.vi_plus[0])
        vim0 = float(self.vortex.vi_minus[0])
        vip1 = float(self.vortex.vi_plus[-1])
        vim1 = float(self.vortex.vi_minus[-1])

        bull_cross = vip1 < vim1 and vip0 > vim0
        bear_cross = vip1 > vim1 and vip0 < vim0

        if self.position:
            if self.position.size > 0 and bear_cross:
                self.log(f'close long & sell vi+={vip0:.3f} vi-={vim0:.3f}')
                self.close()
                self.sell(size=self.p.lot)
                return
            elif self.position.size < 0 and bull_cross:
                self.log(f'close short & buy vi+={vip0:.3f} vi-={vim0:.3f}')
                self.close()
                self.buy(size=self.p.lot)
                return
        else:
            if bull_cross:
                self.log(f'buy vi+={vip0:.3f} vi-={vim0:.3f}')
                self.buy(size=self.p.lot)
                return
            if bear_cross:
                self.log(f'sell vi+={vip0:.3f} vi-={vim0:.3f}')
                self.sell(size=self.p.lot)
                return

    def notify_trade(self, trade):
        """Count entries by direction on open and tally wins/losses on close.

        Args:
            trade: The trade whose status changed; a newly opened trade bumps the
                buy/sell counter by direction, and a closed trade bumps the trade
                counter and the win/loss tally.
        """
        if trade.isopen and not self._position_was_open:
            if trade.size > 0:
                self.buy_count += 1
            elif trade.size < 0:
                self.sell_count += 1
            self._position_was_open = True
            return
        if not trade.isclosed:
            return
        self.trade_count += 1
        if trade.pnlcomm >= 0:
            self.win_count += 1
        else:
            self.loss_count += 1
        self._position_was_open = False
        self.log(f'trade closed pnl={trade.pnlcomm:.2f}')


