def resample_frame(df, rule):
    """Resample an OHLCV frame to a coarser bar rule.

    Args:
        df: Source OHLCV DataFrame indexed by datetime.
        rule: A pandas resample rule string (e.g. ``60min``).

    Returns:
        A resampled OHLCV DataFrame with NaN OHLC rows dropped and openinterest
        gaps filled with zero.
    """
    out = df.resample(rule, label='right', closed='right').agg({
        'open': 'first',
        'high': 'max',
        'low': 'min',
        'close': 'last',
        'volume': 'sum',
        'openinterest': 'last',
    })
    out = out.dropna(subset=['open', 'high', 'low', 'close'])
    out['openinterest'] = out['openinterest'].fillna(0)
    return out


def compute_mfi(frame, period):
    """Compute the Money Flow Index (volume-weighted RSI) of a frame.

    Args:
        frame: OHLCV DataFrame providing high/low/close and volume.
        period: MFI lookback period.

    Returns:
        An MFI Series in 0..100, filling undefined values with 100.0.
    """
    period = int(period)
    typical = (frame['high'].astype(float) + frame['low'].astype(float) + frame['close'].astype(float)) / 3.0
    raw_money_flow = typical * frame['volume'].astype(float)
    direction = typical.diff()
    positive_flow = raw_money_flow.where(direction > 0.0, 0.0)
    negative_flow = raw_money_flow.where(direction < 0.0, 0.0)
    positive_sum = positive_flow.rolling(period, min_periods=period).sum()
    negative_sum = negative_flow.rolling(period, min_periods=period).sum()
    money_ratio = positive_sum / negative_sum.replace(0.0, np.nan)
    mfi = 100.0 - (100.0 / (1.0 + money_ratio))
    return mfi.fillna(100.0).where(negative_sum != 0.0, 100.0)


def rolling_stochastic(series, window, scale_min=0.0, scale_max=100.0):
    """Compute a rolling stochastic rescale of a series.

    Args:
        series: The input Series to transform.
        window: Rolling window length for the high/low range.
        scale_min: Lower bound of the output range.
        scale_max: Upper bound of the output range.

    Returns:
        A Series rescaled into ``[scale_min, scale_max]``; flat-range bars carry
        the previous value forward.
    """
    llv = series.rolling(window, min_periods=1).min()
    hhv = series.rolling(window, min_periods=1).max()
    out = pd.Series(index=series.index, dtype='float64')
    prev = np.nan
    for idx, value in enumerate(series.tolist()):
        low = llv.iloc[idx]
        high = hhv.iloc[idx]
        if pd.isna(value) or pd.isna(low) or pd.isna(high):
            out.iloc[idx] = np.nan
            continue
        if high - low != 0:
            out.iloc[idx] = ((value - low) / (high - low)) * (scale_max - scale_min) + scale_min
        else:
            out.iloc[idx] = prev
        prev = out.iloc[idx]
    return out


def recursive_smooth(series, factor=0.5):
    """Apply a first-order recursive smoothing to a series.

    Args:
        series: The input Series to smooth.
        factor: Smoothing factor applied to the bar-over-bar change.

    Returns:
        The recursively smoothed Series, preserving NaN gaps.
    """
    out = []
    prev = np.nan
    for value in series.tolist():
        if pd.isna(value):
            out.append(np.nan)
            continue
        if pd.isna(prev):
            current = value
        else:
            current = factor * (value - prev) + prev
        out.append(current)
        prev = current
    return pd.Series(out, index=series.index, dtype='float64')


def compute_color_schaff_mfi_trend_cycle(frame, fast_mfi=23, slow_mfi=50, cycle=10, high_level=60, low_level=-60):
    """Compute the Color Schaff MFI Trend Cycle signal columns for a frame.

    Args:
        frame: OHLCV DataFrame to compute on.
        fast_mfi: Fast MFI period for the MACD source.
        slow_mfi: Slow MFI period for the MACD source.
        cycle: Stochastic cycle window for the trend-cycle transform.
        high_level: Upper momentum threshold for color coding.
        low_level: Lower momentum threshold for color coding.

    Returns:
        A copy of ``frame`` with ``stc``, ``color_code``, ``buy_signal``, and
        ``sell_signal`` columns added, dropping rows where stc/color is
        undefined.
    """
    fast = compute_mfi(frame, fast_mfi)
    slow = compute_mfi(frame, slow_mfi)
    macd = fast - slow

    st = rolling_stochastic(macd, int(cycle), scale_min=0.0, scale_max=100.0)
    st = recursive_smooth(st, factor=0.5)
    stc = rolling_stochastic(st, int(cycle), scale_min=-100.0, scale_max=100.0)
    stc = recursive_smooth(stc, factor=0.5)

    color_code = []
    previous = np.nan
    for value in stc.tolist():
        if pd.isna(value):
            color_code.append(np.nan)
            previous = value
            continue
        slope = 0.0 if pd.isna(previous) else value - previous
        clr = 4
        if value > 0:
            if value > high_level:
                clr = 7 if slope >= 0 else 6
            else:
                clr = 5 if slope >= 0 else 4
        if value < 0:
            if value < low_level:
                clr = 0 if slope < 0 else 1
            else:
                clr = 2 if slope < 0 else 3
        color_code.append(float(clr))
        previous = value

    color_code = pd.Series(color_code, index=frame.index, dtype='float64')
    buy_signal = (color_code.shift(1) > 5.0) & (color_code < 6.0)
    sell_signal = (color_code.shift(1) < 2.0) & (color_code > 1.0)

    out = frame.copy()
    out['stc'] = stc
    out['color_code'] = color_code
    out['buy_signal'] = buy_signal.astype(float)
    out['sell_signal'] = sell_signal.astype(float)
    return out.dropna(subset=['stc', 'color_code'])


