def build_signal_frame(df, indicator_minutes, cci_period):
    """Resample to the signal timeframe and compute a CCI column.

    Aggregates the base frame into ``indicator_minutes`` bars and adds a
    Commodity Channel Index built from the typical price and its mean absolute
    deviation over ``cci_period`` bars.

    Args:
        df: Base OHLCV DataFrame indexed by datetime.
        indicator_minutes: Signal bar size in minutes.
        cci_period: Lookback period for the CCI calculation.

    Returns:
        The resampled signal DataFrame with an added ``cci`` column.
    """
    rule = f'{int(indicator_minutes)}min'
    signal_df = df.resample(rule, label='right', closed='right').agg({
        'open': 'first',
        'high': 'max',
        'low': 'min',
        'close': 'last',
        'volume': 'sum',
        'openinterest': 'last',
        'spread': 'last',
    })
    signal_df = signal_df.dropna(subset=['open', 'high', 'low', 'close']).copy()
    typical = (signal_df['high'] + signal_df['low'] + signal_df['close']) / 3.0
    sma = typical.rolling(int(cci_period)).mean()
    mad = (typical - sma).abs().rolling(int(cci_period)).mean()
    denom = (0.015 * mad).replace(0, pd.NA)
    signal_df['cci'] = ((typical - sma) / denom).fillna(0.0)
    signal_df['openinterest'] = signal_df['openinterest'].fillna(0)
    signal_df['spread'] = signal_df['spread'].fillna(0)
    return signal_df


class ProbeStrategy(bt.Strategy):
    """Port of the MT5 ``probe`` expert advisor using a CCI breakout signal.

    Arms stop-entry orders when the H4 CCI crosses back inside its symmetric
    thresholds, protects filled positions with a fixed stop-loss and a trailing
    stop, and prunes pending stop orders that drift too far from price. Executes
    on the M15 feed while reading signals from a separate H4 CCI feed.

    Args:
        lots: Order size in lots for each stop-entry order.
        point_size: Price value of one pip for distances and rounding.
        price_digits: Number of decimals used when rounding order prices.
        stoploss_pips: Initial stop-loss distance in pips (0 disables).
        trailing_stop_pips: Trailing-stop distance in pips (0 disables).
        trailing_step_pips: Minimum favorable move before trailing again.
        cci_max_min: Symmetric CCI threshold for arming entries.
        indent_pips: Distance in pips from price for the stop-entry orders.
        signal_bar: Which completed signal bar to read the CCI from.
        cci_period: Lookback period for the CCI (used when building the feed).
    """

    params = dict(
        lots=1.0,
        point_size=0.01,
        price_digits=2,
        stoploss_pips=50,
        trailing_stop_pips=5,
        trailing_step_pips=5,
        cci_max_min=120.0,
        indent_pips=30,
        signal_bar=1,
        cci_period=60,
    )

    def __init__(self):
        """Bind the execution and signal feeds and reset order/trade state.

        Captures the M15 execution feed and the H4 signal feed, then clears all
        pending-order, stop-order, trailing and trade-counter state.
        """
        self.exec_data = self.datas[0]
        self.signal_data = self.datas[1]
        self.pending_buy = None
        self.pending_sell = None
        self.position_stop = None
        self.stop_cancel_pending = False
        self.stop_replace_price = None
        self.last_signal_dt = None
        self.active_side = None
        self.active_stop_price = None
        self.bar_num = 0
        self.buy_count = 0
        self.sell_count = 0
        self.trade_count = 0
        self.win_count = 0
        self.loss_count = 0

    def log(self, text):
        """Print a timestamped log line for the current execution bar.

        Args:
            text: The message to log alongside the current bar datetime.
        """
        dt = bt.num2date(self.exec_data.datetime[0])
        print(f'{dt.isoformat()}, {text}')

    def _same_order(self, left, right):
        if left is None or right is None:
            return False
        return getattr(left, 'ref', None) == getattr(right, 'ref', None)

    def _round_price(self, value):
        return round(float(value), int(self.p.price_digits))

    def _cancel_pending(self):
        for order in (self.pending_buy, self.pending_sell):
            if order is not None and order.alive():
                self.cancel(order)
        self.pending_buy = None
        self.pending_sell = None

    def _cancel_stop(self):
        if self.position_stop is not None:
            self.cancel(self.position_stop)
            self.position_stop = None
        self.stop_cancel_pending = False
        self.stop_replace_price = None

    def _place_position_stop(self, stop_price):
        rounded = self._round_price(stop_price)
        if self.position.size > 0:
            self.position_stop = self.sell(size=abs(self.position.size), exectype=bt.Order.Stop, price=rounded)
        elif self.position.size < 0:
            self.position_stop = self.buy(size=abs(self.position.size), exectype=bt.Order.Stop, price=rounded)
        else:
            self.position_stop = None
            return
        self.active_stop_price = rounded
        self.stop_cancel_pending = False
        self.stop_replace_price = None

    def _new_signal_bar(self):
        recent_ago = max(int(self.p.signal_bar), 1) - 1
        current = bt.num2date(self.signal_data.datetime[-recent_ago]) if recent_ago else bt.num2date(self.signal_data.datetime[0])
        if self.last_signal_dt == current:
            return False
        self.last_signal_dt = current
        return True

    def _delete_far_pending(self):
        indent = self.p.indent_pips * self.p.point_size
        if self.pending_buy is not None and self.pending_buy.alive():
            if float(self.pending_buy.created.price) - float(self.exec_data.close[0]) > indent * 1.5:
                self.cancel(self.pending_buy)
                self.pending_buy = None
                self.log('cancel stale buy stop')
        if self.pending_sell is not None and self.pending_sell.alive():
            if float(self.exec_data.close[0]) - float(self.pending_sell.created.price) > indent * 1.5:
                self.cancel(self.pending_sell)
                self.pending_sell = None
                self.log('cancel stale sell stop')

    def _apply_trailing(self):
        if not self.position or self.position_stop is None or self.p.trailing_stop_pips <= 0:
            return
        if self.stop_cancel_pending:
            return
        trail = self.p.trailing_stop_pips * self.p.point_size
        step = self.p.trailing_step_pips * self.p.point_size
        if self.position.size > 0:
            candidate = float(self.exec_data.close[0]) - trail
            if self.active_stop_price is None or candidate - self.active_stop_price >= step:
                self.stop_cancel_pending = True
                self.stop_replace_price = candidate
                self.cancel(self.position_stop)
        else:
            candidate = float(self.exec_data.close[0]) + trail
            if self.active_stop_price is None or self.active_stop_price - candidate >= step:
                self.stop_cancel_pending = True
                self.stop_replace_price = candidate
                self.cancel(self.position_stop)

    def _set_buy_stop(self):
        price = self._round_price(float(self.exec_data.close[0]) + self.p.indent_pips * self.p.point_size)
        self.pending_buy = self.buy(size=self.p.lots, exectype=bt.Order.Stop, price=price)
        self.log(f'set buy stop {price:.2f}')

    def _set_sell_stop(self):
        price = self._round_price(float(self.exec_data.close[0]) - self.p.indent_pips * self.p.point_size)
        self.pending_sell = self.sell(size=self.p.lots, exectype=bt.Order.Stop, price=price)
        self.log(f'set sell stop {price:.2f}')

    def next(self):
        """Maintain orders and arm CCI-breakout stop entries each bar.

        Increments the bar counter, prunes far pending orders, updates the
        trailing stop, and once per new signal bar (only when flat with no
        pending order) arms a buy stop above price on an upward CCI threshold
        re-cross or a sell stop below price on a downward re-cross.
        """
        self.bar_num += 1
        self._delete_far_pending()
        self._apply_trailing()
        if len(self.signal_data) < max(int(self.p.signal_bar), 1) + 2:
            return
        if self.position or self.pending_buy is not None or self.pending_sell is not None:
            return
        if not self._new_signal_bar():
            return
        recent_ago = max(int(self.p.signal_bar), 1) - 1
        prev_ago = max(int(self.p.signal_bar), 1)
        cci_now = float(self.signal_data.cci[-recent_ago]) if recent_ago else float(self.signal_data.cci[0])
        cci_prev = float(self.signal_data.cci[-prev_ago])
        if cci_now > -self.p.cci_max_min and cci_prev < -self.p.cci_max_min:
            self._set_buy_stop()
        elif cci_now < self.p.cci_max_min and cci_prev > self.p.cci_max_min:
            self._set_sell_stop()

    def notify_order(self, order):
        """Handle pending-entry fills, protective stops, and trailing replacement.

        On a filled buy/sell stop, increments the counter, cancels the opposing
        pending order, and attaches a protective stop; on a filled or canceled
        position stop, resets stop state or re-places the trailing stop at the
        pending replacement price.

        Args:
            order: The order whose status changed.
        """
        if order.status in (order.Submitted, order.Accepted):
            return
        if self._same_order(order, self.pending_buy):
            if order.status == order.Completed:
                self.buy_count += 1
                self.pending_buy = None
                if self.pending_sell is not None and self.pending_sell.alive():
                    self.cancel(self.pending_sell)
                    self.pending_sell = None
                if self.p.stoploss_pips > 0 and order.executed.size > 0:
                    stop_price = self._round_price(order.executed.price - self.p.stoploss_pips * self.p.point_size)
                    self._place_position_stop(stop_price)
                self.active_side = 'long'
                self.log(f'buy stop filled price={order.executed.price:.2f}')
            elif order.status in (order.Canceled, order.Margin, order.Rejected, order.Expired):
                self.pending_buy = None
            return
        if self._same_order(order, self.pending_sell):
            if order.status == order.Completed:
                self.sell_count += 1
                self.pending_sell = None
                if self.pending_buy is not None and self.pending_buy.alive():
                    self.cancel(self.pending_buy)
                    self.pending_buy = None
                if self.p.stoploss_pips > 0 and order.executed.size < 0:
                    stop_price = self._round_price(order.executed.price + self.p.stoploss_pips * self.p.point_size)
                    self._place_position_stop(stop_price)
                self.active_side = 'short'
                self.log(f'sell stop filled price={order.executed.price:.2f}')
            elif order.status in (order.Canceled, order.Margin, order.Rejected, order.Expired):
                self.pending_sell = None
            return
        if self._same_order(order, self.position_stop) and order.status == order.Completed:
            self.position_stop = None
            self.active_stop_price = None
            self.active_side = None
            self.stop_cancel_pending = False
            self.stop_replace_price = None
        elif self._same_order(order, self.position_stop) and order.status in (order.Canceled, order.Margin, order.Rejected):
            self.position_stop = None
            if order.status == order.Canceled and self.stop_cancel_pending and self.stop_replace_price is not None and self.position:
                self._place_position_stop(self.stop_replace_price)
            else:
                self.stop_cancel_pending = False
                self.stop_replace_price = None

    def notify_trade(self, trade):
        """Tally closed trades and reset stop/position tracking state.

        Args:
            trade: The trade whose status changed; only closed trades update the
                win/loss counters and clear active stop state.
        """
        if not trade.isclosed:
            return
        self.trade_count += 1
        if trade.pnlcomm >= 0:
            self.win_count += 1
        else:
            self.loss_count += 1
        self.active_side = None
        self.active_stop_price = None
        self.stop_cancel_pending = False
        self.stop_replace_price = None
        if not self.position:
            self.position_stop = None
        self.log(f'trade closed pnl={trade.pnlcomm:.2f}')





BASE_DIR = Path(__file__).resolve().parent

MINUTES_PER_TRADING_YEAR = 24 * 60 * 252



def resolve_data_path(filename):
    """Resolve a strategy data filename against the current test directory.

    Args:
        filename: Relative or absolute data file path from configuration.

    Returns:
        Normalized absolute path to the data file.
    """
    path = (BASE_DIR / filename).resolve()
    if not path.exists():
        raise FileNotFoundError(f'Data file not found: {path}')
    return path


def parse_dt(value):
    """Parse an ISO8601 timestamp value from config.

    Args:
        value: Timestamp string in ISO format or falsy value.

    Returns:
        Parsed datetime object, or ``None`` if input is empty.
    """
    if not value:
        return None
    return datetime.datetime.fromisoformat(value)


def load_backtest_frame(config):
    """Build execution and signal dataframes from the given config.

    Args:
        config: Strategy configuration dictionary.

    Returns:
        Mapping with ``data`` (execution feed) and ``signal`` (H4 CCI feed).
    """
    data_cfg = config['data']
    fromdate = parse_dt(data_cfg.get('fromdate'))
    todate = parse_dt(data_cfg.get('todate'))
    df = load_mt5_csv(resolve_data_path(data_cfg['file']), fromdate=fromdate, todate=todate, bar_shift_minutes=data_cfg.get('bar_shift_minutes', 0))
    if df.empty:
        raise ValueError('Loaded data frame is empty')
    signal_df = build_signal_frame(df, indicator_minutes=data_cfg.get('signal_compression_minutes', 240), cci_period=config['params'].get('cci_period', 60))
    print(f'Loaded {len(df)} execution bars: {df.index[0]} -> {df.index[-1]}')
    return {'data': df, 'signal': signal_df}


def add_default_analyzers(cerebro):
    """Attach a standardized set of analyzers used by regression checks.

    Args:
        cerebro: The Cerebro instance to augment.
    """
    cerebro.addanalyzer(bt.analyzers.SharpeRatio, _name='sharpe', timeframe=bt.TimeFrame.Minutes, factor=MINUTES_PER_TRADING_YEAR, annualize=True, riskfreerate=0)
    cerebro.addanalyzer(bt.analyzers.Returns, _name='returns', timeframe=bt.TimeFrame.Minutes, compression=60, tann=MINUTES_PER_TRADING_YEAR)
    cerebro.addanalyzer(bt.analyzers.DrawDown, _name='drawdown')
    cerebro.addanalyzer(bt.analyzers.TradeAnalyzer, _name='trades')
    cerebro.addanalyzer(bt.analyzers.SQN, _name='sqn')


def build_cerebro(config, frame):
    """Construct and configure Cerebro with feeds, strategy, commission, and analyzers.

    Args:
        config: Config dictionary containing execution/backtest settings.
        frame: Prepared frame data produced by ``load_backtest_frame``.

    Returns:
        A configured Cerebro instance.
    """
    bt_cfg = config['backtest']
    data_cfg = config['data']
    params = dict(config.get('params', {}))
    params.pop('cci_period', None)
    cerebro = bt.Cerebro(stdstats=True)
    cerebro.broker.setcash(bt_cfg['initial_cash'])
    comm_type = bt.CommInfoBase.COMM_FIXED if bt_cfg.get('commission_type', 'fixed') == 'fixed' else bt.CommInfoBase.COMM_PERC
    cerebro.broker.setcommission(commission=bt_cfg['commission'], margin=bt_cfg['margin'], mult=bt_cfg['multiplier'], commtype=comm_type, stocklike=bt_cfg.get('stocklike', False))
    execution_feed = Mt5PandasFeed(dataname=frame['data'], timeframe=bt.TimeFrame.Minutes, compression=data_cfg.get('execution_compression_minutes', 15))
    signal_feed = SignalFeed(dataname=frame['signal'], timeframe=bt.TimeFrame.Minutes, compression=data_cfg.get('signal_compression_minutes', 240))
    cerebro.adddata(execution_feed, name=f"{data_cfg['symbol']}_{data_cfg['base_timeframe']}")
    cerebro.adddata(signal_feed, name=f"{data_cfg['symbol']}_{data_cfg['signal_timeframe']}")
    cerebro.addstrategy(ProbeStrategy, **params)
    add_default_analyzers(cerebro)
    return cerebro


def finite_or_none(value):
    """Normalize missing or non-finite metric values to ``None``.

    Args:
        value: Numeric candidate value.

    Returns:
        ``None`` when the value is missing/non-finite, else the value.
    """
    if value is None:
        return None
    if isinstance(value, (int, float)) and not math.isfinite(value):
        return None
    return value


def summarize(results, start_value):
    """Collect and print analyzer summary values for the finished run.

    Args:
        results: Backtrader run result list.
        start_value: Starting portfolio value at the beginning of the backtest.
    """
    strat = results[0]
    end_value = strat.broker.getvalue()
    drawdown = strat.analyzers.drawdown.get_analysis()
    returns = strat.analyzers.returns.get_analysis()
    sharpe = strat.analyzers.sharpe.get_analysis()
    sqn = strat.analyzers.sqn.get_analysis()
    trades = strat.analyzers.trades.get_analysis()
    total_closed = trades.get('total', {}).get('closed', 0)
    won = trades.get('won', {}).get('total', 0)
    lost = trades.get('lost', {}).get('total', 0)
    strike_rate = (won / total_closed * 100.0) if total_closed else 0.0
    print('\n# Backtest Summary')
    print(f'- Start Value: {start_value:.2f}')
    print(f'- End Value: {end_value:.2f}')
    print(f'- Net PnL: {end_value - start_value:.2f}')
    print(f'- Max Drawdown: {drawdown.get("max", {}).get("drawdown", 0.0):.2f}%')
    print(f'- Total Closed Trades: {total_closed}')
    print(f'- Won Trades: {won}')
    print(f'- Lost Trades: {lost}')
    print(f'- Strike Rate: {strike_rate:.2f}%')
    print(f'- Sharpe: {finite_or_none(sharpe.get("sharperatio"))}')
    print(f'- SQN: {finite_or_none(sqn.get("sqn"))}')
    print(f'- Total Return: {returns.get("rtot", 0.0):.6f}')


def main():
    """Run Probe backtest from CLI arguments and optional plotting."""
    parser = argparse.ArgumentParser(description='Run Probe backtest')
    parser.add_argument('--plot', action='store_true', help='Plot result chart')
    args = parser.parse_args()
    config = load_config()
    frame = load_backtest_frame(config)
    cerebro = build_cerebro(config, frame)
    start_value = cerebro.broker.getvalue()
