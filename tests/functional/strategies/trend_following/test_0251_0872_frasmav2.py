def _applied_price(data, price_type, ago=0):
    o = float(data.open[-ago])
    h = float(data.high[-ago])
    l = float(data.low[-ago])
    c = float(data.close[-ago])
    if price_type == 0:
        return c
    if price_type == 1:
        return o
    if price_type == 2:
        return h
    if price_type == 3:
        return l
    if price_type == 4:
        return (h + l) / 2.0
    if price_type == 5:
        return (h + l + c) / 3.0
    if price_type == 6:
        return (h + l + c + c) / 4.0
    return c


class ExpFRASMAv2Strategy(bt.Strategy):
    """Trade FRASMAv2 slope-color turns on H12 signals, executing on M15 bars."""

    params = dict(
        e_period=30,
        normal_speed=20,
        ipc=0,
        signal_bar=1,
        stop_loss_points=1000,
        take_profit_points=2000,
        fixed_lot=0.1,
        point=0.01,
        buy_pos_open=True,
        sell_pos_open=True,
        buy_pos_close=True,
        sell_pos_close=True,
        indicator_minutes=720,
    )

    def __init__(self):
        """Wire the base and H12 signal feeds and the FRASMAv2 indicator.

        Side effects:
            Stores the base/signal feeds, creates ``self.indicator`` on the H12
            feed, and initialises bar, signal, trade, and order-state counters.
        """
        self.base = self.datas[0]
        self.signal_data = self.datas[1]
        self.indicator = bt.indicators.FRASMAv2Indicator(self.signal_data, e_period=self.p.e_period, normal_speed=self.p.normal_speed, ipc=self.p.ipc)
        self.bar_num = 0
        self.signal_count = 0
        self.buy_count = 0
        self.sell_count = 0
        self.trade_count = 0
        self.win_count = 0
        self.loss_count = 0
        self._position_was_open = False
        self._last_signal_len = 0

    def log(self, text):
        """Print a timestamped message using the current base-feed datetime.

        Args:
            text: Message body to log.
        """
        print(f'{bt.num2date(self.base.datetime[0]).isoformat()}, {text}')

    def _check_exit_levels(self):
        if not self.position:
            return False
        cp = float(self.base.close[0])
        pv = float(self.p.point)
        sd = self.p.stop_loss_points * pv if self.p.stop_loss_points > 0 else None
        td = self.p.take_profit_points * pv if self.p.take_profit_points > 0 else None
        ep = float(self.position.price)
        if self.position.size > 0:
            if sd and cp <= ep - sd:
                self.log(f'close long SL {cp:.2f}')
                self.close()
                return True
            if td and cp >= ep + td:
                self.log(f'close long TP {cp:.2f}')
                self.close()
                return True
        else:
            if sd and cp >= ep + sd:
                self.log(f'close short SL {cp:.2f}')
                self.close()
                return True
            if td and cp <= ep - td:
                self.log(f'close short TP {cp:.2f}')
                self.close()
                return True
        return False

    def _val(self, line, offset):
        v = float(line[-offset]) if offset else float(line[0])
        return 0.0 if math.isnan(v) else v

    def next(self):
        """Advance one M15 bar: apply risk exits, then act on H12 color turns.

        Checks fixed stop-loss / take-profit exits first, then, once per new H12
        signal bar, reads the FRASMAv2 ``color`` at the ``signal_bar`` offset to
        open or close longs and shorts.
        """
        self.bar_num += 1
        if len(self.base) < 2:
            return
        if self._check_exit_levels():
            return
        signal_bar = max(int(self.p.signal_bar) - 1, 0)
        min_needed = max(int(self.p.e_period), int(self.p.normal_speed)) + signal_bar + 4
        if len(self.signal_data) < min_needed:
            return
        current_signal_len = len(self.signal_data)
        if current_signal_len == self._last_signal_len:
            return
        self._last_signal_len = current_signal_len

        c0 = self._val(self.indicator.color, signal_bar)
        c1 = self._val(self.indicator.color, signal_bar + 1)
        buy_open = False
        sell_open = False
        buy_close = False
        sell_close = False
        if c1 == 0.0:
            if self.p.buy_pos_open and c0 > 0.0:
                buy_open = True
            if self.p.sell_pos_close:
                sell_close = True
        if c1 == 2.0:
            if self.p.sell_pos_open and c0 < 2.0:
                sell_open = True
            if self.p.buy_pos_close:
                buy_close = True
        cp = float(self.base.close[0])
        sz = float(self.p.fixed_lot)
        if sz <= 0:
            return
        if sell_close and self.position.size < 0:
            self.log(f'close short signal {cp:.2f}')
            self.close()
        if buy_close and self.position.size > 0:
            self.log(f'close long signal {cp:.2f}')
            self.close()
        if buy_open:
            self.signal_count += 1
            self.log(f'buy signal {cp:.2f}')
            if self.position.size <= 0:
                self.buy(size=sz)
        if sell_open:
            self.signal_count += 1
            self.log(f'sell signal {cp:.2f}')
            if self.position.size >= 0:
                self.sell(size=sz)

    def notify_trade(self, trade):
        """Track opened-side counts and tally closed trades into win/loss counters.

        Args:
            trade: The backtrader trade whose state changed; opening increments
                buy/sell counts and closing updates the win/loss tallies.
        """
        if trade.isopen and not self._position_was_open:
            if trade.size > 0:
                self.buy_count += 1
            elif trade.size < 0:
                self.sell_count += 1
            self._position_was_open = True
            return
        if not trade.isclosed:
            return
        self.trade_count += 1
        if trade.pnlcomm >= 0:
            self.win_count += 1
        else:
            self.loss_count += 1
        self._position_was_open = False
        self.log(f'trade closed pnl={trade.pnlcomm:.2f}')


