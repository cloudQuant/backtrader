def parse_dt(value):
    """Parse ISO datetime strings to ``datetime.datetime``.

    Args:
        value: ISO datetime string or ``None``.

    Returns:
        datetime.datetime | None: Parsed datetime or ``None``.
    """
    if not value:
        return None
    return datetime.datetime.fromisoformat(value)


