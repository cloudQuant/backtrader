class CandleTrendStrategy(bt.Strategy):
    """Trade runs of same-color candles with fixed point-based stop/take-profit."""

    params = dict(
        candle_trend_total=3,
        signal_bar=1,
        stop_loss_points=1000,
        take_profit_points=2000,
        mm=0.1,
        point=0.01,
        buy_pos_open=True,
        sell_pos_open=True,
        buy_pos_close=True,
        sell_pos_close=True,
    )

    def __init__(self):
        """Zero the trade/signal counters and per-bar de-duplication state."""
        self.data0 = self.datas[0]
        self.bar_num = 0
        self.signal_count = 0
        self.buy_count = 0
        self.sell_count = 0
        self.trade_count = 0
        self.win_count = 0
        self.loss_count = 0
        self._position_was_open = False
        self._last_processed_dt = None

    def log(self, text):
        """Print ``text`` prefixed with the current bar's ISO timestamp."""
        dt = bt.num2date(self.data0.datetime[0])
        print(f'{dt.isoformat()}, {text}')

    def _position_size(self, price):
        if self.p.mm < 0:
            return abs(float(self.p.mm))
        if price <= 0:
            return 0.0
        cash = self.broker.getcash()
        return round((cash * float(self.p.mm)) / price, 4)

    def _check_exit_levels(self):
        if not self.position:
            return False
        close_price = float(self.data0.close[0])
        point_value = float(self.p.point)
        stop_distance = self.p.stop_loss_points * point_value if self.p.stop_loss_points > 0 else None
        take_distance = self.p.take_profit_points * point_value if self.p.take_profit_points > 0 else None
        entry_price = float(self.position.price)

        if self.position.size > 0:
            if stop_distance is not None and close_price <= entry_price - stop_distance:
                self.log(f'close long by stop loss close={close_price:.2f} entry={entry_price:.2f}')
                self.close()
                return True
            if take_distance is not None and close_price >= entry_price + take_distance:
                self.log(f'close long by take profit close={close_price:.2f} entry={entry_price:.2f}')
                self.close()
                return True
        elif self.position.size < 0:
            if stop_distance is not None and close_price >= entry_price + stop_distance:
                self.log(f'close short by stop loss close={close_price:.2f} entry={entry_price:.2f}')
                self.close()
                return True
            if take_distance is not None and close_price <= entry_price - take_distance:
                self.log(f'close short by take profit close={close_price:.2f} entry={entry_price:.2f}')
                self.close()
                return True
        return False

    def next(self):
        """Process each bar once, check exits, then act on same-color candle runs."""
        self.bar_num += 1
        if len(self.data0) <= self.p.candle_trend_total:
            return

        current_dt = bt.num2date(self.data0.datetime[0])
        if self._last_processed_dt == current_dt:
            return
        self._last_processed_dt = current_dt

        if self._check_exit_levels():
            return

        signal_bar = max(int(self.p.signal_bar), 1)
        total = int(self.p.candle_trend_total)
        if len(self.data0) < signal_bar + total:
            return

        count = 0
        for i in range(signal_bar - 1, signal_bar - 1 + total):
            open_value = float(self.data0.open[-i]) if i else float(self.data0.open[0])
            close_value = float(self.data0.close[-i]) if i else float(self.data0.close[0])
            if open_value < close_value:
                count += 1
            elif open_value > close_value:
                count -= 1

        buy_signal = count == total
        sell_signal = count == -total
        if not buy_signal and not sell_signal:
            return

        close_price = float(self.data0.close[0])
        size = self._position_size(close_price)
        if size <= 0:
            return

        if buy_signal:
            self.signal_count += 1
            self.log(f'buy signal close={close_price:.2f} count={count}')
            if self.position.size < 0 and self.p.sell_pos_close:
                self.close()
            if self.position.size <= 0 and self.p.buy_pos_open:
                self.buy(size=size)
            return

        if sell_signal:
            self.signal_count += 1
            self.log(f'sell signal close={close_price:.2f} count={count}')
            if self.position.size > 0 and self.p.buy_pos_close:
                self.close()
            if self.position.size >= 0 and self.p.sell_pos_open:
                self.sell(size=size)

    def notify_trade(self, trade):
        """Track buy/sell on open and win/loss on close for each trade.

        Args:
            trade: The trade whose status changed; the first open bumps the
                buy or sell counter and a close bumps the trade and win/loss
                counters by sign of PnL.
        """
        if trade.isopen and not self._position_was_open:
            if trade.size > 0:
                self.buy_count += 1
            elif trade.size < 0:
                self.sell_count += 1
            self._position_was_open = True
            return
        if not trade.isclosed:
            return
        self.trade_count += 1
        if trade.pnlcomm >= 0:
            self.win_count += 1
        else:
            self.loss_count += 1
        self._position_was_open = False
        self.log(f'trade closed pnl={trade.pnlcomm:.2f}')


