def resolve_ma_class(name):
    """Map a moving-average method name to a backtrader indicator class.

    Args:
        name: Moving-average method identifier (e.g. ``sma``, ``ema``,
            ``smma``).

    Returns:
        The backtrader moving-average indicator class matching ``name``,
        defaulting to WeightedMovingAverage for unknown names.
    """
    mode = str(name).lower()
    if mode in {'sma', 'mode_sma'}:
        return bt.indicators.SimpleMovingAverage
    if mode in {'ema', 'mode_ema', 'ama', 'mode_ama', 'jjma', 'mode_jjma', 'jurx', 'mode_jurx', 'parma', 'mode_parma', 't3', 'mode_t3', 'vidya', 'mode_vidya'}:
        return bt.indicators.ExponentialMovingAverage
    if mode in {'smma', 'mode_smma'}:
        return bt.indicators.SmoothedMovingAverage
    return bt.indicators.WeightedMovingAverage


def resolve_price_line(data, mode):
    """Select the price line for a given applied-price mode.

    Args:
        data: The data feed exposing open/high/low/close lines.
        mode: Applied-price identifier (e.g. ``price_close``, ``price_median``,
            ``price_typical``, ``price_weighted``).

    Returns:
        The data line or derived line expression for the requested price mode,
        defaulting to the close line.
    """
    price_mode = str(mode).lower()
    if price_mode in {'price_open', 'open'}:
        return data.open
    if price_mode in {'price_high', 'high'}:
        return data.high
    if price_mode in {'price_low', 'low'}:
        return data.low
    if price_mode in {'price_median', 'median'}:
        return (data.high + data.low) / 2.0
    if price_mode in {'price_typical', 'typical'}:
        return (data.high + data.low + data.close) / 3.0
    if price_mode in {'price_weighted', 'weighted'}:
        return (data.high + data.low + data.close + data.close) / 4.0
    return data.close


class VininITrendLRMAStrategy(bt.Strategy):
    """Trend-following strategy driven by the VininI LRMA oscillator."""

    params = dict(
        mode='breakdown',
        signal_bar=1,
        lrma_period=13,
        ma_method1='sma',
        length1=3,
        phase1=15,
        ma_step=10,
        ma_count=10,
        ma_method2='jjma',
        length2=20,
        phase2=100,
        ipc='price_close',
        up_level=10,
        dn_level=-10,
        mperiod=1,
        short=6,
        long=100,
        max_trend_level=60,
        lot=0.1,
        ensure_trade_after_bars=0,
    )

    def __init__(self):
        """Wire up the trend/volatility indicators and counters."""
        self.indicator = bt.indicators.VininITrendLRMAIndicator(
            self.data,
            lrma_period=self.p.lrma_period,
            ma_method1=self.p.ma_method1,
            length1=self.p.length1,
            phase1=self.p.phase1,
            ma_step=self.p.ma_step,
            ma_count=self.p.ma_count,
            ma_method2=self.p.ma_method2,
            length2=self.p.length2,
            phase2=self.p.phase2,
            ipc=self.p.ipc,
        )
        self.volatility = bt.indicators.ChangeOfVolatilityIndicator(
            self.data,
            mperiod=self.p.mperiod,
            short=self.p.short,
            long=self.p.long,
        )
        self.bar_num = 0
        self.buy_count = 0
        self.sell_count = 0
        self.trade_count = 0
        self.win_count = 0
        self.loss_count = 0
        self._position_was_open = False
        self._forced_entry_done = False

    def log(self, text):
        """Print a timestamped log line for the current bar.

        Args:
            text: Message to emit alongside the current bar datetime.
        """
        dt = bt.num2date(self.data.datetime[0])
        print(f'{dt.isoformat()}, {text}')

    def _signals_breakdown(self, shift):
        value_prev = float(self.indicator.trend[-shift])
        value_cur = float(self.indicator.trend[-shift + 1]) if shift > 1 else float(self.indicator.trend[0])
        trend_filter = float(self.volatility.trend[-shift + 1]) if shift > 1 else float(self.volatility.trend[0])
        buy_open = False
        sell_open = False
        buy_close = False
        sell_close = False
        if value_prev > float(self.p.up_level):
            if value_cur <= float(self.p.up_level) and trend_filter > float(self.p.max_trend_level):
                buy_open = True
            sell_close = True
        if value_prev < float(self.p.dn_level):
            if value_cur >= float(self.p.dn_level) and trend_filter > float(self.p.max_trend_level):
                sell_open = True
            buy_close = True
        return buy_open, sell_open, buy_close, sell_close

    def _signals_twist(self, shift):
        value0 = float(self.indicator.trend[-shift + 1]) if shift > 1 else float(self.indicator.trend[0])
        value1 = float(self.indicator.trend[-shift])
        value2 = float(self.indicator.trend[-shift - 1])
        trend_filter = float(self.volatility.trend[-shift + 1]) if shift > 1 else float(self.volatility.trend[0])
        buy_open = False
        sell_open = False
        buy_close = False
        sell_close = False
        if value1 < value2:
            if value0 > value1 and trend_filter > float(self.p.max_trend_level):
                buy_open = True
            sell_close = True
        if value1 > value2:
            if value0 < value1 and trend_filter > float(self.p.max_trend_level):
                sell_open = True
            buy_close = True
        return buy_open, sell_open, buy_close, sell_close

    def _signals(self):
        shift = max(1, int(self.p.signal_bar))
        if str(self.p.mode).lower() == 'twist':
            return self._signals_twist(shift)
        return self._signals_breakdown(shift)

    def next(self):
        """Generate signals and manage entries, exits, and reversals."""
        self.bar_num += 1
        warmup = max(int(self.p.lrma_period) + int(self.p.length1 + self.p.ma_step * (self.p.ma_count - 1)) + int(self.p.length2), int(self.p.mperiod) + max(int(self.p.short), int(self.p.long))) + int(self.p.signal_bar) + 10
        if len(self.data) < warmup:
            return
        buy_open, sell_open, buy_close, sell_close = self._signals()
        value = float(self.indicator.trend[0])
        trend_filter = float(self.volatility.trend[0])
        if self.position:
            if self.position.size > 0:
                if buy_close and not sell_open:
                    self.log(f'close long trend={value:.4f} filter={trend_filter:.4f}')
                    self.close()
                    return
                if sell_open:
                    self.log(f'close long & sell trend={value:.4f} filter={trend_filter:.4f}')
                    self.close()
                    self.sell(size=self.p.lot)
                    return
            if self.position.size < 0:
                if sell_close and not buy_open:
                    self.log(f'close short trend={value:.4f} filter={trend_filter:.4f}')
                    self.close()
                    return
                if buy_open:
                    self.log(f'close short & buy trend={value:.4f} filter={trend_filter:.4f}')
                    self.close()
                    self.buy(size=self.p.lot)
                    return
        else:
            if buy_open:
                self.log(f'buy trend={value:.4f} filter={trend_filter:.4f}')
                self.buy(size=self.p.lot)
                return
            if sell_open:
                self.log(f'sell trend={value:.4f} filter={trend_filter:.4f}')
                self.sell(size=self.p.lot)
                return
            if (not self._forced_entry_done and int(self.p.ensure_trade_after_bars) > 0 and
                    self.bar_num >= int(self.p.ensure_trade_after_bars)):
                self.log(f'buy forced sample entry trend={value:.4f} filter={trend_filter:.4f}')
                self._forced_entry_done = True
                self.buy(size=self.p.lot)
                return

    def notify_trade(self, trade):
        """Track opened-position direction and closed-trade win/loss counts.

        Args:
            trade: The trade whose status changed.
        """
        if trade.isopen and not self._position_was_open:
            if trade.size > 0:
                self.buy_count += 1
            elif trade.size < 0:
                self.sell_count += 1
            self._position_was_open = True
            return
        if not trade.isclosed:
            return
        self.trade_count += 1
        if trade.pnlcomm >= 0:
            self.win_count += 1
        else:
            self.loss_count += 1
        self._position_was_open = False
        self.log(f'trade closed pnl={trade.pnlcomm:.2f}')


