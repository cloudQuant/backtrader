class DigitalMacdStrategy(Strategy):
    """Trade mode-selectable Digital MACD signals with fixed stop/target exits."""

    params = dict(
        mode='MACDtwist',
        signal_bar=1,
        signal_period=5,
        stop_loss_points=1000,
        take_profit_points=2000,
        lot=0.1,
        point=0.01,
        price_digits=2,
    )

    def __init__(self):
        """Wire the Digital MACD indicator and reset counters and risk state.

        Side effects:
            Creates ``self.indicator`` and initialises bar, trade, signal, and
            order counters, entry/stop/target prices, the pending entry
            direction, and the warmup window.
        """
        self.indicator = bt.indicators.DigitalMacd(
            self.data,
            signal_period=self.p.signal_period,
            point=self.p.point,
        )
        self.bar_num = 0
        self.buy_count = 0
        self.sell_count = 0
        self.trade_count = 0
        self.win_count = 0
        self.loss_count = 0
        self.buy_signal_count = 0
        self.sell_signal_count = 0
        self.completed_order_count = 0
        self.rejected_order_count = 0
        self.entry_price = None
        self.stop_price = None
        self.target_price = None
        self.pending_entry_direction = 0
        self.warmup = max(len(bt.indicators.DigitalMacd.FAST_COEFFS), len(bt.indicators.DigitalMacd.SLOW_COEFFS)) + max(int(self.p.signal_period), 1) + 5

    def log(self, text):
        """Print a timestamped message using the current bar datetime.

        Args:
            text: Message body to log.
        """
        dt = num2date(self.data.datetime[0])
        print(f'{dt.isoformat()}, {text}')

    def _signal_indexes(self):
        current = -max(int(self.p.signal_bar), 1)
        previous = current - 1
        older = current - 2
        return current, previous, older

    def _breakdown_signals(self):
        current, previous, _ = self._signal_indexes()
        macd_now = float(self.indicator.macd[current])
        macd_prev = float(self.indicator.macd[previous])
        return macd_prev <= 0.0 and macd_now > 0.0, macd_prev >= 0.0 and macd_now < 0.0

    def _macd_twist_signals(self):
        current, previous, older = self._signal_indexes()
        macd_now = float(self.indicator.macd[current])
        macd_prev = float(self.indicator.macd[previous])
        macd_older = float(self.indicator.macd[older])
        return macd_prev < macd_older and macd_now > macd_prev, macd_prev > macd_older and macd_now < macd_prev

    def _signal_twist_signals(self):
        current, previous, older = self._signal_indexes()
        signal_now = float(self.indicator.signal[current])
        signal_prev = float(self.indicator.signal[previous])
        signal_older = float(self.indicator.signal[older])
        return signal_prev < signal_older and signal_now > signal_prev, signal_prev > signal_older and signal_now < signal_prev

    def _macd_disposition_signals(self):
        current, previous, _ = self._signal_indexes()
        macd_now = float(self.indicator.macd[current])
        macd_prev = float(self.indicator.macd[previous])
        signal_now = float(self.indicator.signal[current])
        signal_prev = float(self.indicator.signal[previous])
        return macd_prev <= signal_prev and macd_now > signal_now, macd_prev >= signal_prev and macd_now < signal_now

    def _get_signals(self):
        mode = str(self.p.mode)
        if mode == 'breakdown':
            return self._breakdown_signals()
        if mode == 'SIGNALtwist':
            return self._signal_twist_signals()
        if mode == 'MACDdisposition':
            return self._macd_disposition_signals()
        return self._macd_twist_signals()

    def _reset_levels(self):
        self.entry_price = None
        self.stop_price = None
        self.target_price = None

    def _open_long(self):
        self.pending_entry_direction = 1
        self.buy(size=self.p.lot)

    def _open_short(self):
        self.pending_entry_direction = -1
        self.sell(size=self.p.lot)

    def _close_position(self, reason):
        self.log(reason)
        self.close()
        self._reset_levels()

    def _manage_protective_levels(self):
        if not self.position or self.entry_price is None:
            return False

        low = float(self.data.low[0])
        high = float(self.data.high[0])

        if self.position.size > 0:
            if self.stop_price is not None and low <= self.stop_price:
                self._close_position(f'close long stop={self.stop_price:.2f}')
                return True
            if self.target_price is not None and high >= self.target_price:
                self._close_position(f'close long target={self.target_price:.2f}')
                return True
        else:
            if self.stop_price is not None and high >= self.stop_price:
                self._close_position(f'close short stop={self.stop_price:.2f}')
                return True
            if self.target_price is not None and low <= self.target_price:
                self._close_position(f'close short target={self.target_price:.2f}')
                return True

        return False

    def next(self):
        """Advance one bar: enforce protective exits, then act on mode signals.

        Skips during warmup, applies stop-loss / take-profit exits, computes the
        mode-specific buy/sell signals, and opens, closes, or reverses the
        position accordingly while updating signal counters.
        """
        self.bar_num += 1
        if len(self.data) < self.warmup + max(int(self.p.signal_bar), 1) + 2:
            return

        if self._manage_protective_levels():
            return

        buy_signal, sell_signal = self._get_signals()
        macd_now = float(self.indicator.macd[0])
        signal_now = float(self.indicator.signal[0])

        if buy_signal:
            self.buy_signal_count += 1
        if sell_signal:
            self.sell_signal_count += 1

        if self.position:
            if self.position.size > 0 and sell_signal:
                self.log(f'close long & sell macd={macd_now:.4f} signal={signal_now:.4f}')
                self.close()
                self._reset_levels()
                self._open_short()
                return
            if self.position.size < 0 and buy_signal:
                self.log(f'close short & buy macd={macd_now:.4f} signal={signal_now:.4f}')
                self.close()
                self._reset_levels()
                self._open_long()
                return
        else:
            if buy_signal:
                self.log(f'buy macd={macd_now:.4f} signal={signal_now:.4f}')
                self._open_long()
                return
            if sell_signal:
                self.log(f'sell macd={macd_now:.4f} signal={signal_now:.4f}')
                self._open_short()
                return

    def notify_order(self, order):
        """Count orders and set entry/stop/target levels on completed fills.

        Args:
            order: The backtrader order whose status changed.
        """
        if order.status in [order.Canceled, order.Margin, order.Rejected]:
            self.rejected_order_count += 1
            self.pending_entry_direction = 0
            self.log(f'order {order.getstatusname()}')
            return

        if order.status != order.Completed:
            return

        self.completed_order_count += 1

        if self.pending_entry_direction == 1 and order.isbuy():
            self.buy_count += 1
            self.entry_price = order.executed.price
            self.stop_price = self.entry_price - self.p.stop_loss_points * self.p.point if self.p.stop_loss_points > 0 else None
            self.target_price = self.entry_price + self.p.take_profit_points * self.p.point if self.p.take_profit_points > 0 else None
            self.pending_entry_direction = 0
            return

        if self.pending_entry_direction == -1 and order.issell():
            self.sell_count += 1
            self.entry_price = order.executed.price
            self.stop_price = self.entry_price + self.p.stop_loss_points * self.p.point if self.p.stop_loss_points > 0 else None
            self.target_price = self.entry_price - self.p.take_profit_points * self.p.point if self.p.take_profit_points > 0 else None
            self.pending_entry_direction = 0
            return

        if not self.position:
            self._reset_levels()

    def notify_trade(self, trade):
        """Tally closed trades into win/loss counters.

        Args:
            trade: The backtrader trade whose state changed; only closed trades
                update the counters.
        """
        if not trade.isclosed:
            return
        self.trade_count += 1
        if trade.pnlcomm >= 0:
            self.win_count += 1
        else:
            self.loss_count += 1
        self.log(f'trade closed pnl={trade.pnlcomm:.2f}')


MINUTES_PER_TRADING_YEAR = 252 * 24 * 60


def prepare_frame(config, base_dir):
    """Load and date-clip the M15 execution frame from config.

    Args:
        config: Resolved configuration dict with a ``data`` section.
        base_dir: Directory used to resolve the relative data file path.

    Returns:
        dict: Mapping with ``data``, the ``fromdate``/``todate`` bounds, and the
        resolved ``filepath`` string.
    """
    data_cfg = config['data']
    fromdate = pd.Timestamp(data_cfg['fromdate']) if data_cfg.get('fromdate') else None
    todate = pd.Timestamp(data_cfg['todate']) if data_cfg.get('todate') else None
    filepath = (base_dir / data_cfg['file']).resolve()
    data = load_mt5_csv(
        filepath,
        fromdate=fromdate,
        todate=todate,
        bar_shift_minutes=int(data_cfg.get('bar_shift_minutes', 0)),
    )
    return {'data': data, 'fromdate': fromdate, 'todate': todate, 'filepath': str(filepath)}


def build_cerebro(config, frame):
    """Assemble a Cerebro engine with the M15 feed, strategy, and analyzers.

    Args:
        config: Resolved configuration dict with ``backtest``, ``data`` and
            ``params`` sections.
        frame: Frame mapping returned by ``prepare_frame``.

    Returns:
        Cerebro: Configured engine with the M15 feed, ``DigitalMacdStrategy``,
        and Sharpe/Returns/DrawDown/TradeAnalyzer/SQN analyzers attached.
    """
    bt_cfg = config['backtest']
    cerebro = Cerebro(stdstats=True)
    cerebro.broker.setcash(bt_cfg['initial_cash'])
    comm_type = CommInfoBase.COMM_FIXED if bt_cfg.get('commission_type', 'fixed') == 'fixed' else CommInfoBase.COMM_PERC
    cerebro.broker.setcommission(
        commission=bt_cfg['commission'],
        margin=bt_cfg['margin'],
        mult=bt_cfg['multiplier'],
        commtype=comm_type,
        stocklike=bt_cfg.get('stocklike', False),
    )
    feed = Mt5PandasFeed(dataname=frame['data'], timeframe=TimeFrame.Minutes, compression=15)
    cerebro.adddata(feed, name=f"{config['data']['symbol']}_{config['data']['timeframe']}")
    cerebro.addstrategy(DigitalMacdStrategy, **config.get('params', {}))
    cerebro.addanalyzer(
        btanalyzers.SharpeRatio,
        _name='sharpe',
        timeframe=TimeFrame.Minutes,
        factor=MINUTES_PER_TRADING_YEAR,
        annualize=True,
        riskfreerate=0,
    )
    cerebro.addanalyzer(
        btanalyzers.Returns,
        _name='returns',
        timeframe=TimeFrame.Minutes,
        compression=15,
        tann=MINUTES_PER_TRADING_YEAR,
    )
    cerebro.addanalyzer(btanalyzers.DrawDown, _name='drawdown')
    cerebro.addanalyzer(btanalyzers.TradeAnalyzer, _name='trades')
    cerebro.addanalyzer(btanalyzers.SQN, _name='sqn')
    return cerebro


def _get_nested(mapping, *keys, default=0):
    current = mapping
    for key in keys:
        if current is None:
            return default
        if hasattr(current, key):
            current = getattr(current, key)
        elif isinstance(current, dict) and key in current:
            current = current[key]
        else:
            return default
    return current


def calculate_ulcer_index(equity_curve):
    """Compute the Ulcer Index (RMS percentage drawdown) of an equity curve.

    Args:
        equity_curve: Sequence of portfolio values in chronological order.

    Returns:
        float: The Ulcer Index, or 0.0 for an empty curve.
    """
    if not equity_curve:
        return 0.0
    peak = None
    squares = []
    for value in equity_curve:
        peak = value if peak is None else max(peak, value)
        if peak and peak != 0:
            dd = (value / peak - 1.0) * 100.0
            squares.append(dd * dd)
    return (sum(squares) / len(squares)) ** 0.5 if squares else 0.0


def extract_metrics(strategy, cerebro):
    """Collect strategy counters and analyzer results into a metrics dict.

    Args:
        strategy: The executed strategy instance holding trade/order counters.
        cerebro: The Cerebro engine used to read starting cash and final value.

    Returns:
        dict: Performance and activity metrics (bar/trade counts, win/loss split,
        returns, Sharpe, drawdown, SQN, profit factor, Ulcer index) compared by
        the regression assertions.
    """
    sharpe = strategy.analyzers.sharpe.get_analysis()
    returns = strategy.analyzers.returns.get_analysis()
    drawdown = strategy.analyzers.drawdown.get_analysis()
    trades = strategy.analyzers.trades.get_analysis()
    sqn = strategy.analyzers.sqn.get_analysis()

    won = int(_get_nested(trades, 'won', 'total', default=0))
    lost = int(_get_nested(trades, 'lost', 'total', default=0))
    total_trades = int(_get_nested(trades, 'total', 'closed', default=0))
    gross_won = _get_nested(trades, 'won', 'pnl', 'total', default=0) or 0
    gross_lost = abs(_get_nested(trades, 'lost', 'pnl', 'total', default=0) or 0)
    initial_cash = float(cerebro.broker.startingcash)
    final_value = float(cerebro.broker.getvalue())

    return {
        'bars': int(getattr(strategy, 'bar_num', 0)),
        'bar_num': int(getattr(strategy, 'bar_num', 0)),
        'buy_count': int(getattr(strategy, 'buy_count', 0)),
        'sell_count': int(getattr(strategy, 'sell_count', 0)),
        'completed_orders': int(getattr(strategy, 'completed_order_count', 0)),
        'rejected_orders': int(getattr(strategy, 'rejected_order_count', 0)),
        'total_trades': total_trades,
        'won': won,
        'lost': lost,
        'win_rate': (won / total_trades * 100.0) if total_trades else 0.0,
        'profit_factor': (gross_won / gross_lost) if gross_lost else None,
        'trade_count': total_trades,
        'initial_cash': initial_cash,
        'final_value': round(final_value, 2),
        'net_pnl': round(final_value - initial_cash, 2),
        'total_return_pct': (final_value / initial_cash - 1.0) * 100.0 if initial_cash else 0.0,
        'annual_return_pct': float(returns.get('rnorm', 0.0) or 0.0) * 100.0,
        'max_drawdown_pct': round(float(_get_nested(drawdown, 'max', 'drawdown', default=0.0)), 4),
        'sharpe_ratio': None if sharpe.get('sharperatio') is None else round(float(sharpe.get('sharperatio')), 6),
        'sqn': None if sqn.get('sqn') is None else round(float(sqn.get('sqn')), 6),
        'ulcer_index': calculate_ulcer_index([initial_cash, final_value]),
    }


def main():
    """Parse CLI args, run the Digital MACD backtest, and emit JSON results.

    Side effects:
        Reads command-line arguments, executes the backtest, writes
        ``backtest_result.json``, prints a metrics payload, and optionally renders
        the chart.
    """
    parser = argparse.ArgumentParser(description='Run the 1104 Digital MACD Backtrader example')
    parser.add_argument('--config', default='config.yaml', help='Path to config.yaml')
    parser.add_argument('--plot', action='store_true', help='Plot after backtest')
    args = parser.parse_args()

    base_dir = Path(__file__).resolve().parent
    config_path = (base_dir / args.config).resolve()
    config = _bt_load_config(_CONFIG, repo=_REPO)
    frame = prepare_frame(config, base_dir)
    cerebro = build_cerebro(config, frame)
    results = cerebro.run()
    strategy = results[0]
    metrics = extract_metrics(strategy, cerebro)

    result_payload = {
        'strategy': config['strategy']['name'],
        'source_ea': config['strategy']['source_ea'],
        'bars': metrics.get('bars', 0),
        'bar_num': metrics.get('bar_num', 0),
        'buy_count': metrics.get('buy_count', 0),
        'sell_count': metrics.get('sell_count', 0),
        'total_trades': metrics.get('total_trades', 0),
        'won': metrics.get('won', 0),
        'lost': metrics.get('lost', 0),
        'win_rate': metrics.get('win_rate', 0),
        'profit_factor': metrics.get('profit_factor'),
        'initial_cash': float(cerebro.broker.startingcash),
        'final_value': metrics.get('final_value', 0),
        'net_pnl': metrics.get('net_pnl', 0),
        'total_return_pct': metrics.get('total_return_pct', 0),
        'annual_return_pct': metrics.get('annual_return_pct', 0),
        'sharpe_ratio': metrics.get('sharpe_ratio'),
        'max_drawdown': metrics.get('max_drawdown_pct', 0),
        'sqn': metrics.get('sqn'),
        'ulcer_index': metrics.get('ulcer_index', 0),
    }

    with open(base_dir / 'backtest_result.json', 'w', encoding='utf-8') as handle:
        json.dump(result_payload, handle, ensure_ascii=False, indent=2)

    print(json.dumps({
        'strategy': config['strategy']['name'],
        'source_ea': config['strategy']['source_ea'],
        'data_file': frame['filepath'],
        'fromdate': None if frame['fromdate'] is None else str(frame['fromdate']),
        'todate': None if frame['todate'] is None else str(frame['todate']),
        'params': config.get('params', {}),
        'metrics': metrics,
    }, ensure_ascii=False, indent=2))

    if args.plot:
        cerebro.plot(style='candlestick')


def _close(actual, expected, *, tol, key):
    """Assert ``actual`` is finite and within ``tol`` of ``expected``."""
    assert actual is not None, f"{key}: expected={expected}, got=None"
    a = float(actual)
    assert math.isfinite(a), f"{key}: expected={expected}, got non-finite {actual}"
    assert abs(a - float(expected)) <= tol, (
        f"{key}: expected={expected}, got={a} (tol={tol})"
    )


