class F2aAOIndicator(bt.Indicator):
    """F2a_AO arrow indicator from a fast/slow/filter EMA system.

    Builds fast, slow and filter EMAs of a weighted price and emits a ``buy``
    arrow below the bar when the fast-minus-slow spread turns up with filter
    confirmation, or a ``sell`` arrow above the bar on the bearish turn, using an
    internal latch so arrows alternate direction.
    """

    lines = ('sell', 'buy')
    params = dict(ma_filtr=3, ma_fast=13, ma_slow=144)

    def __init__(self):
        """Build the fast/slow/filter EMAs and set the minimum period."""
        series = (self.data.close * 5.0 + self.data.open * 2.0 + self.data.high + self.data.low) / 9.0
        self._fast = bt.indicators.ExponentialMovingAverage(series, period=max(1, int(self.p.ma_fast)))
        self._slow = bt.indicators.ExponentialMovingAverage(series, period=max(1, int(self.p.ma_slow)))
        self._filter = bt.indicators.ExponentialMovingAverage(series, period=max(1, int(self.p.ma_filtr)))
        self.addminperiod(max(int(self.p.ma_slow), int(self.p.ma_fast), int(self.p.ma_filtr)) + 20)
        self._trend = 0

    def next(self):
        """Compute buy/sell arrows for the current bar (event-driven mode).

        Detects a confirmed turn in the fast-minus-slow spread and plots a buy
        arrow below the low or a sell arrow above the high, offset by half the
        recent average range, toggling the internal trend latch.
        """
        value1_0 = float(self._fast[0]) - float(self._slow[0])
        value1_1 = float(self._fast[-1]) - float(self._slow[-1])
        value1_2 = float(self._fast[-2]) - float(self._slow[-2])
        current = float(self._filter[0])
        prev = float(self._filter[-1])
        avg_range = 0.0
        for count in range(10):
            avg_range += abs(float(self.data.high[-count]) - float(self.data.low[-count]))
        range_value = avg_range / 10.0
        self.lines.buy[0] = 0.0
        self.lines.sell[0] = 0.0
        if self._trend <= 0:
            if value1_0 > value1_1 and current >= prev and value1_1 <= value1_2:
                self.lines.buy[0] = float(self.data.low[0]) - range_value * 0.5
                self._trend = 1
        if self._trend >= 0:
            if value1_0 < value1_1 and current <= prev and value1_1 >= value1_2:
                self.lines.sell[0] = float(self.data.high[0]) + range_value * 0.5
                self._trend = -1

    def once(self, start, end):
        """Compute buy/sell arrows over a range of bars (vectorized mode).

        Vectorized equivalent of ``next`` used under ``runonce``: iterates the
        arrays from ``start`` to ``end``, detecting confirmed spread turns and
        writing buy/sell arrow values while maintaining the trend latch.

        Args:
            start: First bar index to process.
            end: Stop index (exclusive) for processing.
        """
        fast = self._fast.array
        slow = self._slow.array
        filtr = self._filter.array
        high = self.data.high.array
        low = self.data.low.array
        buy = self.lines.buy.array
        sell = self.lines.sell.array
        trend = 0
        for i in range(start, end):
            buy[i] = 0.0
            sell[i] = 0.0
            if i < 12:
                continue
            value1_0 = float(fast[i]) - float(slow[i])
            value1_1 = float(fast[i - 1]) - float(slow[i - 1])
            value1_2 = float(fast[i - 2]) - float(slow[i - 2])
            current = float(filtr[i])
            prev = float(filtr[i - 1])
            if not all(math.isfinite(v) for v in (value1_0, value1_1, value1_2, current, prev)):
                continue
            avg_range = 0.0
            valid_ranges = 0
            for count in range(10):
                idx = i - count
                bar_range = abs(float(high[idx]) - float(low[idx]))
                if math.isfinite(bar_range):
                    avg_range += bar_range
                    valid_ranges += 1
            range_value = avg_range / valid_ranges if valid_ranges else 0.0
            if trend <= 0 and value1_0 > value1_1 and current >= prev and value1_1 <= value1_2:
                buy[i] = float(low[i]) - range_value * 0.5
                trend = 1
            if trend >= 0 and value1_0 < value1_1 and current <= prev and value1_1 >= value1_2:
                sell[i] = float(high[i]) + range_value * 0.5
                trend = -1
        self._trend = trend


class F2aAOStrategy(bt.Strategy):
    """Port of the MT5 ``Exp_F2a_AO`` arrow strategy with a trend filter.

    Trades the F2aAOIndicator buy/sell arrows, gated by a simple
    candle-direction trend filter, entering in the arrow direction and reversing
    on the opposite arrow with a fixed lot.

    Args:
        inp_timeframe: Source-EA timeframe label retained for provenance.
        trend_bar: Bar offset used by the candle-direction trend filter.
        ma_filtr: Filter EMA period passed to the indicator.
        ma_fast: Fast EMA period passed to the indicator.
        ma_slow: Slow EMA period passed to the indicator.
        signal_bar: Which completed bar to read arrows from.
        lot: Fixed order size in lots.
    """

    params = dict(
        inp_timeframe='D1',
        trend_bar=1,
        ma_filtr=3,
        ma_fast=13,
        ma_slow=144,
        signal_bar=1,
        lot=0.1,
    )

    def __init__(self):
        """Build the F2aAO indicator and reset bar/trade counters."""
        self.indicator = F2aAOIndicator(self.data, ma_filtr=self.p.ma_filtr, ma_fast=self.p.ma_fast, ma_slow=self.p.ma_slow)
        self.bar_num = 0
        self.buy_count = 0
        self.sell_count = 0
        self.trade_count = 0
        self.win_count = 0
        self.loss_count = 0
        self._position_was_open = False

    def log(self, text):
        """Print a timestamped log line for the current bar.

        Args:
            text: The message to log alongside the current bar datetime.
        """
        dt = bt.num2date(self.data.datetime[0])
        print(f'{dt.isoformat()}, {text}')

    def _trend_value(self):
        idx = -max(1, int(self.p.trend_bar))
        return float(self.data.close[idx] - self.data.open[idx])

    def _has_recent_signal(self, line, shift, limit=120):
        start = max(shift + 1, 1)
        max_lookback = min(len(self.data) - 1, start + limit)
        for bar in range(start, max_lookback):
            if float(line[-bar]) != 0.0:
                return True
        return False

    def _signals(self):
        shift = max(1, int(self.p.signal_bar))
        up_value = float(self.indicator.buy[-shift])
        dn_value = float(self.indicator.sell[-shift])
        trend = self._trend_value()
        buy_open = up_value != 0.0
        sell_open = dn_value != 0.0
        buy_close = sell_open
        sell_close = buy_open
        if not buy_close and not sell_close:
            if self.position.size < 0 and self._has_recent_signal(self.indicator.buy, shift):
                sell_close = True
            if self.position.size > 0 and self._has_recent_signal(self.indicator.sell, shift):
                buy_close = True
        if trend <= 0:
            buy_open = False
        if trend >= 0:
            sell_open = False
        return buy_open, sell_open, buy_close, sell_close, trend

    def next(self):
        """Open, close, or reverse on F2a_AO arrow signals each bar.

        Increments the bar counter, skips during warm-up, derives open/close
        signals from the buy/sell arrows and the candle-direction trend filter at
        the configured signal bar, and acts: when flat it opens a long or short;
        when in a position it closes on the close signal or closes and reverses
        on the opposite open signal.
        """
        self.bar_num += 1
        warmup = max(int(self.p.ma_slow), int(self.p.ma_fast), int(self.p.ma_filtr)) + int(self.p.signal_bar) + int(self.p.trend_bar) + 20
        if len(self.data) < warmup:
            return
        buy_open, sell_open, buy_close, sell_close, trend = self._signals()
        if self.position:
            if self.position.size > 0:
                if buy_close and not sell_open:
                    self.log(f'close long trend={trend:.2f}')
                    self.close()
                    return
                if sell_open:
                    self.log(f'close long & sell trend={trend:.2f}')
                    self.close()
                    self.sell(size=self.p.lot)
                    return
            if self.position.size < 0:
                if sell_close and not buy_open:
                    self.log(f'close short trend={trend:.2f}')
                    self.close()
                    return
                if buy_open:
                    self.log(f'close short & buy trend={trend:.2f}')
                    self.close()
                    self.buy(size=self.p.lot)
                    return
        else:
            if buy_open:
                self.log(f'buy trend={trend:.2f}')
                self.buy(size=self.p.lot)
                return
            if sell_open:
                self.log(f'sell trend={trend:.2f}')
                self.sell(size=self.p.lot)
                return

    def notify_trade(self, trade):
        """Count entries when a trade opens and win/loss when it closes.

        Args:
            trade: The trade whose status changed; opening increments the
                buy/sell entry counter and closing updates win/loss counts.
        """
        if trade.isopen and not self._position_was_open:
            if trade.size > 0:
                self.buy_count += 1
            elif trade.size < 0:
                self.sell_count += 1
            self._position_was_open = True
            return
        if not trade.isclosed:
            return
        self.trade_count += 1
        if trade.pnlcomm >= 0:
            self.win_count += 1
        else:
            self.loss_count += 1
        self._position_was_open = False
        self.log(f'trade closed pnl={trade.pnlcomm:.2f}')



