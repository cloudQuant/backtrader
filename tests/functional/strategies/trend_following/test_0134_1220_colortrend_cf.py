def build_resampled_frame(df, indicator_minutes):
    """Resample OHLCV bars to the indicator timeframe.

    Args:
        df: Base minute-level DataFrame.
        indicator_minutes: Resample window in minutes.

    Returns:
        Aggregated OHLCV DataFrame for indicator calculation.
    """
    rule = f'{int(indicator_minutes)}min'
    signal_df = df.resample(rule, label='right', closed='right').agg({
        'open': 'first',
        'high': 'max',
        'low': 'min',
        'close': 'last',
        'volume': 'sum',
        'openinterest': 'last',
    })
    signal_df = signal_df.dropna(subset=['open', 'high', 'low', 'close']).copy()
    signal_df['openinterest'] = signal_df['openinterest'].fillna(0)
    return signal_df


def build_colortrend_cf_frame(df, indicator_minutes, cf_period, point):
    """Build indicator frame with ColorTrend_CF upper and lower lines.

    Args:
        df: Base frame to resample.
        indicator_minutes: Minutes for the signal timeframe.
        cf_period: Lookback period for CF accumulation.
        point: Price scaling factor.

    Returns:
        DataFrame with additional ``upper`` and ``lower`` columns.
    """
    signal_df = build_resampled_frame(df, indicator_minutes)
    prices = signal_df['close'].astype(float).tolist()
    n = len(prices)
    upper = [math.nan] * n
    lower = [math.nan] * n
    xp_window = deque(maxlen=int(cf_period))
    xn_window = deque(maxlen=int(cf_period))
    yp_window = deque(maxlen=int(cf_period))
    yn_window = deque(maxlen=int(cf_period))
    prev_yp = 0.0
    prev_yn = 0.0

    for idx in range(1, n):
        dprice = prices[idx] - prices[idx - 1]
        if dprice > 0:
            xp = dprice
            yp = xp + prev_yp
            xn = 0.0
            yn = 0.0
        else:
            xn = -dprice
            yn = xn + prev_yn
            xp = 0.0
            yp = 0.0

        xp_window.append(xp)
        xn_window.append(xn)
        yp_window.append(yp)
        yn_window.append(yn)
        prev_yp = yp
        prev_yn = yn

        if idx < int(cf_period):
            continue

        chp = sum(xp_window)
        chn = sum(xn_window)
        cffp = sum(yp_window)
        cffn = sum(yn_window)
        upper[idx] = (chp - cffn) / float(point)
        lower[idx] = (chn - cffp) / float(point)

    out = signal_df.copy()
    out['upper'] = upper
    out['lower'] = lower
    return out


class ColorTrendCfStrategy(bt.Strategy):
    """ColorTrend_CF signal strategy with optional direction-based execution rules."""
    params = dict(
        signal_bar=1,
        stop_loss_points=1000,
        take_profit_points=2000,
        mm=-0.1,
        point=0.01,
        buy_pos_open=True,
        sell_pos_open=True,
        buy_pos_close=True,
        sell_pos_close=True,
        indicator_minutes=240,
        cf_period=30,
    )

    def __init__(self):
        """Initialize strategy feeds, counters, and order state."""
        self.base = self.datas[0]
        self.signal = self.datas[1]
        self.bar_num = 0
        self.signal_count = 0
        self.buy_count = 0
        self.sell_count = 0
        self.trade_count = 0
        self.win_count = 0
        self.loss_count = 0
        self._position_was_open = False
        self._last_signal_len = 0

    def log(self, text):
        """Log strategy event text with bar timestamp."""
        dt = bt.num2date(self.base.datetime[0])
        print(f'{dt.isoformat()}, {text}')

    def _line_value(self, line, ago):
        return float(line[-ago]) if ago else float(line[0])

    def _position_size(self, price):
        if self.p.mm < 0:
            return abs(float(self.p.mm))
        if price <= 0:
            return 0.0
        cash = self.broker.getcash()
        return round((cash * float(self.p.mm)) / price, 4)

    def _check_exit_levels(self):
        if not self.position:
            return False
        close_price = float(self.base.close[0])
        point_value = float(self.p.point)
        stop_distance = self.p.stop_loss_points * point_value if self.p.stop_loss_points > 0 else None
        take_distance = self.p.take_profit_points * point_value if self.p.take_profit_points > 0 else None
        entry_price = float(self.position.price)

        if self.position.size > 0:
            if stop_distance is not None and close_price <= entry_price - stop_distance:
                self.log(f'close long by stop loss close={close_price:.2f} entry={entry_price:.2f}')
                self.close()
                return True
            if take_distance is not None and close_price >= entry_price + take_distance:
                self.log(f'close long by take profit close={close_price:.2f} entry={entry_price:.2f}')
                self.close()
                return True
        elif self.position.size < 0:
            if stop_distance is not None and close_price >= entry_price + stop_distance:
                self.log(f'close short by stop loss close={close_price:.2f} entry={entry_price:.2f}')
                self.close()
                return True
            if take_distance is not None and close_price <= entry_price - take_distance:
                self.log(f'close short by take profit close={close_price:.2f} entry={entry_price:.2f}')
                self.close()
                return True
        return False

    def next(self):
        """Handle stop-protection and signal-driven entries/closures each bar."""
        self.bar_num += 1
        if len(self.base) < 2:
            return

        if self._check_exit_levels():
            return

        signal_bar = max(int(self.p.signal_bar), 1)
        if len(self.signal) < signal_bar + 1:
            return

        current_signal_len = len(self.signal)
        if current_signal_len == self._last_signal_len:
            return
        self._last_signal_len = current_signal_len

        recent_ago = signal_bar - 1
        prev_ago = signal_bar
        upper_recent = self._line_value(self.signal.upper, recent_ago)
        lower_recent = self._line_value(self.signal.lower, recent_ago)
        upper_prev = self._line_value(self.signal.upper, prev_ago)
        lower_prev = self._line_value(self.signal.lower, prev_ago)
        if not all(math.isfinite(v) for v in [upper_recent, lower_recent, upper_prev, lower_prev]):
            return

        buy_signal = upper_recent > lower_recent and upper_prev <= lower_prev
        sell_signal = upper_recent < lower_recent and upper_prev >= lower_prev
        if not buy_signal and not sell_signal:
            return

        close_price = float(self.base.close[0])
        size = self._position_size(close_price)
        if size <= 0:
            return

        if buy_signal:
            self.signal_count += 1
            self.log(f'buy signal close={close_price:.2f} upper={upper_recent:.2f} lower={lower_recent:.2f}')
            if self.position.size < 0 and self.p.sell_pos_close:
                self.close()
            if self.position.size <= 0 and self.p.buy_pos_open:
                self.buy(size=size)
            return

        if sell_signal:
            self.signal_count += 1
            self.log(f'sell signal close={close_price:.2f} upper={upper_recent:.2f} lower={lower_recent:.2f}')
            if self.position.size > 0 and self.p.buy_pos_close:
                self.close()
            if self.position.size >= 0 and self.p.sell_pos_open:
                self.sell(size=size)

    def notify_trade(self, trade):
        """Track order-open and closed trade counts including win/loss."""
        if trade.isopen and not self._position_was_open:
            if trade.size > 0:
                self.buy_count += 1
            elif trade.size < 0:
                self.sell_count += 1
            self._position_was_open = True
            return
        if not trade.isclosed:
            return
        self.trade_count += 1
        if trade.pnlcomm >= 0:
            self.win_count += 1
        else:
            self.loss_count += 1
        self._position_was_open = False
        self.log(f'trade closed pnl={trade.pnlcomm:.2f}')



