def load_mt5_csv(filepath, fromdate=None, todate=None, bar_shift_minutes=0):
    """Load a tab-separated MT5 export into an OHLCV DataFrame.

    Args:
        filepath: Path to the MT5 CSV/TSV export file.
        fromdate: Optional inclusive lower bound on the bar timestamp.
        todate: Optional inclusive upper bound on the bar timestamp.
        bar_shift_minutes: Minutes to add to each bar timestamp so bars are
            stamped at their close.

    Returns:
        A datetime-indexed DataFrame with open, high, low, close, tick_volume,
        real_volume, and openinterest columns, filtered to the requested range.
    """
    with open(filepath, 'r', encoding='utf-8') as f:
        lines = f.read().strip().split('\n')
    cleaned = '\n'.join(line.strip().strip('"') for line in lines)
    df = pd.read_csv(io.StringIO(cleaned), sep='\t')
    df['datetime'] = pd.to_datetime(df['<DATE>'] + ' ' + df['<TIME>'], format='%Y.%m.%d %H:%M:%S')
    df = df.rename(columns={
        '<OPEN>': 'open',
        '<HIGH>': 'high',
        '<LOW>': 'low',
        '<CLOSE>': 'close',
        '<TICKVOL>': 'tick_volume',
        '<VOL>': 'real_volume',
    })
    df['openinterest'] = 0
    df = df[['datetime', 'open', 'high', 'low', 'close', 'tick_volume', 'real_volume', 'openinterest']]
    df = df.set_index('datetime')
    if bar_shift_minutes:
        df.index = df.index + pd.Timedelta(minutes=bar_shift_minutes)
    if fromdate is not None:
        df = df[df.index >= fromdate]
    if todate is not None:
        df = df[df.index <= todate]
    return df


def resample_frame(df, rule):
    """Resample an OHLCV DataFrame to a coarser bar interval.

    Args:
        df: Source OHLCV DataFrame indexed by datetime.
        rule: Pandas offset alias describing the target bar size (e.g. '720min').

    Returns:
        A resampled DataFrame with aggregated OHLCV and volume columns, dropping
        bars without price data and filling missing open interest with zero.
    """
    out = df.resample(rule, label='right', closed='right').agg({
        'open': 'first',
        'high': 'max',
        'low': 'min',
        'close': 'last',
        'tick_volume': 'sum',
        'real_volume': 'sum',
        'openinterest': 'last',
    })
    out = out.dropna(subset=['open', 'high', 'low', 'close'])
    out['openinterest'] = out['openinterest'].fillna(0)
    return out


def weighted_moving_average(series, period):
    """Compute a linearly weighted moving average of a series.

    Args:
        series: Input pandas Series.
        period: Window length (clamped to at least 1).

    Returns:
        A Series of the weighted moving average, with leading NaNs for the
        warmup window.
    """
    period = int(max(period, 1))
    weights = np.arange(1, period + 1, dtype=float)
    return series.rolling(period).apply(lambda x: np.dot(x, weights) / weights.sum(), raw=True)


def smma(series, period):
    """Compute a smoothed (Wilder) moving average of a series.

    Args:
        series: Input pandas Series.
        period: Smoothing length (clamped to at least 1).

    Returns:
        A Series of the smoothed moving average aligned to ``series.index``.
    """
    period = int(max(period, 1))
    values = series.to_numpy(dtype=float)
    out = np.full(len(values), np.nan, dtype=float)
    if len(values) < period:
        return pd.Series(out, index=series.index)
    seed = np.nanmean(values[:period])
    out[period - 1] = seed
    for idx in range(period, len(values)):
        prev = out[idx - 1]
        curr = values[idx]
        if np.isnan(prev) or np.isnan(curr):
            continue
        out[idx] = (prev * (period - 1) + curr) / period
    return pd.Series(out, index=series.index)


def smooth_series(series, method, period):
    """Smooth a series using the requested moving-average method.

    Args:
        series: Input pandas Series.
        method: Moving-average mode (e.g. MODE_SMA, MODE_SMMA, MODE_LWMA);
            anything else falls back to an exponential moving average.
        period: Smoothing length (clamped to at least 1).

    Returns:
        A smoothed pandas Series.
    """
    method = str(method or 'MODE_SMA').upper()
    period = int(max(period, 1))
    if method in ('MODE_SMA', 'MODE_SMA_'):
        return series.rolling(period).mean()
    if method in ('MODE_SMMA', 'MODE_SMMA_'):
        return smma(series, period)
    if method in ('MODE_LWMA', 'MODE_LWMA_'):
        return weighted_moving_average(series, period)
    return series.ewm(span=period, adjust=False).mean()


def price_series(frame, applied_price):
    """Select an applied-price series from an OHLCV frame.

    Args:
        frame: OHLCV DataFrame to read price columns from.
        applied_price: MT5-style applied-price key (e.g. PRICE_CLOSE,
            PRICE_MEDIAN, PRICE_TYPICAL, PRICE_DEMARK); unknown keys fall back
            to close.

    Returns:
        A price Series derived per the applied-price convention.
    """
    key = str(applied_price or 'PRICE_CLOSE').upper()
    if key in ('PRICE_CLOSE', 'PRICE_CLOSE_'):
        return frame['close']
    if key in ('PRICE_OPEN', 'PRICE_OPEN_'):
        return frame['open']
    if key in ('PRICE_HIGH', 'PRICE_HIGH_'):
        return frame['high']
    if key in ('PRICE_LOW', 'PRICE_LOW_'):
        return frame['low']
    if key in ('PRICE_MEDIAN', 'PRICE_MEDIAN_'):
        return (frame['high'] + frame['low']) / 2.0
    if key in ('PRICE_TYPICAL', 'PRICE_TYPICAL_'):
        return (frame['high'] + frame['low'] + frame['close']) / 3.0
    if key in ('PRICE_WEIGHTED', 'PRICE_WEIGHTED_'):
        return (frame['high'] + frame['low'] + 2.0 * frame['close']) / 4.0
    if key in ('PRICE_SIMPL', 'PRICE_SIMPL_'):
        return (frame['open'] + frame['close']) / 2.0
    if key in ('PRICE_QUARTER', 'PRICE_QUARTER_'):
        return (frame['high'] + frame['low'] + frame['open'] + frame['close']) / 4.0
    if key in ('PRICE_TRENDFOLLOW0', 'PRICE_TRENDFOLLOW0_'):
        return pd.Series(np.where(frame['close'] >= frame['open'], frame['high'], frame['low']), index=frame.index)
    if key in ('PRICE_TRENDFOLLOW1', 'PRICE_TRENDFOLLOW1_'):
        return pd.Series(np.where(frame['close'] >= frame['open'], frame['low'], frame['high']), index=frame.index)
    if key in ('PRICE_DEMARK', 'PRICE_DEMARK_'):
        cond_up = frame['close'] > frame['open']
        cond_dn = frame['close'] < frame['open']
        return pd.Series(
            np.where(
                cond_up,
                frame['high'] * 2 + frame['low'] + frame['close'],
                np.where(cond_dn, frame['high'] + frame['low'] * 2 + frame['close'], frame['high'] + frame['low'] + frame['close'] * 2),
            ) / 4.0,
            index=frame.index,
        )
    return frame['close']


def compute_ma_by_ma(frame, ma_method1='MODE_SMA', length1=7, phase1=15, ma_method2='MODE_JJMA', length2=7, phase2=15, ipc='PRICE_CLOSE', price_shift=0, point=0.01):
    """Compute the moving-average-of-a-moving-average value and signal lines.

    Args:
        frame: OHLCV DataFrame to enrich.
        ma_method1: Moving-average mode for the first (fast) smoothing.
        length1: Period for the first smoothing.
        phase1: Phase parameter retained for signature compatibility.
        ma_method2: Moving-average mode for the second smoothing.
        length2: Period for the second smoothing.
        phase2: Phase parameter retained for signature compatibility.
        ipc: Applied-price key passed to price_series.
        price_shift: Number of points to vertically shift both output lines.
        point: Point size used to scale ``price_shift``.

    Returns:
        A copy of ``frame`` with ``ind`` (fast) and ``sign`` (slow) columns,
        dropping rows where either is NaN.
    """
    price = pd.Series(price_series(frame, ipc), index=frame.index, dtype=float)
    fast = smooth_series(price, ma_method1, length1)
    slow = smooth_series(fast, ma_method2, length2)
    shift_value = float(point) * float(price_shift)
    out = frame.copy()
    out['ind'] = fast + shift_value
    out['sign'] = slow + shift_value
    return out.dropna(subset=['ind', 'sign'])


