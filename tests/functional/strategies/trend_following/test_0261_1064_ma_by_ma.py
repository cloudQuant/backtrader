def load_backtest_frames(config):
    """Load and resample frames required for M15 and signal-level processing.

    Args:
        config: Resolved configuration containing data range and MaByMa params.

    Returns:
        Dict with keys ``m15`` (base bars), ``signal`` (H12 indicator bars),
        ``fromdate``, and ``todate``.
    """
    data_cfg = config['data']
    params = config['params']
    fromdate = datetime.datetime.fromisoformat(data_cfg['fromdate'])
    todate = datetime.datetime.fromisoformat(data_cfg['todate'])
    base = load_mt5_csv(
        resolve_data_path(data_cfg['file']),
        fromdate=fromdate,
        todate=todate,
        bar_shift_minutes=data_cfg.get('bar_shift_minutes', 0),
    )
    if base.empty:
        raise ValueError('Loaded data frame is empty')
    signal_minutes = data_cfg.get('signal_tf_minutes', 720)
    signal_df = resample_frame(base, f'{signal_minutes}min')
    signal_df = compute_ma_by_ma(
        signal_df,
        ma_method1=params['ma_method1'],
        length1=params['length1'],
        phase1=params['phase1'],
        ma_method2=params['ma_method2'],
        length2=params['length2'],
        phase2=params['phase2'],
        ipc=params['ipc'],
        price_shift=params.get('price_shift', 0),
        point=params.get('point', 0.01),
    )
    signal_df['volume'] = signal_df['tick_volume']
    print(f"Loaded bars: M15={len(base)}, signal={len(signal_df)}")
    return {'m15': base, 'signal': signal_df, 'fromdate': fromdate, 'todate': todate}


def build_cerebro(config, frame):
    """Build a configured Cerebro engine with feeds, strategy, and analyzers.

    Args:
        config: Resolved strategy/backtest configuration.
        frame: Loaded frame bundle returned by :func:`load_backtest_frames`.

    Returns:
        Configured :class:`backtrader.Cerebro` object.
    """
    bt_cfg = config['backtest']
    signal_minutes = config['data'].get('signal_tf_minutes', 720)
    cerebro = bt.Cerebro(stdstats=True)
    cerebro.broker.setcash(bt_cfg['initial_cash'])
    comm_type = bt.CommInfoBase.COMM_FIXED if bt_cfg.get('commission_type', 'fixed') == 'fixed' else bt.CommInfoBase.COMM_PERC
    cerebro.broker.setcommission(
        commission=bt_cfg['commission'],
        margin=bt_cfg['margin'],
        mult=bt_cfg['multiplier'],
        commtype=comm_type,
        stocklike=bt_cfg.get('stocklike', False),
    )
    base_df = frame['m15'].copy()
    base_df['volume'] = base_df['tick_volume']
    feed_m15 = Mt5PandasFeed(dataname=base_df[['open', 'high', 'low', 'close', 'volume', 'openinterest']], timeframe=bt.TimeFrame.Minutes, compression=15)
    sig_df = frame['signal'][['open', 'high', 'low', 'close', 'volume', 'openinterest', 'ind', 'sign']]
    feed_signal = MaByMaFeed(dataname=sig_df, timeframe=bt.TimeFrame.Minutes, compression=signal_minutes)
    cerebro.adddata(feed_m15, name='XAUUSD_M15')
    cerebro.adddata(feed_signal, name='XAUUSD_H12_MABYMA')
