def build_resampled_frame(df, indicator_minutes):
    """Resample an M15 OHLCV frame to the indicator timeframe.

    Args:
        df: Source M15 OHLCV DataFrame indexed by datetime.
        indicator_minutes: Target bar size in minutes (e.g. 240 for H4).

    Returns:
        The resampled OHLCV DataFrame with price-missing bars dropped and
        ``openinterest`` filled with zeros.
    """
    rule = f'{int(indicator_minutes)}min'
    signal_df = df.resample(rule, label='right', closed='right').agg({
        'open': 'first',
        'high': 'max',
        'low': 'min',
        'close': 'last',
        'volume': 'sum',
        'openinterest': 'last',
    })
    signal_df = signal_df.dropna(subset=['open', 'high', 'low', 'close']).copy()
    signal_df['openinterest'] = signal_df['openinterest'].fillna(0)
    return signal_df


def factorial(value):
    """Return the integer factorial of ``value``.

    Args:
        value: Non-negative integer (or value coercible to int).

    Returns:
        The factorial of ``value`` as an int (1 for 0 or 1).
    """
    result = 1
    for i in range(2, int(value) + 1):
        result *= i
    return result


def applied_price(df, code):
    """Select a price series from an OHLC frame by MT5 applied-price code.

    Args:
        df: OHLC DataFrame to derive the price series from.
        code: MT5 applied-price integer code (1=close, 2=open, 3=high,
            4=low, 5=median, 6=typical, 7=weighted, 8=open/close average,
            9=OHLC average, 10/11 candle-direction blends).

    Returns:
        A float pandas Series with the requested price (close by default).
    """
    open_ = df['open'].astype(float)
    high = df['high'].astype(float)
    low = df['low'].astype(float)
    close = df['close'].astype(float)

    code = int(code)
    if code == 1:
        return close
    if code == 2:
        return open_
    if code == 3:
        return high
    if code == 4:
        return low
    if code == 5:
        return (high + low) / 2.0
    if code == 6:
        return (close + high + low) / 3.0
    if code == 7:
        return (2.0 * close + high + low) / 4.0
    if code == 8:
        return (open_ + close) / 2.0
    if code == 9:
        return (open_ + close + high + low) / 4.0
    if code == 10:
        return pd.Series(
            [high.iloc[i] if close.iloc[i] > open_.iloc[i] else low.iloc[i] if close.iloc[i] < open_.iloc[i] else close.iloc[i] for i in range(len(df))],
            index=df.index,
            dtype=float,
        )
    if code == 11:
        return pd.Series(
            [
                (high.iloc[i] + close.iloc[i]) / 2.0 if close.iloc[i] > open_.iloc[i]
                else (low.iloc[i] + close.iloc[i]) / 2.0 if close.iloc[i] < open_.iloc[i]
                else close.iloc[i]
                for i in range(len(df))
            ],
            index=df.index,
            dtype=float,
        )
    return close


def build_bezier_frame(df, indicator_minutes, bperiod, t, ipc, price_shift_points=0, point=0.01):
    """Build the H4 frame with the Bezier curve indicator column.

    Resamples to the indicator timeframe, fits an order-``bperiod`` Bezier curve
    to the applied-price window using binomial weights at parameter ``t``, and
    records the curve value per bar (offset by an optional price shift).

    Args:
        df: Source M15 OHLCV DataFrame.
        indicator_minutes: Indicator bar size in minutes.
        bperiod: Bezier curve order (number of control points minus one).
        t: Bezier parameter in [0, 1] at which the curve is evaluated.
        ipc: Applied-price code passed to :func:`applied_price`.
        price_shift_points: Number of points to add to each curve value.
        point: Price value of one point for the shift.

    Returns:
        The resampled DataFrame with an added ``bezier`` column (NaN until the
        curve window is filled).
    """
    signal_df = build_resampled_frame(df, indicator_minutes)
    period = int(bperiod)
    t = min(max(float(t), 0.0), 1.0)
    price_shift = float(point) * float(price_shift_points)
    src = applied_price(signal_df, ipc)
    bezier_values = [math.nan] * len(signal_df)

    period_factorial = factorial(period)
    weights = []
    for iii in range(period, -1, -1):
        weight = (period_factorial / (factorial(iii) * factorial(period - iii))) * (t ** iii) * ((1.0 - t) ** (period - iii))
        weights.append(weight)

    src_values = src.astype(float).tolist()
    for idx in range(len(signal_df)):
        if idx < period + 1:
            continue
        value = 0.0
        start = idx - period
        for offset, weight in enumerate(weights):
            value += src_values[start + offset] * weight + price_shift
        bezier_values[idx] = value

    signal_df = signal_df.copy()
    signal_df['bezier'] = bezier_values
    return signal_df


class BezierStrategy(bt.Strategy):
    """Bezier curve turn strategy with fixed lot and point-based risk caps.

    Reads three consecutive H4 Bezier values (old, mid, recent): a trough
    (old and recent both above mid) opens or reverses to long, a peak (both
    below mid) opens or reverses to short. Fixed point-based stop-loss and
    take-profit levels bound each position and signal/order/trade/win/loss
    counters feed the regression assertions.
    """

    params = dict(
        signal_bar=1,
        stop_loss_points=1000,
        take_profit_points=2000,
        mm=-0.1,
        point=0.01,
        buy_pos_open=True,
        sell_pos_open=True,
        buy_pos_close=True,
        sell_pos_close=True,
        indicator_minutes=240,
        bperiod=8,
        t=0.5,
        ipc=7,
        price_shift_points=0,
    )

    def __init__(self):
        """Bind the base/signal feeds and reset all counters and state flags."""
        self.base = self.datas[0]
        self.signal = self.datas[1]
        self.bar_num = 0
        self.signal_count = 0
        self.buy_count = 0
        self.sell_count = 0
        self.trade_count = 0
        self.win_count = 0
        self.loss_count = 0
        self._position_was_open = False
        self._last_signal_len = 0

    def log(self, text):
        """Print ``text`` prefixed with the current base bar's ISO timestamp."""
        dt = bt.num2date(self.base.datetime[0])
        print(f'{dt.isoformat()}, {text}')

    def _line_value(self, line, ago):
        return float(line[-ago]) if ago else float(line[0])

    def _has_value(self, value):
        return math.isfinite(value) and not math.isnan(value)

    def _position_size(self, price):
        if self.p.mm < 0:
            return abs(float(self.p.mm))
        if price <= 0:
            return 0.0
        cash = self.broker.getcash()
        return round((cash * float(self.p.mm)) / price, 4)

    def _check_exit_levels(self):
        if not self.position:
            return False
        close_price = float(self.base.close[0])
        point_value = float(self.p.point)
        stop_distance = self.p.stop_loss_points * point_value if self.p.stop_loss_points > 0 else None
        take_distance = self.p.take_profit_points * point_value if self.p.take_profit_points > 0 else None
        entry_price = float(self.position.price)

        if self.position.size > 0:
            if stop_distance is not None and close_price <= entry_price - stop_distance:
                self.log(f'close long by stop loss close={close_price:.2f} entry={entry_price:.2f}')
                self.close()
                return True
            if take_distance is not None and close_price >= entry_price + take_distance:
                self.log(f'close long by take profit close={close_price:.2f} entry={entry_price:.2f}')
                self.close()
                return True
        elif self.position.size < 0:
            if stop_distance is not None and close_price >= entry_price + stop_distance:
                self.log(f'close short by stop loss close={close_price:.2f} entry={entry_price:.2f}')
                self.close()
                return True
            if take_distance is not None and close_price <= entry_price - take_distance:
                self.log(f'close short by take profit close={close_price:.2f} entry={entry_price:.2f}')
                self.close()
                return True
        return False

    def next(self):
        """Advance one base bar: manage risk, then act on the Bezier turn.

        Enforces stop/target exits, waits for a fresh signal bar with valid
        old/mid/recent Bezier values, and opens or reverses the fixed-size
        position on a Bezier trough (long) or peak (short).
        """
        self.bar_num += 1
        if len(self.base) < 2:
            return

        if self._check_exit_levels():
            return

        signal_bar = max(int(self.p.signal_bar), 1)
        if len(self.signal) < signal_bar + 2:
            return

        current_signal_len = len(self.signal)
        if current_signal_len == self._last_signal_len:
            return
        self._last_signal_len = current_signal_len

        recent_ago = signal_bar - 1
        mid_ago = signal_bar
        old_ago = signal_bar + 1
        recent = self._line_value(self.signal.bezier, recent_ago)
        mid = self._line_value(self.signal.bezier, mid_ago)
        old = self._line_value(self.signal.bezier, old_ago)
        if not self._has_value(recent) or not self._has_value(mid) or not self._has_value(old):
            return

        buy_signal = old > mid and recent > mid
        sell_signal = old < mid and recent < mid
        if not buy_signal and not sell_signal:
            return

        close_price = float(self.base.close[0])
        size = self._position_size(close_price)
        if size <= 0:
            return

        if buy_signal:
            self.signal_count += 1
            self.log(f'buy signal close={close_price:.2f} old={old:.2f} mid={mid:.2f} recent={recent:.2f}')
            if self.position.size < 0 and self.p.sell_pos_close:
                self.close()
            if self.position.size <= 0 and self.p.buy_pos_open:
                self.buy(size=size)
            return

        if sell_signal:
            self.signal_count += 1
            self.log(f'sell signal close={close_price:.2f} old={old:.2f} mid={mid:.2f} recent={recent:.2f}')
            if self.position.size > 0 and self.p.buy_pos_close:
                self.close()
            if self.position.size >= 0 and self.p.sell_pos_open:
                self.sell(size=size)

    def notify_trade(self, trade):
        """Count entries on trade open and win/loss tallies on trade close.

        Args:
            trade: The trade being notified; opens increment buy/sell counts
                once, closes increment trade and win/loss counts.
        """
        if trade.isopen and not self._position_was_open:
            if trade.size > 0:
                self.buy_count += 1
            elif trade.size < 0:
                self.sell_count += 1
            self._position_was_open = True
            return
        if not trade.isclosed:
            return
        self.trade_count += 1
        if trade.pnlcomm >= 0:
            self.win_count += 1
        else:
            self.loss_count += 1
        self._position_was_open = False
        self.log(f'trade closed pnl={trade.pnlcomm:.2f}')


