def resample_frame(df, rule):
    """Resample an OHLCV frame to a coarser timeframe using right-closed bars.

    Args:
        df: A datetime-indexed OHLCV frame.
        rule: A pandas offset alias (e.g. ``'240min'``) for the target timeframe.

    Returns:
        pandas.DataFrame: The resampled frame with incomplete OHLC rows dropped.
    """
    out = df.resample(rule, label='right', closed='right').agg({
        'open': 'first',
        'high': 'max',
        'low': 'min',
        'close': 'last',
        'volume': 'sum',
        'openinterest': 'last',
    })
    out = out.dropna(subset=['open', 'high', 'low', 'close'])
    out['openinterest'] = out['openinterest'].fillna(0)
    return out


def compute_atr(frame, period=100):
    """Compute the simple-moving-average Average True Range over a frame.

    Args:
        frame: OHLCV frame providing ``high``, ``low`` and ``close`` columns.
        period: Rolling window length for the ATR average.

    Returns:
        pandas.Series: The ATR aligned to ``frame``.
    """
    prev_close = frame['close'].shift(1)
    tr = pd.concat([
        frame['high'] - frame['low'],
        (frame['high'] - prev_close).abs(),
        (frame['low'] - prev_close).abs(),
    ], axis=1).max(axis=1)
    return tr.rolling(int(period)).mean()


def moving_average(series, period, method='SMA'):
    """Compute a moving average of a series using the requested method.

    Args:
        series: Numeric series to smooth.
        period: Lookback window length.
        method: One of ``'SMA'``, ``'EMA'``, ``'SMMA'`` or ``'LWMA'``.

    Returns:
        pandas.Series: The smoothed series aligned to ``series``.
    """
    period = max(int(period), 1)
    method = str(method).upper()
    if method == 'EMA':
        return series.ewm(span=period, adjust=False).mean()
    if method == 'SMMA':
        return series.ewm(alpha=1.0 / period, adjust=False).mean()
    if method == 'LWMA':
        weights = np.arange(1, period + 1, dtype=float)
        return series.rolling(period).apply(lambda x: np.dot(x, weights) / weights.sum(), raw=True)
    return series.rolling(period).mean()


def compute_ozymandias(frame, length=2, ma_type='SMA', atr_period=100):
    """Compute Ozymandias trend state and band levels.

    Args:
        frame: OHLCV frame containing ``high``, ``low``, and ``close``.
        length: Moving-average length used for smoothing highs and lows.
        ma_type: Moving-average method forwarded to :func:`moving_average`.
        atr_period: ATR lookback period used for band width.

    Returns:
        pandas.DataFrame: Frame with ``ozy_line``, ``ozy_color``,
        ``ozy_upper`` and ``ozy_lower`` columns added.
    """
    length = max(int(length), 1)
    atr = compute_atr(frame, atr_period)
    hma = moving_average(frame['high'], length, ma_type)
    lma = moving_average(frame['low'], length, ma_type)

    ind = np.full(len(frame), np.nan, dtype=float)
    color = np.full(len(frame), np.nan, dtype=float)
    upper = np.full(len(frame), np.nan, dtype=float)
    lower = np.full(len(frame), np.nan, dtype=float)

    high = frame['high'].to_numpy(dtype=float)
    low = frame['low'].to_numpy(dtype=float)
    close = frame['close'].to_numpy(dtype=float)
    atr_values = atr.to_numpy(dtype=float)
    hma_values = hma.to_numpy(dtype=float)
    lma_values = lma.to_numpy(dtype=float)

    trend = 0
    nexttrend = 0
    maxl = 0.0
    minh = 9999999.0
    min_rates_total = max(length, atr_period)

    for i in range(len(frame)):
        if i < min_rates_total or np.isnan(atr_values[i]) or np.isnan(hma_values[i]) or np.isnan(lma_values[i]) or i == 0:
            continue

        hh = np.max(high[max(0, i - length + 1):i + 1])
        ll = np.min(low[max(0, i - length + 1):i + 1])
        atr_half = atr_values[i] / 2.0

        trend0 = trend
        nexttrend0 = nexttrend
        maxl0 = maxl
        minh0 = minh

        if nexttrend0 == 1:
            maxl0 = max(ll, maxl0)
            if hma_values[i] < maxl0 and close[i] < low[i - 1]:
                trend0 = 1
                nexttrend0 = 0
                minh0 = hh

        if nexttrend0 == 0:
            minh0 = min(hh, minh0)
            if lma_values[i] > minh0 and close[i] > high[i - 1]:
                trend0 = 0
                nexttrend0 = 1
                maxl0 = ll

        prev_ind = ind[i - 1] if i > 0 and not np.isnan(ind[i - 1]) else (maxl0 if trend0 == 0 else minh0)
        if trend0 == 0:
            if trend != 0:
                ind[i] = prev_ind
            else:
                ind[i] = max(maxl0, prev_ind)
            color[i] = 0.0
        else:
            if trend != 1:
                ind[i] = prev_ind
            else:
                ind[i] = min(minh0, prev_ind)
            color[i] = 1.0

        upper[i] = ind[i] + atr_half
        lower[i] = ind[i] - atr_half

        trend = trend0
        nexttrend = nexttrend0
        maxl = maxl0
        minh = minh0

    out = frame.copy()
    out['ozy_line'] = ind
    out['ozy_color'] = color
    out['ozy_upper'] = upper
    out['ozy_lower'] = lower
    return out.dropna(subset=['ozy_line', 'ozy_color'])


