def finite_or_none(value):
    """Return ``value`` when it is a finite number, otherwise ``None``."""
    return value if value is not None and math.isfinite(value) else None


def calculate_ulcer_index(values):
    """Compute the Ulcer Index (RMS percentage drawdown) of an equity series.

    Args:
        values: Sequence of broker/equity values in chronological order.

    Returns:
        float: The Ulcer Index, or ``0.0`` when fewer than two values are given.
    """
    if len(values) < 2:
        return 0.0
    peak = values[0]
    squared = 0.0
    for value in values:
        peak = max(peak, value)
        drawdown = (peak - value) / peak * 100.0 if peak > 0 else 0.0
        squared += drawdown ** 2
    return math.sqrt(squared / len(values))


def load_data(config):
    """Load and feature-engineer the daily backtest frame from config.

    Args:
        config: Resolved configuration dict whose ``data`` and ``params``
            sections drive loading and feature preparation.

    Returns:
        dict: Mapping with the prepared ``data`` frame plus ``fromdate`` and
        ``todate`` datetimes.
    """
    data_cfg = config['data']
    fromdate = datetime.fromisoformat(data_cfg['fromdate'])
    todate = datetime.fromisoformat(data_cfg['todate'])
    raw = load_mt5_csv(data_cfg['file'], fromdate=fromdate, todate=todate)
    df = prepare_trend_equity_features(raw, config.get('params', {}))
    return {'data': df, 'fromdate': fromdate, 'todate': todate}


def build_cerebro(frame, config):
    """Assemble a Cerebro engine with the feature feed, broker, and analyzers.

    Args:
        frame: Backtest input dict produced by :func:`load_data`.
        config: Resolved configuration dict providing backtest, data, and
            strategy parameter sections.

    Returns:
        backtrader.Cerebro: Configured engine ready to run.
    """
    cerebro = bt.Cerebro(stdstats=False)
    cerebro.broker.setcash(float(config['backtest']['initial_cash']))
    cerebro.broker.setcommission(commission=float(config['params'].get('commission_pct', 0.0005)))
    feed = TrendEquityFeed(dataname=frame['data'], timeframe=bt.TimeFrame.Days, compression=1)
    cerebro.adddata(feed, name=config['data']['symbol'])
    cerebro.addstrategy(TrendEquityStrategy, **config.get('params', {}))
    cerebro.addanalyzer(bt.analyzers.SharpeRatio, _name='sharpe', timeframe=bt.TimeFrame.Days, compression=1, factor=252, annualize=True, riskfreerate=0)
    cerebro.addanalyzer(bt.analyzers.Returns, _name='returns', timeframe=bt.TimeFrame.Days, tann=252)
    cerebro.addanalyzer(bt.analyzers.DrawDown, _name='drawdown')
    cerebro.addanalyzer(bt.analyzers.TradeAnalyzer, _name='trades')
    cerebro.addanalyzer(bt.analyzers.SQN, _name='sqn')
    return cerebro


def extract_metrics(strat, cerebro, frame, config):
    """Build the regression metrics dict from analyzers and strategy counters.

    Args:
        strat: The executed strategy instance holding the bookkeeping counters.
        cerebro: The Cerebro engine, used to read final broker value.
        frame: Backtest input dict providing bar count and date range.
        config: Resolved configuration dict providing the strategy name and cash.

    Returns:
        dict: Trade counts, exposure-day counts, return/risk figures, and the
        custom Ulcer Index used by the assertions.
    """
    trades = strat.analyzers.trades.get_analysis()
    sharpe = strat.analyzers.sharpe.get_analysis()
    returns = strat.analyzers.returns.get_analysis()
    drawdown = strat.analyzers.drawdown.get_analysis()
    sqn = strat.analyzers.sqn.get_analysis()
    initial_cash = float(config['backtest']['initial_cash'])
    final_value = float(cerebro.broker.getvalue())
    broker_values = [value for _, value in getattr(strat, 'broker_value_series', [])] or [initial_cash, final_value]
    total_trades = trades.get('total', {}).get('total', 0)
    won = trades.get('won', {}).get('total', 0)
    lost = trades.get('lost', {}).get('total', 0)
    gross_won = trades.get('won', {}).get('pnl', {}).get('total', 0) or 0
    gross_lost = abs(trades.get('lost', {}).get('pnl', {}).get('total', 0) or 0)
    return {
        'strategy_name': config['strategy']['name'], 'fromdate': frame['fromdate'], 'todate': frame['todate'], 'bars': len(frame['data']),
        'bar_num': strat.bar_num, 'buy_count': strat.buy_count, 'sell_count': strat.sell_count, 'rebalance_count': getattr(strat, 'rebalance_count', 0),
        'long_days': getattr(strat, 'long_days', 0), 'flat_days': getattr(strat, 'flat_days', 0),
        'initial_cash': initial_cash, 'final_value': final_value, 'net_pnl': final_value - initial_cash, 'total_return_pct': (final_value / initial_cash - 1.0) * 100.0,
        'total_trades': total_trades, 'won': won, 'lost': lost, 'win_rate': (won / total_trades * 100.0) if total_trades else 0.0,
        'profit_factor': (gross_won / gross_lost) if gross_lost else None, 'max_drawdown': drawdown.get('max', {}).get('drawdown', 0.0),
        'sharpe_ratio': finite_or_none(sharpe.get('sharperatio')), 'annual_return_pct': (returns.get('rnorm') or 0.0) * 100.0, 'sqn': finite_or_none(sqn.get('sqn')),
        'ulcer_index': calculate_ulcer_index(broker_values),
    }


def normalize(value):
    """Convert datetimes to ISO strings and non-finite floats to ``None``."""
    if isinstance(value, datetime):
        return value.isoformat()
    if isinstance(value, float) and (math.isnan(value) or math.isinf(value)):
        return None
    return value


