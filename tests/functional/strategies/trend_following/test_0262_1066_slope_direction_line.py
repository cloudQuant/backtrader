def load_mt5_csv(filepath, fromdate=None, todate=None, bar_shift_minutes=0):
    """Load an MT5-exported tab-separated CSV into an OHLCV DataFrame.

    Args:
        filepath: Path to the MT5 export file.
        fromdate: Optional inclusive lower bound for the datetime index.
        todate: Optional inclusive upper bound for the datetime index.
        bar_shift_minutes: Optional number of minutes to shift each bar's
            timestamp forward.

    Returns:
        A DataFrame indexed by datetime with open, high, low, close, tick_volume,
        real_volume, and openinterest columns, filtered to the requested date
        range.
    """
    with open(filepath, 'r', encoding='utf-8') as f:
        lines = f.read().strip().split('\n')
    cleaned = '\n'.join(line.strip().strip('"') for line in lines)
    df = pd.read_csv(io.StringIO(cleaned), sep='\t')
    df['datetime'] = pd.to_datetime(df['<DATE>'] + ' ' + df['<TIME>'], format='%Y.%m.%d %H:%M:%S')
    df = df.rename(columns={
        '<OPEN>': 'open',
        '<HIGH>': 'high',
        '<LOW>': 'low',
        '<CLOSE>': 'close',
        '<TICKVOL>': 'tick_volume',
        '<VOL>': 'real_volume',
    })
    df['openinterest'] = 0
    df = df[['datetime', 'open', 'high', 'low', 'close', 'tick_volume', 'real_volume', 'openinterest']]
    df = df.set_index('datetime')
    if bar_shift_minutes:
        df.index = df.index + pd.Timedelta(minutes=bar_shift_minutes)
    if fromdate is not None:
        df = df[df.index >= fromdate]
    if todate is not None:
        df = df[df.index <= todate]
    return df


def resample_frame(df, rule):
    """Resample an OHLCV+volume frame to a coarser bar rule.

    Args:
        df: Source OHLCV DataFrame indexed by datetime.
        rule: A pandas resample rule string (e.g. ``240min``).

    Returns:
        A resampled DataFrame with NaN OHLC rows dropped and openinterest gaps
        filled with zero.
    """
    out = df.resample(rule, label='right', closed='right').agg({
        'open': 'first',
        'high': 'max',
        'low': 'min',
        'close': 'last',
        'tick_volume': 'sum',
        'real_volume': 'sum',
        'openinterest': 'last',
    })
    out = out.dropna(subset=['open', 'high', 'low', 'close'])
    out['openinterest'] = out['openinterest'].fillna(0)
    return out


def weighted_moving_average(series, period):
    """Compute a linear weighted moving average of a series.

    Args:
        series: The input Series to smooth.
        period: Window length (clamped to at least 1); recent bars weighted more.

    Returns:
        The weighted moving average Series.
    """
    period = int(max(period, 1))
    weights = np.arange(1, period + 1, dtype=float)
    return series.rolling(period).apply(lambda x: np.dot(x, weights) / weights.sum(), raw=True)


def smma(series, period):
    """Compute a smoothed moving average (Wilder-style) of a series.

    Args:
        series: The input Series to smooth.
        period: Smoothing window length (clamped to at least 1).

    Returns:
        The smoothed moving average Series, NaN until enough history exists.
    """
    period = int(max(period, 1))
    values = series.to_numpy(dtype=float)
    out = np.full(len(values), np.nan, dtype=float)
    if len(values) < period:
        return pd.Series(out, index=series.index)
    seed = np.nanmean(values[:period])
    out[period - 1] = seed
    for idx in range(period, len(values)):
        prev = out[idx - 1]
        curr = values[idx]
        if np.isnan(prev) or np.isnan(curr):
            continue
        out[idx] = (prev * (period - 1) + curr) / period
    return pd.Series(out, index=series.index)


def smooth_series(series, method, period):
    """Smooth a series with the requested moving-average method.

    Args:
        series: The input Series to smooth.
        method: Moving-average method (``MODE_SMA``, ``MODE_EMA``,
            ``MODE_SMMA``, or LWMA as the default).
        period: Smoothing window length (clamped to at least 1).

    Returns:
        The smoothed Series.
    """
    method = str(method or 'MODE_LWMA').upper()
    period = int(max(period, 1))
    if method in ('MODE_SMA', 'MODE_SMA_'):
        return series.rolling(period).mean()
    if method in ('MODE_EMA', 'MODE_EMA_'):
        return series.ewm(span=period, adjust=False).mean()
    if method in ('MODE_SMMA', 'MODE_SMMA_'):
        return smma(series, period)
    return weighted_moving_average(series, period)


def price_series(frame, applied_price):
    """Select a price series for the given MT5 applied-price mode.

    Args:
        frame: OHLC DataFrame to derive the price series from.
        applied_price: MT5 applied-price constant (e.g. ``PRICE_CLOSE_``,
            ``PRICE_MEDIAN_``, ``PRICE_TYPICAL_``, ``PRICE_DEMARK_``).

    Returns:
        The price Series corresponding to ``applied_price``, defaulting to close.
    """
    key = str(applied_price or 'PRICE_CLOSE_').upper()
    if key in ('PRICE_CLOSE', 'PRICE_CLOSE_'):
        return frame['close']
    if key in ('PRICE_OPEN', 'PRICE_OPEN_'):
        return frame['open']
    if key in ('PRICE_HIGH', 'PRICE_HIGH_'):
        return frame['high']
    if key in ('PRICE_LOW', 'PRICE_LOW_'):
        return frame['low']
    if key in ('PRICE_MEDIAN', 'PRICE_MEDIAN_'):
        return (frame['high'] + frame['low']) / 2.0
    if key in ('PRICE_TYPICAL', 'PRICE_TYPICAL_'):
        return (frame['high'] + frame['low'] + frame['close']) / 3.0
    if key in ('PRICE_WEIGHTED', 'PRICE_WEIGHTED_'):
        return (frame['high'] + frame['low'] + 2.0 * frame['close']) / 4.0
    if key in ('PRICE_SIMPL', 'PRICE_SIMPL_'):
        return (frame['open'] + frame['close']) / 2.0
    if key in ('PRICE_QUARTER', 'PRICE_QUARTER_'):
        return (frame['high'] + frame['low'] + frame['open'] + frame['close']) / 4.0
    if key in ('PRICE_TRENDFOLLOW0', 'PRICE_TRENDFOLLOW0_'):
        return pd.Series(np.where(frame['close'] >= frame['open'], frame['high'], frame['low']), index=frame.index)
    if key in ('PRICE_TRENDFOLLOW1', 'PRICE_TRENDFOLLOW1_'):
        return pd.Series(np.where(frame['close'] >= frame['open'], frame['low'], frame['high']), index=frame.index)
    if key in ('PRICE_DEMARK', 'PRICE_DEMARK_'):
        cond_up = frame['close'] > frame['open']
        cond_dn = frame['close'] < frame['open']
        return pd.Series(
            np.where(
                cond_up,
                frame['high'] * 2 + frame['low'] + frame['close'],
                np.where(cond_dn, frame['high'] + frame['low'] * 2 + frame['close'], frame['high'] + frame['low'] + frame['close'] * 2),
            ) / 4.0,
            index=frame.index,
        )
    return frame['close']


def compute_slope_direction_line(frame, ma_method1='MODE_LWMA', length1=12, phase1=15, ma_method2='MODE_SMA', phase2=15, ipc='PRICE_CLOSE_', price_shift=0, point=0.01):
    """Compute the Slope Direction Line ind/color values for a frame.

    Args:
        frame: OHLC DataFrame to compute on.
        ma_method1: First-stage moving-average method.
        length1: First-stage moving-average length.
        phase1: Phase parameter retained for config compatibility.
        ma_method2: Second-stage smoothing method.
        phase2: Phase parameter retained for config compatibility.
        ipc: Applied-price mode for the source series.
        price_shift: Price shift in points applied to the output line.
        point: Point size used to scale ``price_shift``.

    Returns:
        A copy of ``frame`` with ``ind`` (the slope line) and ``color`` (slope
        direction state) columns added, dropping rows where either is undefined.
    """
    price = pd.Series(price_series(frame, ipc), index=frame.index, dtype=float)
    length1 = int(max(length1, 1))
    length_x = int(max(length1 // 2, 1))
    length_r = int(max(math.sqrt(length1), 1))
    line_full = smooth_series(price, ma_method1, length1)
    line_half = smooth_series(price, ma_method1, length_x)
    line = 2.0 * line_half - line_full
    xline = smooth_series(line, ma_method2, length_r)
    ind = xline + float(point) * float(price_shift)
    color = pd.Series(1.0, index=frame.index)
    delta = ind.diff()
    color = color.where(~(delta > 0), 2.0)
    color = color.where(~(delta < 0), 0.0)
    out = frame.copy()
    out['ind'] = ind
    out['color'] = color
    return out.dropna(subset=['ind', 'color'])


