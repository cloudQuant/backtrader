class MorningStarStochStrategy(bt.Strategy):
    """
    Morning Star / Evening Star candlestick pattern + Stochastic confirmation.
    Buy: Morning Star (or Morning Doji) AND Stoch %D < 30
    Sell: Evening Star (or Evening Doji) AND Stoch %D > 70
    Exit: Stoch %D crosses overbought/oversold levels
    """
    params = dict(
        stoch_k=8, stoch_d=3, stoch_slow=3,
        ma_period=5,
        stoch_entry_long=30, stoch_entry_short=70,
        stoch_exit_upper=80, stoch_exit_lower=20,
        lot=0.1, point=0.01, price_digits=2,
    )

    def __init__(self):
        self.stoch = bt.indicators.StochasticSlow(
            self.data, period=self.p.stoch_k,
            period_dfast=self.p.stoch_d, period_dslow=self.p.stoch_slow)
        self.close_avg = bt.indicators.SMA(self.data.close, period=self.p.ma_period)
        self.sma_body = bt.indicators.SMA(
            abs(self.data.close - self.data.open), period=self.p.ma_period)
        self.bar_num = 0
        self.buy_count = 0
        self.sell_count = 0
        self.trade_count = 0
        self.win_count = 0
        self.loss_count = 0
        self._position_was_open = False

    def log(self, text):
        dt = bt.num2date(self.data.datetime[0])
        print(f'{dt.isoformat()}, {text}')

    def _avg_body(self):
        return float(self.sma_body[0])

    def _mid_oc(self, ago):
        return (float(self.data.open[ago]) + float(self.data.close[ago])) / 2.0

    def _is_morning_star(self):
        """3-bar pattern: long bearish, small body gap down, bullish close above bar1 midpoint"""
        o3, c3 = float(self.data.open[-3]), float(self.data.close[-3])
        o2, c2 = float(self.data.open[-2]), float(self.data.close[-2])
        c1 = float(self.data.close[-1])
        avg = self._avg_body()
        if avg < self.p.point:
            return False
        return ((o3 - c3) > avg and
                abs(c2 - o2) < avg * 0.5 and
                c2 < c3 and o2 < o3 and
                c1 > self._mid_oc(-3))

    def _is_morning_doji(self):
        o3, c3 = float(self.data.open[-3]), float(self.data.close[-3])
        o2, c2 = float(self.data.open[-2]), float(self.data.close[-2])
        o1, c1 = float(self.data.open[-1]), float(self.data.close[-1])
        avg = self._avg_body()
        if avg < self.p.point:
            return False
        return ((o3 - c3) > avg and
                abs(c2 - o2) < avg * 0.1 and
                c2 < c3 and o2 < o3 and
                o1 > c2 and c1 > c2)

    def _is_evening_star(self):
        """3-bar pattern: long bullish, small body gap up, bearish close below bar1 midpoint"""
        o3, c3 = float(self.data.open[-3]), float(self.data.close[-3])
        o2, c2 = float(self.data.open[-2]), float(self.data.close[-2])
        c1 = float(self.data.close[-1])
        avg = self._avg_body()
        if avg < self.p.point:
            return False
        return ((c3 - o3) > avg and
                abs(c2 - o2) < avg * 0.5 and
                c2 > c3 and o2 > o3 and
                c1 < self._mid_oc(-3))

    def _is_evening_doji(self):
        o3, c3 = float(self.data.open[-3]), float(self.data.close[-3])
        o2, c2 = float(self.data.open[-2]), float(self.data.close[-2])
        o1, c1 = float(self.data.open[-1]), float(self.data.close[-1])
        avg = self._avg_body()
        if avg < self.p.point:
            return False
        return ((c3 - o3) > avg and
                abs(c2 - o2) < avg * 0.1 and
                c2 > c3 and o2 > o3 and
                o1 < c2 and c1 < c2)

    def next(self):
        self.bar_num += 1
        warmup = max(self.p.stoch_k + self.p.stoch_slow, self.p.ma_period) + 5
        if len(self.data) < warmup:
            return
        sd0 = float(self.stoch.percD[0])
        sd1 = float(self.stoch.percD[-1])
        if self.position:
            if self.position.size > 0:
                if ((sd0 > self.p.stoch_exit_lower and sd1 < self.p.stoch_exit_lower) or
                    (sd0 > self.p.stoch_exit_upper and sd1 < self.p.stoch_exit_upper)):
                    self.close()
                    return
            elif self.position.size < 0:
                if ((sd0 < self.p.stoch_exit_upper and sd1 > self.p.stoch_exit_upper) or
                    (sd0 < self.p.stoch_exit_lower and sd1 > self.p.stoch_exit_lower)):
                    self.close()
                    return
        else:
            if (self._is_morning_star() or self._is_morning_doji()) and sd0 < self.p.stoch_entry_long:
                self.buy(size=self.p.lot)
                return
            if (self._is_evening_star() or self._is_evening_doji()) and sd0 > self.p.stoch_entry_short:
                self.sell(size=self.p.lot)
                return

    def notify_trade(self, trade):
        if trade.isopen and not self._position_was_open:
            if trade.size > 0: self.buy_count += 1
            elif trade.size < 0: self.sell_count += 1
            self._position_was_open = True
            return
        if not trade.isclosed: return
        self.trade_count += 1
        if trade.pnlcomm >= 0: self.win_count += 1
        else: self.loss_count += 1
        self._position_was_open = False



