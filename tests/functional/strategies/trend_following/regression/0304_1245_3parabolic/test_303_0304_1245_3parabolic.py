class ThreeParabolicStrategy(bt.Strategy):
    params = dict(
        sar_step=0.02,
        sar_maximum=0.2,
        stop_loss_points=1000,
        take_profit_points=2000,
        mm=-0.1,
        point=0.01,
        buy_pos_open=True,
        sell_pos_open=True,
        buy_pos_close=True,
        sell_pos_close=True,
        timeframe1_minutes=360,
        timeframe2_minutes=180,
        timeframe3_minutes=60,
    )

    def __init__(self):
        self.base = self.datas[0]
        self.tf1 = self.datas[1]
        self.tf2 = self.datas[2]
        self.tf3 = self.datas[3]

        self.sar1 = bt.indicators.ParabolicSAR(self.tf1, af=self.p.sar_step, afmax=self.p.sar_maximum)
        self.sar2 = bt.indicators.ParabolicSAR(self.tf2, af=self.p.sar_step, afmax=self.p.sar_maximum)
        self.sar3 = bt.indicators.ParabolicSAR(self.tf3, af=self.p.sar_step, afmax=self.p.sar_maximum)

        self.bar_num = 0
        self.signal_count = 0
        self.buy_count = 0
        self.sell_count = 0
        self.trade_count = 0
        self.win_count = 0
        self.loss_count = 0
        self._position_was_open = False
        self._last_tf3_len = 0

    def log(self, text):
        dt = bt.num2date(self.base.datetime[0])
        print(f'{dt.isoformat()}, {text}')

    def _position_size(self, price):
        if self.p.mm < 0:
            return abs(float(self.p.mm))
        if price <= 0:
            return 0.0
        cash = self.broker.getcash()
        return round((cash * float(self.p.mm)) / price, 4)

    def _relation(self, sar_value, close_value):
        if not math.isfinite(float(sar_value)) or not math.isfinite(float(close_value)):
            return 0
        if float(sar_value) < float(close_value):
            return 1
        if float(sar_value) > float(close_value):
            return -1
        return 0

    def _check_exit_levels(self):
        if not self.position:
            return False
        close_price = float(self.base.close[0])
        point_value = float(self.p.point)
        stop_distance = self.p.stop_loss_points * point_value if self.p.stop_loss_points > 0 else None
        take_distance = self.p.take_profit_points * point_value if self.p.take_profit_points > 0 else None
        entry_price = float(self.position.price)

        if self.position.size > 0:
            if stop_distance is not None and close_price <= entry_price - stop_distance:
                self.log(f'close long by stop loss close={close_price:.2f} entry={entry_price:.2f}')
                self.close()
                return True
            if take_distance is not None and close_price >= entry_price + take_distance:
                self.log(f'close long by take profit close={close_price:.2f} entry={entry_price:.2f}')
                self.close()
                return True
        elif self.position.size < 0:
            if stop_distance is not None and close_price >= entry_price + stop_distance:
                self.log(f'close short by stop loss close={close_price:.2f} entry={entry_price:.2f}')
                self.close()
                return True
            if take_distance is not None and close_price <= entry_price - take_distance:
                self.log(f'close short by take profit close={close_price:.2f} entry={entry_price:.2f}')
                self.close()
                return True
        return False

    def next(self):
        self.bar_num += 1
        if len(self.base) < 2:
            return
        if min(len(self.tf1), len(self.tf2), len(self.tf3)) < 3:
            return

        if self._check_exit_levels():
            return

        current_tf3_len = len(self.tf3)
        if current_tf3_len == self._last_tf3_len:
            return
        self._last_tf3_len = current_tf3_len

        tf1_trend = self._relation(self.sar1[0], self.tf1.close[0])
        tf2_trend = self._relation(self.sar2[0], self.tf2.close[0])
        tf3_current_trend = self._relation(self.sar3[0], self.tf3.close[0])
        tf3_prev_trend = self._relation(self.sar3[-1], self.tf3.close[-1])

        buy_close = self.p.buy_pos_close and (tf1_trend < 0 or tf2_trend < 0 or tf3_current_trend < 0)
        sell_close = self.p.sell_pos_close and (tf1_trend > 0 or tf2_trend > 0 or tf3_current_trend > 0)

        close_price = float(self.base.close[0])
        size = self._position_size(close_price)
        if size <= 0:
            return

        if sell_close and self.position.size < 0:
            self.close()
        if buy_close and self.position.size > 0:
            self.close()

        buy_open = (
            self.p.buy_pos_open
            and tf1_trend > 0
            and tf2_trend > 0
            and tf3_prev_trend < 0
            and tf3_current_trend > 0
        )
        sell_open = (
            self.p.sell_pos_open
            and tf1_trend < 0
            and tf2_trend < 0
            and tf3_prev_trend > 0
            and tf3_current_trend < 0
        )

        if buy_open:
            self.signal_count += 1
            self.log(f'buy signal tf1={tf1_trend} tf2={tf2_trend} tf3_prev={tf3_prev_trend} tf3={tf3_current_trend} close={close_price:.2f}')
            if self.position.size <= 0:
                self.buy(size=size)
            return

        if sell_open:
            self.signal_count += 1
            self.log(f'sell signal tf1={tf1_trend} tf2={tf2_trend} tf3_prev={tf3_prev_trend} tf3={tf3_current_trend} close={close_price:.2f}')
            if self.position.size >= 0:
                self.sell(size=size)

    def notify_trade(self, trade):
        if trade.isopen and not self._position_was_open:
            if trade.size > 0:
                self.buy_count += 1
            elif trade.size < 0:
                self.sell_count += 1
            self._position_was_open = True
            return
        if not trade.isclosed:
            return
        self.trade_count += 1
        if trade.pnlcomm >= 0:
            self.win_count += 1
        else:
            self.loss_count += 1
        self._position_was_open = False
        self.log(f'trade closed pnl={trade.pnlcomm:.2f}')



