def resolve_ma_class(name):
    """Map a moving-average name to its backtrader indicator class.

    Args:
        name: MA type name (e.g. ``t3``, ``ema``, ``sma``, ``smma`` or MT5-style
            ``mode_*`` variants); several smoothing variants map to bt.indicators.EMA.

    Returns:
        The matching backtrader moving-average indicator class, defaulting to
        the weighted moving average for unrecognized names.
    """
    mode = str(name).lower()
    if mode in {'mode_t3', 't3', 'mode_ema', 'ema', 'mode_ama', 'ama', 'mode_jjma', 'jjma', 'mode_jurx', 'jurx', 'mode_vidya', 'vidya', 'mode_parma', 'parma'}:
        return bt.indicators.ExponentialMovingAverage
    if mode in {'mode_sma', 'sma'}:
        return bt.indicators.SimpleMovingAverage
    if mode in {'mode_smma', 'smma'}:
        return bt.indicators.SmoothedMovingAverage
    return bt.indicators.WeightedMovingAverage


class ColorXADXStrategy(bt.Strategy):
    """Port of the MT5 ``Exp_ColorXADX`` smoothed-ADX crossover strategy.

    Trades +DI/-DI crossovers confirmed by an ADX strength filter: a +DI cross
    below -DI with strong ADX is a long signal and the opposite cross is a short
    signal, entering on the cross and reversing on the opposite signal with a
    fixed lot.

    Args:
        xma_method: Smoothing moving-average type for the DI/ADX lines.
        adx_period: Period for the ADX/DI computation.
        adx_phase: Phase parameter retained from the source EA (unused here).
        extra_high_level: ADX threshold that must be exceeded to allow entries.
        signal_bar: Which completed bar to evaluate signals on.
        lot: Fixed order size in lots.
    """

    params = dict(
        xma_method='t3',
        adx_period=14,
        adx_phase=100,
        extra_high_level=30,
        signal_bar=1,
        lot=0.1,
    )

    def __init__(self):
        """Build the SmoothedADXIndicator and reset bar/trade counters."""
        self.indicator = bt.indicators.SmoothedADXIndicator(
            self.data,
            xma_method=self.p.xma_method,
            adx_period=self.p.adx_period,
            adx_phase=self.p.adx_phase,
        )
        self.bar_num = 0
        self.buy_count = 0
        self.sell_count = 0
        self.trade_count = 0
        self.win_count = 0
        self.loss_count = 0
        self._position_was_open = False

    def log(self, text):
        """Print a timestamped log line for the current bar.

        Args:
            text: The message to log alongside the current bar datetime.
        """
        dt = bt.num2date(self.data.datetime[0])
        print(f'{dt.isoformat()}, {text}')

    def _signals(self):
        shift = max(1, int(self.p.signal_bar))
        mplus_prev = float(self.indicator.plus_di[-shift])
        mminus_prev = float(self.indicator.minus_di[-shift])
        mplus_cur = float(self.indicator.plus_di[-shift + 1]) if shift > 1 else float(self.indicator.plus_di[0])
        mminus_cur = float(self.indicator.minus_di[-shift + 1]) if shift > 1 else float(self.indicator.minus_di[0])
        adx_filter = float(self.indicator.adx[-shift + 1]) if shift > 1 else float(self.indicator.adx[0])
        buy_open = False
        sell_open = False
        buy_close = False
        sell_close = False
        if mplus_prev > mminus_prev:
            if mplus_cur <= mminus_cur and adx_filter > float(self.p.extra_high_level):
                buy_open = True
            sell_close = True
        if mplus_prev < mminus_prev:
            if mplus_cur >= mminus_cur and adx_filter > float(self.p.extra_high_level):
                sell_open = True
            buy_close = True
        return buy_open, sell_open, buy_close, sell_close

    def next(self):
        """Open, close, or reverse on smoothed DI-cross signals each bar.

        Increments the bar counter, skips during warm-up, derives open/close
        signals from the smoothed +DI/-DI crossover with the ADX strength filter
        at the configured signal bar, and acts: when flat it opens a long or
        short; when in a position it closes on the close signal or closes and
        reverses on the opposite open signal.
        """
        self.bar_num += 1
        warmup = int(self.p.adx_period) * 3 + int(self.p.signal_bar) + 5
        if len(self.data) < warmup:
            return
        buy_open, sell_open, buy_close, sell_close = self._signals()
        adx_value = float(self.indicator.adx[0])
        if self.position:
            if self.position.size > 0:
                if buy_close and not sell_open:
                    self.log(f'close long adx={adx_value:.4f}')
                    self.close()
                    return
                if sell_open:
                    self.log(f'close long & sell adx={adx_value:.4f}')
                    self.close()
                    self.sell(size=self.p.lot)
                    return
            if self.position.size < 0:
                if sell_close and not buy_open:
                    self.log(f'close short adx={adx_value:.4f}')
                    self.close()
                    return
                if buy_open:
                    self.log(f'close short & buy adx={adx_value:.4f}')
                    self.close()
                    self.buy(size=self.p.lot)
                    return
        else:
            if buy_open:
                self.log(f'buy adx={adx_value:.4f}')
                self.buy(size=self.p.lot)
                return
            if sell_open:
                self.log(f'sell adx={adx_value:.4f}')
                self.sell(size=self.p.lot)
                return

    def notify_trade(self, trade):
        """Count entries when a trade opens and win/loss when it closes.

        Args:
            trade: The trade whose status changed; opening increments the
                buy/sell entry counter and closing updates win/loss counts.
        """
        if trade.isopen and not self._position_was_open:
            if trade.size > 0:
                self.buy_count += 1
            elif trade.size < 0:
                self.sell_count += 1
            self._position_was_open = True
            return
        if not trade.isclosed:
            return
        self.trade_count += 1
        if trade.pnlcomm >= 0:
            self.win_count += 1
        else:
            self.loss_count += 1
        self._position_was_open = False
        self.log(f'trade closed pnl={trade.pnlcomm:.2f}')


