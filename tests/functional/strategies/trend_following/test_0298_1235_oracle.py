def load_backtest_frame(config):
    """Load and slice the OHLCV frame for the Oracle strategy.

    Args:
        config: Strategy configuration containing data bounds and source file.

    Returns:
        Dictionary with loaded frame and date window boundaries.
    """
    data_cfg = config['data']
    fromdate = datetime.datetime.fromisoformat(data_cfg['fromdate'])
