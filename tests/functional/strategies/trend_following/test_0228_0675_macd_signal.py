class MacdSignalStrategy(bt.Strategy):
    """Strategy class implementing ATR-guided MACD breakout signals.

    Attributes:
        params (dict): Configured strategy parameters.
    """
    params = dict(
        take_profit=10,
        lots=0.01,
        trailing_stop=25,
        pfast=9,
        pslow=15,
        psignal=8,
        level=0.004,
        point=0.01,
        digits_adjust=1,
        price_digits=2,
    )

    def __init__(self):
        """Initialize indicators, backtest tracking metrics, and state variables."""
        self.atr = bt.indicators.ATR(self.data, period=200)
        self.macd = bt.indicators.MACD(self.data.close, period_me1=self.p.pfast, period_me2=self.p.pslow, period_signal=self.p.psignal)

        self.bar_num = 0
        self.signal_count = 0
        self.buy_count = 0
        self.sell_count = 0
        self.trade_count = 0
        self.win_count = 0
        self.loss_count = 0
        self.completed_order_count = 0
        self.rejected_order_count = 0

        self.order = None
        self.stop_price = None
        self.take_profit_price = None

    def _point_value(self):
        return float(self.p.point) * float(self.p.digits_adjust)

    def _delta(self, idx):
        return float(self.macd.macd[idx]) - float(self.macd.signal[idx])

    def _entry_signal(self):
        rr = float(self.atr[-1]) * float(self.p.level)
        delta = self._delta(0)
        delta1 = self._delta(-1)
        if delta > rr and delta1 < rr:
            return 1
        if delta < -rr and delta1 > -rr:
            return -1
        return 0

    def _set_risk(self, side, price):
        tp_distance = float(self.p.take_profit) * self._point_value()
        if side == 'buy':
            self.stop_price = None
            self.take_profit_price = round(price + tp_distance, int(self.p.price_digits))
        else:
            self.stop_price = None
            self.take_profit_price = round(price - tp_distance, int(self.p.price_digits))

    def _manage_position(self):
        if not self.position or self.order is not None:
            return
        delta = self._delta(0)
        high = float(self.data.high[0])
        low = float(self.data.low[0])
        close = float(self.data.close[0])
        trailing_distance = float(self.p.trailing_stop) * self._point_value()
        if self.position.size > 0:
            if delta < 0:
                self.order = self.close()
                return
            if self.take_profit_price is not None and high >= float(self.take_profit_price):
                self.order = self.close()
                return
            if float(self.p.trailing_stop) > 0 and close - float(self.position.price) > trailing_distance:
                candidate_sl = round(close - trailing_distance, int(self.p.price_digits))
                if self.stop_price is None or candidate_sl > float(self.stop_price):
                    self.stop_price = candidate_sl
            if self.stop_price is not None and low <= float(self.stop_price):
                self.order = self.close()
                return
        else:
            if delta > 0:
                self.order = self.close()
                return
            if self.take_profit_price is not None and low <= float(self.take_profit_price):
                self.order = self.close()
                return
            if float(self.p.trailing_stop) > 0 and float(self.position.price) - close > trailing_distance:
                candidate_sl = round(close + trailing_distance, int(self.p.price_digits))
                if self.stop_price is None or candidate_sl < float(self.stop_price):
                    self.stop_price = candidate_sl
            if self.stop_price is not None and high >= float(self.stop_price):
                self.order = self.close()
                return

    def next(self):
        """Execute the strategy decision logic on each new bar."""
        self.bar_num += 1
        if len(self) < 205:
            return
        if self.order is not None:
            return
        if self.position:
            self._manage_position()
            return
        signal = self._entry_signal()
        if signal == 0:
            return
        self.signal_count += 1
        price = float(self.data.close[0])
        if signal > 0:
            self._set_risk('buy', price)
            self.order = self.buy(size=self.p.lots)
        else:
            self._set_risk('sell', price)
            self.order = self.sell(size=self.p.lots)

    def notify_order(self, order):
        """Callback to handle order status updates.

        Args:
            order (bt.Order): The updated order instance.
        """
        if order.status in [bt.Order.Submitted, bt.Order.Accepted]:
            return
        if order.status == bt.Order.Completed:
            self.completed_order_count += 1
            if self.position:
                if order.executed.size > 0:
                    self.buy_count += 1
                elif order.executed.size < 0:
                    self.sell_count += 1
            else:
                self.stop_price = None
                self.take_profit_price = None
        elif order.status in [bt.Order.Canceled, bt.Order.Margin, bt.Order.Rejected, bt.Order.Expired]:
            self.rejected_order_count += 1
        if self.order is not None and order.ref == self.order.ref and order.status not in [bt.Order.Submitted, bt.Order.Accepted]:
            self.order = None

    def notify_trade(self, trade):
        """Callback to handle closed trades and manage win/loss counts.

        Args:
            trade (bt.Trade): The closed trade instance.
        """
        if not trade.isclosed:
            return
        self.trade_count += 1
        if trade.pnlcomm >= 0:
            self.win_count += 1
        else:
            self.loss_count += 1


