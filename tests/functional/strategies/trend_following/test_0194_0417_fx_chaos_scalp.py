def build_cerebro(config, frame):
    """Build Cerebro with three timeframe feeds and the target strategy.

    Args:
        config: Strategy/backtest configuration.
        frame: Dict containing base/h1/d1 frames.

    Returns:
        Ready-to-run Cerebro instance.
    """
    bt_cfg = config['backtest']
    data_cfg = config['data']
    cerebro = bt.Cerebro(stdstats=True)
    cerebro.broker.setcash(bt_cfg['initial_cash'])
    comm_type = bt.CommInfoBase.COMM_FIXED if bt_cfg.get('commission_type', 'fixed') == 'fixed' else bt.CommInfoBase.COMM_PERC
    cerebro.broker.setcommission(
        commission=bt_cfg['commission'],
        margin=bt_cfg['margin'],
        mult=bt_cfg['multiplier'],
        commtype=comm_type,
        stocklike=bt_cfg.get('stocklike', False),
    )
    base_feed = Mt5PandasFeed(dataname=frame['base'], timeframe=bt.TimeFrame.Minutes, compression=data_cfg.get('base_compression_minutes', 15))
    h1_feed = Mt5PandasFeed(dataname=frame['h1'], timeframe=bt.TimeFrame.Minutes, compression=data_cfg.get('h1_compression_minutes', 60))
    d1_feed = Mt5PandasFeed(dataname=frame['d1'], timeframe=bt.TimeFrame.Minutes, compression=data_cfg.get('d1_compression_minutes', 1440))
    cerebro.adddata(base_feed, name=f"{data_cfg['symbol']}_{data_cfg['base_timeframe']}")
    cerebro.adddata(h1_feed, name=f"{data_cfg['symbol']}_{data_cfg['h1_timeframe']}")
    cerebro.adddata(d1_feed, name=f"{data_cfg['symbol']}_{data_cfg['d1_timeframe']}")
    cerebro.addstrategy(FxChaosScalpStrategy, **config.get('params', {}))
    add_default_analyzers(cerebro)
    return cerebro


def finite_or_none(value):
    """Convert non-finite numeric values to ``None``.

    Args:
        value: Number-like value.

    Returns:
        Original value if finite, else None.
    """
    if value is None:
        return None
    if isinstance(value, (int, float)) and not math.isfinite(value):
        return None
    return value


def summarize(results, start_value):
    """Summarize analyzer outputs and print key metrics.

    Args:
        results: Result list returned by ``cerebro.run``.
        start_value: Starting portfolio value before execution.
    """
    strat = results[0]
    end_value = strat.broker.getvalue()
    drawdown = strat.analyzers.drawdown.get_analysis()
    returns = strat.analyzers.returns.get_analysis()
    sharpe = strat.analyzers.sharpe.get_analysis()
    sqn = strat.analyzers.sqn.get_analysis()
    trades = strat.analyzers.trades.get_analysis()
    total_closed = trades.get('total', {}).get('closed', 0)
    won = trades.get('won', {}).get('total', 0)
    lost = trades.get('lost', {}).get('total', 0)
    strike_rate = (won / total_closed * 100.0) if total_closed else 0.0
    print('\n# Backtest Summary')
    print(f'- Start Value: {start_value:.2f}')
    print(f'- End Value: {end_value:.2f}')
    print(f'- Net PnL: {end_value - start_value:.2f}')
    print(f'- Max Drawdown: {drawdown.get("max", {}).get("drawdown", 0.0):.2f}%')
    print(f'- Total Closed Trades: {total_closed}')
    print(f'- Won Trades: {won}')
    print(f'- Lost Trades: {lost}')
    print(f'- Strike Rate: {strike_rate:.2f}%')
    print(f'- Buy Entries: {strat.buy_count}')
    print(f'- Sell Entries: {strat.sell_count}')
    print(f'- Sharpe: {finite_or_none(sharpe.get("sharperatio"))}')
    print(f'- SQN: {finite_or_none(sqn.get("sqn"))}')
    print(f'- Total Return: {returns.get("rtot", 0.0):.6f}')


