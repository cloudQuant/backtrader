"""Inlined regression test for the Exp_MACD_Xtr (MACD_Xtr) trend strategy.

Auto-generated by scripts/inline_regression_tests.py — do not edit manually.
The original config.yaml, run.py, strategy_*.py, and expected.json have been
collapsed into this single self-contained file.

Runs with runonce=True only (no parametrization).
Asserts directly on the strategy's own extract_metrics() output captured at
migration time.

Data Used:
    Symbol XAUUSD (gold spot) on the M15 base timeframe, loaded from the MT5
    export ``tests/datas/XAUUSD_M15.csv`` with each bar timestamp shifted forward
    by 15 minutes. The backtest window runs from 2025-12-03 01:15:00 to
    2026-03-10 09:00:00. Two data feeds drive the strategy: the raw M15 series
    used for execution and risk management, and a 240-minute (H4) signal series
    resampled from the M15 bars and enriched with the MACD_Xtr indicator lines
    (``state``, ``macd``, ``level``).

Strategy Principle:
    MACD_Xtr augments a standard fast/slow EMA MACD with an adaptive volatility
    band. A volatility proxy (volume EMA, Wilder ATR, or rolling standard
    deviation depending on ``source``) is double-smoothed into a dynamic
    ``level``, and the MACD value is classified into a three-color ``state``:
    bullish when MACD rises above the band, bearish when it falls below the
    negative band, and neutral inside the band. Fresh transitions into the
    bullish or bearish state act as entry signals while a return to neutral
    flattens positions, capturing trend bursts that clear the volatility band.

Strategy Logic:
    The M15 feed advances the strategy bar by bar while the resampled H4 feed
    supplies the precomputed ``state`` line. Each bar first checks fixed
    stop-loss/take-profit distances against the current close, then waits for a
    new H4 signal bar. A bullish-state onset buys (after closing any short), a
    bearish-state onset sells (after closing any long), and a neutral state
    closes open positions per the close flags, with position size derived from
    the ``mm`` money-management parameter. Trade notifications track first-open
    direction counts and closed win/loss tallies. After ``cerebro.run``
    completes, ``extract_metrics`` collects analyzer output (Sharpe, returns,
    drawdown, trade stats, SQN) and the test asserts each metric against
    migration-time expected values.
"""
