class TwoPbIdealXOSMAStrategy(bt.Strategy):
    """Trade 2pbIdealXOSMA histogram turning points with reversal handling."""

    params = dict(
        signal_bar=1,
        period1=10,
        period2=10,
        periodx1=10,
        periodx2=10,
        periody1=10,
        periody2=10,
        periodz1=10,
        periodz2=10,
        smooth_method='jjma',
        smooth_period=9,
        smooth_phase=100,
        lot=0.1,
        point=0.01,
        price_digits=2,
    )

    def __init__(self):
        """Attach the 2pbIdealXOSMA indicator and reset counters/state."""
        self.signal = bt.indicators.TwoPbIdealXOSMAIndicator(
            self.data,
            period1=self.p.period1,
            period2=self.p.period2,
            periodx1=self.p.periodx1,
            periodx2=self.p.periodx2,
            periody1=self.p.periody1,
            periody2=self.p.periody2,
            periodz1=self.p.periodz1,
            periodz2=self.p.periodz2,
            smooth_method=self.p.smooth_method,
            smooth_period=self.p.smooth_period,
            smooth_phase=self.p.smooth_phase,
        )
        self.bar_num = 0
        self.buy_count = 0
        self.sell_count = 0
        self.trade_count = 0
        self.win_count = 0
        self.loss_count = 0
        self._position_was_open = False

    def log(self, text):
        """Emit a timestamped strategy log message."""
        dt = bt.num2date(self.data.datetime[0])
        print(f'{dt.isoformat()}, {text}')

    def _turning_values(self):
        offset = max(1, int(self.p.signal_bar))
        return (
            float(self.signal.signal[-offset - 2]),
            float(self.signal.signal[-offset - 1]),
            float(self.signal.signal[-offset]),
        )

    def next(self):
        """Detect histogram turning points and open/close/reverse positions."""
        self.bar_num += 1
        min_bars = max(
            int(self.p.period1),
            int(self.p.period2),
            int(self.p.periodx1),
            int(self.p.periodx2),
            int(self.p.periody1),
            int(self.p.periody2),
            int(self.p.periodz1),
            int(self.p.periodz2),
            int(self.p.smooth_period),
        ) + int(self.p.signal_bar) + 5
        if len(self.data) < min_bars:
            return

        left_value, middle_value, right_value = self._turning_values()
        buy_signal = middle_value < left_value and right_value > middle_value
        sell_signal = middle_value > left_value and right_value < middle_value

        if self.position:
            if self.position.size > 0 and sell_signal:
                self.log(f'close long & sell signal={middle_value:.4f} latest={right_value:.4f}')
                self.close()
                self.sell(size=self.p.lot)
                return
            if self.position.size < 0 and buy_signal:
                self.log(f'close short & buy signal={middle_value:.4f} latest={right_value:.4f}')
                self.close()
                self.buy(size=self.p.lot)
                return
        else:
            if buy_signal:
                self.log(f'buy signal={middle_value:.4f} latest={right_value:.4f}')
                self.buy(size=self.p.lot)
                return
            if sell_signal:
                self.log(f'sell signal={middle_value:.4f} latest={right_value:.4f}')
                self.sell(size=self.p.lot)
                return

    def notify_trade(self, trade):
        """Count entries by direction and tally wins/losses on close."""
        if trade.isopen and not self._position_was_open:
            if trade.size > 0:
                self.buy_count += 1
            elif trade.size < 0:
                self.sell_count += 1
            self._position_was_open = True
            return
        if not trade.isclosed:
            return
        self.trade_count += 1
        if trade.pnlcomm >= 0:
            self.win_count += 1
        else:
            self.loss_count += 1
        self._position_was_open = False
        self.log(f'trade closed pnl={trade.pnlcomm:.2f}')


