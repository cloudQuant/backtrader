class MAMACDStrategy(bt.Strategy):
    params = dict(
        ma1=85,
        ma2=75,
        ma3=5,
        fastema=15,
        slowema=26,
        lots=0.1,
        stop_loss=15,
        take_profit=15,
        point=0.01,
        price_digits=2,
    )

    def __init__(self):
        self.ma_1 = bt.ind.WeightedMovingAverage(self.data.low, period=int(self.p.ma1))
        self.ma_2 = bt.ind.WeightedMovingAverage(self.data.low, period=int(self.p.ma2))
        self.ma_3 = bt.ind.ExponentialMovingAverage(self.data.close, period=int(self.p.ma3))
        self.macd = bt.ind.MACD(self.data.close, period_me1=int(self.p.fastema), period_me2=int(self.p.slowema), period_signal=1)
        self.bar_num = 0
        self.signal_count = 0
        self.buy_count = 0
        self.sell_count = 0
        self.trade_count = 0
        self.win_count = 0
        self.loss_count = 0
        self.completed_order_count = 0
        self.rejected_order_count = 0
        self.order = None
        self.stop_price = None
        self.take_profit_price = None
        self.startb = 0
        self.starts = 0

    def _point(self):
        return float(self.p.point)

    def _round(self, value):
        return round(float(value), int(self.p.price_digits))

    def _manage(self):
        if not self.position or self.order is not None:
            return
        high = float(self.data.high[0])
        low = float(self.data.low[0])
        if self.position.size > 0:
            if self.take_profit_price is not None and high >= float(self.take_profit_price):
                self.order = self.close(); return
            if self.stop_price is not None and low <= float(self.stop_price):
                self.order = self.close(); return
        else:
            if self.take_profit_price is not None and low <= float(self.take_profit_price):
                self.order = self.close(); return
            if self.stop_price is not None and high >= float(self.stop_price):
                self.order = self.close(); return

    def next(self):
        self.bar_num += 1
        if len(self) < max(int(self.p.ma1), int(self.p.ma2), int(self.p.ma3)) + 5 or self.order is not None:
            return
        if self.position:
            self._manage()
            return
        ma1 = float(self.ma_1[-1])
        ma2 = float(self.ma_2[-1])
        ma3 = float(self.ma_3[-1])
        macdcurr = float(self.macd.macd[-1])
        macdlast = float(self.macd.macd[-2])
        if ma3 < ma1 and ma3 < ma2:
            self.startb = 1
        if ma3 > ma1 and ma3 > ma2:
            self.starts = 1
        if ma3 > ma1 and ma3 > ma2 and self.startb == 1:
            if (macdcurr > 0 or macdcurr > macdlast):
                price = float(self.data.close[0])
                sl_dist = float(self.p.stop_loss) * self._point()
                tp_dist = float(self.p.take_profit) * self._point()
                self.stop_price = self._round(price - sl_dist) if sl_dist > 0 else None
                self.take_profit_price = self._round(price + tp_dist) if tp_dist > 0 else None
                self.signal_count += 1
                self.order = self.buy(size=float(self.p.lots))
                self.startb = 0
                return
        if ma3 < ma1 and ma3 < ma2 and self.starts == 1:
            if (macdcurr < 0 or macdcurr < macdlast):
                price = float(self.data.close[0])
                sl_dist = float(self.p.stop_loss) * self._point()
                tp_dist = float(self.p.take_profit) * self._point()
                self.stop_price = self._round(price + sl_dist) if sl_dist > 0 else None
                self.take_profit_price = self._round(price - tp_dist) if tp_dist > 0 else None
                self.signal_count += 1
                self.order = self.sell(size=float(self.p.lots))
                self.starts = 0

    def notify_order(self, order):
        if order.status in [bt.Order.Submitted, bt.Order.Accepted]:
            return
        if order.status == bt.Order.Completed:
            self.completed_order_count += 1
            if self.position:
                if order.executed.size > 0:
                    self.buy_count += 1
                elif order.executed.size < 0:
                    self.sell_count += 1
            else:
                self.stop_price = None
                self.take_profit_price = None
        elif order.status in [bt.Order.Canceled, bt.Order.Margin, bt.Order.Rejected, bt.Order.Expired]:
            self.rejected_order_count += 1
        if self.order is not None and order.ref == self.order.ref and order.status not in [bt.Order.Submitted, bt.Order.Accepted]:
            self.order = None

    def notify_trade(self, trade):
        if not trade.isclosed:
            return
        self.trade_count += 1
        if trade.pnlcomm >= 0:
            self.win_count += 1
        else:
            self.loss_count += 1



