def load_backtest_frame(config):
    """Load and trim the configured M15 CSV into a single-data backtest frame.

    Args:
        config: Resolved config mapping whose ``data`` section names the file,
            date window and bar-shift settings.

    Returns:
        dict: Mapping with the ``data`` frame and resolved ``fromdate`` / ``todate``.

    Raises:
        ValueError: If the loaded frame is empty after date clipping.
    """
    data_cfg = config['data']
    fromdate = datetime.datetime.fromisoformat(data_cfg['fromdate'])
    todate = datetime.datetime.fromisoformat(data_cfg['todate'])
    df = load_mt5_csv(
        resolve_data_path(data_cfg['file']),
        fromdate=fromdate,
        todate=todate,
        bar_shift_minutes=data_cfg.get('bar_shift_minutes', 0),
    )
    if df.empty:
        raise ValueError('Loaded data frame is empty')
    print(f"Loaded {len(df)} bars: {df.index[0]} -> {df.index[-1]}")
    return {'data': df, 'fromdate': fromdate, 'todate': todate}


def build_signal_frame(df, indicator_minutes):
    """Resample the base OHLCV frame to the indicator timeframe.

    Args:
        df: M15 OHLCV frame indexed by datetime.
        indicator_minutes: Target resample period in minutes (the H1 timeframe).

    Returns:
        pandas.DataFrame: The resampled OHLCV frame with incomplete rows dropped.
    """
    rule = f'{int(indicator_minutes)}min'
    signal_df = df.resample(rule, label='right', closed='right').agg({
        'open': 'first',
        'high': 'max',
        'low': 'min',
        'close': 'last',
        'volume': 'sum',
        'openinterest': 'last',
    })
    signal_df = signal_df.dropna(subset=['open', 'high', 'low', 'close'])
    if 'openinterest' in signal_df.columns:
        signal_df['openinterest'] = signal_df['openinterest'].fillna(0)
    return signal_df


def build_cerebro(config, frame):
    """Assemble a cerebro with the broker, base+signal feeds, strategy and analyzers.

    Args:
        config: Resolved config mapping providing ``backtest``, ``data`` and ``params``.
        frame: Frame mapping returned by :func:`load_backtest_frame`.

    Returns:
        bt.Cerebro: The fully wired engine ready to run.

    Raises:
        ValueError: If the resampled signal frame is empty.
    """
    bt_cfg = config['backtest']
    params = config.get('params', {})
    indicator_minutes = params.get('indicator_minutes', 60)
    signal_frame = build_signal_frame(frame['data'], indicator_minutes)
    if signal_frame.empty:
        raise ValueError('Signal frame is empty after resampling')

    cerebro = bt.Cerebro(stdstats=True)
    cerebro.broker.setcash(bt_cfg['initial_cash'])
    comm_type = bt.CommInfoBase.COMM_FIXED if bt_cfg.get('commission_type', 'fixed') == 'fixed' else bt.CommInfoBase.COMM_PERC
    cerebro.broker.setcommission(
        commission=bt_cfg['commission'],
        margin=bt_cfg['margin'],
        mult=bt_cfg['multiplier'],
        commtype=comm_type,
        stocklike=bt_cfg.get('stocklike', False),
    )

    base_feed = Mt5PandasFeed(dataname=frame['data'], timeframe=bt.TimeFrame.Minutes, compression=15)
    signal_feed = Mt5PandasFeed(dataname=signal_frame, timeframe=bt.TimeFrame.Minutes, compression=indicator_minutes)
    cerebro.adddata(base_feed, name=f"{config['data']['symbol']}_{config['data']['timeframe']}")
    cerebro.adddata(signal_feed, name=f"{config['data']['symbol']}_{config['data']['indicator_timeframe']}")

    strategy_params = dict(params)
    strategy_params.pop('indicator_minutes', None)
    cerebro.addstrategy(ExpASCtrendStrategy, **strategy_params)
    cerebro.addanalyzer(bt.analyzers.SharpeRatio, _name='sharpe', timeframe=bt.TimeFrame.Minutes, factor=MINUTES_PER_TRADING_YEAR, annualize=True, riskfreerate=0)
    cerebro.addanalyzer(bt.analyzers.Returns, _name='returns', timeframe=bt.TimeFrame.Minutes, compression=15, tann=MINUTES_PER_TRADING_YEAR)
    cerebro.addanalyzer(bt.analyzers.DrawDown, _name='drawdown')
    cerebro.addanalyzer(bt.analyzers.TradeAnalyzer, _name='trades')
    cerebro.addanalyzer(bt.analyzers.SQN, _name='sqn')
    return cerebro


def extract_metrics(strat, cerebro, frame, config):
    """Collect summary metrics from the strategy, broker and analyzers.

    Args:
        strat: The executed strategy instance carrying counters.
        cerebro: The cerebro whose broker holds the final value.
        frame: Frame mapping returned by :func:`load_backtest_frame`.
        config: Resolved config mapping for cash references.

    Returns:
        dict: Trade counts, equity figures and risk/return ratios keyed for the
        regression assertions.
    """
    sharpe = strat.analyzers.sharpe.get_analysis()
    returns = strat.analyzers.returns.get_analysis()
    drawdown = strat.analyzers.drawdown.get_analysis()
    trades = strat.analyzers.trades.get_analysis()
    sqn = strat.analyzers.sqn.get_analysis()
    initial_cash = config['backtest']['initial_cash']
    final_value = cerebro.broker.getvalue()
    total_trades = trades.get('total', {}).get('total', 0)
    won = trades.get('won', {}).get('total', 0)
    lost = trades.get('lost', {}).get('total', 0)
    gross_won = trades.get('won', {}).get('pnl', {}).get('total', 0) or 0
    gross_lost = abs(trades.get('lost', {}).get('pnl', {}).get('total', 0) or 0)
    return {
        'fromdate': frame['fromdate'],
        'todate': frame['todate'],
        'bars': len(frame['data']),
        'bar_num': strat.bar_num,
        'signal_count': strat.signal_count,
        'buy_count': strat.buy_count,
        'sell_count': strat.sell_count,
        'trade_count': strat.trade_count,
        'win_count': strat.win_count,
        'loss_count': strat.loss_count,
        'initial_cash': initial_cash,
        'final_value': final_value,
        'net_pnl': final_value - initial_cash,
        'total_return_pct': (final_value / initial_cash - 1) * 100,
        'total_trades': total_trades,
        'won': won,
        'lost': lost,
        'win_rate': (won / total_trades * 100) if total_trades else 0,
        'profit_factor': (gross_won / gross_lost) if gross_lost else None,
        'max_drawdown': drawdown.get('max', {}).get('drawdown', 0),
        'sharpe_ratio': sharpe.get('sharperatio'),
        'annual_return_pct': (returns.get('rnorm') or 0) * 100,
        'sqn': sqn.get('sqn'),
    }


def run(plot=False):
    """Execute the full backtest and return results, metrics and the engine.

    Args:
        plot: When True, render the cerebro chart after the run.

    Returns:
        tuple: ``(results, metrics, cerebro)`` from the completed backtest.
    """
    config = _bt_load_config(_CONFIG, repo=_REPO)
    frame = load_backtest_frame(config)
    cerebro = build_cerebro(config, frame)
    print('\nStarting backtest...')
    results = cerebro.run()
    strat = results[0]
    metrics = extract_metrics(strat, cerebro, frame, config)

    if plot:
        cerebro.plot()
    return results, metrics, cerebro


def _close(actual, expected, *, tol, key):
    """Assert ``actual`` is finite and within ``tol`` of ``expected``."""
    assert actual is not None, f"{key}: expected={expected}, got=None"
    a = float(actual)
    assert math.isfinite(a), f"{key}: expected={expected}, got non-finite {actual}"
    assert abs(a - float(expected)) <= tol, (
        f"{key}: expected={expected}, got={a} (tol={tol})"
    )


def _invoke_strategy_main():
    """Call main() or run() depending on what the original script defined."""
    import sys as _sys
    _mod = _sys.modules[__name__]
    if hasattr(_mod, "main") and callable(_mod.main):
        return _mod.main()
    if hasattr(_mod, "run") and callable(_mod.run):
        return _mod.run()
    raise RuntimeError("Neither main() nor run() found in inlined module")


