def calculate_ulcer_index(values):
    """Calculate the Ulcer Index (UI) performance metric for account equity.

    Args:
        values (list): Chronological list of account equity values.

    Returns:
        float: Calculated Ulcer Index value.
    """
    if len(values) < 2:
        return 0.0
    max_value = values[0]
    sum_squared = 0.0
    for v in values:
        if v > max_value:
            max_value = v
        drawdown = (max_value - v) / max_value * 100.0 if max_value > 0 else 0.0
        sum_squared += drawdown ** 2
    return math.sqrt(sum_squared / len(values))



