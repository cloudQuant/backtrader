def finite_or_none(value):
    return value if value is not None and math.isfinite(value) else None


def calculate_ulcer_index(values):
    if len(values) < 2:
        return 0.0
    peak = values[0]
    squared = 0.0
    for value in values:
        peak = max(peak, value)
        drawdown = (peak - value) / peak * 100.0 if peak > 0 else 0.0
        squared += drawdown ** 2
    return math.sqrt(squared / len(values))



def build_weight_lookup(returns_df, params):
    lookback = int(params.get('lookback_days', 63))
    weight_lookup = {}
    for idx in range(lookback, len(returns_df)):
        date = pd.Timestamp(returns_df.index[idx]).tz_localize(None)
        window = returns_df.iloc[idx - lookback:idx]
        weight_lookup[date] = _optimize_weights(window, params)
    return weight_lookup


