def finite_or_none(value):
    """Return the value if it is a finite number, otherwise None.

    Args:
        value: The numeric value to validate.

    Returns:
        The original value when finite, else None.
    """
    return value if value is not None and math.isfinite(value) else None


def calculate_ulcer_index(values):
    """Compute the Ulcer Index of an equity-curve value series.

    Args:
        values: Sequence of portfolio values in chronological order.

    Returns:
        The Ulcer Index (root-mean-square percentage drawdown from running
        peaks); 0.0 when fewer than two values are supplied.
    """
    if len(values) < 2:
        return 0.0
    peak = values[0]
    squared = 0.0
    for value in values:
        peak = max(peak, value)
        drawdown = (peak - value) / peak * 100.0 if peak > 0 else 0.0
        squared += drawdown ** 2
    return math.sqrt(squared / len(values))



def load_backtest_frame(config):
    """Load the daily asset feeds and prepare the monthly TAA signal data.

    Args:
        config: Parsed configuration whose ``data`` section provides the asset
            file paths and inclusive date bounds.

    Returns:
        A dict with the monthly ``signal_df``, the aligned ``monthly_frames``,
        the combined ``monthly_summary``, and the effective ``fromdate`` and
        ``todate``.

    Raises:
        ValueError: If the prepared signal frame is empty.
    """
    data_cfg = config['data']
    fromdate = datetime.fromisoformat(data_cfg['fromdate'])
    todate = datetime.fromisoformat(data_cfg['todate'])
    asset_daily_frames = {}
    for name in ASSET_NAMES:
        asset_daily_frames[name] = load_mt5_csv(data_cfg['assets'][name], fromdate=fromdate, todate=todate)
    signal_df, monthly_frames, monthly_summary = prepare_taa_data(asset_daily_frames, config.get('params', {}))
    if signal_df.empty:
        raise ValueError('Loaded data frame is empty after feature preparation')
    return {'signal_df': signal_df, 'monthly_frames': monthly_frames, 'monthly_summary': monthly_summary, 'fromdate': fromdate, 'todate': todate}




def build_cerebro(config, frame):
    """Assemble the Cerebro engine with the signal and asset feeds and analyzers.

    Args:
        config: Parsed configuration providing cash, commission, and strategy
            parameters.
        frame: The prepared frame dict returned by :func:`load_backtest_frame`.

    Returns:
        A configured :class:`backtrader.Cerebro` ready to run the backtest.
    """
    cerebro = bt.Cerebro(stdstats=True)
    cerebro.broker.setcash(float(config['backtest']['initial_cash']))
    cerebro.broker.setcommission(commission=float(config['params'].get('commission_pct', 0.0005)))
    signal_feed = TacticalAssetAllocationSignalFeed(dataname=frame['signal_df'], timeframe=bt.TimeFrame.Months, compression=1)
    cerebro.adddata(signal_feed, name='SIGNAL')
    for name in ASSET_NAMES:
        feed = bt.feeds.PandasData(dataname=frame['monthly_frames'][name], timeframe=bt.TimeFrame.Months, compression=1)
        cerebro.adddata(feed, name=name)
    cerebro.addstrategy(TacticalAssetAllocationStrategy, **config.get('params', {}))
    cerebro.addanalyzer(bt.analyzers.SharpeRatio, _name='sharpe', timeframe=bt.TimeFrame.Months, compression=1, factor=12, annualize=True, riskfreerate=0)
    cerebro.addanalyzer(bt.analyzers.Returns, _name='returns', timeframe=bt.TimeFrame.Months, tann=12)
    cerebro.addanalyzer(bt.analyzers.DrawDown, _name='drawdown')
    cerebro.addanalyzer(bt.analyzers.TradeAnalyzer, _name='trades')
    cerebro.addanalyzer(bt.analyzers.SQN, _name='sqn')

    return cerebro


def extract_metrics(strat, cerebro, frame, config):
    """Consolidate analyzer output and strategy counters into a metrics dict.

    Args:
        strat: The executed strategy instance holding counters, the broker value
            series, regime month counts, and attached analyzers.
        cerebro: The Cerebro engine used to read the final broker value.
        frame: The prepared frame dict providing date bounds and bar count.
        config: Parsed configuration supplying the strategy name and initial
            cash baseline.

    Returns:
        A dict of performance metrics (returns, win rate, profit factor,
        drawdown, Sharpe, SQN, Ulcer index, regime month counts, and trade/
        rebalance counts) used by the assertions.
    """
    trades = strat.analyzers.trades.get_analysis()
    sharpe = strat.analyzers.sharpe.get_analysis()
    returns = strat.analyzers.returns.get_analysis()
    drawdown = strat.analyzers.drawdown.get_analysis()
    sqn = strat.analyzers.sqn.get_analysis()
    initial_cash = float(config['backtest']['initial_cash'])
    final_value = float(cerebro.broker.getvalue())
    broker_values = [value for _, value in getattr(strat, 'broker_value_series', [])] or [initial_cash, final_value]
    total_trades = trades.get('total', {}).get('total', 0)
    won = trades.get('won', {}).get('total', 0)
    lost = trades.get('lost', {}).get('total', 0)
    gross_won = trades.get('won', {}).get('pnl', {}).get('total', 0) or 0
    gross_lost = abs(trades.get('lost', {}).get('pnl', {}).get('total', 0) or 0)
    return {
        'strategy_name': config['strategy']['name'],
        'fromdate': frame['fromdate'],
        'todate': frame['todate'],
        'bars': len(frame['signal_df']),
        'bar_num': strat.bar_num,
        'buy_count': strat.buy_count,
        'sell_count': strat.sell_count,
        'rebalance_count': strat.rebalance_count,
        'offensive_months': getattr(strat, 'offensive_months', 0),
        'defensive_months': getattr(strat, 'defensive_months', 0),
        'neutral_months': getattr(strat, 'neutral_months', 0),
        'initial_cash': initial_cash,
        'final_value': final_value,
        'net_pnl': final_value - initial_cash,
        'total_return_pct': (final_value / initial_cash - 1.0) * 100.0,
        'total_trades': total_trades,
        'won': won,
        'lost': lost,
        'win_rate': (won / total_trades * 100.0) if total_trades else 0.0,
        'profit_factor': (gross_won / gross_lost) if gross_lost else None,
        'max_drawdown': drawdown.get('max', {}).get('drawdown', 0.0),
        'sharpe_ratio': finite_or_none(sharpe.get('sharperatio')),
        'annual_return_pct': (returns.get('rnorm') or 0.0) * 100.0,
        'sqn': finite_or_none(sqn.get('sqn')),
        'ulcer_index': calculate_ulcer_index(broker_values),
    }


def normalize(value):
    """Convert datetimes and non-finite floats into JSON-safe values.

    Args:
        value: The value to normalize.

    Returns:
        An ISO-format string for datetimes, None for NaN/inf floats, and the
        value unchanged otherwise.
    """
    if isinstance(value, datetime):
        return value.isoformat()
    if isinstance(value, float) and (math.isnan(value) or math.isinf(value)):
        return None
    return value

    if plot:
        cerebro.plot()
    return metrics


def _close(actual, expected, *, tol, key):
    """Assert ``actual`` is finite and within ``tol`` of ``expected``."""
    assert actual is not None, f"{key}: expected={expected}, got=None"
    a = float(actual)
    assert math.isfinite(a), f"{key}: expected={expected}, got non-finite {actual}"
    assert abs(a - float(expected)) <= tol, (
        f"{key}: expected={expected}, got={a} (tol={tol})"
    )


def _resolve_loader():
    """Locate the data-loading helper (varies by strategy)."""
    for name in ("load_inputs", "load_data", "load_backtest_frame", "prepare_inputs", "prepare_data"):
        fn = globals().get(name)
        if callable(fn):
            return fn
    raise RuntimeError("No inputs loader found in inlined module")


def _build_cerebro_compat(inputs, config):
    """Call build_cerebro with whichever signature the original used."""
    import inspect
    sig = inspect.signature(build_cerebro)
    params = list(sig.parameters.keys())
    if params and params[0].lower() in ("config", "cfg", "configuration"):
        return build_cerebro(config, inputs)
    try:
        return build_cerebro(inputs, config)
    except TypeError:
        return build_cerebro(config, inputs)


def _extract_metrics_compat(strat, cerebro, inputs, config):
    """Call extract_metrics with whichever signature the original used."""
    for args in (
        (strat, cerebro, inputs, config),
        (strat, cerebro, config, inputs),
        (strat, cerebro, inputs),
        (strat, cerebro),
    ):
        try:
            return extract_metrics(*args)
        except TypeError:
            continue
    raise RuntimeError("extract_metrics failed for all argument orderings")


