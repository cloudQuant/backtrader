def prepare_open_to_open_taa_features(df, params):
    """Calculate moving average crossover and momentum signals, constructing a composite aggregate indicator.

    Args:
        df (pd.DataFrame): Input raw historical price DataFrame.
        params (dict): Configured strategy parameters.

    Returns:
        pd.DataFrame: Processed features DataFrame containing composite trend signals and scheduled rebalance flags.
    """
    lookback = int(params.get('lookback', 63))
    rebalance_days = int(params.get('rebalance_days', 21))

    out = df.copy()
    out['open_return'] = out['open'].pct_change(lookback)
    out['signal'] = (out['open_return'] > 0).astype(float)

    out['rebalance_flag'] = 0.0
    cnt = 0
    for i in range(len(out)):
        cnt += 1
        if cnt >= rebalance_days:
            out.iloc[i, out.columns.get_loc('rebalance_flag')] = 1.0
            cnt = 0

    out = out[['open', 'high', 'low', 'close', 'volume', 'openinterest',
               'open_return', 'signal', 'rebalance_flag']].copy()
    return out.dropna()


