class HierarchicalRiskParityStrategy(bt.Strategy):
    """Strategy class implementing Hierarchical Risk Parity dynamic asset allocation.

    Attributes:
        params (dict): Configured strategy parameters.
    """
    params = dict(
        vol_lookback=60,
        rebalance_days=21,
        lot_size=1.0,
        num_clusters=3,
    )

    def __init__(self):
        """Initialize tracking metrics, list series, and state variables."""
        self.bar_num = 0
        self.buy_count = 0
        self.sell_count = 0
        self.pending_order = None
        self.entry_bar = 0
        self.broker_value_series = []

    def _get_position_size(self, target_notional_pct=1.0, price=None):
        """Calculate the target position size based on target portfolio percentage.

        Args:
            target_notional_pct (float, optional): Portfolio allocation weight. Defaults to 1.0.
            price (float, optional): Reference asset price. Defaults to None.

        Returns:
            float: Calculated position size.
        """
        if target_notional_pct <= 0:
            return 0.0
        broker_value = float(self.broker.getvalue())
        execution_price = float(self.data.close[0] if price is None else price)
        if broker_value <= 0 or execution_price <= 0:
            return 0.0
        comminfo = self.broker.getcommissioninfo(self.data)
        multiplier = float(getattr(comminfo.p, 'mult', 1.0) or 1.0)
        if multiplier <= 0:
            return 0.0
        size = broker_value * float(target_notional_pct) / (execution_price * multiplier)
        return max(0.01, round(size, 2))

    def next(self):
        """Execute the strategy decision logic on each new bar."""
        self.bar_num += 1
        self.broker_value_series.append((bt.num2date(self.data.datetime[0]), float(self.broker.getvalue())))
        if self.pending_order is not None:
            return

        target_pct = float(self.data.position_pct[0])
        if not self.position:
            if target_pct > 0.1:
                self.buy_count += 1
                self.pending_order = self.buy(size=self._get_position_size(target_notional_pct=target_pct))
        else:
            current_pct = abs(float(self.position.size)) * float(self.data.close[0]) * 100.0 / float(self.broker.getvalue()) if self.broker.getvalue() > 0 else 0
            if target_pct < 0.1:
                self.sell_count += 1
                self.pending_order = self.close()
            elif abs(current_pct - target_pct * 100) > 20:
                self.sell_count += 1
                self.pending_order = self.close()


    def notify_order(self, order):
        """Callback to handle order status updates.

        Args:
            order (bt.Order): The updated order instance.
        """
        if order.status in (order.Submitted, order.Accepted):
            return
        self.pending_order = None

    def notify_trade(self, trade):
        """Callback to handle trade status updates.

        Args:
            trade (bt.Trade): The updated trade instance.
        """
        pass


