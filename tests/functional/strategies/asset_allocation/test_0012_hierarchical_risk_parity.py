def prepare_hierarchical_risk_parity_features(df, params):
    """Calculate rolling volatility metrics and assign targeted portfolio allocation percentages.

    Args:
        df (pd.DataFrame): Input raw historical price DataFrame.
        params (dict): Configured strategy parameters.

    Returns:
        pd.DataFrame: Processed features DataFrame containing rolling annual volatility and targeted position sizing.
    """
    out = df.copy()
    lookback = int(params.get('vol_lookback', 60))
    returns = out['close'].pct_change()
    out['vol'] = returns.rolling(lookback).std() * np.sqrt(252)
    vol_median = out['vol'].rolling(252).median()
    # Low vol = 100%, medium = 70%, high = 40%
    out['position_pct'] = 0.7
    out.loc[out['vol'] < vol_median * 0.8, 'position_pct'] = 1.0
    out.loc[out['vol'] > vol_median * 1.5, 'position_pct'] = 0.4
    out = out[['open', 'high', 'low', 'close', 'volume', 'openinterest', 'vol', 'position_pct']].copy()
    return out.dropna()


