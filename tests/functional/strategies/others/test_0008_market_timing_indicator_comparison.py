def load_mt5_csv(filepath, fromdate=None, todate=None):
    """Load a MetaTrader 5 daily CSV export into a backtrader-ready OHLCV frame.

    Args:
        filepath: Path to the MT5 export file (tab- or comma-separated).
        fromdate: Optional inclusive lower bound used to clip the frame.
        todate: Optional inclusive upper bound used to clip the frame.

    Returns:
        pandas.DataFrame: A datetime-indexed frame with ``open``, ``high``,
        ``low``, ``close``, ``volume`` and ``openinterest`` columns sorted in
        ascending time order.
    """
    with open(filepath, 'r', encoding='utf-8', errors='ignore') as handle:
        lines = [line.strip().strip('"') for line in handle.readlines() if line.strip()]
    cleaned = '\n'.join(lines)
    sep = '\t' if '\t' in lines[0] else ','
    df = pd.read_csv(io.StringIO(cleaned), sep=sep)
    dt_text = df['<DATE>'].astype(str) + ' ' + df['<TIME>'].astype(str)
    parsed = pd.to_datetime(dt_text, format='%Y.%m.%d %H:%M', errors='coerce')
    if parsed.isna().any():
        parsed = pd.to_datetime(dt_text, format='%Y.%m.%d %H:%M:%S', errors='coerce')
    df['datetime'] = parsed
    df = df.rename(columns={
        '<OPEN>': 'open',
        '<HIGH>': 'high',
        '<LOW>': 'low',
        '<CLOSE>': 'close',
        '<TICKVOL>': 'tick_volume',
        '<VOL>': 'real_volume',
    })
    df['openinterest'] = 0
    df['volume'] = df['tick_volume'] if 'tick_volume' in df.columns else 0
    df = df[['datetime', 'open', 'high', 'low', 'close', 'volume', 'openinterest']]
    df = df.dropna(subset=['datetime']).set_index('datetime').sort_index()
    if fromdate is not None:
        df = df[df.index >= fromdate]
    if todate is not None:
        df = df[df.index <= todate]
    return df


def prepare_market_timing_data(df, params):
    """Compute the MA filter, confirmation runs and target-exposure signal.

    Args:
        df: Daily OHLCV frame indexed by date.
        params: Strategy parameter mapping (``ma_period``, ``confirm_days``).

    Returns:
        pandas.DataFrame: The frame augmented with the MA, above/below run
        counts, buy/sell signals, ``target_exposure`` and ``signal_change``
        columns, with warm-up rows dropped.
    """
    out = df.copy()
    ma_period = int(params.get('ma_period', 200))
    confirm_days = int(params.get('confirm_days', 10))

    out['ma'] = out['close'].rolling(ma_period).mean()
    out['above_ma'] = (out['close'] > out['ma']).astype(float)
    out['below_ma'] = (out['close'] < out['ma']).astype(float)
    out['consecutive_above'] = out['above_ma'].rolling(confirm_days).sum()
    out['consecutive_below'] = out['below_ma'].rolling(confirm_days).sum()
    out['buy_signal'] = (out['consecutive_above'] >= confirm_days).astype(float)
    out['sell_signal'] = (out['consecutive_below'] >= confirm_days).astype(float)

    target = []
    current_target = 0.0
    for idx in out.index:
        if pd.isna(out.at[idx, 'ma']):
            target.append(current_target)
            continue
        if out.at[idx, 'buy_signal'] > 0.5:
            current_target = 1.0
        elif out.at[idx, 'sell_signal'] > 0.5:
            current_target = 0.0
        target.append(current_target)

    out['target_exposure'] = pd.Series(target, index=out.index, dtype='float64')
    out['signal_change'] = out['target_exposure'].ne(out['target_exposure'].shift(1)).astype(float)
    columns = [
        'open', 'high', 'low', 'close', 'volume', 'openinterest',
        'ma', 'above_ma', 'consecutive_above', 'consecutive_below',
        'buy_signal', 'sell_signal', 'target_exposure', 'signal_change',
    ]
    return out[columns].copy().dropna()


