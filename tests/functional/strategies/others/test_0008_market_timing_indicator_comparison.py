class MarketTimingIndicatorComparisonStrategy(bt.Strategy):
    """Long-or-cash strategy timing gold with a confirmed 200-day MA filter."""

    params = dict(
        invest_pct=0.99,
        ma_period=200,
        confirm_days=10,
        commission_pct=0.0005,
    )

    def __init__(self):
        """Initialize strategy bookkeeping state.

        Args:
            self: Strategy instance.
        """
        self.bar_num = 0
        self.pending_order = None
        self.trade_count = 0
        self.win_count = 0
        self.loss_count = 0
        self.signal_change_count = 0
        self.broker_value_series = []

    def next(self):
        """Evaluate each bar and rebalance when the signal exposure changes.

        Args:
            self: Strategy instance.
        """
        self.bar_num += 1
        self.broker_value_series.append((bt.num2date(self.data.datetime[0]), float(self.broker.getvalue())))
        if self.pending_order is not None:
            return
        if float(self.data.signal_change[0]) <= 0.5:
            return
        self.signal_change_count += 1
        target = float(self.data.target_exposure[0]) * float(self.p.invest_pct)
        self.pending_order = self.order_target_percent(target=target)

    def notify_order(self, order):
        """Clear the pending-order state once the order lifecycle leaves active states.

        Args:
            order: Submitted backtrader order instance.
        """
        if order.status in (order.Submitted, order.Accepted):
            return
        self.pending_order = None

    def notify_trade(self, trade):
        """Track win/loss counters after each closed trade.

        Args:
            trade: Closed backtrader trade instance.
        """
        if not trade.isclosed:
            return
        self.trade_count += 1
        if trade.pnlcomm >= 0:
            self.win_count += 1
        else:
            self.loss_count += 1





