def calculate_ulcer_index(values):
    """Compute Ulcer Index from an equity curve sequence.

    Args:
        values: Iterable of equity values.

    Returns:
        The Ulcer Index value as a float.
    """
    if len(values) < 2:
        return 0.0
    peak = values[0]
    squared = 0.0
    for value in values:
        peak = max(peak, value)
        drawdown = (peak - value) / peak * 100.0 if peak > 0 else 0.0
        squared += drawdown ** 2
    return math.sqrt(squared / len(values))



