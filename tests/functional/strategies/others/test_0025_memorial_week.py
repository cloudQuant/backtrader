class MemorialWeekStrategy(bt.Strategy):
    """Memorial-week seasonal long with fixed holding period and risk exits."""

    params = dict(
        holding_days=2,
        position_size=0.95,
        stop_loss=0.02,
        take_profit=0.015,
        commission_pct=0.0005,
    )

    def __init__(self):
        """Initialize bar/trade counters, entry tracking, and risk-price state."""
        self.bar_num = 0
        self.buy_count = 0
        self.sell_count = 0
        self.signal_days = 0
        self.pending_order = None
        self.entry_bar = 0
        self.entry_price = None
        self.stop_price = None
        self.take_profit_price = None
        self.broker_value_series = []

    def next(self):
        """Record equity, open on the entry signal, and exit on stop/target/time."""
        self.bar_num += 1
        self.broker_value_series.append((bt.num2date(self.data.datetime[0]), float(self.broker.getvalue())))
        if self.pending_order is not None:
            return

        close = float(self.data.close[0])
        low = float(self.data.low[0])
        high = float(self.data.high[0])

        if self.position:
            if self.stop_price is not None and low <= self.stop_price:
                self.sell_count += 1
                self.pending_order = self.close()
                return
            if self.take_profit_price is not None and high >= self.take_profit_price:
                self.sell_count += 1
                self.pending_order = self.close()
                return
            if self.bar_num - self.entry_bar >= int(self.p.holding_days):
                self.sell_count += 1
                self.pending_order = self.close()
                return
            return

        if float(self.data.entry_signal[0]) > 0.5:
            self.signal_days += 1
            self.buy_count += 1
            self.entry_bar = self.bar_num
            self.entry_price = close
            self.stop_price = close * (1.0 - float(self.p.stop_loss))
            self.take_profit_price = close * (1.0 + float(self.p.take_profit))
            self.pending_order = self.order_target_percent(target=float(self.p.position_size))

    def notify_order(self, order):
        """Clear the pending order and reset risk prices once flat."""
        if order.status in (order.Submitted, order.Accepted):
            return
        self.pending_order = None
        if not self.position:
            self.entry_price = None
            self.stop_price = None
            self.take_profit_price = None


