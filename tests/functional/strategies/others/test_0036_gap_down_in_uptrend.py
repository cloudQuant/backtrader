def prepare_gap_down_in_uptrend_features(df, params):
    """Compute the trend MA, uptrend flag, overnight gap and the entry signal.

    Args:
        df: Daily OHLCV frame indexed by date.
        params: Strategy parameter mapping (``ma_period``, ``gap_threshold``).

    Returns:
        pandas.DataFrame: The frame augmented with ``uptrend``, ``gap_pct`` and
        ``entry_signal`` columns, with warm-up rows dropped.
    """
    ma_period = int(params.get('ma_period', 200))
    gap_threshold = float(params.get('gap_threshold', -0.02))

    out = df.copy()
    out['ma'] = out['close'].rolling(ma_period).mean()
    out['uptrend'] = (out['close'] > out['ma']).astype(float)
    out['gap_pct'] = (out['open'] - out['close'].shift(1)) / out['close'].shift(1)
    out['entry_signal'] = ((out['uptrend'] > 0.5) & (out['gap_pct'] < gap_threshold)).astype(float)

    out = out[['open', 'high', 'low', 'close', 'volume', 'openinterest',
               'uptrend', 'gap_pct', 'entry_signal']].copy()
    return out.dropna()


