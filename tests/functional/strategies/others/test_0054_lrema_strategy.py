def finite_or_none(value):
    """Return a numeric scalar or ``None`` for non-finite values.

    Args:
        value: Value that may be float, datetime, etc.

    Returns:
        Any: Input value when finite (or non-float), otherwise ``None``.
    """
    return value if value is not None and math.isfinite(value) else None


def calculate_ulcer_index(values):
    """Compute the Ulcer Index from a drawdown series represented by equity values.

    Args:
        values: Iterable of account equity observations.

    Returns:
        float: Ulcer Index percentage RMS drawdown.
    """
    if len(values) < 2:
        return 0.0
    peak = values[0]
    squared = 0.0
    for value in values:
        peak = max(peak, value)
        drawdown = (peak - value) / peak * 100.0 if peak > 0 else 0.0
        squared += drawdown ** 2
    return math.sqrt(squared / len(values))



