def get_sharpe_analyzer_kwargs(config):
    """Build SharpeRatio analyzer kwargs matched to the data timeframe.

    Args:
        config: Configuration mapping with a ``data`` section carrying the
            timeframe code (e.g. ``M15``, ``H1``, ``D1``).

    Returns:
        A dict of keyword arguments configuring timeframe, compression, and
        annualization factor for the SharpeRatio analyzer.
    """
    data_cfg = config.get('data', {}) if isinstance(config, dict) else {}
    timeframe_value = str(data_cfg.get('timeframe', 'D1')).upper()
    if timeframe_value.startswith('M') and timeframe_value[1:].isdigit():
        compression = max(1, int(timeframe_value[1:]))
        return dict(timeframe=bt.TimeFrame.Minutes, compression=compression, factor=252 * 24 * 60 / compression, annualize=True, riskfreerate=0)
    if timeframe_value.startswith('H') and timeframe_value[1:].isdigit():
        hours = max(1, int(timeframe_value[1:]))
        return dict(timeframe=bt.TimeFrame.Minutes, compression=hours * 60, factor=252 * 24 / hours, annualize=True, riskfreerate=0)
    return dict(timeframe=bt.TimeFrame.Days, compression=1, factor=252, annualize=True, riskfreerate=0)


def get_returns_analyzer_kwargs(config):
    """Build Returns analyzer kwargs matched to the data timeframe.

    Args:
        config: Configuration mapping with a ``data`` section carrying the
            timeframe code (e.g. ``M15``, ``H1``, ``D1``).

    Returns:
        A dict with the ``tann`` annualization factor for the Returns analyzer.
    """
    data_cfg = config.get('data', {}) if isinstance(config, dict) else {}
    timeframe_value = str(data_cfg.get('timeframe', 'D1')).upper()
    if timeframe_value.startswith('M') and timeframe_value[1:].isdigit():
        compression = max(1, int(timeframe_value[1:]))
        return dict(tann=252 * 24 * 60 / compression)
    if timeframe_value.startswith('H') and timeframe_value[1:].isdigit():
        hours = max(1, int(timeframe_value[1:]))
        return dict(tann=252 * 24 / hours)
    return dict(tann=252)


def finite_or_none(x):
    """Return the value if it is truthy and finite, otherwise None.

    Args:
        x: Numeric value to validate.

    Returns:
        ``x`` when it is finite and non-zero/non-empty, otherwise None.
    """
    return x if x and math.isfinite(x) else None


def calculate_ulcer_index(values):
    """Compute the Ulcer Index of an equity series.

    Args:
        values: Sequence of equity values in chronological order.

    Returns:
        The Ulcer Index (RMS of percentage drawdowns), or 0.0 if fewer than two
        values are provided.
    """
    if len(values) < 2:
        return 0.0
    max_value = values[0]
    sum_squared = 0.0
    for v in values:
        if v > max_value:
            max_value = v
        drawdown = (max_value - v) / max_value * 100.0 if max_value > 0 else 0.0
        sum_squared += drawdown ** 2
    return math.sqrt(sum_squared / len(values))



def load_data(config):
    """Load and prepare the multi-timeframe H1 feature frame.

    Args:
        config: Resolved configuration mapping with ``data`` and ``params``.

    Returns:
        A dict with the prepared H1 feature frame and the from/to datetime
        bounds.
    """
    data_cfg = config['data']
    fromdate = datetime.fromisoformat(data_cfg['fromdate'])
    todate = datetime.fromisoformat(data_cfg['todate'])
