def load_mt5_csv(filepath, fromdate=None, todate=None):
    """Load a MetaTrader 5 daily CSV export into an OHLCV DataFrame.

    Args:
        filepath: Path to the MT5 export (tab- or comma-separated).
        fromdate: Optional inclusive lower bound on the datetime index.
        todate: Optional inclusive upper bound on the datetime index.

    Returns:
        pandas.DataFrame: Indexed by datetime with open/high/low/close/volume/
        openinterest columns, sorted ascending and sliced to the range.
    """
    with open(filepath, 'r', encoding='utf-8', errors='ignore') as handle:
        lines = [line.strip().strip('"') for line in handle.readlines() if line.strip()]
    cleaned = '\n'.join(lines)
    sep = '\t' if '\t' in lines[0] else ','
    df = pd.read_csv(io.StringIO(cleaned), sep=sep)
    dt_text = df['<DATE>'].astype(str) + ' ' + df['<TIME>'].astype(str)
    parsed = pd.to_datetime(dt_text, format='%Y.%m.%d %H:%M', errors='coerce')
    if parsed.isna().any():
        parsed = pd.to_datetime(dt_text, format='%Y.%m.%d %H:%M:%S', errors='coerce')
    df['datetime'] = parsed
    df = df.rename(columns={
        '<OPEN>': 'open', '<HIGH>': 'high', '<LOW>': 'low', '<CLOSE>': 'close',
        '<TICKVOL>': 'tick_volume', '<VOL>': 'real_volume',
    })
    df['openinterest'] = 0
    df['volume'] = df['tick_volume'] if 'tick_volume' in df.columns else 0
    df = df[['datetime', 'open', 'high', 'low', 'close', 'volume', 'openinterest']]
    df = df.set_index('datetime').sort_index()
    if fromdate is not None:
        df = df[df.index >= fromdate]
    if todate is not None:
        df = df[df.index <= todate]
    return df


def calculate_rsi(prices, period=14):
    """Compute the Relative Strength Index of a price series.

    Args:
        prices: Series of close prices.
        period: Rolling window length for the average gain/loss.

    Returns:
        pandas.Series: RSI values in the 0-100 range, defaulting to 50 where
        the calculation is undefined.
    """
    delta = prices.diff()
    gain = delta.where(delta > 0, 0.0)
    loss = -delta.where(delta < 0, 0.0)
    avg_gain = gain.rolling(window=period).mean()
    avg_loss = loss.rolling(window=period).mean().replace(0, pd.NA)
    rs = avg_gain / avg_loss
    return (100 - (100 / (1 + rs))).fillna(50.0)


def prepare_cbi_bullish_features(df, params):
    """Compute the RSI/new-low/volume-spike features and combined entry signal.

    Args:
        df: OHLCV DataFrame for the traded symbol.
        params: Strategy params controlling RSI, low, and volume windows plus
            the oversold and volume-spike thresholds.

    Returns:
        pandas.DataFrame: OHLCV plus the derived feature columns and the boolean
        ``entry_signal`` line, with warmup rows dropped.
    """
    out = df.copy()
    rsi_period = int(params.get('rsi_period', 14))
    rsi_oversold = float(params.get('rsi_oversold', 25))
    low_period = int(params.get('low_period', 20))
    volume_window = int(params.get('volume_window', 20))
    volume_multiplier = float(params.get('volume_multiplier', 1.5))
    out['rsi'] = calculate_rsi(out['close'], rsi_period)
    out['rolling_low'] = out['close'].rolling(window=low_period).min()
    out['avg_volume'] = out['volume'].rolling(window=volume_window).mean()
    out['is_new_low'] = (out['close'] <= out['rolling_low']).astype(float)
    out['oversold'] = (out['rsi'] < rsi_oversold).astype(float)
    out['volume_spike'] = (out['volume'] > out['avg_volume'] * volume_multiplier).astype(float)
    out['entry_signal'] = ((out['is_new_low'] > 0.5) & (out['oversold'] > 0.5) & (out['volume_spike'] > 0.5)).astype(float)
    out = out[['open', 'high', 'low', 'close', 'volume', 'openinterest', 'rsi', 'rolling_low', 'avg_volume', 'is_new_low', 'oversold', 'volume_spike', 'entry_signal']].copy()
    return out.dropna()


