def finite_or_none(value):
    """Return ``value`` if it is not None and finite, otherwise ``None``."""
    return value if value is not None and math.isfinite(value) else None


def calculate_ulcer_index(values):
    """Compute the Ulcer Index (RMS of percentage drawdowns) of an equity curve.

    Args:
        values: Sequence of portfolio equity values in chronological order.

    Returns:
        float: The Ulcer Index, or ``0.0`` when fewer than two values are given.
    """
    if len(values) < 2:
        return 0.0
    peak = values[0]
    squared = 0.0
    for value in values:
        peak = max(peak, value)
        drawdown = (peak - value) / peak * 100.0 if peak > 0 else 0.0
        squared += drawdown ** 2
    return math.sqrt(squared / len(values))



def resolve_data_file(file_value):
    """Resolve a configured data path against several known locations.

    Args:
        file_value: The raw ``data.file`` value from the config.

    Returns:
        Path: The first existing candidate path.

    Raises:
        FileNotFoundError: If none of the candidate locations exist.
    """
    path_value = Path(str(file_value))
    candidates = []
    if path_value.is_absolute():
        candidates.append(path_value)
    candidates.append((BASE_DIR / path_value).resolve())
    candidates.append((Path(__file__).resolve().parents[4] / 'tests' / 'datas' / path_value.name).resolve())
    candidates.append((Path(__file__).resolve().parents[4] / 'tests' / 'datas' / 'mt5_1d_data' / path_value.name).resolve())
    for candidate in candidates:
        if candidate.exists():
            return candidate
    raise FileNotFoundError('Could not resolve data file from config: {0}'.format(file_value))


def load_inputs(config):
    """Load the raw price data and build the CBI bullish feature frame.

    Args:
        config: Resolved configuration dict with the ``data`` file/date range
            and strategy ``params``.

    Returns:
        dict: ``data`` (feature DataFrame) plus parsed ``fromdate``/``todate``.

    Raises:
        ValueError: If the prepared feature frame is empty.
    """
    data_cfg = config['data']
    fromdate = datetime.fromisoformat(data_cfg['fromdate'])
    todate = datetime.fromisoformat(data_cfg['todate'])
    raw_df = load_mt5_csv(resolve_data_file(data_cfg['file']), fromdate=fromdate, todate=todate)
    feature_df = prepare_cbi_bullish_features(raw_df, config.get('params', {}))
    if feature_df.empty:
        raise ValueError('Feature dataframe is empty after preparation')
    return {'data': feature_df, 'fromdate': fromdate, 'todate': todate}


def build_cerebro(inputs, config):
    """Assemble the Cerebro engine with the feed, commission, and analyzers.

    Args:
        inputs: Output of :func:`load_inputs` holding the feature frame.
        config: Resolved configuration dict with backtest settings and params.

    Returns:
        bt.Cerebro: Configured engine ready to run the CBI bullish strategy.
    """
    cerebro = bt.Cerebro(stdstats=False)
    bt_cfg = config['backtest']
    cerebro.broker.setcash(float(bt_cfg['initial_cash']))
    comminfo = ComminfoFuturesPercent(
        commission=float(bt_cfg.get('commission', 0.0)),
        margin=float(bt_cfg.get('margin', 0.01)),
        mult=float(bt_cfg.get('multiplier', 1.0)),
    )
    cerebro.broker.addcommissioninfo(comminfo)
    feed = Mt5CBIBullishFeed(dataname=inputs['data'], timeframe=bt.TimeFrame.Days, compression=1)
    cerebro.adddata(feed, name=config['data']['symbol'])
    cerebro.addstrategy(CBIBullishSignalStrategy, **config.get('params', {}))
    cerebro.addanalyzer(bt.analyzers.SharpeRatio, _name='sharpe', timeframe=bt.TimeFrame.Days, compression=1, factor=252, annualize=True, riskfreerate=0)
    cerebro.addanalyzer(bt.analyzers.Returns, _name='returns', timeframe=bt.TimeFrame.Days, tann=252)
    cerebro.addanalyzer(bt.analyzers.DrawDown, _name='drawdown')
    cerebro.addanalyzer(bt.analyzers.TradeAnalyzer, _name='trades')
    cerebro.addanalyzer(bt.analyzers.SQN, _name='sqn')
    return cerebro


def extract_metrics(strat, cerebro, inputs, config):
    """Aggregate analyzer output and strategy counters into a metrics dict.

    Args:
        strat: The executed strategy instance carrying run-time counters.
        cerebro: The Cerebro engine providing the final broker value.
        inputs: The :func:`load_inputs` result for date range and bar counts.
        config: Resolved configuration dict for naming and initial cash.

    Returns:
        dict: Performance metrics (returns, drawdown, Sharpe, SQN, Ulcer Index,
        exit-reason counts, trade tallies) consumed by the regression assertions.
    """
    trades = strat.analyzers.trades.get_analysis()
    sharpe = strat.analyzers.sharpe.get_analysis()
    returns = strat.analyzers.returns.get_analysis()
    drawdown = strat.analyzers.drawdown.get_analysis()
    sqn = strat.analyzers.sqn.get_analysis()
    initial_cash = float(config['backtest']['initial_cash'])
    final_value = float(cerebro.broker.getvalue())
    broker_values = [value for _, value in getattr(strat, 'broker_value_series', [])] or [initial_cash, final_value]
    total_trades = trades.get('total', {}).get('closed', 0)
    won = trades.get('won', {}).get('total', 0)
    lost = trades.get('lost', {}).get('total', 0)
    gross_won = trades.get('won', {}).get('pnl', {}).get('total', 0.0) or 0.0
    gross_lost = abs(trades.get('lost', {}).get('pnl', {}).get('total', 0.0) or 0.0)
    return {
        'strategy_name': config['strategy']['name'],
        'fromdate': inputs['fromdate'].isoformat(),
        'todate': inputs['todate'].isoformat(),
        'bars': len(inputs['data']),
        'bar_num': strat.bar_num,
        'signal_count': strat.signal_count,
        'buy_count': strat.buy_count,
        'sell_count': strat.sell_count,
        'time_exit_count': strat.time_exit_count,
        'stop_loss_exit_count': strat.stop_loss_exit_count,
        'take_profit_exit_count': strat.take_profit_exit_count,
        'initial_cash': initial_cash,
        'final_value': final_value,
        'net_pnl': final_value - initial_cash,
        'total_return_pct': (final_value / initial_cash - 1.0) * 100.0,
        'total_trades': total_trades,
        'won': won,
        'lost': lost,
        'win_rate': (won / total_trades * 100.0) if total_trades else 0.0,
        'profit_factor': (gross_won / gross_lost) if gross_lost else None,
        'max_drawdown': drawdown.get('max', {}).get('drawdown', 0.0),
        'sharpe_ratio': finite_or_none(sharpe.get('sharperatio')),
        'annual_return_pct': (returns.get('rnorm') or 0.0) * 100.0,
        'sqn': finite_or_none(sqn.get('sqn')),
        'ulcer_index': calculate_ulcer_index(broker_values),
    }





