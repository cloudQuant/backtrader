def load_mt5_csv(filepath, fromdate=None, todate=None):
    """Load MT5 data and convert to sorted OHLCV frame for backtesting.

    Args:
        filepath: Input data path.
        fromdate: Optional start datetime filter.
        todate: Optional end datetime filter.

    Returns:
        Normalized dataframe indexed by datetime.
    """
    with open(filepath, 'r', encoding='utf-8', errors='ignore') as handle:
        lines = [line.strip().strip('"') for line in handle.readlines() if line.strip()]
    cleaned = '\n'.join(lines)
    sep = '\t' if '\t' in lines[0] else ','
    df = pd.read_csv(io.StringIO(cleaned), sep=sep)
    dt_text = df['<DATE>'].astype(str) + ' ' + df['<TIME>'].astype(str)
    parsed = pd.to_datetime(dt_text, format='%Y.%m.%d %H:%M', errors='coerce')
    if parsed.isna().any():
        parsed = pd.to_datetime(dt_text, format='%Y.%m.%d %H:%M:%S', errors='coerce')
    df['datetime'] = parsed
    df = df.rename(columns={
        '<OPEN>': 'open', '<HIGH>': 'high', '<LOW>': 'low',
        '<CLOSE>': 'close', '<TICKVOL>': 'tick_volume', '<VOL>': 'real_volume',
    })
    df['openinterest'] = 0
    df['volume'] = df['tick_volume'] if 'tick_volume' in df.columns else 0
    df = df[['datetime', 'open', 'high', 'low', 'close', 'volume', 'openinterest']]
    df = df.set_index('datetime').sort_index()
    if fromdate is not None:
        df = df[df.index >= fromdate]
    if todate is not None:
        df = df[df.index <= todate]
    return df



def prepare_exit_rules_testing_features(df, params):
    """Compute RSI-based entry and exit signal columns.

    Args:
        df: Base OHLCV dataframe.
        params: Strategy parameters including RSI period and thresholds.

    Returns:
        Dataframe with added ``rsi``, ``entry_signal``, ``exit_signal``.
    """
    rsi_period = int(params.get('rsi_period', 2))
    rsi_oversold = float(params.get('rsi_oversold', 10))
    rsi_overbought = float(params.get('rsi_overbought', 90))

    out = df.copy()
    delta = out['close'].diff()
    gain = delta.where(delta > 0, 0)
    loss = (-delta).where(delta < 0, 0)
    avg_gain = gain.rolling(window=rsi_period).mean()
    avg_loss = loss.rolling(window=rsi_period).mean()
    rs = avg_gain / avg_loss.replace(0, np.inf)
    out['rsi'] = 100 - (100 / (1 + rs))
    out['entry_signal'] = (out['rsi'] < rsi_oversold).astype(float)
    out['exit_signal'] = (out['rsi'] > rsi_overbought).astype(float)

    out = out[['open', 'high', 'low', 'close', 'volume', 'openinterest',
               'rsi', 'entry_signal', 'exit_signal']].copy()
    return out.dropna()


class Mt5ExitRulesTestingFeed(bt.feeds.PandasData):
    """Pandas feed exposing RSI and entry/exit signal series."""
    lines = ('rsi', 'entry_signal', 'exit_signal',)
    params = (
        ('datetime', None), ('open', 0), ('high', 1), ('low', 2),
        ('close', 3), ('volume', 4), ('openinterest', 5),
        ('rsi', 6),
        ('entry_signal', 7),
        ('exit_signal', 8),
    )


class ExitRulesTestingStrategy(bt.Strategy):
    """Exit-rules strategy combining entry/exit thresholds and max hold duration."""
    params = dict(
        rsi_period=2,
        rsi_oversold=10,
        rsi_overbought=90,
        holding_days=5,
        lot_size=1.0,
    )

    def __init__(self):
        """Initialize strategy counters, broker tracking, and internal state."""
        self.bar_num = 0
        self.buy_count = 0
        self.sell_count = 0
        self.pending_order = None
        self.entry_bar = 0
        self.broker_value_series = []

    def _get_position_size(self, target_notional_pct=1.0, price=None):
        if target_notional_pct <= 0:
            return 0.0
        broker_value = float(self.broker.getvalue())
        execution_price = float(self.data.close[0] if price is None else price)
        if broker_value <= 0 or execution_price <= 0:
            return 0.0
        comminfo = self.broker.getcommissioninfo(self.data)
        multiplier = float(getattr(comminfo.p, 'mult', 1.0) or 1.0)
        if multiplier <= 0:
            return 0.0
        size = broker_value * float(target_notional_pct) / (execution_price * multiplier)
        return max(0.01, round(size, 2))


    def next(self):
        """Advance counters and apply entry/exit conditions each bar."""
        self.bar_num += 1
        self.broker_value_series.append((bt.num2date(self.data.datetime[0]), float(self.broker.getvalue())))
        if self.pending_order is not None:
            return
        entry = float(self.data.entry_signal[0]) > 0.5
        exit_sig = float(self.data.exit_signal[0]) > 0.5
        if not self.position:
            if entry:
                self.buy_count += 1
                self.entry_bar = self.bar_num
                self.pending_order = self.buy(size=self._get_position_size())
        else:
            if exit_sig or (self.bar_num - self.entry_bar >= self.p.holding_days):
                self.sell_count += 1
                self.pending_order = self.close()

    def notify_order(self, order):
        """Reset pending order tracking when an order is finalized."""
        if order.status in (order.Submitted, order.Accepted):
            return
        self.pending_order = None

    def notify_trade(self, trade):
        """Trade lifecycle callback placeholder."""
        pass


