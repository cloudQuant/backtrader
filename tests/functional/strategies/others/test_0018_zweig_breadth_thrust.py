class ZweigBreadthThrustStrategy(bt.Strategy):
    """Long-only strategy buying gold on a breadth thrust with stop and time exits."""

    params = dict(
        holding_days=20,
        stop_loss_pct=0.03,
        position_size=0.90,
        ema_period=10,
        low_threshold=0.4,
        high_threshold=0.615,
        lookback_days=10,
        commission_pct=0.0005,
    )

    def __init__(self):
        """Initialize counters, the pending order slot and entry/exit tracking."""
        self.bar_num = 0
        self.buy_count = 0
        self.sell_count = 0
        self.pending_order = None
        self.entry_bar = 0
        self.entry_price = None
        self.stop_price = None
        self.broker_value_series = []

    def next(self):
        """Record equity, manage stop/time exits, then enter long on a thrust signal."""
        self.bar_num += 1
        self.broker_value_series.append((bt.num2date(self.data.datetime[0]), float(self.broker.getvalue())))
        if self.pending_order is not None:
            return
        low = float(self.data.low[0])
        close = float(self.data.close[0])
        if self.position:
            if self.stop_price is not None and low <= self.stop_price:
                self.sell_count += 1
                self.pending_order = self.close()
                return
            if self.bar_num - self.entry_bar >= int(self.p.holding_days):
                self.sell_count += 1
                self.pending_order = self.close()
                return
            return
        if float(self.data.thrust_signal[0]) > 0.5:
            self.entry_bar = self.bar_num
            self.entry_price = close
            self.stop_price = close * (1.0 - float(self.p.stop_loss_pct))
            self.buy_count += 1
            self.pending_order = self.order_target_percent(target=float(self.p.position_size))

    def notify_order(self, order):
        """Clear the pending-order slot and reset exit levels when flat."""
        if order.status in (order.Submitted, order.Accepted):
            return
        self.pending_order = None
        if not self.position:
            self.entry_price = None
            self.stop_price = None


