class GapNGoFadeStrategy(bt.Strategy):
    params = dict(
        low_window=50,
        atr_window=14,
        gap_threshold_pct=0.003,
        gap_atr_multiple=0.5,
        hold_days=2,
        short_target_pct=1.0,
        commission_pct=0.0005,
    )

    def __init__(self):
        self.bar_num = 0
        self.buy_count = 0
        self.sell_count = 0
        self.trade_count = 0
        self.win_count = 0
        self.loss_count = 0
        self.pending_order = None
        self.broker_value_series = []

    def next(self):
        self.bar_num += 1
        self.broker_value_series.append((bt.num2date(self.data.datetime[0]), float(self.broker.getvalue())))
        if self.pending_order is not None:
            return
        if not self.position and float(self.data.setup_signal[0]) > 0.5:
            self.sell_count += 1
            self.pending_order = self.order_target_percent(target=float(self.data.target_pct[0]))
            return
        if self.position and self.position.size < 0 and float(self.data.exit_signal[0]) > 0.5:
            self.buy_count += 1
            self.pending_order = self.close()

    def notify_order(self, order):
        if order.status in (order.Submitted, order.Accepted):
            return
        self.pending_order = None

    def notify_trade(self, trade):
        if not trade.isclosed:
            return
        self.trade_count += 1
        if trade.pnlcomm >= 0:
            self.win_count += 1
        else:
            self.loss_count += 1





