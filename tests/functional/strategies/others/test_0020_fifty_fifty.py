def load_mt5_csv(filepath, fromdate=None, todate=None):
    """Load a MetaTrader 5 daily CSV export into an OHLCV DataFrame.

    Args:
        filepath: Path to the MT5 export (tab- or comma-separated).
        fromdate: Optional inclusive lower bound on the datetime index.
        todate: Optional inclusive upper bound on the datetime index.

    Returns:
        pandas.DataFrame: Indexed by datetime with open/high/low/close/volume/
        openinterest columns, sorted ascending and sliced to the range.
    """
    with open(filepath, 'r', encoding='utf-8', errors='ignore') as handle:
        lines = [line.strip().strip('"') for line in handle.readlines() if line.strip()]
    cleaned = '\n'.join(lines)
    sep = '\t' if '\t' in lines[0] else ','
    df = pd.read_csv(io.StringIO(cleaned), sep=sep)
    dt_text = df['<DATE>'].astype(str) + ' ' + df['<TIME>'].astype(str)
    parsed = pd.to_datetime(dt_text, format='%Y.%m.%d %H:%M', errors='coerce')
    if parsed.isna().any():
        parsed = pd.to_datetime(dt_text, format='%Y.%m.%d %H:%M:%S', errors='coerce')
    df['datetime'] = parsed
    df = df.rename(columns={
        '<OPEN>': 'open',
        '<HIGH>': 'high',
        '<LOW>': 'low',
        '<CLOSE>': 'close',
        '<TICKVOL>': 'tick_volume',
        '<VOL>': 'real_volume',
    })
    df['openinterest'] = 0
    df['volume'] = df['tick_volume'] if 'tick_volume' in df.columns else 0
    df = df[['datetime', 'open', 'high', 'low', 'close', 'volume', 'openinterest']]
    df = df.dropna(subset=['datetime']).set_index('datetime').sort_index()
    if fromdate is not None:
        df = df[df.index >= fromdate]
    if todate is not None:
        df = df[df.index <= todate]
    return df


def prepare_fifty_fifty_features(price_df, params):
    """Compute the moving average, trend signals, and year-start marker.

    Args:
        price_df: OHLCV DataFrame for the traded symbol.
        params: Strategy params providing the ``ma_period`` and
            ``confirmation_days`` windows.

    Returns:
        pandas.DataFrame: OHLCV plus the MA, above/below flags, confirmation
        trend entry/exit signals, and year-start marker, with warmup rows dropped.
    """
    out = price_df.copy()
    ma_period = int(params.get('ma_period', 200))
    confirmation_days = int(params.get('confirmation_days', 5))
    out['ma'] = out['close'].rolling(ma_period).mean()
    out['above_ma'] = (out['close'] > out['ma']).astype(float)
    out['below_ma'] = (out['close'] < out['ma']).astype(float)
    out['trend_entry_signal'] = (out['above_ma'].rolling(confirmation_days).sum() >= confirmation_days).astype(float)
    out['trend_exit_signal'] = (out['below_ma'].rolling(confirmation_days).sum() >= confirmation_days).astype(float)
    years = pd.Series(out.index.year, index=out.index)
    first_dates = years.groupby(years).transform('idxmin')
    out['is_year_start'] = (pd.Index(out.index) == pd.Index(first_dates)).astype(float)
    cols = [
        'open', 'high', 'low', 'close', 'volume', 'openinterest',
        'ma', 'above_ma', 'below_ma', 'trend_entry_signal', 'trend_exit_signal', 'is_year_start',
    ]
    return out[cols].dropna()


