class UnfilledGapStrategy(bt.Strategy):
    """Long breakout on clustered unfilled gaps with gap-floor stop and exits."""

    params = dict(
        holding_days=5,
        stop_loss=0.02,
        take_profit=0.03,
        position_size=0.95,
        gap_type='opening',
        min_gap_size=0.002,
        lookback_period=30,
        required_unfilled_gaps=2,
        max_gap_spacing=5,
        commission_pct=0.0005,
    )

    def __init__(self):
        """Initialize bar/trade counters, entry tracking, and risk-price state."""
        self.bar_num = 0
        self.buy_count = 0
        self.sell_count = 0
        self.pending_order = None
        self.entry_bar = 0
        self.stop_price = None
        self.take_profit_price = None
        self.broker_value_series = []

    def _get_position_size(self, target_notional_pct=1.0, price=None):
        if target_notional_pct <= 0:
            return 0.0
        broker_value = float(self.broker.getvalue())
        execution_price = float(self.data.close[0] if price is None else price)
        if broker_value <= 0 or execution_price <= 0:
            return 0.0
        comminfo = self.broker.getcommissioninfo(self.data)
        multiplier = float(getattr(comminfo.p, 'mult', 1.0) or 1.0)
        if multiplier <= 0:
            return 0.0
        size = broker_value * float(target_notional_pct) / (execution_price * multiplier)
        return max(0.01, round(size, 2))

    def next(self):
        """Record equity, open on the entry signal, and exit on stop/target/time."""
        self.bar_num += 1
        self.broker_value_series.append((bt.num2date(self.data.datetime[0]), float(self.broker.getvalue())))
        if self.pending_order is not None:
            return

        low = float(self.data.low[0])
        high = float(self.data.high[0])
        close = float(self.data.close[0])

        if self.position:
            if self.stop_price is not None and low <= self.stop_price:
                self.sell_count += 1
                self.pending_order = self.close()
                return
            if self.take_profit_price is not None and high >= self.take_profit_price:
                self.sell_count += 1
                self.pending_order = self.close()
                return
            if self.bar_num - self.entry_bar >= int(self.p.holding_days):
                self.sell_count += 1
                self.pending_order = self.close()
                return
            return

        if float(self.data.entry_signal[0]) > 0.5:
            self.buy_count += 1
            self.entry_bar = self.bar_num
            gap_floor = float(self.data.second_gap_floor[0]) if self.data.second_gap_floor[0] == self.data.second_gap_floor[0] else close * (1.0 - float(self.p.stop_loss))
            self.stop_price = max(gap_floor, close * (1.0 - float(self.p.stop_loss)))
            self.take_profit_price = close * (1.0 + float(self.p.take_profit))
            self.pending_order = self.buy(size=self._get_position_size(target_notional_pct=float(self.p.position_size)))

    def notify_order(self, order):
        """Clear the pending order and reset risk prices once flat."""
        if order.status in (order.Submitted, order.Accepted):
            return
        self.pending_order = None
        if not self.position:
            self.stop_price = None
            self.take_profit_price = None


