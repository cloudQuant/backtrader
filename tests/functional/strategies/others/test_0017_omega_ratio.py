class OmegaRatioStrategy(bt.Strategy):
    """Interval-rebalanced strategy that holds gold in favorable Omega regimes."""

    params = dict(
        rebalance_interval_days=5,
        position_size=0.90,
        threshold=0.0,
        window=252,
        upper_threshold=1.2,
        lower_threshold=0.8,
        commission_pct=0.0005,
    )

    def __init__(self):
        """Initialize bar/trade counters, the pending order slot and equity series."""
        self.bar_num = 0
        self.buy_count = 0
        self.sell_count = 0
        self.pending_order = None
        self.broker_value_series = []

    def _current_weight(self):
        value = float(self.broker.getvalue())
        if value <= 0 or not self.position:
            return 0.0
        return float(self.position.size) * float(self.data.close[0]) / value

    def next(self):
        """Record equity and rebalance to the target weight on the review interval."""
        self.bar_num += 1
        self.broker_value_series.append((bt.num2date(self.data.datetime[0]), float(self.broker.getvalue())))
        if self.pending_order is not None:
            return
        if self.bar_num > 1 and (self.bar_num - 1) % max(1, int(self.p.rebalance_interval_days)) != 0:
            return
        target = float(self.p.position_size) if float(self.data.signal[0]) > 0.5 else 0.0
        current = self._current_weight()
        if abs(target - current) < 0.01:
            return
        if target > current:
            self.buy_count += 1
        else:
            self.sell_count += 1
        self.pending_order = self.order_target_percent(target=target)

    def notify_order(self, order):
        """Clear the pending-order slot once an order leaves the live states."""
        if order.status in (order.Submitted, order.Accepted):
            return
        self.pending_order = None


