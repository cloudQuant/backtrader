def prepare_gold_ml_prediction_features(df, params):
    """Create RSI/MA/volatility-derived composite prediction features.

    Args:
        df: Raw OHLCV DataFrame.
        params: Strategy parameters controlling windows and threshold.

    Returns:
        DataFrame enriched with prediction scores and dropped invalid rows.
    """
    rsi_period = int(params.get('rsi_period', 14))
    ma_fast = int(params.get('ma_fast', 20))
    ma_slow = int(params.get('ma_slow', 60))
    vol_period = int(params.get('vol_period', 20))
    threshold = float(params.get('threshold', 0.6))

    out = df.copy()
    # RSI score (0-1, oversold=1)
    delta = out['close'].diff()
    gain = delta.where(delta > 0, 0)
    loss = (-delta).where(delta < 0, 0)
    avg_gain = gain.rolling(window=rsi_period).mean()
    avg_loss = loss.rolling(window=rsi_period).mean()
    rs = avg_gain / avg_loss.replace(0, np.inf)
    rsi = 100 - (100 / (1 + rs))
    out['rsi_score'] = 1.0 - rsi / 100.0

    # MA score (fast > slow = 1)
    fast_ma = out['close'].rolling(ma_fast).mean()
    slow_ma = out['close'].rolling(ma_slow).mean()
    out['ma_score'] = (fast_ma > slow_ma).astype(float)

    # Vol score (low vol = high score)
    ret = out['close'].pct_change()
    vol = ret.rolling(vol_period).std()
    out['vol_score'] = 1.0 - vol.rolling(min(252, len(vol))).rank(pct=True)

    # Composite
    out['composite_score'] = (out['rsi_score'] + out['ma_score'] + out['vol_score']) / 3.0

    out = out[['open', 'high', 'low', 'close', 'volume', 'openinterest',
               'rsi_score', 'ma_score', 'vol_score', 'composite_score']].copy()
    return out.dropna()


