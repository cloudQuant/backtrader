def compute_rsi(series, period):
    """Compute RSI using exponential smoothing style averages.

    Args:
        series: Price series.
        period: RSI period.

    Returns:
        RSI series aligned to the input index.
    """
    delta = series.diff()
    gain = delta.clip(lower=0)
    loss = -delta.clip(upper=0)
    avg_gain = gain.ewm(alpha=1.0 / period, adjust=False, min_periods=period).mean()
    avg_loss = loss.ewm(alpha=1.0 / period, adjust=False, min_periods=period).mean()
    rs = avg_gain / avg_loss.replace(0, pd.NA)
    rsi = 100.0 - (100.0 / (1.0 + rs))
    return rsi.fillna(50.0)


def get_probability(a1, a2, a3, x0, x1, x2, x3, x4, x5, x6, x7):
    """Evaluate the RNN-style probability from lagged RSI probabilities.

    Args:
        a1: Current normalized RSI value.
        a2: RSI shifted by one RSI-period.
        a3: RSI shifted by two RSI-periods.
        x0: Probability parameter set.
        x1: Probability parameter set.
        x2: Probability parameter set.
        x3: Probability parameter set.
        x4: Probability parameter set.
        x5: Probability parameter set.
        x6: Probability parameter set.
        x7: Probability parameter set.

    Returns:
        Probability in range ``[0, 1]``.
    """
    pn1 = 1.0 - a1
    pn2 = 1.0 - a2
    pn3 = 1.0 - a3
    probability = (
        pn1 * (pn2 * (pn3 * x0 + a3 * x1) + a2 * (pn3 * x2 + a3 * x3))
        + a1 * (pn2 * (pn3 * x4 + a3 * x5) + a2 * (pn3 * x6 + a3 * x7))
    )
    return probability / 100.0


def build_signal_frame(
    filepath,
    fromdate=None,
    todate=None,
    bar_shift_minutes=15,
    lots=1.0,
    sltp_pips=100,
    x0=6,
    x1=96,
    x2=90,
    x3=35,
    x4=64,
    x5=83,
    x6=66,
    x7=50,
    rsi_period=9,
    rsi_price='open',
):
    """Build signal columns used by the RNN strategy feed.

    Args:
        filepath: Input data path.
        fromdate: Optional period start datetime.
        todate: Optional period end datetime.
        bar_shift_minutes: Bar timestamp shift in minutes.
        lots: Strategy lot parameter (kept for compatibility).
        sltp_pips: Stop/TP distance in pips.
        x0: Probability weight.
        x1: Probability weight.
        x2: Probability weight.
        x3: Probability weight.
        x4: Probability weight.
        x5: Probability weight.
        x6: Probability weight.
        x7: Probability weight.
        rsi_period: RSI period.
        rsi_price: Input price for RSI calculation.

    Returns:
        DataFrame with RSI, probability, and buy/sell signal columns.
    """
    frame = load_mt5_csv(filepath, fromdate=fromdate, todate=todate, bar_shift_minutes=bar_shift_minutes)
    price_series = frame['open'] if str(rsi_price).lower() == 'open' else frame['close']
    frame['rsi'] = compute_rsi(price_series, rsi_period)
    frame['a1'] = frame['rsi'] / 100.0
    frame['a2'] = frame['rsi'].shift(rsi_period) / 100.0
    frame['a3'] = frame['rsi'].shift(rsi_period * 2) / 100.0
    frame['probability_short'] = get_probability(frame['a1'], frame['a2'], frame['a3'], x0, x1, x2, x3, x4, x5, x6, x7)
    frame['signal'] = frame['probability_short'] * 2.0 - 1.0
    frame['buy_signal'] = frame['signal'] < 0.0
    frame['sell_signal'] = frame['signal'] >= 0.0
    return frame.dropna(subset=['a1', 'a2', 'a3', 'probability_short', 'signal'])


