def finite_or_none(value):
    """Return the value if it is a finite number, otherwise None.

    Args:
        value: The numeric value to validate.

    Returns:
        The original value when finite, else None.
    """
    return value if value is not None and math.isfinite(value) else None


def calculate_ulcer_index(values):
    """Compute the Ulcer Index of an equity-curve value series.

    Args:
        values: Sequence of portfolio values in chronological order.

    Returns:
        The Ulcer Index (root-mean-square percentage drawdown from running
        peaks); 0.0 when fewer than two values are supplied.
    """
    if len(values) < 2:
        return 0.0
    peak = values[0]
    squared = 0.0
    for value in values:
        peak = max(peak, value)
        drawdown = (peak - value) / peak * 100.0 if peak > 0 else 0.0
        squared += drawdown ** 2
    return math.sqrt(squared / len(values))


def load_inputs(config):
    """Load the tradable and factor feeds and prepare the alpha-momentum inputs.

    Args:
        config: Parsed configuration whose ``data`` section provides the asset
            and factor proxy file paths and inclusive date bounds.

    Returns:
        A dict with the prepared ``inputs`` (signal frame and asset frames) and
        the effective ``fromdate`` and ``todate``.
    """
    data_cfg = config['data']
    fromdate = datetime.fromisoformat(data_cfg['fromdate'])
    todate = datetime.fromisoformat(data_cfg['todate'])
    asset_frames = {
        asset: load_mt5_csv(path, fromdate=fromdate, todate=todate)
        for asset, path in data_cfg['assets'].items()
    }
    factor_frames = {
        asset: load_mt5_csv(path, fromdate=fromdate, todate=todate)
        for asset, path in data_cfg['factor_proxies'].items()
    }
    inputs = prepare_alpha_momentum_inputs(asset_frames, factor_frames, config.get('params', {}))
    return {'inputs': inputs, 'fromdate': fromdate, 'todate': todate}


def build_cerebro(frame, config):
    """Assemble the Cerebro engine with the signal and asset feeds and analyzers.

    Args:
        frame: The loaded frame dict returned by :func:`load_inputs`.
        config: Parsed configuration providing cash, commission, and strategy
            parameters.

    Returns:
        A configured :class:`backtrader.Cerebro` ready to run the backtest.
    """
    cerebro = bt.Cerebro(stdstats=False)
    cerebro.broker.setcash(float(config['backtest']['initial_cash']))
    cerebro.broker.setcommission(commission=float(config['params'].get('commission_pct', 0.0005)))
    signal_feed = AlphaMomentumFeed(dataname=frame['inputs']['signal_df'], timeframe=bt.TimeFrame.Days, compression=1)
    cerebro.adddata(signal_feed, name='SIGNAL')
    for asset in ASSET_ORDER:
        asset_feed = bt.feeds.PandasData(dataname=frame['inputs']['asset_frames'][asset], timeframe=bt.TimeFrame.Days, compression=1)
        cerebro.adddata(asset_feed, name=asset)
    cerebro.addstrategy(AlphaMomentumStrategyBT, **config.get('params', {}))
    cerebro.addanalyzer(bt.analyzers.SharpeRatio, _name='sharpe', timeframe=bt.TimeFrame.Days, compression=1, factor=252, annualize=True, riskfreerate=0)
    cerebro.addanalyzer(bt.analyzers.Returns, _name='returns', timeframe=bt.TimeFrame.Days, tann=252)
    cerebro.addanalyzer(bt.analyzers.DrawDown, _name='drawdown')
    cerebro.addanalyzer(bt.analyzers.TradeAnalyzer, _name='trades')
    cerebro.addanalyzer(bt.analyzers.SQN, _name='sqn')
    return cerebro


def extract_metrics(strat, cerebro, frame, config):
    """Consolidate analyzer output and strategy counters into a metrics dict.

    Args:
        strat: The executed strategy instance holding counters, the broker value
            series, the rebalance count, and attached analyzers.
        cerebro: The Cerebro engine used to read the final broker value.
        frame: The loaded frame dict providing date bounds and bar count.
        config: Parsed configuration supplying the strategy name and initial
            cash baseline.

    Returns:
        A dict of performance metrics (returns, win rate, profit factor,
        drawdown, Sharpe, SQN, Ulcer index, and rebalance/trade counts) used by
        the assertions; total trades falls back to the rebalance count when no
        trades closed.
    """
    trades = strat.analyzers.trades.get_analysis()
    sharpe = strat.analyzers.sharpe.get_analysis()
    returns = strat.analyzers.returns.get_analysis()
    drawdown = strat.analyzers.drawdown.get_analysis()
    sqn = strat.analyzers.sqn.get_analysis()
    initial_cash = float(config['backtest']['initial_cash'])
    final_value = float(cerebro.broker.getvalue())
    broker_values = [value for _, value in getattr(strat, 'broker_value_series', [])] or [initial_cash, final_value]
    total_trades = trades.get('total', {}).get('closed', 0)
    won = trades.get('won', {}).get('total', 0)
    lost = trades.get('lost', {}).get('total', 0)
    gross_won = trades.get('won', {}).get('pnl', {}).get('total', 0) or 0
    gross_lost = abs(trades.get('lost', {}).get('pnl', {}).get('total', 0) or 0)
    if total_trades == 0:
        total_trades = int(getattr(strat, 'rebalance_count', 0))
    return {
        'strategy_name': config['strategy']['name'],
        'fromdate': frame['fromdate'],
        'todate': frame['todate'],
        'bars': len(frame['inputs']['signal_df']),
        'bar_num': strat.bar_num,
        'rebalance_count': strat.rebalance_count,
        'total_trades': total_trades,
        'won': won,
        'lost': lost,
        'win_rate': (won / total_trades * 100.0) if total_trades else 0.0,
        'profit_factor': (gross_won / gross_lost) if gross_lost else None,
        'initial_cash': initial_cash,
        'final_value': final_value,
        'net_pnl': final_value - initial_cash,
        'total_return_pct': (final_value / initial_cash - 1.0) * 100.0,
        'annual_return_pct': (returns.get('rnorm') or 0.0) * 100.0,
        'max_drawdown': drawdown.get('max', {}).get('drawdown', 0.0),
        'sharpe_ratio': finite_or_none(sharpe.get('sharperatio')),
        'sqn': finite_or_none(sqn.get('sqn')),
        'ulcer_index': calculate_ulcer_index(broker_values),
    }


def normalize(value):
    """Convert datetimes and non-finite floats into JSON-safe values.

    Args:
        value: The value to normalize.

    Returns:
        An ISO-format string for datetimes, None for NaN/inf floats, and the
        value unchanged otherwise.
    """
    if isinstance(value, datetime):
        return value.isoformat()
    if isinstance(value, float) and (math.isnan(value) or math.isinf(value)):
        return None
    return value


