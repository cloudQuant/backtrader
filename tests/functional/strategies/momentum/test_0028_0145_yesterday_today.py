class YesterdayTodayStrategy(bt.Strategy):
    """Strategy class implementing multi-timeframe breakout based on previous day's high/low.

    Attributes:
        params (dict): Configured strategy parameters.
    """
    params = dict(
        fixed_lot=1.0,
        stop_loss_pips=50,
        take_profit_pips=50,
        point_size=0.01,
        verbose=False,
    )

    def __init__(self):
        """Initialize indicators, backtest tracking metrics, and state variables."""
        self.data_m15 = self.datas[0]
        self.data_d1 = self.datas[1]
        self.last_bar_dt = None
        self.entry_order = None
        self.stop_order = None
        self.limit_order = None
        self.bar_num = 0
        self.buy_count = 0
        self.sell_count = 0
        self.trade_count = 0
        self.win_count = 0
        self.loss_count = 0

    def log(self, text):
        """Log message with current bar's timestamp.

        Args:
            text (str): Content string to log.
        """
        if not self.p.verbose:
            return
        dt = bt.num2date(self.data_m15.datetime[0])
        print(f'{dt.isoformat()}, {text}')

    def _new_bar(self):
        """Verify if a new bar has commenced on the M15 timeframe.

        Returns:
            bool: True if a new bar has started, otherwise False.
        """
        current = bt.num2date(self.data_m15.datetime[0])
        if self.last_bar_dt == current:
            return False
        self.last_bar_dt = current
        return True

    def _cancel_exits(self):
        """Cancel outstanding stop-loss and take-profit exit orders."""
        if self.stop_order is not None:
            self.cancel(self.stop_order)
            self.stop_order = None
        if self.limit_order is not None:
            self.cancel(self.limit_order)
            self.limit_order = None

    def _place_exits(self):
        """Calculate and submit new fixed stop-loss and take-profit exit orders."""
        if not self.position:
            return
        stop_distance = float(self.p.stop_loss_pips) * float(self.p.point_size)
        limit_distance = float(self.p.take_profit_pips) * float(self.p.point_size)
        size = abs(self.position.size)
        if self.position.size > 0:
            if stop_distance > 0:
                self.stop_order = self.sell(size=size, exectype=bt.Order.Stop, price=self.position.price - stop_distance)
            if limit_distance > 0:
                self.limit_order = self.sell(size=size, exectype=bt.Order.Limit, price=self.position.price + limit_distance, oco=self.stop_order)
        else:
            if stop_distance > 0:
                self.stop_order = self.buy(size=size, exectype=bt.Order.Stop, price=self.position.price + stop_distance)
            if limit_distance > 0:
                self.limit_order = self.buy(size=size, exectype=bt.Order.Limit, price=self.position.price - limit_distance, oco=self.stop_order)

    def next(self):
        """Execute the strategy decision logic on each new bar."""
        self.bar_num += 1
        if not self._new_bar():
            return
        if len(self.data_d1) < 2:
            return
        if self.position or self.entry_order is not None:
            return
        prev_high = float(self.data_d1.high[-1])
        prev_low = float(self.data_d1.low[-1])
        current_close = float(self.data_m15.close[0])
        size = max(0.01, float(self.p.fixed_lot))
        if current_close > prev_high:
            self.entry_order = self.buy(size=size)
            self.buy_count += 1
            self.log(f'OPEN LONG prev_high={prev_high:.5f} close={current_close:.5f}')
            return
        if current_close < prev_low:
            self.entry_order = self.sell(size=size)
            self.sell_count += 1
            self.log(f'OPEN SHORT prev_low={prev_low:.5f} close={current_close:.5f}')

    def notify_order(self, order):
        """Callback to handle order status updates.

        Args:
            order (bt.Order): The updated order instance.
        """
        if order.status in [order.Submitted, order.Accepted]:
            return
        if order == self.entry_order:
            if order.status == order.Completed:
                side = 'long' if order.isbuy() else 'short'
                self.log(f'ENTRY FILLED side={side} price={order.executed.price:.5f} size={order.executed.size}')
                self.entry_order = None
                self._place_exits()
                return
            if order.status in [order.Canceled, order.Margin, order.Rejected]:
                self.entry_order = None
                return
        if order == self.stop_order:
            if order.status == order.Completed:
                self.log(f'STOP FILLED price={order.executed.price:.5f} size={order.executed.size}')
                self.stop_order = None
                self.limit_order = None
                return
            if order.status in [order.Canceled, order.Margin, order.Rejected]:
                self.stop_order = None
                return
        if order == self.limit_order:
            if order.status == order.Completed:
                self.log(f'TAKE PROFIT FILLED price={order.executed.price:.5f} size={order.executed.size}')
                self.limit_order = None
                self.stop_order = None
                return
            if order.status in [order.Canceled, order.Margin, order.Rejected]:
                self.limit_order = None

    def notify_trade(self, trade):
        """Callback to handle closed trades and manage win/loss counts.

        Args:
            trade (bt.Trade): The closed trade instance.
        """
        if not trade.isclosed:
            return
        self.trade_count += 1
        if trade.pnlcomm >= 0:
            self.win_count += 1
        else:
            self.loss_count += 1
        self._cancel_exits()
        self.log(f'TRADE CLOSED pnl={trade.pnlcomm:.2f} net={self.broker.getvalue():.2f}')


