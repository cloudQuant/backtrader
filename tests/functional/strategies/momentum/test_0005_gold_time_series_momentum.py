class GoldTimeSeriesMomentumStrategy(bt.Strategy):
    """Single-asset time-series momentum strategy with volatility targeting.

    Rebalances monthly toward the volatility-scaled target exposure read from
    the feed, applies a percentage stop-loss, and tracks bar counts, buy/sell
    counts and broker value over time.
    """

    params = dict(
        stop_loss_pct=0.08,
        lookback_months=12,
        strong_threshold=0.1,
        allow_short=False,
        vol_lookback=20,
        target_vol=0.15,
        min_position_scale=0.5,
        max_position_scale=1.5,
        commission_pct=0.0005,
    )

    def __init__(self):
        """Reset bar/order counters and per-position direction and stop state."""
        self.bar_num = 0
        self.buy_count = 0
        self.sell_count = 0
        self.pending_order = None
        self.entry_direction = 0
        self.stop_price = None
        self.broker_value_series = []

    def next(self):
        """Record equity, enforce the stop, and rebalance on the monthly flag."""
        self.bar_num += 1
        self.broker_value_series.append((bt.num2date(self.data.datetime[0]), float(self.broker.getvalue())))
        if self.pending_order is not None:
            return
        close = float(self.data.close[0])
        high = float(self.data.high[0])
        low = float(self.data.low[0])
        if self.position:
            if self.entry_direction > 0 and self.stop_price is not None and low <= self.stop_price:
                self.sell_count += 1
                self.pending_order = self.close()
                return
            if self.entry_direction < 0 and self.stop_price is not None and high >= self.stop_price:
                self.buy_count += 1
                self.pending_order = self.close()
                return
        if float(self.data.rebalance_flag[0]) < 0.5:
            return
        target_pct = float(self.data.target_pct[0]) if self.data.target_pct[0] == self.data.target_pct[0] else 0.0
        current_size = float(self.position.size)
        if abs(target_pct) < 1e-8:
            if current_size != 0:
                if current_size > 0:
                    self.sell_count += 1
                else:
                    self.buy_count += 1
                self.pending_order = self.close()
            return
        direction = 1 if target_pct > 0 else -1
        if current_size == 0 or (current_size > 0 and direction < 0) or (current_size < 0 and direction > 0):
            if direction > 0:
                self.buy_count += 1
            else:
                self.sell_count += 1
        self.entry_direction = direction
        self.stop_price = close * (1.0 - float(self.p.stop_loss_pct)) if direction > 0 else close * (1.0 + float(self.p.stop_loss_pct))
        self.pending_order = self.order_target_percent(target=target_pct)

    def notify_order(self, order):
        """Clear the pending order and reset entry state once the book is flat."""
        if order.status in (order.Submitted, order.Accepted):
            return
        self.pending_order = None
        if not self.position:
            self.entry_direction = 0
            self.stop_price = None





