def resolve_ma_class(name):
    """Map a moving-average mode name to a backtrader indicator class.

    Args:
        name: The MA mode name (e.g. ``sma``, ``ema``, ``smma``, or a MODE_*
            alias).

    Returns:
        The matching backtrader moving-average indicator class, defaulting to
        the weighted moving average for unrecognised names.
    """
    mode = str(name).lower()
    if mode in {'mode_sma', 'sma'}:
        return bt.indicators.SimpleMovingAverage
    if mode in {'mode_ema', 'ema', 'mode_jjma', 'jjma', 'mode_jurx', 'jurx', 'mode_parma', 'parma', 'mode_t3', 't3', 'mode_vidya', 'vidya', 'mode_ama', 'ama'}:
        return bt.indicators.ExponentialMovingAverage
    if mode in {'mode_smma', 'smma'}:
        return bt.indicators.SmoothedMovingAverage
    return bt.indicators.WeightedMovingAverage


def resolve_price_line(data, mode):
    """Map a price mode name to the corresponding feed price line.

    Args:
        data: The data feed whose OHLC lines are referenced.
        mode: The price mode name (e.g. ``close``, ``median``, ``typical``,
            ``weighted``).

    Returns:
        The selected price line or derived price expression, defaulting to the
        close for unrecognised modes.
    """
    price_mode = str(mode).lower()
    if price_mode in {'price_close', 'close'}:
        return data.close
    if price_mode in {'price_open', 'open'}:
        return data.open
    if price_mode in {'price_high', 'high'}:
        return data.high
    if price_mode in {'price_low', 'low'}:
        return data.low
    if price_mode in {'price_median', 'median'}:
        return (data.high + data.low) / 2.0
    if price_mode in {'price_typical', 'typical'}:
        return (data.high + data.low + data.close) / 3.0
    if price_mode in {'price_weighted', 'weighted'}:
        return (2.0 * data.close + data.high + data.low) / 4.0
    if price_mode in {'price_simpl', 'simpl'}:
        return (data.open + data.close) / 2.0
    if price_mode in {'price_quarter', 'quarter'}:
        return (data.high + data.low + data.open + data.close) / 4.0
    return data.close


class BlauCMomentumStrategy(bt.Strategy):
    """Trade BlauCMomentum zero-line crossings or slope turns.

    Reads the BlauCMomentum oscillator at the signal bar and, depending on the
    ``mode`` (breakdown or twist), opens, closes, or reverses positions on
    zero-line crossings or oscillator turning points.
    """

    params = dict(
        mode='twist',
        xma_method='ema',
        xlength=1,
        xlength1=20,
        xlength2=5,
        xlength3=3,
        xphase=15,
        ipc1='price_close',
        ipc2='price_open',
        signal_bar=1,
        lot=0.1,
    )

    def __init__(self):
        """Build the BlauCMomentum indicator and reset counters."""
        self.indicator = bt.indicators.BlauCMomentumIndicator(
            self.data,
            xma_method=self.p.xma_method,
            xlength=self.p.xlength,
            xlength1=self.p.xlength1,
            xlength2=self.p.xlength2,
            xlength3=self.p.xlength3,
            xphase=self.p.xphase,
            ipc1=self.p.ipc1,
            ipc2=self.p.ipc2,
        )
        self.bar_num = 0
        self.buy_count = 0
        self.sell_count = 0
        self.trade_count = 0
        self.win_count = 0
        self.loss_count = 0
        self._position_was_open = False

    def log(self, text):
        """Print a timestamped log line for the current bar.

        Args:
            text: The message to log alongside the current bar datetime.
        """
        dt = bt.num2date(self.data.datetime[0])
        print(f'{dt.isoformat()}, {text}')

    def _signals(self):
        shift = max(1, int(self.p.signal_bar))
        mode = str(self.p.mode).lower()
        buy_open = sell_open = buy_close = sell_close = False
        if mode == 'breakdown':
            v0 = float(self.indicator.value[-shift])
            v1 = float(self.indicator.value[-shift - 1])
            if v1 > 0:
                if v0 <= 0:
                    buy_open = True
                sell_close = True
            if v1 < 0:
                if v0 >= 0:
                    sell_open = True
                buy_close = True
        else:
            v0 = float(self.indicator.value[-shift])
            v1 = float(self.indicator.value[-shift - 1])
            v2 = float(self.indicator.value[-shift - 2])
            if v1 < v2:
                if v0 >= v1:
                    buy_open = True
                sell_close = True
            if v1 > v2:
                if v0 <= v1:
                    sell_open = True
                buy_close = True
        return buy_open, sell_open, buy_close, sell_close

    def next(self):
        """Open, close, or reverse positions on the BlauCMomentum signals.

        Increments the bar counter, skips during warm-up, and derives the
        buy/sell open/close signals for the current bar. When in a position it
        closes or reverses on the appropriate signal; when flat it opens a long
        or short on a buy/sell-open signal.
        """
        self.bar_num += 1
        warmup = int(self.p.xlength) + int(self.p.xlength1) + int(self.p.xlength2) + int(self.p.xlength3) + int(self.p.signal_bar) + 5
        if len(self.data) < warmup + 2:
            return
        buy_open, sell_open, buy_close, sell_close = self._signals()
        value = float(self.indicator.value[0])
        if self.position:
            if self.position.size > 0:
                if buy_close and not sell_open:
                    self.log(f'close long value={value:.2f}')
                    self.close()
                    return
                if sell_open:
                    self.log(f'close long & sell value={value:.2f}')
                    self.close()
                    self.sell(size=self.p.lot)
                    return
            if self.position.size < 0:
                if sell_close and not buy_open:
                    self.log(f'close short value={value:.2f}')
                    self.close()
                    return
                if buy_open:
                    self.log(f'close short & buy value={value:.2f}')
                    self.close()
                    self.buy(size=self.p.lot)
                    return
        else:
            if buy_open:
                self.log(f'buy value={value:.2f}')
                self.buy(size=self.p.lot)
                return
            if sell_open:
                self.log(f'sell value={value:.2f}')
                self.sell(size=self.p.lot)
                return

    def notify_trade(self, trade):
        """Count entries on open and win/loss on close.

        Args:
            trade: The trade whose status changed; opening increments the
                buy/sell entry counter and closing updates win/loss counts.
        """
        if trade.isopen and not self._position_was_open:
            if trade.size > 0:
                self.buy_count += 1
            elif trade.size < 0:
                self.sell_count += 1
            self._position_was_open = True
            return
        if not trade.isclosed:
            return
        self.trade_count += 1
        if trade.pnlcomm >= 0:
            self.win_count += 1
        else:
            self.loss_count += 1
        self._position_was_open = False
        self.log(f'trade closed pnl={trade.pnlcomm:.2f}')


