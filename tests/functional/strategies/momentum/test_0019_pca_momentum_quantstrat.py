def get_sharpe_analyzer_kwargs(config):
    """Retrieve arguments for configuring the Sharpe Ratio analyzer based on timeframe.

    Args:
        config (dict): Backtest configuration dictionary.

    Returns:
        dict: Parameter dictionary for SharpeRatio initialization.
    """
    data_cfg = config.get("data", {}) if isinstance(config, dict) else {}
    timeframe_value = str(data_cfg.get("timeframe", "D1")).upper()
    if timeframe_value.startswith("M") and timeframe_value[1:].isdigit():
        compression = max(1, int(timeframe_value[1:]))
        return dict(
            timeframe=bt.TimeFrame.Minutes,
            compression=compression,
            factor=252 * 24 * 60 / compression,
            annualize=True,
            riskfreerate=0,
        )
    if timeframe_value.startswith("H") and timeframe_value[1:].isdigit():
        hours = max(1, int(timeframe_value[1:]))
        return dict(
            timeframe=bt.TimeFrame.Minutes,
            compression=hours * 60,
            factor=252 * 24 / hours,
            annualize=True,
            riskfreerate=0,
        )
    return dict(
        timeframe=bt.TimeFrame.Days,
        compression=1,
        factor=252,
        annualize=True,
        riskfreerate=0,
    )


def finite_or_none(x):
    """Return the value if it is finite, otherwise None.

    Args:
        x (float): Number to check.

    Returns:
        float or None: Checked value or None if non-finite.
    """
    return x if x and math.isfinite(x) else None


def calculate_ulcer_index(values):
    """Calculate the Ulcer Index drawdown volatility measure for portfolio values.

    Args:
        values (list of float): Time-series of portfolio/broker values.

    Returns:
        float: Calculated Ulcer Index value.
    """
    if len(values) < 2:
        return 0.0
    max_value = values[0]
    sum_squared = 0.0
    for v in values:
        if v > max_value:
            max_value = v
        drawdown = (max_value - v) / max_value * 100.0 if max_value > 0 else 0.0
        sum_squared += drawdown**2
    return math.sqrt(sum_squared / len(values))


