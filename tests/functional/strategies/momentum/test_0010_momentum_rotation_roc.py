class MomentumRotationROCStrategy(bt.Strategy):
    """Monthly cross-sectional rotation across the four precious metals.

    Reads the precomputed target weights from the signal feed and, on each
    rebalance flag, rotates every asset to its target size via
    ``order_target_size`` while tracking bar, buy/sell and rebalance counts.
    """

    params = dict(
        lot_size=1.0,
        roc_periods=[21, 63, 252],
        roc_weights=[0.2, 0.3, 0.5],
        top_n=1,
        rebalance_freq='M',
    )

    def __init__(self):
        """Bind the signal and asset feeds and reset counters and equity state."""
        self.signal = self.datas[0]
        self.asset_map = {name: self.getdatabyname(name) for name in ASSET_ORDER}
        self.order_refs = set()
        self.bar_num = 0
        self.buy_count = 0
        self.sell_count = 0
        self.rebalance_count = 0
        self.broker_value_series = []

    def _submit(self, order):
        """Track a submitted order's ref so the strategy waits for it to settle."""
        if order is not None:
            self.order_refs.add(order.ref)

    def _target_size(self, data, target_weight):
        """Convert a target portfolio weight into a contract size for a feed.

        Args:
            data: The asset data feed to size.
            target_weight: Desired portfolio weight for the asset.

        Returns:
            A size rounded to two decimals, or 0.0 when value, price or
            multiplier make a position impossible.
        """
        broker_value = float(self.broker.getvalue())
        price = float(data.close[0])
        if broker_value <= 0 or price <= 0:
            return 0.0
        comminfo = self.broker.getcommissioninfo(data)
        multiplier = float(getattr(comminfo.p, 'mult', 1.0) or 1.0)
        if multiplier <= 0:
            return 0.0
        raw_size = broker_value * float(self.p.lot_size) * float(target_weight) / (price * multiplier)
        return round(raw_size, 2)

    def next(self):
        """Record equity and rotate all assets to targets on a rebalance bar."""
        self.bar_num += 1
        self.broker_value_series.append((bt.num2date(self.signal.datetime[0]), float(self.broker.getvalue())))
        if self.order_refs:
            return
        if float(self.signal.rebalance_flag[0]) <= 0.5:
            return
        self.rebalance_count += 1
        for symbol in ASSET_ORDER:
            data = self.asset_map[symbol]
            target_weight = float(getattr(self.signal, f'{symbol.lower()}_weight')[0])
            current_size = float(self.getposition(data).size)
            target_size = self._target_size(data, target_weight)
            order = self.order_target_size(data=data, target=target_size)
            self._submit(order)
            if order is not None:
                if target_size > current_size:
                    self.buy_count += 1
                elif target_size < current_size:
                    self.sell_count += 1

    def notify_order(self, order):
        """Drop a settled order's ref so the next rebalance can proceed."""
        if order.status in (order.Submitted, order.Accepted):
            return
        self.order_refs.discard(order.ref)


