def get_sharpe_analyzer_kwargs(config):
    """Retrieve arguments for configuring the Sharpe Ratio analyzer based on timeframe.

    Args:
        config (dict): Backtest configuration dictionary.

    Returns:
        dict: Parameter dictionary for SharpeRatio initialization.
    """
    data_cfg = config.get('data', {}) if isinstance(config, dict) else {}
    timeframe_value = str(data_cfg.get('timeframe', 'D1')).upper()
    if timeframe_value.startswith('M') and timeframe_value[1:].isdigit():
        compression = max(1, int(timeframe_value[1:]))
        return dict(timeframe=bt.TimeFrame.Minutes, compression=compression, factor=252 * 24 * 60 / compression, annualize=True, riskfreerate=0)
    if timeframe_value.startswith('H') and timeframe_value[1:].isdigit():
        hours = max(1, int(timeframe_value[1:]))
        return dict(timeframe=bt.TimeFrame.Minutes, compression=hours * 60, factor=252 * 24 / hours, annualize=True, riskfreerate=0)
    return dict(timeframe=bt.TimeFrame.Days, compression=1, factor=252, annualize=True, riskfreerate=0)


def resolve_data_path(filename):
    """Resolve data file path relative to BASE_DIR and ensure it exists.

    Args:
        filename (str): Name or path of data file.

    Raises:
        FileNotFoundError: If the data file does not exist.

    Returns:
        Path: Resolved absolute path.
    """
    path = (BASE_DIR / filename).resolve()
    if not path.exists():
        raise FileNotFoundError(f'Data file not found: {path}')
    return path


def load_backtest_frame(config):
    """Load daily market data for Gold and Benchmark ETF, and prepare Dual Momentum features.

    Args:
        config (dict): Configuration dictionary.

    Raises:
        ValueError: If the loaded data frame is empty.

    Returns:
        dict: Loaded data frame dictionary containing Gold daily bars and aggregated monthly table.
    """
    data_cfg = config['data']
    params = dict(config.get('params', {}))
    fromdate = datetime.datetime.fromisoformat(data_cfg['fromdate'])
    todate = datetime.datetime.fromisoformat(data_cfg['todate'])
    gold_df = load_mt5_csv(
        resolve_data_path(data_cfg['gold_file']),
        fromdate=fromdate,
        todate=todate,
        bar_shift_minutes=data_cfg.get('bar_shift_minutes', 0),
    )
    benchmark_df = load_mt5_csv(
        resolve_data_path(data_cfg['benchmark_file']),
        fromdate=fromdate,
        todate=todate,
        bar_shift_minutes=data_cfg.get('bar_shift_minutes', 0),
    )
    frame, monthly_table = prepare_dual_momentum_features(gold_df, benchmark_df, params)
    if frame.empty:
        raise ValueError('Loaded data frame is empty after feature preparation')
    print(f"Loaded {len(frame)} bars: {frame.index[0]} -> {frame.index[-1]}")
    return {'data': frame, 'monthly_table': monthly_table, 'fromdate': fromdate, 'todate': todate, 'params': params}


def add_default_analyzers(cerebro, config):
    """Register default performance and statistics analyzers to Cerebro.

    Args:
        cerebro (bt.Cerebro): Cerebro engine instance.
        config (dict): Configuration dictionary.
    """
    sharpe_kwargs = get_sharpe_analyzer_kwargs(config)
    cerebro.addanalyzer(bt.analyzers.SharpeRatio, _name='sharpe', **sharpe_kwargs)
    cerebro.addanalyzer(bt.analyzers.Returns, _name='returns', timeframe=bt.TimeFrame.Days, tann=TRADING_DAYS_PER_YEAR)
    cerebro.addanalyzer(bt.analyzers.DrawDown, _name='drawdown')
    cerebro.addanalyzer(bt.analyzers.TradeAnalyzer, _name='trades')
    cerebro.addanalyzer(bt.analyzers.SQN, _name='sqn')


def build_cerebro(config, frame):
    """Construct and configure Cerebro instance with feed, analyzers and strategies.

    Args:
        config (dict): Backtest configuration.
        frame (dict): Loaded and processed data frame dictionary.

    Returns:
        bt.Cerebro: Configured Cerebro backtest engine.
    """
    bt_cfg = config['backtest']
    cerebro = bt.Cerebro(stdstats=True)
    cerebro.broker.setcash(bt_cfg['initial_cash'])
    comminfo = ComminfoFuturesPercent(
        commission=float(bt_cfg.get('commission', 0.0)),
        margin=float(bt_cfg.get('margin', 0.01)),
        mult=float(bt_cfg.get('multiplier', 1.0)),
    )
    cerebro.broker.addcommissioninfo(comminfo)
    feed = Mt5DualMomentumFeed(dataname=frame['data'], timeframe=bt.TimeFrame.Days, compression=1)
    cerebro.adddata(feed, name=f"{config['data']['gold_symbol']}_{config['data']['timeframe']}")
    cerebro.addstrategy(DualMomentumStrategy, **frame['params'])
    add_default_analyzers(cerebro, config)
    return cerebro


def normalize(value):
    """Normalize date and special float values for consistent JSON output.

    Args:
        value (any): Value to be normalized.

    Returns:
        any: Normalized value (e.g. ISO string for datetime, None for non-finite floats).
    """
    if value is None or isinstance(value, (str, int, float, bool)):
        return value
    isoformat = getattr(value, 'isoformat', None)
    if callable(isoformat):
        try:
            return value.isoformat(sep=' ')
        except TypeError:
            return value.isoformat()
    return str(value)


def finite_or_none(value):
    """Return the value if it is finite, otherwise None.

    Args:
        value (float): Number to check.

    Returns:
        float or None: Checked value or None if non-finite.
    """
    if value is None:
        return None
    if isinstance(value, float):
        if value != value:
            return None
        if value == float('inf') or value == float('-inf'):
            return None
    return value


def calculate_ulcer_index(equity_curve):
    """Calculate the Ulcer Index drawdown volatility measure for portfolio values.

    Args:
        equity_curve (list of float): Time-series of portfolio/broker values.

    Returns:
        float: Calculated Ulcer Index value.
    """
    if not equity_curve:
        return 0.0
    peak = None
    squared_drawdowns = []
    for value in equity_curve:
        peak = value if peak is None else max(peak, value)
        if peak and peak != 0:
            drawdown_pct = (value / peak - 1.0) * 100.0
            squared_drawdowns.append(drawdown_pct * drawdown_pct)
    if not squared_drawdowns:
        return 0.0
    return float(sum(squared_drawdowns) / len(squared_drawdowns)) ** 0.5


def extract_monthly_stats(monthly_table):
    """Extract monthly statistics (below MA percentages, large loss occurrences).

    Args:
        monthly_table (pd.DataFrame): Aggregated monthly indicator table.

    Returns:
        dict: Monthly performance metrics summary.
    """
    table = monthly_table.dropna(subset=['gold_momentum', 'benchmark_momentum'])
    if table.empty:
        return {
            'monthly_rows': 0,
            'gold_selected_months': 0,
            'cash_months': 0,
            'gold_selected_pct': 0.0,
        }
    total_months = int(len(table))
    gold_selected_months = int((table['gold_selected'] >= 0.5).sum())
    cash_months = total_months - gold_selected_months
    return {
        'monthly_rows': total_months,
        'gold_selected_months': gold_selected_months,
        'cash_months': cash_months,
        'gold_selected_pct': gold_selected_months / total_months * 100.0 if total_months else 0.0,
    }


def extract_metrics(strat, cerebro, frame, config):
    """Extract backtest results, returns, Sharpe ratio, and drawdowns.

    Args:
        strat (bt.Strategy): Run strategy instance containing observers/analyzers.
        cerebro (bt.Cerebro): Backtest Cerebro engine.
        frame (dict): Loaded data frame dictionary.
        config (dict): Strategy and backtest configuration dictionary.

    Returns:
        dict: Performance and trade metrics dict.
    """
    sharpe = strat.analyzers.sharpe.get_analysis()
    returns = strat.analyzers.returns.get_analysis()
    drawdown = strat.analyzers.drawdown.get_analysis()
    ta = strat.analyzers.trades.get_analysis()
    sqn = strat.analyzers.sqn.get_analysis()
    
    initial_cash = config['backtest']['initial_cash']
    final_value = cerebro.broker.getvalue()
    broker_values = [value for _, value in getattr(strat, 'broker_value_series', [])] or [initial_cash, final_value]
    
    # Extract trade stats from TradeAnalyzer
    total_trades = ta.get('total', {}).get('total', 0)
    won = ta.get('won', {}).get('total', 0)
    lost = ta.get('lost', {}).get('total', 0)
    gross_won = ta.get('won', {}).get('pnl', {}).get('total', 0) or 0
    gross_lost = abs(ta.get('lost', {}).get('pnl', {}).get('total', 0) or 0)
    
    res = {
        'strategy_name': config['strategy']['name'],
        'fromdate': frame['fromdate'],
        'todate': frame['todate'],
        'bars': len(frame['data']),
        'bar_num': strat.bar_num,
        'rebalance_count': strat.rebalance_count,
        'buy_count': strat.buy_count,
        'sell_count': strat.sell_count,
        'trade_count': total_trades,
        'win_count': won,
        'loss_count': lost,
        'gold_month_count': strat.gold_month_count,
        'cash_month_count': strat.cash_month_count,
        'switch_count': strat.switch_count,
        'initial_cash': initial_cash,
        'final_value': final_value,
        'net_pnl': final_value - initial_cash,
        'total_return_pct': (final_value / initial_cash - 1.0) * 100.0,
        'total_trades': total_trades,
        'won': won,
        'lost': lost,
        'win_rate': (won / total_trades * 100.0) if total_trades else 0.0,
        'profit_factor': (gross_won / gross_lost) if gross_lost else None,
        'max_drawdown': drawdown.get('max', {}).get('drawdown', 0.0),
        'sharpe_ratio': finite_or_none(sharpe.get('sharperatio')),
        'annual_return_pct': (returns.get('rnorm') or 0.0) * 100.0,
        'sqn': finite_or_none(sqn.get('sqn')),
        'ulcer_index': calculate_ulcer_index(broker_values),
    }
    res.update(extract_monthly_stats(frame['monthly_table']))
    return res


def run(plot=False):
    """Main execution function to run the strategy backtest.

    Args:
        plot (bool, optional): Whether to plot results. Defaults to False.

    Returns:
        tuple: (results, metrics, cerebro) backtest output.
    """
    config = _bt_load_config(_CONFIG, repo=_REPO)
    frame = load_backtest_frame(config)
    cerebro = build_cerebro(config, frame)
    print('\nStarting backtest...')
    results = cerebro.run()
    strat = results[0]
    metrics = extract_metrics(strat, cerebro, frame, config)

    if plot:
        cerebro.plot()
    return results, metrics, cerebro


def _close(actual, expected, *, tol, key):
    """Assert ``actual`` is finite and within ``tol`` of ``expected``."""
    assert actual is not None, f"{key}: expected={expected}, got=None"
    a = float(actual)
    assert math.isfinite(a), f"{key}: expected={expected}, got non-finite {actual}"
    assert abs(a - float(expected)) <= tol, (
        f"{key}: expected={expected}, got={a} (tol={tol})"
    )


