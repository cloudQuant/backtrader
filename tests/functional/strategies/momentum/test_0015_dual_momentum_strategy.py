def normalize(value):
    if value is None or isinstance(value, (str, int, float, bool)):
        return value
    isoformat = getattr(value, 'isoformat', None)
    if callable(isoformat):
        try:
            return value.isoformat(sep=' ')
        except TypeError:
            return value.isoformat()
    return str(value)


def finite_or_none(value):
    if value is None:
        return None
    if isinstance(value, float):
        if value != value:
            return None
        if value == float('inf') or value == float('-inf'):
            return None
    return value


def calculate_ulcer_index(equity_curve):
    if not equity_curve:
        return 0.0
    peak = None
    squared_drawdowns = []
    for value in equity_curve:
        peak = value if peak is None else max(peak, value)
        if peak and peak != 0:
            drawdown_pct = (value / peak - 1.0) * 100.0
            squared_drawdowns.append(drawdown_pct * drawdown_pct)
    if not squared_drawdowns:
        return 0.0
    return float(sum(squared_drawdowns) / len(squared_drawdowns)) ** 0.5


def extract_monthly_stats(monthly_table):
    table = monthly_table.dropna(subset=['gold_momentum', 'benchmark_momentum'])
    if table.empty:
        return {
            'monthly_rows': 0,
            'gold_selected_months': 0,
            'cash_months': 0,
            'gold_selected_pct': 0.0,
        }
    total_months = int(len(table))
    gold_selected_months = int((table['gold_selected'] >= 0.5).sum())
    cash_months = total_months - gold_selected_months
    return {
        'monthly_rows': total_months,
        'gold_selected_months': gold_selected_months,
        'cash_months': cash_months,
        'gold_selected_pct': gold_selected_months / total_months * 100.0 if total_months else 0.0,
    }


def extract_metrics(strat, cerebro, frame, config):
    """提取回测指标 - 参考 trend_pullback 实现"""
    sharpe = strat.analyzers.sharpe.get_analysis()
    returns = strat.analyzers.returns.get_analysis()
    drawdown = strat.analyzers.drawdown.get_analysis()
    ta = strat.analyzers.trades.get_analysis()
    sqn = strat.analyzers.sqn.get_analysis()
    
    initial_cash = config['backtest']['initial_cash']
    final_value = cerebro.broker.getvalue()
    broker_values = [value for _, value in getattr(strat, 'broker_value_series', [])] or [initial_cash, final_value]
    
    # 从 TradeAnalyzer 获取交易统计
    total_trades = ta.get('total', {}).get('total', 0)
    won = ta.get('won', {}).get('total', 0)
    lost = ta.get('lost', {}).get('total', 0)
    gross_won = ta.get('won', {}).get('pnl', {}).get('total', 0) or 0
    gross_lost = abs(ta.get('lost', {}).get('pnl', {}).get('total', 0) or 0)
    
    metrics = {
        'strategy_name': config['strategy']['name'],
        'fromdate': frame['fromdate'],
        'todate': frame['todate'],
        'bars': len(frame['data']),
        'bar_num': strat.bar_num,
        'rebalance_count': strat.rebalance_count,
        'buy_count': strat.buy_count,
        'sell_count': strat.sell_count,
        'trade_count': total_trades,
        'win_count': won,
        'loss_count': lost,
        'gold_month_count': strat.gold_month_count,
        'cash_month_count': strat.cash_month_count,
        'switch_count': strat.switch_count,
        'initial_cash': initial_cash,
        'final_value': final_value,
        'net_pnl': final_value - initial_cash,
        'total_return_pct': (final_value / initial_cash - 1.0) * 100.0,
        'total_trades': total_trades,
        'won': won,
        'lost': lost,
        'win_rate': (won / total_trades * 100.0) if total_trades else 0.0,
        'profit_factor': (gross_won / gross_lost) if gross_lost else None,
        'max_drawdown': drawdown.get('max', {}).get('drawdown', 0.0),
        'sharpe_ratio': finite_or_none(sharpe.get('sharperatio')),
        'annual_return_pct': (returns.get('rnorm') or 0.0) * 100.0,
        'sqn': finite_or_none(sqn.get('sqn')),
        'ulcer_index': calculate_ulcer_index(broker_values),
    }
    metrics.update(extract_monthly_stats(frame['monthly_table']))
    return metrics





