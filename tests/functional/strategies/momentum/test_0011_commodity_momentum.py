def prepare_commodity_momentum_features(df, params):
    """Compute momentum, trend filters, and monthly rebalance/target signals.

    Args:
        df: The daily OHLCV DataFrame to operate on.
        params: Strategy parameters supplying the momentum lookback (months) and
            the fast/slow trend moving-average windows.

    Returns:
        A copy of ``df`` augmented with ``momentum``, fast/slow trend MAs,
        ``trend_filter``, ``rebalance_signal``, and ``target_signal`` columns,
        with warm-up rows dropped.
    """
    out = df.copy()
    lookback_months = int(params.get('lookback_months', 12))
    trend_fast = int(params.get('trend_fast', 50))
    trend_slow = int(params.get('trend_slow', 200))
    lookback_days = lookback_months * 21
    close = out['close']
    out['momentum'] = close / close.shift(lookback_days) - 1.0
    out['trend_fast_ma'] = close.rolling(window=trend_fast).mean()
    out['trend_slow_ma'] = close.rolling(window=trend_slow).mean()
    out['trend_filter'] = ((out['trend_fast_ma'] > out['trend_slow_ma']) & (close > out['trend_slow_ma'])).astype(float)
    out['month_key'] = out.index.to_period('M').astype(str)
    out['rebalance_signal'] = (out['month_key'] != out['month_key'].shift(1)).astype(float)
    out['target_signal'] = 0.0
    rebalance_mask = out['rebalance_signal'] > 0.5
    out.loc[rebalance_mask & (out['momentum'] > 0) & (out['trend_filter'] > 0.5), 'target_signal'] = 1.0
    out.loc[rebalance_mask & ((out['momentum'] <= 0) | (out['trend_filter'] <= 0.5)), 'target_signal'] = 0.0
    out = out[['open', 'high', 'low', 'close', 'volume', 'openinterest', 'momentum', 'trend_fast_ma', 'trend_slow_ma', 'trend_filter', 'rebalance_signal', 'target_signal']].copy()
    return out.dropna()


