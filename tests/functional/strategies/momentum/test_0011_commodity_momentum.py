class CommodityMomentumStrategy(bt.Strategy):
    """Hold a full long when monthly momentum and trend agree, else stay flat.

    On each monthly rebalance bar it reads the precomputed target signal and
    sizes a full long position when momentum is positive and the trend filter is
    up, otherwise flattens, tracking buy/sell and win/loss counts.
    """

    params = dict(
        lot_size=1.0,
        lookback_months=12,
        rebalance_freq='M',
        trend_fast=50,
        trend_slow=200,
    )

    def __init__(self):
        """Reset counters, the pending order slot, and the last target signal."""
        self.bar_num = 0
        self.buy_count = 0
        self.sell_count = 0
        self.trade_count = 0
        self.win_count = 0
        self.loss_count = 0
        self.pending_order = None
        self.last_target_signal = None
        self.broker_value_series = []

    def _get_position_size(self, target_notional_pct=1.0, price=None):
        if target_notional_pct <= 0:
            return 0.0
        broker_value = float(self.broker.getvalue())
        execution_price = float(self.data.close[0] if price is None else price)
        if broker_value <= 0 or execution_price <= 0:
            return 0.0
        comminfo = self.broker.getcommissioninfo(self.data)
        multiplier = float(getattr(comminfo.p, 'mult', 1.0) or 1.0)
        if multiplier <= 0:
            return 0.0
        size = broker_value * float(target_notional_pct) / (execution_price * multiplier)
        return max(0.01, round(size, 2))

    def next(self):
        """Rebalance the long/flat position on monthly target-signal changes.

        Increments the bar counter, records broker value, and skips while an
        order is pending or off the monthly rebalance bar. When the target signal
        changes it sizes a full long (or flat) position via order_target_size and
        counts the direction as a buy or sell.
        """
        self.bar_num += 1
        self.broker_value_series.append((bt.num2date(self.data.datetime[0]), float(self.broker.getvalue())))
        if self.pending_order is not None:
            return
        if float(self.data.rebalance_signal[0]) <= 0.5:
            return
        target_signal = float(self.data.target_signal[0])
        if self.last_target_signal is not None and abs(target_signal - self.last_target_signal) < 1e-9:
            return
        current_size = float(self.position.size)
        target_size = self._get_position_size(target_notional_pct=float(self.p.lot_size) * target_signal) if target_signal > 0 else 0.0
        self.pending_order = self.order_target_size(target=target_size)
        if self.pending_order is not None:
            if target_size > current_size:
                self.buy_count += 1
            elif target_size < current_size:
                self.sell_count += 1
        self.last_target_signal = target_signal

    def notify_order(self, order):
        """Clear the pending order slot once an order leaves flight.

        Args:
            order: The order whose status changed; submitted/accepted states are
                ignored and any other terminal state clears the pending slot.
        """
        if order.status in (order.Submitted, order.Accepted):
            return
        self.pending_order = None

    def notify_trade(self, trade):
        """Tally closed trades into win/loss counters.

        Args:
            trade: The trade whose status changed; only closed trades count.
        """
        if not trade.isclosed:
            return
        self.trade_count += 1
        if trade.pnlcomm >= 0:
            self.win_count += 1
        else:
            self.loss_count += 1


