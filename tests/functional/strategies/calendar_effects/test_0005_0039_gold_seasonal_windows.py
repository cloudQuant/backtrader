class GoldSeasonalWindowsStrategy(bt.Strategy):
    """Holds a leveraged gold long only inside spring/autumn seasonal windows.

    Enters a leveraged long on the precomputed entry signal and closes on the
    exit signal, staying flat outside the two seasonal windows.
    """

    params = dict(
        leverage=1.5,
        spring_entry_month=3,
        spring_entry_td=10,
        spring_exit_month=6,
        spring_exit_td=2,
        autumn_entry_month=10,
        autumn_entry_td=12,
        autumn_exit_month=11,
        autumn_exit_td=19,
    )

    def __init__(self):
        """Initialize counters, pending-order state, and the value series."""
        self.bar_num = 0
        self.buy_count = 0
        self.sell_count = 0
        self.pending_order = None
        self.broker_value_series = []

    def _get_target_size(self, target_notional_pct, price=None):
        if target_notional_pct <= 0:
            return 0.0
        broker_value = float(self.broker.getvalue())
        execution_price = float(self.data.close[0] if price is None else price)
        if broker_value <= 0 or execution_price <= 0:
            return 0.0
        comminfo = self.broker.getcommissioninfo(self.data)
        multiplier = float(getattr(comminfo.p, 'mult', 1.0) or 1.0)
        if multiplier <= 0:
            return 0.0
        size = broker_value * float(target_notional_pct) / (execution_price * multiplier)
        return max(0.01, round(size, 2))

    def next(self):
        """Record broker value and trade the seasonal entry/exit signals.

        When flat, opens a leveraged long on an entry signal; when in a
        position, closes on an exit signal. Counts buys on entry and sells on
        exit and skips while an order is pending.
        """
        self.bar_num += 1
        self.broker_value_series.append((bt.num2date(self.data.datetime[0]), float(self.broker.getvalue())))
        if self.pending_order is not None:
            return
        entry_signal = float(self.data.entry_signal[0]) > 0.5
        exit_signal = float(self.data.exit_signal[0]) > 0.5
        if not self.position:
            if entry_signal:
                self.buy_count += 1
                target_size = self._get_target_size(float(self.p.leverage))
                self.pending_order = self.buy(size=target_size)
            return
        if exit_signal:
            self.sell_count += 1
            self.pending_order = self.close()

    def notify_order(self, order):
        """Clear the pending order once it reaches a terminal status.

        Args:
            order: The order whose status changed; submitted/accepted orders are
                ignored and any other terminal status clears the pending order.
        """
        if order.status in (order.Submitted, order.Accepted):
            return
        self.pending_order = None


