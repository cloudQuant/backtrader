class GoldTurnOfMonthStrategy(bt.Strategy):
    """Long-only gold seasonality strategy trading turn-of-month windows.

    Enters a full-equity long on the precomputed entry signal and exits on the
    exit signal or a fixed percentage stop-loss from the entry price.
    """

    params = dict(
        stop_loss_pct=0.02,
        turn_of_month_days=5,
        special_months=[5, 6, 8, 11],
        special_days=[1, 2, 3, 10, 11, 12],
        commission_pct=0.0005,
    )

    def __init__(self):
        """Initialize counters, pending-order state, and stop/entry tracking."""
        self.bar_num = 0
        self.buy_count = 0
        self.sell_count = 0
        self.pending_order = None
        self.entry_price = None
        self.stop_price = None
        self.broker_value_series = []

    def next(self):
        """Record broker value and manage entries/exits on calendar signals.

        When flat, opens a full-equity long on an entry signal and sets the
        stop price; when in a position, closes on the stop being hit or on an
        exit signal. Counts buys on entry and sells on exit.
        """
        self.bar_num += 1
        self.broker_value_series.append((bt.num2date(self.data.datetime[0]), float(self.broker.getvalue())))
        if self.pending_order is not None:
            return
        close = float(self.data.close[0])
        low = float(self.data.low[0])
        if self.position:
            if self.stop_price is not None and low <= self.stop_price:
                self.sell_count += 1
                self.pending_order = self.close()
                return
            if float(self.data.exit_signal[0]) > 0.5:
                self.sell_count += 1
                self.pending_order = self.close()
                return
            return
        if float(self.data.entry_signal[0]) > 0.5:
            self.buy_count += 1
            self.pending_order = self.order_target_percent(target=1.0)
            self.entry_price = close
            self.stop_price = close * (1.0 - float(self.p.stop_loss_pct))

    def notify_order(self, order):
        """Clear the pending order and reset stop state when flat.

        Args:
            order: The order whose status changed; submitted/accepted orders are
                ignored and any other terminal status clears the pending order.
        """
        if order.status in (order.Submitted, order.Accepted):
            return
        self.pending_order = None
        if not self.position:
            self.entry_price = None
            self.stop_price = None


