class CommoditySeasonalityFrontRunningStrategy(bt.Strategy):
    """Front-runs seasonal gold strength, scaling from partial to full exposure.

    Enters a partial long in the front-run month ahead of each strong month and
    scales to full during the strong months, exiting on the window end or a fixed
    percentage stop-loss.
    """

    params = dict(
        stop_loss_pct=0.03,
        strong_months=[1, 2, 9, 10, 11, 12],
        weak_months=[3, 7, 8],
        front_run_months_before=1,
        front_run_target_percent=0.75,
        strong_target_percent=1.0,
        commission_pct=0.0005,
    )

    def __init__(self):
        """Initialize counters, order/stop state, and seasonal day tallies."""
        self.bar_num = 0
        self.buy_count = 0
        self.sell_count = 0
        self.trade_count = 0
        self.win_count = 0
        self.loss_count = 0
        self.pending_order = None
        self.entry_price = None
        self.stop_price = None
        self.broker_value_series = []
        self.front_run_days = 0
        self.strong_days = 0
        self.weak_days = 0

    def next(self):
        """Record broker value and manage the front-running seasonal position.

        Tallies front-run/strong/weak days, and while in a position exits on a
        stop-loss hit or window-end exit signal or rescales to the current target;
        when flat it enters on an entry signal and sets the stop, counting buys
        and sells. Skips while an order is pending.
        """
        self.bar_num += 1
        self.broker_value_series.append((bt.num2date(self.data.datetime[0]), float(self.broker.getvalue())))
        if float(self.data.is_front_run_month[0]) > 0.5:
            self.front_run_days += 1
        if float(self.data.is_strong_month[0]) > 0.5:
            self.strong_days += 1
        if float(self.data.is_weak_month[0]) > 0.5:
            self.weak_days += 1
        if self.pending_order is not None:
            return
        close = float(self.data.close[0])
        low = float(self.data.low[0])
        if self.position:
            if self.stop_price is not None and low <= self.stop_price:
                self.sell_count += 1
                self.pending_order = self.close()
                return
            if float(self.data.exit_signal[0]) > 0.5 or float(self.data.target_percent[0]) <= 0:
                self.sell_count += 1
                self.pending_order = self.close()
                return
            target_pct = max(0.0, min(1.0, float(self.data.target_percent[0])))
            self.pending_order = self.order_target_percent(target=target_pct)
            return
        if float(self.data.entry_signal[0]) > 0.5 and float(self.data.target_percent[0]) > 0:
            self.buy_count += 1
            self.pending_order = self.order_target_percent(target=float(self.data.target_percent[0]))
            self.entry_price = close
            self.stop_price = close * (1.0 - float(self.p.stop_loss_pct))

    def notify_order(self, order):
        """Reset pending order and entry/stop context when order lifecycle ends."""
        if order.status in (order.Submitted, order.Accepted):
            return
        self.pending_order = None
        if not self.position:
            self.entry_price = None
            self.stop_price = None

    def notify_trade(self, trade):
        """Update trade counters after each closed position."""
        if not trade.isclosed:
            return
        self.trade_count += 1
        if trade.pnlcomm >= 0:
            self.win_count += 1
        else:
            self.loss_count += 1


