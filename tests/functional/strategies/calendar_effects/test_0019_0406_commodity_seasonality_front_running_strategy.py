def finite_or_none(value):
    """Return value when finite and non-null, else ``None``."""
    return value if value is not None and math.isfinite(value) else None


def calculate_ulcer_index(values):
    """Compute the Ulcer Index from an equity/drawdown series.

    Args:
        values: Iterable of portfolio values or draws.

    Returns:
        Ulcer Index as float.
    """
    if len(values) < 2:
        return 0.0
    peak = values[0]
    squared = 0.0
    for value in values:
        peak = max(peak, value)
        drawdown = (peak - value) / peak * 100.0 if peak > 0 else 0.0
        squared += drawdown ** 2
    return math.sqrt(squared / len(values))


def load_inputs(config):
    """Load raw data and construct seasonality features for the strategy."""
    data_cfg = config['data']
    fromdate = datetime.fromisoformat(data_cfg['fromdate'])
    todate = datetime.fromisoformat(data_cfg['todate'])
