class GoldSeasonalityRotationStrategy(bt.Strategy):
    """Rotates gold exposure by month bucket and long-term trend on rebalances.

    On each monthly rebalance the strategy reads the precomputed target percent
    and issues order_target_size to move gold exposure toward it, sized from
    broker value and the contract multiplier.
    """

    params = dict(
        strong_months=[1, 8, 9, 11, 12],
        weak_months=[2, 3, 6, 7],
        sma_window=200,
        strong_weight=1.0,
        strong_downtrend_weight=0.5,
        neutral_weight=0.5,
        neutral_downtrend_weight=0.25,
        weak_weight=0.0,
    )

    def __init__(self):
        """Initialize counters, pending-order state, and the last target percent."""
        self.bar_num = 0
        self.buy_count = 0
        self.sell_count = 0
        self.pending_order = None
        self.last_target_pct = 0.0
        self.broker_value_series = []

    def _get_target_size(self, target_notional_pct, price=None):
        if target_notional_pct <= 0:
            return 0.0
        broker_value = float(self.broker.getvalue())
        execution_price = float(self.data.close[0] if price is None else price)
        if broker_value <= 0 or execution_price <= 0:
            return 0.0
        comminfo = self.broker.getcommissioninfo(self.data)
        multiplier = float(getattr(comminfo.p, 'mult', 1.0) or 1.0)
        if multiplier <= 0:
            return 0.0
        size = broker_value * float(target_notional_pct) / (execution_price * multiplier)
        return max(0.01, round(size, 2))

    def next(self):
        """Record broker value and rebalance gold exposure on monthly signals.

        On a rebalance signal, sizes the target notional from the feed's target
        percent and issues order_target_size, counting a buy when the target
        exposure rises and a sell when it falls. Skips while an order is pending.
        """
        self.bar_num += 1
        self.broker_value_series.append((bt.num2date(self.data.datetime[0]), float(self.broker.getvalue())))
        if self.pending_order is not None:
            return
        if float(self.data.rebalance_signal[0]) <= 0.5:
            return
        target_pct = float(self.data.target_pct[0])
        target_size = self._get_target_size(target_pct)
        current_size = float(self.position.size)
        if abs(target_size - current_size) < 0.01:
            return
        if target_pct > self.last_target_pct:
            self.buy_count += 1
        elif target_pct < self.last_target_pct:
            self.sell_count += 1
        self.pending_order = self.order_target_size(target=target_size)
        self.last_target_pct = target_pct

    def notify_order(self, order):
        """Clear the pending order once it reaches a terminal status.

        Args:
            order: The order whose status changed; submitted/accepted orders are
                ignored and any other terminal status clears the pending order.
        """
        if order.status in (order.Submitted, order.Accepted):
            return
        self.pending_order = None





