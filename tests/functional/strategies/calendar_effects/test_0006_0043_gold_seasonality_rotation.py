def load_mt5_csv(filepath, fromdate=None, todate=None):
    """Load an MT5-exported daily CSV into an OHLCV DataFrame.

    Handles both tab- and comma-separated exports and tolerates timestamps with
    or without seconds.

    Args:
        filepath: Path to the MT5 export file.
        fromdate: Optional inclusive lower bound on the datetime index.
        todate: Optional inclusive upper bound on the datetime index.

    Returns:
        A DataFrame indexed by datetime with open, high, low, close, volume, and
        openinterest columns, sorted ascending and filtered to the date range.
    """
    with open(filepath, 'r', encoding='utf-8', errors='ignore') as handle:
        lines = [line.strip().strip('"') for line in handle.readlines() if line.strip()]
    cleaned = '\n'.join(lines)
    sep = '\t' if '\t' in lines[0] else ','
    df = pd.read_csv(io.StringIO(cleaned), sep=sep)
    dt_text = df['<DATE>'].astype(str) + ' ' + df['<TIME>'].astype(str)
    parsed = pd.to_datetime(dt_text, format='%Y.%m.%d %H:%M', errors='coerce')
    if parsed.isna().any():
        parsed = pd.to_datetime(dt_text, format='%Y.%m.%d %H:%M:%S', errors='coerce')
    df['datetime'] = parsed
    df = df.rename(columns={
        '<OPEN>': 'open',
        '<HIGH>': 'high',
        '<LOW>': 'low',
        '<CLOSE>': 'close',
        '<TICKVOL>': 'tick_volume',
        '<VOL>': 'real_volume',
    })
    df['openinterest'] = 0
    df['volume'] = df['tick_volume'] if 'tick_volume' in df.columns else 0
    df = df[['datetime', 'open', 'high', 'low', 'close', 'volume', 'openinterest']]
    df = df.dropna(subset=['datetime']).set_index('datetime').sort_index()
    if fromdate is not None:
        df = df[df.index >= fromdate]
    if todate is not None:
        df = df[df.index <= todate]
    return df


def prepare_seasonality_rotation_features(df, params):
    """Derive seasonality-plus-trend target exposure lines from a price frame.

    Buckets each month as strong/weak/neutral, computes a long SMA and up-trend
    flag, flags monthly rebalances, and maps each bar to a clipped target
    exposure percent based on its month bucket and trend state.

    Args:
        df: Daily OHLCV DataFrame indexed by datetime.
        params: Strategy parameter dict supplying the strong/weak month sets, SMA
            window, and the per-regime target weights.

    Returns:
        A DataFrame with OHLCV plus the ``month``, ``sma_long``, ``trend_up``,
        ``rebalance_signal``, and ``target_pct`` columns, with warm-up rows
        dropped.
    """
    strong_months = set(int(x) for x in params.get('strong_months', [1, 8, 9, 11, 12]))
    weak_months = set(int(x) for x in params.get('weak_months', [2, 3, 6, 7]))
    sma_window = int(params.get('sma_window', 200))
    strong_weight = float(params.get('strong_weight', 1.0))
    strong_downtrend_weight = float(params.get('strong_downtrend_weight', 0.5))
    neutral_weight = float(params.get('neutral_weight', 0.5))
    neutral_downtrend_weight = float(params.get('neutral_downtrend_weight', 0.25))
    weak_weight = float(params.get('weak_weight', 0.0))

    out = df.copy()
    out['month'] = out.index.month
    out['sma_long'] = out['close'].rolling(sma_window).mean()
    out['trend_up'] = (out['close'] > out['sma_long']).astype(float)
    out['rebalance_signal'] = (out['month'] != out['month'].shift(1)).astype(float)

    def calc_target(row):
        month = int(row['month'])
        trend_up = float(row['trend_up']) > 0.5
        if month in strong_months:
            return strong_weight if trend_up else strong_downtrend_weight
        if month in weak_months:
            return weak_weight
        return neutral_weight if trend_up else neutral_downtrend_weight

    out['target_pct'] = out.apply(calc_target, axis=1)
    out['target_pct'] = out['target_pct'].clip(lower=0.0, upper=1.0)

    cols = [
        'open', 'high', 'low', 'close', 'volume', 'openinterest',
        'month', 'sma_long', 'trend_up', 'rebalance_signal', 'target_pct'
    ]
    return out[cols].dropna(subset=['open', 'high', 'low', 'close', 'sma_long'])


