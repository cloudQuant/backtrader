class QuadWitchingSeasonalStrategy(bt.Strategy):
    """Trades directional gold positions in the post-quad-witching window.

    Goes short after bearish-month witching and long after bullish-month
    witching on the precomputed entry signal, holding until a stop-loss,
    take-profit, or the window-end exit signal closes the position.
    """

    params = dict(
        stop_loss=0.02,
        take_profit=0.03,
        position_size=0.90,
        bearish_months=[3, 6],
        bullish_months=[9, 12],
        holding_days=5,
        commission_pct=0.0005,
    )

    def __init__(self):
        """Initialize counters, pending-order state, and entry/direction tracking."""
        self.bar_num = 0
        self.buy_count = 0
        self.sell_count = 0
        self.pending_order = None
        self.entry_price = None
        self.trade_direction = 0
        self.broker_value_series = []

    def _get_position_size(self, target_notional_pct=1.0, price=None):
        broker_value = float(self.broker.getvalue())
        execution_price = float(self.data.close[0] if price is None else price)
        if broker_value <= 0 or execution_price <= 0:
            return 0.0
        comminfo = self.broker.getcommissioninfo(self.data)
        multiplier = float(getattr(comminfo.p, 'mult', 1.0) or 1.0)
        if multiplier <= 0:
            return 0.0
        direction = 1.0 if target_notional_pct >= 0 else -1.0
        size = broker_value * abs(float(target_notional_pct)) / (execution_price * multiplier)
        return direction * round(size, 2)

    def next(self):
        """Record broker value and trade the quad-witching window each bar.

        When flat, opens a directional position on an entry signal sized from
        broker value, multiplier, and direction. When in a position, exits on a
        stop-loss, take-profit, or the window-end exit signal, counting the
        closing leg as a buy or sell. Skips while an order is pending.
        """
        self.bar_num += 1
        self.broker_value_series.append((bt.num2date(self.data.datetime[0]), float(self.broker.getvalue())))
        if self.pending_order is not None:
            return
        entry = float(self.data.entry_signal[0]) > 0.5
        exit_now = float(self.data.exit_signal[0]) > 0.5
        direction = int(float(self.data.direction[0]))
        close = float(self.data.close[0])
        low = float(self.data.low[0])
        high = float(self.data.high[0])
        if self.position:
            if self.trade_direction > 0:
                if low <= self.entry_price * (1.0 - float(self.p.stop_loss)):
                    self.sell_count += 1
                    self.pending_order = self.close()
                    return
                if high >= self.entry_price * (1.0 + float(self.p.take_profit)):
                    self.sell_count += 1
                    self.pending_order = self.close()
                    return
            elif self.trade_direction < 0:
                if high >= self.entry_price * (1.0 + float(self.p.stop_loss)):
                    self.buy_count += 1
                    self.pending_order = self.close()
                    return
                if low <= self.entry_price * (1.0 - float(self.p.take_profit)):
                    self.buy_count += 1
                    self.pending_order = self.close()
                    return
            if exit_now:
                if self.trade_direction > 0:
                    self.sell_count += 1
                else:
                    self.buy_count += 1
                self.pending_order = self.close()
                return
            return
        if entry and direction != 0:
            self.entry_price = close
            self.trade_direction = direction
            target_size = self._get_position_size(target_notional_pct=float(self.p.position_size) * direction)
            if target_size > 0:
                self.buy_count += 1
            else:
                self.sell_count += 1
            self.pending_order = self.order_target_size(target=target_size)

    def notify_order(self, order):
        """Clear the pending order and reset entry/direction state when flat.

        Args:
            order: The order whose status changed; submitted/accepted orders are
                ignored and any other terminal status clears the pending order.
        """
        if order.status in (order.Submitted, order.Accepted):
            return
        self.pending_order = None
        if not self.position:
            self.entry_price = None
            self.trade_direction = 0





