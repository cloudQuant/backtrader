class CompositeSeasonalStrategy(bt.Strategy):
    """Composite seasonal strategy with dynamic target exposure and rebalance control."""

    params = dict(
        month_start_weight=0.8,
        month_end_weight=0.8,
        friday_weight=0.3,
        monday_weight=0.5,
        indian_wedding_weight=1.5,
        chinese_new_year_weight=1.2,
        summer_slowdown_weight=0.8,
        entry_threshold=0.8,
        max_abs_score=3.0,
        base_position_size=0.1,
        max_position_size=0.15,
        allow_short=True,
        commission_pct=0.0005,
    )

    def __init__(self):
        """Initialize counters and containers for position and signal tracking."""
        self.bar_num = 0
        self.buy_count = 0
        self.sell_count = 0
        self.strong_days = 0
        self.weak_days = 0
        self.neutral_days = 0
        self.pending_order = None
        self.broker_value_series = []

    def next(self):
        """Track portfolio value, accumulate signal days, and rebalance to target."""
        self.bar_num += 1
        self.broker_value_series.append((bt.num2date(self.data.datetime[0]), float(self.broker.getvalue())))
        if self.pending_order is not None:
            return

        if float(self.data.strong_signal[0]) > 0.5:
            self.strong_days += 1
        elif float(self.data.weak_signal[0]) > 0.5:
            self.weak_days += 1
        else:
            self.neutral_days += 1

        target = float(self.data.target_position[0])
        position_value_pct = 0.0
        if self.position.size and float(self.data.close[0]) > 0 and float(self.broker.getvalue()) > 0:
            position_value_pct = self.position.size * float(self.data.close[0]) / float(self.broker.getvalue())
        if abs(target - position_value_pct) < 0.02:
            return
        if target > position_value_pct:
            self.buy_count += 1
        elif target < position_value_pct:
            self.sell_count += 1
        self.pending_order = self.order_target_percent(target=target)

    def notify_order(self, order):
        """Clear the pending-order flag once an order exits active states.

        Args:
            order: The order whose status changed.
        """
        if order.status in (order.Submitted, order.Accepted):
            return
        self.pending_order = None





