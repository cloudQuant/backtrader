class GoldEndOfMonthSeasonalityStrategy(bt.Strategy):
    """Long-only gold strategy trading the end-of-month window of a target month.

    Enters a long on the precomputed entry signal and closes on the exit signal,
    sizing the position from broker value and the contract multiplier.
    """

    params = dict(
        lot_size=1.0,
        target_month=8,
        entry_offset_from_month_end=5,
        exit_offset_from_month_end=1,
    )

    def __init__(self):
        """Initialize counters, pending-order state, and the value series."""
        self.bar_num = 0
        self.buy_count = 0
        self.sell_count = 0
        self.trade_count = 0
        self.win_count = 0
        self.loss_count = 0
        self.pending_order = None
        self.broker_value_series = []

    def _get_position_size(self, target_notional_pct=1.0, price=None):
        if target_notional_pct <= 0:
            return 0.0
        broker_value = float(self.broker.getvalue())
        execution_price = float(self.data.close[0] if price is None else price)
        if broker_value <= 0 or execution_price <= 0:
            return 0.0
        comminfo = self.broker.getcommissioninfo(self.data)
        multiplier = float(getattr(comminfo.p, 'mult', 1.0) or 1.0)
        if multiplier <= 0:
            return 0.0
        size = broker_value * float(target_notional_pct) / (execution_price * multiplier)
        return max(0.01, round(size, 2))

    def next(self):
        """Record broker value and trade the end-of-month entry/exit signals.

        When flat, opens a long on an entry signal; when in a position, closes
        on an exit signal. Counts buys on entry and sells on exit and skips while
        an order is pending.
        """
        self.bar_num += 1
        self.broker_value_series.append((bt.num2date(self.data.datetime[0]), float(self.broker.getvalue())))
        if self.pending_order is not None:
            return
        if self.position:
            if float(self.data.exit_signal[0]) > 0.5:
                self.sell_count += 1
                self.pending_order = self.close()
            return
        if float(self.data.entry_signal[0]) > 0.5:
            self.buy_count += 1
            self.pending_order = self.buy(size=self._get_position_size(target_notional_pct=float(self.p.lot_size)))

    def notify_order(self, order):
        """Clear the pending order once it reaches a terminal status.

        Args:
            order: The order whose status changed; submitted/accepted orders are
                ignored and any other terminal status clears the pending order.
        """
        if order.status in (order.Submitted, order.Accepted):
            return
        self.pending_order = None

    def notify_trade(self, trade):
        """Tally closed trades into win/loss buckets.

        Args:
            trade: The trade being notified; only closed trades update counts.
        """
        if not trade.isclosed:
            return
        self.trade_count += 1
        if trade.pnlcomm >= 0:
            self.win_count += 1
        else:
            self.loss_count += 1


