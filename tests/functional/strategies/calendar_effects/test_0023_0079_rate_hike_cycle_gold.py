class RateHikeCycleGoldStrategy(bt.Strategy):
    """Event-driven gold strategy keyed to Fed rate-hike cycles.

    Reads the precomputed target signal from the feed and rebalances the gold
    position toward it via ``order_target_size`` whenever it changes, tracking
    bar counts, buy/sell counts, signal switches and hike events.
    """

    params = dict(
        position_size=0.5,
        hike_start_dates=['2015-12-16', '2022-03-16'],
        short_term_days=30,
        long_term_days=252,
        proxy_window=60,
        real_rate_threshold=0.5,
    )

    def __init__(self):
        """Bind the signal and gold feeds and reset counters and signal state."""
        self.signal = self.datas[0]
        self.gold = self.getdatabyname('XAUUSD')
        self.order_refs = set()
        self.bar_num = 0
        self.buy_count = 0
        self.sell_count = 0
        self.switch_count = 0
        self.hike_event_count = 0
        self.last_target_signal = 0.0
        self.broker_value_series = []

    def _submit(self, order):
        """Track a submitted order's ref so the strategy waits for it to settle."""
        if order is not None:
            self.order_refs.add(order.ref)

    def _target_size(self, target_signal):
        """Convert a signed target signal into a signed gold contract size.

        Args:
            target_signal: Signed target exposure; positive long, negative short.

        Returns:
            A signed size rounded to two decimals, or 0.0 when value, price or
            multiplier make a position impossible.
        """
        broker_value = float(self.broker.getvalue())
        price = float(self.gold.close[0])
        if broker_value <= 0 or price <= 0:
            return 0.0
        comminfo = self.broker.getcommissioninfo(self.gold)
        multiplier = float(getattr(comminfo.p, 'mult', 1.0) or 1.0)
        if multiplier <= 0:
            return 0.0
        raw_size = broker_value * float(target_signal) / (price * multiplier)
        return round(raw_size, 2)

    def next(self):
        """Rebalance gold toward the target signal whenever it changes."""
        self.bar_num += 1
        self.broker_value_series.append((bt.num2date(self.signal.datetime[0]), float(self.broker.getvalue())))
        if self.order_refs:
            return
        if float(self.signal.hike_start_flag[0]) > 0.5:
            self.hike_event_count += 1
        target_signal = float(self.signal.target_signal[0])
        if abs(target_signal - self.last_target_signal) < 1e-9:
            return
        current_size = float(self.getposition(self.gold).size)
        target_size = self._target_size(target_signal)
        order = self.order_target_size(data=self.gold, target=target_size)
        self._submit(order)
        if order is not None:
            if target_size > current_size:
                self.buy_count += 1
            elif target_size < current_size:
                self.sell_count += 1
            if self.last_target_signal != 0.0 and target_signal != self.last_target_signal:
                self.switch_count += 1
        self.last_target_signal = target_signal

    def notify_order(self, order):
        """Drop a settled order's ref so the next rebalance can proceed."""
        if order.status in (order.Submitted, order.Accepted):
            return
        self.order_refs.discard(order.ref)


