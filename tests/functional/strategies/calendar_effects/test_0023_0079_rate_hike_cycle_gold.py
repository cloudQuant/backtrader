class AssetCommissionInfo(bt.CommInfoBase):
    """Percentage futures commission scheme for the gold feed.

    Applies a percent commission on the notional (size * price * multiplier)
    under a margin/leverage model rather than the stock-like default.
    """

    params = (
        ('commission', 0.0002),
        ('margin', 0.01),
        ('mult', 100.0),
        ('stocklike', False),
        ('commtype', bt.CommInfoBase.COMM_PERC),
    )

    def _getcommission(self, size, price, pseudoexec, role=None):
        """Return the commission for a trade of ``size`` at ``price``.

        Args:
            size: Signed order size.
            price: Execution price.
            pseudoexec: Whether this is a pseudo (pre-trade) computation.
            role: Optional order role passed by the broker.

        Returns:
            The absolute commission as ``|size| * price * mult * commission``.
        """
        return abs(size) * price * self.p.mult * self.p.commission


def normalize(value):
    """Convert a metric value into a JSON-serialisable form.

    Args:
        value: A metric value of any type.

    Returns:
        Primitives unchanged, datetimes as ISO strings, and anything else
        coerced to ``str``.
    """
    if value is None or isinstance(value, (str, int, float, bool)):
        return value
    isoformat = getattr(value, 'isoformat', None)
    if callable(isoformat):
        try:
            return value.isoformat(sep=' ')
        except TypeError:
            return value.isoformat()
    return str(value)


def finite_or_none(value):
    """Return ``value`` when it is a finite number, otherwise ``None``.

    Args:
        value: A numeric value or ``None``.

    Returns:
        The original value if finite, else ``None`` for NaN/inf inputs.
    """
    if value is None:
        return None
    if isinstance(value, float) and (value != value or value in (float('inf'), float('-inf'))):
        return None
    return value


def calculate_ulcer_index(equity_curve):
    """Compute the Ulcer Index over an equity-value series.

    The Ulcer Index is the root mean square of percentage drawdowns from the
    running peak, measuring downside volatility.

    Args:
        equity_curve: Sequence of broker/equity values in chronological order.

    Returns:
        The Ulcer Index as a float, or 0.0 when no drawdowns can be computed.
    """
    if not equity_curve:
        return 0.0
    peak = None
    squared_drawdowns = []
    for value in equity_curve:
        peak = value if peak is None else max(peak, value)
        if peak and peak != 0:
            drawdown_pct = (value / peak - 1.0) * 100.0
            squared_drawdowns.append(drawdown_pct * drawdown_pct)
    if not squared_drawdowns:
        return 0.0
    return float(sum(squared_drawdowns) / len(squared_drawdowns)) ** 0.5


def load_inputs(config):
    """Load the gold and rate-proxy series and build the signal frame.

    Args:
        config: Resolved configuration mapping with ``data`` and ``params``.

    Returns:
        A dict with the aligned ``asset_frames``, the ``signal_df`` and the
        ``fromdate``/``todate`` datetimes.
    """
    data_cfg = config['data']
    fromdate = datetime.fromisoformat(data_cfg['fromdate'])
    todate = datetime.fromisoformat(data_cfg['todate'])
    asset_frames = {
        asset_name: load_mt5_csv((BASE_DIR / path).resolve(), fromdate=fromdate, todate=todate)
        for asset_name, path in data_cfg['assets'].items()
    }
    aligned_frames, signal_df = prepare_rate_hike_inputs(asset_frames, config.get('params', {}))
    return {
        'asset_frames': aligned_frames,
        'signal_df': signal_df,
        'fromdate': fromdate,
        'todate': todate,
    }


def build_cerebro(inputs, config):
    """Assemble Cerebro with the signal and gold feeds, commission and analyzers.

    Args:
        inputs: Mapping from :func:`load_inputs` with the aligned frames and
            signal frame.
        config: Resolved configuration with ``backtest`` and ``params``.

    Returns:
        A configured :class:`bt.Cerebro` instance ready to run.
    """
    bt_cfg = config['backtest']
    cerebro = bt.Cerebro(stdstats=False)
    cerebro.broker.setcash(float(bt_cfg['initial_cash']))
    signal_feed = RateHikeSignalFeed(dataname=inputs['signal_df'], timeframe=bt.TimeFrame.Days, compression=1)
    cerebro.adddata(signal_feed, name='SIGNAL')
    gold_feed = bt.feeds.PandasData(dataname=inputs['asset_frames']['XAUUSD'], timeframe=bt.TimeFrame.Days, compression=1)
    cerebro.adddata(gold_feed, name='XAUUSD')
    cerebro.broker.addcommissioninfo(
        AssetCommissionInfo(
            commission=float(bt_cfg.get('xauusd_commission', 0.0002)),
            margin=float(bt_cfg.get('xauusd_margin', 0.01)),
            mult=float(bt_cfg.get('xauusd_multiplier', 100.0)),
        ),
        name='XAUUSD',
    )
    cerebro.addstrategy(RateHikeCycleGoldStrategy, **config.get('params', {}))
    cerebro.addanalyzer(bt.analyzers.SharpeRatio, _name='sharpe', timeframe=bt.TimeFrame.Days, compression=1, factor=252, annualize=True, riskfreerate=0)
    cerebro.addanalyzer(bt.analyzers.Returns, _name='returns', timeframe=bt.TimeFrame.Days, tann=TRADING_DAYS_PER_YEAR)
    cerebro.addanalyzer(bt.analyzers.DrawDown, _name='drawdown')
    cerebro.addanalyzer(bt.analyzers.TradeAnalyzer, _name='trades')
    cerebro.addanalyzer(bt.analyzers.SQN, _name='sqn')
    return cerebro


def extract_metrics(strat, cerebro, inputs, config):
    """Collect performance metrics from the strategy and analyzers.

    Args:
        strat: The executed strategy instance.
        cerebro: The Cerebro engine after the run.
        inputs: Mapping from :func:`load_inputs` (frames, signal and bounds).
        config: Resolved configuration mapping.

    Returns:
        A dict of counts, signal-day stats, returns, risk and trade statistics
        used by the regression assertions.
    """
    sharpe = strat.analyzers.sharpe.get_analysis()
    returns = strat.analyzers.returns.get_analysis()
    drawdown = strat.analyzers.drawdown.get_analysis()
    trades = strat.analyzers.trades.get_analysis()
    sqn = strat.analyzers.sqn.get_analysis()
    initial_cash = float(config['backtest']['initial_cash'])
    final_value = float(cerebro.broker.getvalue())
    broker_values = [value for _, value in getattr(strat, 'broker_value_series', [])] or [initial_cash, final_value]
    total_trades = trades.get('total', {}).get('total', 0)
    won = trades.get('won', {}).get('total', 0)
    lost = trades.get('lost', {}).get('total', 0)
    gross_won = trades.get('won', {}).get('pnl', {}).get('total', 0) or 0
    gross_lost = abs(trades.get('lost', {}).get('pnl', {}).get('total', 0) or 0)
    signal_df = inputs['signal_df']
    return {
        'strategy_name': config['strategy']['name'],
        'fromdate': inputs['fromdate'],
        'todate': inputs['todate'],
        'bars': len(signal_df),
        'bar_num': strat.bar_num,
        'hike_event_count': strat.hike_event_count,
        'switch_count': strat.switch_count,
        'buy_count': strat.buy_count,
        'sell_count': strat.sell_count,
        'avg_real_rate_zscore': float(signal_df['real_rate_zscore'].mean()),
        'signal_days': int((signal_df['target_signal'] != 0).sum()),
        'long_signal_days': int((signal_df['target_signal'] > 0).sum()),
        'short_signal_days': int((signal_df['target_signal'] < 0).sum()),
        'initial_cash': initial_cash,
        'final_value': final_value,
        'net_pnl': final_value - initial_cash,
        'total_return_pct': (final_value / initial_cash - 1.0) * 100.0,
        'total_trades': total_trades,
        'won': won,
        'lost': lost,
        'win_rate': (won / total_trades * 100.0) if total_trades else 0.0,
        'profit_factor': (gross_won / gross_lost) if gross_lost else None,
        'max_drawdown': drawdown.get('max', {}).get('drawdown', 0.0),
        'sharpe_ratio': finite_or_none(sharpe.get('sharperatio')),
        'annual_return_pct': (returns.get('rnorm') or 0.0) * 100.0,
        'sqn': finite_or_none(sqn.get('sqn')),
        'ulcer_index': calculate_ulcer_index(broker_values),
    }


def _close(actual, expected, *, tol, key):
    """Assert ``actual`` is finite and within ``tol`` of ``expected``."""
    assert actual is not None, f"{key}: expected={expected}, got=None"
    a = float(actual)
    assert math.isfinite(a), f"{key}: expected={expected}, got non-finite {actual}"
    assert abs(a - float(expected)) <= tol, (
        f"{key}: expected={expected}, got={a} (tol={tol})"
    )


def _resolve_loader():
    """Locate the data-loading helper (varies by strategy)."""
    for name in ("load_inputs", "load_data", "load_backtest_frame", "prepare_inputs", "prepare_data"):
        fn = globals().get(name)
        if callable(fn):
            return fn
    raise RuntimeError("No inputs loader found in inlined module")


def _build_cerebro_compat(inputs, config):
    """Call build_cerebro with whichever signature the original used."""
    try:
        return build_cerebro(inputs, config)
    except TypeError:
        return build_cerebro(config, inputs)


