class SeasonalTradingStrategy(bt.Strategy):
    """Allocates by monthly seasonal state: full in strong, half neutral, flat weak.

    Each bar it reads the precomputed target percent and rebalances toward it via
    order_target_percent when current exposure drifts beyond a tolerance, tracking
    strong/weak/neutral day counts.
    """

    params = dict(
        rebalance_tolerance=0.05,
        min_history_years=5,
        strong_weight=1.0,
        neutral_weight=0.5,
        weak_weight=0.0,
        commission_pct=0.0005,
    )

    def __init__(self):
        """Initialize counters, pending-order state, and seasonal day tallies."""
        self.bar_num = 0
        self.buy_count = 0
        self.sell_count = 0
        self.trade_count = 0
        self.win_count = 0
        self.loss_count = 0
        self.pending_order = None
        self.broker_value_series = []
        self.strong_days = 0
        self.weak_days = 0
        self.neutral_days = 0

    def _current_exposure(self):
        broker_value = float(self.broker.getvalue())
        price = float(self.data.close[0])
        comminfo = self.broker.getcommissioninfo(self.data)
        multiplier = float(getattr(comminfo.p, 'mult', 1.0) or 1.0)
        if broker_value <= 0 or price <= 0 or multiplier <= 0:
            return 0.0
        return float(self.position.size) * price * multiplier / broker_value

    def next(self):
        """Record broker value and rebalance to the monthly target each bar.

        Tallies strong/weak/neutral days and rebalances toward the feed's target
        percent via order_target_percent when current exposure drifts beyond the
        tolerance, counting buys and sells. Skips while an order is pending.
        """
        self.bar_num += 1
        self.broker_value_series.append((bt.num2date(self.data.datetime[0]), float(self.broker.getvalue())))
        if float(self.data.is_strong_month[0]) > 0.5:
            self.strong_days += 1
        elif float(self.data.is_weak_month[0]) > 0.5:
            self.weak_days += 1
        else:
            self.neutral_days += 1
        if self.pending_order is not None:
            return
        target_percent = float(self.data.target_percent[0])
        current_exposure = self._current_exposure()
        if abs(target_percent - current_exposure) <= float(self.p.rebalance_tolerance):
            return
        if target_percent > current_exposure:
            self.buy_count += 1
        elif target_percent < current_exposure:
            self.sell_count += 1
        self.pending_order = self.order_target_percent(target=target_percent)

    def notify_order(self, order):
        """Clear the pending order once it reaches a terminal status.

        Args:
            order: The order whose status changed; submitted/accepted orders are
                ignored and any other terminal status clears the pending order.
        """
        if order.status in (order.Submitted, order.Accepted):
            return
        self.pending_order = None

    def notify_trade(self, trade):
        """Tally closed trades into win/loss buckets.

        Args:
            trade: The trade being notified; only closed trades update counts.
        """
        if not trade.isclosed:
            return
        self.trade_count += 1
        if trade.pnlcomm >= 0:
            self.win_count += 1
        else:
            self.loss_count += 1


