def load_mt5_csv(filepath, fromdate=None, todate=None):
    """Load a MetaTrader 5 daily CSV export into an OHLCV frame.

    Handles both tab- and comma-separated exports and tolerant datetime
    parsing for minute or second precision timestamps.

    Args:
        filepath: Path to the MT5 ``.csv`` export to read.
        fromdate: Optional inclusive lower bound for the datetime index.
        todate: Optional inclusive upper bound for the datetime index.

    Returns:
        A pandas DataFrame indexed by datetime with ``open``, ``high``, ``low``,
        ``close``, ``volume`` and ``openinterest`` columns, sorted ascending.
    """
    with open(filepath, 'r', encoding='utf-8', errors='ignore') as handle:
        lines = [line.strip().strip('"') for line in handle.readlines() if line.strip()]
    cleaned = '\n'.join(lines)
    sep = '\t' if '\t' in lines[0] else ','
    df = pd.read_csv(io.StringIO(cleaned), sep=sep)
    dt_text = df['<DATE>'].astype(str) + ' ' + df['<TIME>'].astype(str)
    parsed = pd.to_datetime(dt_text, format='%Y.%m.%d %H:%M', errors='coerce')
    if parsed.isna().any():
        parsed = pd.to_datetime(dt_text, format='%Y.%m.%d %H:%M:%S', errors='coerce')
    df['datetime'] = parsed
    df = df.rename(columns={
        '<OPEN>': 'open', '<HIGH>': 'high', '<LOW>': 'low',
        '<CLOSE>': 'close', '<TICKVOL>': 'tick_volume', '<VOL>': 'real_volume',
    })
    df['openinterest'] = 0
    df['volume'] = df['tick_volume'] if 'tick_volume' in df.columns else 0
    df = df[['datetime', 'open', 'high', 'low', 'close', 'volume', 'openinterest']]
    df = df.dropna(subset=['datetime']).set_index('datetime').sort_index()
    if fromdate is not None:
        df = df[df.index >= fromdate]
    if todate is not None:
        df = df[df.index <= todate]
    return df


def is_first_friday(ts):
    """Return True if the timestamp falls on the month's first Friday.

    The first Friday of the month approximates the US jobs-report release date.

    Args:
        ts: A pandas/``datetime`` timestamp to test.

    Returns:
        True when ``ts`` is the first Friday of its month.
    """
    month_calendar = calendar.monthcalendar(ts.year, ts.month)
    first_friday = month_calendar[0][calendar.FRIDAY]
    if first_friday == 0:
        first_friday = month_calendar[1][calendar.FRIDAY]
    return ts.day == first_friday


def prepare_jobs_report_new_high_features(df, params):
    """Compute the jobs-report-day, new-high, strong-day and entry signals.

    Derives the daily return, the first-Friday jobs-report flag, the rolling
    prior high and new-high flag, a strong-up-day flag, and combines them into a
    single entry-signal column.

    Args:
        df: OHLCV DataFrame indexed by datetime.
        params: Strategy parameter mapping providing the new-high lookback and
            the minimum daily gain.

    Returns:
        A DataFrame with the OHLCV columns plus the calendar/breakout and entry
        signal columns, with warm-up rows dropped.
    """
    out = df.copy()
    new_high_period = int(params.get('new_high_period', 20))
    min_gain_pct = float(params.get('min_gain_pct', 0.005))

    out['daily_return'] = out['close'].pct_change()
    out['jobs_report_day'] = pd.Index(out.index).map(is_first_friday).astype(float)
    out['prior_high'] = out['close'].rolling(new_high_period).max().shift(1)
    out['is_new_high'] = (out['close'] > out['prior_high']).astype(float)
    out['strong_up_day'] = (out['daily_return'] >= min_gain_pct).astype(float)
    out['entry_signal'] = (
        (out['jobs_report_day'] > 0.5)
        & (out['is_new_high'] > 0.5)
        & (out['strong_up_day'] > 0.5)
    ).astype(float)
    cols = [
        'open', 'high', 'low', 'close', 'volume', 'openinterest',
        'daily_return', 'jobs_report_day', 'prior_high', 'is_new_high', 'strong_up_day', 'entry_signal',
    ]
    return out[cols].dropna()


