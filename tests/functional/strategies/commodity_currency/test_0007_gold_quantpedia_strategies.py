class GoldQuantpediaStrategy(bt.Strategy):
    """Long-only strategy rebalancing to the precomputed composite target.

    Sizes a gold position proportionally to the combined anomaly score read from
    the feed, rebalancing via ``order_target_size`` whenever the desired
    exposure changes, and records bar counts, order counts and broker value.
    """

    params = dict(
        trend_lookback_months=12,
        skew_lookback_months=24,
        payday_window_days=5,
        trend_weight=0.5,
        skew_weight=0.3,
        payday_weight=0.2,
        entry_threshold=0.35,
        commission_pct=0.0002,
    )

    def __init__(self):
        """Reset bar/order counters, target tracking and the equity series."""
        self.bar_num = 0
        self.buy_count = 0
        self.sell_count = 0
        self.pending_order = None
        self.last_target = 0.0
        self.broker_value_series = []

    def _get_target_size(self, target_notional_pct, price=None):
        """Convert a target notional percentage into a contract size.

        Args:
            target_notional_pct: Desired exposure as a fraction of broker value.
            price: Optional execution price; defaults to the current close.

        Returns:
            A non-negative size rounded to two decimals, or 0.0 when the target,
            value, price or multiplier make a position impossible.
        """
        if target_notional_pct <= 0:
            return 0.0
        broker_value = float(self.broker.getvalue())
        execution_price = float(self.data.close[0] if price is None else price)
        if broker_value <= 0 or execution_price <= 0:
            return 0.0
        comminfo = self.broker.getcommissioninfo(self.data)
        multiplier = float(getattr(comminfo.p, 'mult', 1.0) or 1.0)
        if multiplier <= 0:
            return 0.0
        size = broker_value * float(target_notional_pct) / (execution_price * multiplier)
        return max(0.0, round(size, 2))

    def next(self):
        """Record equity and rebalance toward the composite target each bar."""
        self.bar_num += 1
        self.broker_value_series.append((bt.num2date(self.data.datetime[0]), float(self.broker.getvalue())))
        if self.pending_order is not None:
            return
        target_pct = float(self.data.target_pct[0]) if self.data.target_pct[0] == self.data.target_pct[0] else 0.0
        target_size = self._get_target_size(target_pct)
        current_size = float(self.position.size)
        if abs(target_pct - self.last_target) < 1e-8 and abs(target_size - current_size) < 0.01:
            return
        if target_pct > 0 and self.last_target <= 0:
            self.buy_count += 1
        elif target_pct <= 0 and self.last_target > 0:
            self.sell_count += 1
        self.pending_order = self.order_target_size(target=target_size)
        self.last_target = target_pct

    def notify_order(self, order):
        """Clear the pending-order slot once the order leaves the live states."""
        if order.status in (order.Submitted, order.Accepted):
            return
        self.pending_order = None





