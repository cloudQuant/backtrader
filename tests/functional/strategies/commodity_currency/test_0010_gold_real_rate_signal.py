class GoldRealRateSignalStrategy(bt.Strategy):
    """Long-only gold strategy driven by a falling-real-rate signal.

    On each monthly rebalance bar it reads the precomputed long signal and
    target exposure from the feed and sizes the gold position via
    ``order_target_percent``, tracking bar counts and buy/sell counts.
    """

    params = dict(
        signal_window=63,
        trend_window=126,
        entry_threshold=0.0,
        stop_loss_pct=0.08,
        base_position_pct=0.5,
        max_position_pct=1.0,
        vol_window=21,
        high_vol_threshold=0.25,
        rebalance_frequency='monthly',
        commission_pct=0.0005,
    )

    def __init__(self):
        """Reset bar/order counters and the broker-value tracking series."""
        self.bar_num = 0
        self.buy_count = 0
        self.sell_count = 0
        self.pending_order = None
        self.broker_value_series = []

    def next(self):
        """Record equity and rebalance gold to the target on a monthly bar."""
        self.bar_num += 1
        self.broker_value_series.append((bt.num2date(self.data.datetime[0]), float(self.broker.getvalue())))
        if self.pending_order is not None:
            return
        if float(self.data.rebalance_signal[0]) <= 0.5:
            return
        target_pct = float(self.data.target_pct[0]) if float(self.data.long_signal[0]) > 0.5 else 0.0
        current_size = self.position.size
        self.pending_order = self.order_target_percent(target=target_pct)
        if self.pending_order is not None:
            if target_pct > 0 and current_size <= 0:
                self.buy_count += 1
            elif target_pct == 0 and current_size > 0:
                self.sell_count += 1

    def notify_order(self, order):
        """Clear the pending-order slot once the order leaves the live states."""
        if order.status in (order.Submitted, order.Accepted):
            return
        self.pending_order = None


