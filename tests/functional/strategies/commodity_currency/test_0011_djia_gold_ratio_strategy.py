class DJIAGoldRatioStrategy(bt.Strategy):
    """Long gold when its strength versus the DJIA confirms, flat otherwise."""

    params = dict(
        short_ma_window=50,
        long_ma_window=200,
        percentile_window=504,
        upper_percentile=0.7,
        lower_percentile=0.3,
        gold_momentum_window=63,
        rebalance_frequency='weekly',
        commission_pct=0.0005,
    )

    def __init__(self):
        """Initialize bar/trade counters, the pending order, and equity series."""
        self.bar_num = 0
        self.buy_count = 0
        self.sell_count = 0
        self.pending_order = None
        self.broker_value_series = []

    def next(self):
        """Record equity and move toward the target weight on rebalance bars."""
        self.bar_num += 1
        self.broker_value_series.append((bt.num2date(self.data.datetime[0]), float(self.broker.getvalue())))
        if self.pending_order is not None:
            return
        if float(self.data.rebalance_signal[0]) <= 0.5:
            return
        target_pct = float(self.data.target_pct[0]) if float(self.data.long_signal[0]) > 0.5 else 0.0
        current_size = self.position.size
        self.pending_order = self.order_target_percent(target=target_pct)
        if self.pending_order is not None:
            if target_pct > 0 and current_size <= 0:
                self.buy_count += 1
            elif target_pct == 0 and current_size > 0:
                self.sell_count += 1

    def notify_order(self, order):
        """Clear the pending order once it is no longer in a live state."""
        if order.status in (order.Submitted, order.Accepted):
            return
        self.pending_order = None


