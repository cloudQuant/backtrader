def load_mt5_csv(filepath, fromdate=None, todate=None):
    """Load a MetaTrader 5 daily CSV export into an OHLCV DataFrame.

    Args:
        filepath: Path to the MT5 export (tab- or comma-separated).
        fromdate: Optional inclusive lower bound on the datetime index.
        todate: Optional inclusive upper bound on the datetime index.

    Returns:
        pandas.DataFrame: Indexed by datetime with open/high/low/close/volume/
        openinterest columns, sorted ascending and sliced to the range.
    """
    with open(filepath, 'r', encoding='utf-8', errors='ignore') as handle:
        lines = [line.strip().strip('"') for line in handle.readlines() if line.strip()]
    cleaned = '\n'.join(lines)
    sep = '\t' if '\t' in lines[0] else ','
    df = pd.read_csv(io.StringIO(cleaned), sep=sep)
    dt_text = df['<DATE>'].astype(str) + ' ' + df['<TIME>'].astype(str)
    parsed = pd.to_datetime(dt_text, format='%Y.%m.%d %H:%M', errors='coerce')
    if parsed.isna().any():
        parsed = pd.to_datetime(dt_text, format='%Y.%m.%d %H:%M:%S', errors='coerce')
    df['datetime'] = parsed
    df = df.rename(columns={
        '<OPEN>': 'open', '<HIGH>': 'high', '<LOW>': 'low', '<CLOSE>': 'close',
        '<TICKVOL>': 'tick_volume', '<VOL>': 'real_volume',
    })
    df['openinterest'] = 0
    df['volume'] = df['tick_volume'] if 'tick_volume' in df.columns else 0
    df = df[['datetime', 'open', 'high', 'low', 'close', 'volume', 'openinterest']]
    df = df.dropna(subset=['datetime']).set_index('datetime').sort_index()
    if fromdate is not None:
        df = df[df.index >= fromdate]
    if todate is not None:
        df = df[df.index <= todate]
    return df


def prepare_skewness_data(price_map, params):
    """Align the commodity frames and compute each asset's rolling skewness.

    Args:
        price_map: Mapping of asset symbol to its raw OHLCV DataFrame.
        params: Strategy params providing the ``skewness_lookback`` window.

    Returns:
        tuple: ``(prepared, score_df)`` where ``prepared`` maps each symbol to
        its aligned OHLCV frame with a ``skewness`` line, and ``score_df`` is the
        combined skewness matrix with all-NaN rows dropped.
    """
    aligned_index = None
    prepared = {}
    skewness_lookback = int(params.get('skewness_lookback', 126))
    for symbol, frame in price_map.items():
        aligned_index = frame.index if aligned_index is None else aligned_index.intersection(frame.index)
    aligned_index = aligned_index.sort_values()
    for symbol, frame in price_map.items():
        px = frame.loc[aligned_index].copy()
        price_ratio = (px['close'] / px['close'].shift(1)).replace([np.inf, -np.inf], np.nan)
        price_ratio = price_ratio.where(price_ratio > 0)
        log_returns = np.log(price_ratio)
        skewness = log_returns.rolling(skewness_lookback).skew()
        prepared[symbol] = px[['open', 'high', 'low', 'close', 'volume', 'openinterest']].copy()
        prepared[symbol]['skewness'] = skewness.astype(float)
    score_df = pd.DataFrame({symbol: frame['skewness'] for symbol, frame in prepared.items()}, index=aligned_index)
    return prepared, score_df.dropna(how='all')


