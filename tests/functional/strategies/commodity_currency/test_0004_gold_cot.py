def finite_or_none(value):
    """Return the value when it is finite, otherwise None.

    Args:
        value: A numeric value or None.

    Returns:
        ``value`` when it is not None and finite; otherwise None.
    """
    return value if value is not None and math.isfinite(value) else None


def calculate_ulcer_index(values):
    """Compute the ulcer index of an equity curve.

    Args:
        values: Sequence of portfolio values ordered by time.

    Returns:
        The ulcer index (root-mean-square percentage drawdown); 0.0 when fewer
        than two values are supplied.
    """
    if len(values) < 2:
        return 0.0
    peak = values[0]
    squared = 0.0
    for value in values:
        peak = max(peak, value)
        drawdown = (peak - value) / peak * 100.0 if peak > 0 else 0.0
        squared += drawdown ** 2
    return math.sqrt(squared / len(values))



def load_cached_cot_frame(cache_path, fromdate, todate):
    """Load cached COT data and filter it to the backtest date range.

    Args:
        cache_path: Path to the cached COT CSV file.
        fromdate: Inclusive lower bound for the release-date index.
        todate: Inclusive upper bound for the release-date index.

    Returns:
        A DataFrame indexed by release date, sorted and filtered to the range.
    """
    cot_df = pd.read_csv(cache_path)
    cot_df['release_date'] = pd.to_datetime(cot_df['release_date'])
    cot_df = cot_df.dropna(subset=['release_date']).set_index('release_date').sort_index()
    return cot_df[(cot_df.index >= fromdate) & (cot_df.index <= todate)]


def load_inputs(config):
    """Load and align price/COT inputs, returning a prepared backtest frame."""
    data_cfg = config['data']
    fromdate = datetime.fromisoformat(data_cfg['fromdate'])
    todate = datetime.fromisoformat(data_cfg['todate'])
    price_df = load_mt5_csv(data_cfg['price_file'], fromdate=fromdate, todate=todate)
    cache_value = data_cfg.get('cot_cache_file')
    cache_path = (BASE_DIR / cache_value).resolve() if cache_value else None
    if cache_path and cache_path.exists():
        cot_df = load_cached_cot_frame(cache_path, fromdate, todate)
    else:
        cot_df = load_cot_legacy_gold(
            cot_url=data_cfg['cot_url'],
            market_name=data_cfg['cot_market_name'],
            exclude_name=data_cfg.get('cot_exclude_name'),
            start_year=fromdate.year,
            end_year=todate.year,
        )
        cot_df = cot_df[(cot_df.index >= fromdate) & (cot_df.index <= todate)]
    frame = prepare_gold_cot_features(price_df, cot_df, config.get('params', {}))
    return {'data': frame, 'fromdate': fromdate, 'todate': todate}


def build_cerebro(frame, config):
    """Build Cerebro with the gold weekly signal feed and configured analyzers."""
    cerebro = bt.Cerebro(stdstats=False)
    cerebro.broker.setcash(float(config['backtest']['initial_cash']))
    cerebro.broker.setcommission(commission=float(config['backtest'].get('commission', 0.0)))
    feed = GoldCotFeed(dataname=frame['data'], timeframe=bt.TimeFrame.Weeks, compression=1)
    cerebro.adddata(feed, name=f"{config['data']['symbol']}_{config['data']['timeframe']}")
    cerebro.addstrategy(GoldCotStrategy, **config.get('params', {}))
    cerebro.addanalyzer(bt.analyzers.SharpeRatio, _name='sharpe', timeframe=bt.TimeFrame.Weeks, compression=1, factor=52, annualize=True, riskfreerate=0)
    cerebro.addanalyzer(bt.analyzers.TradeAnalyzer, _name='trades')
    cerebro.addanalyzer(bt.analyzers.DrawDown, _name='drawdown')
    cerebro.addanalyzer(bt.analyzers.Returns, _name='returns', tann=52)
    cerebro.addanalyzer(bt.analyzers.SQN, _name='sqn')
    return cerebro


def extract_metrics(strat, cerebro, frame, config):
    """Collect sharpe/returns/drawdown/trade metrics from analyzers and broker values."""
    sharpe = strat.analyzers.sharpe.get_analysis()
    returns = strat.analyzers.returns.get_analysis()
    drawdown = strat.analyzers.drawdown.get_analysis()
    trades = strat.analyzers.trades.get_analysis()
    sqn = strat.analyzers.sqn.get_analysis()
    initial_cash = float(config['backtest']['initial_cash'])
    final_value = float(cerebro.broker.getvalue())
    broker_values = [value for _, value in getattr(strat, 'broker_value_series', [])] or [initial_cash, final_value]
    total_trades = trades.get('total', {}).get('total', 0)
    won = trades.get('won', {}).get('total', 0)
    lost = trades.get('lost', {}).get('total', 0)
    gross_won = trades.get('won', {}).get('pnl', {}).get('total', 0) or 0
    gross_lost = abs(trades.get('lost', {}).get('pnl', {}).get('total', 0) or 0)
    return {
        'strategy_name': config['strategy']['name'],
        'fromdate': frame['fromdate'],
        'todate': frame['todate'],
        'bars': len(frame['data']),
        'bar_num': strat.bar_num,
        'buy_count': strat.buy_count,
        'sell_count': strat.sell_count,
        'trade_count': total_trades,
        'win_count': won,
        'loss_count': lost,
        'initial_cash': initial_cash,
        'final_value': final_value,
        'net_pnl': final_value - initial_cash,
        'total_return_pct': (final_value / initial_cash - 1.0) * 100.0,
        'total_trades': total_trades,
        'won': won,
        'lost': lost,
        'win_rate': (won / total_trades * 100.0) if total_trades else 0.0,
        'profit_factor': (gross_won / gross_lost) if gross_lost else None,
        'max_drawdown': drawdown.get('max', {}).get('drawdown', 0.0),
        'sharpe_ratio': finite_or_none(sharpe.get('sharperatio')),
        'annual_return_pct': (returns.get('rnorm') or 0.0) * 100.0,
        'sqn': finite_or_none(sqn.get('sqn')),
        'ulcer_index': calculate_ulcer_index(broker_values),
    }


def normalize(value):
    """Serialize datetimes and non-finite numeric values for test output."""
    if isinstance(value, datetime):
        return value.isoformat()
    if isinstance(value, float) and (math.isnan(value) or math.isinf(value)):
        return None
    return value


def _close(actual, expected, *, tol, key):
    """Assert ``actual`` is finite and within ``tol`` of ``expected``."""
    assert actual is not None, f"{key}: expected={expected}, got=None"
    a = float(actual)
    assert math.isfinite(a), f"{key}: expected={expected}, got non-finite {actual}"
    assert abs(a - float(expected)) <= tol, (
        f"{key}: expected={expected}, got={a} (tol={tol})"
    )


def _resolve_loader():
    """Locate the data-loading helper (varies by strategy)."""
    for name in ("load_inputs", "load_data", "load_backtest_frame", "prepare_inputs", "prepare_data"):
        fn = globals().get(name)
        if callable(fn):
            return fn
    raise RuntimeError("No inputs loader found in inlined module")


def _build_cerebro_compat(inputs, config):
    """Call build_cerebro with whichever signature the original used."""
    try:
        return build_cerebro(inputs, config)
    except TypeError:
        return build_cerebro(config, inputs)


