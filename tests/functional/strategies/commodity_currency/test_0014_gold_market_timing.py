class GoldMarketTimingStrategy(bt.Strategy):
    """Scale gold exposure by a weighted blend of four timing signals."""

    params = dict(
        sma_fast=20,
        sma_slow=100,
        momentum_period=63,
        vol_window=21,
        vol_lookback=252,
        rsi_period=14,
        commission_pct=0.0005,
    )

    def __init__(self):
        """Bind the signal/asset feeds and initialize counters and equity series."""
        self.signal_data = self.datas[0]
        self.asset_data = self.datas[1]
        self.pending_order = None
        self.bar_num = 0
        self.rebalance_count = 0
        self.trade_count = 0
        self.win_count = 0
        self.loss_count = 0
        self.broker_value_series = []

    def next(self):
        """Record equity and rebalance to the target weight when the signal changes."""
        self.bar_num += 1
        self.broker_value_series.append((bt.num2date(self.signal_data.datetime[0]), float(self.broker.getvalue())))
        if self.pending_order is not None:
            return
        if float(self.signal_data.signal_change[0]) <= 0.5:
            return
        self.rebalance_count += 1
        target = float(self.signal_data.target_position[0])
        self.pending_order = self.order_target_percent(data=self.asset_data, target=target)

    def notify_order(self, order):
        """Clear the pending order once the rebalance order leaves a live state."""
        if order.status in (order.Submitted, order.Accepted):
            return
        if self.pending_order is not None and order.ref == self.pending_order.ref:
            self.pending_order = None

    def notify_trade(self, trade):
        """Count each closed trade as a win or loss by net commissioned PnL."""
        if not trade.isclosed:
            return
        self.trade_count += 1
        if trade.pnlcomm >= 0:
            self.win_count += 1
        else:
            self.loss_count += 1





