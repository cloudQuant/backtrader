def normalize(value):
    """Convert values to JSON-friendly types, coercing datetimes to strings."""
    if value is None or isinstance(value, (str, int, float, bool)):
        return value
    isoformat = getattr(value, 'isoformat', None)
    if callable(isoformat):
        try:
            return value.isoformat(sep=' ')
        except TypeError:
            return value.isoformat()
    return str(value)


def finite_or_none(value):
    """Return the value when it is finite, otherwise ``None``."""
    if value is None:
        return None
    if isinstance(value, float) and (value != value or value in (float('inf'), float('-inf'))):
        return None
    return value


def calculate_ulcer_index(equity_curve):
    """Compute the Ulcer Index from an equity curve.

    Args:
        equity_curve: Sequence of portfolio values over time.

    Returns:
        The Ulcer Index (root-mean-square of percentage drawdowns), or 0.0
        when the curve is empty or has no measurable drawdowns.
    """
    if not equity_curve:
        return 0.0
    peak = None
    squared_drawdowns = []
    for value in equity_curve:
        peak = value if peak is None else max(peak, value)
        if peak and peak != 0:
            drawdown_pct = (value / peak - 1.0) * 100.0
            squared_drawdowns.append(drawdown_pct * drawdown_pct)
    if not squared_drawdowns:
        return 0.0
    return float(sum(squared_drawdowns) / len(squared_drawdowns)) ** 0.5


