def finite_or_none(value):
    """Return ``value`` when it is a finite number, otherwise ``None``.

    Args:
        value: A numeric value or ``None``.

    Returns:
        The original value if finite, else ``None``.
    """
    return value if value is not None and math.isfinite(value) else None


def calculate_ulcer_index(values):
    """Compute the Ulcer Index over an equity-value series.

    The Ulcer Index is the root mean square of percentage drawdowns from the
    running peak, measuring downside volatility.

    Args:
        values: Sequence of broker/equity values in chronological order.

    Returns:
        The Ulcer Index as a float, or 0.0 when fewer than two values exist.
    """
    if len(values) < 2:
        return 0.0
    peak = values[0]
    squared = 0.0
    for value in values:
        peak = max(peak, value)
        drawdown = (peak - value) / peak * 100.0 if peak > 0 else 0.0
        squared += drawdown ** 2
    return math.sqrt(squared / len(values))


