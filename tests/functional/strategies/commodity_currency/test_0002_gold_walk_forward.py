class GoldWalkForwardStrategy(bt.Strategy):
    params = dict(
        atr_stop_multiple=1.5,
        train_window=252,
        test_window=63,
        step_window=63,
        momentum_windows=[10, 20, 30, 60, 120],
        mean_reversion_windows=[5, 10, 20, 30],
        signal_thresholds=[0.01, 0.02, 0.03, 0.05],
        vol_window=20,
        atr_window=14,
        target_volatility=0.15,
        base_target_percent=0.03,
        max_target_percent=0.05,
        commission_estimate=0.0002,
    )

    def __init__(self):
        self.bar_num = 0
        self.buy_count = 0
        self.sell_count = 0
        self.short_count = 0
        self.cover_count = 0
        self.trade_count = 0
        self.win_count = 0
        self.loss_count = 0
        self.stop_exit_count = 0
        self.reverse_exit_count = 0
        self.pending_order = None
        self.entry_price = None
        self.entry_side = 0
        self.entry_atr = None
        self.broker_value_series = []

    def _get_position_size(self, target_notional_pct=1.0, price=None):
        if target_notional_pct <= 0:
            return 0.0
        broker_value = float(self.broker.getvalue())
        execution_price = float(self.data.close[0] if price is None else price)
        if broker_value <= 0 or execution_price <= 0:
            return 0.0
        comminfo = self.broker.getcommissioninfo(self.data)
        multiplier = float(getattr(comminfo.p, 'mult', 1.0) or 1.0)
        if multiplier <= 0:
            return 0.0
        size = broker_value * float(target_notional_pct) / (execution_price * multiplier)
        return max(0.01, round(size, 2))

    def next(self):
        self.bar_num += 1
        self.broker_value_series.append((bt.num2date(self.data.datetime[0]), float(self.broker.getvalue())))
        if self.pending_order is not None:
            return

        signal = float(self.data.wf_signal[0])
        target_percent = float(self.data.target_percent[0])
        atr = float(self.data.atr[0]) if self.data.atr[0] == self.data.atr[0] else 0.0
        close_price = float(self.data.close[0])

        if self.position:
            if self.entry_side > 0:
                stop_level = self.entry_price - float(self.p.atr_stop_multiple) * self.entry_atr
                if atr > 0 and close_price <= stop_level:
                    self.sell_count += 1
                    self.stop_exit_count += 1
                    self.pending_order = self.close()
                    return
                if signal < 0:
                    self.sell_count += 1
                    self.reverse_exit_count += 1
                    self.pending_order = self.close()
                    return
            else:
                stop_level = self.entry_price + float(self.p.atr_stop_multiple) * self.entry_atr
                if atr > 0 and close_price >= stop_level:
                    self.cover_count += 1
                    self.stop_exit_count += 1
                    self.pending_order = self.close()
                    return
                if signal > 0:
                    self.cover_count += 1
                    self.reverse_exit_count += 1
                    self.pending_order = self.close()
                    return
            if signal == 0:
                if self.position.size > 0:
                    self.sell_count += 1
                else:
                    self.cover_count += 1
                self.pending_order = self.close()
            return

        size = self._get_position_size(target_notional_pct=target_percent)
        if size <= 0:
            return
        if signal > 0:
            self.buy_count += 1
            self.entry_side = 1
            self.entry_atr = atr
            self.pending_order = self.buy(size=size)
        elif signal < 0:
            self.short_count += 1
            self.entry_side = -1
            self.entry_atr = atr
            self.pending_order = self.sell(size=size)

    def notify_order(self, order):
        if order.status in (order.Submitted, order.Accepted):
            return
        if order.status == order.Completed:
            if self.position:
                self.entry_price = float(order.executed.price)
            else:
                self.entry_price = None
                self.entry_side = 0
                self.entry_atr = None
        self.pending_order = None

    def notify_trade(self, trade):
        if not trade.isclosed:
            return
        self.trade_count += 1
        if trade.pnlcomm >= 0:
            self.win_count += 1
        else:
            self.loss_count += 1





