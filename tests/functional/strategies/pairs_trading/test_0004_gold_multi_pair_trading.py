class FuturesCommission(bt.CommInfoBase):
    """Percentage, stock-like commission scheme for the metal legs."""

    params = (('commission', 0.0005), ('commtype', bt.CommInfoBase.COMM_PERC), ('stocklike', True))


def finite_or_none(value):
    """Return the value if it is finite, otherwise None.

    Args:
        value: A numeric value or None.

    Returns:
        The value when it is non-None and finite, else None.
    """
    return value if value is not None and math.isfinite(value) else None


def calculate_ulcer_index(values):
    """Compute the Ulcer Index of an equity series.

    Args:
        values: Sequence of broker/equity values in chronological order.

    Returns:
        The Ulcer Index (root-mean-square percentage drawdown from running
        peaks), or 0.0 when there are fewer than two values.
    """
    if len(values) < 2:
        return 0.0
    peak = values[0]
    squared = 0.0
    for value in values:
        peak = max(peak, value)
        drawdown = (peak - value) / peak * 100.0 if peak > 0 else 0.0
        squared += drawdown ** 2
    return math.sqrt(squared / len(values))


def load_inputs(config):
    """Load the four daily frames and build the aligned multi-pair signal.

    Args:
        config: Parsed configuration providing the ``data`` and ``params``
            sections.

    Returns:
        A dict with the aligned ``gold_df``, per-symbol ``pair_frames``, the
        derived ``signal_df``, and the ``fromdate``/``todate`` bounds.
    """
    data_cfg = config['data']
    fromdate = datetime.fromisoformat(data_cfg['fromdate'])
    todate = datetime.fromisoformat(data_cfg['todate'])
    gold_df = load_mt5_csv((BASE_DIR / data_cfg['gold_file']).resolve(), fromdate=fromdate, todate=todate)
    pair_frames = {}
    for symbol in PAIR_SYMBOLS:
        pair_frames[symbol] = load_mt5_csv((BASE_DIR / data_cfg['pair_files'][symbol]).resolve(), fromdate=fromdate, todate=todate)
    gold_df, pair_frames, signal_df = prepare_multi_pair_inputs(gold_df, pair_frames, config.get('params', {}))
    common_index = signal_df.index
    gold_df = gold_df.loc[common_index].copy()
    pair_frames = {symbol: frame.loc[common_index].copy() for symbol, frame in pair_frames.items()}
    return {'gold_df': gold_df, 'pair_frames': pair_frames, 'signal_df': signal_df, 'fromdate': fromdate, 'todate': todate}


def build_cerebro(inputs, config):
    """Assemble the Cerebro engine, signal/metal feeds, strategy, and analyzers.

    Args:
        inputs: The loaded inputs dict returned by :func:`load_inputs`.
        config: Parsed configuration providing backtest and commission settings.

    Returns:
        A configured :class:`backtrader.Cerebro` with the signal feed, the gold
        and pair price feeds, a percentage commission, the strategy, and the
        default analyzers.
    """
    cerebro = bt.Cerebro(stdstats=False)
    cerebro.broker.setcash(float(config['backtest']['initial_cash']))
    cerebro.broker.addcommissioninfo(FuturesCommission(commission=float(config['params'].get('commission_pct', 0.0005))))
    signal_feed = MultiPairSignalFeed(dataname=inputs['signal_df'], timeframe=bt.TimeFrame.Days, compression=1)
    cerebro.adddata(signal_feed, name='SIGNAL')
    gold_feed = bt.feeds.PandasData(dataname=inputs['gold_df'], timeframe=bt.TimeFrame.Days, compression=1)
    cerebro.adddata(gold_feed, name='XAUUSD')
    for symbol in PAIR_SYMBOLS:
        feed = bt.feeds.PandasData(dataname=inputs['pair_frames'][symbol], timeframe=bt.TimeFrame.Days, compression=1)
        cerebro.adddata(feed, name=symbol)
    cerebro.addstrategy(GoldMultiPairTradingStrategy, **config.get('params', {}))
    cerebro.addanalyzer(bt.analyzers.SharpeRatio, _name='sharpe', timeframe=bt.TimeFrame.Days, compression=1, factor=252, annualize=True, riskfreerate=0)
    cerebro.addanalyzer(bt.analyzers.Returns, _name='returns', tann=252)
    cerebro.addanalyzer(bt.analyzers.DrawDown, _name='drawdown')
    cerebro.addanalyzer(bt.analyzers.TradeAnalyzer, _name='trades')
    cerebro.addanalyzer(bt.analyzers.SQN, _name='sqn')
    return cerebro


def extract_metrics(strat, cerebro, inputs, config):
    """Consolidate analyzer output and strategy counters into a metrics dict.

    Args:
        strat: The executed strategy instance carrying counters and value series.
        cerebro: The Cerebro engine used to read final broker value.
        inputs: The loaded inputs dict providing the date range and bar count.
        config: Parsed configuration providing the strategy name and initial cash.

    Returns:
        A dict of summary metrics (bar/trade counts, PnL, return, win rate,
        profit factor, drawdown, Sharpe, annualized return, SQN, and Ulcer
        Index).
    """
    trades = strat.analyzers.trades.get_analysis()
    sharpe = strat.analyzers.sharpe.get_analysis()
    returns = strat.analyzers.returns.get_analysis()
    drawdown = strat.analyzers.drawdown.get_analysis()
    sqn = strat.analyzers.sqn.get_analysis()
    initial_cash = float(config['backtest']['initial_cash'])
    final_value = float(cerebro.broker.getvalue())
    broker_values = [value for _, value in getattr(strat, 'broker_value_series', [])] or [initial_cash, final_value]
    total_trades = trades.get('total', {}).get('total', 0)
    won = trades.get('won', {}).get('total', 0)
    lost = trades.get('lost', {}).get('total', 0)
    gross_won = trades.get('won', {}).get('pnl', {}).get('total', 0) or 0
    gross_lost = abs(trades.get('lost', {}).get('pnl', {}).get('total', 0) or 0)
    return {
        'strategy_name': config['strategy']['name'],
        'fromdate': inputs['fromdate'],
        'todate': inputs['todate'],
        'bars': len(inputs['signal_df']),
        'bar_num': strat.bar_num,
        'buy_count': strat.buy_count,
        'sell_count': strat.sell_count,
        'initial_cash': initial_cash,
        'final_value': final_value,
        'net_pnl': final_value - initial_cash,
        'total_return_pct': (final_value / initial_cash - 1.0) * 100.0,
        'total_trades': total_trades,
        'won': won,
        'lost': lost,
        'win_rate': (won / total_trades * 100.0) if total_trades else 0.0,
        'profit_factor': (gross_won / gross_lost) if gross_lost else None,
        'max_drawdown': drawdown.get('max', {}).get('drawdown', 0.0),
        'sharpe_ratio': finite_or_none(sharpe.get('sharperatio')),
        'annual_return_pct': (returns.get('rnorm') or 0.0) * 100.0,
        'sqn': finite_or_none(sqn.get('sqn')),
        'ulcer_index': calculate_ulcer_index(broker_values),
    }


def normalize(value):
    """Coerce a metric value into a JSON-serializable form.

    Args:
        value: An arbitrary metric value.

    Returns:
        An ISO string for datetimes, None for non-finite floats, or the value
        unchanged otherwise.
    """
    if isinstance(value, datetime):
        return value.isoformat()
    if isinstance(value, float) and (math.isnan(value) or math.isinf(value)):
        return None
    return value


