def load_mt5_csv(filepath, fromdate=None, todate=None):
    """Load a MetaTrader 5 daily CSV export into an OHLCV frame.

    Handles both tab- and comma-separated exports and tolerant datetime
    parsing for minute or second precision timestamps.

    Args:
        filepath: Path to the MT5 ``.csv`` export to read.
        fromdate: Optional inclusive lower bound for the datetime index.
        todate: Optional inclusive upper bound for the datetime index.

    Returns:
        A pandas DataFrame indexed by datetime with ``open``, ``high``, ``low``,
        ``close``, ``volume`` and ``openinterest`` columns, sorted ascending.
    """
    with open(filepath, 'r', encoding='utf-8', errors='ignore') as handle:
        lines = [line.strip().strip('"') for line in handle.readlines() if line.strip()]
    cleaned = '\n'.join(lines)
    sep = '\t' if '\t' in lines[0] else ','
    df = pd.read_csv(io.StringIO(cleaned), sep=sep)
    dt_text = df['<DATE>'].astype(str) + ' ' + df['<TIME>'].astype(str)
    parsed = pd.to_datetime(dt_text, format='%Y.%m.%d %H:%M', errors='coerce')
    if parsed.isna().any():
        parsed = pd.to_datetime(dt_text, format='%Y.%m.%d %H:%M:%S', errors='coerce')
    df['datetime'] = parsed
    df = df.rename(columns={
        '<OPEN>': 'open', '<HIGH>': 'high', '<LOW>': 'low', '<CLOSE>': 'close',
        '<TICKVOL>': 'tick_volume', '<VOL>': 'real_volume',
    })
    df['openinterest'] = 0
    df['volume'] = df['tick_volume'] if 'tick_volume' in df.columns else 0
    df = df[['datetime', 'open', 'high', 'low', 'close', 'volume', 'openinterest']]
    df = df.dropna(subset=['datetime']).set_index('datetime').sort_index()
    if fromdate is not None:
        df = df[df.index >= fromdate]
    if todate is not None:
        df = df[df.index <= todate]
    return df


def prepare_pair_inputs(frame_a, frame_b, params):
    """Align the two asset frames and compute the rolling spread z-score.

    Aligns the frames to common dates, estimates a rolling hedge ratio (beta)
    of log-A on log-B, builds the residual spread and standardises it into a
    z-score along with the rolling return correlation.

    Args:
        frame_a: OHLCV DataFrame for asset A (the primary leg).
        frame_b: OHLCV DataFrame for asset B (the hedge leg).
        params: Strategy parameter mapping providing ``lookback_bars``.

    Returns:
        A tuple ``(frame_a, frame_b, signal_df)`` restricted to the valid index,
        where ``signal_df`` holds ``corr``, ``beta``, ``spread`` and ``zscore``.
    """
    aligned_index = frame_a.index.intersection(frame_b.index).sort_values()
    frame_a = frame_a.loc[aligned_index][['open', 'high', 'low', 'close', 'volume', 'openinterest']].copy()
    frame_b = frame_b.loc[aligned_index][['open', 'high', 'low', 'close', 'volume', 'openinterest']].copy()
    lookback = int(params.get('lookback_bars', 60))
    close_a = frame_a['close'].astype(float)
    close_b = frame_b['close'].astype(float)
    log_a = np.log(close_a.clip(lower=1e-9))
    log_b = np.log(close_b.clip(lower=1e-9))
    ret_a = log_a.diff()
    ret_b = log_b.diff()
    corr = ret_a.rolling(lookback).corr(ret_b)
    beta = log_a.rolling(lookback).cov(log_b) / log_b.rolling(lookback).var()
    spread = log_a - beta * log_b
    spread_mean = spread.rolling(lookback).mean()
    spread_std = spread.rolling(lookback).std().replace(0, np.nan)
    zscore = (spread - spread_mean) / spread_std
    signal_df = pd.DataFrame({'corr': corr, 'beta': beta, 'spread': spread, 'zscore': zscore}, index=aligned_index)
    signal_df = signal_df.replace([np.inf, -np.inf], np.nan).dropna().copy()
    valid_index = signal_df.index
    return frame_a.loc[valid_index].copy(), frame_b.loc[valid_index].copy(), signal_df


class PairsTradingStrategy(bt.Strategy):
    """Gold/silver statistical-arbitrage pairs strategy on the spread z-score.

    Trades the standardised residual spread between two correlated assets: it
    longs the cheap leg and shorts the expensive leg when the z-score stretches,
    then mean-reverts out via an exit band, stop band or max holding period,
    sizing each leg by the rolling hedge ratio.
    """

    params = dict(
        asset_a_symbol='XAUUSD',
        asset_b_symbol='XAGUSD',
        min_correlation=0.6,
        entry_z=2.0,
        exit_z=0.5,
        stop_loss_z=3.5,
        pair_weight=0.20,
        max_holding_days=20,
        signal_lookup=None,
        lookback_bars=60,
        commission_pct=0.0005,
    )

    def __init__(self):
        """Reset order tracking, counters, the equity series and spread state."""
        self.order_refs = set()
        self.bar_num = 0
        self.buy_count = 0
        self.sell_count = 0
        self.trade_count = 0
        self.win_count = 0
        self.loss_count = 0
        self.broker_value_series = []
        self.long_spread_days = 0
        self.short_spread_days = 0
        self.flat_days = 0
        self.spread_state = 'flat'
        self.state_entry_bar = None

    def _submit(self, order):
        """Track a submitted order's ref so the strategy waits for it to settle."""
        if order is not None:
            self.order_refs.add(order.ref)

    def _target_size(self, data, target_pct):
        """Convert a signed target weight into a signed contract size for a feed.

        Args:
            data: The asset data feed to size.
            target_pct: Signed target weight; positive is long, negative short.

        Returns:
            A signed size (min magnitude 0.01), or 0.0 when value, price or
            multiplier make a position impossible.
        """
        broker_value = float(self.broker.getvalue())
        price = float(data.close[0])
        if broker_value <= 0 or price <= 0:
            return 0.0
        comminfo = self.broker.getcommissioninfo(data)
        multiplier = float(getattr(comminfo.p, 'mult', 1.0) or 1.0)
        if multiplier <= 0:
            return 0.0
        size = broker_value * abs(float(target_pct)) / (price * multiplier)
        size = max(0.01, round(size, 2))
        return size if target_pct >= 0 else -size

    def _apply_targets(self, data_a, data_b, pct_a, pct_b):
        """Rebalance both legs toward their signed target weights.

        Args:
            data_a: Primary leg data feed.
            data_b: Hedge leg data feed.
            pct_a: Signed target weight for the primary leg.
            pct_b: Signed target weight for the hedge leg.
        """
        for data, target_pct in ((data_a, pct_a), (data_b, pct_b)):
            current_pos = float(self.getposition(data).size)
            target_size = self._target_size(data, target_pct)
            if abs(target_size - current_pos) < 0.01:
                continue
            if target_size > current_pos:
                self.buy_count += 1
            elif target_size < current_pos:
                self.sell_count += 1
            self._submit(self.order_target_size(data=data, target=target_size))

    def _flatten(self, data_a, data_b):
        """Reset the spread state and target both legs to zero exposure."""
        self.spread_state = 'flat'
        self.state_entry_bar = None
        self._apply_targets(data_a, data_b, 0.0, 0.0)

    def next(self):
        """Read the day's spread signal and drive the entry/exit state machine."""
        self.bar_num += 1
        data_a = self.datas[0]
        data_b = self.datas[1]
        current_dt = pd.Timestamp(bt.num2date(data_a.datetime[0])).tz_localize(None)
        self.broker_value_series.append((bt.num2date(data_a.datetime[0]), float(self.broker.getvalue())))
        if self.order_refs:
            return
        signal = (self.p.signal_lookup or {}).get(current_dt)
        if signal is None:
            return
        corr = float(signal['corr'])
        beta = float(signal['beta'])
        zscore = float(signal['zscore'])
        if not np.isfinite(corr) or not np.isfinite(beta) or not np.isfinite(zscore) or abs(beta) < 1e-6:
            return
        if corr < float(self.p.min_correlation):
            self.flat_days += 1
            self._flatten(data_a, data_b)
            return
        if self.spread_state == 'flat':
            if zscore <= -float(self.p.entry_z):
                self.spread_state = 'long_spread'
                self.state_entry_bar = self.bar_num
            elif zscore >= float(self.p.entry_z):
                self.spread_state = 'short_spread'
                self.state_entry_bar = self.bar_num
        elif self.state_entry_bar is not None and self.bar_num - self.state_entry_bar >= int(self.p.max_holding_days):
            self._flatten(data_a, data_b)
            self.flat_days += 1
            return
        elif self.spread_state == 'long_spread':
            if zscore >= -float(self.p.exit_z) or zscore <= -float(self.p.stop_loss_z):
                self._flatten(data_a, data_b)
                self.flat_days += 1
                return
        elif self.spread_state == 'short_spread':
            if zscore <= float(self.p.exit_z) or zscore >= float(self.p.stop_loss_z):
                self._flatten(data_a, data_b)
                self.flat_days += 1
                return
        base_weight = float(self.p.pair_weight) / (1.0 + abs(beta))
        hedge_weight = base_weight * abs(beta)
        if self.spread_state == 'long_spread':
            self.long_spread_days += 1
            pct_a, pct_b = base_weight, -hedge_weight
        elif self.spread_state == 'short_spread':
            self.short_spread_days += 1
            pct_a, pct_b = -base_weight, hedge_weight
        else:
            self.flat_days += 1
            pct_a, pct_b = 0.0, 0.0
        self._apply_targets(data_a, data_b, pct_a, pct_b)

    def notify_order(self, order):
        """Drop a settled order's ref so the next rebalance can proceed."""
        if order.status in (order.Submitted, order.Accepted):
            return
        self.order_refs.discard(order.ref)

    def notify_trade(self, trade):
        """Count closed trades and tally win/loss outcomes."""
        if not trade.isclosed:
            return
        self.trade_count += 1
        if trade.pnlcomm >= 0:
            self.win_count += 1
        else:
            self.loss_count += 1





