def load_mt5_csv(filepath, fromdate=None, todate=None):
    """Load an MT5-exported CSV into a backtrader-ready OHLCV DataFrame.

    Args:
        filepath: Path to the MT5 CSV/TSV export file.
        fromdate: Optional inclusive lower bound for the datetime index.
        todate: Optional inclusive upper bound for the datetime index.

    Returns:
        A DataFrame indexed by datetime with open, high, low, close, volume, and
        openinterest columns, sorted ascending and filtered to the date range.
    """
    with open(filepath, 'r', encoding='utf-8', errors='ignore') as handle:
        lines = [line.strip().strip('"') for line in handle.readlines() if line.strip()]
    cleaned = '\n'.join(lines)
    sep = '\t' if '\t' in lines[0] else ','
    df = pd.read_csv(io.StringIO(cleaned), sep=sep)
    dt_text = df['<DATE>'].astype(str) + ' ' + df['<TIME>'].astype(str)
    parsed = pd.to_datetime(dt_text, format='%Y.%m.%d %H:%M', errors='coerce')
    if parsed.isna().any():
        parsed = pd.to_datetime(dt_text, format='%Y.%m.%d %H:%M:%S', errors='coerce')
    df['datetime'] = parsed
    df = df.rename(columns={
        '<OPEN>': 'open', '<HIGH>': 'high', '<LOW>': 'low',
        '<CLOSE>': 'close', '<TICKVOL>': 'tick_volume', '<VOL>': 'real_volume',
    })
    df['openinterest'] = 0
    df['volume'] = df['tick_volume'] if 'tick_volume' in df.columns else 0
    df = df[['datetime', 'open', 'high', 'low', 'close', 'volume', 'openinterest']]
    df = df.set_index('datetime').sort_index()
    if fromdate is not None:
        df = df[df.index >= fromdate]
    if todate is not None:
        df = df[df.index <= todate]
    return df



def prepare_pairs_trading_features(df, params):
    """Compute the rolling z-score and entry/exit signal columns.

    Args:
        df: OHLCV DataFrame containing a ``close`` column.
        params: Mapping of strategy parameters (lookback, entry_z, exit_z).

    Returns:
        A DataFrame with OHLCV plus ``z_score``, ``entry_signal``, and
        ``exit_signal`` columns, with warmup NaN rows dropped.
    """
    lookback = int(params.get('lookback', 60))
    entry_z = float(params.get('entry_z', 2.0))
    exit_z = float(params.get('exit_z', 0.5))

    out = df.copy()
    ma = out['close'].rolling(lookback).mean()
    std = out['close'].rolling(lookback).std()
    out['z_score'] = (out['close'] - ma) / std.replace(0, np.inf)
    out['entry_signal'] = 0.0
    out.loc[out['z_score'] < -entry_z, 'entry_signal'] = 1.0
    out.loc[out['z_score'] > entry_z, 'entry_signal'] = -1.0
    out['exit_signal'] = (abs(out['z_score']) < exit_z).astype(float)

    out = out[['open', 'high', 'low', 'close', 'volume', 'openinterest',
               'z_score', 'entry_signal', 'exit_signal']].copy()
    return out.dropna()


class Mt5PairsTradingFeed(bt.feeds.PandasData):
    """PandasData feed exposing z-score and entry/exit signals as extra lines."""

    lines = ('z_score', 'entry_signal', 'exit_signal',)
    params = (
        ('datetime', None), ('open', 0), ('high', 1), ('low', 2),
        ('close', 3), ('volume', 4), ('openinterest', 5),
        ('z_score', 6),
        ('entry_signal', 7),
        ('exit_signal', 8),
    )


class PairsTradingStrategy(bt.Strategy):
    """Single-asset z-score mean-reversion strategy with a holding-period stop."""

    params = dict(
        lookback=60,
        entry_z=2.0,
        exit_z=0.5,
        max_holding=20,
        lot_size=1.0,
    )

    def __init__(self):
        """Initialize bar, order, entry-tracking, and trade counters."""
        self.bar_num = 0
        self.buy_count = 0
        self.sell_count = 0
        self.pending_order = None
        self.entry_bar = 0
        self.broker_value_series = []

    def _get_position_size(self, target_notional_pct=1.0, price=None):
        if target_notional_pct <= 0:
            return 0.0
        broker_value = float(self.broker.getvalue())
        execution_price = float(self.data.close[0] if price is None else price)
        if broker_value <= 0 or execution_price <= 0:
            return 0.0
        comminfo = self.broker.getcommissioninfo(self.data)
        multiplier = float(getattr(comminfo.p, 'mult', 1.0) or 1.0)
        if multiplier <= 0:
            return 0.0
        size = broker_value * float(target_notional_pct) / (execution_price * multiplier)
        return max(0.01, round(size, 2))


    def next(self):
        """Open on a long z-score extreme and exit on reversion or time stop."""
        self.bar_num += 1
        self.broker_value_series.append((bt.num2date(self.data.datetime[0]), float(self.broker.getvalue())))
        if self.pending_order is not None:
            return
        entry = float(self.data.entry_signal[0])
        exit_sig = float(self.data.exit_signal[0]) > 0.5
        if not self.position:
            if entry > 0.5:
                self.buy_count += 1
                self.entry_bar = self.bar_num
                self.pending_order = self.buy(size=self._get_position_size())
        else:
            if exit_sig or (self.bar_num - self.entry_bar >= self.p.max_holding):
                self.sell_count += 1
                self.pending_order = self.close()

    def notify_order(self, order):
        """Clear the pending order reference once it is no longer live.

        Args:
            order: The order whose status changed.
        """
        if order.status in (order.Submitted, order.Accepted):
            return
        self.pending_order = None

    def notify_trade(self, trade):
        """Ignore trade notifications; trade stats come from the analyzer.

        Args:
            trade: The trade whose status changed.
        """
        pass


