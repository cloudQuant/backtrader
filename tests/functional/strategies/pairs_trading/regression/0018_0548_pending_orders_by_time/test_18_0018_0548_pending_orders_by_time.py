class PendingOrdersByTimeStrategy(bt.Strategy):
    params = dict(
        opening_hour=9,
        closing_hour=2,
        distance=20,
        lots=0.1,
        stop_loss=20,
        take_profit=500,
        point=0.01,
        price_digits=2,
    )

    def __init__(self):
        self.bar_num = 0
        self.signal_count = 0
        self.buy_count = 0
        self.sell_count = 0
        self.trade_count = 0
        self.win_count = 0
        self.loss_count = 0
        self.completed_order_count = 0
        self.rejected_order_count = 0
        self.order = None
        self.stop_price = None
        self.take_profit_price = None
        self.pending_buy = None
        self.pending_sell = None

    def _point(self):
        return float(self.p.point)

    def _round(self, value):
        return round(float(value), int(self.p.price_digits))

    def _place_pending_pair(self):
        price = float(self.data.close[0])
        dist = float(self.p.distance) * self._point()
        sl = float(self.p.stop_loss) * self._point()
        self.pending_sell = {
            'entry': self._round(price - dist),
            'sl': self._round(price - dist + sl) if sl > 0 else None,
        }
        self.pending_buy = {
            'entry': self._round(price + dist),
            'sl': self._round(price + dist - sl) if sl > 0 else None,
        }

    def _clear_pending(self):
        self.pending_buy = None
        self.pending_sell = None

    def _arm_position(self, direction, entry_price):
        tp = float(self.p.take_profit) * self._point()
        if direction == 'buy':
            self.stop_price = self.pending_buy['sl'] if self.pending_buy else None
            self.take_profit_price = self._round(entry_price + tp) if tp > 0 else None
            self.signal_count += 1
            self.order = self.buy(size=float(self.p.lots))
        else:
            self.stop_price = self.pending_sell['sl'] if self.pending_sell else None
            self.take_profit_price = self._round(entry_price - tp) if tp > 0 else None
            self.signal_count += 1
            self.order = self.sell(size=float(self.p.lots))
        self._clear_pending()

    def _check_pending_trigger(self):
        if self.position or self.order is not None:
            return
        high = float(self.data.high[0])
        low = float(self.data.low[0])
        if self.pending_buy and high >= float(self.pending_buy['entry']) and self.pending_sell and low <= float(self.pending_sell['entry']):
            open_price = float(self.data.open[0])
            if abs(open_price - float(self.pending_buy['entry'])) <= abs(open_price - float(self.pending_sell['entry'])):
                self._arm_position('buy', float(self.pending_buy['entry']))
            else:
                self._arm_position('sell', float(self.pending_sell['entry']))
            return
        if self.pending_buy and high >= float(self.pending_buy['entry']):
            self._arm_position('buy', float(self.pending_buy['entry']))
            return
        if self.pending_sell and low <= float(self.pending_sell['entry']):
            self._arm_position('sell', float(self.pending_sell['entry']))

    def _manage_position(self, dt):
        if not self.position or self.order is not None:
            return
        high = float(self.data.high[0])
        low = float(self.data.low[0])
        if dt.hour == int(self.p.closing_hour):
            self.order = self.close(); return
        if self.position.size > 0:
            if self.take_profit_price is not None and high >= float(self.take_profit_price):
                self.order = self.close(); return
            if self.stop_price is not None and low <= float(self.stop_price):
                self.order = self.close(); return
        else:
            if self.take_profit_price is not None and low <= float(self.take_profit_price):
                self.order = self.close(); return
            if self.stop_price is not None and high >= float(self.stop_price):
                self.order = self.close(); return

    def next(self):
        self.bar_num += 1
        dt = self.data.datetime.datetime(0)
        if self.order is not None:
            return
        if dt.hour == int(self.p.closing_hour):
            self._clear_pending()
        self._manage_position(dt)
        if self.order is not None:
            return
        self._check_pending_trigger()
        if self.order is not None:
            return
        if dt.hour == int(self.p.opening_hour) and not self.position and self.pending_buy is None and self.pending_sell is None:
            self._place_pending_pair()

    def notify_order(self, order):
        if order.status in [bt.Order.Submitted, bt.Order.Accepted]:
            return
        if order.status == bt.Order.Completed:
            self.completed_order_count += 1
            if self.position:
                if order.executed.size > 0:
                    self.buy_count += 1
                elif order.executed.size < 0:
                    self.sell_count += 1
            else:
                self.stop_price = None
                self.take_profit_price = None
        elif order.status in [bt.Order.Canceled, bt.Order.Margin, bt.Order.Rejected, bt.Order.Expired]:
            self.rejected_order_count += 1
        if self.order is not None and order.ref == self.order.ref and order.status not in [bt.Order.Submitted, bt.Order.Accepted]:
            self.order = None

    def notify_trade(self, trade):
        if not trade.isclosed:
            return
        self.trade_count += 1
        if trade.pnlcomm >= 0:
            self.win_count += 1
        else:
            self.loss_count += 1



