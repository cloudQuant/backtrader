class ZeroCrossingPairsStrategy(bt.Strategy):
    """Zero-crossing gold/silver pairs strategy rebalancing on precomputed signals."""

    params = dict(
        hedge_window=60,
        zscore_window=20,
        exit_z=0.5,
        gross_exposure=1.0,
    )

    def __init__(self):
        """Bind the signal/asset feeds and initialize tracking counters."""
        self.signal_data = self.datas[0]
        self.asset_data = {data._name: data for data in self.datas[1:]}
        self.pending_orders = []
        self.bar_num = 0
        self.rebalance_count = 0
        self.trade_count = 0
        self.win_count = 0
        self.loss_count = 0
        self.broker_value_series = []

    def next(self):
        """Record equity and rebalance both legs when the signal flag fires."""
        self.bar_num += 1
        self.broker_value_series.append((bt.num2date(self.signal_data.datetime[0]), float(self.broker.getvalue())))
        if self.pending_orders:
            return
        if float(self.signal_data.rebalance_flag[0]) <= 0.5:
            return
        self.rebalance_count += 1
        order1 = self.order_target_percent(data=self.asset_data['XAUUSD'], target=float(self.signal_data.gold_weight[0]))
        order2 = self.order_target_percent(data=self.asset_data['XAGUSD'], target=float(self.signal_data.silver_weight[0]))
        for order in (order1, order2):
            if order is not None:
                self.pending_orders.append(order)

    def notify_order(self, order):
        """Drop the order from the pending list once it is no longer live.

        Args:
            order: The order whose status changed.
        """
        if order.status in (order.Submitted, order.Accepted):
            return
        self.pending_orders = [o for o in self.pending_orders if o.ref != order.ref]

    def notify_trade(self, trade):
        """Tally closed trades into win/loss counters.

        Args:
            trade: The trade whose status changed; only closed trades count.
        """
        if not trade.isclosed:
            return
        self.trade_count += 1
        if trade.pnlcomm >= 0:
            self.win_count += 1
        else:
            self.loss_count += 1





