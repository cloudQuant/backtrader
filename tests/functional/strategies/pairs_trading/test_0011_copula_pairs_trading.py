class CopulaPairsTradingStrategy(bt.Strategy):
    """Single-asset copula (empirical-CDF) mean-reversion strategy with a time stop."""

    params = dict(
        lookback=60,
        entry_threshold=0.1,
        exit_threshold=0.5,
        holding_days=15,
        lot_size=1.0,
    )

    def __init__(self):
        """Initialize bar, order, entry-tracking, and trade counters."""
        self.bar_num = 0
        self.buy_count = 0
        self.sell_count = 0
        self.pending_order = None
        self.entry_bar = 0
        self.broker_value_series = []

    def _get_position_size(self, target_notional_pct=1.0, price=None):
        if target_notional_pct <= 0:
            return 0.0
        broker_value = float(self.broker.getvalue())
        execution_price = float(self.data.close[0] if price is None else price)
        if broker_value <= 0 or execution_price <= 0:
            return 0.0
        comminfo = self.broker.getcommissioninfo(self.data)
        multiplier = float(getattr(comminfo.p, 'mult', 1.0) or 1.0)
        if multiplier <= 0:
            return 0.0
        size = broker_value * float(target_notional_pct) / (execution_price * multiplier)
        return max(0.01, round(size, 2))


    def next(self):
        """Open on a positive CDF signal and exit on reversion or time stop."""
        self.bar_num += 1
        self.broker_value_series.append((bt.num2date(self.data.datetime[0]), float(self.broker.getvalue())))
        if self.pending_order is not None:
            return
        signal = float(self.data.signal[0])
        cdf = float(self.data.empirical_cdf[0])
        if not self.position:
            if signal > 0.5:
                self.buy_count += 1
                self.entry_bar = self.bar_num
                self.pending_order = self.buy(size=self._get_position_size())
        else:
            if abs(cdf - 0.5) < self.p.exit_threshold or (self.bar_num - self.entry_bar >= self.p.holding_days):
                self.sell_count += 1
                self.pending_order = self.close()

    def notify_order(self, order):
        """Clear the pending order reference once it is no longer live.

        Args:
            order: The order whose status changed.
        """
        if order.status in (order.Submitted, order.Accepted):
            return
        self.pending_order = None

    def notify_trade(self, trade):
        """Ignore trade notifications; trade stats come from the analyzer.

        Args:
            trade: The trade whose status changed.
        """
        pass


