def prepare_copula_pairs_trading_features(df, params):
    """Compute the rolling empirical CDF and the position signal column.

    Args:
        df: OHLCV DataFrame containing a ``close`` column.
        params: Mapping of strategy parameters (lookback, entry_threshold).

    Returns:
        A DataFrame with OHLCV plus ``empirical_cdf`` and ``signal`` columns,
        with warmup NaN rows dropped.
    """
    lookback = int(params.get('lookback', 60))
    entry_threshold = float(params.get('entry_threshold', 0.1))

    out = df.copy()
    ret = out['close'].pct_change()
    # Empirical CDF as copula proxy
    def ecdf_rank(x):
        """Return the percentile rank of the last value in the window."""
        return x.rank(pct=True).iloc[-1] if len(x) > 0 else 0.5
    out['empirical_cdf'] = ret.rolling(lookback).apply(ecdf_rank, raw=False)
    out['signal'] = 0.0
    out.loc[out['empirical_cdf'] < entry_threshold, 'signal'] = 1.0
    out.loc[out['empirical_cdf'] > (1.0 - entry_threshold), 'signal'] = -1.0

    out = out[['open', 'high', 'low', 'close', 'volume', 'openinterest',
               'empirical_cdf', 'signal']].copy()
    return out.dropna()


class Mt5CopulaPairsTradingFeed(bt.feeds.PandasData):
    """PandasData feed exposing the empirical CDF and signal as extra lines."""

