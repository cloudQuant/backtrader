def resample_frame(df, rule):
    """Resample OHLCV data by timeframe rule string.

    Args:
        df: Source dataframe indexed by datetime.
        rule: Pandas offset alias such as ``'240min'``.

    Returns:
        Resampled and cleaned OHLCV dataframe.
    """
    out = df.resample(rule, label='right', closed='right').agg({
        'open': 'first',
        'high': 'max',
        'low': 'min',
        'close': 'last',
        'volume': 'sum',
        'openinterest': 'last',
    })
    out = out.dropna(subset=['open', 'high', 'low', 'close'])
    out['openinterest'] = out['openinterest'].fillna(0)
    return out


def compute_applied_price(frame, mode):
    """Return a price series according to configured application mode."""
    mode = str(mode).lower()
    if mode in ('price_open', 'open'):
        return frame['open'].astype(float)
    if mode in ('price_high', 'high'):
        return frame['high'].astype(float)
    if mode in ('price_low', 'low'):
        return frame['low'].astype(float)
    if mode in ('price_median', 'median'):
        return (frame['high'].astype(float) + frame['low'].astype(float)) / 2.0
    if mode in ('price_typical', 'typical'):
        return (frame['high'].astype(float) + frame['low'].astype(float) + frame['close'].astype(float)) / 3.0
    if mode in ('price_weighted', 'weighted'):
        return (frame['high'].astype(float) + frame['low'].astype(float) + 2.0 * frame['close'].astype(float)) / 4.0
    if mode in ('price_simpl', 'simpl'):
        return (frame['open'].astype(float) + frame['close'].astype(float)) / 2.0
    if mode in ('price_quarter', 'quarter'):
        return (frame['high'].astype(float) + frame['low'].astype(float) + frame['open'].astype(float) + frame['close'].astype(float)) / 4.0
    return frame['close'].astype(float)


def apply_ma(series, period, method):
    """Apply moving-average smoothing based on configured method."""
    period = max(1, int(period))
    mode = str(method).lower()
    if mode in ('mode_sma', 'sma', '0'):
        return series.rolling(period, min_periods=period).mean()
    if mode in ('mode_ema', 'ema', '1', 'mode_jjma', 'jjma', 'mode_jurx', 'jurx', 'mode_parma', 'parma', 'mode_t3', 't3', 'mode_vidya', 'vidya', 'mode_ama', 'ama'):
        return series.ewm(span=period, adjust=False, min_periods=period).mean()
    if mode in ('mode_smma', 'smma', '2'):
        return series.ewm(alpha=1.0 / period, adjust=False, min_periods=period).mean()
    weights = np.arange(1, period + 1, dtype='float64')
    weight_sum = float(weights.sum())
    return series.rolling(period, min_periods=period).apply(lambda values: float(np.dot(values, weights)) / weight_sum, raw=True)


def compute_cci(price, period):
    """Compute classic CCI-like oscillator from price series."""
    period = max(1, int(period))
    sma = price.rolling(period, min_periods=period).mean()
    mad = price.rolling(period, min_periods=period).apply(lambda values: float(np.mean(np.abs(values - np.mean(values)))), raw=True)
    return (price - sma) / (0.015 * mad.replace(0.0, np.nan))


def compute_cronex_cci(frame, cci_period=25, cci_price='price_close', xma_method='mode_sma', fast_period=14, slow_period=25):
    """Compute CronexCCI fast/slow lines and crossover signal flags."""
    price = compute_applied_price(frame, cci_price)
    cci = compute_cci(price, cci_period)
    fast = apply_ma(cci, fast_period, xma_method)
    slow = apply_ma(fast, slow_period, xma_method)

    buy_signal = (fast.shift(1) > slow.shift(1)) & (fast <= slow)
    sell_signal = (fast.shift(1) < slow.shift(1)) & (fast >= slow)

    out = frame.copy()
    out['cronex_fast'] = fast
    out['cronex_slow'] = slow
    out['buy_signal'] = buy_signal.astype(float)
    out['sell_signal'] = sell_signal.astype(float)
    return out.dropna(subset=['cronex_fast', 'cronex_slow'])


