class RSI2MeanReversionStrategy(bt.Strategy):
    """RSI2 mean-reversion strategy with exit priority and optional limit context."""
    params = dict(
        rsi_period=2,
        rsi_buy_threshold=5,
        rsi_sell_threshold=30,
        sma_period=100,
        atr_period=10,
        atr_multiplier=0.5,
        lot_size=1.0,
    )

    def __init__(self):
        """Initialize state used by assertions and order/position control."""
        self.bar_num = 0
        self.buy_count = 0
        self.sell_count = 0
        self.trade_count = 0
        self.win_count = 0
        self.loss_count = 0
        self.pending_order = None
        self.broker_value_series = []


    def _get_position_size(self, target_notional_pct=1.0, price=None):
        """Compute notional-based position size with futures multiplier."""
        if target_notional_pct <= 0:
            return 0.0
        broker_value = float(self.broker.getvalue())
        execution_price = float(self.data.close[0] if price is None else price)
        if broker_value <= 0 or execution_price <= 0:
            return 0.0
        comminfo = self.broker.getcommissioninfo(self.data)
        multiplier = float(getattr(comminfo.p, 'mult', 1.0) or 1.0)
        if multiplier <= 0:
            return 0.0
        size = broker_value * float(target_notional_pct) / (execution_price * multiplier)
        return max(0.01, round(size, 2))

    def log(self, text):
        """Print timestamped strategy log line."""
        dt = bt.num2date(self.data.datetime[0])
        print(f'{dt.isoformat()}, {text}')

    def next(self):
        """Advance one bar: prioritize exits then evaluate entries."""
        self.bar_num += 1
        self.broker_value_series.append((bt.num2date(self.data.datetime[0]), float(self.broker.getvalue())))
        
        if self.pending_order is not None:
            return
        
        # Exit first if already in position and sell signal triggers.
        if self.position:
            if float(self.data.sell_signal[0]) > 0.5:
                self.sell_count += 1
                self.pending_order = self.close()
                self.log(f'exit signal: RSI > {self.p.rsi_sell_threshold}')
            return
        
        # Enter at market price when entry condition is met.
        if float(self.data.buy_signal[0]) > 0.5:
            self.buy_count += 1
            self.pending_order = self.buy(size=self._get_position_size(target_notional_pct=float(self.p.lot_size)))
            self.log(f'entry signal: RSI={float(self.data.rsi[0]):.2f} < {self.p.rsi_buy_threshold}')

    def notify_order(self, order):
        """Reset pending status once order transitions to terminal status."""
        if order.status in (order.Submitted, order.Accepted):
            return
        self.pending_order = None

    def notify_trade(self, trade):
        """Update win/loss counters after trade settlement."""
        if not trade.isclosed:
            return
        self.trade_count += 1
        if trade.pnlcomm >= 0:
            self.win_count += 1
        else:
            self.loss_count += 1
        self.log(f'trade closed pnl={trade.pnlcomm:.2f}')


