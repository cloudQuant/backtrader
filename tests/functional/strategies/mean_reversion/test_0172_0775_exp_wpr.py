class ExpWprStrategy(bt.Strategy):
    """Trade Williams %R level crossings on H4 with fixed SL/TP on M15."""

    params = dict(
        mm=0.1,
        mm_mode='LOT',
        stop_loss=1000,
        take_profit=2000,
        buy_pos_open=True,
        sell_pos_open=True,
        buy_pos_close=True,
        sell_pos_close=True,
        trend='DIRECT',
        wpr_period=14,
        high_level=-40,
        low_level=-60,
        signal_bar=1,
        size=0.1,
        point=0.01,
        digits_adjust=10,
        price_digits=2,
    )

    def __init__(self):
        """Bind the M15/H4 feeds, build the H4 Williams %R, and reset state."""
        self.m15 = self.datas[0]
        self.h4 = self.datas[1]
        self.wpr = bt.indicators.WilliamsR(self.h4, period=self.p.wpr_period)

        self.bar_num = 0
        self.buy_count = 0
        self.sell_count = 0
        self.trade_count = 0
        self.win_count = 0
        self.loss_count = 0

        self.entry_order = None
        self.stop_price = None
        self.take_profit_price = None
        self.current_side = None
        self.last_signal_dt = None

    def log(self, text):
        """Print a timestamped strategy log line.

        Args:
            text: Message to print with the current bar timestamp.
        """
        dt = bt.num2date(self.m15.datetime[0])
        print(f'{dt.isoformat()}, {text}')

    def _trade_unit(self):
        return self.p.point * self.p.digits_adjust

    def _enough_history(self):
        return len(self.h4) >= self.p.wpr_period + max(3, self.p.signal_bar + 2)

    def _close_long_signal(self, buy_close):
        return buy_close and self.position and self.position.size > 0

    def _close_short_signal(self, sell_close):
        return sell_close and self.position and self.position.size < 0

    def _evaluate_signals(self):
        idx = -self.p.signal_bar
        prev_idx = idx - 1
        wpr_now = float(self.wpr[idx])
        wpr_prev = float(self.wpr[prev_idx])

        buy_open = buy_close = sell_open = sell_close = False

        if self.p.trend == 'DIRECT':
            if wpr_prev > self.p.low_level and wpr_now <= self.p.low_level:
                if self.p.buy_pos_open:
                    buy_open = True
                if self.p.sell_pos_close:
                    sell_close = True
            if wpr_prev < self.p.high_level and wpr_now >= self.p.high_level:
                if self.p.sell_pos_open:
                    sell_open = True
                if self.p.buy_pos_close:
                    buy_close = True
        else:
            if wpr_prev > self.p.low_level and wpr_now <= self.p.low_level:
                if self.p.sell_pos_open:
                    sell_open = True
                if self.p.buy_pos_close:
                    buy_close = True
            if wpr_prev < self.p.high_level and wpr_now >= self.p.high_level:
                if self.p.buy_pos_open:
                    buy_open = True
                if self.p.sell_pos_close:
                    sell_close = True
        return buy_open, buy_close, sell_open, sell_close, wpr_now, wpr_prev

    def _manage_risk(self):
        if not self.position:
            return False
        high = float(self.m15.high[0])
        low = float(self.m15.low[0])
        if self.position.size > 0:
            if self.stop_price is not None and low <= self.stop_price:
                self.entry_order = self.close()
                return True
            if self.take_profit_price is not None and high >= self.take_profit_price:
                self.entry_order = self.close()
                return True
        else:
            if self.stop_price is not None and high >= self.stop_price:
                self.entry_order = self.close()
                return True
            if self.take_profit_price is not None and low <= self.take_profit_price:
                self.entry_order = self.close()
                return True
        return False

    def _set_risk_prices(self, side):
        price = float(self.m15.close[0])
        unit = self._trade_unit()
        if side == 'buy':
            self.stop_price = round(price - self.p.stop_loss * unit, self.p.price_digits) if self.p.stop_loss > 0 else None
            self.take_profit_price = round(price + self.p.take_profit * unit, self.p.price_digits) if self.p.take_profit > 0 else None
        else:
            self.stop_price = round(price + self.p.stop_loss * unit, self.p.price_digits) if self.p.stop_loss > 0 else None
            self.take_profit_price = round(price - self.p.take_profit * unit, self.p.price_digits) if self.p.take_profit > 0 else None
        self.current_side = side

    def next(self):
        """Run per-bar logic: enforce risk, gate duplicated H4 signals, and open/close."""
        self.bar_num += 1
        if not self._enough_history():
            return
        if self.entry_order is not None:
            return
        if self._manage_risk():
            return

        signal_dt = bt.num2date(self.h4.datetime[-self.p.signal_bar])
        if self.last_signal_dt == signal_dt:
            return
        self.last_signal_dt = signal_dt

        buy_open, buy_close, sell_open, sell_close, wpr_now, wpr_prev = self._evaluate_signals()
        self.log(f'wpr signal prev={wpr_prev:.2f} now={wpr_now:.2f} buy_open={buy_open} sell_open={sell_open}')

        if self._close_long_signal(buy_close):
            self.entry_order = self.close()
            return
        if self._close_short_signal(sell_close):
            self.entry_order = self.close()
            return

        if buy_open and (not self.position or self.position.size <= 0):
            if self.position and self.position.size < 0:
                self.entry_order = self.close()
                return
            self._set_risk_prices('buy')
            self.entry_order = self.buy(size=self.p.size)
            return
        if sell_open and (not self.position or self.position.size >= 0):
            if self.position and self.position.size > 0:
                self.entry_order = self.close()
                return
            self._set_risk_prices('sell')
            self.entry_order = self.sell(size=self.p.size)

    def notify_order(self, order):
        """Handle order state transitions and reset risk parameters on close."""
        if order.status in [bt.Order.Submitted, bt.Order.Accepted]:
            return
        if order.status == bt.Order.Completed:
            if self.position:
                if order.executed.size > 0:
                    self.buy_count += 1
                elif order.executed.size < 0:
                    self.sell_count += 1
                self.log(f'entry filled price={order.executed.price:.2f} size={order.executed.size:.2f}')
            else:
                self.stop_price = None
                self.take_profit_price = None
                self.current_side = None
                self.log(f'position closed price={order.executed.price:.2f} size={order.executed.size:.2f}')
        elif order.status in [bt.Order.Canceled, bt.Order.Margin, bt.Order.Rejected, bt.Order.Expired]:
            self.log(f'order failed status={order.getstatusname()}')
        if self.entry_order is not None and order.ref == self.entry_order.ref and order.status not in [bt.Order.Submitted, bt.Order.Accepted]:
            self.entry_order = None

    def notify_trade(self, trade):
        """Accumulate trade outcome counters when a trade is closed."""
        if not trade.isclosed:
            return
        self.trade_count += 1
        if trade.pnlcomm >= 0:
            self.win_count += 1
        else:
            self.loss_count += 1
        self.log(f'trade closed pnl={trade.pnlcomm:.2f}')



