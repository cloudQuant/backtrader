class BlauErgodicStrategy(Strategy):
    """Trade Blau Ergodic oscillator signals with selectable signal modes.

    The strategy reads buy/sell signals from the Blau Ergodic oscillator using
    one of three modes (breakdown, twist, cloudtwist), reverses on opposing
    signals, and manages exits with fixed-point stop-loss and take-profit
    levels. Optional relaxed entries and a forced sample entry ensure activity.
    Signal, order, trade and oscillator-range statistics are tracked.
    """

    params = dict(
        mode='twist',
        signal_bar=1,
        xlength=2,
        xlength1=20,
        xlength2=5,
        xlength3=3,
        xlength4=3,
        stop_loss_points=1000,
        take_profit_points=2000,
        lot=0.1,
        point=0.01,
        price_digits=2,
        relaxed_entries=False,
        ensure_trade_after_bars=0,
    )

    def __init__(self):
        """Build the Blau Ergodic oscillator, reset counters and warmup length."""
        self.osc = bt.indicators.BlauErgodic(
            self.data,
            xlength=self.p.xlength,
            xlength1=self.p.xlength1,
            xlength2=self.p.xlength2,
            xlength3=self.p.xlength3,
            xlength4=self.p.xlength4,
        )
        self.bar_num = 0
        self.buy_count = 0
        self.sell_count = 0
        self.trade_count = 0
        self.win_count = 0
        self.loss_count = 0
        self.buy_signal_count = 0
        self.sell_signal_count = 0
        self.completed_order_count = 0
        self.rejected_order_count = 0
        self.main_min = None
        self.main_max = None
        self.spread_min = None
        self.spread_max = None
        self.nonzero_spread_count = 0
        self.entry_price = None
        self.stop_price = None
        self.target_price = None
        self.pending_entry_direction = 0
        self._forced_entry_done = False
        self.warmup = max(
            int(self.p.xlength) + int(self.p.xlength1) + int(self.p.xlength2) + int(self.p.xlength3) + int(self.p.xlength4) + 5,
            40,
        )

    def log(self, text):
        """Print a timestamped log line for the current bar."""
        dt = num2date(self.data.datetime[0])
        print(f'{dt.isoformat()}, {text}')

    def _signal_indexes(self):
        current = -max(int(self.p.signal_bar), 1)
        previous = current - 1
        older = current - 2
        return current, previous, older

    def _breakdown_signals(self):
        current, previous, _ = self._signal_indexes()
        hist_now = float(self.osc.spread[current])
        hist_prev = float(self.osc.spread[previous])
        return hist_prev <= 0.0 and hist_now > 0.0, hist_prev >= 0.0 and hist_now < 0.0

    def _twist_signals(self):
        current, previous, older = self._signal_indexes()
        hist_now = float(self.osc.spread[current])
        hist_prev = float(self.osc.spread[previous])
        hist_older = float(self.osc.spread[older])
        return hist_prev < hist_older and hist_now > hist_prev, hist_prev > hist_older and hist_now < hist_prev

    def _cloudtwist_signals(self):
        current, previous, _ = self._signal_indexes()
        main_now = float(self.osc.main[current])
        main_prev = float(self.osc.main[previous])
        signal_now = float(self.osc.signal[current])
        signal_prev = float(self.osc.signal[previous])
        return main_prev <= signal_prev and main_now > signal_now, main_prev >= signal_prev and main_now < signal_now

    def _get_signals(self):
        mode = str(self.p.mode).lower()
        if mode == 'breakdown':
            return self._breakdown_signals()
        if mode == 'cloudtwist':
            return self._cloudtwist_signals()
        return self._twist_signals()

    def _reset_levels(self):
        self.entry_price = None
        self.stop_price = None
        self.target_price = None

    def _open_long(self):
        self.pending_entry_direction = 1
        self.buy(size=self.p.lot)

    def _open_short(self):
        self.pending_entry_direction = -1
        self.sell(size=self.p.lot)

    def _close_long(self, reason):
        self.log(reason)
        self.close()
        self._reset_levels()

    def _close_short(self, reason):
        self.log(reason)
        self.close()
        self._reset_levels()

    def _manage_protective_levels(self):
        if not self.position or self.entry_price is None:
            return False

        low = float(self.data.low[0])
        high = float(self.data.high[0])

        if self.position.size > 0:
            if self.stop_price is not None and low <= self.stop_price:
                self._close_long(f'close long stop={self.stop_price:.2f}')
                return True
            if self.target_price is not None and high >= self.target_price:
                self._close_long(f'close long target={self.target_price:.2f}')
                return True
        else:
            if self.stop_price is not None and high >= self.stop_price:
                self._close_short(f'close short stop={self.stop_price:.2f}')
                return True
            if self.target_price is not None and low <= self.target_price:
                self._close_short(f'close short target={self.target_price:.2f}')
                return True

        return False

    def next(self):
        """Advance one bar: manage protective levels, then act on mode signals."""
        self.bar_num += 1
        if len(self.data) < self.warmup + max(int(self.p.signal_bar), 1) + 2:
            return

        if self._manage_protective_levels():
            return

        buy_signal, sell_signal = self._get_signals()
        spread_now = float(self.osc.spread[0])
        spread_prev = float(self.osc.spread[-1])
        main_now = float(self.osc.main[0])
        signal_now = float(self.osc.signal[0])

        if self.p.relaxed_entries and not (buy_signal or sell_signal):
            buy_signal = spread_now > spread_prev
            sell_signal = spread_now < spread_prev

        if math.isfinite(main_now):
            self.main_min = main_now if self.main_min is None else min(self.main_min, main_now)
            self.main_max = main_now if self.main_max is None else max(self.main_max, main_now)
        if math.isfinite(spread_now):
            self.spread_min = spread_now if self.spread_min is None else min(self.spread_min, spread_now)
            self.spread_max = spread_now if self.spread_max is None else max(self.spread_max, spread_now)
            if abs(spread_now) > 1e-9:
                self.nonzero_spread_count += 1

        if buy_signal:
            self.buy_signal_count += 1
        if sell_signal:
            self.sell_signal_count += 1

        if self.position:
            if self.position.size > 0 and sell_signal:
                self.log(
                    f'close long & sell main={main_now:.2f} signal={signal_now:.2f} hist={spread_now:.2f}'
                )
                self.close()
                self._reset_levels()
                self._open_short()
                return
            if self.position.size < 0 and buy_signal:
                self.log(
                    f'close short & buy main={main_now:.2f} signal={signal_now:.2f} hist={spread_now:.2f}'
                )
                self.close()
                self._reset_levels()
                self._open_long()
                return
        else:
            if buy_signal:
                self.log(f'buy main={main_now:.2f} signal={signal_now:.2f} hist={spread_now:.2f}')
                self._open_long()
                return
            if sell_signal:
                self.log(f'sell main={main_now:.2f} signal={signal_now:.2f} hist={spread_now:.2f}')
                self._open_short()
                return
            if (not self._forced_entry_done and int(self.p.ensure_trade_after_bars) > 0 and
                    self.bar_num >= int(self.p.ensure_trade_after_bars)):
                self.log(f'buy forced sample entry main={main_now:.2f} signal={signal_now:.2f} hist={spread_now:.2f}')
                self._forced_entry_done = True
                self._open_long()
                return

    def notify_order(self, order):
        """Track order outcomes and set entry/stop/target prices on fills."""
        if order.status in [order.Canceled, order.Margin, order.Rejected]:
            self.rejected_order_count += 1
            self.pending_entry_direction = 0
            self.log(f'order {order.getstatusname()}')
            return

        if order.status != order.Completed:
            return

        self.completed_order_count += 1

        if self.pending_entry_direction == 1 and order.isbuy():
            self.buy_count += 1
            self.entry_price = order.executed.price
            self.stop_price = (
                self.entry_price - self.p.stop_loss_points * self.p.point
                if self.p.stop_loss_points > 0 else None
            )
            self.target_price = (
                self.entry_price + self.p.take_profit_points * self.p.point
                if self.p.take_profit_points > 0 else None
            )
            self.pending_entry_direction = 0
            return

        if self.pending_entry_direction == -1 and order.issell():
            self.sell_count += 1
            self.entry_price = order.executed.price
            self.stop_price = (
                self.entry_price + self.p.stop_loss_points * self.p.point
                if self.p.stop_loss_points > 0 else None
            )
            self.target_price = (
                self.entry_price - self.p.take_profit_points * self.p.point
                if self.p.take_profit_points > 0 else None
            )
            self.pending_entry_direction = 0
            return

        if not self.position:
            self._reset_levels()

    def notify_trade(self, trade):
        """Count closed trades as wins or losses based on net PnL."""
        if not trade.isclosed:
            return
        self.trade_count += 1
        if trade.pnlcomm >= 0:
            self.win_count += 1
        else:
            self.loss_count += 1
        self.log(f'trade closed pnl={trade.pnlcomm:.2f}')


BASE_DIR = Path(__file__).resolve().parent

MINUTES_PER_TRADING_YEAR = 24 * 60 * 252


def resolve_data_path(filename):
    """Resolve a data filename relative to this test's directory.

    Args:
        filename: A path string, absolute or relative to BASE_DIR.

    Returns:
        The resolved absolute Path.

    Raises:
        FileNotFoundError: If the resolved path does not exist.
    """
    path = (BASE_DIR / filename).resolve()
    if not path.exists():
        raise FileNotFoundError(f'Data file not found: {path}')
    return path


def load_backtest_frame(config):
    """Load the M15 OHLCV frame described by the config's data section.

    Args:
        config: The resolved configuration dictionary.

    Returns:
        A dict with keys 'data' (the DataFrame), 'fromdate' and 'todate'.

    Raises:
        ValueError: If the loaded frame is empty.
    """
    data_cfg = config['data']
    fromdate = datetime.datetime.fromisoformat(data_cfg['fromdate'])
    todate = datetime.datetime.fromisoformat(data_cfg['todate'])
    df = load_mt5_csv(
        resolve_data_path(data_cfg['file']),
        fromdate=fromdate,
        todate=todate,
        bar_shift_minutes=data_cfg.get('bar_shift_minutes', 0),
    )
    if df.empty:
        raise ValueError('Loaded data frame is empty')
    print(f"Loaded {len(df)} bars: {df.index[0]} -> {df.index[-1]}")
    return {'data': df, 'fromdate': fromdate, 'todate': todate}


def build_cerebro(config, frame):
    """Assemble the Cerebro engine with the feed, strategy and analyzers.

    Args:
        config: The resolved configuration dictionary.
        frame: The dict returned by load_backtest_frame.

    Returns:
        A configured Cerebro instance ready to run, with the M15 feed, the
        strategy and the performance analyzers attached.
    """
    bt_cfg = config['backtest']
    cerebro = Cerebro(stdstats=True)
    cerebro.broker.setcash(bt_cfg['initial_cash'])
    comm_type = CommInfoBase.COMM_FIXED if bt_cfg.get('commission_type', 'fixed') == 'fixed' else CommInfoBase.COMM_PERC
    cerebro.broker.setcommission(
        commission=bt_cfg['commission'],
        margin=bt_cfg['margin'],
        mult=bt_cfg['multiplier'],
        commtype=comm_type,
        stocklike=bt_cfg.get('stocklike', False),
    )
    feed = Mt5PandasFeed(dataname=frame['data'], timeframe=TimeFrame.Minutes, compression=15)
    cerebro.adddata(feed, name=f"{config['data']['symbol']}_{config['data']['timeframe']}")
    cerebro.addstrategy(BlauErgodicStrategy, **config.get('params', {}))
    cerebro.addanalyzer(
        btanalyzers.SharpeRatio,
        _name='sharpe',
        timeframe=TimeFrame.Minutes,
        factor=MINUTES_PER_TRADING_YEAR,
        annualize=True,
        riskfreerate=0,
    )
    cerebro.addanalyzer(btanalyzers.Returns, _name='returns', timeframe=TimeFrame.Minutes, compression=15, tann=MINUTES_PER_TRADING_YEAR)
    cerebro.addanalyzer(btanalyzers.DrawDown, _name='drawdown')
    cerebro.addanalyzer(btanalyzers.TradeAnalyzer, _name='trades')
    cerebro.addanalyzer(btanalyzers.SQN, _name='sqn')
    return cerebro


def extract_metrics(strat, cerebro, frame, config):
    """Collect strategy counters and analyzer results into a metrics dict.

    Args:
        strat: The executed strategy instance.
        cerebro: The Cerebro engine after the run (for broker valuation).
        frame: The dict returned by load_backtest_frame (bar count, date range).
        config: The resolved configuration dictionary.

    Returns:
        A dictionary of bar, signal, order, trade and performance metrics,
        including net PnL, returns, win rate, profit factor, drawdown, Sharpe
        ratio and SQN.
    """
    sharpe = strat.analyzers.sharpe.get_analysis()
    returns = strat.analyzers.returns.get_analysis()
    drawdown = strat.analyzers.drawdown.get_analysis()
    trades = strat.analyzers.trades.get_analysis()
    sqn = strat.analyzers.sqn.get_analysis()
    initial_cash = config['backtest']['initial_cash']
    final_value = cerebro.broker.getvalue()
    total_trades = trades.get('total', {}).get('total', 0)
    won = trades.get('won', {}).get('total', 0)
    lost = trades.get('lost', {}).get('total', 0)
    gross_won = trades.get('won', {}).get('pnl', {}).get('total', 0) or 0
    gross_lost = abs(trades.get('lost', {}).get('pnl', {}).get('total', 0) or 0)
    return {
        'fromdate': frame['fromdate'],
        'todate': frame['todate'],
        'bars': len(frame['data']),
        'bar_num': strat.bar_num,
        'buy_signal_count': strat.buy_signal_count,
        'sell_signal_count': strat.sell_signal_count,
        'completed_order_count': strat.completed_order_count,
        'rejected_order_count': strat.rejected_order_count,
        'buy_count': strat.buy_count,
        'sell_count': strat.sell_count,
        'trade_count': strat.trade_count,
        'win_count': strat.win_count,
        'loss_count': strat.loss_count,
        'initial_cash': initial_cash,
        'final_value': final_value,
        'net_pnl': final_value - initial_cash,
        'total_return_pct': (final_value / initial_cash - 1) * 100,
        'total_trades': total_trades,
        'won': won,
        'lost': lost,
        'win_rate': (won / total_trades * 100) if total_trades else 0,
        'profit_factor': (gross_won / gross_lost) if gross_lost else None,
        'max_drawdown': drawdown.get('max', {}).get('drawdown', 0),
        'sharpe_ratio': sharpe.get('sharperatio'),
        'annual_return_pct': (returns.get('rnorm') or 0) * 100,
        'sqn': sqn.get('sqn'),
    }


def _close(actual, expected, *, tol, key):
    """Assert ``actual`` is finite and within ``tol`` of ``expected``."""
    assert actual is not None, f"{key}: expected={expected}, got=None"
    a = float(actual)
    assert math.isfinite(a), f"{key}: expected={expected}, got non-finite {actual}"
    assert abs(a - float(expected)) <= tol, (
        f"{key}: expected={expected}, got={a} (tol={tol})"
    )


def _resolve_loader():
    """Locate the data-loading helper (varies by strategy)."""
    for name in ("load_inputs", "load_data", "load_backtest_frame", "prepare_inputs", "prepare_data"):
        fn = globals().get(name)
        if callable(fn):
            return fn
    raise RuntimeError("No inputs loader found in inlined module")


def _build_cerebro_compat(inputs, config):
    """Call build_cerebro with whichever signature the original used."""
    import inspect
    sig = inspect.signature(build_cerebro)
    params = list(sig.parameters.keys())
    if params and params[0].lower() in ("config", "cfg", "configuration"):
        return build_cerebro(config, inputs)
    try:
        return build_cerebro(inputs, config)
    except TypeError:
        return build_cerebro(config, inputs)


def _extract_metrics_compat(strat, cerebro, inputs, config):
    """Call extract_metrics with whichever signature the original used."""
    for args in (
        (strat, cerebro, inputs, config),
        (strat, cerebro, config, inputs),
        (strat, cerebro, inputs),
        (strat, cerebro),
    ):
        try:
            return extract_metrics(*args)
        except TypeError:
            continue
    raise RuntimeError("extract_metrics failed for all argument orderings")


