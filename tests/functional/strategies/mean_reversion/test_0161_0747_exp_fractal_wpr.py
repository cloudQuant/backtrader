def load_mt5_csv(filepath, fromdate=None, todate=None, bar_shift_minutes=0):
    """Load an MT5-exported CSV into a backtrader-ready OHLCV DataFrame.

    Args:
        filepath: Path to the MT5 tab-separated export file.
        fromdate: Optional inclusive lower bound for the datetime index.
        todate: Optional inclusive upper bound for the datetime index.
        bar_shift_minutes: Minutes to add to each timestamp (e.g. to stamp bars
            at their close).

    Returns:
        A DataFrame indexed by datetime with open, high, low, close, tick_volume
        and openinterest columns, filtered to the requested date range.
    """
    with open(filepath, 'r', encoding='utf-8') as f:
        lines = f.read().strip().split('\n')
    cleaned = '\n'.join(line.strip().strip('"') for line in lines)
    df = pd.read_csv(io.StringIO(cleaned), sep='\t')
    df['datetime'] = pd.to_datetime(df['<DATE>'] + ' ' + df['<TIME>'], format='%Y.%m.%d %H:%M:%S')
    df = df.rename(columns={
        '<OPEN>': 'open',
        '<HIGH>': 'high',
        '<LOW>': 'low',
        '<CLOSE>': 'close',
        '<TICKVOL>': 'tick_volume',
        '<VOL>': 'real_volume',
    })
    df['openinterest'] = 0
    df = df[['datetime', 'open', 'high', 'low', 'close', 'tick_volume', 'openinterest']]
    df = df.set_index('datetime')
    if bar_shift_minutes:
        df.index = df.index + pd.Timedelta(minutes=bar_shift_minutes)
    if fromdate is not None:
        df = df[df.index >= fromdate]
    if todate is not None:
        df = df[df.index <= todate]
    return df


def resample_frame(df, rule):
    """Resample an OHLCV frame to a coarser bar size.

    Args:
        df: The source OHLCV DataFrame (with a tick_volume column) indexed by
            datetime.
        rule: A pandas offset alias for the target bar size (e.g. ``'60min'``).

    Returns:
        A resampled DataFrame with right-labelled, right-closed bars, rows
        lacking OHLC dropped, openinterest filled with zeros and a ``volume``
        column mirroring tick_volume.
    """
    out = df.resample(rule, label='right', closed='right').agg({
        'open': 'first',
        'high': 'max',
        'low': 'min',
        'close': 'last',
        'tick_volume': 'sum',
        'openinterest': 'last',
    })
    out = out.dropna(subset=['open', 'high', 'low', 'close'])
    out['openinterest'] = out['openinterest'].fillna(0)
    out['volume'] = out['tick_volume']
    return out


def price_series(frame, applied_price='PRICE_CLOSE_'):
    """Select an applied-price series from an OHLC frame.

    Args:
        frame: OHLC DataFrame to select from.
        applied_price: MT5 applied-price code (e.g. ``'PRICE_CLOSE_'``,
            ``'PRICE_TYPICAL_'``); unrecognised values fall back to close.

    Returns:
        The chosen price Series (open, high, low, median, typical, weighted or
        close).
    """
    mapping = str(applied_price).upper()
    if mapping == 'PRICE_OPEN_':
        return frame['open']
    if mapping == 'PRICE_HIGH_':
        return frame['high']
    if mapping == 'PRICE_LOW_':
        return frame['low']
    if mapping == 'PRICE_MEDIAN_':
        return (frame['high'] + frame['low']) / 2.0
    if mapping == 'PRICE_TYPICAL_':
        return (frame['high'] + frame['low'] + frame['close']) / 3.0
    if mapping == 'PRICE_WEIGHTED_':
        return (frame['high'] + frame['low'] + 2.0 * frame['close']) / 4.0
    return frame['close']


def _highest(values, n, index):
    return np.max(values[index - n + 1:index + 1])


def _lowest(values, n, index):
    return np.min(values[index - n + 1:index + 1])


def compute_fractal_wpr(frame, e_period=30, normal_speed=30, applied_price='PRICE_CLOSE_'):
    """Compute the adaptive Fractal Williams %R oscillator on a frame.

    Estimates a fractal-dimension ratio from the recent versus prior high-low
    range, uses it to scale the Williams %R lookback bar by bar, and returns the
    resulting -100..0 oscillator.

    Args:
        frame: OHLC DataFrame to compute on.
        e_period: Lookback used for the fractal-dimension range estimate.
        normal_speed: Base Williams %R lookback that the fractal factor scales.
        applied_price: MT5 applied-price code passed to :func:`price_series`.

    Returns:
        A copy of ``frame`` with an added ``fractal_wpr`` column, with warm-up
        rows lacking the oscillator dropped.
    """
    out = frame.copy()
    close_like = price_series(out, applied_price).to_numpy(dtype=float)
    high = out['high'].to_numpy(dtype=float)
    low = out['low'].to_numpy(dtype=float)
    rates_total = len(out)
    values = np.full(rates_total, np.nan, dtype=float)
    start = int(max(e_period, normal_speed))
    wpr_prev = -50.0
    for index in range(start, rates_total):
        diff = max(high[index - e_period + 1:index + 1]) - min(low[index - e_period + 1:index + 1])
        prior_diff = max(high[index - e_period:index]) - min(low[index - e_period:index]) if index - e_period >= 0 else diff
        if prior_diff == 0 or diff == 0:
            trail_dim = 1.0
        else:
            ratio = diff / prior_diff if prior_diff else 1.0
            hurst = np.clip(ratio, 0.1, 10.0)
            trail_dim = 1.0 / hurst
        beta = trail_dim / 2.0
        speed = max(2, int(round(normal_speed * beta)))
        if index < speed:
            continue
        max_high = _highest(high, speed, index)
        min_low = _lowest(low, speed, index)
        spread = max_high - min_low
        if spread != 0:
            wpr = -(max_high - close_like[index]) * 100.0 / spread
        else:
            wpr = wpr_prev
        values[index] = wpr
        if index != rates_total - 1:
            wpr_prev = wpr
    out['fractal_wpr'] = values
    return out.dropna(subset=['fractal_wpr'])


