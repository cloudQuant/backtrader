class ExpFractalWPRStrategy(bt.Strategy):
    """Adaptive Fractal %R threshold-crossing mean-reversion strategy.

    Reads the H1 Fractal Williams %R from the indicator feed and, on crossings
    of the low/high levels (mapped by ``trend`` mode), opens or closes
    fixed-size positions on the M15 base feed, bounded by point-based stop-loss
    and take-profit levels.
    """

    params = dict(
        stop_loss=1000,
        take_profit=2000,
        buy_pos_open=True,
        sell_pos_open=True,
        buy_pos_close=True,
        sell_pos_close=True,
        trend='DIRECT',
        high_level=-30,
        low_level=-70,
        signal_bar=1,
        point=0.01,
        digits_adjust=10,
        price_digits=2,
        size=0.1,
        e_period=30,
        normal_speed=30,
        applied_price='PRICE_CLOSE_',
    )

    def __init__(self):
        """Bind the base/indicator feeds and reset counters and order state."""
        self.base = self.datas[0]
        self.ind = self.datas[1]
        self.fwpr = self.ind.fractal_wpr

        self.bar_num = 0
        self.signal_count = 0
        self.buy_count = 0
        self.sell_count = 0
        self.trade_count = 0
        self.win_count = 0
        self.loss_count = 0
        self.completed_order_count = 0
        self.rejected_order_count = 0

        self.order = None
        self.last_signal_dt = None
        self.stop_price = None
        self.take_profit_price = None

    def _unit(self):
        return float(self.p.point) * float(self.p.digits_adjust)

    def _set_risk(self, side):
        unit = self._unit()
        price = float(self.base.close[0])
        if side == 'buy':
            self.stop_price = round(price - float(self.p.stop_loss) * unit, int(self.p.price_digits))
            self.take_profit_price = round(price + float(self.p.take_profit) * unit, int(self.p.price_digits))
        else:
            self.stop_price = round(price + float(self.p.stop_loss) * unit, int(self.p.price_digits))
            self.take_profit_price = round(price - float(self.p.take_profit) * unit, int(self.p.price_digits))

    def _manage_risk(self):
        if not self.position or self.order is not None:
            return False
        high = float(self.base.high[0])
        low = float(self.base.low[0])
        if self.position.size > 0:
            if low <= self.stop_price or high >= self.take_profit_price:
                self.order = self.close()
                return True
        else:
            if high >= self.stop_price or low <= self.take_profit_price:
                self.order = self.close()
                return True
        return False

    def next(self):
        """Advance one bar: manage risk, then act on a Fractal %R signal.

        Skips any pending order, exits an open position through ``_manage_risk``
        (stop/target), then reads the signal-bar Fractal %R values, maps level
        crossings to entry/exit flags by ``trend`` mode, and closes or opens a
        fixed-``size`` long/short while arming risk levels and counting signals.
        """
        self.bar_num += 1
        if self.order is not None:
            return
        if self._manage_risk():
            return
        idx = max(int(self.p.signal_bar), 1)
        try:
            prev_val = float(self.fwpr[-idx - 1])
            curr_val = float(self.fwpr[-idx])
        except (IndexError, TypeError, ValueError):
            return
        signal_dt = bt.num2date(self.ind.datetime[-idx])
        if self.last_signal_dt == signal_dt:
            return
        self.last_signal_dt = signal_dt
        buy_open = buy_close = sell_open = sell_close = False
        direct = str(self.p.trend).upper() == 'DIRECT'
        if direct:
            if prev_val > float(self.p.low_level) and curr_val <= float(self.p.low_level):
                if self.p.buy_pos_open:
                    buy_open = True
                if self.p.sell_pos_close:
                    sell_close = True
            if prev_val < float(self.p.high_level) and curr_val >= float(self.p.high_level):
                if self.p.sell_pos_open:
                    sell_open = True
                if self.p.buy_pos_close:
                    buy_close = True
        else:
            if prev_val > float(self.p.low_level) and curr_val <= float(self.p.low_level):
                if self.p.sell_pos_open:
                    sell_open = True
                if self.p.buy_pos_close:
                    buy_close = True
            if prev_val < float(self.p.high_level) and curr_val >= float(self.p.high_level):
                if self.p.buy_pos_open:
                    buy_open = True
                if self.p.sell_pos_close:
                    sell_close = True
        if buy_close and self.position and self.position.size > 0:
            self.order = self.close()
            return
        if sell_close and self.position and self.position.size < 0:
            self.order = self.close()
            return
        if buy_open and (not self.position or self.position.size <= 0):
            if self.position and self.position.size < 0:
                self.order = self.close()
                return
            self.signal_count += 1
            self._set_risk('buy')
            self.order = self.buy(size=self.p.size)
            return
        if sell_open and (not self.position or self.position.size >= 0):
            if self.position and self.position.size > 0:
                self.order = self.close()
                return
            self.signal_count += 1
            self._set_risk('sell')
            self.order = self.sell(size=self.p.size)

    def notify_order(self, order):
        """Update order counters and reset state when an order resolves.

        Tracks completed orders (incrementing buy/sell counts on entry and
        clearing risk levels on a flat close), counts cancelled/rejected/expired
        orders, and clears the tracked order handle once it leaves the live
        states.

        Args:
            order: The order whose status changed.
        """
        if order.status in [bt.Order.Submitted, bt.Order.Accepted]:
            return
        if order.status == bt.Order.Completed:
            self.completed_order_count += 1
            if self.position:
                if order.executed.size > 0:
                    self.buy_count += 1
                elif order.executed.size < 0:
                    self.sell_count += 1
            else:
                self.stop_price = None
                self.take_profit_price = None
        elif order.status in [bt.Order.Canceled, bt.Order.Margin, bt.Order.Rejected, bt.Order.Expired]:
            self.rejected_order_count += 1
        if self.order is not None and order.ref == self.order.ref and order.status not in [bt.Order.Submitted, bt.Order.Accepted]:
            self.order = None

    def notify_trade(self, trade):
        """Tally win/loss counts when a trade closes.

        Args:
            trade: The trade whose status changed; closed trades increment the
                trade counter and the win or loss count by sign of PnL.
        """
        if not trade.isclosed:
            return
        self.trade_count += 1
        if trade.pnlcomm >= 0:
            self.win_count += 1
        else:
            self.loss_count += 1



