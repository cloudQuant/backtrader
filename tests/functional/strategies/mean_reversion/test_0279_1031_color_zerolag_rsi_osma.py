def load_backtest_frames(config):
    """Load base M15 bars and derived H4 signal bars for a full backtest run.

    Args:
        config: Normalized configuration mapping containing ``data`` and ``params``.

    Returns:
        Dictionary containing the base frame, H4 signal frame, and date range used.
    """
    data_cfg = config['data']
    params = config['params']
    fromdate = datetime.datetime.fromisoformat(data_cfg['fromdate'])
    todate = datetime.datetime.fromisoformat(data_cfg['todate'])
    base = load_mt5_csv(resolve_data_path(data_cfg['file']), fromdate=fromdate, todate=todate, bar_shift_minutes=data_cfg.get('bar_shift_minutes', 0))
    if base.empty:
        raise ValueError('Loaded data frame is empty')
    h4 = resample_frame(base, '{0}min'.format(data_cfg.get('signal_tf_minutes', 240)))
    h4 = compute_color_zerolag_rsi_osma(h4, smoothing1=params['smoothing1'], smoothing2=params['smoothing2'], applied_price=params['applied_price'], factor1=params['factor1'], rsi_period1=params['rsi_period1'], factor2=params['factor2'], rsi_period2=params['rsi_period2'], factor3=params['factor3'], rsi_period3=params['rsi_period3'], factor4=params['factor4'], rsi_period4=params['rsi_period4'], factor5=params['factor5'], rsi_period5=params['rsi_period5'])
    print('Loaded bars: M15={0}, H4={1}'.format(len(base), len(h4)))
    return {'m15': base, 'h4': h4, 'fromdate': fromdate, 'todate': todate}


def build_cerebro(config, frame):
    """Build and configure a ``bt.Cerebro`` instance for the strategy.

    Args:
        config: Full configuration dictionary including backtest parameters.
        frame: Preloaded backtest frames from :func:`load_backtest_frames`.

    Returns:
        Configured ``bt.Cerebro`` object with two feeds, strategy, and analyzers.
    """
    bt_cfg = config['backtest']
    cerebro = bt.Cerebro(stdstats=True)
    cerebro.broker.setcash(bt_cfg['initial_cash'])
    comm_type = bt.CommInfoBase.COMM_FIXED if bt_cfg.get('commission_type', 'fixed') == 'fixed' else bt.CommInfoBase.COMM_PERC
    cerebro.broker.setcommission(commission=bt_cfg['commission'], margin=bt_cfg['margin'], mult=bt_cfg['multiplier'], commtype=comm_type, stocklike=bt_cfg.get('stocklike', False))

    feed_m15 = Mt5PandasFeed(dataname=frame['m15'], timeframe=bt.TimeFrame.Minutes, compression=15)
    feed_h4 = ColorZerolagRSIOSMAFeed(dataname=frame['h4'][['open', 'high', 'low', 'close', 'volume', 'openinterest', 'fast', 'slow', 'hist', 'buy_signal', 'sell_signal']], timeframe=bt.TimeFrame.Minutes, compression=240)

    cerebro.adddata(feed_m15, name='XAUUSD_M15')
    cerebro.adddata(feed_h4, name='XAUUSD_H4_ColorZerolagRSIOSMA')
    cerebro.addstrategy(ColorZerolagRSIOSMAStrategy, **config.get('params', {}))
    cerebro.addanalyzer(bt.analyzers.SharpeRatio, _name='sharpe', timeframe=bt.TimeFrame.Minutes, factor=MINUTES_PER_TRADING_YEAR, annualize=True, riskfreerate=0)
    cerebro.addanalyzer(bt.analyzers.Returns, _name='returns', timeframe=bt.TimeFrame.Minutes, compression=15, tann=MINUTES_PER_TRADING_YEAR)
    cerebro.addanalyzer(bt.analyzers.DrawDown, _name='drawdown')
    cerebro.addanalyzer(bt.analyzers.TradeAnalyzer, _name='trades')
    cerebro.addanalyzer(bt.analyzers.SQN, _name='sqn')
    return cerebro


def extract_metrics(strat, cerebro, frame, config):
    """Extract test assertions metrics from strategy state and analyzers.

    Args:
        strat: Executed strategy instance.
        cerebro: Cerebro instance after run.
        frame: Frame dictionary produced by :func:`load_backtest_frames`.
        config: Backtest configuration dictionary.

    Returns:
        Dictionary of normalized metrics consumed by the regression test.
    """
