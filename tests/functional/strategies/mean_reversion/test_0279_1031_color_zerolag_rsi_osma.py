def resample_frame(df, rule):
    """Resample an OHLCV frame to a coarser timeframe.

    Args:
        df: Source OHLCV DataFrame indexed by datetime.
        rule: A pandas offset alias (for example ``'240min'``) for the target
            timeframe.

    Returns:
        A resampled DataFrame with right-closed, right-labelled bars and rows
        lacking price data dropped.
    """
    out = df.resample(rule, label='right', closed='right').agg({
        'open': 'first',
        'high': 'max',
        'low': 'min',
        'close': 'last',
        'volume': 'sum',
        'openinterest': 'last',
    })
    out = out.dropna(subset=['open', 'high', 'low', 'close'])
    out['openinterest'] = out['openinterest'].fillna(0)
    return out


def compute_applied_price(frame, mode):
    """Select an applied-price series from an OHLC frame.

    Args:
        frame: OHLC DataFrame providing ``open``, ``high``, ``low`` and
            ``close`` columns.
        mode: Applied-price selector matching MetaTrader semantics (open, high,
            low, median, typical, weighted); anything else falls back to close.

    Returns:
        A float pandas Series with the requested applied price per bar.
    """
    mode = str(mode).lower()
    if mode in ('price_open', 'open'):
        return frame['open'].astype(float)
    if mode in ('price_high', 'high'):
        return frame['high'].astype(float)
    if mode in ('price_low', 'low'):
        return frame['low'].astype(float)
    if mode in ('price_median', 'median'):
        return (frame['high'].astype(float) + frame['low'].astype(float)) / 2.0
    if mode in ('price_typical', 'typical'):
        return (frame['high'].astype(float) + frame['low'].astype(float) + frame['close'].astype(float)) / 3.0
    if mode in ('price_weighted', 'weighted'):
        return (frame['high'].astype(float) + frame['low'].astype(float) + 2.0 * frame['close'].astype(float)) / 4.0
    return frame['close'].astype(float)


def compute_rsi(price, period):
    """Compute a Wilder-smoothed RSI series for a price series.

    Args:
        price: Price Series to evaluate.
        period: RSI lookback period.

    Returns:
        A pandas Series of RSI values in the 0..100 range, defaulting to 100
        where the average loss is zero.
    """
    period = int(period)
    delta = price.diff()
    gain = delta.clip(lower=0.0)
    loss = (-delta).clip(lower=0.0)
    avg_gain = gain.ewm(alpha=1.0 / period, adjust=False, min_periods=period).mean()
    avg_loss = loss.ewm(alpha=1.0 / period, adjust=False, min_periods=period).mean()
    rs = avg_gain / avg_loss.replace(0.0, np.nan)
    rsi = 100.0 - (100.0 / (1.0 + rs))
    return rsi.fillna(100.0).where(avg_loss != 0.0, 100.0)


def compute_color_zerolag_rsi_osma(frame, smoothing1=15, smoothing2=7, applied_price='close', factor1=0.05, rsi_period1=8, factor2=0.10, rsi_period2=21, factor3=0.16, rsi_period3=34, factor4=0.26, rsi_period4=55, factor5=0.43, rsi_period5=89):
    """Compute the ColorZerolagRSIOSMA indicator lines and turning-point signals.

    Blends five weighted RSI lengths into a zero-lag fast line, double-smooths
    it into a slow line and an OsMA-style histogram, then derives buy/sell flags
    from histogram troughs and peaks.

    Args:
        frame: OHLC DataFrame the indicator is computed over.
        smoothing1: First smoothing constant for the slow line.
        smoothing2: Second smoothing constant for the histogram.
        applied_price: Applied-price selector passed to
            :func:`compute_applied_price`.
        factor1: Weight applied to the ``rsi_period1`` bt.indicators.RSI.
        rsi_period1: Length of the first RSI component.
        factor2: Weight applied to the second/third RSI components.
        rsi_period2: Length of the second RSI component.
        factor3: Weight associated with the third RSI component.
        rsi_period3: Length of the third RSI component.
        factor4: Weight applied to the fourth RSI component.
        rsi_period4: Length of the fourth RSI component.
        factor5: Weight applied to the fifth RSI component.
        rsi_period5: Length of the fifth RSI component.

    Returns:
        A copy of ``frame`` with ``fast``, ``slow``, ``hist``, ``buy_signal``
        and ``sell_signal`` columns added and rows lacking a histogram dropped.
    """
    price = compute_applied_price(frame, applied_price)
    rsi1 = compute_rsi(price, rsi_period1)
    rsi2 = compute_rsi(price, rsi_period2)
    rsi3 = compute_rsi(price, rsi_period3)
    rsi4 = compute_rsi(price, rsi_period4)
    rsi5 = compute_rsi(price, rsi_period5)

    fast = factor1 * rsi1 + factor2 * rsi2 + factor2 * rsi3 + factor4 * rsi4 + factor5 * rsi5
    smooth_const1 = (float(smoothing1) - 1.0) / float(smoothing1)
    smooth_const2 = (float(smoothing2) - 1.0) / float(smoothing2)

    slow_values = []
    hist_values = []
    prev_slow = np.nan
    prev_hist = np.nan
    for value in fast.tolist():
        if pd.isna(value):
            slow_values.append(np.nan)
            hist_values.append(np.nan)
            continue
        if pd.isna(prev_slow):
            slow = value / float(smoothing1)
            hist = (value - slow) / float(smoothing2)
        else:
            slow = value / float(smoothing1) + prev_slow * smooth_const1
            if pd.isna(prev_hist):
                hist = (value - slow) / float(smoothing2)
            else:
                hist = (value - slow) / float(smoothing2) + prev_hist * smooth_const2
        prev_slow = slow
        prev_hist = hist
        slow_values.append(slow)
        hist_values.append(hist)

    slow = pd.Series(slow_values, index=frame.index, dtype='float64')
    hist = pd.Series(hist_values, index=frame.index, dtype='float64')
    buy_signal = (hist.shift(1) < hist.shift(2)) & (hist > hist.shift(1))
    sell_signal = (hist.shift(1) > hist.shift(2)) & (hist < hist.shift(1))

    out = frame.copy()
    out['fast'] = fast
    out['slow'] = slow
    out['hist'] = hist
    out['buy_signal'] = buy_signal.astype(float)
    out['sell_signal'] = sell_signal.astype(float)
    return out.dropna(subset=['hist'])


