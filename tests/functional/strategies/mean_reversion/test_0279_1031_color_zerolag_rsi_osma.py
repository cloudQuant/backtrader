class ColorZerolagRSIOSMAStrategy(bt.Strategy):
    """Dual-timeframe strategy trading H4 ColorZerolagRSIOSMA turning points.

    Reads precomputed histogram turning-point flags from the H4 signal feed and
    executes long/short entries on the M15 feed with fixed virtual stop-loss and
    take-profit management, while counting signals, orders and closed trades.
    """

    params = dict(
        mm=0.1,
        mm_mode='LOT',
        stop_loss=1000,
        take_profit=2000,
        deviation=10,
        buy_pos_open=True,
        sell_pos_open=True,
        buy_pos_close=True,
        sell_pos_close=True,
        signal_bar=1,
        size=0.1,
        point=0.01,
        digits_adjust=10,
        price_digits=2,
        smoothing1=15,
        smoothing2=7,
        applied_price='close',
        factor1=0.05,
        rsi_period1=8,
        factor2=0.1,
        rsi_period2=21,
        factor3=0.16,
        rsi_period3=34,
        factor4=0.26,
        rsi_period4=55,
        factor5=0.43,
        rsi_period5=89,
    )

    def __init__(self):
        """Bind the M15/H4 feeds and signal lines and reset counters and state."""
        self.m15 = self.datas[0]
        self.h4 = self.datas[1]
        self.fast = self.h4.fast
        self.slow = self.h4.slow
        self.hist = self.h4.hist
        self.buy_signal = self.h4.buy_signal
        self.sell_signal = self.h4.sell_signal

        self.bar_num = 0
        self.buy_signal_count = 0
        self.sell_signal_count = 0
        self.buy_count = 0
        self.sell_count = 0
        self.trade_count = 0
        self.win_count = 0
        self.loss_count = 0
        self.completed_order_count = 0
        self.rejected_order_count = 0

        self.entry_order = None
        self.stop_price = None
        self.take_profit_price = None
        self.last_signal_dt = None

    def log(self, text):
        """Print a timestamped log line using the M15 feed's current datetime."""
        dt = bt.num2date(self.m15.datetime[0])
        print('{0}, {1}'.format(dt.isoformat(), text))

    def _trade_unit(self):
        """Return the price distance of one stop/take point after digit adjustment."""
        return self.p.point * self.p.digits_adjust

    def _signal(self, line, idx):
        """Return True when the indicator ``line`` is an active (>0.5) flag at ``idx``.

        Args:
            line: Buy or sell signal line from the H4 feed.
            idx: Positive bar offset back from the current bar.

        Returns:
            True if the value at the offset is finite and exceeds 0.5.
        """
        try:
            value = float(line[-idx])
        except (TypeError, ValueError, IndexError):
            return False
        return not math.isnan(value) and value > 0.5

    def _enough_history(self):
        """Return True when enough histogram history exists to read the signal bar."""
        idx = max(int(self.p.signal_bar), 1)
        try:
            float(self.hist[-idx])
            float(self.hist[-idx - 1])
        except (TypeError, ValueError, IndexError):
            return False
        return True

    def _manage_risk(self):
        """Close the open position if the bar breaches its stop or take-profit level.

        Returns:
            True if an exit order was submitted, otherwise False.
        """
        if not self.position:
            return False
        high = float(self.m15.high[0])
        low = float(self.m15.low[0])
        if self.position.size > 0:
            if self.stop_price is not None and low <= self.stop_price:
                self.entry_order = self.close()
                return True
            if self.take_profit_price is not None and high >= self.take_profit_price:
                self.entry_order = self.close()
                return True
        else:
            if self.stop_price is not None and high >= self.stop_price:
                self.entry_order = self.close()
                return True
            if self.take_profit_price is not None and low <= self.take_profit_price:
                self.entry_order = self.close()
                return True
        return False

    def _set_risk_prices(self, side):
        """Compute and store stop-loss and take-profit prices for a new position.

        Args:
            side: Either ``'buy'`` or ``'sell'`` indicating the entry direction.
        """
        price = float(self.m15.close[0])
        unit = self._trade_unit()
        if side == 'buy':
            self.stop_price = round(price - self.p.stop_loss * unit, self.p.price_digits) if self.p.stop_loss > 0 else None
            self.take_profit_price = round(price + self.p.take_profit * unit, self.p.price_digits) if self.p.take_profit > 0 else None
        else:
            self.stop_price = round(price + self.p.stop_loss * unit, self.p.price_digits) if self.p.stop_loss > 0 else None
            self.take_profit_price = round(price - self.p.take_profit * unit, self.p.price_digits) if self.p.take_profit > 0 else None

    def next(self):
        """Advance one bar using debounced H4 signals and manage open positions.

        The method skips execution while waiting for sufficient indicator history,
        while an entry order is still pending, or when risk management already
        requested an exit on the current bar. It then reads the H4 signal bar and
        drives closes/entries on the M15 feed while updating signal and trade
        counters.

        Returns:
            None.
        """
        self.bar_num += 1
        if self.entry_order is not None:
            return
        if not self._enough_history():
            return
        if self._manage_risk():
            return

        idx = max(int(self.p.signal_bar), 1)
        signal_dt = bt.num2date(self.h4.datetime[-idx])
        if self.last_signal_dt == signal_dt:
            return
        self.last_signal_dt = signal_dt

        buy_open = self.p.buy_pos_open and self._signal(self.buy_signal, idx)
        sell_open = self.p.sell_pos_open and self._signal(self.sell_signal, idx)
        buy_close = self.p.buy_pos_close and sell_open
        sell_close = self.p.sell_pos_close and buy_open

        if buy_open:
            self.buy_signal_count += 1
        if sell_open:
            self.sell_signal_count += 1

        self.log('zerolagrsiosma fast={0:.6f} slow={1:.6f} hist={2:.6f} buy_open={3} sell_open={4}'.format(float(self.fast[-idx]), float(self.slow[-idx]), float(self.hist[-idx]), buy_open, sell_open))

        if buy_close and self.position and self.position.size > 0:
            self.entry_order = self.close()
            return
        if sell_close and self.position and self.position.size < 0:
            self.entry_order = self.close()
            return

        if buy_open and (not self.position or self.position.size <= 0):
            if self.position and self.position.size < 0:
                self.entry_order = self.close()
                return
            self._set_risk_prices('buy')
            self.entry_order = self.buy(size=self.p.size)
            return

        if sell_open and (not self.position or self.position.size >= 0):
            if self.position and self.position.size > 0:
                self.entry_order = self.close()
                return
            self._set_risk_prices('sell')
            self.entry_order = self.sell(size=self.p.size)

    def notify_order(self, order):
        """Update runtime counters for submitted/completed/cancelled orders.

        Args:
            order: Order object returned by Backtrader after broker events.

        Returns:
            None.
        """
        if order.status in [bt.Order.Submitted, bt.Order.Accepted]:
            return
        if order.status == bt.Order.Completed:
            self.completed_order_count += 1
            if self.position:
                if order.executed.size > 0:
                    self.buy_count += 1
                elif order.executed.size < 0:
                    self.sell_count += 1
            else:
                self.stop_price = None
                self.take_profit_price = None
        elif order.status in [bt.Order.Canceled, bt.Order.Margin, bt.Order.Rejected, bt.Order.Expired]:
            self.rejected_order_count += 1
        if self.entry_order is not None and order.ref == self.entry_order.ref and order.status not in [bt.Order.Submitted, bt.Order.Accepted]:
            self.entry_order = None

    def notify_trade(self, trade):
        """Update trade outcomes when a position is closed.

        Args:
            trade: Trade object provided by Backtrader in the notification callback.

        Returns:
            None.
        """
        if not trade.isclosed:
            return
        self.trade_count += 1
        if trade.pnlcomm >= 0:
            self.win_count += 1
        else:
            self.loss_count += 1


