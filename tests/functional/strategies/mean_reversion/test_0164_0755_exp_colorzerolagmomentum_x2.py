    todate = datetime.datetime.fromisoformat(data_cfg['todate'])
    base = load_mt5_csv(resolve_data_path(data_cfg['file']), fromdate=fromdate, todate=todate, bar_shift_minutes=data_cfg.get('bar_shift_minutes', 0))
    if base.empty:
        raise ValueError('Loaded data frame is empty')
    slow_df = compute_color_zerolag_momentum(
        resample_frame(base, f"{data_cfg.get('slow_tf_minutes', 360)}min"),
        smoothing=params['smoothing'],
        applied_price=params.get('applied_price', 'PRICE_CLOSE'),
        factor1=params['factor1'],
        momentum_period1=params['momentum_period1'],
        factor2=params['factor2'],
        momentum_period2=params['momentum_period2'],
        factor3=params['factor3'],
        momentum_period3=params['momentum_period3'],
        factor4=params['factor4'],
        momentum_period4=params['momentum_period4'],
        factor5=params['factor5'],
        momentum_period5=params['momentum_period5'],
    )
    fast_df = compute_color_zerolag_momentum(
        resample_frame(base, f"{data_cfg.get('fast_tf_minutes', 30)}min"),
        smoothing=params['smoothing_fast'],
        applied_price=params.get('applied_price_fast', 'PRICE_CLOSE'),
        factor1=params['factor1_fast'],
        momentum_period1=params['momentum_period1_fast'],
        factor2=params['factor2_fast'],
        momentum_period2=params['momentum_period2_fast'],
        factor3=params['factor3_fast'],
        momentum_period3=params['momentum_period3_fast'],
        factor4=params['factor4_fast'],
        momentum_period4=params['momentum_period4_fast'],
        factor5=params['factor5_fast'],
        momentum_period5=params['momentum_period5_fast'],
    )
    print(f'Loaded bars: base={len(base)}, slow={len(slow_df)}, fast={len(fast_df)}')
    return {'base': base, 'slow': slow_df, 'fast': fast_df, 'fromdate': fromdate, 'todate': todate}


