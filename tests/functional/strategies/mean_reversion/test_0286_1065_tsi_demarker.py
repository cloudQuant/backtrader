def resample_frame(df, rule):
    """Resample an OHLCV DataFrame to a coarser bar interval.

    Args:
        df: Source OHLCV DataFrame indexed by datetime.
        rule: Pandas offset alias describing the target bar size (e.g. '480min').

    Returns:
        A resampled DataFrame with aggregated OHLCV and volume columns, dropping
        bars without price data and filling missing open interest with zero.
    """
    out = df.resample(rule, label='right', closed='right').agg({
        'open': 'first',
        'high': 'max',
        'low': 'min',
        'close': 'last',
        'tick_volume': 'sum',
        'real_volume': 'sum',
        'openinterest': 'last',
    })
    out = out.dropna(subset=['open', 'high', 'low', 'close'])
    out['openinterest'] = out['openinterest'].fillna(0)
    return out


def weighted_moving_average(series, period):
    """Compute a linearly weighted moving average of a series.

    Args:
        series: Input pandas Series.
        period: Window length (clamped to at least 1).

    Returns:
        A Series of the weighted moving average, with leading NaNs for the
        warmup window.
    """
    period = int(max(period, 1))
    weights = np.arange(1, period + 1, dtype=float)
    return series.rolling(period).apply(lambda x: np.dot(x, weights) / weights.sum(), raw=True)


def smma(series, period):
    """Compute a smoothed (Wilder) moving average of a series.

    Args:
        series: Input pandas Series.
        period: Smoothing length (clamped to at least 1).

    Returns:
        A Series of the smoothed moving average aligned to ``series.index``.
    """
    period = int(max(period, 1))
    values = series.to_numpy(dtype=float)
    out = np.full(len(values), np.nan, dtype=float)
    if len(values) < period:
        return pd.Series(out, index=series.index)
    seed = np.nanmean(values[:period])
    out[period - 1] = seed
    for idx in range(period, len(values)):
        prev = out[idx - 1]
        curr = values[idx]
        if np.isnan(prev) or np.isnan(curr):
            continue
        out[idx] = (prev * (period - 1) + curr) / period
    return pd.Series(out, index=series.index)


def smooth_series(series, method, period):
    """Smooth a series using the requested moving-average method.

    Args:
        series: Input pandas Series.
        method: Moving-average mode (e.g. MODE_SMA, MODE_EMA, MODE_SMMA);
            anything else falls back to a weighted moving average.
        period: Smoothing length (clamped to at least 1).

    Returns:
        A smoothed pandas Series.
    """
    method = str(method or 'MODE_SMA').upper()
    period = int(max(period, 1))
    if method in ('MODE_SMA', 'MODE_SMA_'):
        return series.rolling(period).mean()
    if method in ('MODE_EMA', 'MODE_EMA_'):
        return series.ewm(span=period, adjust=False).mean()
    if method in ('MODE_SMMA', 'MODE_SMMA_'):
        return smma(series, period)
    return weighted_moving_average(series, period)


def compute_demarker(frame, demarker_period=25):
    """Compute the DeMarker oscillator from a frame's high/low prices.

    Args:
        frame: OHLCV DataFrame with high and low columns.
        demarker_period: Averaging window for the DeMax/DeMin components.

    Returns:
        A pandas Series of DeMarker values with infinities replaced by NaN.
    """
    high_diff = frame['high'].diff()
    low_diff = frame['low'].shift(1) - frame['low']
    demax = high_diff.where(high_diff > 0, 0.0)
    demin = low_diff.where(low_diff > 0, 0.0)
    avg_demax = demax.rolling(int(demarker_period)).mean()
    avg_demin = demin.rolling(int(demarker_period)).mean()
    demarker = avg_demax / (avg_demax + avg_demin)
    return demarker.replace([np.inf, -np.inf], np.nan)


def compute_tsi_demarker(frame, xma_method='MODE_SMA', demarker_period=25, mom_period=1, xlength1=5, xlength2=8, xlength3=12, xphase=15):
    """Compute the TSI-of-DeMarker oscillator and its signal line.

    Args:
        frame: OHLCV DataFrame to enrich.
        xma_method: Moving-average mode used for all smoothing steps.
        demarker_period: Averaging window for the DeMarker oscillator.
        mom_period: Momentum lag applied to the DeMarker series.
        xlength1: First smoothing length for momentum and its absolute value.
        xlength2: Second smoothing length applied to the first smoothing.
        xlength3: Smoothing length for the signal line.
        xphase: Phase parameter retained for signature compatibility.

    Returns:
        A copy of ``frame`` with ``ind`` (TSI) and ``sign`` (signal) columns,
        dropping rows where either is NaN.
    """
    out = frame.copy()
    demarker = compute_demarker(out, demarker_period=demarker_period)
    mtm = demarker - demarker.shift(int(max(mom_period, 1)))
    absmtm = mtm.abs()
    xmtm = smooth_series(mtm, xma_method, xlength1)
    xabsmtm = smooth_series(absmtm, xma_method, xlength1)
    xxmtm = smooth_series(xmtm, xma_method, xlength2)
    xxabsmtm = smooth_series(xabsmtm, xma_method, xlength2)
    tsi = np.where(xxabsmtm != 0, 100.0 * xxmtm / xxabsmtm, 0.0)
    tsi = pd.Series(tsi, index=out.index).replace(0.0, 1e-9)
    xtsi = smooth_series(tsi, xma_method, xlength3).replace(0.0, 1e-9)
    out['ind'] = tsi
    out['sign'] = xtsi
    return out.dropna(subset=['ind', 'sign'])


