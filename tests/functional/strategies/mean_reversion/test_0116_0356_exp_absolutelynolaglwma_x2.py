def resample_frame(df, minutes):
    """Resample a dataframe into fixed-minute OHLCV bars.

    Args:
        df: Source dataframe indexed by datetime.
        minutes: Target bar interval in minutes.

    Returns:
        Resampled OHLCV data with cleaned non-finite metadata columns.
    """
    rule = f'{minutes}min'
    out = df.resample(rule, label='right', closed='right').agg({
        'open': 'first',
        'high': 'max',
        'low': 'min',
        'close': 'last',
        'volume': 'sum',
        'openinterest': 'last',
        'spread': 'last',
    })
    out = out.dropna(subset=['open', 'high', 'low', 'close'])
    out['openinterest'] = out['openinterest'].fillna(0)
    out['spread'] = out['spread'].fillna(0)
    return out


