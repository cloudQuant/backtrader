def resample_frame(df, rule):
    """Resample an intraday OHLCV frame to a coarser timeframe.

    Args:
        df: Datetime-indexed OHLCV frame at the base (M15) timeframe.
        rule: Pandas offset alias for the target timeframe (e.g. ``'480min'``).

    Returns:
        pandas.DataFrame: Right-labelled, right-closed resampled frame with
        first/max/min/last aggregation for OHLC, summed volume, and forward
        open interest; rows with missing OHLC are dropped.
    """
    out = df.resample(rule, label='right', closed='right').agg({
        'open': 'first',
        'high': 'max',
        'low': 'min',
        'close': 'last',
        'volume': 'sum',
        'openinterest': 'last',
    })
    out = out.dropna(subset=['open', 'high', 'low', 'close'])
    out['openinterest'] = out['openinterest'].fillna(0)
    return out


def compute_fisher_cg_oscillator(frame, length=10):
    """Compute the Fisher Center-of-Gravity oscillator and its trigger line.

    The center-of-gravity is derived from the HL2 midprice over ``length`` bars,
    range-normalised to [0, 1], smoothed with a 4/3/2/1 weighted average, and
    Fisher-transformed into ``fcg``. The ``trigger`` line is ``fcg`` lagged by one
    bar.

    Args:
        frame: Datetime-indexed OHLCV frame (typically the resampled H8 frame).
        length: Lookback window for the center-of-gravity computation.

    Returns:
        pandas.DataFrame: Copy of ``frame`` with added ``fcg`` and ``trigger``
        columns, dropping rows where either value is undefined.
    """
    length = max(int(length), 1)
    hl2 = ((frame['high'] + frame['low']) / 2.0).to_numpy(dtype=float)
    cg_shift = (length + 1.0) / 2.0
    cg = np.full(len(frame), np.nan, dtype=float)
    value1 = np.full(len(frame), np.nan, dtype=float)
    fcg = np.full(len(frame), np.nan, dtype=float)
    trigger = np.full(len(frame), np.nan, dtype=float)

    for bar in range(length - 1, len(frame)):
        numerator = 0.0
        denominator = 0.0
        for count in range(length):
            price = hl2[bar - count]
            numerator += (1.0 + count) * price
            denominator += price
        cg[bar] = (-numerator / denominator + cg_shift) if denominator != 0.0 else 0.0

        cg_window = cg[max(0, bar - length + 1): bar + 1]
        cg_window = cg_window[~np.isnan(cg_window)]
        if cg_window.size == 0:
            continue
        hh = np.max(cg_window)
        ll = np.min(cg_window)
        value1[bar] = (cg[bar] - ll) / (hh - ll) if hh != ll else 0.0

        if bar < length - 1 + 3:
            continue
        v0 = value1[bar]
        v1 = value1[bar - 1]
        v2 = value1[bar - 2]
        v3 = value1[bar - 3]
        if any(np.isnan(v) for v in [v0, v1, v2, v3]):
            continue
        value2 = (4.0 * v0 + 3.0 * v1 + 2.0 * v2 + v3) / 10.0
        scaled = 1.98 * (value2 - 0.5)
        scaled = min(max(scaled, -0.999999), 0.999999)
        fcg[bar] = 0.5 * math.log((1.0 + scaled) / (1.0 - scaled))
        if bar > 0 and not np.isnan(fcg[bar - 1]):
            trigger[bar] = fcg[bar - 1]

    out = frame.copy()
    out['fcg'] = fcg
    out['trigger'] = trigger
    return out.dropna(subset=['fcg', 'trigger'])


