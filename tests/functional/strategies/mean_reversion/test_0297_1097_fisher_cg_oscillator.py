def load_backtest_frames(config):
    """Build the M15 execution frame and the H8 oscillator-signal frame.

    Args:
        config: Resolved configuration dict with ``data`` and ``params`` sections.

    Returns:
        dict: Mapping with ``m15`` (base frame), ``h8`` (resampled frame carrying
        the Fisher CG oscillator), and the ``fromdate``/``todate`` bounds.

    Raises:
        ValueError: If the loaded base frame is empty.
    """
    data_cfg = config['data']
    params = config['params']
    fromdate = datetime.datetime.fromisoformat(data_cfg['fromdate'])
    todate = datetime.datetime.fromisoformat(data_cfg['todate'])
    base = load_mt5_csv(resolve_data_path(data_cfg['file']), fromdate=fromdate, todate=todate, bar_shift_minutes=data_cfg.get('bar_shift_minutes', 0))
    if base.empty:
        raise ValueError('Loaded data frame is empty')
    h8 = resample_frame(base, '{0}min'.format(data_cfg.get('signal_tf_minutes', 480)))
    h8 = compute_fisher_cg_oscillator(h8, length=params['length'])
    print('Loaded bars: M15={0}, H8={1}'.format(len(base), len(h8)))
    return {'m15': base, 'h8': h8, 'fromdate': fromdate, 'todate': todate}


def build_cerebro(config, frame):
    """Assemble a Cerebro engine with both feeds, the strategy, and analyzers.

    Args:
        config: Resolved configuration dict with ``backtest`` and ``params``.
        frame: Frame mapping returned by ``load_backtest_frames``.

    Returns:
        bt.Cerebro: Configured engine with the M15 execution feed, H8 signal
        feed, ``FisherCGOscillatorStrategy``, and Sharpe/Returns/DrawDown/
        TradeAnalyzer/SQN analyzers attached.
    """
    bt_cfg = config['backtest']
    cerebro = bt.Cerebro(stdstats=True)
    cerebro.broker.setcash(bt_cfg['initial_cash'])
    comm_type = bt.CommInfoBase.COMM_FIXED if bt_cfg.get('commission_type', 'fixed') == 'fixed' else bt.CommInfoBase.COMM_PERC
    cerebro.broker.setcommission(commission=bt_cfg['commission'], margin=bt_cfg['margin'], mult=bt_cfg['multiplier'], commtype=comm_type, stocklike=bt_cfg.get('stocklike', False))
    feed_m15 = Mt5PandasFeed(dataname=frame['m15'], timeframe=bt.TimeFrame.Minutes, compression=15)
    feed_h8 = FisherCGFeed(dataname=frame['h8'][['open', 'high', 'low', 'close', 'volume', 'openinterest', 'fcg', 'trigger']], timeframe=bt.TimeFrame.Minutes, compression=480)
    cerebro.adddata(feed_m15, name='XAUUSD_M15')
    cerebro.adddata(feed_h8, name='XAUUSD_H8_FCG')
    cerebro.addstrategy(FisherCGOscillatorStrategy, **config.get('params', {}))
    cerebro.addanalyzer(bt.analyzers.SharpeRatio, _name='sharpe', timeframe=bt.TimeFrame.Minutes, factor=MINUTES_PER_TRADING_YEAR, annualize=True, riskfreerate=0)
    cerebro.addanalyzer(bt.analyzers.Returns, _name='returns', timeframe=bt.TimeFrame.Minutes, compression=15, tann=MINUTES_PER_TRADING_YEAR)
    cerebro.addanalyzer(bt.analyzers.DrawDown, _name='drawdown')
    cerebro.addanalyzer(bt.analyzers.TradeAnalyzer, _name='trades')
    cerebro.addanalyzer(bt.analyzers.SQN, _name='sqn')
    return cerebro


def extract_metrics(strat, cerebro, frame, config):
    """Collect strategy counters and analyzer results into a metrics dict.

    Args:
        strat: The executed strategy instance holding trade/order counters.
        cerebro: The Cerebro engine used to read the final portfolio value.
        frame: Frame mapping returned by ``load_backtest_frames``.
        config: Resolved configuration dict (for initial cash).

    Returns:
        dict: Performance and activity metrics (bar/signal/trade counts, win/loss
        split, returns, Sharpe, drawdown, SQN, profit factor) compared by the
        regression assertions.
    """
    sharpe = strat.analyzers.sharpe.get_analysis()
    returns = strat.analyzers.returns.get_analysis()
    drawdown = strat.analyzers.drawdown.get_analysis()
    trades = strat.analyzers.trades.get_analysis()
    sqn = strat.analyzers.sqn.get_analysis()
    initial_cash = config['backtest']['initial_cash']
    final_value = cerebro.broker.getvalue()
    total_trades = trades.get('total', {}).get('total', 0)
    won = trades.get('won', {}).get('total', 0)
    lost = trades.get('lost', {}).get('total', 0)
    gross_won = trades.get('won', {}).get('pnl', {}).get('total', 0) or 0
    gross_lost = abs(trades.get('lost', {}).get('pnl', {}).get('total', 0) or 0)
    return {
        'fromdate': frame['fromdate'],
        'todate': frame['todate'],
        'bars_m15': len(frame['m15']),
        'bars_h8': len(frame['h8']),
        'bar_num': strat.bar_num,
        'buy_signals': strat.buy_signal_count,
        'sell_signals': strat.sell_signal_count,
        'buy_count': strat.buy_count,
        'sell_count': strat.sell_count,
        'trade_count': strat.trade_count,
        'win_count': strat.win_count,
        'loss_count': strat.loss_count,
        'completed_orders': strat.completed_order_count,
        'rejected_orders': strat.rejected_order_count,
        'initial_cash': initial_cash,
        'final_value': final_value,
        'net_pnl': final_value - initial_cash,
        'total_return_pct': (final_value / initial_cash - 1) * 100,
        'total_trades': total_trades,
        'won': won,
        'lost': lost,
        'win_rate': (won / total_trades * 100) if total_trades else 0,
        'profit_factor': (gross_won / gross_lost) if gross_lost else None,
        'max_drawdown': drawdown.get('max', {}).get('drawdown', 0),
        'sharpe_ratio': sharpe.get('sharperatio'),
        'annual_return_pct': (returns.get('rnorm') or 0) * 100,
        'sqn': sqn.get('sqn'),
    }


def run(plot=False):
    """Run the full backtest pipeline and return results, metrics, and engine.

    Args:
        plot: When True, render the Cerebro plot after the run.

    Returns:
        tuple: ``(results, metrics, cerebro)`` where ``results`` is the strategy
        list, ``metrics`` is the ``extract_metrics`` dict, and ``cerebro`` is the
        engine instance.
    """
    config = _bt_load_config(_CONFIG, repo=_REPO)
    frame = load_backtest_frames(config)
    cerebro = build_cerebro(config, frame)
    print('\nStarting backtest...')
    results = cerebro.run()
    strat = results[0]
    metrics = extract_metrics(strat, cerebro, frame, config)

    if plot:
        cerebro.plot()
    return results, metrics, cerebro


def _close(actual, expected, *, tol, key):
    """Assert ``actual`` is finite and within ``tol`` of ``expected``."""
    assert actual is not None, f"{key}: expected={expected}, got=None"
    a = float(actual)
    assert math.isfinite(a), f"{key}: expected={expected}, got non-finite {actual}"
    assert abs(a - float(expected)) <= tol, (
        f"{key}: expected={expected}, got={a} (tol={tol})"
    )


