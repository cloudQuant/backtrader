def calculate_ulcer_index(values):
    """Calculate ulcer index from a sequence of values.

    Args:
        values: Time series of values.

    Returns:
        Ulcer index value.
    """
    if len(values) < 2:
        return 0.0
    max_value = values[0]
    sum_squared = 0.0
    for v in values:
        if v > max_value:
            max_value = v
        drawdown = (max_value - v) / max_value * 100.0 if max_value > 0 else 0.0
        sum_squared += drawdown ** 2
    return math.sqrt(sum_squared / len(values))


