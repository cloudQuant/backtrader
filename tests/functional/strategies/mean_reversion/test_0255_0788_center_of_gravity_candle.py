def load_backtest_frame(config):
    data_cfg = config['data']
    fromdate = datetime.datetime.fromisoformat(data_cfg['fromdate'])
    todate = datetime.datetime.fromisoformat(data_cfg['todate'])
    base_df = load_mt5_csv(resolve_data_path(data_cfg['file']), fromdate=fromdate, todate=todate, bar_shift_minutes=data_cfg.get('bar_shift_minutes', 0))
    if base_df.empty:
        raise ValueError('Loaded data frame is empty')
    indicator_minutes = int(data_cfg.get('indicator_timeframe_minutes', 360))
    signal_df = resample_frame(base_df, indicator_minutes)
    if signal_df.empty:
        raise ValueError('Resampled signal frame is empty')
    print(f"Loaded {len(base_df)} base bars and {len(signal_df)} indicator bars: {signal_df.index[0]} -> {signal_df.index[-1]}")
    return {'data': signal_df, 'fromdate': fromdate, 'todate': todate}


def build_cerebro(config, frame):
    bt_cfg = config['backtest']
    data_cfg = config['data']
    cerebro = bt.Cerebro(stdstats=True)
    cerebro.broker.setcash(bt_cfg['initial_cash'])
    comm_type = bt.CommInfoBase.COMM_FIXED if bt_cfg.get('commission_type', 'fixed') == 'fixed' else bt.CommInfoBase.COMM_PERC
    cerebro.broker.setcommission(commission=bt_cfg['commission'], margin=bt_cfg['margin'], mult=bt_cfg['multiplier'], commtype=comm_type, stocklike=bt_cfg.get('stocklike', False))
    feed = Mt5PandasFeed(dataname=frame['data'], timeframe=bt.TimeFrame.Minutes, compression=int(data_cfg.get('indicator_timeframe_minutes', 360)))
    cerebro.adddata(feed, name=f"{data_cfg['symbol']}_H6")
    cerebro.addstrategy(CenterOfGravityCandleStrategy, **config.get('params', {}))
    cerebro.addanalyzer(bt.analyzers.SharpeRatio, _name='sharpe', timeframe=bt.TimeFrame.Minutes, factor=MINUTES_PER_TRADING_YEAR, annualize=True, riskfreerate=0)
    cerebro.addanalyzer(bt.analyzers.Returns, _name='returns', timeframe=bt.TimeFrame.Minutes, compression=int(data_cfg.get('indicator_timeframe_minutes', 360)), tann=MINUTES_PER_TRADING_YEAR)
    cerebro.addanalyzer(bt.analyzers.DrawDown, _name='drawdown')
    cerebro.addanalyzer(bt.analyzers.TradeAnalyzer, _name='trades')
    cerebro.addanalyzer(bt.analyzers.SQN, _name='sqn')
    return cerebro


def extract_metrics(strat, cerebro, frame, config):
    sharpe = strat.analyzers.sharpe.get_analysis()
    returns = strat.analyzers.returns.get_analysis()
    drawdown = strat.analyzers.drawdown.get_analysis()
    trades = strat.analyzers.trades.get_analysis()
    sqn = strat.analyzers.sqn.get_analysis()
    ic = config['backtest']['initial_cash']
    fv = cerebro.broker.getvalue()
    tt = trades.get('total', {}).get('total', 0)
    w = trades.get('won', {}).get('total', 0)
    l = trades.get('lost', {}).get('total', 0)
    gw = trades.get('won', {}).get('pnl', {}).get('total', 0) or 0
    gl = abs(trades.get('lost', {}).get('pnl', {}).get('total', 0) or 0)
    return {
        'fromdate': frame['fromdate'], 'todate': frame['todate'], 'bars': len(frame['data']),
        'bar_num': strat.bar_num, 'buy_count': strat.buy_count, 'sell_count': strat.sell_count,
        'trade_count': strat.trade_count, 'win_count': strat.win_count, 'loss_count': strat.loss_count,
        'initial_cash': ic, 'final_value': fv, 'net_pnl': fv - ic, 'total_return_pct': (fv / ic - 1) * 100,
        'total_trades': tt, 'won': w, 'lost': l, 'win_rate': (w / tt * 100) if tt else 0,
        'profit_factor': (gw / gl) if gl else None, 'max_drawdown': drawdown.get('max', {}).get('drawdown', 0),
        'sharpe_ratio': sharpe.get('sharperatio'), 'annual_return_pct': (returns.get('rnorm') or 0) * 100,
        'sqn': sqn.get('sqn')}


