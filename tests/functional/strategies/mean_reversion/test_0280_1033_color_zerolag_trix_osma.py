def resample_frame(df, rule):
    """Resample an OHLCV frame to a coarser candle frequency.

    Args:
        df: Source DataFrame indexed by datetime.
        rule: Pandas offset alias such as ``240min``.

    Returns:
        Resampled DataFrame with incomplete OHLC rows removed.
    """
    out = df.resample(rule, label='right', closed='right').agg({
        'open': 'first',
        'high': 'max',
        'low': 'min',
        'close': 'last',
        'volume': 'sum',
        'openinterest': 'last',
    })
    out = out.dropna(subset=['open', 'high', 'low', 'close'])
    out['openinterest'] = out['openinterest'].fillna(0)
    return out


def compute_applied_price(frame, mode):
    """Select an applied-price series for indicator calculation.

    Args:
        frame: OHLC frame with open, high, low, and close columns.
        mode: Price mode selector (open/high/low/median/typical/weighted).

    Returns:
        Price series corresponding to the requested selector.
    """
    mode = str(mode).lower()
    if mode in ('price_open', 'open'):
        return frame['open'].astype(float)
    if mode in ('price_high', 'high'):
        return frame['high'].astype(float)
    if mode in ('price_low', 'low'):
        return frame['low'].astype(float)
    if mode in ('price_median', 'median'):
        return (frame['high'].astype(float) + frame['low'].astype(float)) / 2.0
    if mode in ('price_typical', 'typical'):
        return (frame['high'].astype(float) + frame['low'].astype(float) + frame['close'].astype(float)) / 3.0
    if mode in ('price_weighted', 'weighted'):
        return (frame['high'].astype(float) + frame['low'].astype(float) + 2.0 * frame['close'].astype(float)) / 4.0
    return frame['close'].astype(float)


def compute_trix(price, period):
    """Compute TRIX as a triple-smoothed percentage change series.

    Args:
        price: Price series used to compute the TRIX stack.
        period: EMA period for each smoothing stage.

    Returns:
        TRIX series scaled by 100, with NaN for warm-up bars.
    """
    period = int(period)
    ema1 = price.ewm(span=period, adjust=False, min_periods=period).mean()
    ema2 = ema1.ewm(span=period, adjust=False, min_periods=period).mean()
    ema3 = ema2.ewm(span=period, adjust=False, min_periods=period).mean()
    return ema3.pct_change() * 100.0


def compute_color_zerolag_trix_osma(frame, smoothing1=15, smoothing2=7, applied_price='close', factor1=0.05, trix_period1=8, factor2=0.10, trix_period2=21, factor3=0.16, trix_period3=34, factor4=0.26, trix_period4=55, factor5=0.43, trix_period5=89):
    """Compute ColorZerolagTriXOSMA indicator components and turn signals.

    Args:
        frame: OHLC DataFrame on the target signal timeframe.
        smoothing1: EMA-like smoothing constant for the slow line.
        smoothing2: Secondary smoothing constant for histogram generation.
        applied_price: Price mode passed to :func:`compute_applied_price`.
        factor1: Weight for first TRIX component.
        trix_period1: Period for first TRIX component.
        factor2: Weight for second TRIX component.
        trix_period2: Period for second TRIX component.
        factor3: Weight for third TRIX component.
        trix_period3: Period for third TRIX component.
        factor4: Weight for fourth TRIX component.
        trix_period4: Period for fourth TRIX component.
        factor5: Weight for fifth TRIX component.
        trix_period5: Period for fifth TRIX component.

    Returns:
        A copy of ``frame`` augmented with fast/slow/hist and signal columns.
    """
    price = compute_applied_price(frame, applied_price)
    trix1 = compute_trix(price, trix_period1)
    trix2 = compute_trix(price, trix_period2)
    trix3 = compute_trix(price, trix_period3)
    trix4 = compute_trix(price, trix_period4)
    trix5 = compute_trix(price, trix_period5)

    fast = factor1 * trix1 + factor2 * trix2 + factor2 * trix3 + factor4 * trix4 + factor5 * trix5
    smooth_const1 = (float(smoothing1) - 1.0) / float(smoothing1)
    smooth_const2 = (float(smoothing2) - 1.0) / float(smoothing2)

    slow_values = []
    hist_values = []
    prev_slow = np.nan
    prev_hist = np.nan
    for value in fast.tolist():
        if pd.isna(value):
            slow_values.append(np.nan)
            hist_values.append(np.nan)
            continue
        if pd.isna(prev_slow):
            slow = value / float(smoothing1)
            hist = (value - slow) / float(smoothing2)
        else:
            slow = value / float(smoothing1) + prev_slow * smooth_const1
            if pd.isna(prev_hist):
                hist = (value - slow) / float(smoothing2)
            else:
                hist = (value - slow) / float(smoothing2) + prev_hist * smooth_const2
        prev_slow = slow
        prev_hist = hist
        slow_values.append(slow)
        hist_values.append(hist)

    slow = pd.Series(slow_values, index=frame.index, dtype='float64')
    hist = pd.Series(hist_values, index=frame.index, dtype='float64')
    buy_signal = (hist.shift(1) < hist.shift(2)) & (hist > hist.shift(1))
    sell_signal = (hist.shift(1) > hist.shift(2)) & (hist < hist.shift(1))

    out = frame.copy()
    out['fast'] = fast
    out['slow'] = slow
    out['hist'] = hist
    out['buy_signal'] = buy_signal.astype(float)
    out['sell_signal'] = sell_signal.astype(float)
    return out.dropna(subset=['hist'])


