def resolve_ma_class(name):
    """Resolve a moving-average name string to a Backtrader indicator class.

    Args:
        name: Method token from strategy config.

    Returns:
        Backtrader moving average class.
    """
    mode = str(name).lower()
    if mode in {'mode_sma', 'sma'}:
        return bt.indicators.SimpleMovingAverage
    if mode in {'mode_ema', 'ema', 'mode_jjma', 'jjma', 'mode_jurx', 'jurx', 'mode_parma', 'parma', 'mode_t3', 't3', 'mode_vidya', 'vidya', 'mode_ama', 'ama'}:
        return bt.indicators.ExponentialMovingAverage
    if mode in {'mode_smma', 'smma'}:
        return bt.indicators.SmoothedMovingAverage
    return bt.indicators.WeightedMovingAverage


def resolve_price_line(data, mode):
    """Resolve a price selector token into a Backtrader line for arithmetic usage.

    Args:
        data: Backtrader data feed.
        mode: Price selector string.

    Returns:
        Data feed line for the selected price.
    """
    price_mode = str(mode).lower()
    if price_mode in {'price_close', 'close'}:
        return data.close
    if price_mode in {'price_open', 'open'}:
        return data.open
    if price_mode in {'price_high', 'high'}:
        return data.high
    if price_mode in {'price_low', 'low'}:
        return data.low
    if price_mode in {'price_median', 'median'}:
        return (data.high + data.low) / 2.0
    if price_mode in {'price_typical', 'typical'}:
        return (data.high + data.low + data.close) / 3.0
    if price_mode in {'price_weighted', 'weighted'}:
        return (2.0 * data.close + data.high + data.low) / 4.0
    if price_mode in {'price_simpl', 'simpl'}:
        return (data.open + data.close) / 2.0
    if price_mode in {'price_quarter', 'quarter'}:
        return (data.high + data.low + data.open + data.close) / 4.0
    return data.close


class BlauCMIIndicator(bt.Indicator):
    """Compute the CMI oscillator from nested moving averages of momentum."""

    lines = ('value',)
    params = dict(
        xma_method='ema',
        xlength=1,
        xlength1=20,
        xlength2=5,
        xlength3=3,
        xphase=15,
        ipc1='price_close',
        ipc2='price_open',
    )

    def __init__(self):
        """Set up numerator/denominator moving averages for oscillator output."""
        ma_cls = resolve_ma_class(self.p.xma_method)
        price1 = resolve_price_line(self.data, self.p.ipc1)
        price2 = resolve_price_line(self.data, self.p.ipc2)
        mom = price1 - price2(-max(0, int(self.p.xlength) - 1))
        abs_mom = abs(mom)
        xmom = ma_cls(mom, period=max(1, int(self.p.xlength1)))
        xabsmom = ma_cls(abs_mom, period=max(1, int(self.p.xlength1)))
        xxmom = ma_cls(xmom, period=max(1, int(self.p.xlength2)))
        xxabsmom = ma_cls(xabsmom, period=max(1, int(self.p.xlength2)))
        xxxmom = ma_cls(xxmom, period=max(1, int(self.p.xlength3)))
        xxxabsmom = ma_cls(xxabsmom, period=max(1, int(self.p.xlength3)))
        self._numerator = xxxmom
        self._denominator = xxxabsmom
        self.addminperiod(int(self.p.xlength1) + int(self.p.xlength2) + int(self.p.xlength3) + int(self.p.xlength) + 5)

    def next(self):
        """Populate the indicator value on a per-bar basis."""
        den = float(self._denominator[0])
        self.lines.value[0] = 100.0 * float(self._numerator[0]) / den if den else 0.0

    def once(self, start, end):
        """Compute indicator values in a vectorized range for Backtrader backfills."""
        numerator = self._numerator.array
        denominator = self._denominator.array
        value_line = self.lines.value.array
        while len(value_line) < end:
            value_line.append(float('nan'))

        actual_end = min(end, len(numerator), len(denominator))
        for i in range(start, actual_end):
            den = float(denominator[i])
            value_line[i] = 100.0 * float(numerator[i]) / den if den else 0.0


class BlauCMIStrategy(bt.Strategy):
    """Trade indicator turning points with directional reversal handling."""

    params = dict(
        xma_method='ema',
        xlength=1,
        xlength1=20,
        xlength2=5,
        xlength3=3,
        xphase=15,
        ipc1='price_close',
        ipc2='price_open',
        signal_bar=1,
        lot=0.1,
    )

    def __init__(self):
        """Attach CMI indicator and initialize all counters/flags."""
        self.indicator = BlauCMIIndicator(
            self.data,
            xma_method=self.p.xma_method,
            xlength=self.p.xlength,
            xlength1=self.p.xlength1,
            xlength2=self.p.xlength2,
            xlength3=self.p.xlength3,
            xphase=self.p.xphase,
            ipc1=self.p.ipc1,
            ipc2=self.p.ipc2,
        )
        self.bar_num = 0
        self.buy_count = 0
        self.sell_count = 0
        self.trade_count = 0
        self.win_count = 0
        self.loss_count = 0
        self._position_was_open = False

    def log(self, text):
        """Emit a timestamped diagnostic line."""
        dt = bt.num2date(self.data.datetime[0])
        print(f'{dt.isoformat()}, {text}')

    def _signals(self):
        shift = max(1, int(self.p.signal_bar))
        v0 = float(self.indicator.value[-shift])
        v1 = float(self.indicator.value[-shift - 1])
        v2 = float(self.indicator.value[-shift - 2])
        buy_open = False
        sell_open = False
        buy_close = False
        sell_close = False
        if v1 < v2:
            if v0 > v1:
                buy_open = True
            sell_close = True
        if v1 > v2:
            if v0 < v1:
                sell_open = True
            buy_close = True
        return buy_open, sell_open, buy_close, sell_close

    def next(self):
        """Detect buy/sell transitions and manage open/close/reverse trades."""
        self.bar_num += 1
        warmup = int(self.p.xlength) + int(self.p.xlength1) + int(self.p.xlength2) + int(self.p.xlength3) + int(self.p.signal_bar) + 5
        if len(self.data) < warmup + 2:
            return
        buy_open, sell_open, buy_close, sell_close = self._signals()
        value = float(self.indicator.value[0])
        if self.position:
            if self.position.size > 0:
                if buy_close and not sell_open:
                    self.log(f'close long value={value:.2f}')
                    self.close()
                    return
                if sell_open:
                    self.log(f'close long & sell value={value:.2f}')
                    self.close()
                    self.sell(size=self.p.lot)
                    return
            if self.position.size < 0:
                if sell_close and not buy_open:
                    self.log(f'close short value={value:.2f}')
                    self.close()
                    return
                if buy_open:
                    self.log(f'close short & buy value={value:.2f}')
                    self.close()
                    self.buy(size=self.p.lot)
                    return
        else:
            if buy_open:
                self.log(f'buy value={value:.2f}')
                self.buy(size=self.p.lot)
                return
            if sell_open:
                self.log(f'sell value={value:.2f}')
                self.sell(size=self.p.lot)
                return

    def notify_trade(self, trade):
        """Update position counters on trade open and closed events."""
        if trade.isopen and not self._position_was_open:
            if trade.size > 0:
                self.buy_count += 1
            elif trade.size < 0:
                self.sell_count += 1
            self._position_was_open = True
            return
        if not trade.isclosed:
            return
        self.trade_count += 1
        if trade.pnlcomm >= 0:
            self.win_count += 1
        else:
            self.loss_count += 1
        self._position_was_open = False
        self.log(f'trade closed pnl={trade.pnlcomm:.2f}')



