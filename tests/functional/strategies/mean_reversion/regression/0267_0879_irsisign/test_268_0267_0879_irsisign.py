def build_signal_frame(df, indicator_minutes):
    rule = f'{int(indicator_minutes)}min'
    signal_df = df.resample(rule, label='right', closed='right').agg(
        {
            'open': 'first',
            'high': 'max',
            'low': 'min',
            'close': 'last',
            'volume': 'sum',
            'openinterest': 'last',
        }
    )
    signal_df = signal_df.dropna(subset=['open', 'high', 'low', 'close'])
    if 'openinterest' in signal_df.columns:
        signal_df['openinterest'] = signal_df['openinterest'].fillna(0)
    return signal_df


def build_cerebro(config, frame):
    backtest_cfg = config['backtest']
    params = config.get('params', {})
    indicator_minutes = params.get('indicator_minutes', 60)
    signal_df = build_signal_frame(frame['data'], indicator_minutes)
    if signal_df.empty:
        raise ValueError('Signal dataframe is empty after resampling')

    cerebro = bt.Cerebro(stdstats=True)
    cerebro.broker.setcash(backtest_cfg['initial_cash'])
    commtype = bt.CommInfoBase.COMM_FIXED if backtest_cfg.get('commission_type', 'fixed') == 'fixed' else bt.CommInfoBase.COMM_PERC
    cerebro.broker.setcommission(
        commission=backtest_cfg['commission'],
        margin=backtest_cfg['margin'],
        mult=backtest_cfg['multiplier'],
        commtype=commtype,
        stocklike=backtest_cfg.get('stocklike', False),
    )

    data_cfg = config['data']
    cerebro.adddata(
        Mt5PandasFeed(dataname=frame['data'], timeframe=bt.TimeFrame.Minutes, compression=15),
        name=f"{data_cfg['symbol']}_{data_cfg['timeframe']}",
    )
    cerebro.adddata(
        Mt5PandasFeed(dataname=signal_df, timeframe=bt.TimeFrame.Minutes, compression=indicator_minutes),
        name=f"{data_cfg['symbol']}_{data_cfg['indicator_timeframe']}",
    )

    strategy_params = dict(params)
    strategy_params.pop('indicator_minutes', None)
    cerebro.addstrategy(ExpIRSISignStrategy, **strategy_params)
    cerebro.addanalyzer(bt.analyzers.SharpeRatio, _name='sharpe', timeframe=bt.TimeFrame.Minutes, factor=MINUTES_PER_TRADING_YEAR, annualize=True, riskfreerate=0.0)
    cerebro.addanalyzer(bt.analyzers.Returns, _name='returns', timeframe=bt.TimeFrame.Minutes, compression=15, tann=MINUTES_PER_TRADING_YEAR)
    cerebro.addanalyzer(bt.analyzers.DrawDown, _name='drawdown')
    cerebro.addanalyzer(bt.analyzers.TradeAnalyzer, _name='trades')
    cerebro.addanalyzer(bt.analyzers.SQN, _name='sqn')
    return cerebro


def extract_metrics(strategy, cerebro, frame, config):
    sharpe = strategy.analyzers.sharpe.get_analysis()
    returns = strategy.analyzers.returns.get_analysis()
    drawdown = strategy.analyzers.drawdown.get_analysis()
    trades = strategy.analyzers.trades.get_analysis()
    sqn = strategy.analyzers.sqn.get_analysis()
    initial_cash = config['backtest']['initial_cash']
    final_value = cerebro.broker.getvalue()
    total_trades = trades.get('total', {}).get('total', 0)
    won = trades.get('won', {}).get('total', 0)
    lost = trades.get('lost', {}).get('total', 0)
    gross_won = trades.get('won', {}).get('pnl', {}).get('total', 0) or 0
    gross_lost = abs(trades.get('lost', {}).get('pnl', {}).get('total', 0) or 0)
    return {
        'fromdate': frame['fromdate'],
        'todate': frame['todate'],
        'bars': len(frame['data']),
        'bar_num': strategy.bar_num,
        'signal_count': strategy.signal_count,
        'buy_count': strategy.buy_count,
        'sell_count': strategy.sell_count,
        'trade_count': strategy.trade_count,
        'win_count': strategy.win_count,
        'loss_count': strategy.loss_count,
        'initial_cash': initial_cash,
        'final_value': final_value,
        'net_pnl': final_value - initial_cash,
        'total_return_pct': (final_value / initial_cash - 1.0) * 100.0,
        'total_trades': total_trades,
        'won': won,
        'lost': lost,
        'win_rate': (won / total_trades * 100.0) if total_trades else 0.0,
        'profit_factor': (gross_won / gross_lost) if gross_lost else None,
        'max_drawdown': drawdown.get('max', {}).get('drawdown', 0),
        'sharpe_ratio': sharpe.get('sharperatio'),
        'annual_return_pct': (returns.get('rnorm') or 0) * 100.0,
        'sqn': sqn.get('sqn'),
    }


