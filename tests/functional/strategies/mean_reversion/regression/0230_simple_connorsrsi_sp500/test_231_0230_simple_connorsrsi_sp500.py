class ConnorsRSIMeanReversionStrategy(bt.Strategy):
    params = dict(
        lookback_days=20,
        crsi_entry=22.5,
        entry_discount_pct=1.5,
        crsi_exit=60.0,
        rsi_period=3,
        streak_period=2,
        rank_period=100,
        lot_size=1.0,
    )

    def __init__(self):
        self.bar_num = 0
        self.setup_count = 0
        self.buy_count = 0
        self.sell_count = 0
        self.trade_count = 0
        self.win_count = 0
        self.loss_count = 0
        self.expired_count = 0
        self.cancelled_count = 0
        self.rejected_count = 0
        self.order = None
        self.pending_signal_price = None



    def _get_position_size(self, target_notional_pct=1.0, price=None):
        if target_notional_pct <= 0:
            return 0.0
        broker_value = float(self.broker.getvalue())
        execution_price = float(self.data.close[0] if price is None else price)
        if broker_value <= 0 or execution_price <= 0:
            return 0.0
        comminfo = self.broker.getcommissioninfo(self.data)
        multiplier = float(getattr(comminfo.p, 'mult', 1.0) or 1.0)
        if multiplier <= 0:
            return 0.0
        size = broker_value * float(target_notional_pct) / (execution_price * multiplier)
        return max(0.01, round(size, 2))

    def log(self, text):
        dt = bt.num2date(self.data.datetime[0])
        print(f'{dt.isoformat()}, {text}')

    def _entry_signal_ready(self):
        if len(self.data) < 2:
            return False
        return float(self.data.close[-1]) > float(self.data.trend_ma[-1]) and float(self.data.crsi[-1]) < float(self.p.crsi_entry)

    def next(self):
        self.bar_num += 1
        if self.order is not None:
            return
        if self.position:
            if float(self.data.crsi[0]) >= float(self.p.crsi_exit):
                self.sell_count += 1
                self.order = self.close()
                self.log(f'exit signal crsi={float(self.data.crsi[0]):.2f}')
            return
        if not self._entry_signal_ready():
            return
        self.setup_count += 1
        # 市价买入，不再使用限价单
        self.order = self.buy(size=self._get_position_size(target_notional_pct=float(self.p.lot_size)))
        self.log(f'place buy market prev_crsi={float(self.data.crsi[-1]):.2f}')

    def notify_order(self, order):
        if order.status in (order.Submitted, order.Accepted):
            return
        if order.status == order.Completed:
            if order.isbuy():
                self.buy_count += 1
                self.log(f'buy filled price={order.executed.price:.2f}')
            else:
                self.log(f'sell filled price={order.executed.price:.2f}')
        elif order.status == order.Expired:
            self.expired_count += 1
            self.log('entry order expired')
        elif order.status == order.Canceled:
            self.cancelled_count += 1
        elif order.status in (order.Margin, order.Rejected):
            self.rejected_count += 1
            self.log(f'order failed status={order.getstatusname()}')
        # 无论订单状态如何，都清除挂单引用
        self.order = None

    def notify_trade(self, trade):
        if not trade.isclosed:
            return
        self.trade_count += 1
        if trade.pnlcomm >= 0:
            self.win_count += 1
        else:
            self.loss_count += 1
        self.log(f'trade closed pnl={trade.pnlcomm:.2f}')





