def normalize(value):
    if value is None or isinstance(value, (str, int, float, bool)):
        return value
    isoformat = getattr(value, 'isoformat', None)
    if callable(isoformat):
        try:
            return value.isoformat(sep=' ')
        except TypeError:
            return value.isoformat()
    return str(value)


def calculate_ulcer_index(equity_curve):
    if not equity_curve:
        return 0.0
    peak = equity_curve[0]
    drawdown_squares = []
    for value in equity_curve:
        peak = max(peak, value)
        drawdown_pct = ((peak - value) / peak * 100.0) if peak > 0 else 0.0
        drawdown_squares.append(drawdown_pct * drawdown_pct)
    return math.sqrt(sum(drawdown_squares) / len(drawdown_squares)) if drawdown_squares else 0.0


