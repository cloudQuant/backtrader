class ExtremumStrategy(bt.Strategy):
    params = dict(
        n_bars=20,
        stop_loss_points=1000,
        take_profit_points=2000,
        mm=-0.1,
        point=0.01,
        buy_pos_open=True,
        sell_pos_open=True,
        buy_pos_close=True,
        sell_pos_close=True,
        indicator_minutes=240,
    )

    def __init__(self):
        self.base = self.datas[0]
        self.signal_data = self.datas[1]
        self.bar_num = 0
        self.signal_count = 0
        self.buy_count = 0
        self.sell_count = 0
        self.trade_count = 0
        self.win_count = 0
        self.loss_count = 0
        self._position_was_open = False
        self._last_signal_len = 0

    def log(self, text):
        dt = bt.num2date(self.base.datetime[0])
        print(f'{dt.isoformat()}, {text}')

    def _line_value(self, line, ago):
        return float(line[-ago]) if ago else float(line[0])

    def _extremum_sum(self, ago=0):
        if len(self.signal_data) < self.p.n_bars + ago + 1:
            return None
        current_high = self._line_value(self.signal_data.high, ago)
        current_low = self._line_value(self.signal_data.low, ago)
        n_value = 0.0
        m_value = 0.0
        for i in range(self.p.n_bars):
            ref_high = self._line_value(self.signal_data.high, ago + i)
            ref_low = self._line_value(self.signal_data.low, ago + i)
            high_diff = current_high - ref_high
            low_diff = current_low - ref_low
            if current_high > ref_high and high_diff > n_value:
                n_value = high_diff
            if current_low < ref_low and low_diff < m_value:
                m_value = current_high - ref_low
        return n_value + m_value

    def _signal_state(self, ago=0):
        value = self._extremum_sum(ago)
        if value is None:
            return None
        if value > 0:
            return 'green'
        if value < 0:
            return 'red'
        return 'flat'

    def _position_size(self, price):
        if self.p.mm < 0:
            return abs(self.p.mm)
        if price <= 0:
            return 0.0
        cash = self.broker.getcash()
        return round((cash * self.p.mm) / price, 4)

    def _check_exit_levels(self):
        if not self.position:
            return False
        close_price = float(self.base.close[0])
        point_value = float(self.p.point)
        stop_distance = self.p.stop_loss_points * point_value if self.p.stop_loss_points > 0 else None
        take_distance = self.p.take_profit_points * point_value if self.p.take_profit_points > 0 else None
        entry_price = float(self.position.price)

        if self.position.size > 0:
            if stop_distance is not None and close_price <= entry_price - stop_distance:
                self.log(f'close long by stop loss close={close_price:.2f} entry={entry_price:.2f}')
                self.close()
                return True
            if take_distance is not None and close_price >= entry_price + take_distance:
                self.log(f'close long by take profit close={close_price:.2f} entry={entry_price:.2f}')
                self.close()
                return True
        elif self.position.size < 0:
            if stop_distance is not None and close_price >= entry_price + stop_distance:
                self.log(f'close short by stop loss close={close_price:.2f} entry={entry_price:.2f}')
                self.close()
                return True
            if take_distance is not None and close_price <= entry_price - take_distance:
                self.log(f'close short by take profit close={close_price:.2f} entry={entry_price:.2f}')
                self.close()
                return True
        return False

    def next(self):
        self.bar_num += 1
        if len(self.base) < 2:
            return

        if self._check_exit_levels():
            return

        if len(self.signal_data) < self.p.n_bars + 2:
            return

        current_signal_len = len(self.signal_data)
        if current_signal_len == self._last_signal_len:
            return
        self._last_signal_len = current_signal_len

        prev_state = self._signal_state(1)
        current_state = self._signal_state(0)
        if prev_state is None or current_state is None:
            return

        close_price = float(self.base.close[0])
        size = self._position_size(close_price)
        if size <= 0:
            return

        if prev_state == 'red' and current_state == 'green':
            self.signal_count += 1
            self.log(f'buy signal h4_state={prev_state}->{current_state} close={close_price:.2f}')
            if self.position.size < 0 and self.p.sell_pos_close:
                self.close()
            if self.position.size <= 0 and self.p.buy_pos_open:
                self.buy(size=size)
            return

        if prev_state == 'green' and current_state == 'red':
            self.signal_count += 1
            self.log(f'sell signal h4_state={prev_state}->{current_state} close={close_price:.2f}')
            if self.position.size > 0 and self.p.buy_pos_close:
                self.close()
            if self.position.size >= 0 and self.p.sell_pos_open:
                self.sell(size=size)

    def notify_trade(self, trade):
        if trade.isopen and not self._position_was_open:
            if trade.size > 0:
                self.buy_count += 1
            elif trade.size < 0:
                self.sell_count += 1
            self._position_was_open = True
            return
        if not trade.isclosed:
            return
        self.trade_count += 1
        if trade.pnlcomm >= 0:
            self.win_count += 1
        else:
            self.loss_count += 1
        self._position_was_open = False
        self.log(f'trade closed pnl={trade.pnlcomm:.2f}')



