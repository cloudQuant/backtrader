def resample_ohlcv(df, rule):
    """Resample OHLCV dataframe into a new timeframe using candlestick aggregation.

    Args:
        df: Source OHLCV dataframe.
        rule: Pandas resampling rule (for example ``4h``).

    Returns:
        Aggregated dataframe.
    """
    agg = {
        'open': 'first',
        'high': 'max',
        'low': 'min',
        'close': 'last',
        'volume': 'sum',
        'openinterest': 'last',
        'spread': 'last',
    }
    out = df.resample(rule, label='right', closed='right').agg(agg).dropna()
    return out


