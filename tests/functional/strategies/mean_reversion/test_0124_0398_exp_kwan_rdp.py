def load_mt5_csv(filepath, fromdate=None, todate=None, bar_shift_minutes=0):
    """Load MT5 format historical CSV data file into a pandas DataFrame.

    Args:
        filepath (str or Path): Path to the MT5 CSV file.
        fromdate (datetime.datetime, optional): Start date to filter data. Defaults to None.
        todate (datetime.datetime, optional): End date to filter data. Defaults to None.
        bar_shift_minutes (int): Minutes to shift data timestamps. Defaults to 0.

    Returns:
        pd.DataFrame: Cleaned and sorted DataFrame containing MT5 data.
    """
    with open(filepath, 'r', encoding='utf-8') as f:
        lines = f.read().strip().split('\n')
    cleaned = '\n'.join(line.strip().strip('"') for line in lines if line.strip())
    df = pd.read_csv(io.StringIO(cleaned), sep='\t')
    df['datetime'] = pd.to_datetime(df['<DATE>'] + ' ' + df['<TIME>'], format='%Y.%m.%d %H:%M:%S')
    df = df.rename(columns={
        '<OPEN>': 'open',
        '<HIGH>': 'high',
        '<LOW>': 'low',
        '<CLOSE>': 'close',
        '<TICKVOL>': 'volume',
        '<VOL>': 'openinterest',
        '<SPREAD>': 'spread',
    })
    df = df[['datetime', 'open', 'high', 'low', 'close', 'volume', 'openinterest', 'spread']]
    df = df.set_index('datetime').sort_index()
    if bar_shift_minutes:
        df.index = df.index + pd.Timedelta(minutes=bar_shift_minutes)
    if fromdate is not None:
        df = df[df.index >= fromdate]
    if todate is not None:
        df = df[df.index <= todate]
    return df


def resample_frame(df, minutes):
    """Resample raw M15 data into higher-timeframe signals.

    Args:
        df (pd.DataFrame): Raw historical market data.
        minutes (int): Target resampling timeframe in minutes.

    Returns:
        pd.DataFrame: Resampled higher-timeframe market data.
    """
    rule = f'{int(minutes)}min'
    out = df.resample(rule, label='right', closed='right').agg({
        'open': 'first',
        'high': 'max',
        'low': 'min',
        'close': 'last',
        'volume': 'sum',
        'openinterest': 'last',
        'spread': 'last',
    })
    out = out.dropna(subset=['open', 'high', 'low', 'close'])
    out['openinterest'] = out['openinterest'].fillna(0)
    out['spread'] = out['spread'].fillna(0)
    return out



def load_backtest_frame(config: dict) -> dict:
    """Load historical market data and prepare resampled signal frames.

    Args:
        config (dict): Backtest configuration containing data paths and compressions.

    Raises:
        ValueError: If the loaded data frame is empty.

    Returns:
        dict: Loaded data frame dictionary containing execution and resampled signals.
    """
    data_cfg = config['data']
    data_file = data_cfg['file']
    if not os.path.isabs(data_file):
        data_file = os.path.normpath(os.path.join(SCRIPT_DIR, data_file))
    fromdate = dt.datetime.strptime(data_cfg['fromdate'], '%Y-%m-%d %H:%M:%S')
    todate = dt.datetime.strptime(data_cfg['todate'], '%Y-%m-%d %H:%M:%S')
    frame = load_mt5_csv(
        data_file,
        fromdate=fromdate,
        todate=todate,
        bar_shift_minutes=data_cfg.get('bar_shift_minutes', 0),
    )
    if frame.empty:
        raise ValueError('Loaded data frame is empty')
    signal = resample_frame(frame, data_cfg.get('signal_compression_minutes', 60))
    return {'data': frame, 'signal': signal}


