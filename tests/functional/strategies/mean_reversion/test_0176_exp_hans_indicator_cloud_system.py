def parse_dt(value):
    """Parse an ISO datetime string into a datetime, or None.

    Args:
        value: ISO-formatted datetime string, or a falsy value.

    Returns:
        A ``datetime.datetime`` instance, or None when ``value`` is falsy.
    """
    if not value:
        return None
    return datetime.datetime.fromisoformat(value)


def load_backtest_frame(config):
    """Load execution bars and build the Hans signal frame from the config.

    Args:
        config: Resolved configuration dict with ``data`` and ``params``.

    Returns:
        Dict with ``data`` (execution frame) and ``signal`` (Hans frame) keys.

    Raises:
        ValueError: If the loaded execution frame is empty.
    """
    data_cfg = config['data']
    params = config['params']
    fromdate = parse_dt(data_cfg.get('fromdate'))
    todate = parse_dt(data_cfg.get('todate'))
    df = load_mt5_csv(resolve_data_path(data_cfg['file']), fromdate=fromdate, todate=todate, bar_shift_minutes=data_cfg.get('bar_shift_minutes', 0))
    if df.empty:
        raise ValueError('Loaded data frame is empty')
    signal_df = build_hans_indicator_frame(
        df,
        indicator_minutes=data_cfg.get('signal_compression_minutes', 30),
        local_timezone=params.get('local_timezone', 0),
        dest_timezone=params.get('dest_timezone', 4),
        pips_for_entry=params.get('pips_for_entry', 100),
        point_size=params.get('point_size', 0.01),
    )
    print(f'Loaded {len(df)} execution bars: {df.index[0]} -> {df.index[-1]}')
    return {'data': df, 'signal': signal_df}


def add_default_analyzers(cerebro):
    """Attach the standard Sharpe, returns, drawdown, trade, and SQN analyzers.

    Args:
        cerebro: The cerebro instance to configure.
    """
    cerebro.addanalyzer(bt.analyzers.SharpeRatio, _name='sharpe', timeframe=bt.TimeFrame.Minutes, factor=MINUTES_PER_TRADING_YEAR, annualize=True, riskfreerate=0)
    cerebro.addanalyzer(bt.analyzers.Returns, _name='returns', timeframe=bt.TimeFrame.Minutes, compression=60, tann=MINUTES_PER_TRADING_YEAR)
    cerebro.addanalyzer(bt.analyzers.DrawDown, _name='drawdown')
    cerebro.addanalyzer(bt.analyzers.TradeAnalyzer, _name='trades')
    cerebro.addanalyzer(bt.analyzers.SQN, _name='sqn')


def build_cerebro(config, frame):
    """Assemble a cerebro with broker, both feeds, strategy, and analyzers.

    Args:
        config: Resolved configuration dict.
        frame: Dict with ``data`` and ``signal`` DataFrames.

    Returns:
        The configured ``bt.Cerebro`` instance ready to run.
    """
    bt_cfg = config['backtest']
    data_cfg = config['data']
    params = dict(config.get('params', {}))
    for key in ['local_timezone', 'dest_timezone', 'pips_for_entry']:
        params.pop(key, None)
    cerebro = bt.Cerebro(stdstats=True)
    cerebro.broker.setcash(bt_cfg['initial_cash'])
    comm_type = bt.CommInfoBase.COMM_FIXED if bt_cfg.get('commission_type', 'fixed') == 'fixed' else bt.CommInfoBase.COMM_PERC
    cerebro.broker.setcommission(commission=bt_cfg['commission'], margin=bt_cfg['margin'], mult=bt_cfg['multiplier'], commtype=comm_type, stocklike=bt_cfg.get('stocklike', False))
    execution_feed = Mt5PandasFeed(dataname=frame['data'], timeframe=bt.TimeFrame.Minutes, compression=data_cfg.get('execution_compression_minutes', 15))
    signal_feed = HansFeed(dataname=frame['signal'], timeframe=bt.TimeFrame.Minutes, compression=data_cfg.get('signal_compression_minutes', 30))
    cerebro.adddata(execution_feed, name=f"{data_cfg['symbol']}_{data_cfg['base_timeframe']}")
    cerebro.adddata(signal_feed, name=f"{data_cfg['symbol']}_{data_cfg['signal_timeframe']}")
    cerebro.addstrategy(ExpHansIndicatorCloudSystemStrategy, **params)
    add_default_analyzers(cerebro)
    return cerebro


def finite_or_none(value):
    """Return the value unless it is a non-finite number, in which case None.

    Args:
        value: Any value, typically a float metric.

    Returns:
        ``value`` when it is None or a finite number, otherwise None.
    """
    if value is None:
        return None
    if isinstance(value, (int, float)) and not math.isfinite(value):
        return None
    return value


def summarize(results, start_value):
    """Print a human-readable backtest summary from analyzer output.

    Args:
        results: List of strategy results returned by ``cerebro.run``.
        start_value: Broker value at the start of the run.
    """
    strat = results[0]
    end_value = strat.broker.getvalue()
    drawdown = strat.analyzers.drawdown.get_analysis()
    returns = strat.analyzers.returns.get_analysis()
    sharpe = strat.analyzers.sharpe.get_analysis()
    sqn = strat.analyzers.sqn.get_analysis()
    trades = strat.analyzers.trades.get_analysis()
    total_closed = trades.get('total', {}).get('closed', 0)
    won = trades.get('won', {}).get('total', 0)
    lost = trades.get('lost', {}).get('total', 0)
    strike_rate = (won / total_closed * 100.0) if total_closed else 0.0
    print('\n# Backtest Summary')
    print(f'- Start Value: {start_value:.2f}')
    print(f'- End Value: {end_value:.2f}')
    print(f'- Net PnL: {end_value - start_value:.2f}')
    print(f'- Max Drawdown: {drawdown.get("max", {}).get("drawdown", 0.0):.2f}%')
    print(f'- Total Closed Trades: {total_closed}')
    print(f'- Won Trades: {won}')
    print(f'- Lost Trades: {lost}')
    print(f'- Strike Rate: {strike_rate:.2f}%')
    print(f'- Sharpe: {finite_or_none(sharpe.get("sharperatio"))}')
    print(f'- SQN: {finite_or_none(sqn.get("sqn"))}')
    print(f'- Total Return: {returns.get("rtot", 0.0):.6f}')


def extract_metrics(results, start_value):
    """Collect strategy counters and analyzer output into a metrics dict.

    Args:
        results: Backtest result list returned by ``cerebro.run``.
        start_value: Broker value at the start of the run.

    Returns:
        Dict[str, Any]: Backtest metric values keyed by metric name.

    Raises:
        Exception: Propagates any exception from missing analyzer data access.
    """
    strat = results[0]
    end_value = strat.broker.getvalue()
    drawdown = strat.analyzers.drawdown.get_analysis()
    returns = strat.analyzers.returns.get_analysis()
    sharpe = strat.analyzers.sharpe.get_analysis()
    sqn = strat.analyzers.sqn.get_analysis()
    trades = strat.analyzers.trades.get_analysis()
    total_closed = trades.get('total', {}).get('closed', 0)
    won = trades.get('won', {}).get('total', 0)
    lost = trades.get('lost', {}).get('total', 0)
    gross_won = trades.get('won', {}).get('pnl', {}).get('total', 0) or 0
    gross_lost = abs(trades.get('lost', {}).get('pnl', {}).get('total', 0) or 0)
    return {
        'bar_num': strat.bar_num,
        'buy_count': strat.buy_count,
        'sell_count': strat.sell_count,
        'trade_count': strat.trade_count,
        'win_count': strat.win_count,
        'loss_count': strat.loss_count,
        'initial_cash': start_value,
        'final_value': end_value,
        'net_pnl': end_value - start_value,
        'total_return_pct': (end_value / start_value - 1.0) * 100.0,
        'total_trades': total_closed,
        'won': won,
        'lost': lost,
        'win_rate': (won / total_closed * 100.0) if total_closed else 0.0,
        'profit_factor': (gross_won / gross_lost) if gross_lost else None,
        'max_drawdown': drawdown.get('max', {}).get('drawdown', 0.0),
        'sharpe_ratio': finite_or_none(sharpe.get('sharperatio')),
        'annual_return_pct': (returns.get('rnorm') or 0.0) * 100.0,
        'sqn': finite_or_none(sqn.get('sqn')),
    }



def run(plot=False):
    """Run the compiled strategy backtest and return execution artifacts.

    Args:
        plot: Whether to display a candlestick chart after execution.

    Returns:
        tuple[list, dict, bt.Cerebro]: ``(results, metrics, cerebro)`` where
            ``results`` is the list returned by Backtrader.
    """
    config = load_config()
    frame = load_backtest_frame(config)
    cerebro = build_cerebro(config, frame)
    start_value = cerebro.broker.getvalue()
    results = cerebro.run(runonce=False)
    metrics = extract_metrics(results, start_value)

    if plot:
        cerebro.plot(style='candlestick')
    return results, metrics, cerebro


def main():
    """Parse command-line arguments and run the strategy backtest."""
    parser = argparse.ArgumentParser(description='Run Exp_Hans_Indicator_Cloud_System backtest')
    parser.add_argument('--plot', action='store_true', help='Plot result chart')
    args = parser.parse_args()
    run(plot=args.plot)


def _close(actual, expected, *, tol, key):
    """Assert ``actual`` is finite and within ``tol`` of ``expected``."""
    assert actual is not None, f"{key}: expected={expected}, got=None"
    a = float(actual)
    assert math.isfinite(a), f"{key}: expected={expected}, got non-finite {actual}"
    assert abs(a - float(expected)) <= tol, (
        f"{key}: expected={expected}, got={a} (tol={tol})"
    )


def _invoke_strategy_main():
    """Call main() or run() depending on what the original script defined."""
    import sys as _sys
    _mod = _sys.modules[__name__]
    if hasattr(_mod, "main") and callable(_mod.main):
        return _mod.main()
    if hasattr(_mod, "run") and callable(_mod.run):
        return _mod.run()
    raise RuntimeError("Neither main() nor run() found in inlined module")


