class ZigZagEvgeTrofiVer1Strategy(bt.Strategy):
    """Strategy class implementing ZigZag swing pivot trading logic.

    Attributes:
        params (dict): Configured strategy parameters.
    """
    params = dict(
        depth=17,
        deviation=7,
        backstep=5,
        lot=0.10,
        signal_reverse=False,
        urgency=2,
        point=0.01,
    )

    def __init__(self):
        """Initialize indicators, backtest tracking metrics, and state variables."""
        self.signal_indicator = bt.indicators.ZigZagRecentPivotSignal(
            self.data,
            depth=self.p.depth,
            deviation=self.p.deviation,
            backstep=self.p.backstep,
            point=self.p.point,
        )
        self.order = None
        self.bar_num = 0
        self.buy_count = 0
        self.sell_count = 0
        self.trade_count = 0
        self.win_count = 0
        self.loss_count = 0

    def next(self):
        """Execute the strategy decision logic on each new bar."""
        self.bar_num += 1
        if len(self.data) <= self.p.depth + self.p.backstep + 2:
            return
        if self.order:
            return

        raw_signal = int(self.signal_indicator.signal[0])
        pivot_age = int(self.signal_indicator.pivot_age[0]) if self.signal_indicator.pivot_age[0] < 999999 else 999999
        signal = raw_signal
        if self.p.signal_reverse:
            signal = -signal
        if signal == 0 or pivot_age > self.p.urgency:
            return

        if signal == 1 and self.position.size < 0:
            self.order = self.close()
            return
        if signal == -1 and self.position.size > 0:
            self.order = self.close()
            return

        if signal == 1:
            self.order = self.buy(size=self.p.lot)
        elif signal == -1:
            self.order = self.sell(size=self.p.lot)

    def notify_order(self, order):
        """Callback to handle order status updates.

        Args:
            order (bt.Order): The updated order instance.
        """
        if order.status in [bt.Order.Submitted, bt.Order.Accepted]:
            return
        if order.status == order.Completed:
            if order.isbuy() and order.executed.size > 0:
                self.buy_count += 1
            elif order.issell() and order.executed.size < 0:
                self.sell_count += 1
        if order.status in [bt.Order.Completed, bt.Order.Canceled, bt.Order.Margin, bt.Order.Rejected, bt.Order.Expired]:
            self.order = None

    def notify_trade(self, trade):
        """Callback to handle closed trades and manage win/loss counts.

        Args:
            trade (bt.Trade): The closed trade instance.
        """
        if not trade.isclosed:
            return
        self.trade_count += 1
        if trade.pnlcomm >= 0:
            self.win_count += 1
        else:
            self.loss_count += 1


