def load_mt5_csv(filepath, fromdate=None, todate=None, bar_shift_minutes=0):
    """Load MT5-formatted data and return a normalized OHLCV frame.

    Parameters:
        filepath: Data file path.
        fromdate: Optional start datetime.
        todate: Optional end datetime.
        bar_shift_minutes: Minute offset applied to index.

    Returns:
        DataFrame with datetime index and OHLCV + openinterest columns.
    """
    with open(filepath, 'r', encoding='utf-8') as f:
        lines = f.read().strip().split('\n')
    cleaned = '\n'.join(line.strip().strip('"') for line in lines)
    df = pd.read_csv(io.StringIO(cleaned), sep='\t')
    df['datetime'] = pd.to_datetime(df['<DATE>'] + ' ' + df['<TIME>'], format='%Y.%m.%d %H:%M:%S')
    df = df.rename(columns={
        '<OPEN>': 'open',
        '<HIGH>': 'high',
        '<LOW>': 'low',
        '<CLOSE>': 'close',
        '<TICKVOL>': 'tick_volume',
        '<VOL>': 'real_volume',
    })
    df['openinterest'] = 0
    df = df[['datetime', 'open', 'high', 'low', 'close', 'tick_volume', 'real_volume', 'openinterest']]
    df = df.set_index('datetime')
    if bar_shift_minutes:
        df.index = df.index + pd.Timedelta(minutes=bar_shift_minutes)
    if fromdate is not None:
        df = df[df.index >= fromdate]
    if todate is not None:
        df = df[df.index <= todate]
    return df


def resample_frame(df, rule):
    """Resample OHLCV bars for signal timeframe calculations."""
    """Aggregate OHLCV fields with right-labeled bins.

    Parameters:
        df: Input time series.
        rule: Pandas frequency spec.

    Returns:
        Resampled DataFrame with cleaned bars.
    """
    out = df.resample(rule, label='right', closed='right').agg({
        'open': 'first',
        'high': 'max',
        'low': 'min',
        'close': 'last',
        'tick_volume': 'sum',
        'real_volume': 'sum',
        'openinterest': 'last',
    })
    out = out.dropna(subset=['open', 'high', 'low', 'close'])
    out['openinterest'] = out['openinterest'].fillna(0)
    return out


def smooth_series(series, period, method='MODE_EMA'):
    """Smooth a series using configured moving-average style.

    Args:
        series: Input pandas Series.
        period: Window period.
        method: One of MODE_EMA / MODE_SMA / MODE_SMMA / MODE_LWMA.

    Returns:
        Smoothed series.
    """
    period = max(int(period), 1)
    if method == 'MODE_SMA':
        return series.rolling(period, min_periods=period).mean()
    if method == 'MODE_SMMA':
        return series.ewm(alpha=1.0 / period, adjust=False).mean()
    if method == 'MODE_LWMA':
        weights = np.arange(1, period + 1, dtype=float)
        return series.rolling(period, min_periods=period).apply(lambda x: np.dot(x, weights) / weights.sum(), raw=True)
    return series.ewm(span=period, adjust=False).mean()


def compute_blau_hlm(frame, xma_method='MODE_EMA', xlength=2, xlength1=20, xlength2=5, xlength3=3, xlength4=3, point=0.01):
    """Compute BlauHLM components and colorized histogram lines.

    Parameters:
        frame: OHLCV source frame.
        xma_method: Smoothing method for intermediate series.
        xlength: Base lookback for HLM delta.
        xlength1: First smoothing period.
        xlength2: Second smoothing period.
        xlength3: Third smoothing period.
        xlength4: Signal smoothing period.
        point: Point-size scaling.

    Returns:
        Frame with ``up``, ``dn``, ``hist`` and ``color`` columns.
    """
    out = frame.copy()
    xlength = max(int(xlength), 1)
    xlength1 = max(int(xlength1), 1)
    xlength2 = max(int(xlength2), 1)
    xlength3 = max(int(xlength3), 1)
    xlength4 = max(int(xlength4), 1)
    bars = len(out)
    hlm = pd.Series(index=out.index, dtype=float)
    for i in range(bars):
        if i < xlength - 1:
            hlm.iloc[i] = np.nan
            continue
        hmu = float(out['high'].iloc[i] - out['high'].iloc[i - (xlength - 1)])
        lmd = float(-(out['low'].iloc[i] - out['low'].iloc[i - (xlength - 1)]))
        hmu = hmu if hmu > 0 else 0.0
        lmd = lmd if lmd > 0 else 0.0
        hlm.iloc[i] = (hmu - lmd) / point
    xhlm = smooth_series(hlm, xlength1, xma_method)
    xxhlm = smooth_series(xhlm, xlength2, xma_method)
    xxxhlm = smooth_series(xxhlm, xlength3, xma_method)
    sign = smooth_series(xxxhlm, xlength4, xma_method)
    color = pd.Series(index=out.index, dtype=float)
    for i in range(bars):
        if i == 0 or pd.isna(xxxhlm.iloc[i]) or pd.isna(xxxhlm.iloc[i - 1]):
            color.iloc[i] = np.nan
            continue
        curr = float(xxxhlm.iloc[i])
        prev = float(xxxhlm.iloc[i - 1])
        clr = 2.0
        if curr > 0:
            if curr > prev:
                clr = 4.0
            elif curr < prev:
                clr = 3.0
        elif curr < 0:
            if curr < prev:
                clr = 0.0
            elif curr > prev:
                clr = 1.0
        color.iloc[i] = clr
    out['up'] = xxxhlm
    out['dn'] = sign
    out['hist'] = xxxhlm
    out['color'] = color
    out = out.dropna(subset=['up', 'dn', 'hist'])
    return out


