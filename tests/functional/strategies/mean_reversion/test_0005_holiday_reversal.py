def load_mt5_csv(filepath, fromdate=None, todate=None):
    """Load MT5 CSV data into a normalized OHLCV DataFrame."""
    with open(filepath, 'r', encoding='utf-8', errors='ignore') as handle:
        lines = [line.strip().strip('"') for line in handle.readlines() if line.strip()]
    cleaned = '\n'.join(lines)
    sep = '\t' if '\t' in lines[0] else ','
    df = pd.read_csv(io.StringIO(cleaned), sep=sep)
    dt_text = df['<DATE>'].astype(str) + ' ' + df['<TIME>'].astype(str)
    parsed = pd.to_datetime(dt_text, format='%Y.%m.%d %H:%M', errors='coerce')
    if parsed.isna().any():
        parsed = pd.to_datetime(dt_text, format='%Y.%m.%d %H:%M:%S', errors='coerce')
    df['datetime'] = parsed
    df = df.rename(columns={
        '<OPEN>': 'open', '<HIGH>': 'high', '<LOW>': 'low',
        '<CLOSE>': 'close', '<TICKVOL>': 'tick_volume', '<VOL>': 'real_volume',
    })
    df['openinterest'] = 0
    df['volume'] = df['tick_volume'] if 'tick_volume' in df.columns else 0
    df = df[['datetime', 'open', 'high', 'low', 'close', 'volume', 'openinterest']]
    df = df.set_index('datetime').sort_index()
    if fromdate is not None:
        df = df[df.index >= fromdate]
    if todate is not None:
        df = df[df.index <= todate]
    return df


def get_holiday_dates(year):
    """Generate a fixed set of holiday dates for the given year."""
    holidays = []
    # New Year's Day (January 1).
    holidays.append(datetime(year, 1, 1))
    # Labour Day (May 1).
    holidays.append(datetime(year, 5, 1))
    # US Independence Day (July 4).
    holidays.append(datetime(year, 7, 4))
    # U.S. Thanksgiving (fourth Thursday in November).
    try:
        thanksgiving = pd.date_range(start=f'{year}-11-01', end=f'{year}-11-30', freq='WOM-4THU')[0]
        holidays.append(thanksgiving)
    except:
        pass
    # Christmas Day (December 25).
    holidays.append(datetime(year, 12, 25))
    return holidays


def prepare_holiday_reversal_features(df, params):
    """Prepare momentum and holiday-window features used by the strategy."""
    out = df.copy()
    lookback = int(params.get('lookback', 20))
    momentum_threshold = float(params.get('momentum_threshold', 0.0))
    
    # Calculate momentum over the configured lookback window.
    out['momentum'] = out['close'] / out['close'].shift(lookback) - 1
    
    # Tag a 1-week-before and 1-week-after holiday window.
    out['is_holiday_week'] = 0.0
    for year in range(out.index[0].year, out.index[-1].year + 1):
        for holiday in get_holiday_dates(year):
            # Holiday impact window includes 7 days before and after.
            holiday_week_start = holiday - pd.Timedelta(days=7)
            holiday_week_end = holiday + pd.Timedelta(days=7)
            mask = (out.index >= holiday_week_start) & (out.index <= holiday_week_end)
            out.loc[mask, 'is_holiday_week'] = 1.0
    
    # Entry on holiday-window plus negative momentum condition.
    out['entry_signal'] = ((out['is_holiday_week'] > 0.5) & (out['momentum'] < momentum_threshold)).astype(float)
    
    # Exit signal is time-based outside the entry condition.
    out['exit_signal'] = 0.0
    
    out = out[['open', 'high', 'low', 'close', 'volume', 'openinterest', 
               'momentum', 'is_holiday_week', 'entry_signal', 'exit_signal']].copy()
    return out.dropna()


class Mt5HolidayReversalFeed(bt.feeds.PandasData):
    """Backtrader feed for momentum and holiday-week reversal features."""
    lines = ('momentum', 'is_holiday_week', 'entry_signal', 'exit_signal',)
    params = (
        ('datetime', None), ('open', 0), ('high', 1), ('low', 2),
        ('close', 3), ('volume', 4), ('openinterest', 5),
        ('momentum', 6), ('is_holiday_week', 7), ('entry_signal', 8), ('exit_signal', 9),
    )


class HolidayReversalStrategy(bt.Strategy):
    """Holiday-window mean-reversion strategy with duration-based exits."""
    params = dict(
        lookback=20,
        momentum_threshold=0.0,
        holding_days=4,
        lot_size=1.0,
    )

    def __init__(self):
        """Initialize trade statistics and broker-value collection."""
        self.bar_num = 0
        self.buy_count = 0
        self.sell_count = 0
        self.trade_count = 0
        self.win_count = 0
        self.loss_count = 0
        self.pending_order = None
        self.entry_bar = 0
        self.broker_value_series = []


    def _get_position_size(self, target_notional_pct=1.0, price=None):
        """Compute position size from broker value with commodity contract multiplier."""
        if target_notional_pct <= 0:
            return 0.0
        broker_value = float(self.broker.getvalue())
        execution_price = float(self.data.close[0] if price is None else price)
        if broker_value <= 0 or execution_price <= 0:
            return 0.0
        comminfo = self.broker.getcommissioninfo(self.data)
        multiplier = float(getattr(comminfo.p, 'mult', 1.0) or 1.0)
        if multiplier <= 0:
            return 0.0
        size = broker_value * float(target_notional_pct) / (execution_price * multiplier)
        return max(0.01, round(size, 2))

    def next(self):
        """Run per-bar logic with fixed-horizon exit precedence."""
        self.bar_num += 1
        self.broker_value_series.append((bt.num2date(self.data.datetime[0]), float(self.broker.getvalue())))
        
        if self.pending_order is not None:
            return
        
        entry_signal = float(self.data.entry_signal[0]) > 0.5
        
        # Enter when flat and the entry signal is active.
        if not self.position:
            if entry_signal:
                self.buy_count += 1
                self.entry_bar = self.bar_num
                self.pending_order = self.buy(size=self._get_position_size(target_notional_pct=float(self.p.lot_size)))
            return
        
        # Exit after the configured holding period when in position.
        holding_days = self.bar_num - self.entry_bar
        if holding_days >= self.p.holding_days:
            self.sell_count += 1
            self.pending_order = self.close()

    def notify_order(self, order):
        """Clear pending order pointer when order reaches terminal status."""
        if order.status in (order.Submitted, order.Accepted):
            return
        self.pending_order = None

    def notify_trade(self, trade):
        """Accumulate win/loss metrics on closed trade."""
        if not trade.isclosed:
            return
        self.trade_count += 1
        if trade.pnlcomm >= 0:
            self.win_count += 1
        else:
            self.loss_count += 1


