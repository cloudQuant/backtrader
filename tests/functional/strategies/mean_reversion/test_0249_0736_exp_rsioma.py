class ExpRSIOMAStrategy(bt.Strategy):
    """Trade RSIOMA/MARSIOMA signals with fixed stop-loss and take-profit.

    Reads the precomputed RSIOMA and trigger lines from the H4 indicator feed,
    derives buy/sell open and close flags from the configured signal mode, and
    enters in the signal direction sized by ``lots`` while protecting positions
    with point-based stop-loss and take-profit levels on the M15 feed.
    """

    params = dict(
        stop_loss=1000,
        take_profit=2000,
        lots=0.1,
        buy_pos_open=True,
        sell_pos_open=True,
        buy_pos_close=True,
        sell_pos_close=True,
        mode='histdisposition',
        high_level=20.0,
        middle_level=0.0,
        low_level=-20.0,
        point=0.01,
        digits_adjust=10,
        price_digits=2,
    )

    def __init__(self):
        """Bind the base and indicator feeds and reset counters and risk state."""
        self.base = self.datas[0]
        self.ind = self.datas[1]

        self.bar_num = 0
        self.signal_count = 0
        self.buy_count = 0
        self.sell_count = 0
        self.trade_count = 0
        self.win_count = 0
        self.loss_count = 0
        self.completed_order_count = 0
        self.rejected_order_count = 0

        self.order = None
        self.stop_price = None
        self.take_profit_price = None
        self.last_signal_dt = None

    def _unit(self):
        return float(self.p.point) * float(self.p.digits_adjust)

    def _set_risk(self, side):
        unit = self._unit()
        price = float(self.base.close[0])
        if side == 'buy':
            self.stop_price = round(price - float(self.p.stop_loss) * unit, int(self.p.price_digits)) if self.p.stop_loss > 0 else None
            self.take_profit_price = round(price + float(self.p.take_profit) * unit, int(self.p.price_digits)) if self.p.take_profit > 0 else None
        else:
            self.stop_price = round(price + float(self.p.stop_loss) * unit, int(self.p.price_digits)) if self.p.stop_loss > 0 else None
            self.take_profit_price = round(price - float(self.p.take_profit) * unit, int(self.p.price_digits)) if self.p.take_profit > 0 else None

    def _mode_signal(self):
        mode = str(self.p.mode).lower()
        macd0 = float(self.ind.rsioma[-1])
        macd1 = float(self.ind.rsioma[0])
        signal0 = float(self.ind.trigger[-1])
        signal1 = float(self.ind.trigger[0])
        macd2 = float(self.ind.rsioma[-2]) if len(self.ind) > 2 else macd0
        signal2 = float(self.ind.trigger[-2]) if len(self.ind) > 2 else signal0
        buy_open = sell_open = buy_close = sell_close = False
        if mode == 'breakdown':
            if macd1 > float(self.p.high_level):
                if self.p.buy_pos_open and macd0 <= float(self.p.high_level):
                    buy_open = True
                if self.p.sell_pos_close:
                    sell_close = True
            if macd1 < float(self.p.low_level):
                if self.p.sell_pos_open and macd0 >= float(self.p.low_level):
                    sell_open = True
                if self.p.buy_pos_close:
                    buy_close = True
        elif mode == 'histtwist':
            if macd1 > macd2:
                if self.p.buy_pos_open and macd0 <= macd1:
                    buy_open = True
                if self.p.sell_pos_close:
                    sell_close = True
            if macd1 < macd2:
                if self.p.sell_pos_open and macd0 >= macd1:
                    sell_open = True
                if self.p.buy_pos_close:
                    buy_close = True
        elif mode == 'signaltwist':
            if signal1 > signal2:
                if self.p.buy_pos_open and signal0 <= signal1:
                    buy_open = True
                if self.p.sell_pos_close:
                    sell_close = True
            if signal1 < signal2:
                if self.p.sell_pos_open and signal0 >= signal1:
                    sell_open = True
                if self.p.buy_pos_close:
                    buy_close = True
        else:
            if macd1 > signal1:
                if self.p.buy_pos_open and macd0 <= signal0:
                    buy_open = True
                if self.p.sell_pos_close:
                    sell_close = True
            if macd1 < signal1:
                if self.p.sell_pos_open and macd0 >= signal0:
                    sell_open = True
                if self.p.buy_pos_close:
                    buy_close = True
        return buy_open, sell_open, buy_close, sell_close

    def _manage_position(self, buy_close, sell_close):
        if not self.position or self.order is not None:
            return False
        high = float(self.base.high[0])
        low = float(self.base.low[0])
        if self.position.size > 0:
            if buy_close and self.p.buy_pos_close:
                self.order = self.close()
                return True
            if self.stop_price is not None and low <= self.stop_price:
                self.order = self.close()
                return True
            if self.take_profit_price is not None and high >= self.take_profit_price:
                self.order = self.close()
                return True
        else:
            if sell_close and self.p.sell_pos_close:
                self.order = self.close()
                return True
            if self.stop_price is not None and high >= self.stop_price:
                self.order = self.close()
                return True
            if self.take_profit_price is not None and low <= self.take_profit_price:
                self.order = self.close()
                return True
        return False

    def next(self):
        """Evaluate the mode signal once per indicator bar and act on it.

        Skips while an order is pending or the indicator history is too short.
        Manages an open position first (signal exit or stop/take-profit), and
        otherwise opens a long or short sized by ``lots`` when a new buy/sell
        signal fires, recording the signal and setting risk levels.
        """
        self.bar_num += 1
        if len(self.ind) < 3:
            return
        if self.order is not None:
            return
        signal_dt = bt.num2date(self.ind.datetime[0])
        buy_open, sell_open, buy_close, sell_close = self._mode_signal()
        if self.position:
            self._manage_position(buy_close, sell_close)
            return
        if self.last_signal_dt == signal_dt:
            return
        if buy_open:
            self.signal_count += 1
            self._set_risk('buy')
            self.order = self.buy(size=self.p.lots)
            self.last_signal_dt = signal_dt
            return
        if sell_open:
            self.signal_count += 1
            self._set_risk('sell')
            self.order = self.sell(size=self.p.lots)
            self.last_signal_dt = signal_dt

    def notify_order(self, order):
        """Track completed/rejected orders, entry counts, and clear risk on close.

        Args:
            order: The order whose status changed; submitted/accepted states are
                ignored, completions update buy/sell or reset stop/take-profit,
                and failures increment the rejected count.
        """
        if order.status in [bt.Order.Submitted, bt.Order.Accepted]:
            return
        if order.status == bt.Order.Completed:
            self.completed_order_count += 1
            if self.position:
                if order.executed.size > 0:
                    self.buy_count += 1
                elif order.executed.size < 0:
                    self.sell_count += 1
            else:
                self.stop_price = None
                self.take_profit_price = None
        elif order.status in [bt.Order.Canceled, bt.Order.Margin, bt.Order.Rejected, bt.Order.Expired]:
            self.rejected_order_count += 1
        if self.order is not None and order.ref == self.order.ref and order.status not in [bt.Order.Submitted, bt.Order.Accepted]:
            self.order = None

    def notify_trade(self, trade):
        """Tally closed trades into win/loss buckets.

        Args:
            trade: The trade being notified; only closed trades update counts.
        """
        if not trade.isclosed:
            return
        self.trade_count += 1
        if trade.pnlcomm >= 0:
            self.win_count += 1
        else:
            self.loss_count += 1


