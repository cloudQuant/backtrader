class ConnorsRSIMeanReversionStrategy(bt.Strategy):
    """Mean-reversion strategy entering on ConnorsRSI oversold setups and exiting when RSI(2) recovers."""
    params = dict(
        high_lookback_weeks=26,
        recent_high_max_days=30,
        crsi_entry=20.0,
        entry_discount_pct=0.5,
        rsi_exit=55.0,
        trend_ma_days=200,
        rsi_period=3,
        streak_period=2,
        rank_period=100,
        lot_size=1.0,
    )

    def __init__(self):
        """Initialise counters, state variables, and order tracker for ConnorsRSI mean-reversion strategy."""
        self.bar_num = 0
        self.setup_count = 0
        self.buy_count = 0
        self.sell_count = 0
        self.trade_count = 0
        self.win_count = 0
        self.loss_count = 0
        self.expired_count = 0
        self.cancelled_count = 0
        self.rejected_count = 0
        self.order = None
        self.broker_value_series = []


    def _get_position_size(self, target_notional_pct=1.0, price=None):
        if target_notional_pct <= 0:
            return 0.0
        broker_value = float(self.broker.getvalue())
        execution_price = float(self.data.close[0] if price is None else price)
        if broker_value <= 0 or execution_price <= 0:
            return 0.0
        comminfo = self.broker.getcommissioninfo(self.data)
        multiplier = float(getattr(comminfo.p, 'mult', 1.0) or 1.0)
        if multiplier <= 0:
            return 0.0
        size = broker_value * float(target_notional_pct) / (execution_price * multiplier)
        return max(0.01, round(size, 2))

    def _entry_signal_ready(self):
        if len(self.data) < 1:
            return False
        return float(self.data.setup_signal[0]) > 0.5

    def next(self):
        """Evaluate entry/exit signals each bar: enter on ConnorsRSI setup, exit when RSI(2) recovers above threshold."""
        self.bar_num += 1
        self.broker_value_series.append((bt.num2date(self.data.datetime[0]), float(self.broker.getvalue())))
        if self.order is not None:
            return
        if self.position:
            if float(self.data.rsi2[0]) >= float(self.p.rsi_exit):
                self.sell_count += 1
                self.order = self.close()
            return
        if not self._entry_signal_ready():
            return
        self.setup_count += 1
        limit_price = float(self.data.close[-1]) * (1.0 - float(self.p.entry_discount_pct) / 100.0)
        current_dt = bt.num2date(self.data.datetime[0])
        valid_until = current_dt + timedelta(days=1)
        self.order = self.buy(size=self._get_position_size(target_notional_pct=float(self.p.lot_size), price=limit_price), exectype=bt.Order.Limit, price=limit_price, valid=valid_until)

    def notify_order(self, order):
        """Track order lifecycle: count completed buys, expired, cancelled, and rejected orders; clear reference on final status."""
        if order.status in (order.Submitted, order.Accepted):
            return
        if order.status == order.Completed:
            if order.isbuy():
                self.buy_count += 1
        elif order.status == order.Expired:
            self.expired_count += 1
        elif order.status == order.Canceled:
            self.cancelled_count += 1
        elif order.status in (order.Margin, order.Rejected):
            self.rejected_count += 1
        # Clear order reference regardless of final status
        self.order = None

    def notify_trade(self, trade):
        """Track trade outcome: increment win/loss count on closed trades."""
        if not trade.isclosed:
            return
        self.trade_count += 1
        if trade.pnlcomm >= 0:
            self.win_count += 1
        else:
            self.loss_count += 1


