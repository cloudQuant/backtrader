class DynamicMomentumContrarianStrategy(bt.Strategy):
    """Dynamic regime-switching mean-reversion strategy with target rebalance."""
    params = dict(
        momentum_lookback_days=252,
        crash_window_days=21,
        crash_threshold=-0.15,
        contrarian_duration_days=63,
        target_percent=0.95,
    )

    def __init__(self):
        """Initialize state counters and pending-order tracking."""
        self.bar_num = 0
        self.rebalance_count = 0
        self.buy_count = 0
        self.sell_count = 0
        self.trade_count = 0
        self.win_count = 0
        self.loss_count = 0
        self.momentum_mode_days = 0
        self.contrarian_mode_days = 0
        self.flat_days = 0
        self.switch_count = 0
        self.pending_order = None
        self.last_target_pct = None
        self.broker_value_series = []


    def _get_position_size(self, target_notional_pct=1.0, price=None):
        """Calculate size for target notional percentage orders.

        Args:
            target_notional_pct: Fraction of account equity to allocate.
            price: Optional execution price override.

        Returns:
            Rounded position size, bounded to a minimum.
        """
        if target_notional_pct <= 0:
            return 0.0
        broker_value = float(self.broker.getvalue())
        execution_price = float(self.data.close[0] if price is None else price)
        if broker_value <= 0 or execution_price <= 0:
            return 0.0
        comminfo = self.broker.getcommissioninfo(self.data)
        multiplier = float(getattr(comminfo.p, 'mult', 1.0) or 1.0)
        if multiplier <= 0:
            return 0.0
        size = broker_value * float(target_notional_pct) / (execution_price * multiplier)
        return max(0.01, round(size, 2))


    def _current_position_pct(self):
        """Return current position exposure as account percentage."""
        broker_value = float(self.broker.getvalue())
        if broker_value <= 0:
            return 0.0
        price = float(self.data.close[0])
        if price <= 0:
            return 0.0
        comminfo = self.broker.getcommissioninfo(self.data)
        multiplier = float(getattr(comminfo.p, 'mult', 1.0) or 1.0)
        if multiplier <= 0:
            return 0.0
        return float(self.position.size) * price * multiplier / broker_value


    def _order_target_notional_pct(self, target_pct):
        """Submit order to reach target notional percentage."""
        target_size = self._get_position_size(target_notional_pct=target_pct)
        return self.order_target_size(target=target_size)

    def next(self):
        """Advance one bar and rebalance when target regime changes."""
        self.bar_num += 1
        self.broker_value_series.append((bt.num2date(self.data.datetime[0]), float(self.broker.getvalue())))
        regime = int(round(float(self.data.regime[0])))
        if regime == 1:
            self.contrarian_mode_days += 1
        elif regime == 2:
            self.momentum_mode_days += 1
        else:
            self.flat_days += 1
        target_pct = float(self.data.target_pct[0])
        if self.pending_order is not None:
            return
        if self.last_target_pct is not None and abs(target_pct - self.last_target_pct) > 1e-9:
            self.switch_count += 1
        self.last_target_pct = target_pct
        current_pct = self._current_position_pct()
        if abs(current_pct - target_pct) < 0.02:
            return
        self.rebalance_count += 1
        self.pending_order = self._order_target_notional_pct(target_pct)
        if target_pct > current_pct:
            self.buy_count += 1
        elif target_pct < current_pct:
            self.sell_count += 1

    def notify_order(self, order):
        """Clear pending-order marker after terminal status updates."""
        if order.status in (order.Submitted, order.Accepted):
            return
        # Clear the pending-order reference regardless of final status.
        self.pending_order = None

    def notify_trade(self, trade):
        """Update trade counters after each closed trade."""
        if not trade.isclosed:
            return
        self.trade_count += 1
        if trade.pnlcomm >= 0:
            self.win_count += 1
        else:
            self.loss_count += 1


