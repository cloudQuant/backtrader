def load_mt5_csv(filepath, fromdate=None, todate=None, bar_shift_minutes=0):
    """Load and normalize MT5 CSV bars into a DataFrame.

    Args:
        filepath: Source path.
        fromdate: Optional start datetime.
        todate: Optional end datetime.
        bar_shift_minutes: Optional timestamp shift in minutes.

    Returns:
        DataFrame indexed by datetime with OHLCV columns.
    """
    with open(filepath, 'r', encoding='utf-8') as f:
        lines = f.read().strip().split('\n')
    cleaned = '\n'.join(line.strip().strip('"') for line in lines if line.strip())
    df = pd.read_csv(io.StringIO(cleaned), sep='\t')
    df['datetime'] = pd.to_datetime(df['<DATE>'] + ' ' + df['<TIME>'], format='%Y.%m.%d %H:%M:%S')
    df = df.rename(columns={
        '<OPEN>': 'open',
        '<HIGH>': 'high',
        '<LOW>': 'low',
        '<CLOSE>': 'close',
        '<TICKVOL>': 'volume',
        '<VOL>': 'openinterest',
        '<SPREAD>': 'spread',
    })
    df = df[['datetime', 'open', 'high', 'low', 'close', 'volume', 'openinterest', 'spread']]
    df = df.set_index('datetime').sort_index()
    if bar_shift_minutes:
        df.index = df.index + pd.Timedelta(minutes=bar_shift_minutes)
    if fromdate is not None:
        df = df[df.index >= fromdate]
    if todate is not None:
        df = df[df.index <= todate]
    return df


def resample_ohlcv(df, minutes):
    """Resample OHLCV series to a coarser period.

    Args:
        df: Original dataframe.
        minutes: Target resample size in minutes.

    Returns:
        Aggregated dataframe with canonical OHLCV fields.
    """
    rule = f'{int(minutes)}min'
    out = df.resample(rule, label='right', closed='right').agg({
        'open': 'first',
        'high': 'max',
        'low': 'min',
        'close': 'last',
        'volume': 'sum',
        'openinterest': 'last',
        'spread': 'last',
    })
    out = out.dropna(subset=['open', 'high', 'low', 'close']).copy()
    out['openinterest'] = out['openinterest'].fillna(0)
    out['spread'] = out['spread'].fillna(0)
    return out


def build_pm(ftma, rank1, rank2, rank3, shift1, shift2, shift3):
    """Build fine-tuning moving-average weights.

    Args:
        ftma: Window length.
        rank1: Exponent for first weighting phase.
        rank2: Exponent for second weighting phase.
        rank3: Exponent for symmetric edge weighting.
        shift1: Initial weight bias.
        shift2: Secondary weight bias.
        shift3: Edge weight bias.

    Returns:
        Normalized weight list.
    """
    pm = []
    total = 0.0
    for h in range(int(ftma)):
        value = shift1 + math.pow(h / (ftma - 1.0), rank1) * (1.0 - shift1)
        value = (shift2 + math.pow(1.0 - (h / (ftma - 1.0)), rank2) * (1.0 - shift2)) * value
        if (h / (ftma - 1.0)) < 0.5:
            value = (shift3 + math.pow(1.0 - (h / (ftma - 1.0)) * 2.0, rank3) * (1.0 - shift3)) * value
        else:
            value = (shift3 + math.pow((h / (ftma - 1.0)) * 2.0 - 1.0, rank3) * (1.0 - shift3)) * value
        pm.append(value)
        total += value
    return [value / total for value in pm]


def weighted_series(values, weights):
    """Calculate a custom weighted moving value for each index.

    Args:
        values: Numeric sequence.
        weights: Weight sequence aligned from current to history.

    Returns:
        Weighted series list with leading NaNs.
    """
    window = len(weights)
    out = [math.nan] * len(values)
    for idx in range(window - 1, len(values)):
        acc = 0.0
        for offset, weight in enumerate(weights):
            acc += weight * values[idx - offset]
        out[idx] = acc
    return out


def build_signal_frame(df, execution_minutes, ftma, rank1, rank2, rank3, shift1, shift2, shift3, gap_points, signal_bar):
    """Build feature frame for fine tuning candle signals.

    Args:
        df: Raw minute bars.
        execution_minutes: Signal timeframe in minutes.
        ftma: Fine tuning MA window.
        rank1: Rank exponent 1.
        rank2: Rank exponent 2.
        rank3: Rank exponent 3.
        shift1: Bias shift 1.
        shift2: Bias shift 2.
        shift3: Bias shift 3.
        gap_points: Maximum gap to reuse previous close.
        signal_bar: Number of bars used for transition detection.

    Returns:
        DataFrame with OHLCV and Boolean signal columns.
    """
    frame = resample_ohlcv(df, execution_minutes)
    weights = build_pm(ftma, rank1, rank2, rank3, shift1, shift2, shift3)
    wopen = weighted_series(frame['open'].tolist(), weights)
    whigh_raw = weighted_series(frame['high'].tolist(), weights)
    wlow_raw = weighted_series(frame['low'].tolist(), weights)
    wclose = weighted_series(frame['close'].tolist(), weights)
    gap = float(gap_points)
    o_buf, h_buf, l_buf, c_buf, color = [], [], [], [], []
    prev_close = math.nan
    for idx in range(len(frame)):
        o = wopen[idx]
        h = whigh_raw[idx]
        l = wlow_raw[idx]
        c = wclose[idx]
        if math.isnan(o) or math.isnan(h) or math.isnan(l) or math.isnan(c):
            o_buf.append(math.nan)
            h_buf.append(math.nan)
            l_buf.append(math.nan)
            c_buf.append(math.nan)
            color.append(math.nan)
            continue
        max_v = max(o, c, h, l)
        min_v = min(o, c, h, l)
        if not math.isnan(prev_close) and abs(frame['open'].iloc[idx] - frame['close'].iloc[idx]) <= gap:
            o = prev_close
        o_buf.append(o)
        h_buf.append(max_v)
        l_buf.append(min_v)
        c_buf.append(c)
        prev_close = c
        if o < c:
            color.append(2.0)
        elif o > c:
            color.append(0.0)
        else:
            color.append(1.0)
    frame['ft_open'] = o_buf
    frame['ft_high'] = h_buf
    frame['ft_low'] = l_buf
    frame['ft_close'] = c_buf
    frame['color'] = color
    frame = frame.dropna(subset=['ft_open', 'ft_close', 'color']).copy()
    colors = frame['color'].tolist()
    buy_open = [False] * len(frame)
    sell_open = [False] * len(frame)
    buy_close = [False] * len(frame)
    sell_close = [False] * len(frame)
    sb = int(signal_bar)
    for idx in range(sb + 1, len(frame)):
        older = colors[idx - (sb + 1)]
        newer = colors[idx - sb]
        if older == 2.0 and newer < 2.0:
            buy_open[idx] = True
            sell_close[idx] = True
        if older == 0.0 and newer > 0.0:
            sell_open[idx] = True
            buy_close[idx] = True
    frame['buy_open'] = buy_open
    frame['sell_open'] = sell_open
    frame['buy_close'] = buy_close
    frame['sell_close'] = sell_close
    return frame[['open', 'high', 'low', 'close', 'volume', 'openinterest', 'spread', 'buy_open', 'sell_open', 'buy_close', 'sell_close']]


