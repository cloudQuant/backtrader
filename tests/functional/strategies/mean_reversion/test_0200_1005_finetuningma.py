def _price_value(data, shift, mode):
    key = str(mode).lower()
    open_ = float(data.open[-shift]) if shift else float(data.open[0])
    high = float(data.high[-shift]) if shift else float(data.high[0])
    low = float(data.low[-shift]) if shift else float(data.low[0])
    close = float(data.close[-shift]) if shift else float(data.close[0])
    if key in ('close', '1', 'price_close'):
        return close
    if key in ('open', '2', 'price_open'):
        return open_
    if key in ('high', '3', 'price_high'):
        return high
    if key in ('low', '4', 'price_low'):
        return low
    if key in ('median', '5', 'price_median'):
        return (high + low) / 2.0
    if key in ('typical', '6', 'price_typical'):
        return (close + high + low) / 3.0
    if key in ('weighted', '7', 'price_weighted'):
        return (2.0 * close + high + low) / 4.0
    if key in ('simple', '8', 'price_simpl'):
        return (open_ + close) / 2.0
    if key in ('quarter', '9', 'price_quarter'):
        return (open_ + close + high + low) / 4.0
    if key in ('trendfollow0', '10', 'price_trendfollow0'):
        if close > open_:
            return high
        if close < open_:
            return low
        return close
    if key in ('trendfollow1', '11', 'price_trendfollow1'):
        if close > open_:
            return (high + close) / 2.0
        if close < open_:
            return (low + close) / 2.0
        return close
    return close


class FineTuningMA(Indicator):
    """Weighted moving average with rank/shift-shaped per-bar weights."""

    lines = ('value',)
    params = dict(ftma=10, rank1=2.0, rank2=2.0, rank3=2.0, shift1=1.0, shift2=1.0, shift3=1.0, ipc='typical')

    def __init__(self):
        """Precompute the normalized FineTuningMA weights and warmup period."""
        self.ftma = max(int(self.p.ftma), 1)
        self.weights = []
        total = 0.0
        for h in range(self.ftma):
            denom = max(self.ftma - 1.0, 1.0)
            part = float(h) / denom
            w = self.p.shift1 + math.pow(part, float(self.p.rank1)) * (1.0 - self.p.shift1)
            w = (self.p.shift2 + math.pow(1.0 - part, float(self.p.rank2)) * (1.0 - self.p.shift2)) * w
            w = (self.p.shift3 + math.pow(1.0 - abs(part - 0.5) * 2.0, float(self.p.rank3)) * (1.0 - self.p.shift3)) * w
            self.weights.append(w)
            total += w
        self.weights = [w / total for w in self.weights] if total else [1.0 / self.ftma] * self.ftma
        self.addminperiod(self.ftma + 2)

    def next(self):
        """Output the weighted average of the applied price over the window."""
        if len(self.data) < self.ftma:
            self.l.value[0] = _price_value(self.data, 0, self.p.ipc)
            return
        total = 0.0
        for h in range(self.ftma):
            total += self.weights[h] * _price_value(self.data, h, self.p.ipc)
        self.l.value[0] = total


class FineTuningMAStrategy(Strategy):
    """Trade FineTuningMA slope reversals with fixed stop-loss and take-profit."""

    params = dict(
        signal_bar=1,
        ftma=10,
        rank1=2.0,
        rank2=2.0,
        rank3=2.0,
        shift1=1.0,
        shift2=1.0,
        shift3=1.0,
        ipc='typical',
        stop_loss_points=1000,
        take_profit_points=2000,
        lot=0.1,
        point=0.01,
        buy_pos_open=True,
        sell_pos_open=True,
        buy_pos_close=True,
        sell_pos_close=True,
    )

    def __init__(self):
        """Build the FineTuningMA indicator and zero counters and risk state."""
        self.indicator = FineTuningMA(
            self.data,
            ftma=self.p.ftma,
            rank1=self.p.rank1,
            rank2=self.p.rank2,
            rank3=self.p.rank3,
            shift1=self.p.shift1,
            shift2=self.p.shift2,
            shift3=self.p.shift3,
            ipc=self.p.ipc,
        )
        self.bar_num = 0
        self.buy_signal_count = 0
        self.sell_signal_count = 0
        self.buy_count = 0
        self.sell_count = 0
        self.trade_count = 0
        self.win_count = 0
        self.loss_count = 0
        self.completed_order_count = 0
        self.rejected_order_count = 0
        self.entry_price = None
        self.stop_price = None
        self.target_price = None
        self.pending_entry_direction = 0
        self.warmup = max(int(self.p.ftma) + max(int(self.p.signal_bar), 1) + 5, 30)

    def log(self, text):
        """Print ``text`` prefixed with the current bar's ISO timestamp."""
        dt = num2date(self.data.datetime[0])
        print(f'{dt.isoformat()}, {text}')

    def _get_signals(self):
        shift = max(int(self.p.signal_bar), 1)
        v0 = float(self.indicator.value[-shift])
        v1 = float(self.indicator.value[-(shift + 1)])
        v2 = float(self.indicator.value[-(shift + 2)])
        buy_open = self.p.buy_pos_open and v1 < v2 and v0 > v1
        sell_open = self.p.sell_pos_open and v1 > v2 and v0 < v1
        buy_close = self.p.buy_pos_close and sell_open
        sell_close = self.p.sell_pos_close and buy_open
        return buy_open, sell_open, buy_close, sell_close

    def _reset_levels(self):
        self.entry_price = None
        self.stop_price = None
        self.target_price = None

    def _open_long(self):
        self.pending_entry_direction = 1
        self.buy(size=self.p.lot)

    def _open_short(self):
        self.pending_entry_direction = -1
        self.sell(size=self.p.lot)

    def _close_long(self, reason):
        self.log(reason)
        self.close()
        self._reset_levels()

    def _close_short(self, reason):
        self.log(reason)
        self.close()
        self._reset_levels()

    def _manage_protective_levels(self):
        if not self.position or self.entry_price is None:
            return False
        low = float(self.data.low[0])
        high = float(self.data.high[0])
        if self.position.size > 0:
            if self.stop_price is not None and low <= self.stop_price:
                self._close_long(f'close long stop={self.stop_price:.2f}')
                return True
            if self.target_price is not None and high >= self.target_price:
                self._close_long(f'close long target={self.target_price:.2f}')
                return True
        else:
            if self.stop_price is not None and high >= self.stop_price:
                self._close_short(f'close short stop={self.stop_price:.2f}')
                return True
            if self.target_price is not None and low <= self.target_price:
                self._close_short(f'close short target={self.target_price:.2f}')
                return True
        return False

    def next(self):
        """Manage protective exits, then open or reverse on MA slope reversals."""
        self.bar_num += 1
        if len(self.data) < self.warmup:
            return
        if self._manage_protective_levels():
            return

        buy_open, sell_open, buy_close, sell_close = self._get_signals()
        if buy_open:
            self.buy_signal_count += 1
        if sell_open:
            self.sell_signal_count += 1

        if self.position:
            if self.position.size > 0:
                if buy_close:
                    self._close_long('close long on downward reversal')
                    if sell_open:
                        self._open_short()
                    return
            else:
                if sell_close:
                    self._close_short('close short on upward reversal')
                    if buy_open:
                        self._open_long()
                    return
        else:
            if buy_open:
                self.log('buy on FineTuningMA reversal')
                self._open_long()
                return
            if sell_open:
                self.log('sell on FineTuningMA reversal')
                self._open_short()
                return

    def notify_order(self, order):
        """Record fills, set stop/target prices, and count rejected orders."""
        if order.status in [order.Canceled, order.Margin, order.Rejected]:
            self.rejected_order_count += 1
            self.pending_entry_direction = 0
            self.log(f'order {order.getstatusname()}')
            return
        if order.status != order.Completed:
            return
        self.completed_order_count += 1
        if self.pending_entry_direction == 1 and order.isbuy():
            self.buy_count += 1
            self.entry_price = order.executed.price
            self.stop_price = self.entry_price - self.p.stop_loss_points * self.p.point if self.p.stop_loss_points > 0 else None
            self.target_price = self.entry_price + self.p.take_profit_points * self.p.point if self.p.take_profit_points > 0 else None
            self.pending_entry_direction = 0
            return
        if self.pending_entry_direction == -1 and order.issell():
            self.sell_count += 1
            self.entry_price = order.executed.price
            self.stop_price = self.entry_price + self.p.stop_loss_points * self.p.point if self.p.stop_loss_points > 0 else None
            self.target_price = self.entry_price - self.p.take_profit_points * self.p.point if self.p.take_profit_points > 0 else None
            self.pending_entry_direction = 0
            return
        if not self.position:
            self._reset_levels()

    def notify_trade(self, trade):
        """Count each closed trade as a win or loss by realized PnL."""
        if not trade.isclosed:
            return
        self.trade_count += 1
        if trade.pnlcomm >= 0:
            self.win_count += 1
        else:
            self.loss_count += 1
        self.log(f'trade closed pnl={trade.pnlcomm:.2f}')



