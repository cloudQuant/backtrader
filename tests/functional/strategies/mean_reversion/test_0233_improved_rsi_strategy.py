def finite_or_none(value):
    """Return ``value`` when it is a finite number, otherwise None.

    Args:
        value: A numeric value or None.

    Returns:
        The value if it is not None and finite, else None.
    """
    return value if value is not None and math.isfinite(value) else None


def calculate_ulcer_index(values):
    """Compute the Ulcer Index from an equity series.

    Args:
        values: Sequence of portfolio values in chronological order.

    Returns:
        The Ulcer Index (root-mean-square percentage drawdown from running
        peaks) as a float, or 0.0 when fewer than two values are supplied.
    """
    if len(values) < 2:
        return 0.0
    peak = values[0]
    squared = 0.0
    for value in values:
        peak = max(peak, value)
        drawdown = (peak - value) / peak * 100.0 if peak > 0 else 0.0
        squared += drawdown ** 2
    return math.sqrt(squared / len(values))



