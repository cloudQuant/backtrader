def resample_to_h8(df):
    """Resample an M15 OHLCV frame to the H8 (8-hour) signal timeframe.

    Args:
        df: Source M15 OHLCV DataFrame indexed by datetime.

    Returns:
        An H8 DataFrame with open/high/low/close/volume/tick_volume aggregated
        and rows lacking price data dropped.
    """
    agg = {
        'open': 'first',
        'high': 'max',
        'low': 'min',
        'close': 'last',
        'volume': 'sum',
        'tick_volume': 'sum',
        'openinterest': 'sum',
    }
    h8 = df.resample('8h', label='right', closed='right').agg(agg)
    return h8.dropna(subset=['open', 'high', 'low', 'close'])


def ema(series, period):
    """Compute an exponential moving average over a price series.

    Args:
        series: Input numeric series.
        period: EMA span in bars.

    Returns:
        A pandas Series of EMA values with no warm-up adjustment.
    """
    return series.ewm(span=period, adjust=False).mean()


def recursive_histogram(frame, period=13, gamma=0.6, ma_length=12, volume_type='tick'):
    """Compute the XBullsBearsEyes volume-weighted oscillator histogram.

    Measures bull/bear power against a price EMA, runs the summed power through a
    four-stage Laguerre recursive filter, re-centers the result around zero,
    weights it by volume, and smooths the series into a histogram.

    Args:
        frame: H8 OHLCV DataFrame with a ``tick_volume`` column.
        period: EMA period for the price reference line.
        gamma: Laguerre filter smoothing factor in [0, 1).
        ma_length: Rolling window used to smooth the histogram and volume.
        volume_type: ``'tick'`` to weight by tick volume, otherwise real volume.

    Returns:
        A tuple ``(histogram, smoothed_volume)`` of aligned pandas Series.
    """
    price_ema = ema(frame['close'], period)
    bears = frame['low'] - price_ema
    bulls = frame['high'] - price_ema
    volume = frame['tick_volume'] if str(volume_type).lower() == 'tick' else frame['volume']

    l0 = l1 = l2 = l3 = 0.0
    raw_values = []
    for idx in range(len(frame)):
        total_power = float(bears.iloc[idx] + bulls.iloc[idx])
        l0_prev, l1_prev, l2_prev, l3_prev = l0, l1, l2, l3
        l0 = (1.0 - gamma) * total_power + gamma * l0_prev
        l1 = -gamma * l0 + l0_prev + gamma * l1_prev
        l2 = -gamma * l1 + l1_prev + gamma * l2_prev
        l3 = -gamma * l2 + l2_prev + gamma * l3_prev
        cu = 0.0
        cd = 0.0
        if l0 >= l1:
            cu = l0 - l1
        else:
            cd = l1 - l0
        if l1 >= l2:
            cu += l1 - l2
        else:
            cd += l2 - l1
        if l2 >= l3:
            cu += l2 - l3
        else:
            cd += l3 - l2
        result = cu / (cu + cd) if (cu + cd) != 0 else 0.0
        raw_values.append((result * 100.0 - 50.0) * float(volume.iloc[idx]))

    histogram = pd.Series(raw_values, index=frame.index).rolling(ma_length).mean()
    smoothed_volume = volume.rolling(ma_length).mean()
    return histogram, smoothed_volume


def assign_zone_color(histogram, smoothed_volume, high_level_2, high_level_1, low_level_1, low_level_2):
    """Map each histogram bar to a discrete zone color.

    Compares the histogram against volume-scaled threshold bands to classify each
    bar as strong-up (0), up (1), neutral (2), down (3), or strong-down (4).

    Args:
        histogram: Smoothed oscillator histogram series.
        smoothed_volume: Smoothed volume series used to scale the bands.
        high_level_2: Multiplier for the strong-up band.
        high_level_1: Multiplier for the up band.
        low_level_1: Multiplier for the down band.
        low_level_2: Multiplier for the strong-down band.

    Returns:
        A pandas Series of zone-color codes (0-4), with NA where inputs are NaN.
    """
    colors = pd.Series(index=histogram.index, dtype='float64')
    max_level = high_level_2 * smoothed_volume
    up_level = high_level_1 * smoothed_volume
    down_level = low_level_1 * smoothed_volume
    min_level = low_level_2 * smoothed_volume

    for idx in range(len(histogram)):
        value = histogram.iat[idx]
        if pd.isna(value) or pd.isna(smoothed_volume.iat[idx]):
            colors.iat[idx] = pd.NA
            continue
        color = 2.0
        if value > max_level.iat[idx]:
            color = 0.0
        elif value > up_level.iat[idx]:
            color = 1.0
        elif value < min_level.iat[idx]:
            color = 4.0
        elif value < down_level.iat[idx]:
            color = 3.0
        colors.iat[idx] = color
    return colors


def build_signal_frame(
    filepath,
    fromdate=None,
    todate=None,
    bar_shift_minutes=15,
    period=13,
    gamma=0.6,
    high_level_2=25,
    high_level_1=10,
    low_level_1=-10,
    low_level_2=-25,
    ma_length=12,
    signal_bar=1,
    volume_type='tick',
):
    """Build the H8 signal frame with histogram, zones, and trade signals.

    Loads the base M15 data, resamples to H8, computes the volume-weighted
    histogram and zone colors, then derives shifted buy/sell/close signals.

    Args:
        filepath: Path to the MT5 CSV export file.
        fromdate: Optional inclusive lower bound on the datetime index.
        todate: Optional inclusive upper bound on the datetime index.
        bar_shift_minutes: Minutes to add to each base timestamp.
        period: EMA period for the histogram price reference.
        gamma: Laguerre filter smoothing factor.
        high_level_2: Multiplier for the strong-up band.
        high_level_1: Multiplier for the up band.
        low_level_1: Multiplier for the down band.
        low_level_2: Multiplier for the strong-down band.
        ma_length: Rolling window for histogram and volume smoothing.
        signal_bar: Number of bars to shift zone colors when deriving signals.
        volume_type: ``'tick'`` to weight by tick volume, otherwise real volume.

    Returns:
        An H8 DataFrame with histogram, smoothed_volume, zone_color, and the
        derived buy/sell/close signal columns, with warm-up rows dropped.
    """
    base = load_mt5_csv(filepath, fromdate=fromdate, todate=todate, bar_shift_minutes=bar_shift_minutes)
    frame = resample_to_h8(base)
    histogram, smoothed_volume = recursive_histogram(frame, period=period, gamma=gamma, ma_length=ma_length, volume_type=volume_type)
    frame['histogram'] = histogram
    frame['smoothed_volume'] = smoothed_volume
    frame['zone_color'] = assign_zone_color(histogram, smoothed_volume, high_level_2, high_level_1, low_level_1, low_level_2)

    current_color = frame['zone_color'].shift(signal_bar)
    previous_color = frame['zone_color'].shift(signal_bar + 1)

    frame['buy_signal_normal'] = (previous_color == 1.0) & (current_color > 1.0)
    frame['buy_signal_strong'] = (previous_color == 0.0) & (current_color > 0.0)
    frame['sell_signal_normal'] = (previous_color == 3.0) & (current_color < 3.0)
    frame['sell_signal_strong'] = (previous_color == 4.0) & (current_color < 4.0)
    frame['close_buy_signal'] = frame['sell_signal_normal'] | frame['sell_signal_strong']
    frame['close_sell_signal'] = frame['buy_signal_normal'] | frame['buy_signal_strong']
    return frame.dropna(subset=['histogram', 'smoothed_volume', 'zone_color'])


