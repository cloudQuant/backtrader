def load_backtest_frame(config):
    """Build the signal frame and return backtest frame metadata.

    Args:
        config: Resolved strategy configuration.

    Returns:
        Dict containing prepared frame data plus date boundaries.
    """
    data_cfg = config['data']
    params = config['params']
    fromdate = datetime.datetime.fromisoformat(data_cfg['fromdate'])
    todate = datetime.datetime.fromisoformat(data_cfg['todate'])
    frame = build_signal_frame(
        resolve_data_path(data_cfg['file']),
        fromdate=fromdate,
        todate=todate,
        bar_shift_minutes=data_cfg.get('bar_shift_minutes', 15),
        period=params['period'],
        gamma=params['gamma'],
        high_level_2=params['high_level_2'],
        high_level_1=params['high_level_1'],
        low_level_1=params['low_level_1'],
        low_level_2=params['low_level_2'],
        ma_length=params['ma_length'],
        signal_bar=params['signal_bar'],
        volume_type=params['volume_type'],
    )
    if frame.empty:
        raise ValueError('Loaded signal frame is empty')
    print(f"Loaded {len(frame)} H8 bars: {frame.index[0]} -> {frame.index[-1]}")
    return {'data': frame, 'fromdate': fromdate, 'todate': todate}


def build_cerebro(config, frame):
    """Construct a cerebro engine with feed, strategy, and analyzers.

    Args:
        config: Strategy and backtest configuration.
        frame: Backtest payload from :func:`load_backtest_frame`.

    Returns:
        Configured ``bt.Cerebro``.
    """
    bt_cfg = config['backtest']
    cerebro = bt.Cerebro(stdstats=True, cheat_on_open=True)
    cerebro.broker.setcash(bt_cfg['initial_cash'])
    comm_type = bt.CommInfoBase.COMM_FIXED if bt_cfg.get('commission_type', 'fixed') == 'fixed' else bt.CommInfoBase.COMM_PERC
    cerebro.broker.setcommission(
        commission=bt_cfg['commission'],
        margin=bt_cfg['margin'],
        mult=bt_cfg['multiplier'],
        commtype=comm_type,
        stocklike=bt_cfg.get('stocklike', False),
    )
    feed = XBullsBearsEyesVolFeed(dataname=frame['data'], timeframe=bt.TimeFrame.Minutes, compression=480)
    cerebro.adddata(feed, name=f"{config['data']['symbol']}_{config['data']['timeframe']}")
    cerebro.addstrategy(
        XBullsBearsEyesVolStrategy,
        mm1=config['params']['mm1'],
        mm2=config['params']['mm2'],
        lot_or_risk=config['params']['lot_or_risk'],
        stop_loss_points=config['params']['stop_loss_points'],
        take_profit_points=config['params']['take_profit_points'],
        point=config['params']['point'],
        margin_per_lot=config['backtest']['margin'],
        lot_step=config['params']['lot_step'],
        lot_min=config['params']['lot_min'],
        lot_max=config['params']['lot_max'],
    )
    cerebro.addanalyzer(bt.analyzers.SharpeRatio, _name='sharpe', timeframe=bt.TimeFrame.Minutes, factor=MINUTES_PER_TRADING_YEAR, annualize=True, riskfreerate=0)
    cerebro.addanalyzer(bt.analyzers.Returns, _name='returns', timeframe=bt.TimeFrame.Minutes, compression=480, tann=MINUTES_PER_TRADING_YEAR)
    cerebro.addanalyzer(bt.analyzers.DrawDown, _name='drawdown')
    cerebro.addanalyzer(bt.analyzers.TradeAnalyzer, _name='trades')
    cerebro.addanalyzer(bt.analyzers.SQN, _name='sqn')
    return cerebro


def extract_metrics(strat, cerebro, frame, config):
    """Extract strategy/analyzer statistics used by regression assertions.

    Args:
        strat: Strategy instance after run.
        cerebro: Running cerebro engine.
        frame: Backtest frame metadata.
        config: Test configuration.

    Returns:
        Dictionary of computed and analyzer metrics.
    """
    sharpe = strat.analyzers.sharpe.get_analysis()
    returns = strat.analyzers.returns.get_analysis()
