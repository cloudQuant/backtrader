def resample_frame(df, rule):
    """Resample bars using a pandas offset rule.

    Args:
        df: Source OHLCV DataFrame indexed by datetime.
        rule: Offset alias, e.g. ``'240min'``.

    Returns:
        Aggregated and cleaned OHLCV DataFrame.
    """
    out = df.resample(rule, label='right', closed='right').agg({
        'open': 'first',
        'high': 'max',
        'low': 'min',
        'close': 'last',
        'volume': 'sum',
        'openinterest': 'last',
    })
    out = out.dropna(subset=['open', 'high', 'low', 'close'])
    out['openinterest'] = out['openinterest'].fillna(0)
    return out


def rolling_lwma(series, period):
    """Compute linear-weighted moving average over ``series``."""
    period = int(period)
    weights = np.arange(1, period + 1, dtype='float64')
    weight_sum = float(weights.sum())
    return series.rolling(period, min_periods=period).apply(lambda values: float(np.dot(values, weights)) / weight_sum, raw=True)


def apply_ma(series, period, method):
    """Apply selected MA kernel by name."""
    period = int(period)
    name = str(method).lower()
    if name in ('mode_sma', 'sma', '0'):
        return series.rolling(period, min_periods=period).mean()
    if name in ('mode_ema', 'ema', '1'):
        return series.ewm(span=period, adjust=False, min_periods=period).mean()
    if name in ('mode_smma', 'smma', '2'):
        return series.ewm(alpha=1.0 / period, adjust=False, min_periods=period).mean()
    if name in ('mode_lwma', 'lwma', '3'):
        return rolling_lwma(series, period)
    return series.rolling(period, min_periods=period).mean()


def compute_applied_price(frame, mode):
    """Select the source price series based on configured applied price mode."""
    mode = str(mode).lower()
    if mode in ('price_open_', 'open'):
        return frame['open'].astype(float)
    if mode in ('price_high_', 'high'):
        return frame['high'].astype(float)
    if mode in ('price_low_', 'low'):
        return frame['low'].astype(float)
    if mode in ('price_median_', 'median'):
        return (frame['high'].astype(float) + frame['low'].astype(float)) / 2.0
    if mode in ('price_typical_', 'typical'):
        return (frame['high'].astype(float) + frame['low'].astype(float) + frame['close'].astype(float)) / 3.0
    if mode in ('price_weighted_', 'weighted'):
        return (frame['high'].astype(float) + frame['low'].astype(float) + 2.0 * frame['close'].astype(float)) / 4.0
    if mode in ('price_simpl_', 'simpl'):
        return (frame['open'].astype(float) + frame['close'].astype(float)) / 2.0
    if mode in ('price_quarter_', 'quarter'):
        return (frame['high'].astype(float) + frame['low'].astype(float) + frame['open'].astype(float) + frame['close'].astype(float)) / 4.0
    return frame['close'].astype(float)


def compute_center_of_gravity_osma(frame, period=10, smooth_period1=3, ma_method_1='sma', smooth_period2=3, ma_method_2='sma', applied_price='close', point=0.01):
    """Compute center-of-gravity OSMA histogram and signal booleans."""
    price = compute_applied_price(frame, applied_price)
    sma = apply_ma(price, period, 'sma')
    lwma = apply_ma(price, period, 'lwma')
    res1 = (sma * lwma) / float(point)
    res2 = apply_ma(res1, smooth_period1, ma_method_1)
    res3 = res1 - res2
    hist = apply_ma(res3, smooth_period2, ma_method_2)

    buy_signal = (hist.shift(1) < hist.shift(2)) & (hist > hist.shift(1))
    sell_signal = (hist.shift(1) > hist.shift(2)) & (hist < hist.shift(1))

    out = frame.copy()
    out['hist'] = hist
    out['buy_signal'] = buy_signal.astype(float)
    out['sell_signal'] = sell_signal.astype(float)
    return out.dropna(subset=['hist'])


