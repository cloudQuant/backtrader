def resample_frame(df, rule):
    """Resample an OHLCV DataFrame to a coarser timeframe.

    Args:
        df: The base timeframe OHLCV DataFrame.
        rule: A pandas resample rule string (e.g. '5min', '30min').

    Returns:
        A higher-timeframe OHLCV DataFrame with right-labelled bars and rows
        missing price data dropped.
    """
    out = df.resample(rule, label='right', closed='right').agg({
        'open': 'first',
        'high': 'max',
        'low': 'min',
        'close': 'last',
        'volume': 'sum',
        'openinterest': 'last',
    })
    out = out.dropna(subset=['open', 'high', 'low', 'close'])
    out['openinterest'] = out['openinterest'].fillna(0)
    return out


