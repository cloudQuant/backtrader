class ExpWeightOscillatorStrategy(bt.Strategy):
    """Threshold-crossing mean-reversion strategy on the weight oscillator.

    Reads the smoothed composite oscillator from the H4 indicator feed and, on
    crossings of the low/high levels (mapped by ``trend`` mode), opens or closes
    fixed-lot positions on the M15 base feed, bounded by point-based stop-loss
    and take-profit levels.
    """

    params = dict(
        stop_loss=1000,
        take_profit=2000,
        buy_pos_open=True,
        sell_pos_open=True,
        buy_pos_close=True,
        sell_pos_close=True,
        trend='direct',
        high_level=70.0,
        low_level=30.0,
        lot=0.1,
        point=0.01,
        digits_adjust=10,
        price_digits=2,
    )

    def __init__(self):
        """Bind the base/indicator feeds and reset counters and order state."""
        self.base = self.datas[0]
        self.ind = self.datas[1]

        self.bar_num = 0
        self.signal_count = 0
        self.buy_count = 0
        self.sell_count = 0
        self.trade_count = 0
        self.win_count = 0
        self.loss_count = 0
        self.completed_order_count = 0
        self.rejected_order_count = 0

        self.order = None
        self.stop_price = None
        self.take_profit_price = None
        self.last_signal_dt = None

    def _unit(self):
        return float(self.p.point) * float(self.p.digits_adjust)

    def _set_risk(self, side, price=None):
        unit = self._unit()
        if price is None:
            price = float(self.base.close[0])
        if side == 'buy':
            self.stop_price = round(price - float(self.p.stop_loss) * unit, int(self.p.price_digits)) if self.p.stop_loss > 0 else None
            self.take_profit_price = round(price + float(self.p.take_profit) * unit, int(self.p.price_digits)) if self.p.take_profit > 0 else None
        else:
            self.stop_price = round(price + float(self.p.stop_loss) * unit, int(self.p.price_digits)) if self.p.stop_loss > 0 else None
            self.take_profit_price = round(price - float(self.p.take_profit) * unit, int(self.p.price_digits)) if self.p.take_profit > 0 else None

    def _signal(self):
        prev_val = float(self.ind.weight_osc[-1])
        curr_val = float(self.ind.weight_osc[0])
        buy_open = sell_open = buy_close = sell_close = False
        if str(self.p.trend).lower() == 'direct':
            if curr_val > float(self.p.low_level) and prev_val <= float(self.p.low_level):
                buy_open = bool(self.p.buy_pos_open)
                sell_close = bool(self.p.sell_pos_close)
            if curr_val < float(self.p.high_level) and prev_val >= float(self.p.high_level):
                sell_open = bool(self.p.sell_pos_open)
                buy_close = bool(self.p.buy_pos_close)
        else:
            if curr_val > float(self.p.low_level) and prev_val <= float(self.p.low_level):
                sell_open = bool(self.p.sell_pos_open)
                buy_close = bool(self.p.buy_pos_close)
            if curr_val < float(self.p.high_level) and prev_val >= float(self.p.high_level):
                buy_open = bool(self.p.buy_pos_open)
                sell_close = bool(self.p.sell_pos_close)
        return buy_open, sell_open, buy_close, sell_close

    def _manage_position(self, buy_close, sell_close):
        if not self.position or self.order is not None:
            return False
        high = float(self.base.high[0])
        low = float(self.base.low[0])
        if self.position.size > 0:
            if buy_close:
                self.order = self.close()
                return True
            if self.take_profit_price is not None and high >= self.take_profit_price:
                self.order = self.close()
                return True
            if self.stop_price is not None and low <= self.stop_price:
                self.order = self.close()
                return True
        else:
            if sell_close:
                self.order = self.close()
                return True
            if self.take_profit_price is not None and low <= self.take_profit_price:
                self.order = self.close()
                return True
            if self.stop_price is not None and high >= self.stop_price:
                self.order = self.close()
                return True
        return False

    def next(self):
        """Advance one bar: manage an open position or open a new one on signal.

        Skips the indicator warm-up and any pending order, evaluates the entry/
        exit flags via ``_signal``, manages an existing position through
        ``_manage_position``, and otherwise opens a fixed-lot long or short on a
        fresh signal while arming risk levels and counting the signal.
        """
        self.bar_num += 1
        if len(self.ind) < 2:
            return
        if self.order is not None:
            return
        signal_dt = bt.num2date(self.ind.datetime[0])
        buy_open, sell_open, buy_close, sell_close = self._signal()
        if self.position:
            self._manage_position(buy_close, sell_close)
            return
        if self.last_signal_dt == signal_dt:
            return
        if buy_open:
            self.signal_count += 1
            self._set_risk('buy')
            self.order = self.buy(size=self.p.lot)
            self.last_signal_dt = signal_dt
            return
        if sell_open:
            self.signal_count += 1
            self._set_risk('sell')
            self.order = self.sell(size=self.p.lot)
            self.last_signal_dt = signal_dt

    def notify_order(self, order):
        """Update order counters and reset state when an order resolves.

        Tracks completed orders (incrementing buy/sell counts on entry and
        clearing risk levels on a flat close), counts cancelled/rejected/expired
        orders, and clears the tracked order handle once it leaves the live
        states.

        Args:
            order: The order whose status changed.
        """
        if order.status in [bt.Order.Submitted, bt.Order.Accepted]:
            return
        if order.status == bt.Order.Completed:
            self.completed_order_count += 1
            if self.position:
                if order.executed.size > 0:
                    self.buy_count += 1
                elif order.executed.size < 0:
                    self.sell_count += 1
            else:
                self.stop_price = None
                self.take_profit_price = None
        elif order.status in [bt.Order.Canceled, bt.Order.Margin, bt.Order.Rejected, bt.Order.Expired]:
            self.rejected_order_count += 1
        if self.order is not None and order.ref == self.order.ref and order.status not in [bt.Order.Submitted, bt.Order.Accepted]:
            self.order = None

    def notify_trade(self, trade):
        """Tally win/loss counts when a trade closes.

        Args:
            trade: The trade whose status changed; closed trades increment the
                trade counter and the win or loss count by sign of PnL.
        """
        if not trade.isclosed:
            return
        self.trade_count += 1
        if trade.pnlcomm >= 0:
            self.win_count += 1
        else:
            self.loss_count += 1


