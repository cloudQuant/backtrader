def resample_frame(df, rule):
    """Resample an OHLCV frame to a coarser bar size.

    Args:
        df: The source OHLCV DataFrame indexed by datetime.
        rule: A pandas offset alias for the target bar size (e.g. ``'240min'``).

    Returns:
        A resampled DataFrame with right-labelled, right-closed bars, rows
        lacking OHLC dropped and openinterest filled with zeros.
    """
    out = df.resample(rule, label='right', closed='right').agg({
        'open': 'first',
        'high': 'max',
        'low': 'min',
        'close': 'last',
        'volume': 'sum',
        'openinterest': 'last',
    })
    out = out.dropna(subset=['open', 'high', 'low', 'close'])
    out['openinterest'] = out['openinterest'].fillna(0)
    return out


def smooth_series(series, period=5):
    """Exponentially smooth a series.

    Args:
        series: The pandas Series to smooth.
        period: EMA span in bars.

    Returns:
        The EMA-smoothed Series (non-adjusted).
    """
    return series.ewm(span=period, adjust=False).mean()


def compute_weight_oscillator(frame, rsi_weight=1.0, rsi_period=14, mfi_weight=1.0, mfi_period=14, wpr_weight=1.0, wpr_period=14, demarker_weight=1.0, demarker_period=14, b_length=5):
    """Compute the smoothed weighted composite oscillator on a frame.

    Blends RSI, Money Flow Index, Williams %R (shifted to 0..100) and DeMarker
    (scaled to 0..100) by their weights, then EMA-smooths the result.

    Args:
        frame: OHLCV DataFrame (with a volume column) to compute on.
        rsi_weight: Weight applied to the RSI component.
        rsi_period: Lookback period for bt.indicators.RSI.
        mfi_weight: Weight applied to the Money Flow Index component.
        mfi_period: Lookback period for the Money Flow Index.
        wpr_weight: Weight applied to the Williams %R component.
        wpr_period: Lookback period for Williams %R.
        demarker_weight: Weight applied to the DeMarker component.
        demarker_period: Lookback period for bt.indicators.DeMarker.
        b_length: EMA span used to smooth the combined oscillator.

    Returns:
        A copy of ``frame`` with an added ``weight_osc`` column, with warm-up
        rows lacking the oscillator dropped.
    """
    work = frame.copy()
    close = work['close']
    high = work['high']
    low = work['low']
    volume = work['volume']

    delta = close.diff()
    gain = delta.clip(lower=0).rolling(rsi_period, min_periods=rsi_period).mean()
    loss = (-delta.clip(upper=0)).rolling(rsi_period, min_periods=rsi_period).mean()
    rs = gain / loss.replace(0, pd.NA)
    rsi = 100 - (100 / (1 + rs))

    typical = (high + low + close) / 3.0
    money_flow = typical * volume
    pos_flow = money_flow.where(typical > typical.shift(1), 0.0).rolling(mfi_period, min_periods=mfi_period).sum()
    neg_flow = money_flow.where(typical < typical.shift(1), 0.0).rolling(mfi_period, min_periods=mfi_period).sum()
    mfr = pos_flow / neg_flow.replace(0, pd.NA)
    mfi = 100 - (100 / (1 + mfr))

    highest = high.rolling(wpr_period, min_periods=wpr_period).max()
    lowest = low.rolling(wpr_period, min_periods=wpr_period).min()
    wpr = -100 * (highest - close) / (highest - lowest).replace(0, pd.NA)
    wpr_adj = wpr + 100.0

    demax = (high - high.shift(1)).clip(lower=0)
    demin = (low.shift(1) - low).clip(lower=0)
    demarker = demax.rolling(demarker_period, min_periods=demarker_period).mean() / (
        demax.rolling(demarker_period, min_periods=demarker_period).mean() + demin.rolling(demarker_period, min_periods=demarker_period).mean()
    ).replace(0, pd.NA)
    demarker_adj = demarker * 100.0

    sum_weight = rsi_weight + mfi_weight + wpr_weight + demarker_weight
    weight_osc = (rsi_weight * rsi + mfi_weight * mfi + wpr_weight * wpr_adj + demarker_weight * demarker_adj) / sum_weight
    work['weight_osc'] = smooth_series(weight_osc, b_length)
    return work.dropna(subset=['weight_osc'])


