class LeadingStrategy(bt.Strategy):
    """Trade netlead/EMA crossovers with fixed stop-loss and take-profit.

    Reads the precomputed netlead and EMA lines from the H4 signal feed and
    enters in the crossover direction sized by ``size``, closing on the opposite
    crossover and protecting positions with point-based stop-loss and
    take-profit levels on the M15 feed.
    """

    params = dict(
        mm=0.1,
        mm_mode='LOT',
        stop_loss=1000,
        take_profit=2000,
        deviation=10,
        buy_pos_open=True,
        sell_pos_open=True,
        buy_pos_close=True,
        sell_pos_close=True,
        signal_bar=1,
        alpha1=0.25,
        alpha2=0.33,
        size=0.1,
        point=0.01,
        digits_adjust=10,
        price_digits=2,
    )

    def __init__(self):
        """Bind the M15 and H4 feeds and reset counters and risk state."""
        self.m15 = self.datas[0]
        self.h4 = self.datas[1]
        self.netlead = self.h4.netlead
        self.ema_line = self.h4.ema_line

        self.bar_num = 0
        self.buy_signal_count = 0
        self.sell_signal_count = 0
        self.buy_count = 0
        self.sell_count = 0
        self.trade_count = 0
        self.win_count = 0
        self.loss_count = 0
        self.completed_order_count = 0
        self.rejected_order_count = 0

        self.entry_order = None
        self.stop_price = None
        self.take_profit_price = None
        self.last_signal_dt = None

    def log(self, text):
        """Print ``text`` prefixed with the current M15 bar timestamp."""
        dt = bt.num2date(self.m15.datetime[0])
        print('{0}, {1}'.format(dt.isoformat(), text))

    def _trade_unit(self):
        return self.p.point * self.p.digits_adjust

    def _enough_history(self):
        idx = max(int(self.p.signal_bar), 1)
        try:
            values = [float(self.netlead[-idx]), float(self.netlead[-(idx + 1)]), float(self.ema_line[-idx]), float(self.ema_line[-(idx + 1)])]
        except (TypeError, ValueError, IndexError):
            return False
        return not any(math.isnan(v) for v in values)

    def _manage_risk(self):
        if not self.position:
            return False
        high = float(self.m15.high[0])
        low = float(self.m15.low[0])
        if self.position.size > 0:
            if self.stop_price is not None and low <= self.stop_price:
                self.entry_order = self.close()
                return True
            if self.take_profit_price is not None and high >= self.take_profit_price:
                self.entry_order = self.close()
                return True
        else:
            if self.stop_price is not None and high >= self.stop_price:
                self.entry_order = self.close()
                return True
            if self.take_profit_price is not None and low <= self.take_profit_price:
                self.entry_order = self.close()
                return True
        return False

    def _set_risk_prices(self, side):
        price = float(self.m15.close[0])
        unit = self._trade_unit()
        if side == 'buy':
            self.stop_price = round(price - self.p.stop_loss * unit, self.p.price_digits) if self.p.stop_loss > 0 else None
            self.take_profit_price = round(price + self.p.take_profit * unit, self.p.price_digits) if self.p.take_profit > 0 else None
        else:
            self.stop_price = round(price + self.p.stop_loss * unit, self.p.price_digits) if self.p.stop_loss > 0 else None
            self.take_profit_price = round(price - self.p.take_profit * unit, self.p.price_digits) if self.p.take_profit > 0 else None

    def _evaluate_signals(self):
        idx = max(int(self.p.signal_bar), 1)
        nl_now = float(self.netlead[-idx])
        nl_prev = float(self.netlead[-(idx + 1)])
        ema_now = float(self.ema_line[-idx])
        ema_prev = float(self.ema_line[-(idx + 1)])

        buy_open = buy_close = sell_open = sell_close = False
        if nl_prev > ema_prev:
            if self.p.buy_pos_open and nl_now < ema_now:
                buy_open = True
                self.buy_signal_count += 1
            if self.p.sell_pos_close:
                sell_close = True
        if nl_prev < ema_prev:
            if self.p.sell_pos_open and nl_now > ema_now:
                sell_open = True
                self.sell_signal_count += 1
            if self.p.buy_pos_close:
                buy_close = True
        return buy_open, buy_close, sell_open, sell_close, nl_prev, nl_now, ema_prev, ema_now

    def next(self):
        """Enforce risk exits, then act on each new signal bar's crossover.

        Waits for enough indicator history, skips while an order is pending, and
        applies stop-loss/take-profit first. On each new signal bar it evaluates
        the netlead/EMA crossover, closes opposing positions, and opens a long or
        short sized by ``size`` while setting risk prices.
        """
        self.bar_num += 1
        if self.entry_order is not None:
            return
        if not self._enough_history():
            return
        if self._manage_risk():
            return

        signal_dt = bt.num2date(self.h4.datetime[-max(int(self.p.signal_bar), 1)])
        if self.last_signal_dt == signal_dt:
            return
        self.last_signal_dt = signal_dt

        buy_open, buy_close, sell_open, sell_close, nl_prev, nl_now, ema_prev, ema_now = self._evaluate_signals()
        self.log('leading prev={0:.4f}/{1:.4f} now={2:.4f}/{3:.4f} buy_open={4} sell_open={5}'.format(nl_prev, ema_prev, nl_now, ema_now, buy_open, sell_open))

        if buy_close and self.position and self.position.size > 0:
            self.entry_order = self.close()
            return
        if sell_close and self.position and self.position.size < 0:
            self.entry_order = self.close()
            return
        if buy_open and (not self.position or self.position.size <= 0):
            if self.position and self.position.size < 0:
                self.entry_order = self.close()
                return
            self._set_risk_prices('buy')
            self.entry_order = self.buy(size=self.p.size)
            return
        if sell_open and (not self.position or self.position.size >= 0):
            if self.position and self.position.size > 0:
                self.entry_order = self.close()
                return
            self._set_risk_prices('sell')
            self.entry_order = self.sell(size=self.p.size)

    def notify_order(self, order):
        """Track completed/rejected orders, entry counts, and clear risk on close.

        Args:
            order: The order whose status changed; submitted/accepted states are
                ignored, completions update buy/sell or reset stop/take-profit,
                and failures increment the rejected count.
        """
        if order.status in [bt.Order.Submitted, bt.Order.Accepted]:
            return
        if order.status == bt.Order.Completed:
            self.completed_order_count += 1
            if self.position:
                if order.executed.size > 0:
                    self.buy_count += 1
                elif order.executed.size < 0:
                    self.sell_count += 1
            else:
                self.stop_price = None
                self.take_profit_price = None
        elif order.status in [bt.Order.Canceled, bt.Order.Margin, bt.Order.Rejected, bt.Order.Expired]:
            self.rejected_order_count += 1
        if self.entry_order is not None and order.ref == self.entry_order.ref and order.status not in [bt.Order.Submitted, bt.Order.Accepted]:
            self.entry_order = None

    def notify_trade(self, trade):
        """Tally closed trades into win/loss buckets.

        Args:
            trade: The trade being notified; only closed trades update counts.
        """
        if not trade.isclosed:
            return
        self.trade_count += 1
        if trade.pnlcomm >= 0:
            self.win_count += 1
        else:
            self.loss_count += 1



