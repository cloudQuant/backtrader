def resample_frame(df, rule):
    """Resample an OHLCV+volume frame to a coarser bar rule.

    Args:
        df: Source OHLCV DataFrame indexed by datetime.
        rule: A pandas resample rule string (e.g. ``240min``).

    Returns:
        A resampled DataFrame with NaN OHLC rows dropped and openinterest gaps
        filled with zero.
    """
    out = df.resample(rule, label='right', closed='right').agg({
        'open': 'first',
        'high': 'max',
        'low': 'min',
        'close': 'last',
        'tick_volume': 'sum',
        'real_volume': 'sum',
        'openinterest': 'last',
    })
    out = out.dropna(subset=['open', 'high', 'low', 'close'])
    out['openinterest'] = out['openinterest'].fillna(0)
    return out


def resolve_price(frame, price_mode):
    """Select a price series for the given applied-price mode.

    Args:
        frame: OHLC DataFrame to derive the price series from.
        price_mode: Applied-price constant (e.g. ``PRICE_CLOSE``,
            ``PRICE_MEDIAN``, ``PRICE_TYPICAL``, ``PRICE_WEIGHTED``).

    Returns:
        The price Series corresponding to ``price_mode``, defaulting to close.
    """
    if price_mode == PRICE_OPEN:
        return frame['open']
    if price_mode == PRICE_HIGH:
        return frame['high']
    if price_mode == PRICE_LOW:
        return frame['low']
    if price_mode == PRICE_MEDIAN:
        return (frame['high'] + frame['low']) / 2.0
    if price_mode == PRICE_TYPICAL:
        return (frame['high'] + frame['low'] + frame['close']) / 3.0
    if price_mode == PRICE_WEIGHTED:
        return (frame['high'] + frame['low'] + frame['close'] + frame['close']) / 4.0
    return frame['close']


def smooth_series(series, period, method='MODE_SMA'):
    """Smooth a series using the requested moving-average method.

    Args:
        series: The input Series to smooth.
        period: Smoothing window length (clamped to at least 1).
        method: Moving-average method (``MODE_SMA``, ``MODE_EMA``,
            ``MODE_SMMA``, or ``MODE_LWMA``).

    Returns:
        The smoothed Series.
    """
    period = max(int(period), 1)
    if method == 'MODE_EMA':
        return series.ewm(span=period, adjust=False).mean()
    if method == 'MODE_SMMA':
        return series.ewm(alpha=1.0 / period, adjust=False).mean()
    if method == 'MODE_LWMA':
        weights = np.arange(1, period + 1, dtype=float)
        return series.rolling(period, min_periods=period).apply(lambda x: np.dot(x, weights) / weights.sum(), raw=True)
    return series.rolling(period, min_periods=period).mean()


def compute_rsi(series, period=25):
    """Compute the Wilder-smoothed RSI of a price series.

    Args:
        series: The price Series to compute RSI on.
        period: RSI lookback period.

    Returns:
        An RSI Series bounded in 0..100, with neutral 50.0 fill where undefined.
    """
    delta = series.diff()
    gain = delta.clip(lower=0.0)
    loss = -delta.clip(upper=0.0)
    avg_gain = gain.ewm(alpha=1.0 / period, adjust=False, min_periods=period).mean()
    avg_loss = loss.ewm(alpha=1.0 / period, adjust=False, min_periods=period).mean()
    rs = avg_gain / avg_loss.replace(0, np.nan)
    rsi = 100 - (100 / (1 + rs))
    rsi = rsi.where(~((avg_loss == 0) & (avg_gain > 0)), 100.0)
    rsi = rsi.where(~((avg_gain == 0) & (avg_loss > 0)), 0.0)
    return rsi.fillna(50.0)


def compute_cronex_rsi(frame, rsi_period=25, rsi_price=PRICE_CLOSE, xma_method='MODE_SMA', fast_period=14, slow_period=25):
    """Compute the Cronex RSI ind/sign lines for a frame.

    Args:
        frame: OHLC DataFrame to compute on.
        rsi_period: RSI lookback period.
        rsi_price: Applied-price mode for the RSI source.
        xma_method: Moving-average method used for both smoothings.
        fast_period: Fast smoothing period producing the ``ind`` line.
        slow_period: Slow smoothing period producing the ``sign`` line.

    Returns:
        A copy of ``frame`` with ``ind`` and ``sign`` columns added, dropping
        rows where either is undefined.
    """
    out = frame.copy()
    price = resolve_price(out, rsi_price)
    rsi = compute_rsi(price, rsi_period)
    ind = smooth_series(rsi, fast_period, xma_method)
    sign = smooth_series(ind, slow_period, xma_method)
    out['ind'] = ind
    out['sign'] = sign
    out = out.dropna(subset=['ind', 'sign'])
    return out


