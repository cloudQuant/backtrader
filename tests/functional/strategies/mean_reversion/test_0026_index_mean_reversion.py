def finite_or_none(value):
    """Return a finite value, otherwise ``None``."""
    return value if value is not None and math.isfinite(value) else None


def calculate_ulcer_index(values):
    """Compute ulcer index from the broker-value sequence.

    Args:
        values: Equity curve values.

    Returns:
        Ulcer index numeric value.
    """
    if len(values) < 2:
        return 0.0
    peak = values[0]
    squared = 0.0
    for value in values:
        peak = max(peak, value)
        drawdown = (peak - value) / peak * 100.0 if peak > 0 else 0.0
        squared += drawdown ** 2
    return math.sqrt(squared / len(values))



