def load_backtest_frames(config):
    """Prepare base and signal dataframes from config settings."""
    data_cfg = config['data']
    params = config['params']
    fromdate = datetime.datetime.fromisoformat(data_cfg['fromdate'])
    todate = datetime.datetime.fromisoformat(data_cfg['todate'])
    base = load_mt5_csv(resolve_data_path(data_cfg['file']), fromdate=fromdate, todate=todate, bar_shift_minutes=data_cfg.get('bar_shift_minutes', 0))
    if base.empty:
        raise ValueError('Loaded data frame is empty')
    signal_minutes = data_cfg.get('signal_tf_minutes', 240)
    signal_df = resample_frame(base, f'{signal_minutes}min')
    signal_df = compute_jbraintrend1stop(
        signal_df,
        atr_period=params['atr_period'],
        sto_period=params['sto_period'],
        ma_method=params['ma_method'],
        stop_dperiod=params['stop_dperiod'],
        length_=params['length_'],
        point=params['point'],
    )
    signal_df['volume'] = signal_df['tick_volume']
    print(f'Loaded bars: M15={len(base)}, signal={len(signal_df)}')
    return {'m15': base, 'signal': signal_df, 'fromdate': fromdate, 'todate': todate}


def build_cerebro(config, frame):
    """Create and configure Cerebro with feeds, strategy, analyzers, and optional logger."""
    bt_cfg = config['backtest']
    signal_minutes = config['data'].get('signal_tf_minutes', 240)
    cerebro = bt.Cerebro(stdstats=True)
    cerebro.broker.setcash(bt_cfg['initial_cash'])
    comm_type = bt.CommInfoBase.COMM_FIXED if bt_cfg.get('commission_type', 'fixed') == 'fixed' else bt.CommInfoBase.COMM_PERC
    cerebro.broker.setcommission(commission=bt_cfg['commission'], margin=bt_cfg['margin'], mult=bt_cfg['multiplier'], commtype=comm_type, stocklike=bt_cfg.get('stocklike', False))
    base_df = frame['m15'].copy()
    base_df['volume'] = base_df['tick_volume']
    feed_m15 = Mt5PandasFeed(dataname=base_df[['open', 'high', 'low', 'close', 'volume', 'openinterest']], timeframe=bt.TimeFrame.Minutes, compression=15)
    sig = frame['signal'][['open', 'high', 'low', 'close', 'volume', 'openinterest', 'sell_stop', 'buy_stop', 'sell_stop_line', 'buy_stop_line']]
    feed_signal = JBrainTrend1StopFeed(dataname=sig, timeframe=bt.TimeFrame.Minutes, compression=signal_minutes)
    cerebro.adddata(feed_m15, name='XAUUSD_M15')
