def parse_dt(value):
    """Parse ISO format datetime string value into datetime object.

    Args:
        value (str): Datetime string value.

    Returns:
        datetime.datetime or None: Parsed datetime object.
    """
    if not value:
        return None
    return datetime.datetime.fromisoformat(value)


