class XBullsBearsEyesVolDirectStrategy(bt.Strategy):
    """Trade colour flips of the volume-weighted Bulls/Bears direct line.

    Acts on the precomputed buy/sell/close signals on each open bar (cheat on
    open): opens longs/shorts on fresh signals with fixed point-based stop-loss
    and take-profit brackets, and closes on stop, target, or a reverse signal.
    """

    params = dict(
        lot=0.1,
        stop_loss_points=1000,
        take_profit_points=2000,
        point=0.01,
        period=13,
        gamma=0.6,
        ma_length=12,
        signal_bar=1,
        volume_type='tick',
    )

    def __init__(self):
        """Reset order state, bracket levels, pending side, and counters."""
        self.order = None
        self.current_stop = None
        self.current_take_profit = None
        self.pending_stop = None
        self.pending_take_profit = None
        self.pending_side = None
        self.bar_num = 0
        self.buy_count = 0
        self.sell_count = 0
        self.trade_count = 0
        self.win_count = 0
        self.loss_count = 0

    def log(self, text):
        """Print a timestamped log line for the current bar.

        Args:
            text: The message to log alongside the current bar datetime.
        """
        dt = bt.num2date(self.data.datetime[0])
        print(f'{dt.isoformat()}, {text}')

    def next(self):
        """Advance the bar counter; trading happens in :meth:`next_open`."""
        self.bar_num += 1

    def _close_if_exit_hit(self):
        if not self.position:
            return False
        low = float(self.data.low[-1])
        high = float(self.data.high[-1])
        if self.position.size > 0:
            if self.current_stop is not None and low <= self.current_stop:
                self.log(f'close long stop={self.current_stop:.2f}')
                self.order = self.close()
                return True
            if self.current_take_profit is not None and high >= self.current_take_profit:
                self.log(f'close long take_profit={self.current_take_profit:.2f}')
                self.order = self.close()
                return True
            if bool(self.data.close_buy_signal[0]):
                self.log('close long signal_reverse')
                self.order = self.close()
                return True
        else:
            if self.current_stop is not None and high >= self.current_stop:
                self.log(f'close short stop={self.current_stop:.2f}')
                self.order = self.close()
                return True
            if self.current_take_profit is not None and low <= self.current_take_profit:
                self.log(f'close short take_profit={self.current_take_profit:.2f}')
                self.order = self.close()
                return True
            if bool(self.data.close_sell_signal[0]):
                self.log('close short signal_reverse')
                self.order = self.close()
                return True
        return False

    def next_open(self):
        """Manage exits and open new bracketed positions at the bar open.

        Skips while an order is pending or during warm-up. When holding a
        position it checks stop/take-profit/reverse exits; when flat it opens a
        long on a buy signal or a short on a sell signal, recording the pending
        stop and take-profit levels.
        """
        if self.order:
            return
        if len(self.data) < 2:
            return

        if self.position:
            self._close_if_exit_hit()
            return

        entry_price = float(self.data.open[0])
        if bool(self.data.buy_signal[0]):
            self.pending_side = 'long'
            self.pending_stop = entry_price - self.p.stop_loss_points * self.p.point if self.p.stop_loss_points else None
            self.pending_take_profit = entry_price + self.p.take_profit_points * self.p.point if self.p.take_profit_points else None
            self.log(
                'buy '
                f'size={self.p.lot:.2f} '
                f'open={entry_price:.2f} '
                f'direct={float(self.data.direct[0]):.2f} '
                f'color={float(self.data.color[0]):.0f}'
            )
            self.order = self.buy(size=self.p.lot)
            return

        if bool(self.data.sell_signal[0]):
            self.pending_side = 'short'
            self.pending_stop = entry_price + self.p.stop_loss_points * self.p.point if self.p.stop_loss_points else None
            self.pending_take_profit = entry_price - self.p.take_profit_points * self.p.point if self.p.take_profit_points else None
            self.log(
                'sell '
                f'size={self.p.lot:.2f} '
                f'open={entry_price:.2f} '
                f'direct={float(self.data.direct[0]):.2f} '
                f'color={float(self.data.color[0]):.0f}'
            )
            self.order = self.sell(size=self.p.lot)

    def notify_order(self, order):
        """Confirm fills, apply the stored brackets, and count entries.

        Args:
            order: The order whose status changed. On a completed entry it
                increments the buy/sell counter and activates the pending stop
                and take-profit; submitted/accepted states are ignored and all
                pending state is cleared once the order settles.
        """
        if order.status in [order.Submitted, order.Accepted]:
            return
        if order.status == order.Completed:
            if self.pending_side == 'long' and self.position.size > 0:
                self.buy_count += 1
                self.current_stop = self.pending_stop
                self.current_take_profit = self.pending_take_profit
            elif self.pending_side == 'short' and self.position.size < 0:
                self.sell_count += 1
                self.current_stop = self.pending_stop
                self.current_take_profit = self.pending_take_profit
            elif self.position.size == 0:
                self.current_stop = None
                self.current_take_profit = None
        self.order = None
        self.pending_stop = None
        self.pending_take_profit = None
        self.pending_side = None

    def notify_trade(self, trade):
        """Tally closed trades into win/loss counters.

        Args:
            trade: The trade whose status changed; only closed trades count.
        """
        if not trade.isclosed:
            return
        self.trade_count += 1
        if trade.pnlcomm > 0:
            self.win_count += 1
        else:
            self.loss_count += 1
        self.log(f'trade closed pnl={trade.pnlcomm:.2f}')




