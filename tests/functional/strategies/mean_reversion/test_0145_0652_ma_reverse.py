class MaReverseStrategy(bt.Strategy):
    """Mean-reversion strategy that enters counter-trend positions after a streak of unidirectional bars vs bt.indicators.SMA."""
    params = dict(
        ma_period=14,
        count_threshold=150,
        take_profit=30,
        lots=1.0,
        point=0.01,
        price_digits=2,
    )

    def __init__(self):
        """Initialize SMA indicator, bar counter, and order/risk state variables."""
        self.sma = bt.indicators.SMA(self.data.close, period=self.p.ma_period)
        self.bar_num = 0
        self.signal_count = 0
        self.buy_count = 0
        self.sell_count = 0
        self.trade_count = 0
        self.win_count = 0
        self.loss_count = 0
        self.completed_order_count = 0
        self.rejected_order_count = 0
        self.order = None
        self.take_profit_price = None
        self.count = 0

    def _point(self):
        return float(self.p.point)

    def _round(self, value):
        return round(float(value), int(self.p.price_digits))

    def _manage_position(self):
        if not self.position or self.order is not None:
            return
        high = float(self.data.high[0])
        low = float(self.data.low[0])
        if self.position.size > 0 and self.take_profit_price is not None and high >= float(self.take_profit_price):
            self.order = self.close()
            return
        if self.position.size < 0 and self.take_profit_price is not None and low <= float(self.take_profit_price):
            self.order = self.close()

    def next(self):
        """Bar-by-bar logic: update streak count, check threshold, place reversal order with TP."""
        self.bar_num += 1
        if len(self) < self.p.ma_period + 2:
            return
        if self.order is not None:
            return

        price = float(self.data.close[0])
        ma = float(self.sma[0])

        if price == ma:
            self.count = 0
        if price - ma > 0:
            if self.count < 0:
                self.count = 0
            self.count += 1
        elif price - ma < 0:
            if self.count > 0:
                self.count = 0
            self.count -= 1

        if self.position:
            self._manage_position()
            return

        tp_dist = float(self.p.take_profit) * self._point()
        if self.count > int(self.p.count_threshold) and price > ma:
            self.signal_count += 1
            self.take_profit_price = self._round(price - tp_dist)
            self.order = self.sell(size=self.p.lots)
        elif self.count < -int(self.p.count_threshold) and price < ma:
            self.signal_count += 1
            self.take_profit_price = self._round(price + tp_dist)
            self.order = self.buy(size=self.p.lots)

    def notify_order(self, order):
        """Track order lifecycle: count completions/rejections, track buy/sell counts."""
        if order.status in [bt.Order.Submitted, bt.Order.Accepted]:
            return
        if order.status == bt.Order.Completed:
            self.completed_order_count += 1
            if self.position:
                if order.executed.size > 0:
                    self.buy_count += 1
                elif order.executed.size < 0:
                    self.sell_count += 1
            else:
                self.take_profit_price = None
        elif order.status in [bt.Order.Canceled, bt.Order.Margin, bt.Order.Rejected, bt.Order.Expired]:
            self.rejected_order_count += 1
        if self.order is not None and order.ref == self.order.ref and order.status not in [bt.Order.Submitted, bt.Order.Accepted]:
            self.order = None

    def notify_trade(self, trade):
        """Track trade outcomes: win/loss count based on closed trade P&L."""
        if not trade.isclosed:
            return
        self.trade_count += 1
        if trade.pnlcomm >= 0:
            self.win_count += 1
        else:
            self.loss_count += 1


