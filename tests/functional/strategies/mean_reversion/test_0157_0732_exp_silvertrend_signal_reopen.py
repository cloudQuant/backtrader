def load_mt5_csv(filepath, fromdate=None, todate=None, bar_shift_minutes=0):
    """Load and normalize MT5 tab-separated data to a Backtrader-style DataFrame.

    Args:
        filepath: MT5 export file path.
        fromdate: Optional inclusive start datetime filter.
        todate: Optional inclusive end datetime filter.
        bar_shift_minutes: Optional shift applied to all bar timestamps.

    Returns:
        Parsed DataFrame indexed by `datetime`.
    """
    with open(filepath, 'r', encoding='utf-8') as f:
        lines = f.read().strip().split('\n')
    cleaned = '\n'.join(line.strip().strip('"') for line in lines)
    df = pd.read_csv(io.StringIO(cleaned), sep='\t')
    df['datetime'] = pd.to_datetime(df['<DATE>'] + ' ' + df['<TIME>'], format='%Y.%m.%d %H:%M:%S')
    df = df.rename(columns={
        '<OPEN>': 'open',
        '<HIGH>': 'high',
        '<LOW>': 'low',
        '<CLOSE>': 'close',
        '<TICKVOL>': 'tick_volume',
        '<VOL>': 'real_volume',
    })
    df['openinterest'] = 0
    df['volume'] = df['tick_volume']
    df = df[['datetime', 'open', 'high', 'low', 'close', 'volume', 'openinterest']]
    df = df.set_index('datetime')
    if bar_shift_minutes:
        df.index = df.index + pd.Timedelta(minutes=bar_shift_minutes)
    if fromdate is not None:
        df = df[df.index >= fromdate]
    if todate is not None:
        df = df[df.index <= todate]
    return df


def resample_frame(df, rule):
    """Aggregate base bars into a higher timeframe signal frame.

    Args:
        df: Base frame indexed by datetime.
        rule: Resample rule string, e.g. `"240min"`.

    Returns:
        Resampled OHLCV frame with cleaned edge bars.
    """
    out = df.resample(rule, label='right', closed='right').agg({
        'open': 'first',
        'high': 'max',
        'low': 'min',
        'close': 'last',
        'volume': 'sum',
        'openinterest': 'last',
    })
    out = out.dropna(subset=['open', 'high', 'low', 'close'])
    out['openinterest'] = out['openinterest'].fillna(0)
    return out


def compute_silvertrend_signal(frame, ssp=9, risk=3):
    """Compute SilverTrend buy/sell signal levels.

    Args:
        frame: OHLCV frame to compute signals on.
        ssp: Sensitivity window length.
        risk: Risk percentage used for envelope thresholds.

    Returns:
        Frame with `buy_arrow` and `sell_arrow` columns.
    """
    work = frame.copy()
    k = int(risk)
    start_bars = ssp + 1
    old = False
    uptrend = False
    buy_arrow = []
    sell_arrow = []
    for idx in range(len(work)):
        if idx < start_bars:
            buy_arrow.append(0.0)
            sell_arrow.append(0.0)
            continue
        window = work.iloc[max(0, idx - ssp + 1): idx + 1]
        high_max = window['high'].max()
        low_min = window['low'].min()
        avg_range = (window['high'] - window['low']).mean()
        rng = avg_range
        smin = low_min + (high_max - low_min) * k / 100.0
        smax = high_max - (high_max - low_min) * k / 100.0
        close = float(work['close'].iloc[idx])
        if close < smin:
            uptrend = False
        if close > smax:
            uptrend = True
        buy_val = 0.0
        sell_val = 0.0
        low = float(work['low'].iloc[idx])
        high = float(work['high'].iloc[idx])
        if uptrend != old and uptrend:
            buy_val = low - rng * 0.5
        if uptrend != old and not uptrend:
            sell_val = high + rng * 0.5
        buy_arrow.append(buy_val)
        sell_arrow.append(sell_val)
        old = uptrend
    work['buy_arrow'] = buy_arrow
    work['sell_arrow'] = sell_arrow
    return work


