def resolve_data_path(filename):
    """Resolve a fixture file relative to the current strategy directory.

    Args:
        filename: Relative fixture file path.

    Returns:
        Absolute file path.

    Raises:
        FileNotFoundError: If fixture path does not exist.
    """
    path = (BASE_DIR / filename).resolve()
    if not path.exists():
        raise FileNotFoundError(f'Data file not found: {path}')
    return path


def parse_dt(value):
    """Parse ISO timestamp string to datetime.

    Args:
        value: ISO datetime string or falsy value.

    Returns:
        ``datetime`` object or ``None``.
    """
    if not value:
        return None
    return datetime.datetime.fromisoformat(value)


def load_backtest_frame(config):
    """Build M15, H1, and D1 frames required by the strategy.

    Args:
        config: Strategy configuration dictionary.

    Returns:
        Dict containing base, H1, and D1 DataFrames.
    """
    data_cfg = config['data']
    fromdate = parse_dt(data_cfg.get('fromdate'))
    todate = parse_dt(data_cfg.get('todate'))
    df = load_mt5_csv(
        resolve_data_path(data_cfg['file']),
        fromdate=fromdate,
        todate=todate,
        bar_shift_minutes=data_cfg.get('bar_shift_minutes', 0),
    )
    if df.empty:
        raise ValueError('Loaded data frame is empty')
    h1_df = df.resample('1h').agg({
        'open': 'first',
        'high': 'max',
        'low': 'min',
        'close': 'last',
        'volume': 'sum',
        'openinterest': 'last',
        'spread': 'last',
    }).dropna(subset=['open', 'high', 'low', 'close'])
    d1_df = df.resample('1D').agg({
        'open': 'first',
        'high': 'max',
        'low': 'min',
        'close': 'last',
        'volume': 'sum',
        'openinterest': 'last',
        'spread': 'last',
    }).dropna(subset=['open', 'high', 'low', 'close'])
    print(f'Loaded {len(df)} bars: {df.index[0]} -> {df.index[-1]}')
    return {'data': df, 'h1_data': h1_df, 'd1_data': d1_df}


def build_cerebro(config, frame):
    """Construct Cerebro with three timeframe feeds and analyzers.

    Args:
        config: Strategy config.
        frame: Backtest frame payload.

    Returns:
        Configured Cerebro instance.
    """
    bt_cfg = config['backtest']
    data_cfg = config['data']
    cerebro = bt.Cerebro(stdstats=True)
    cerebro.broker.setcash(bt_cfg['initial_cash'])
    comm_type = bt.CommInfoBase.COMM_FIXED if bt_cfg.get('commission_type', 'fixed') == 'fixed' else bt.CommInfoBase.COMM_PERC
    cerebro.broker.setcommission(
        commission=bt_cfg['commission'],
        margin=bt_cfg['margin'],
        mult=bt_cfg['multiplier'],
        commtype=comm_type,
        stocklike=bt_cfg.get('stocklike', False),
    )
    m15_feed = Mt5PandasFeed(dataname=frame['data'], timeframe=bt.TimeFrame.Minutes, compression=data_cfg.get('execution_compression_minutes', 15))
    h1_feed = Mt5PandasFeed(dataname=frame['h1_data'], timeframe=bt.TimeFrame.Minutes, compression=60)
    d1_feed = Mt5PandasFeed(dataname=frame['d1_data'], timeframe=bt.TimeFrame.Days, compression=1)
    cerebro.adddata(m15_feed, name=f"{data_cfg['symbol']}_{data_cfg['execution_timeframe']}")
    cerebro.adddata(h1_feed, name=f"{data_cfg['symbol']}_H1")
    cerebro.adddata(d1_feed, name=f"{data_cfg['symbol']}_D1")
    cerebro.addstrategy(DayTradingPamxaStrategy, **config['params'])
    cerebro.addanalyzer(bt.analyzers.SharpeRatio, _name='sharpe', timeframe=bt.TimeFrame.Minutes, factor=MINUTES_PER_TRADING_YEAR, annualize=True, riskfreerate=0)
    cerebro.addanalyzer(bt.analyzers.Returns, _name='returns', timeframe=bt.TimeFrame.Minutes, compression=data_cfg.get('execution_compression_minutes', 15), tann=MINUTES_PER_TRADING_YEAR)
    cerebro.addanalyzer(bt.analyzers.DrawDown, _name='drawdown')
    cerebro.addanalyzer(bt.analyzers.TradeAnalyzer, _name='trades')
    cerebro.addanalyzer(bt.analyzers.SQN, _name='sqn')
    return cerebro


def finite_or_none(value):
    """Return None for non-finite numeric values."""
    if value is None:
        return None
    if isinstance(value, (int, float)) and not math.isfinite(value):
        return None
    return value


def extract_metrics(results, start_value):
    """Generate summary metrics from strategy/analyzer outputs.

    Args:
        results: Result list returned by Cerebro.run().
        start_value: Initial broker value before execution.

    Returns:
        Dictionary of bar, return, trade, drawdown and efficiency metrics.
    """
    strat = results[0]
    end_value = strat.broker.getvalue()
    drawdown = strat.analyzers.drawdown.get_analysis()
    returns = strat.analyzers.returns.get_analysis()
    sharpe = strat.analyzers.sharpe.get_analysis()
    sqn = strat.analyzers.sqn.get_analysis()
    trades = strat.analyzers.trades.get_analysis()
    total_closed = trades.get('total', {}).get('closed', 0)
    won = trades.get('won', {}).get('total', 0)
    lost = trades.get('lost', {}).get('total', 0)
    gross_won = trades.get('won', {}).get('pnl', {}).get('total', 0) or 0
    gross_lost = abs(trades.get('lost', {}).get('pnl', {}).get('total', 0) or 0)
    return {
        'bar_num': strat.bar_num,
        'buy_count': strat.buy_count,
        'sell_count': strat.sell_count,
        'trade_count': strat.trade_count,
        'win_count': strat.win_count,
        'loss_count': strat.loss_count,
        'initial_cash': start_value,
        'final_value': end_value,
        'net_pnl': end_value - start_value,
        'total_return_pct': (end_value / start_value - 1.0) * 100.0,
        'total_trades': total_closed,
        'won': won,
        'lost': lost,
        'win_rate': (won / total_closed * 100.0) if total_closed else 0.0,
        'profit_factor': (gross_won / gross_lost) if gross_lost else None,
        'max_drawdown': drawdown.get('max', {}).get('drawdown', 0.0),
        'sharpe_ratio': finite_or_none(sharpe.get('sharperatio')),
        'annual_return_pct': (returns.get('rnorm') or 0.0) * 100.0,
        'sqn': finite_or_none(sqn.get('sqn')),
    }


def run(plot=False):
    """Run the backtest and return ``(results, metrics, cerebro)``.

    Args:
        plot: Whether to display a candlestick plot.

    Returns:
        ``(results, metrics, cerebro)`` tuple.
    """
    config = _bt_load_config(_CONFIG, repo=_REPO)
    frame = load_backtest_frame(config)
    cerebro = build_cerebro(config, frame)
    start_value = cerebro.broker.getvalue()
    print('\nStarting backtest...')
    results = cerebro.run(runonce=False)
    metrics = extract_metrics(results, start_value)

    if plot:
        cerebro.plot(style='candlestick')
    return results, metrics, cerebro


def _close(actual, expected, *, tol, key):
    """Assert ``actual`` is finite and within ``tol`` of ``expected``."""
    assert actual is not None, f"{key}: expected={expected}, got=None"
    a = float(actual)
    assert math.isfinite(a), f"{key}: expected={expected}, got non-finite {actual}"
    assert abs(a - float(expected)) <= tol, (
        f"{key}: expected={expected}, got={a} (tol={tol})"
    )


