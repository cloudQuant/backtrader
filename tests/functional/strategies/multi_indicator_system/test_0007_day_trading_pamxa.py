class DayTradingPamxaStrategy(bt.Strategy):
    """Day trading strategy using AO direction and stochastic regime transitions."""

    params = dict(
        fixed_lot=1.0,
        stop_loss_pips=50,
        take_profit_pips=50,
        trailing_stop_pips=25,
        trailing_step_pips=5,
        sto_k_period=5,
        sto_d_period=3,
        sto_slowing=3,
        sto_level_up=75,
        sto_level_down=25,
        point_size=0.01,
        verbose=False,
    )

    def __init__(self):
        """Initialize indicators, state variables, and counters."""
        self.data_m15 = self.datas[0]
        self.data_h1 = self.datas[1]
        self.data_d1 = self.datas[2]
        self.ao = bt.indicators.AwesomeOscillator(self.data_d1)
        self.sto = bt.indicators.Stochastic(self.data_h1, period=self.p.sto_k_period, period_dfast=self.p.sto_d_period, period_dslow=self.p.sto_slowing)
        self.last_bar_dt = None
        self.entry_order = None
        self.stop_order = None
        self.limit_order = None
        self.stop_price = None
        self.limit_price = None
        self.pending_direction = None
        self.bar_num = 0
        self.buy_count = 0
        self.sell_count = 0
        self.trade_count = 0
        self.win_count = 0
        self.loss_count = 0

    def log(self, text):
        """Conditionally print timestamped strategy messages."""
        if not self.p.verbose:
            return
        dt = bt.num2date(self.data_m15.datetime[0])
        print(f'{dt.isoformat()}, {text}')

    def _new_bar(self):
        current = bt.num2date(self.data_m15.datetime[0])
        if self.last_bar_dt == current:
            return False
        self.last_bar_dt = current
        return True

    def _cancel_exit_orders(self):
        if self.stop_order is not None:
            self.cancel(self.stop_order)
            self.stop_order = None
        if self.limit_order is not None:
            self.cancel(self.limit_order)
            self.limit_order = None
        self.stop_price = None
        self.limit_price = None

    def _place_exit_orders(self):
        if not self.position:
            return
        self._cancel_exit_orders()
        stop_distance = float(self.p.stop_loss_pips) * float(self.p.point_size)
        limit_distance = float(self.p.take_profit_pips) * float(self.p.point_size)
        size = abs(self.position.size)
        if self.position.size > 0:
            if stop_distance > 0:
                self.stop_price = self.position.price - stop_distance
                self.stop_order = self.sell(size=size, exectype=bt.Order.Stop, price=self.stop_price)
            if limit_distance > 0:
                self.limit_price = self.position.price + limit_distance
                self.limit_order = self.sell(size=size, exectype=bt.Order.Limit, price=self.limit_price, oco=self.stop_order)
        else:
            if stop_distance > 0:
                self.stop_price = self.position.price + stop_distance
                self.stop_order = self.buy(size=size, exectype=bt.Order.Stop, price=self.stop_price)
            if limit_distance > 0:
                self.limit_price = self.position.price - limit_distance
                self.limit_order = self.buy(size=size, exectype=bt.Order.Limit, price=self.limit_price, oco=self.stop_order)

    def _update_trailing_stop(self):
        if not self.position or self.p.trailing_stop_pips <= 0:
            return
        trailing_distance = float(self.p.trailing_stop_pips) * float(self.p.point_size)
        trailing_step = float(self.p.trailing_step_pips) * float(self.p.point_size)
        close_price = float(self.data_m15.close[0])
        if self.position.size > 0:
            if close_price - self.position.price <= trailing_distance + trailing_step:
                return
            candidate = close_price - trailing_distance
            if self.stop_price is None or candidate > self.stop_price + trailing_step:
                self.stop_price = candidate
                size = abs(self.position.size)
                if self.stop_order is not None:
                    self.cancel(self.stop_order)
                self.stop_order = self.sell(size=size, exectype=bt.Order.Stop, price=self.stop_price, oco=self.limit_order)
        else:
            if self.position.price - close_price <= trailing_distance + trailing_step:
                return
            candidate = close_price + trailing_distance
            if self.stop_price is None or candidate < self.stop_price - trailing_step:
                self.stop_price = candidate
                size = abs(self.position.size)
                if self.stop_order is not None:
                    self.cancel(self.stop_order)
                self.stop_order = self.buy(size=size, exectype=bt.Order.Stop, price=self.stop_price, oco=self.limit_order)

    def _signal_ready(self):
        """Return True when all required indicator histories are available."""
        return len(self.data_d1) >= 3 and len(self.data_h1) >= 2 and len(self.ao) >= 3 and len(self.sto.percK) >= 2

    def _check_signal(self):
        """Evaluate AO/stochastic rules and return ``buy``/``sell``/None."""
        ao_prev2 = float(self.ao[-2])
        ao_prev1 = float(self.ao[-1])
        sto_main = float(self.sto.percK[-1])
        sto_signal = float(self.sto.percD[-1])
        if ao_prev2 < 0.0 and ao_prev1 > 0.0:
            if sto_main < self.p.sto_level_down or sto_signal < self.p.sto_level_down:
                return 'buy'
        if ao_prev2 > 0.0 and ao_prev1 < 0.0:
            if sto_main > self.p.sto_level_up or sto_signal > self.p.sto_level_up:
                return 'sell'
        if sto_main < self.p.sto_level_down or sto_signal < self.p.sto_level_down:
            return 'buy'
        if sto_main > self.p.sto_level_up or sto_signal > self.p.sto_level_up:
            return 'sell'
        return None

    def next(self):
        """Advance counters and apply signal-driven entry/exit orchestration."""
        self.bar_num += 1
        if not self._new_bar():
            return
        if not self._signal_ready():
            return
        if self.entry_order is not None:
            return
        if self.pending_direction is not None:
            if self.position:
                if self.pending_direction == 'buy' and self.position.size < 0:
                    self._cancel_exit_orders()
                    self.close()
                    return
                if self.pending_direction == 'sell' and self.position.size > 0:
                    self._cancel_exit_orders()
                    self.close()
                    return
                self.pending_direction = None
            if not self.position:
                size = max(0.01, float(self.p.fixed_lot))
                if self.pending_direction == 'buy':
                    self.entry_order = self.buy(size=size)
                    self.buy_count += 1
                else:
                    self.entry_order = self.sell(size=size)
                    self.sell_count += 1
                self.log(f'OPEN {self.pending_direction.upper()}')
                self.pending_direction = None
                return
        if self.position:
            self._update_trailing_stop()
            return
        signal = self._check_signal()
        if signal is not None:
            self.pending_direction = signal
            size = max(0.01, float(self.p.fixed_lot))
            if signal == 'buy':
                self.entry_order = self.buy(size=size)
                self.buy_count += 1
            else:
                self.entry_order = self.sell(size=size)
                self.sell_count += 1
            self.log(f'OPEN {signal.upper()}')

    def notify_order(self, order):
        """Track entry and protective order lifecycle state transitions."""
        if order.status in [order.Submitted, order.Accepted]:
            return
        if order == self.entry_order:
            if order.status == order.Completed:
                self.entry_order = None
                self._place_exit_orders()
                return
            if order.status in [order.Canceled, order.Margin, order.Rejected]:
                self.entry_order = None
                return
        if order == self.stop_order:
            if order.status == order.Completed:
                self.stop_order = None
                self.limit_order = None
                self.stop_price = None
                self.limit_price = None
                return
            if order.status in [order.Canceled, order.Margin, order.Rejected]:
                self.stop_order = None
                return
        if order == self.limit_order:
            if order.status == order.Completed:
                self.limit_order = None
                self.stop_order = None
                self.stop_price = None
                self.limit_price = None
                return
            if order.status in [order.Canceled, order.Margin, order.Rejected]:
                self.limit_order = None

    def notify_trade(self, trade):
        """Update trade statistics when a trade is closed."""
        if not trade.isclosed:
            return
        self.trade_count += 1
        if trade.pnlcomm >= 0:
            self.win_count += 1
        else:
            self.loss_count += 1
        self._cancel_exit_orders()
        self.log(f'TRADE CLOSED pnl={trade.pnlcomm:.2f} net={self.broker.getvalue():.2f}')


