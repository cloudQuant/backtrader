class ThreeIndicatorsStrategy(bt.Strategy):
    """Three-indicator threshold strategy with long/short flip logic and trade stats.

    The strategy opens on strong directional consensus between candle direction,
    MACD slope, stochastic zone, and RSI zone, and tracks completed trade metrics
    via Backtrader analyzers and order/trade notifications.
    """

    params = dict(
        lots=1.0,
        work_timeframe='15min',
        macd_fast_period=11,
        macd_slow_period=53,
        macd_signal_period=26,
        macd_applied_price='close',
        sto_k_period=40,
        sto_d_period=23,
        sto_slowing=82,
        sto_ma_method='sma',
        sto_price_field='lowhigh',
        rsi_period=86,
        rsi_applied_price='close',
    )

    def __init__(self):
        """Initialize counters used for runtime assertions.

        Returns:
            None.
        """
        self.order = None
        self.bar_num = 0
        self.buy_count = 0
        self.sell_count = 0
        self.trade_count = 0
        self.win_count = 0
        self.loss_count = 0

    def log(self, text):
        """Print a timestamped log message.

        Args:
            text: Message payload.
        """
        dt = bt.num2date(self.data.datetime[0])
        print(f'{dt.isoformat()}, {text}')

    def next(self):
        """Increment the bar counter on every bar update."""
        self.bar_num += 1

    def next_open(self):
        """Evaluate and route position changes at each bar open."""
        if self.order:
            return
        if len(self.data) < 2:
            return

        buy_signal = bool(self.data.buy_signal[0])
        sell_signal = bool(self.data.sell_signal[0])
        target_size = None

        if not self.position:
            if buy_signal:
                target_size = self.p.lots
            if sell_signal:
                target_size = -self.p.lots
        elif self.position.size > 0 and sell_signal:
            target_size = -self.p.lots
        elif self.position.size < 0 and buy_signal:
            target_size = self.p.lots

        if target_size is None:
            return

        self.log(
            f'target={target_size:.2f} '
            f'candle={int(self.data.candle_signal[0])} '
            f'macd={int(self.data.macd_signal_flag[0])} '
            f'sto={int(self.data.sto_signal_flag[0])} '
            f'rsi={int(self.data.rsi_signal_flag[0])}'
        )
        self.order = self.order_target_size(target=target_size)

    def notify_order(self, order):
        """Update side counters when a submitted order completes.

        Args:
            order: Backtrader order instance.
        """
        if order.status in [order.Submitted, order.Accepted]:
            return
        if order.status == order.Completed:
            if self.position.size > 0:
                self.buy_count += 1
            elif self.position.size < 0:
                self.sell_count += 1
        self.order = None

    def notify_trade(self, trade):
        """Update trade outcome counters and log final PnL for closed trades.

        Args:
            trade: Backtrader trade object.
        """
        if not trade.isclosed:
            return
        self.trade_count += 1
        if trade.pnlcomm > 0:
            self.win_count += 1
        else:
            self.loss_count += 1
        self.log(f'trade closed pnl={trade.pnlcomm:.2f}')




