class HaramiMFIStrategy(bt.Strategy):
    """Strategy class implementing Harami pattern recognition with MFI filtering.

    Attributes:
        params (dict): Configured strategy parameters.
    """
    params = dict(
        mfi_period=37,
        ma_period=11,
        lot=0.1,
        point=0.01,
        price_digits=2,
        mfi_entry_long=30,
        mfi_entry_short=70,
        mfi_exit_upper=80,
        mfi_exit_lower=20,
    )

    def __init__(self):
        """Initialize indicators, backtest tracking metrics, and state variables."""
        self.mfi = bt.indicators.MFI(self.data, period=self.p.mfi_period)
        self.sma = bt.indicators.SMA(self.data.close, period=self.p.ma_period)
        self.bar_num = 0
        self.buy_count = 0
        self.sell_count = 0
        self.trade_count = 0
        self.win_count = 0
        self.loss_count = 0
        self._position_was_open = False

    def log(self, text):
        """Log message with current bar's timestamp.

        Args:
            text (str): Content string to log.
        """
        dt = bt.num2date(self.data.datetime[0])
        print(f'{dt.isoformat()}, {text}')

    def _avg_body(self):
        """Retrieve rolling average body size of recent bars.

        Returns:
            float: Average body size.
        """
        count = min(self.p.ma_period, len(self.data) - 1)
        if count <= 0:
            return 0.0
        total = 0.0
        for i in range(-count, 0):
            total += abs(float(self.data.close[i]) - float(self.data.open[i]))
        return total / count

    def _bullish_harami(self):
        """Verify bullish Harami pattern conditions on the last 2 bars.

        Returns:
            bool: True if bullish Harami is recognized, otherwise False.
        """
        if len(self.data) < 3:
            return False
        o2 = float(self.data.open[-2])
        c2 = float(self.data.close[-2])
        o1 = float(self.data.open[-1])
        c1 = float(self.data.close[-1])
        avg = self._avg_body()
        if avg <= 0:
            return False
        mid2 = (float(self.data.high[-2]) + float(self.data.low[-2])) / 2.0
        close_avg = float(self.sma[-2])
        return (
            c1 > o1 and
            (o2 - c2) > avg and
            c1 < o2 and
            o1 > c2 and
            mid2 < close_avg
        )

    def _bearish_harami(self):
        """Verify bearish Harami pattern conditions on the last 2 bars.

        Returns:
            bool: True if bearish Harami is recognized, otherwise False.
        """
        if len(self.data) < 3:
            return False
        o2 = float(self.data.open[-2])
        c2 = float(self.data.close[-2])
        o1 = float(self.data.open[-1])
        c1 = float(self.data.close[-1])
        avg = self._avg_body()
        if avg <= 0:
            return False
        mid2 = (float(self.data.high[-2]) + float(self.data.low[-2])) / 2.0
        close_avg = float(self.sma[-2])
        return (
            c1 < o1 and
            (c2 - o2) > avg and
            c1 > o2 and
            o1 < c2 and
            mid2 > close_avg
        )

    def next(self):
        """Execute the strategy decision logic on each new bar."""
        self.bar_num += 1
        if len(self.data) < max(self.p.mfi_period, self.p.ma_period) + 5:
            return

        mfi_1 = float(self.mfi.mfi[-1])
        mfi_2 = float(self.mfi.mfi[-2]) if len(self.mfi.mfi) > 2 else mfi_1
        bull = self._bullish_harami()
        bear = self._bearish_harami()

        if self.position:
            if self.position.size > 0:
                exit_long = ((mfi_1 > 70 and mfi_2 < 70) or (mfi_1 < 30 and mfi_2 > 30))
                if exit_long or (bear and mfi_1 > 60):
                    self.log(f'close long mfi={mfi_1:.2f}')
                    self.close()
                    if bear and mfi_1 > 60:
                        self.sell(size=self.p.lot)
                    return
            elif self.position.size < 0:
                exit_short = ((mfi_1 > 30 and mfi_2 < 30) or (mfi_1 > 70 and mfi_2 < 70))
                if exit_short or (bull and mfi_1 < 40):
                    self.log(f'close short mfi={mfi_1:.2f}')
                    self.close()
                    if bull and mfi_1 < 40:
                        self.buy(size=self.p.lot)
                    return
        else:
            if bull and mfi_1 < 40:
                self.log(f'buy harami mfi={mfi_1:.2f}')
                self.buy(size=self.p.lot)
                return
            if bear and mfi_1 > 60:
                self.log(f'sell harami mfi={mfi_1:.2f}')
                self.sell(size=self.p.lot)
                return

    def notify_trade(self, trade):
        """Callback to handle closed trades and manage win/loss counts.

        Args:
            trade (bt.Trade): The closed trade instance.
        """
        if trade.isopen and not self._position_was_open:
            if trade.size > 0:
                self.buy_count += 1
            elif trade.size < 0:
                self.sell_count += 1
            self._position_was_open = True
            return
        if not trade.isclosed:
            return
        self.trade_count += 1
        if trade.pnlcomm >= 0:
            self.win_count += 1
        else:
            self.loss_count += 1
        self._position_was_open = False
        self.log(f'trade closed pnl={trade.pnlcomm:.2f}')


