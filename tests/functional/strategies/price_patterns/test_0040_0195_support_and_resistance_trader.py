def load_backtest_frame(config):
    """Build the support/resistance signal frame for the backtest.

    Args:
        config: Parsed configuration whose ``data`` and ``params`` sections
            provide the file path, date range, and indicator settings.

    Returns:
        A dict with the signal ``data`` DataFrame and the ``fromdate`` and
        ``todate`` datetimes.

    Raises:
        ValueError: If the computed signal frame is empty.
    """
    data_cfg = config['data']
    params = config['params']
    fromdate = datetime.datetime.fromisoformat(data_cfg['fromdate'])
    todate = datetime.datetime.fromisoformat(data_cfg['todate'])
    frame = build_signal_frame(
        resolve_data_path(data_cfg['file']),
        symbol=data_cfg['symbol'],
        fromdate=fromdate,
        todate=todate,
        bar_shift_minutes=data_cfg.get('bar_shift_minutes', 15),
        ma_period1=params['ma_period1'],
        ma_period2=params['ma_period2'],
        cluster_period=params['cluster_period'],
        xau_cluster_period=params['xau_cluster_period'],
        resistance=params['resistance'],
        price_decimals=params['price_decimals'],
        near_threshold=params['near_threshold'],
    )
    if frame.empty:
        raise ValueError('Loaded signal frame is empty')
    print(f"Loaded {len(frame)} bars: {frame.index[0]} -> {frame.index[-1]}")
    return {'data': frame, 'fromdate': fromdate, 'todate': todate}


def build_cerebro(config, frame):
    """Assemble the Cerebro engine (cheat-on-open), feed, strategy, analyzers.

    Args:
        config: Parsed configuration providing backtest, data, and strategy
            parameter sections.
        frame: The loaded frame dict returned by :func:`load_backtest_frame`.

    Returns:
        A configured :class:`backtrader.Cerebro` with cheat_on_open enabled, the
        support/resistance signal feed, the strategy, and the default analyzers.
    """
    bt_cfg = config['backtest']
    cerebro = bt.Cerebro(stdstats=True, cheat_on_open=True)
    cerebro.broker.setcash(bt_cfg['initial_cash'])
    comm_type = bt.CommInfoBase.COMM_FIXED if bt_cfg.get('commission_type', 'fixed') == 'fixed' else bt.CommInfoBase.COMM_PERC
    cerebro.broker.setcommission(
        commission=bt_cfg['commission'],
        margin=bt_cfg['margin'],
        mult=bt_cfg['multiplier'],
        commtype=comm_type,
        stocklike=bt_cfg.get('stocklike', False),
    )
    feed = SupportResistanceFeed(dataname=frame['data'], timeframe=bt.TimeFrame.Minutes, compression=15)
    cerebro.adddata(feed, name=f"{config['data']['symbol']}_{config['data']['timeframe']}")
    cerebro.addstrategy(
        SupportAndResistanceTraderStrategy,
        lot=config['params']['lot'],
        stop_loss_points=config['params']['stop_loss_points'],
        take_profit_points=config['params']['take_profit_points'],
        point=config['params']['point'],
        ensure_trade_after_bars=config['params'].get('ensure_trade_after_bars', 0),
    )
    cerebro.addanalyzer(bt.analyzers.SharpeRatio, _name='sharpe', timeframe=bt.TimeFrame.Minutes, factor=MINUTES_PER_TRADING_YEAR, annualize=True, riskfreerate=0)
    cerebro.addanalyzer(bt.analyzers.Returns, _name='returns', timeframe=bt.TimeFrame.Minutes, compression=15, tann=MINUTES_PER_TRADING_YEAR)
    cerebro.addanalyzer(bt.analyzers.DrawDown, _name='drawdown')
    cerebro.addanalyzer(bt.analyzers.TradeAnalyzer, _name='trades')
    cerebro.addanalyzer(bt.analyzers.SQN, _name='sqn')
    return cerebro


def extract_metrics(strat, cerebro, frame, config):
    """Consolidate analyzer output and strategy counters into a metrics dict.

    Args:
        strat: The executed strategy instance carrying trade counters.
        cerebro: The Cerebro engine used to read final broker value.
        frame: The loaded frame dict providing the date range and bar count.
        config: Parsed configuration providing the initial cash.

    Returns:
        A dict of summary metrics (bar/trade counts, PnL, return, win rate,
        profit factor, drawdown, Sharpe, a clamped annualized return, and SQN).
    """
    sharpe = strat.analyzers.sharpe.get_analysis()
    returns = strat.analyzers.returns.get_analysis()
    drawdown = strat.analyzers.drawdown.get_analysis()
    trades = strat.analyzers.trades.get_analysis()
    sqn = strat.analyzers.sqn.get_analysis()
    rnorm = returns.get('rnorm')
    annual_return_pct = None
    if rnorm is not None and math.isfinite(rnorm):
        candidate_annual_return_pct = rnorm * 100
        if abs(candidate_annual_return_pct) <= 1000:
            annual_return_pct = candidate_annual_return_pct
    initial_cash = config['backtest']['initial_cash']
    final_value = cerebro.broker.getvalue()
    total_trades = trades.get('total', {}).get('total', 0)
    won = trades.get('won', {}).get('total', 0)
    lost = trades.get('lost', {}).get('total', 0)
    gross_win = trades.get('won', {}).get('pnl', {}).get('total', 0) or 0
    gross_loss = abs(trades.get('lost', {}).get('pnl', {}).get('total', 0) or 0)
    return {
        'fromdate': frame['fromdate'],
        'todate': frame['todate'],
        'bars': len(frame['data']),
        'bar_num': strat.bar_num,
        'buy_count': strat.buy_count,
        'sell_count': strat.sell_count,
        'trade_count': strat.trade_count,
        'initial_cash': initial_cash,
        'final_value': final_value,
        'net_pnl': final_value - initial_cash,
        'total_return_pct': (final_value / initial_cash - 1) * 100,
        'total_trades': total_trades,
        'won': won,
        'lost': lost,
        'win_rate': (won / total_trades * 100) if total_trades else 0,
        'profit_factor': (gross_win / gross_loss) if gross_loss else None,
        'max_drawdown': drawdown.get('max', {}).get('drawdown', 0),
        'sharpe_ratio': sharpe.get('sharperatio'),
        'annual_return_pct': annual_return_pct,
        'sqn': sqn.get('sqn'),
    }



def run(plot=False):
    """Run the full backtest end-to-end and return its results.

    Args:
        plot: If True, render the Cerebro plot after the run completes.

    Returns:
        A tuple of (results, metrics, cerebro) where results is the list of
        strategy instances, metrics is the extract_metrics() dict, and cerebro
        is the engine used for the run.
    """
    config = load_config()
    frame = load_backtest_frame(config)
    cerebro = build_cerebro(config, frame)
    print('\nStarting backtest...')
    results = cerebro.run()
    strat = results[0]
    metrics = extract_metrics(strat, cerebro, frame, config)

    if plot:
        cerebro.plot()
    return results, metrics, cerebro


def _close(actual, expected, *, tol, key):
    """Assert ``actual`` is finite and within ``tol`` of ``expected``."""
    assert actual is not None, f"{key}: expected={expected}, got=None"
    a = float(actual)
    assert math.isfinite(a), f"{key}: expected={expected}, got non-finite {actual}"
    assert abs(a - float(expected)) <= tol, (
        f"{key}: expected={expected}, got={a} (tol={tol})"
    )


def _invoke_strategy_main():
    """Call main() or run() depending on what the original script defined."""
    import sys as _sys
    _mod = _sys.modules[__name__]
    if hasattr(_mod, "main") and callable(_mod.main):
        return _mod.main()
