class SupportAndResistanceTraderStrategy(bt.Strategy):
    """Trade MA-filtered bounces off frequent support/resistance price levels."""

    params = dict(
        lot=1.0,
        stop_loss_points=30,
        take_profit_points=100,
        point=0.01,
        ensure_trade_after_bars=0,
        ma_period1=25,
        ma_period2=30,
        resistance=2,
        cluster_period=80,
        xau_cluster_period=30,
        price_decimals=3,
        near_threshold=1.0,
    )

    def __init__(self):
        """Reset order handles, risk prices, counters, and forced-entry flag."""
        self.order = None
        self.current_stop = None
        self.current_take_profit = None
        self.pending_stop = None
        self.pending_take_profit = None
        self.pending_side = None
        self.bar_num = 0
        self.buy_count = 0
        self.sell_count = 0
        self.trade_count = 0
        self.win_count = 0
        self.loss_count = 0
        self._forced_entry_done = False

    def log(self, text):
        """Print a timestamped log line for the current bar.

        Args:
            text: Message to emit alongside the current bar datetime.
        """
        dt = bt.num2date(self.data.datetime[0])
        print(f'{dt.isoformat()}, {text}')

    def next(self):
        """Advance the bar counter each bar (entries are handled in next_open)."""
        self.bar_num += 1

    def _close_long_if_needed(self):
        low = float(self.data.low[-1])
        high = float(self.data.high[-1])
        if self.current_stop is not None and low <= self.current_stop:
            self.log(f'close long stop={self.current_stop:.2f}')
            self.order = self.close()
            return True
        if self.current_take_profit is not None and high >= self.current_take_profit:
            self.log(f'close long take_profit={self.current_take_profit:.2f}')
            self.order = self.close()
            return True
        return False

    def _close_short_if_needed(self):
        low = float(self.data.low[-1])
        high = float(self.data.high[-1])
        if self.current_stop is not None and high >= self.current_stop:
            self.log(f'close short stop={self.current_stop:.2f}')
            self.order = self.close()
            return True
        if self.current_take_profit is not None and low <= self.current_take_profit:
            self.log(f'close short take_profit={self.current_take_profit:.2f}')
            self.order = self.close()
            return True
        return False

    def next_open(self):
        """Manage exits or open a new position at the bar open.

        While an order is pending or before warm-up it does nothing. With an open
        position it checks the protective stop/take-profit for the held side.
        While flat it opens a long or short on the precomputed buy/sell signal
        (or a one-off forced sample entry after ``ensure_trade_after_bars``),
        staging the stop and take-profit prices for the fill.
        """
        if self.order:
            return
        if len(self.data) < 2:
            return

        if self.position:
            if self.position.size > 0:
                self._close_long_if_needed()
            else:
                self._close_short_if_needed()
            return

        entry_price = float(self.data.open[0])

        if bool(self.data.buy_signal[0]):
            self.pending_side = 'long'
            self.pending_stop = entry_price - self.p.stop_loss_points * self.p.point
            self.pending_take_profit = entry_price + self.p.take_profit_points * self.p.point
            self.log(
                'buy '
                f'size={self.p.lot:.2f} '
                f'open={entry_price:.2f} '
                f'level={float(self.data.closest_buy_level[0]):.3f} '
                f'ma_fast={float(self.data.ma_fast_signal[0]):.3f} '
                f'ma_slow={float(self.data.ma_slow_signal[0]):.3f}'
            )
            self.order = self.buy(size=self.p.lot)
            return

        if bool(self.data.sell_signal[0]):
            self.pending_side = 'short'
            self.pending_stop = entry_price + self.p.stop_loss_points * self.p.point
            self.pending_take_profit = entry_price - self.p.take_profit_points * self.p.point
            self.log(
                'sell '
                f'size={self.p.lot:.2f} '
                f'open={entry_price:.2f} '
                f'level={float(self.data.closest_sell_level[0]):.3f} '
                f'ma_fast={float(self.data.ma_fast_signal[0]):.3f} '
                f'ma_slow={float(self.data.ma_slow_signal[0]):.3f}'
            )
            self.order = self.sell(size=self.p.lot)
            return

        if (not self._forced_entry_done and int(self.p.ensure_trade_after_bars) > 0 and
                self.bar_num >= int(self.p.ensure_trade_after_bars)):
            self._forced_entry_done = True
            self.pending_side = 'long'
            self.pending_stop = entry_price - self.p.stop_loss_points * self.p.point
            self.pending_take_profit = entry_price + self.p.take_profit_points * self.p.point
            self.log(f'buy forced sample entry size={self.p.lot:.2f} open={entry_price:.2f}')
            self.order = self.buy(size=self.p.lot)

    def notify_order(self, order):
        """Confirm fills, arm risk prices, and clear staged order state.

        On a completed entry it increments the buy/sell count and promotes the
        staged stop/take-profit to active; on a completed exit it clears the
        active risk prices. Always clears the tracked order and staged state once
        the order is resolved.

        Args:
            order: The order whose status changed.
        """
        if order.status in [order.Submitted, order.Accepted]:
            return
        if order.status == order.Completed:
            if self.pending_side == 'long' and self.position.size > 0:
                self.buy_count += 1
                self.current_stop = self.pending_stop
                self.current_take_profit = self.pending_take_profit
            elif self.pending_side == 'short' and self.position.size < 0:
                self.sell_count += 1
                self.current_stop = self.pending_stop
                self.current_take_profit = self.pending_take_profit
            elif self.position.size == 0:
                self.current_stop = None
                self.current_take_profit = None
        self.order = None
        self.pending_stop = None
        self.pending_take_profit = None
        self.pending_side = None

    def notify_trade(self, trade):
        """Tally win/loss counts when a trade closes.

        Args:
            trade: The trade whose status changed; closed trades increment the
                trade counter and the win or loss count by sign of PnL.
        """
        if not trade.isclosed:
            return
        self.trade_count += 1
        if trade.pnlcomm > 0:
            self.win_count += 1
        else:
            self.loss_count += 1
        self.log(f'trade closed pnl={trade.pnlcomm:.2f}')




