class MeetingLinesRsiStrategy(bt.Strategy):
    """Strategy class implementing Meeting Lines pattern recognition and RSI filtering.

    Attributes:
        params (dict): Configured strategy parameters.
    """
    params = dict(
        rsi_period=11,
        rsi_entry_long=40,
        rsi_entry_short=60,
        rsi_exit_upper=70,
        rsi_exit_lower=30,
        ma_period=3,
        lot=0.1,
        point=0.01,
        price_digits=2,
        body_multiplier=1.0,
        close_tolerance_multiplier=0.1,
    )

    def __init__(self):
        """Initialize indicators, backtest tracking metrics, and state variables."""
        self.rsi = bt.indicators.RSI(self.data.close, period=self.p.rsi_period)
        self.body_sma = bt.indicators.SMA(abs(self.data.close - self.data.open), period=self.p.ma_period)
        self.bar_num = 0
        self.buy_count = 0
        self.sell_count = 0
        self.trade_count = 0
        self.win_count = 0
        self.loss_count = 0
        self._position_was_open = False

    def log(self, text):
        """Log message with current bar's timestamp.

        Args:
            text (str): Content string to log.
        """
        dt = bt.num2date(self.data.datetime[0])
        print(f'{dt.isoformat()}, {text}')

    def _avg_body(self):
        """Retrieve rolling average body size of recent bars.

        Returns:
            float: Average body size.
        """
        return float(self.body_sma[-1])

    def _is_bullish_meeting_lines(self):
        """Verify bullish Meeting Lines pattern conditions on the last 2 bars.

        Returns:
            bool: True if bullish meeting lines pattern is formed, otherwise False.
        """
        o2, c2 = float(self.data.open[-2]), float(self.data.close[-2])
        o1, c1 = float(self.data.open[-1]), float(self.data.close[-1])
        avg = self._avg_body()
        if avg < self.p.point:
            return False
        return ((o2 - c2) > float(self.p.body_multiplier) * avg and
                (c1 - o1) > float(self.p.body_multiplier) * avg and
                abs(c1 - c2) < float(self.p.close_tolerance_multiplier) * avg)

    def _is_bearish_meeting_lines(self):
        """Verify bearish Meeting Lines pattern conditions on the last 2 bars.

        Returns:
            bool: True if bearish meeting lines pattern is formed, otherwise False.
        """
        o2, c2 = float(self.data.open[-2]), float(self.data.close[-2])
        o1, c1 = float(self.data.open[-1]), float(self.data.close[-1])
        avg = self._avg_body()
        if avg < self.p.point:
            return False
        return ((c2 - o2) > float(self.p.body_multiplier) * avg and
                (o1 - c1) > float(self.p.body_multiplier) * avg and
                abs(c1 - c2) < float(self.p.close_tolerance_multiplier) * avg)

    def next(self):
        """Execute the strategy decision logic on each new bar."""
        self.bar_num += 1
        warmup = max(self.p.rsi_period, self.p.ma_period) + 5
        if len(self.data) < warmup:
            return

        rsi0 = float(self.rsi[0])
        rsi1 = float(self.rsi[-1])

        if self.position:
            if self.position.size > 0:
                if ((rsi0 < self.p.rsi_exit_upper and rsi1 > self.p.rsi_exit_upper) or
                    (rsi0 < self.p.rsi_exit_lower and rsi1 > self.p.rsi_exit_lower)):
                    self.log(f'close long rsi={rsi0:.1f}')
                    self.close()
                    return
            elif self.position.size < 0:
                if ((rsi0 > self.p.rsi_exit_lower and rsi1 < self.p.rsi_exit_lower) or
                    (rsi0 > self.p.rsi_exit_upper and rsi1 < self.p.rsi_exit_upper)):
                    self.log(f'close short rsi={rsi0:.1f}')
                    self.close()
                    return
        else:
            if self._is_bullish_meeting_lines() and rsi1 < self.p.rsi_entry_long:
                self.log(f'buy meeting_lines+rsi rsi={rsi1:.1f}')
                self.buy(size=self.p.lot)
                return
            if self._is_bearish_meeting_lines() and rsi1 > self.p.rsi_entry_short:
                self.log(f'sell meeting_lines+rsi rsi={rsi1:.1f}')
                self.sell(size=self.p.lot)
                return

    def notify_trade(self, trade):
        """Callback to handle closed trades and manage win/loss counts.

        Args:
            trade (bt.Trade): The closed trade instance.
        """
        if trade.isopen and not self._position_was_open:
            if trade.size > 0:
                self.buy_count += 1
            elif trade.size < 0:
                self.sell_count += 1
            self._position_was_open = True
            return
        if not trade.isclosed:
            return
        self.trade_count += 1
        if trade.pnlcomm >= 0:
            self.win_count += 1
        else:
            self.loss_count += 1
        self._position_was_open = False
        self.log(f'trade closed pnl={trade.pnlcomm:.2f}')



