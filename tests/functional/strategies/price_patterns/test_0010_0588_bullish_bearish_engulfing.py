class BullishBearishEngulfingStrategy(bt.Strategy):
    """Strategy class implementing Bullish and Bearish Engulfing pattern breakout logic.

    Attributes:
        params (dict): Configured strategy parameters.
    """
    params = dict(
        bullish_engulfing='buy',
        bearish_engulfing='sell',
        shift=1,
        distance=0,
        opposite_signal=True,
        lots=0.1,
        point=0.01,
        price_digits=2,
    )

    def __init__(self):
        """Initialize indicators, backtest tracking metrics, and state variables."""
        self.bar_num = 0
        self.signal_count = 0
        self.buy_count = 0
        self.sell_count = 0
        self.trade_count = 0
        self.win_count = 0
        self.loss_count = 0
        self.completed_order_count = 0
        self.rejected_order_count = 0
        self.order = None
        self.pending_direction = None

    def _point(self):
        """Calculate and retrieve floating point size multiplier.

        Returns:
            float: Fixed point size.
        """
        return float(self.p.point)

    def next(self):
        """Execute the strategy decision logic on each new bar."""
        self.bar_num += 1
        s = int(self.p.shift)
        if len(self) < s + 3:
            return
        if self.order is not None:
            return

        if not self.position and self.pending_direction is not None:
            self.signal_count += 1
            if self.pending_direction == 'buy':
                self.order = self.buy(size=self.p.lots)
            else:
                self.order = self.sell(size=self.p.lots)
            self.pending_direction = None
            return

        c0_open = float(self.data.open[-s])
        c0_close = float(self.data.close[-s])
        c0_high = float(self.data.high[-s])
        c0_low = float(self.data.low[-s])
        c1_open = float(self.data.open[-(s + 1)])
        c1_close = float(self.data.close[-(s + 1)])
        c1_high = float(self.data.high[-(s + 1)])
        c1_low = float(self.data.low[-(s + 1)])
        dist = float(self.p.distance) * self._point()

        bullish = (
            c0_open < c0_close and
            c1_open > c1_close and
            c0_high > c1_high + dist and
            c0_close > c1_open + dist and
            c0_open < c1_close - dist and
            c0_low < c1_low - dist
        )
        bearish = (
            c0_open > c0_close and
            c1_open < c1_close and
            c0_high > c1_high + dist and
            c0_open > c1_close + dist and
            c0_close < c1_open - dist and
            c0_low < c1_low - dist
        )

        if bullish:
            direction = str(self.p.bullish_engulfing).lower()
            if self.p.opposite_signal:
                desired = 'sell' if direction == 'buy' else 'buy'
                if self.position and ((desired == 'buy' and self.position.size < 0) or (desired == 'sell' and self.position.size > 0)):
                    self.pending_direction = desired
                    self.order = self.close()
                    return
                if not self.position:
                    self.signal_count += 1
                    self.order = self.buy(size=self.p.lots) if desired == 'buy' else self.sell(size=self.p.lots)
                    return
            else:
                if self.position.size < 0:
                    self.pending_direction = 'buy'
                    self.order = self.close()
                    return
                if not self.position:
                    self.signal_count += 1
                    self.order = self.buy(size=self.p.lots)
                    return

        if bearish:
            direction = str(self.p.bearish_engulfing).lower()
            if self.p.opposite_signal:
                desired = 'sell' if direction == 'sell' else 'buy'
                if self.position and ((desired == 'buy' and self.position.size < 0) or (desired == 'sell' and self.position.size > 0)):
                    self.pending_direction = desired
                    self.order = self.close()
                    return
                if not self.position:
                    self.signal_count += 1
                    self.order = self.buy(size=self.p.lots) if desired == 'buy' else self.sell(size=self.p.lots)
                    return
            else:
                if self.position.size > 0:
                    self.pending_direction = 'sell'
                    self.order = self.close()
                    return
                if not self.position:
                    self.signal_count += 1
                    self.order = self.sell(size=self.p.lots)

    def notify_order(self, order):
        """Callback to handle order status updates.

        Args:
            order (bt.Order): The updated order instance.
        """
        if order.status in [bt.Order.Submitted, bt.Order.Accepted]:
            return
        if order.status == bt.Order.Completed:
            self.completed_order_count += 1
            if self.position:
                if order.executed.size > 0:
                    self.buy_count += 1
                elif order.executed.size < 0:
                    self.sell_count += 1
        elif order.status in [bt.Order.Canceled, bt.Order.Margin, bt.Order.Rejected, bt.Order.Expired]:
            self.rejected_order_count += 1
        if self.order is not None and order.ref == self.order.ref and order.status not in [bt.Order.Submitted, bt.Order.Accepted]:
            self.order = None

    def notify_trade(self, trade):
        """Callback to handle closed trades and manage win/loss counts.

        Args:
            trade (bt.Trade): The closed trade instance.
        """
        if not trade.isclosed:
            return
        self.trade_count += 1
        if trade.pnlcomm >= 0:
            self.win_count += 1
        else:
            self.loss_count += 1


