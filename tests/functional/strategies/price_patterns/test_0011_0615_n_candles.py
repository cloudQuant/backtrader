class Mt5PandasFeed(bt.feeds.PandasData):
    """PandasData feed mapping the standard OHLCV column positions."""

    params = (
        ('datetime', None), ('open', 0), ('high', 1), ('low', 2), ('close', 3), ('volume', 4), ('openinterest', 5),
    )


class NCandlesStrategy(bt.Strategy):
    """N consecutive same-direction candles strategy (EA 0615).

    If the last N completed bars are all bullish -> buy.
    If the last N completed bars are all bearish -> sell.
    No SL/TP, no position management beyond entry.
    """

    params = dict(
        n_candles=3,
        lots=0.01,
        point=0.01,
        price_digits=2,
    )

    def __init__(self):
        """Initialize bar, signal, order, and trade-statistics counters."""
        self.bar_num = 0
        self.signal_count = 0
        self.buy_count = 0
        self.sell_count = 0
        self.trade_count = 0
        self.win_count = 0
        self.loss_count = 0
        self.completed_order_count = 0
        self.rejected_order_count = 0
        self.order = None

    def next(self):
        """Buy/sell when the last N completed candles share one direction."""
        self.bar_num += 1
        n = int(self.p.n_candles)
        if len(self) < n + 2:
            return
        if self.order is not None:
            return

        all_bull = True
        all_bear = True
        for i in range(1, n + 1):
            o = float(self.data.open[-i])
            c = float(self.data.close[-i])
            if o >= c:
                all_bull = False
            if o <= c:
                all_bear = False

        if not all_bull and not all_bear:
            return

        self.signal_count += 1
        if all_bull:
            self.order = self.buy(size=self.p.lots)
        elif all_bear:
            self.order = self.sell(size=self.p.lots)

    def notify_order(self, order):
        """Update fill/rejection counters and clear the order reference.

        Args:
            order: The order whose status changed.
        """
        if order.status in [bt.Order.Submitted, bt.Order.Accepted]:
            return
        if order.status == bt.Order.Completed:
            self.completed_order_count += 1
            if order.executed.size > 0:
                self.buy_count += 1
            elif order.executed.size < 0:
                self.sell_count += 1
        elif order.status in [bt.Order.Canceled, bt.Order.Margin, bt.Order.Rejected, bt.Order.Expired]:
            self.rejected_order_count += 1
        if self.order is not None and order.ref == self.order.ref and order.status not in [bt.Order.Submitted, bt.Order.Accepted]:
            self.order = None

    def notify_trade(self, trade):
        """Tally closed trades into win/loss counters.

        Args:
            trade: The trade whose status changed; only closed trades count.
        """
        if not trade.isclosed:
            return
        self.trade_count += 1
        if trade.pnlcomm >= 0:
            self.win_count += 1
        else:
            self.loss_count += 1


