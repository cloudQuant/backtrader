class Exp3LineBreakStrategy(bt.Strategy):
    """Strategy that trades XAUUSD based on higher-timeframe 3-line-break reversals.

    Risk model and order handling are fixed at initialization-time parameters:
    opposite-signal reversal close-out, fixed position size and point-based
    stop-loss / take-profit checks before each new entry decision.

    The strategy subscribes to two feeds:
    - base feed (M15): execution and order fills.
    - signal feed (H12): trend input for signal timing.
    """

    params = dict(
        lines_break=3,
        signal_bar=1,
        stop_loss_points=1000,
        take_profit_points=2000,
        fixed_lot=0.1,
        point=0.0001,
        buy_pos_open=True,
        sell_pos_open=True,
        buy_pos_close=True,
        sell_pos_close=True,
        indicator_minutes=720,
    )

    def __init__(self):
        """Initialize strategy state and bind base/signal datasets/indicator.

        Creates counters used by regression assertions and a trend indicator wired to
        the signal feed.
        """
        self.base = self.datas[0]
        self.signal_data = self.datas[1]
        self.indicator = bt.indicators.ThreeLineBreakIndicator(self.signal_data, lines_break=self.p.lines_break)
        self.bar_num = 0
        self.signal_count = 0
        self.buy_count = 0
        self.sell_count = 0
        self.trade_count = 0
        self.win_count = 0
        self.loss_count = 0
        self._position_was_open = False
        self._last_signal_len = 0

    def log(self, text):
        """Print a timestamped execution log entry.

        Args:
            text: Human-readable log message for backtest diagnostics.
        """
        dt = bt.num2date(self.base.datetime[0])
        print(f'{dt.isoformat()}, {text}')

    def _check_exit_levels(self):
        if not self.position:
            return False
        close_price = float(self.base.close[0])
        point_value = float(self.p.point)
        stop_distance = self.p.stop_loss_points * point_value if self.p.stop_loss_points > 0 else None
        take_distance = self.p.take_profit_points * point_value if self.p.take_profit_points > 0 else None
        entry_price = float(self.position.price)

        if self.position.size > 0:
            if stop_distance is not None and close_price <= entry_price - stop_distance:
                self.log(f'close long by stop loss close={close_price:.5f} entry={entry_price:.5f}')
                self.close()
                return True
            if take_distance is not None and close_price >= entry_price + take_distance:
                self.log(f'close long by take profit close={close_price:.5f} entry={entry_price:.5f}')
                self.close()
                return True
        elif self.position.size < 0:
            if stop_distance is not None and close_price >= entry_price + stop_distance:
                self.log(f'close short by stop loss close={close_price:.5f} entry={entry_price:.5f}')
                self.close()
                return True
            if take_distance is not None and close_price <= entry_price - take_distance:
                self.log(f'close short by take profit close={close_price:.5f} entry={entry_price:.5f}')
                self.close()
                return True
        return False

    def next(self):
        """Evaluate exits then apply trend flips into long/short entries.

        The method:
        1. skips until sufficient base signal history exists,
        2. enforces fixed risk exits,
        3. detects the latest trend transition from the indicator,
        4. closes opposing positions when configured,
        5. opens a new position in the reversal direction.
        """
        self.bar_num += 1
        if len(self.base) < 2:
            return

        if self._check_exit_levels():
            return

        current_ago = max(int(self.p.signal_bar) - 1, 0)
        prev_ago = current_ago + 1
        if len(self.signal_data) < int(self.p.lines_break) + prev_ago + 3:
            return

        current_signal_len = len(self.signal_data)
        if current_signal_len == self._last_signal_len:
            return
        self._last_signal_len = current_signal_len

        prev_trend = float(self.indicator.trend[-prev_ago])
        curr_trend = float(self.indicator.trend[-current_ago]) if current_ago else float(self.indicator.trend[0])
        if not math.isfinite(prev_trend) or not math.isfinite(curr_trend):
            return

        close_price = float(self.base.close[0])
        size = float(self.p.fixed_lot)
        if size <= 0:
            return

        if self.position.size < 0 and curr_trend == 0.0 and self.p.sell_pos_close:
            self.log(f'close short by trend={curr_trend} close={close_price:.5f}')
            self.close()
        if self.position.size > 0 and curr_trend == 1.0 and self.p.buy_pos_close:
            self.log(f'close long by trend={curr_trend} close={close_price:.5f}')
            self.close()

        if prev_trend == 1.0 and curr_trend == 0.0:
            self.signal_count += 1
            self.log(f'buy signal trend={prev_trend}->{curr_trend} close={close_price:.5f}')
            if self.position.size <= 0 and self.p.buy_pos_open:
                self.buy(size=size)
            return

        if prev_trend == 0.0 and curr_trend == 1.0:
            self.signal_count += 1
            self.log(f'sell signal trend={prev_trend}->{curr_trend} close={close_price:.5f}')
            if self.position.size >= 0 and self.p.sell_pos_open:
                self.sell(size=size)

    def notify_trade(self, trade):
        """Track trade lifecycle counters and classify win/loss outcomes.

        Args:
            trade: Trade notification object dispatched by Backtrader.
        """
        if trade.isopen and not self._position_was_open:
            if trade.size > 0:
                self.buy_count += 1
            elif trade.size < 0:
                self.sell_count += 1
            self._position_was_open = True
            return
        if not trade.isclosed:
            return
        self.trade_count += 1
        if trade.pnlcomm >= 0:
            self.win_count += 1
        else:
            self.loss_count += 1
        self._position_was_open = False
        self.log(f'trade closed pnl={trade.pnlcomm:.2f}')


