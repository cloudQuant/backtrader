class SimpleThreeInsidePatternStrategy(bt.Strategy):
    """Trade bullish/bearish three-inside candlestick reversals with brackets."""

    params = dict(
        stop_loss=500,
        take_profit=500,
        lot=0.1,
        magic_number=3119,
        point_size=0.01,
        lot_min=0.01,
        lot_max=100.0,
        lot_step=0.01,
    )

    def __init__(self):
        """Reset order handles, bracket trackers, and trade counters."""
        self.entry_order = None
        self.stop_order = None
        self.limit_order = None
        self.pending_orders = []
        self.bar_num = 0
        self.buy_count = 0
        self.sell_count = 0
        self.trade_count = 0
        self.win_count = 0
        self.loss_count = 0

    def log(self, text):
        """Print a timestamped log line for the current bar.

        Args:
            text: Message to emit alongside the current bar datetime.
        """
        dt = bt.num2date(self.data.datetime[0])
        print(f'{dt.isoformat()}, {text}')

    def _normalize_lot(self):
        lot = min(max(self.p.lot, self.p.lot_min), self.p.lot_max)
        lot = int(lot / self.p.lot_step) * self.p.lot_step
        return round(max(lot, self.p.lot_min), 4)

    def bullish_three_inside(self):
        """Return True if the last three bars form a bullish three-inside-up.

        Returns:
            bool: True when a bearish first bar, an inside bullish middle bar,
            and a confirming bullish bar that closes above the first bar's high
            are all present.
        """
        older_open = float(self.data.open[-2])
        older_close = float(self.data.close[-2])
        older_high = float(self.data.high[-2])
        older_low = float(self.data.low[-2])
        middle_open = float(self.data.open[-1])
        middle_close = float(self.data.close[-1])
        latest_open = float(self.data.open[0])
        latest_close = float(self.data.close[0])
        return (
            older_open > older_close and
            middle_open < middle_close and
            middle_open > older_low and
            middle_close < older_high and
            latest_open < latest_close and
            latest_open > middle_open and
            latest_open < middle_close and
            latest_close > older_high
        )

    def bearish_three_inside(self):
        """Return True if the last three bars form a bearish three-inside-down.

        Returns:
            bool: True when a bullish first bar, an inside bearish middle bar,
            and a confirming bearish bar that closes below the first bar's low
            are all present.
        """
        older_open = float(self.data.open[-2])
        older_close = float(self.data.close[-2])
        older_high = float(self.data.high[-2])
        older_low = float(self.data.low[-2])
        middle_open = float(self.data.open[-1])
        middle_close = float(self.data.close[-1])
        latest_open = float(self.data.open[0])
        latest_close = float(self.data.close[0])
        return (
            older_open < older_close and
            middle_open > middle_close and
            middle_open < older_high and
            middle_close > older_low and
            latest_open > latest_close and
            latest_open < middle_open and
            latest_open > middle_close and
            latest_close < older_low
        )

    def next(self):
        """Detect a three-inside pattern while flat and open a bracketed trade.

        Increments the bar counter, skips during warm-up or when a position or
        entry order is already live, then on a bullish pattern submits a
        buy_bracket and on a bearish pattern submits a sell_bracket sized by the
        normalized lot, with stop-loss and take-profit legs offset by the
        configured point distances.
        """
        self.bar_num += 1
        if len(self.data) < 3:
            return
        if self.position or self.entry_order is not None:
            return
        size = self._normalize_lot()
        if self.bullish_three_inside():
            close_price = float(self.data.close[0])
            sl = close_price - self.p.stop_loss * self.p.point_size
            tp = close_price + self.p.take_profit * self.p.point_size
            orders = self.buy_bracket(size=size, stopprice=sl, limitprice=tp)
            self.entry_order, self.stop_order, self.limit_order = orders
            self.pending_orders = list(orders)
            self.entry_order.addinfo(kind='entry_long')
            self.buy_count += 1
            return
        if self.bearish_three_inside():
            close_price = float(self.data.close[0])
            sl = close_price + self.p.stop_loss * self.p.point_size
            tp = close_price - self.p.take_profit * self.p.point_size
            orders = self.sell_bracket(size=size, stopprice=sl, limitprice=tp)
            self.entry_order, self.stop_order, self.limit_order = orders
            self.pending_orders = list(orders)
            self.entry_order.addinfo(kind='entry_short')
            self.sell_count += 1

    def notify_order(self, order):
        """Log bracket entry fills and clear completed or dead bracket legs.

        Args:
            order: The order whose status changed; completed entry legs are
                logged with their stop/limit prices, and any non-alive entry,
                stop, or limit leg is cleared from the tracked handles.
        """
        if order.status in (order.Submitted, order.Accepted):
            return
        kind = getattr(order.info, 'kind', None)
        if order.status == order.Completed:
            if kind == 'entry_long':
                entry_price = order.executed.price
                sl = self.stop_order.created.price if self.stop_order is not None else float('nan')
                tp = self.limit_order.created.price if self.limit_order is not None else float('nan')
                self.log(f'long entry price={entry_price:.2f} sl={sl:.2f} tp={tp:.2f}')
            elif kind == 'entry_short':
                entry_price = order.executed.price
                sl = self.stop_order.created.price if self.stop_order is not None else float('nan')
                tp = self.limit_order.created.price if self.limit_order is not None else float('nan')
                self.log(f'short entry price={entry_price:.2f} sl={sl:.2f} tp={tp:.2f}')
        else:
            pass

        if order in self.pending_orders and not order.alive():
            self.pending_orders = [o for o in self.pending_orders if o is not order and o.alive()]
        if order is self.entry_order and not order.alive():
            self.entry_order = None
        if order is self.stop_order and not order.alive():
            self.stop_order = None
        if order is self.limit_order and not order.alive():
            self.limit_order = None

    def notify_trade(self, trade):
        """Tally win/loss on close and reset bracket/order trackers.

        Args:
            trade: The trade whose status changed; closed trades increment the
                trade counter and the win or loss count, then all entry/stop/
                limit handles and the pending-order list are cleared.
        """
        if not trade.isclosed:
            return
        self.trade_count += 1
        if trade.pnlcomm >= 0:
            self.win_count += 1
        else:
            self.loss_count += 1
        self.log(f'trade closed pnl={trade.pnlcomm:.2f}')
        self.entry_order = None
        self.stop_order = None
        self.limit_order = None
        self.pending_orders = []





