class EngulfingRSIStrategy(bt.Strategy):
    """Strategy class implementing Bullish/Bearish Engulfing pattern recognition with RSI filtering.

    Attributes:
        params (dict): Configured strategy parameters.
    """
    params = dict(
        rsi_period=11,
        ma_period=5,
        lot=0.1,
        point=0.01,
        price_digits=2,
        rsi_entry_long=40,
        rsi_entry_short=60,
        rsi_exit_upper=70,
        rsi_exit_lower=30,
    )

    def __init__(self):
        """Initialize indicators, backtest tracking metrics, and state variables."""
        self.rsi = bt.indicators.RSI(self.data.close, period=self.p.rsi_period)
        self.sma = bt.indicators.SMA(self.data.close, period=self.p.ma_period)
        self.bar_num = 0
        self.buy_count = 0
        self.sell_count = 0
        self.trade_count = 0
        self.win_count = 0
        self.loss_count = 0
        self._position_was_open = False

    def log(self, text):
        """Log message with current bar's timestamp.

        Args:
            text (str): Content string to log.
        """
        dt = bt.num2date(self.data.datetime[0])
        print(f'{dt.isoformat()}, {text}')

    def _avg_body(self):
        """Retrieve rolling average body size of recent bars.

        Returns:
            float: Average body size.
        """
        count = min(self.p.ma_period, len(self.data) - 1)
        if count <= 0:
            return 0.0
        total = 0.0
        for i in range(-count, 0):
            total += abs(float(self.data.close[i]) - float(self.data.open[i]))
        return total / count

    def _bullish_engulfing(self):
        """Verify bullish Engulfing pattern conditions on the last 2 bars.

        Returns:
            bool: True if bullish Engulfing is recognized, otherwise False.
        """
        if len(self.data) < 3:
            return False
        o2 = float(self.data.open[-2])
        c2 = float(self.data.close[-2])
        o1 = float(self.data.open[-1])
        c1 = float(self.data.close[-1])
        avg = self._avg_body()
        if avg <= 0:
            return False
        mid2 = (o2 + c2) / 2.0
        close_avg = float(self.sma[-2])
        return (
            o2 > c2 and
            (c1 - o1) > avg and
            c1 > o2 and
            mid2 < close_avg and
            o1 < c2
        )

    def _bearish_engulfing(self):
        """Verify bearish Engulfing pattern conditions on the last 2 bars.

        Returns:
            bool: True if bearish Engulfing is recognized, otherwise False.
        """
        if len(self.data) < 3:
            return False
        o2 = float(self.data.open[-2])
        c2 = float(self.data.close[-2])
        o1 = float(self.data.open[-1])
        c1 = float(self.data.close[-1])
        avg = self._avg_body()
        if avg <= 0:
            return False
        mid2 = (o2 + c2) / 2.0
        close_avg = float(self.sma[-2])
        return (
            o2 < c2 and
            (o1 - c1) > avg and
            c1 < o2 and
            mid2 > close_avg and
            o1 > c2
        )

    def next(self):
        """Execute the strategy decision logic on each new bar."""
        self.bar_num += 1
        if len(self.data) < max(self.p.rsi_period, self.p.ma_period) + 5:
            return

        rsi_1 = float(self.rsi[-1])
        rsi_2 = float(self.rsi[-2]) if len(self.rsi) > 2 else rsi_1
        bull_eng = self._bullish_engulfing()
        bear_eng = self._bearish_engulfing()

        if self.position:
            if self.position.size > 0:
                exit_long = ((rsi_1 < 70 and rsi_2 > 70) or (rsi_1 < 30 and rsi_2 > 30))
                if exit_long or (bear_eng and rsi_1 > 60):
                    self.log(f'close long rsi={rsi_1:.2f}')
                    self.close()
                    if bear_eng and rsi_1 > 60:
                        self.sell(size=self.p.lot)
                    return
            elif self.position.size < 0:
                exit_short = ((rsi_1 > 30 and rsi_2 < 30) or (rsi_1 > 70 and rsi_2 < 70))
                if exit_short or (bull_eng and rsi_1 < 40):
                    self.log(f'close short rsi={rsi_1:.2f}')
                    self.close()
                    if bull_eng and rsi_1 < 40:
                        self.buy(size=self.p.lot)
                    return
        else:
            if bull_eng and rsi_1 < 40:
                self.log(f'buy engulfing rsi={rsi_1:.2f}')
                self.buy(size=self.p.lot)
                return
            if bear_eng and rsi_1 > 60:
                self.log(f'sell engulfing rsi={rsi_1:.2f}')
                self.sell(size=self.p.lot)
                return

    def notify_trade(self, trade):
        """Callback to handle closed trades and manage win/loss counts.

        Args:
            trade (bt.Trade): The closed trade instance.
        """
        if trade.isopen and not self._position_was_open:
            if trade.size > 0:
                self.buy_count += 1
            elif trade.size < 0:
                self.sell_count += 1
            self._position_was_open = True
            return
        if not trade.isclosed:
            return
        self.trade_count += 1
        if trade.pnlcomm >= 0:
            self.win_count += 1
        else:
            self.loss_count += 1
        self._position_was_open = False
        self.log(f'trade closed pnl={trade.pnlcomm:.2f}')



