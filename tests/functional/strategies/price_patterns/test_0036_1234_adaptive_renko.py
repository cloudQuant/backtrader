def build_resampled_frame(df, indicator_minutes):
    """Resample an OHLCV DataFrame to the indicator timeframe.

    Args:
        df: Source M15 DataFrame indexed by datetime with OHLCV columns.
        indicator_minutes: Target bar size in minutes for the signal series.

    Returns:
        A resampled DataFrame with rows lacking OHLC data dropped and
        openinterest gaps filled with zero.
    """
    rule = f'{int(indicator_minutes)}min'
    signal_df = df.resample(rule, label='right', closed='right').agg({
        'open': 'first',
        'high': 'max',
        'low': 'min',
        'close': 'last',
        'volume': 'sum',
        'openinterest': 'last',
    })
    signal_df = signal_df.dropna(subset=['open', 'high', 'low', 'close']).copy()
    signal_df['openinterest'] = signal_df['openinterest'].fillna(0)
    return signal_df


def atr_wilder(df, period):
    """Compute the Wilder-smoothed Average True Range of a frame.

    Args:
        df: DataFrame with high, low, and close columns.
        period: Smoothing period for the ATR.

    Returns:
        A Series of ATR values aligned to ``df``'s index.
    """
    period = int(period)
    high = df['high'].astype(float)
    low = df['low'].astype(float)
    close = df['close'].astype(float)
    prev_close = close.shift(1)
    tr = pd.concat([
        high - low,
        (high - prev_close).abs(),
        (low - prev_close).abs(),
    ], axis=1).max(axis=1)
    return tr.ewm(alpha=1.0 / period, adjust=False, min_periods=period).mean()


def stddev_sma_close(df, period):
    """Compute the rolling population standard deviation of close prices.

    Args:
        df: DataFrame with a close column.
        period: Rolling window length.

    Returns:
        A Series of rolling standard deviations (ddof=0) of the close.
    """
    period = int(period)
    close = df['close'].astype(float)
    return close.rolling(period, min_periods=period).std(ddof=0)


def build_adaptive_renko_frame(df, indicator_minutes, k, indicator_mode, vlt_period, price_mode, wide_min, point):
    """Build the resampled frame with Adaptive Renko trend lines.

    Args:
        df: Source M15 OHLCV DataFrame.
        indicator_minutes: Target signal timeframe in minutes.
        k: Multiplier applied to the volatility measure for brick sizing.
        indicator_mode: Volatility source (0=ATR, 1=stddev).
        vlt_period: Lookback period for the volatility measure.
        price_mode: Price source for brick comparison (0=high/low, 1=close).
        wide_min: Minimum brick width in points (floor on brick size).
        point: Instrument point size used to scale the minimum width.

    Returns:
        A copy of the resampled frame with ``up_trend`` and ``dn_trend`` columns
        appended.
    """
    signal_df = build_resampled_frame(df, indicator_minutes)
    indicator_mode = int(indicator_mode)
    price_mode = int(price_mode)
    vlt_period = int(vlt_period)
    sens = float(wide_min) * float(point)

    if indicator_mode == INDICATOR_ATR:
        ind_array = atr_wilder(signal_df, vlt_period)
    else:
        ind_array = stddev_sma_close(signal_df, vlt_period)

    high = signal_df['high'].astype(float).to_numpy()
    low = signal_df['low'].astype(float).to_numpy()
    close = signal_df['close'].astype(float).to_numpy()
    vlt = ind_array.to_numpy(dtype=float)
    n = len(signal_df)

    up_buffer = np.full(n, np.nan, dtype=float)
    dn_buffer = np.full(n, np.nan, dtype=float)
    up_trend = np.zeros(n, dtype=float)
    dn_trend = np.zeros(n, dtype=float)

    start = vlt_period
    if n <= start:
        signal_df = signal_df.copy()
        signal_df['up_trend'] = up_trend
        signal_df['dn_trend'] = dn_trend
        return signal_df

    if price_mode == PRICE_CLOSE:
        hi0 = close[start]
        lo0 = close[start]
    else:
        hi0 = high[start]
        lo0 = low[start]
    brick = max(float(k) * (hi0 - lo0), sens)
    up = hi0
    dn = lo0
    trend = 0
    up_buffer[start] = up
    dn_buffer[start] = dn

    for idx in range(start, n):
        if price_mode == PRICE_CLOSE:
            hi = close[idx]
            lo = close[idx]
        else:
            hi = high[idx]
            lo = low[idx]

        current_vlt = vlt[idx]
        if not math.isfinite(current_vlt):
            current_vlt = sens
        current_vlt = max(float(k) * current_vlt, sens)

        if hi > up + brick:
            if abs(brick) > 1e-12:
                up += math.floor((hi - up) / brick) * brick
            brick = current_vlt
            dn = up - brick

        if lo < dn - brick:
            if abs(brick) > 1e-12:
                dn -= math.floor((dn - lo) / brick) * brick
            brick = current_vlt
            up = dn + brick

        up_buffer[idx] = up
        dn_buffer[idx] = dn
        up_trend[idx] = 0.0
        dn_trend[idx] = 0.0

        prev_idx = idx - 1
        prev_up = up_buffer[prev_idx]
        prev_dn = dn_buffer[prev_idx]
        if math.isfinite(prev_up) and prev_up < up:
            trend = 1
        if math.isfinite(prev_dn) and prev_dn > dn:
            trend = -1

        if trend > 0:
            up_trend[idx] = dn - brick
        if trend < 0:
            dn_trend[idx] = up + brick

    signal_df = signal_df.copy()
    signal_df['up_trend'] = up_trend
    signal_df['dn_trend'] = dn_trend
    return signal_df


class AdaptiveRenkoStrategy(bt.Strategy):
    """Dual-timeframe Adaptive Renko trend strategy with fixed SL/TP risk."""

    params = dict(
        signal_bar=1,
        stop_loss_points=1000,
        take_profit_points=2000,
        mm=-0.1,
        point=0.01,
        buy_pos_open=True,
        sell_pos_open=True,
        buy_pos_close=True,
        sell_pos_close=True,
        indicator_minutes=240,
        k=1.0,
        indicator_mode=0,
        vlt_period=10,
        price_mode=1,
        wide_min=2,
        lot=0.1,
    )

    def __init__(self):
        """Bind the base/signal feeds and initialize counters and state."""
        self.base = self.datas[0]
        self.signal = self.datas[1]
        self.bar_num = 0
        self.signal_count = 0
        self.buy_count = 0
        self.sell_count = 0
        self.trade_count = 0
        self.win_count = 0
        self.loss_count = 0
        self._position_was_open = False
        self._last_signal_len = 0

    def log(self, text):
        """Print strategy messages with execution datetime."""
        
        dt = bt.num2date(self.base.datetime[0])
        print(f'{dt.isoformat()}, {text}')

    def _line_value(self, line, ago):
        return float(line[-ago]) if ago else float(line[0])

    @staticmethod
    def _has_value(value):
        return math.isfinite(value) and abs(value) > 1e-12

    def _position_size(self, price):
        if self.p.mm < 0:
            return abs(float(self.p.mm))
        if price <= 0:
            return 0.0
        cash = self.broker.getcash()
        return round((cash * float(self.p.mm)) / price, 4)

    def _check_exit_levels(self):
        if not self.position:
            return False
        close_price = float(self.base.close[0])
        point_value = float(self.p.point)
        stop_distance = self.p.stop_loss_points * point_value if self.p.stop_loss_points > 0 else None
        take_distance = self.p.take_profit_points * point_value if self.p.take_profit_points > 0 else None
        entry_price = float(self.position.price)

        if self.position.size > 0:
            if stop_distance is not None and close_price <= entry_price - stop_distance:
                self.log(f'close long by stop loss close={close_price:.2f} entry={entry_price:.2f}')
                self.close()
                return True
            if take_distance is not None and close_price >= entry_price + take_distance:
                self.log(f'close long by take profit close={close_price:.2f} entry={entry_price:.2f}')
                self.close()
                return True
        elif self.position.size < 0:
            if stop_distance is not None and close_price >= entry_price + stop_distance:
                self.log(f'close short by stop loss close={close_price:.2f} entry={entry_price:.2f}')
                self.close()
                return True
            if take_distance is not None and close_price <= entry_price - take_distance:
                self.log(f'close short by take profit close={close_price:.2f} entry={entry_price:.2f}')
                self.close()
                return True
        return False

    def next(self):
        """Evaluate Renko signal transitions and execute trades with exits.

        Skips until a signal transition is detected on the higher-timeframe
        series, then applies close/reopen logic, position sizing, and order
        execution counters.
        """
        self.bar_num += 1
        if len(self.base) < 2:
            return

        if self._check_exit_levels():
            return

        signal_bar = max(int(self.p.signal_bar), 1)
        if len(self.signal) < signal_bar + 1:
            return

        current_signal_len = len(self.signal)
        if current_signal_len == self._last_signal_len:
            return
        self._last_signal_len = current_signal_len

        recent_ago = signal_bar - 1
        prev_ago = signal_bar
        up_recent = self._line_value(self.signal.up_trend, recent_ago)
        up_prev = self._line_value(self.signal.up_trend, prev_ago)
        dn_recent = self._line_value(self.signal.dn_trend, recent_ago)
        dn_prev = self._line_value(self.signal.dn_trend, prev_ago)

        buy_signal = self._has_value(up_recent) and not self._has_value(up_prev)
        sell_signal = self._has_value(dn_recent) and not self._has_value(dn_prev)
        if not buy_signal and not sell_signal:
            return

        close_price = float(self.base.close[0])
        size = self._position_size(close_price)
        if size <= 0:
            return

        if buy_signal:
            self.signal_count += 1
            self.log(f'buy signal close={close_price:.2f} up_trend={up_recent:.2f}')
            if self.position.size < 0 and self.p.sell_pos_close:
                self.close()
            if self.position.size <= 0 and self.p.buy_pos_open:
                self.buy(size=size)
            return

        if sell_signal:
            self.signal_count += 1
            self.log(f'sell signal close={close_price:.2f} dn_trend={dn_recent:.2f}')
            if self.position.size > 0 and self.p.buy_pos_close:
                self.close()
            if self.position.size >= 0 and self.p.sell_pos_open:
                self.sell(size=size)

    def notify_trade(self, trade):
        """Update close/open counters and position state when trade changes."""
        if trade.isopen and not self._position_was_open:
            if trade.size > 0:
                self.buy_count += 1
            elif trade.size < 0:
                self.sell_count += 1
            self._position_was_open = True
            return
        if not trade.isclosed:
            return
        self.trade_count += 1
        if trade.pnlcomm >= 0:
            self.win_count += 1
        else:
            self.loss_count += 1
        self._position_was_open = False
        self.log(f'trade closed pnl={trade.pnlcomm:.2f}')



