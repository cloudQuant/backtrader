class HammerRsiStrategy(bt.Strategy):
    """Strategy class implementing Hammer and Hanging Man pattern recognition with RSI filtering.

    Attributes:
        params (dict): Configured strategy parameters.
    """
    params = dict(
        rsi_period=14,
        rsi_entry_long=40,
        rsi_entry_short=60,
        rsi_exit_upper=70,
        rsi_exit_lower=30,
        ma_period=5,
        lot=0.1,
        point=0.01,
        price_digits=2,
    )

    def __init__(self):
        """Initialize indicators, backtest tracking metrics, and state variables."""
        self.rsi = bt.indicators.RSI(self.data.close, period=self.p.rsi_period)
        self.close_avg = bt.indicators.SMA(self.data.close, period=self.p.ma_period)
        self.bar_num = 0
        self.buy_count = 0
        self.sell_count = 0
        self.trade_count = 0
        self.win_count = 0
        self.loss_count = 0
        self._position_was_open = False

    def _is_hammer(self):
        """Check if a bullish Hammer pattern has formed on the previous candle.

        Returns:
            bool: True if Hammer is recognized, otherwise False.
        """
        o1, h1, l1, c1 = (float(self.data.open[-1]), float(self.data.high[-1]), float(self.data.low[-1]), float(self.data.close[-1]))
        o2, c2 = float(self.data.open[-2]), float(self.data.close[-2])
        mid1 = (o1 + c1) / 2.0
        avg2 = float(self.close_avg[-2])
        rng = h1 - l1
        if rng < self.p.point:
            return False
        body_low = min(o1, c1)
        return mid1 < avg2 and body_low > (h1 - rng / 3.0) and c1 < c2 and o1 < o2

    def _is_hanging_man(self):
        """Check if a bearish Hanging Man pattern has formed on the previous candle.

        Returns:
            bool: True if Hanging Man is recognized, otherwise False.
        """
        o1, h1, l1, c1 = (float(self.data.open[-1]), float(self.data.high[-1]), float(self.data.low[-1]), float(self.data.close[-1]))
        o2, c2 = float(self.data.open[-2]), float(self.data.close[-2])
        mid1 = (o1 + c1) / 2.0
        avg2 = float(self.close_avg[-2])
        rng = h1 - l1
        if rng < self.p.point:
            return False
        body_low = min(o1, c1)
        return mid1 > avg2 and body_low > (h1 - rng / 3.0) and c1 > c2 and o1 > o2

    def next(self):
        """Execute the strategy decision logic on each new bar."""
        self.bar_num += 1
        warmup = max(self.p.rsi_period, self.p.ma_period) + 5
        if len(self.data) < warmup:
            return
        rsi0 = float(self.rsi[0])
        rsi1 = float(self.rsi[-1])
        if self.position:
            if self.position.size > 0:
                if ((rsi0 > self.p.rsi_exit_lower and rsi1 < self.p.rsi_exit_lower) or
                    (rsi0 < self.p.rsi_exit_upper and rsi1 > self.p.rsi_exit_upper)):
                    self.close()
                    return
            elif self.position.size < 0:
                if ((rsi0 < self.p.rsi_exit_upper and rsi1 > self.p.rsi_exit_upper) or
                    (rsi0 > self.p.rsi_exit_lower and rsi1 < self.p.rsi_exit_lower)):
                    self.close()
                    return
        else:
            if self._is_hammer() and rsi0 < self.p.rsi_entry_long:
                self.buy(size=self.p.lot)
                return
            if self._is_hanging_man() and rsi0 > self.p.rsi_entry_short:
                self.sell(size=self.p.lot)
                return

    def notify_trade(self, trade):
        """Callback to handle closed trades and manage win/loss counts.

        Args:
            trade (bt.Trade): The closed trade instance.
        """
        if trade.isopen and not self._position_was_open:
            if trade.size > 0:
                self.buy_count += 1
            elif trade.size < 0:
                self.sell_count += 1
            self._position_was_open = True
            return
        if not trade.isclosed:
            return
        self.trade_count += 1
        if trade.pnlcomm >= 0:
            self.win_count += 1
        else:
            self.loss_count += 1
        self._position_was_open = False



