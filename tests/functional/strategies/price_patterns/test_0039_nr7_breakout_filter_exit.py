def build_cerebro(frame, config):
    """Assemble the Cerebro engine, filtered-NR7 feed, strategy, and analyzers.

    Args:
        frame: The loaded data dict returned by :func:`load_data`.
        config: Parsed configuration providing backtest and commission settings.

    Returns:
        A configured :class:`backtrader.Cerebro` with the NR7 signal feed, a
        percentage futures commission, the strategy, and the default analyzers.
    """
    cerebro = bt.Cerebro(stdstats=False)
    bt_cfg = config['backtest']
    cerebro.broker.setcash(float(bt_cfg['initial_cash']))
    comminfo = ComminfoFuturesPercent(
        commission=float(bt_cfg.get('commission', 0.0)),
        margin=float(bt_cfg.get('margin', 0.01)),
        mult=float(bt_cfg.get('multiplier', 1.0)),
    )
    cerebro.broker.addcommissioninfo(comminfo)
    feed = Mt5NR7FilteredFeed(dataname=frame['data'], timeframe=bt.TimeFrame.Days, compression=1)
    cerebro.adddata(feed, name=f"{config['data']['symbol']}_{config['data']['timeframe']}")
    cerebro.addstrategy(NR7BreakoutFilterExitStrategy, **config.get('params', {}))
    sharpe_kwargs = get_sharpe_analyzer_kwargs(config)
    cerebro.addanalyzer(bt.analyzers.SharpeRatio, _name='sharpe', **sharpe_kwargs)
    cerebro.addanalyzer(bt.analyzers.TradeAnalyzer, _name='trades')
    cerebro.addanalyzer(bt.analyzers.DrawDown, _name='drawdown')
    cerebro.addanalyzer(bt.analyzers.Returns, _name='returns')
    cerebro.addanalyzer(bt.analyzers.SQN, _name='sqn')
    return cerebro


def extract_metrics(strat, cerebro, frame, config):
    """Consolidate analyzer output and strategy counters into a metrics dict.

    Args:
        strat: The executed strategy instance carrying counters and value series.
        cerebro: The Cerebro engine used to read final broker value.
        frame: The loaded data dict providing the date range and bar count.
        config: Parsed configuration providing the strategy name and initial cash.

    Returns:
        A dict of summary metrics (bar/trade counts, PnL, return, win rate,
        profit factor, drawdown, Sharpe, annualized return, SQN, and Ulcer
        Index).
    """
    sharpe = strat.analyzers.sharpe.get_analysis().get('sharperatio')
    drawdown = strat.analyzers.drawdown.get_analysis()
    trades = strat.analyzers.trades.get_analysis()
    returns = strat.analyzers.returns.get_analysis()
    sqn = strat.analyzers.sqn.get_analysis().get('sqn')
    initial_cash = float(config['backtest']['initial_cash'])
    final_value = float(cerebro.broker.getvalue())
    equity_values = [value for _, value in getattr(strat, 'broker_value_series', [])] or [initial_cash, final_value]
    total_trades = trades.get('total', {}).get('closed', 0)
    won = trades.get('won', {}).get('total', 0)
    lost = trades.get('lost', {}).get('total', 0)
    gross_profit = trades.get('won', {}).get('pnl', {}).get('total', 0.0) or 0.0
    gross_loss = abs(trades.get('lost', {}).get('pnl', {}).get('total', 0.0) or 0.0)
    profit_factor = gross_profit / gross_loss if gross_loss > 0 else None
    win_rate = won / total_trades * 100.0 if total_trades else 0.0
    total_return_pct = (final_value / initial_cash - 1.0) * 100.0
    annual_return_pct = (returns.get('rnorm') or 0.0) * 100.0
    max_dd = drawdown.get('max', {}).get('drawdown', 0.0) or 0.0
    return {
        'strategy_name': config['strategy']['name'],
        'fromdate': frame['fromdate'].isoformat(),
        'todate': frame['todate'].isoformat(),
        'bars': len(frame['data']),
        'bar_num': strat.bar_num,
        'buy_count': strat.buy_count,
        'sell_count': strat.sell_count,
        'total_trades': total_trades,
        'won': won,
        'lost': lost,
        'win_rate': win_rate,
        'profit_factor': profit_factor,
        'final_value': final_value,
        'net_pnl': final_value - initial_cash,
        'total_return_pct': total_return_pct,
        'annual_return_pct': annual_return_pct,
        'max_drawdown': max_dd,
        'sharpe_ratio': finite_or_none(sharpe),
        'sqn': finite_or_none(sqn),
        'ulcer_index': calculate_ulcer_index(equity_values),
    }





def main():
    """Run the full filtered-NR7 backtest end-to-end and compute its metrics."""
    config = load_config()
    frame = load_data(config)
    cerebro = build_cerebro(frame, config)
    results = cerebro.run()
    strat = results[0]
    metrics = extract_metrics(strat, cerebro, frame, config)


def _close(actual, expected, *, tol, key):
    """Assert ``actual`` is finite and within ``tol`` of ``expected``."""
    assert actual is not None, f"{key}: expected={expected}, got=None"
    a = float(actual)
    assert math.isfinite(a), f"{key}: expected={expected}, got non-finite {actual}"
    assert abs(a - float(expected)) <= tol, (
        f"{key}: expected={expected}, got={a} (tol={tol})"
    )


def _invoke_strategy_main():
    """Call main() or run() depending on what the original script defined."""
