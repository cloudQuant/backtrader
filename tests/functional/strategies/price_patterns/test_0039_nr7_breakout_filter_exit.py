class NR7BreakoutFilterExitStrategy(bt.Strategy):
    """Volatility-filtered NR7 breakout with reverse-signal and ATR exits."""

    params = dict(
        stop_loss_atr=2.5,
        take_profit_atr=4.0,
        time_exit=5,
        lot_size=1.0,
        lookback=7,
        atr_period=14,
        atr_ma_period=50,
        use_trend_filter=True,
        ma_period=50,
        volatility_threshold=2.5,
    )

    def __init__(self):
        """Reset counters, order/risk state, and the broker value series."""
        self.bar_num = 0
        self.buy_count = 0
        self.sell_count = 0
        self.trade_count = 0
        self.win_count = 0
        self.loss_count = 0
        self.pending_order = None
        self.entry_price = None
        self.entry_bar = None
        self.stop_loss = None
        self.take_profit = None
        self.position_type = 0
        self.broker_value_series = []

    def _get_position_size(self, target_notional_pct=1.0, price=None):
        if target_notional_pct <= 0:
            return 0.0
        broker_value = float(self.broker.getvalue())
        execution_price = float(self.data.close[0] if price is None else price)
        if broker_value <= 0 or execution_price <= 0:
            return 0.0
        comminfo = self.broker.getcommissioninfo(self.data)
        multiplier = float(getattr(comminfo.p, 'mult', 1.0) or 1.0)
        if multiplier <= 0:
            return 0.0
        size = broker_value * float(target_notional_pct) / (execution_price * multiplier)
        return max(0.01, round(size, 2))

    def next(self):
        """Manage an open filtered-NR7 trade or open a new one on a breakout.

        Increments the bar counter and records broker value, skips while an order
        is pending, then—when in a position—exits on an opposing reverse signal,
        the ATR stop, the ATR take-profit, or after ``time_exit`` bars, or—when
        flat—enters long/short on a filtered breakout with ATR risk prices and
        notional-percentage sizing.
        """
        self.bar_num += 1
        self.broker_value_series.append((bt.num2date(self.data.datetime[0]), float(self.broker.getvalue())))
        if self.pending_order is not None:
            return
        if self.position:
            bars_held = self.bar_num - self.entry_bar
            if self.position_type == 1 and float(self.data.reverse_long_exit[0]) > 0.5:
                self.pending_order = self.close()
                self.position_type = 0
                return
            if self.position_type == -1 and float(self.data.reverse_short_exit[0]) > 0.5:
                self.pending_order = self.close()
                self.position_type = 0
                return
            if self.position_type == 1 and self.data.low[0] < self.stop_loss:
                self.pending_order = self.close()
                self.position_type = 0
                return
            if self.position_type == -1 and self.data.high[0] > self.stop_loss:
                self.pending_order = self.close()
                self.position_type = 0
                return
            if self.position_type == 1 and self.data.high[0] > self.take_profit:
                self.pending_order = self.close()
                self.position_type = 0
                return
            if self.position_type == -1 and self.data.low[0] < self.take_profit:
                self.pending_order = self.close()
                self.position_type = 0
                return
            if bars_held >= self.p.time_exit:
                self.pending_order = self.close()
                self.position_type = 0
            return
        atr = float(self.data.atr[0])
        if atr <= 0:
            return
        if float(self.data.breakout_up[0]) > 0.5:
            self.buy_count += 1
            self.entry_price = float(self.data.close[0])
            self.entry_bar = self.bar_num
            self.stop_loss = self.entry_price - self.p.stop_loss_atr * atr
            self.take_profit = self.entry_price + self.p.take_profit_atr * atr
            self.position_type = 1
            self.pending_order = self.buy(size=self._get_position_size(target_notional_pct=float(self.p.lot_size)))
        elif float(self.data.breakout_down[0]) > 0.5:
            self.sell_count += 1
            self.entry_price = float(self.data.close[0])
            self.entry_bar = self.bar_num
            self.stop_loss = self.entry_price + self.p.stop_loss_atr * atr
            self.take_profit = self.entry_price - self.p.take_profit_atr * atr
            self.position_type = -1
            self.pending_order = self.sell(size=self._get_position_size(target_notional_pct=float(self.p.lot_size)))

    def notify_order(self, order):
        """Clear the pending order once it is no longer live.

        Args:
            order: The order whose status changed.
        """
        if order.status in (order.Submitted, order.Accepted):
            return
        self.pending_order = None

    def notify_trade(self, trade):
        """Tally win/loss counts when a trade closes.

        Args:
            trade: The trade whose status changed; closed trades increment the
                trade counter and the win or loss count by sign of PnL.
        """
        if not trade.isclosed:
            return
        self.trade_count += 1
        if trade.pnlcomm >= 0:
            self.win_count += 1
        else:
            self.loss_count += 1





