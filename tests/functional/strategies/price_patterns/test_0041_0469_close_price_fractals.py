class ClosePriceFractals(bt.Indicator):
    """Custom 5-period Fractal indicator calculated solely on closing prices.

    Lines:
        upper: Contains fractal peak closing price if upper fractal is formed.
        lower: Contains fractal valley closing price if lower fractal is formed.
    """
    lines = ('upper', 'lower')

    def __init__(self):
        """Initialize the indicator with a minimum period of 5 bars."""
        self.addminperiod(5)

    def next(self):
        """Determine if a peak or valley fractal forms on bar -2 based on close prices."""
        self.lines.upper[0] = float('nan')
        self.lines.lower[0] = float('nan')
        candidate = float(self.data.close[-2])
        if candidate > float(self.data.close[-3]) and candidate > float(self.data.close[-4]) and candidate >= float(self.data.close[-1]) and candidate >= float(self.data.close[0]):
            self.lines.upper[0] = candidate
        if candidate < float(self.data.close[-3]) and candidate < float(self.data.close[-4]) and candidate <= float(self.data.close[-1]) and candidate <= float(self.data.close[0]):
            self.lines.lower[0] = candidate


class ClosePriceFractalsStrategy(bt.Strategy):
    """Strategy class implementing close price fractal trend following and trailing stops.

    Attributes:
        params (dict): Configured strategy parameters.
    """
    params = dict(
        start_hour=10,
        end_hour=22,
        lots=0.1,
        stop_loss_pips=30,
        take_profit_pips=50,
        trailing_stop_pips=15,
        trailing_step_pips=5,
        point=0.01,
    )

    def __init__(self):
        """Initialize indicator instance, backtest metrics, and position stop loss/take profit levels."""
        self.fractals = ClosePriceFractals(self.data)
        self.order = None
        self.bar_num = 0
        self.buy_count = 0
        self.sell_count = 0
        self.trade_count = 0
        self.win_count = 0
        self.loss_count = 0
        self.stop_price = None
        self.take_profit_price = None
        self.trailing_anchor = None

    def _in_trade_window(self):
        """Verify if current time falls within the configured start/end hour range.

        Returns:
            bool: True if inside the trading window, otherwise False.
        """
        dt = self.data.datetime.datetime(0)
        hour = dt.hour
        if self.p.start_hour < self.p.end_hour:
            return self.p.start_hour <= hour < self.p.end_hour
        if self.p.start_hour > self.p.end_hour:
            return not (hour < self.p.start_hour and hour >= self.p.end_hour)
        return True

    def _clear_risk(self):
        """Reset active stop loss, take profit and trailing anchor references."""
        self.stop_price = None
        self.take_profit_price = None
        self.trailing_anchor = None

    def _set_entry_risk(self, price, direction):
        """Calculate and set the initial stop loss and take profit price levels.

        Args:
            price (float): Order execution price.
            direction (int): Position direction (1 for Long, -1 for Short).
        """
        sl = self.p.stop_loss_pips * self.p.point
        tp = self.p.take_profit_pips * self.p.point
        if direction > 0:
            self.stop_price = price - sl if self.p.stop_loss_pips > 0 else None
            self.take_profit_price = price + tp if self.p.take_profit_pips > 0 else None
        else:
            self.stop_price = price + sl if self.p.stop_loss_pips > 0 else None
            self.take_profit_price = price - tp if self.p.take_profit_pips > 0 else None
        self.trailing_anchor = price

    def _update_trailing(self):
        """Update trailing stop loss level based on price movements in trade direction.

        Returns:
            bool: False always.
        """
        if not self.position or self.p.trailing_stop_pips <= 0:
            return False
        trail = self.p.trailing_stop_pips * self.p.point
        step = self.p.trailing_step_pips * self.p.point
        current = float(self.data.close[0])
        if self.position.size > 0:
            if current - self.position.price > trail + step:
                candidate = current - trail
                threshold = current - (trail + step)
                if self.stop_price is None or self.stop_price < threshold:
                    self.stop_price = candidate
        else:
            if self.position.price - current > trail + step:
                candidate = current + trail
                threshold = current + (trail + step)
                if self.stop_price is None or self.stop_price > threshold:
                    self.stop_price = candidate
        return False

    def _check_exit_levels(self):
        """Check if the low/high prices of current bar touch or cross risk exit levels.

        Returns:
            bool: True if an exit order was placed, otherwise False.
        """
        if not self.position:
            self._clear_risk()
            return False
        if self.position.size > 0:
            if self.stop_price is not None and float(self.data.low[0]) <= self.stop_price:
                self.order = self.close()
                return True
            if self.take_profit_price is not None and float(self.data.high[0]) >= self.take_profit_price:
                self.order = self.close()
                return True
        else:
            if self.stop_price is not None and float(self.data.high[0]) >= self.stop_price:
                self.order = self.close()
                return True
            if self.take_profit_price is not None and float(self.data.low[0]) <= self.take_profit_price:
                self.order = self.close()
                return True
        return False

    def _last_two_fractals(self, line):
        """Retrieve the last two valid fractal values by looking back through history.

        Args:
            line (bt.LineSeries): Indicator line to look back into.

        Returns:
            tuple: (latest_fractal, previous_fractal) or (None, None).
        """
        values = []
        lookback = min(len(self.data) - 1, 30)
        for i in range(0, lookback):
            value = line[-i]
            if value == value:
                values.append(float(value))
            if len(values) >= 2:
                break
        if len(values) >= 2:
            return values[0], values[1]
        return None, None

    def next(self):
        """Execute the strategy decision logic on each new bar."""
        self.bar_num += 1
        if len(self.data) < 8:
            return
        if self.order:
            return
        if not self._in_trade_window():
            if self.position:
                self.order = self.close()
            return
        if self._check_exit_levels():
            return
        self._update_trailing()

        last_lower, previous_lower = self._last_two_fractals(self.fractals.lower)
        last_upper, previous_upper = self._last_two_fractals(self.fractals.upper)

        if last_lower is not None and previous_lower is not None and previous_lower < last_lower:
            if self.position.size < 0:
                self.order = self.close()
                return
            if self.position.size == 0:
                self.order = self.buy(size=self.p.lots)
                return

        if last_upper is not None and previous_upper is not None and previous_upper > last_upper:
            if self.position.size > 0:
                self.order = self.close()
                return
            if self.position.size == 0:
                self.order = self.sell(size=self.p.lots)

    def notify_order(self, order):
        """Handle order life cycle updates and trigger risk level calculations.

        Args:
            order (bt.Order): Updated order instance.
        """
        if order.status in [bt.Order.Submitted, bt.Order.Accepted]:
            return
        if order.status == order.Completed:
            if order.isbuy() and order.executed.size > 0:
                self.buy_count += 1
                if self.position.size > 0:
                    self._set_entry_risk(order.executed.price, 1)
            elif order.issell() and order.executed.size < 0:
                self.sell_count += 1
                if self.position.size < 0:
                    self._set_entry_risk(order.executed.price, -1)
            elif self.position.size == 0:
                self._clear_risk()
        if order.status in [bt.Order.Completed, bt.Order.Canceled, bt.Order.Margin, bt.Order.Rejected]:
            self.order = None

    def notify_trade(self, trade):
        """Callback to handle closed trades and manage win/loss counts.

        Args:
            trade (bt.Trade): The closed trade instance.
        """
        if not trade.isclosed:
            return
        self.trade_count += 1
        if trade.pnlcomm >= 0:
            self.win_count += 1
        else:
            self.loss_count += 1
        if not self.position:
            self._clear_risk()



