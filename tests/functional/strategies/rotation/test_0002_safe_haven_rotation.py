def load_data(config):
    """Load the safe-haven assets and build the rotation signal inputs.

    Args:
        config: Resolved configuration mapping.

    Returns:
        A dict with the prepared inputs and the fromdate/todate bounds used to
        drive the backtest.
    """
    data_cfg = config['data']
