class ColorBBCandlesStrategy(bt.Strategy):
    """Trade on ColorBBCandles state transitions with position reversals.

    Buys on upward state breaks and sells on downward breaks, with explicit
    neutral exits when state returns to center.
    """
    params = dict(
        period=100,
        deviation1=1.0,
        deviation2=1.5,
        deviation3=2.0,
        deviation4=2.5,
        deviation5=3.0,
        ma_method='ema',
        applied_price='close',
        signal_bar=1,
        lot=0.1,
        point=0.01,
        price_digits=2,
    )

    def __init__(self):
        """Create the indicator and initialize trade counters/state."""
        self.signal = bt.indicators.ColorBBCandlesIndicator(
            self.data,
            period=self.p.period,
            deviation1=self.p.deviation1,
            deviation2=self.p.deviation2,
            deviation3=self.p.deviation3,
            deviation4=self.p.deviation4,
            deviation5=self.p.deviation5,
            ma_method=self.p.ma_method,
            applied_price=self.p.applied_price,
        )
        self.bar_num = 0
        self.buy_count = 0
        self.sell_count = 0
        self.trade_count = 0
        self.win_count = 0
        self.loss_count = 0
        self._position_was_open = False

    def log(self, text):
        """Write a timestamped strategy message."""
        dt = bt.num2date(self.data.datetime[0])
        print(f'{dt.isoformat()}, {text}')

    def _state_values(self):
        base = -int(self.p.signal_bar)
        return float(self.signal.state[base - 1]), float(self.signal.state[base])

    def next(self):
        """Process state transitions and open/close market positions."""
        self.bar_num += 1
        if len(self.data) < self.p.period + self.p.signal_bar + 5:
            return

        prev_state, curr_state = self._state_values()
        buy_signal = curr_state > 5.0 and prev_state < 6.0
        sell_signal = curr_state < 5.0 and prev_state > 4.0
        if not buy_signal and not sell_signal and not self.position:
            buy_signal = curr_state > 5.0
            sell_signal = curr_state < 5.0
        flat_signal = curr_state == 5.0

        if flat_signal and self.position:
            self.log(f'close neutral state={curr_state:.0f}')
            self.close()
            return

        if self.position:
            if self.position.size > 0 and sell_signal:
                self.log(f'close long & sell state={curr_state:.0f}')
                self.close()
                self.sell(size=self.p.lot)
                return
            if self.position.size < 0 and buy_signal:
                self.log(f'close short & buy state={curr_state:.0f}')
                self.close()
                self.buy(size=self.p.lot)
                return
        else:
            if buy_signal:
                self.log(f'buy state={curr_state:.0f}')
                self.buy(size=self.p.lot)
                return
            if sell_signal:
                self.log(f'sell state={curr_state:.0f}')
                self.sell(size=self.p.lot)
                return

    def notify_trade(self, trade):
        """Record completed trade outcome and open/close counters."""
        if trade.isopen and not self._position_was_open:
            if trade.size > 0:
                self.buy_count += 1
            elif trade.size < 0:
                self.sell_count += 1
            self._position_was_open = True
            return
        if not trade.isclosed:
            return
        self.trade_count += 1
        if trade.pnlcomm >= 0:
            self.win_count += 1
        else:
            self.loss_count += 1
        self._position_was_open = False
        self.log(f'trade closed pnl={trade.pnlcomm:.2f}')


