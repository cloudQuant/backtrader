class RingIndex:
    """Circular index map used to address rolling buffers without copying.

    Maintains a rotating mapping from logical bar offsets to physical buffer
    slots, mirroring the ring-buffer indexing used by the original EA.
    """

    def __init__(self, size):
        """Initialize the ring map.

        Args:
            size: Number of slots in the ring buffer.
        """
        self.size = size
        self.count = 1
        self.map = [0] * size

    def rotate(self):
        """Advance the ring by one position and rebuild the index map."""
        self.count -= 1
        if self.count < 0:
            self.count = self.size - 1
        for idx in range(self.size):
            numb = idx + self.count
            if numb > self.size - 1:
                numb -= self.size
            self.map[idx] = numb


