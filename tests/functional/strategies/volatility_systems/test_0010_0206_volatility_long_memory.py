class VolatilityLongMemoryStrategy(bt.Strategy):
    """Hurst-regime strategy that rebalances exposure on precomputed signals."""

    params = dict(
        vol_window=21,
        hurst_window=252,
        regime_compare_window=63,
        min_lag=2,
        max_lag=20,
        trend_threshold=0.55,
        trend_strong_threshold=0.6,
        mean_reversion_threshold=0.45,
        mean_reversion_strong_threshold=0.4,
        regime_change_threshold=0.1,
        fast_ma_period=20,
        slow_ma_period=50,
        momentum_window=21,
        mean_window=20,
        zscore_entry_threshold=1.5,
        zscore_exit_threshold=0.35,
        rsi_period=14,
        rsi_oversold=35,
        rsi_overbought=65,
        neutral_exposure=0.25,
        moderate_exposure=0.75,
        strong_exposure=1.0,
        regime_change_exposure=0.25,
        min_exposure_change=0.05,
        allow_short=True,
    )

    def __init__(self):
        """Initialize bar/regime counters and the broker-value tracking series."""
        self.bar_num = 0
        self.pending_order = None
        self.trade_count = 0
        self.win_count = 0
        self.loss_count = 0
        self.signal_change_count = 0
        self.regime_change_count = 0
        self.trend_days = 0
        self.mean_reversion_days = 0
        self.neutral_days = 0
        self.broker_value_series = []

    def next(self):
        """Track regime stats and rebalance on target exposure changes.

        The method:
        1. increments regime day counters from ``regime_code``,
        2. records regime switches from ``regime_change``,
        3. skips while an order is pending,
        4. issues ``order_target_percent`` only when ``signal_change`` triggers.
        """
        self.bar_num += 1
        self.broker_value_series.append((bt.num2date(self.data.datetime[0]), float(self.broker.getvalue())))
        regime_code = float(self.data.regime_code[0])
        if regime_code > 0.5:
            self.trend_days += 1
        elif regime_code < -0.5:
            self.mean_reversion_days += 1
        else:
            self.neutral_days += 1

        if float(self.data.regime_change[0]) > 0.5:
            self.regime_change_count += 1

        if self.pending_order is not None:
            return
        if float(self.data.signal_change[0]) <= 0.5:
            return

        self.signal_change_count += 1
        self.pending_order = self.order_target_percent(target=float(self.data.target_exposure[0]))

    def notify_order(self, order):
        """Clear pending order state when the order is accepted or completed."""
        if order.status in (order.Submitted, order.Accepted):
            return
        self.pending_order = None

    def notify_trade(self, trade):
        """Increment closed-trade counters and win/loss tallies."""
        if not trade.isclosed:
            return
        self.trade_count += 1
        if trade.pnlcomm >= 0:
            self.win_count += 1
        else:
            self.loss_count += 1





