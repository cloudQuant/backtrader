def load_mt5_csv(filepath, fromdate=None, todate=None, bar_shift_minutes=15):
    """Load a MetaTrader 5 CSV export into a backtrader-ready OHLCV frame.

    Args:
        filepath: Path to the tab-separated MT5 export file.
        fromdate: Optional inclusive lower bound used to clip the frame.
        todate: Optional inclusive upper bound used to clip the frame.
        bar_shift_minutes: Minutes to add to each timestamp so the index marks
            the bar close instead of its open.

    Returns:
        pandas.DataFrame: A datetime-indexed frame with ``open``, ``high``,
        ``low``, ``close``, ``volume`` and ``openinterest`` columns.
    """
    with open(filepath, 'r', encoding='utf-8') as f:
        lines = f.read().strip().split('\n')
    cleaned = '\n'.join(line.strip().strip('"') for line in lines)
    df = pd.read_csv(io.StringIO(cleaned), sep='\t')
    df['datetime'] = pd.to_datetime(df['<DATE>'] + ' ' + df['<TIME>'], format='%Y.%m.%d %H:%M:%S')
    df = df.rename(columns={
        '<OPEN>': 'open',
        '<HIGH>': 'high',
        '<LOW>': 'low',
        '<CLOSE>': 'close',
        '<TICKVOL>': 'volume',
        '<VOL>': 'openinterest',
    })
    df = df[['datetime', 'open', 'high', 'low', 'close', 'volume', 'openinterest']]
    df = df.set_index('datetime')
    if bar_shift_minutes:
        df.index = df.index + pd.Timedelta(minutes=bar_shift_minutes)
    if fromdate is not None:
        df = df[df.index >= fromdate]
    if todate is not None:
        df = df[df.index <= todate]
    return df


def resample_to_h1(df):
    """Resample an intrabar OHLCV frame up to right-labeled hourly bars.

    Args:
        df: A datetime-indexed OHLCV frame at a sub-hourly resolution.

    Returns:
        pandas.DataFrame: Hourly OHLCV bars with incomplete rows dropped.
    """
    agg = {
        'open': 'first',
        'high': 'max',
        'low': 'min',
        'close': 'last',
        'volume': 'sum',
        'openinterest': 'sum',
    }
    h1 = df.resample('1h', label='right', closed='right').agg(agg)
    return h1.dropna(subset=['open', 'high', 'low', 'close'])


def signal_ma_from_open_and_prev_closes(frame, period):
    """Average the current bar open with the previous ``period - 1`` closes.

    Args:
        frame: OHLCV frame providing ``open`` and ``close`` columns.
        period: Number of bars blended into the moving average.

    Returns:
        pandas.Series: The blended moving-average signal aligned to ``frame``.
    """
    total = frame['open'].copy()
    for shift in range(1, period):
        total = total + frame['close'].shift(shift)
    return total / period


def build_signal_frame(
    filepath,
    fromdate=None,
    todate=None,
    bar_shift_minutes=15,
    fast_ma_period=5,
    slow_ma_period=25,
    buy_threshold=0.0015,
):
    """Build the hourly signal frame with MAs, conditions and the buy signal.

    Args:
        filepath: Path to the MT5 CSV export.
        fromdate: Optional inclusive lower date bound.
        todate: Optional inclusive upper date bound.
        bar_shift_minutes: Minutes to shift each timestamp to the bar close.
        fast_ma_period: Lookback for the fast blended signal MA.
        slow_ma_period: Lookback for the slow blended signal MA.
        buy_threshold: Minimum gap below the open required to trigger a buy.

    Returns:
        pandas.DataFrame: Hourly frame carrying the signal MAs, the take-profit
        price and the boolean entry condition columns, warm-up rows dropped.
    """
    base = load_mt5_csv(filepath, fromdate=fromdate, todate=todate, bar_shift_minutes=bar_shift_minutes)
    frame = resample_to_h1(base)
    frame['ma_fast_signal'] = signal_ma_from_open_and_prev_closes(frame, fast_ma_period)
    frame['ma_slow_signal'] = signal_ma_from_open_and_prev_closes(frame, slow_ma_period)
    frame['ma_fast_prev2'] = frame['close'].rolling(fast_ma_period).mean().shift(2)
    frame['tp_price'] = frame['ma_fast_signal']
    frame['buy_condition_1'] = frame['ma_fast_signal'] - frame['open'] < -buy_threshold
    frame['buy_condition_2'] = frame['ma_fast_signal'] > frame['ma_fast_prev2']
    frame['tp_valid'] = frame['tp_price'] > frame['open']
    frame['buy_signal'] = frame['buy_condition_1'] & frame['buy_condition_2'] & frame['tp_valid']
    return frame.dropna(subset=['ma_fast_signal', 'ma_slow_signal', 'ma_fast_prev2'])


