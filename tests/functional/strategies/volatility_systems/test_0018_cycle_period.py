class RingIndex:
    """Rotating index map used to address a fixed-size circular buffer.

    Maintains a length-``size`` mapping from logical bar offsets to physical
    buffer slots so that the cycle-period computation can reuse a small fixed
    set of arrays while walking forward bar by bar.
    """

    def __init__(self, size):
        """Initialize the ring with ``size`` slots.

        Args:
            size: Number of physical slots in the ring buffer.
        """
        self.size = size
        self.count = 1
        self.map = [0] * size

    def rotate(self):
        """Advance the ring by one step and rebuild the offset-to-slot map."""
        self.count -= 1
        if self.count < 0:
            self.count = self.size - 1
        for idx in range(self.size):
            numb = idx + self.count
            if numb > self.size - 1:
                numb -= self.size
            self.map[idx] = numb


def load_mt5_csv(filepath, fromdate=None, todate=None, bar_shift_minutes=0):
    """Load a tab-separated MT5 export into an OHLCV DataFrame.

    Args:
        filepath: Path to the MT5 CSV/TSV export file.
        fromdate: Optional inclusive lower bound on the bar timestamp.
        todate: Optional inclusive upper bound on the bar timestamp.
        bar_shift_minutes: Minutes to add to each bar timestamp so bars are
            stamped at their close.

    Returns:
        A datetime-indexed DataFrame with open, high, low, close, volume, and
        openinterest columns, filtered to the requested date range.
    """
    with open(filepath, 'r', encoding='utf-8') as f:
        lines = f.read().strip().split('\n')
    cleaned = '\n'.join(line.strip().strip('"') for line in lines)
    df = pd.read_csv(io.StringIO(cleaned), sep='\t')
    df['datetime'] = pd.to_datetime(df['<DATE>'] + ' ' + df['<TIME>'], format='%Y.%m.%d %H:%M:%S')
    df = df.rename(columns={
        '<OPEN>': 'open',
        '<HIGH>': 'high',
        '<LOW>': 'low',
        '<CLOSE>': 'close',
        '<TICKVOL>': 'volume',
        '<VOL>': 'openinterest',
    })
    df = df[['datetime', 'open', 'high', 'low', 'close', 'volume', 'openinterest']]
    df = df.set_index('datetime')
    if bar_shift_minutes:
        df.index = df.index + pd.Timedelta(minutes=bar_shift_minutes)
    if fromdate is not None:
        df = df[df.index >= fromdate]
    if todate is not None:
        df = df[df.index <= todate]
    return df


def resample_frame(df, rule):
    """Resample an OHLCV DataFrame to a coarser bar interval.

    Args:
        df: Source OHLCV DataFrame indexed by datetime.
        rule: Pandas offset alias describing the target bar size (e.g. '360min').

    Returns:
        A resampled DataFrame with aggregated OHLCV columns, dropping bars that
        have no price data and filling missing open interest with zero.
    """
    out = df.resample(rule, label='right', closed='right').agg({
        'open': 'first',
        'high': 'max',
        'low': 'min',
        'close': 'last',
        'volume': 'sum',
        'openinterest': 'last',
    })
    out = out.dropna(subset=['open', 'high', 'low', 'close'])
    out['openinterest'] = out['openinterest'].fillna(0)
    return out


def compute_cycle_period(highs, lows, alpha):
    """Compute the dominant cycle period series from high/low prices.

    Implements the Ehlers-style CyclePeriod calculation: it smooths the median
    price, derives a detrended cycle component, estimates in-phase and
    quadrature parts, takes the median phase change, and exponentially smooths
    the resulting instantaneous period.

    Args:
        highs: Series of bar high prices.
        lows: Series of bar low prices.
        alpha: Smoothing coefficient controlling the cycle filter response.

    Returns:
        A float64 Series of the smoothed cycle period aligned to ``highs.index``.
    """
    k0 = (1.0 - 0.5 * alpha) ** 2
    k1 = 2.0
    k2 = k1 * (1.0 - alpha)
    k3 = (1.0 - alpha) ** 2
    f0 = 0.0962
    f1 = 0.5769
    f2 = 0.5
    f3 = 0.08
    median = 5
    median2 = median // 2
    med2 = median % 2 == 0

    count1 = RingIndex(7)
    count2 = RingIndex(median)
    smooth = [0.0] * 7
    cycle = [0.0] * 7
    q1 = [0.0] * 7
    i1 = [0.0] * 7
    price = [0.0] * 7
    delta_phase = [0.0] * median

    inst_period = 1.0
    cperiod = 1.0
    result = []

    for bar in range(len(highs)):
        bar0 = count1.map[0]
        bar1 = count1.map[1]
        bar2 = count1.map[2]
        bar3 = count1.map[3]
        bar4 = count1.map[4]
        bar6 = count1.map[6]

        price[bar0] = (float(highs.iloc[bar]) + float(lows.iloc[bar])) / 2.0
        smooth[bar0] = (price[bar0] + 2.0 * price[bar1] + 2.0 * price[bar2] + price[bar3]) / 6.0

        if bar < 6:
            cycle[bar0] = (price[bar0] - 2.0 * price[bar1] + price[bar2]) / 4.0
        else:
            cycle[bar0] = k0 * (smooth[bar0] - k1 * smooth[bar1] + smooth[bar2]) + k2 * cycle[bar1] - k3 * cycle[bar2]

        q1[bar0] = (f0 * cycle[bar0] + f1 * cycle[bar2] - f1 * cycle[bar4] - f0 * cycle[bar6]) * (f2 + f3 * inst_period)
        i1[bar0] = cycle[bar3]

        if q1[bar0] and q1[bar1]:
            denom = 1.0 + i1[bar0] * i1[bar1] / (q1[bar0] * q1[bar1])
            delta_phase[count2.map[0]] = (i1[bar0] / q1[bar0] - i1[bar1] / q1[bar1]) / denom

        phase_idx = count2.map[0]
        delta_phase[phase_idx] = max(0.1, delta_phase[phase_idx])
        delta_phase[phase_idx] = min(1.1, delta_phase[phase_idx])

        median_values = sorted(delta_phase)
        if med2:
            median_delta = (median_values[median2] + median_values[median2 + 1]) / 2.0
        else:
            median_delta = median_values[median2]

        dc = 15.0 if not median_delta else 6.28318 / median_delta + 0.5
        inst_period = 0.67 * inst_period + 0.33 * dc
        cperiod = 0.85 * cperiod + 0.15 * inst_period
        result.append(cperiod)

        if bar < len(highs) - 1:
            count1.rotate()
            count2.rotate()

    return pd.Series(result, index=highs.index, dtype='float64')


def compute_cycle_period_signals(frame, alpha=0.07):
    """Attach cycle-period values and turn-based buy/sell signals to a frame.

    A buy signal marks a trough (the cycle period turns up) and a sell signal
    marks a peak (the cycle period turns down), each evaluated on the prior two
    bars.

    Args:
        frame: OHLCV DataFrame to enrich.
        alpha: Smoothing coefficient passed to compute_cycle_period.

    Returns:
        A copy of ``frame`` with ``cycle_period``, ``buy_signal``, and
        ``sell_signal`` columns, dropping rows without a cycle period.
    """
    value = compute_cycle_period(frame['high'], frame['low'], alpha)
    buy_signal = (value.shift(1) < value.shift(2)) & (value > value.shift(1))
    sell_signal = (value.shift(1) > value.shift(2)) & (value < value.shift(1))

    out = frame.copy()
    out['cycle_period'] = value
    out['buy_signal'] = buy_signal.astype(float)
    out['sell_signal'] = sell_signal.astype(float)
    return out.dropna(subset=['cycle_period'])


