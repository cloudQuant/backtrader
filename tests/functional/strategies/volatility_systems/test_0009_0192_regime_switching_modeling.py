class RegimeSwitchingModelingStrategy(bt.Strategy):
    """Trend exposure strategy driven by regime prediction confidence and persistence."""
    params = dict(
        n_states=2,
        train_window=252,
        retrain_interval=21,
        volatility_window=21,
        momentum_window=63,
        min_state_prob=0.55,
        min_persistence_prob=0.55,
        bull_volatility_threshold=0.018,
        neutral_exposure=0.35,
        short_exposure=0.5,
        allow_short=True,
        random_state=42,
    )

    def __init__(self):
        """Initialize state counters, order tracking, and broker value snapshots."""
        self.bar_num = 0
        self.pending_order = None
        self.trade_count = 0
        self.win_count = 0
        self.loss_count = 0
        self.retrain_count = 0
        self.signal_change_count = 0
        self.broker_value_series = []

    def next(self):
        """Capture equity curve snapshots and issue target exposure orders on signals."""
        self.bar_num += 1
        self.broker_value_series.append((bt.num2date(self.data.datetime[0]), float(self.broker.getvalue())))
        if float(self.data.retrain_point[0]) > 0.5:
            self.retrain_count += 1
        if self.pending_order is not None:
            return
        if float(self.data.signal_change[0]) <= 0.5:
            return
        self.signal_change_count += 1
        self.pending_order = self.order_target_percent(target=float(self.data.target_exposure[0]))

    def notify_order(self, order):
        """Clear pending order marker when the order is completed/failed."""
        if order.status in (order.Submitted, order.Accepted):
            return
        self.pending_order = None

    def notify_trade(self, trade):
        """Update win/loss counters after trade close."""
        if not trade.isclosed:
            return
        self.trade_count += 1
        if trade.pnlcomm >= 0:
            self.win_count += 1
        else:
            self.loss_count += 1





