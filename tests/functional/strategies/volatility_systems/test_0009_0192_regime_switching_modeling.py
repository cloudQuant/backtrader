def calculate_ulcer_index(values):
    """Calculate the Ulcer Index from a sequence of equity values."""
    if len(values) < 2:
        return 0.0
    max_value = values[0]
    sum_squared = 0.0
    for value in values:
        if value > max_value:
            max_value = value
        drawdown = (max_value - value) / max_value * 100.0 if max_value > 0 else 0.0
        sum_squared += drawdown ** 2
    return math.sqrt(sum_squared / len(values))



