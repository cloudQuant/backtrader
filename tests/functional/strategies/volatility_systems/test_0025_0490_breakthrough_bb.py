class BreakthroughBbStrategy(bt.Strategy):
    """Bollinger Band breakout filtered by a short moving-average slope."""

    params = dict(
        ma_period=9,
        bands_period=28,
        deviation=1.6,
        lot=0.1,
    )

    def __init__(self):
        """Build the SMA and Bollinger Bands indicators and zero the counters."""
        self.ma = bt.indicators.SimpleMovingAverage(self.data.close, period=self.p.ma_period)
        self.bbands = bt.indicators.BollingerBands(self.data.close, period=self.p.bands_period, devfactor=self.p.deviation)
        self.order = None
        self.bar_num = 0
        self.buy_count = 0
        self.sell_count = 0
        self.trade_count = 0
        self.win_count = 0
        self.loss_count = 0
        self._position_was_open = False

    def next(self):
        """Exit on a midline reversion, else enter on a confirmed band breakout."""
        self.bar_num += 1
        warmup = max(self.p.bands_period, self.p.ma_period) + 5
        if len(self.data) < warmup:
            return
        if self.order:
            return

        close_1 = float(self.data.close[-1])
        close_4 = float(self.data.close[-4])
        bb_mid_1 = float(self.bbands.mid[-1])
        bb_up_1 = float(self.bbands.top[-1])
        bb_low_1 = float(self.bbands.bot[-1])
        ma_1 = float(self.ma[-1])
        ma_4 = float(self.ma[-4])

        if self.position:
            if self.position.size > 0 and close_1 < bb_mid_1:
                self.order = self.close()
                return
            if self.position.size < 0 and close_1 > bb_mid_1:
                self.order = self.close()
                return
            return

        if close_4 < bb_up_1 and close_1 > bb_up_1 and ma_1 > ma_4:
            self.order = self.buy(size=self.p.lot)
            return

        if close_4 > bb_low_1 and close_1 < bb_low_1 and ma_1 < ma_4:
            self.order = self.sell(size=self.p.lot)
            return

    def notify_order(self, order):
        """Clear the working order once it reaches a terminal status.

        Args:
            order: The order whose status changed.
        """
        if order.status in [bt.Order.Submitted, bt.Order.Accepted]:
            return
        if order.status in [bt.Order.Completed, bt.Order.Canceled, bt.Order.Margin, bt.Order.Rejected]:
            self.order = None

    def notify_trade(self, trade):
        """Track buy/sell on open and win/loss on close for each trade.

        Args:
            trade: The trade whose status changed; the first open bumps the
                buy or sell counter and a close bumps the trade and win/loss
                counters by sign of PnL.
        """
        if trade.isopen and not self._position_was_open:
            if trade.size > 0:
                self.buy_count += 1
            elif trade.size < 0:
                self.sell_count += 1
            self._position_was_open = True
            return
        if not trade.isclosed:
            return
        self.trade_count += 1
        if trade.pnlcomm >= 0:
            self.win_count += 1
        else:
            self.loss_count += 1
        self._position_was_open = False



