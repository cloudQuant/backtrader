def finite_or_none(value):
    """Return the value if it is truthy and finite, otherwise None.

    Args:
        value: A numeric value or None.

    Returns:
        The value when it is not None and finite, else None.
    """
    return value if value is not None and math.isfinite(value) else None


def calculate_ulcer_index(values):
    """Compute the Ulcer Index of an equity series.

    Args:
        values: Sequence of broker/equity values in chronological order.

    Returns:
        The Ulcer Index (root-mean-square percentage drawdown from running
        peaks), or 0.0 when there are fewer than two values.
    """
    if len(values) < 2:
        return 0.0
    peak = values[0]
    squared = 0.0
    for value in values:
        peak = max(peak, value)
        drawdown = (peak - value) / peak * 100.0 if peak > 0 else 0.0
        squared += drawdown ** 2
    return math.sqrt(squared / len(values))



def load_inputs(config):
    """Load and align the metal frames and build the switching signal frame.

    Args:
        config: Parsed configuration providing the ``data`` and ``params``
            sections.

    Returns:
        A dict with the ``signal_df`` switching frame, the aligned
        ``asset_frames``, the ``summary`` frame, and the ``fromdate``/``todate``
        bounds.
    """
    data_cfg = config['data']
    fromdate = datetime.fromisoformat(data_cfg['fromdate'])
    todate = datetime.fromisoformat(data_cfg['todate'])
    asset_frames = {}
    for asset_name, path in data_cfg['assets'].items():
        asset_frames[asset_name] = load_mt5_csv(path, fromdate=fromdate, todate=todate)
    signal_df, aligned_frames, summary = prepare_paired_switching_data(asset_frames, config.get('params', {}))
    return {'signal_df': signal_df, 'asset_frames': aligned_frames, 'summary': summary, 'fromdate': fromdate, 'todate': todate}


def build_cerebro(inputs, config):
    """Assemble the Cerebro engine, three feeds, strategy, and analyzers.

    Args:
        inputs: The loaded inputs dict returned by :func:`load_inputs`.
        config: Parsed configuration providing backtest and commission settings.

    Returns:
        A configured :class:`backtrader.Cerebro` with the signal, gold, and
        silver feeds, a percentage commission, the strategy, and the default
        analyzers.
    """
    cerebro = bt.Cerebro(stdstats=False)
    cerebro.broker.setcash(float(config['backtest']['initial_cash']))
    cerebro.broker.setcommission(commission=float(config['params'].get('commission_pct', 0.0005)))
    signal_feed = GoldPairedSwitchingSignalFeed(dataname=inputs['signal_df'], timeframe=bt.TimeFrame.Days, compression=1)
    cerebro.adddata(signal_feed, name='SIGNAL')
    for asset_name, frame in inputs['asset_frames'].items():
        feed = bt.feeds.PandasData(dataname=frame, timeframe=bt.TimeFrame.Days, compression=1)
        cerebro.adddata(feed, name=asset_name)
    cerebro.addstrategy(GoldPairedSwitchingStrategy, **config.get('params', {}))
    cerebro.addanalyzer(bt.analyzers.SharpeRatio, _name='sharpe', timeframe=bt.TimeFrame.Days, compression=1, factor=252, annualize=True, riskfreerate=0)
    cerebro.addanalyzer(bt.analyzers.Returns, _name='returns', timeframe=bt.TimeFrame.Days, tann=252)
    cerebro.addanalyzer(bt.analyzers.DrawDown, _name='drawdown')
    cerebro.addanalyzer(bt.analyzers.TradeAnalyzer, _name='trades')
    cerebro.addanalyzer(bt.analyzers.SQN, _name='sqn')
    return cerebro


def extract_metrics(strat, cerebro, inputs, config):
    """Collect regression metrics from analyzers and strategy counters.

    Args:
        strat: Strategy instance from ``cerebro.run()``.
        cerebro: Completed Backtrader engine.
        inputs: The loaded inputs dict (provides bars and date bounds).
        config: Full configuration (used for initial cash and strategy name).

    Returns:
        A flattened metrics dict (rebalance/switch counts, returns, drawdown,
        Sharpe, SQN, and an Ulcer Index) used by the regression assertions.
    """
    trades = strat.analyzers.trades.get_analysis()
    sharpe = strat.analyzers.sharpe.get_analysis()
    returns = strat.analyzers.returns.get_analysis()
    drawdown = strat.analyzers.drawdown.get_analysis()
    sqn = strat.analyzers.sqn.get_analysis()
    initial_cash = float(config['backtest']['initial_cash'])
    final_value = float(cerebro.broker.getvalue())
    broker_values = [value for _, value in getattr(strat, 'broker_value_series', [])] or [initial_cash, final_value]
    total_trades = trades.get('total', {}).get('total', 0)
    won = trades.get('won', {}).get('total', 0)
    lost = trades.get('lost', {}).get('total', 0)
    gross_won = trades.get('won', {}).get('pnl', {}).get('total', 0) or 0
    gross_lost = abs(trades.get('lost', {}).get('pnl', {}).get('total', 0) or 0)
    return {
        'strategy_name': config['strategy']['name'],
        'fromdate': inputs['fromdate'],
        'todate': inputs['todate'],
        'bars': len(inputs['signal_df']),
        'bar_num': strat.bar_num,
        'rebalance_count': strat.rebalance_count,
        'switch_count': strat.switch_count,
        'buy_count': strat.buy_count,
        'sell_count': strat.sell_count,
        'initial_cash': initial_cash,
        'final_value': final_value,
        'net_pnl': final_value - initial_cash,
        'total_return_pct': (final_value / initial_cash - 1.0) * 100.0,
        'total_trades': total_trades,
        'won': won,
        'lost': lost,
        'win_rate': (won / total_trades * 100.0) if total_trades else 0.0,
        'profit_factor': (gross_won / gross_lost) if gross_lost else None,
        'max_drawdown': drawdown.get('max', {}).get('drawdown', 0.0),
        'sharpe_ratio': finite_or_none(sharpe.get('sharperatio')),
        'annual_return_pct': (returns.get('rnorm') or 0.0) * 100.0,
        'sqn': finite_or_none(sqn.get('sqn')),
        'ulcer_index': calculate_ulcer_index(broker_values),
    }


def normalize(value):
    """Convert a value to a JSON-friendly scalar.

    Args:
        value: Any value; datetimes are ISO-formatted and non-finite floats map
            to None.

    Returns:
        The ISO string for datetimes, None for NaN/inf floats, or the value
        unchanged otherwise.
    """
    if isinstance(value, datetime):
        return value.isoformat()
    if isinstance(value, float) and (math.isnan(value) or math.isinf(value)):
        return None
    return value





