def load_mt5_csv(filepath, fromdate=None, todate=None, bar_shift_minutes=0):
    """Load MT5 CSV export as a normalized OHLCV dataframe.

    Args:
        filepath: Input CSV path.
        fromdate: Optional inclusive lower date boundary.
        todate: Optional inclusive upper date boundary.
        bar_shift_minutes: Optional minute shift for bar timestamp.

    Returns:
        pandas.DataFrame: Normalized OHLCV frame indexed by datetime.
    """
    with open(filepath, 'r', encoding='utf-8') as f:
        lines = f.read().strip().split('\n')
    cleaned = '\n'.join(line.strip().strip('"') for line in lines)
    df = pd.read_csv(io.StringIO(cleaned), sep='\t')
    df['datetime'] = pd.to_datetime(df['<DATE>'] + ' ' + df['<TIME>'], format='%Y.%m.%d %H:%M:%S')
    df = df.rename(columns={
        '<OPEN>': 'open',
        '<HIGH>': 'high',
        '<LOW>': 'low',
        '<CLOSE>': 'close',
        '<TICKVOL>': 'volume',
        '<VOL>': 'openinterest',
    })
    df = df[['datetime', 'open', 'high', 'low', 'close', 'volume', 'openinterest']]
    df = df.set_index('datetime')
    if bar_shift_minutes:
        df.index = df.index + pd.Timedelta(minutes=bar_shift_minutes)
    if fromdate is not None:
        df = df[df.index >= fromdate]
    if todate is not None:
        df = df[df.index <= todate]
    return df


def resample_frame(df, rule):
    """Resample OHLCV bars using a right-closed aggregation window.

    Args:
        df: Base timeframe dataframe.
        rule: Pandas resample rule string, for example ``"480min"``.

    Returns:
        pandas.DataFrame: Aggregated OHLCV frame.
    """
    out = df.resample(rule, label='right', closed='right').agg({
        'open': 'first',
        'high': 'max',
        'low': 'min',
        'close': 'last',
        'volume': 'sum',
        'openinterest': 'last',
    })
    out = out.dropna(subset=['open', 'high', 'low', 'close'])
    out['openinterest'] = out['openinterest'].fillna(0)
    return out


def compute_fisher_cyber_cycle(frame, alpha=0.07, length=8):
    """Compute Fisher transform and trigger for the cyber cycle indicator.

    Args:
        frame: Input OHLCV dataframe.
        alpha: Smoothing constant for cycle calculation.
        length: Window length used to normalize Fisher values.

    Returns:
        pandas.DataFrame: Input frame with ``fish`` and ``trigger`` columns.
    """
    alpha = float(alpha)
    length = max(int(length), 1)
    hl2 = ((frame['high'] + frame['low']) / 2.0).to_numpy(dtype=float)
    price = hl2.copy()
    smooth = np.full(len(frame), np.nan, dtype=float)
    cycle = np.full(len(frame), np.nan, dtype=float)
    value1 = np.full(len(frame), np.nan, dtype=float)
    fish = np.full(len(frame), np.nan, dtype=float)
    trigger = np.full(len(frame), np.nan, dtype=float)

    k0 = (1.0 - 0.5 * alpha) ** 2
    k2 = 2.0 * (1.0 - alpha)
    k3 = (1.0 - alpha) ** 2

    for bar in range(len(frame)):
        if bar >= 3:
            smooth[bar] = (price[bar] + 2.0 * price[bar - 1] + 2.0 * price[bar - 2] + price[bar - 3]) / 6.0
        else:
            smooth[bar] = price[bar]

        if bar < 2:
            continue

        if bar < length + 3:
            cycle[bar] = (price[bar] + 2.0 * price[bar - 1] + price[bar - 2]) / 4.0
        else:
            cycle[bar] = k0 * (smooth[bar] - 2.0 * smooth[bar - 1] + smooth[bar - 2]) + k2 * cycle[bar - 1] - k3 * cycle[bar - 2]

        start = max(0, bar - length + 1)
        window = cycle[start:bar + 1]
        window = window[~np.isnan(window)]
        if window.size == 0:
            continue

        hh = np.max(window)
        ll = np.min(window)
        value1[bar] = (cycle[bar] - ll) / (hh - ll) if hh != ll else 0.0

        if bar < 3:
            continue
        vals = value1[bar - 3:bar + 1]
        if np.isnan(vals).any():
            continue
        weighted = (4.0 * vals[-1] + 3.0 * vals[-2] + 2.0 * vals[-3] + vals[-4]) / 10.0
        scaled = 1.98 * (weighted - 0.5)
        scaled = min(max(scaled, -0.999999), 0.999999)
        fish[bar] = 0.5 * math.log((1.0 + scaled) / (1.0 - scaled))
        if bar > 0 and not np.isnan(fish[bar - 1]):
            trigger[bar] = fish[bar - 1]

    out = frame.copy()
    out['fish'] = fish
    out['trigger'] = trigger
    return out.dropna(subset=['fish', 'trigger'])


