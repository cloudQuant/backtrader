class The20sV020Strategy(Strategy):
    """Trade The20sV020 reversal signals with fixed stop-loss and take-profit."""

    params = dict(
        alg='MODE_1',
        level=100,
        ratio=0.2,
        direct=False,
        signal_bar=1,
        stop_loss_points=1000,
        take_profit_points=2000,
        lot=0.1,
        point=0.01,
    )

    def __init__(self):
        """Build the signal indicator and reset order/trade counters and state."""
        self.indicator = bt.indicators.The20sV020Signal(
            self.data,
            alg=self.p.alg,
            level=self.p.level,
            ratio=self.p.ratio,
            direct=self.p.direct,
            point=self.p.point,
        )
        self.buy_count = 0
        self.sell_count = 0
        self.trade_count = 0
        self.win_count = 0
        self.loss_count = 0
        self.buy_signal_count = 0
        self.sell_signal_count = 0
        self.completed_order_count = 0
        self.rejected_order_count = 0
        self.entry_price = None
        self.stop_price = None
        self.target_price = None
        self.pending_entry_direction = 0
        self.warmup = 25

    def _reset_levels(self):
        self.entry_price = None
        self.stop_price = None
        self.target_price = None

    def _open_long(self):
        self.pending_entry_direction = 1
        self.buy(size=self.p.lot)

    def _open_short(self):
        self.pending_entry_direction = -1
        self.sell(size=self.p.lot)

    def _close_position(self):
        self.close()
        self._reset_levels()

    def _manage_protective_levels(self):
        if not self.position or self.entry_price is None:
            return False

        low = float(self.data.low[0])
        high = float(self.data.high[0])

        if self.position.size > 0:
            if self.stop_price is not None and low <= self.stop_price:
                self._close_position()
                return True
            if self.target_price is not None and high >= self.target_price:
                self._close_position()
                return True
        else:
            if self.stop_price is not None and high >= self.stop_price:
                self._close_position()
                return True
            if self.target_price is not None and low <= self.target_price:
                self._close_position()
                return True

        return False

    def _has_buy_signal(self):
        value = float(self.indicator.buy[-max(int(self.p.signal_bar), 1)])
        return not math.isnan(value) and value != 0.0

    def _has_sell_signal(self):
        value = float(self.indicator.sell[-max(int(self.p.signal_bar), 1)])
        return not math.isnan(value) and value != 0.0

    def next(self):
        """Manage protective exits, then open, close, or reverse on a signal.

        Skips during warm-up, first handles stop/take-profit exits via
        :meth:`_manage_protective_levels`, then reads the buy/sell signals at
        ``signal_bar`` and tallies their counts. While in a position it reverses
        on an opposing signal; while flat it opens long on a buy signal or short
        on a sell signal.
        """
        if len(self.data) < self.warmup:
            return

        if self._manage_protective_levels():
            return

        buy_signal = self._has_buy_signal()
        sell_signal = self._has_sell_signal()

        if buy_signal:
            self.buy_signal_count += 1
        if sell_signal:
            self.sell_signal_count += 1

        if self.position:
            if self.position.size > 0 and sell_signal:
                self._close_position()
                self._open_short()
                return
            if self.position.size < 0 and buy_signal:
                self._close_position()
                self._open_long()
                return
        else:
            if buy_signal:
                self._open_long()
                return
            if sell_signal:
                self._open_short()
                return

    def notify_order(self, order):
        """Count completed/rejected orders and set bracket prices on entry fills.

        Args:
            order: The order whose status changed; a completed buy/sell matching
                the pending entry direction records the entry price and derives
                the stop-loss and take-profit levels.
        """
        if order.status in [order.Canceled, order.Margin, order.Rejected]:
            self.rejected_order_count += 1
            self.pending_entry_direction = 0
            return

        if order.status != order.Completed:
            return

        self.completed_order_count += 1

        if self.pending_entry_direction == 1 and order.isbuy():
            self.buy_count += 1
            self.entry_price = order.executed.price
            self.stop_price = self.entry_price - self.p.stop_loss_points * self.p.point if self.p.stop_loss_points > 0 else None
            self.target_price = self.entry_price + self.p.take_profit_points * self.p.point if self.p.take_profit_points > 0 else None
            self.pending_entry_direction = 0
            return

        if self.pending_entry_direction == -1 and order.issell():
            self.sell_count += 1
            self.entry_price = order.executed.price
            self.stop_price = self.entry_price + self.p.stop_loss_points * self.p.point if self.p.stop_loss_points > 0 else None
            self.target_price = self.entry_price - self.p.take_profit_points * self.p.point if self.p.take_profit_points > 0 else None
            self.pending_entry_direction = 0
            return

        if not self.position:
            self._reset_levels()

    def notify_trade(self, trade):
        """Tally win/loss counts when a trade closes.

        Args:
            trade: The trade whose status changed; closed trades increment the
                trade counter and the win or loss count by sign of PnL.
        """
        if not trade.isclosed:
            return
        self.trade_count += 1
        if trade.pnlcomm >= 0:
            self.win_count += 1
        else:
            self.loss_count += 1


MINUTES_PER_TRADING_YEAR = 252 * 24 * 60


def prepare_frame(config, base_dir):
    """Load the configured date range into a backtest frame bundle.

    Args:
        config: Parsed configuration providing the ``data`` section.
        base_dir: Directory used to resolve the relative data file path.

    Returns:
        A dict with the loaded ``data`` DataFrame, the ``fromdate``/``todate``
        bounds, and the resolved ``filepath``.
    """
    data_cfg = config['data']
    fromdate = pd.Timestamp(data_cfg['fromdate']) if data_cfg.get('fromdate') else None
    todate = pd.Timestamp(data_cfg['todate']) if data_cfg.get('todate') else None
    filepath = (base_dir / data_cfg['file']).resolve()
    data = load_mt5_csv(
        filepath,
        fromdate=fromdate,
        todate=todate,
        bar_shift_minutes=int(data_cfg.get('bar_shift_minutes', 0)),
    )
    return {'data': data, 'fromdate': fromdate, 'todate': todate, 'filepath': str(filepath)}


def build_cerebro(config, frame):
    """Assemble the Cerebro engine, feed, strategy, and analyzers.

    Args:
        config: Parsed configuration providing backtest and commission settings.
        frame: The loaded data dict returned by :func:`prepare_frame`.

    Returns:
        A configured :class:`backtrader.cerebro.Cerebro` with the M15 feed, a
        fixed-commission futures broker, the strategy, and the default analyzers.
    """
    bt_cfg = config['backtest']
    cerebro = Cerebro(stdstats=True)
    cerebro.broker.setcash(bt_cfg['initial_cash'])
    comm_type = CommInfoBase.COMM_FIXED if bt_cfg.get('commission_type', 'fixed') == 'fixed' else CommInfoBase.COMM_PERC
    cerebro.broker.setcommission(
        commission=bt_cfg['commission'],
        margin=bt_cfg['margin'],
        mult=bt_cfg['multiplier'],
        commtype=comm_type,
        stocklike=bt_cfg.get('stocklike', False),
    )
    feed = Mt5PandasFeed(dataname=frame['data'], timeframe=TimeFrame.Minutes, compression=15)
    cerebro.adddata(feed, name=f"{config['data']['symbol']}_{config['data']['timeframe']}")
    cerebro.addstrategy(The20sV020Strategy, **config.get('params', {}))
    cerebro.addanalyzer(
        btanalyzers.SharpeRatio,
        _name='sharpe',
        timeframe=TimeFrame.Minutes,
        factor=MINUTES_PER_TRADING_YEAR,
        annualize=True,
        riskfreerate=0,
    )
    cerebro.addanalyzer(
        btanalyzers.Returns,
        _name='returns',
        timeframe=TimeFrame.Minutes,
        compression=15,
        tann=MINUTES_PER_TRADING_YEAR,
    )
    cerebro.addanalyzer(btanalyzers.DrawDown, _name='drawdown')
    cerebro.addanalyzer(btanalyzers.TradeAnalyzer, _name='trades')
    cerebro.addanalyzer(btanalyzers.SQN, _name='sqn')
    return cerebro


def _get_nested(mapping, *keys, default=0):
    current = mapping
    for key in keys:
        if current is None:
            return default
        if hasattr(current, key):
            current = getattr(current, key)
        elif isinstance(current, dict) and key in current:
            current = current[key]
        else:
            return default
    return current


def extract_metrics(strategy, cerebro):
    """Collect regression metrics from analyzers and strategy counters.

    Args:
        strategy: Strategy instance from ``cerebro.run()``.
        cerebro: Completed Backtrader engine.

    Returns:
        A flattened metrics dict (final value, PnL, trade/order/signal counts,
        drawdown, returns, Sharpe, and SQN) used by the regression assertions.
    """
    sharpe = strategy.analyzers.sharpe.get_analysis()
    returns = strategy.analyzers.returns.get_analysis()
    drawdown = strategy.analyzers.drawdown.get_analysis()
    trades = strategy.analyzers.trades.get_analysis()
    sqn = strategy.analyzers.sqn.get_analysis()

    return {
        'final_value': round(cerebro.broker.getvalue(), 2),
        'net_pnl': round(cerebro.broker.getvalue() - cerebro.broker.startingcash, 2),
        'total_trades': int(_get_nested(trades, 'total', 'closed', default=0)),
        'won_trades': int(_get_nested(trades, 'won', 'total', default=0)),
        'lost_trades': int(_get_nested(trades, 'lost', 'total', default=0)),
        'buy_signals': int(getattr(strategy, 'buy_signal_count', 0)),
        'sell_signals': int(getattr(strategy, 'sell_signal_count', 0)),
        'buy_orders': int(getattr(strategy, 'buy_count', 0)),
        'sell_orders': int(getattr(strategy, 'sell_count', 0)),
        'completed_orders': int(getattr(strategy, 'completed_order_count', 0)),
        'rejected_orders': int(getattr(strategy, 'rejected_order_count', 0)),
        'max_drawdown_pct': round(float(_get_nested(drawdown, 'max', 'drawdown', default=0.0)), 4),
        'returns_rtot': round(float(returns.get('rtot', 0.0)), 6),
        'returns_rnorm': round(float(returns.get('rnorm', 0.0)), 6),
        'returns_rnorm100': round(float(returns.get('rnorm100', 0.0)), 4),
        'sharpe': None if sharpe.get('sharperatio') is None else round(float(sharpe.get('sharperatio')), 6),
        'sqn': None if sqn.get('sqn') is None else round(float(sqn.get('sqn')), 6),
    }


def run(plot=False):
    """Run the backtest end-to-end and return ``(results, metrics, cerebro)``.

    Args:
        plot: When True, render the Cerebro candlestick plot after the run.

    Returns:
        A tuple of the raw ``cerebro.run()`` results, the extracted metrics dict,
        and the Cerebro engine.
    """
    base_dir = Path(__file__).resolve().parent
    config_path = (base_dir / 'config.yaml').resolve()
    config = _bt_load_config(_CONFIG, repo=_REPO)
    frame = prepare_frame(config, base_dir)
    cerebro = build_cerebro(config, frame)
    results = cerebro.run()
    strategy = results[0]
    metrics = extract_metrics(strategy, cerebro)
    if plot:
        cerebro.plot(style='candlestick')
    return results, metrics, cerebro


