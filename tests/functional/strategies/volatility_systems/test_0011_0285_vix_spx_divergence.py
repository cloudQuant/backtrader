class VixSpxDivergenceStrategy(bt.Strategy):
    """Fade new highs on rising volatility with fixed stop/take-profit and time exit."""

    params = dict(
        holding_days=3,
        position_size=0.95,
        stop_loss=0.02,
        take_profit=0.015,
        new_high_period=50,
        vol_lookback=20,
        vol_rise_threshold=0.02,
        corr_lookback=20,
        correlation_cutoff=0.3,
        monday_weight=1.5,
        commission_pct=0.0005,
    )

    def __init__(self):
        """Zero the bar/trade counters and order/risk state, and value series."""
        self.bar_num = 0
        self.buy_count = 0
        self.sell_count = 0
        self.pending_order = None
        self.entry_bar = 0
        self.stop_price = None
        self.take_profit_price = None
        self.broker_value_series = []

    def _get_position_size(self, target_notional_pct=1.0, price=None):
        broker_value = float(self.broker.getvalue())
        execution_price = float(self.data.close[0] if price is None else price)
        if broker_value <= 0 or execution_price <= 0:
            return 0.0
        comminfo = self.broker.getcommissioninfo(self.data)
        multiplier = float(getattr(comminfo.p, 'mult', 1.0) or 1.0)
        if multiplier <= 0:
            return 0.0
        direction = 1.0 if target_notional_pct >= 0 else -1.0
        size = broker_value * abs(float(target_notional_pct)) / (execution_price * multiplier)
        return direction * round(size, 2)

    def next(self):
        """Manage stop/take-profit/time exits, else open a strength-scaled short.

        Increments the bar counter and records broker value, skips while an order
        is pending, exits an open position on the stop, take-profit, or holding
        limit, and—while flat—opens a strength-scaled short on an entry signal
        with fixed stop and take-profit prices.
        """
        self.bar_num += 1
        self.broker_value_series.append((bt.num2date(self.data.datetime[0]), float(self.broker.getvalue())))
        if self.pending_order is not None:
            return

        low = float(self.data.low[0])
        high = float(self.data.high[0])
        close = float(self.data.close[0])

        if self.position:
            if self.stop_price is not None and high >= self.stop_price:
                self.buy_count += 1
                self.pending_order = self.close()
                return
            if self.take_profit_price is not None and low <= self.take_profit_price:
                self.buy_count += 1
                self.pending_order = self.close()
                return
            if self.bar_num - self.entry_bar >= int(self.p.holding_days):
                self.buy_count += 1
                self.pending_order = self.close()
                return
            return

        if float(self.data.entry_signal[0]) > 0.5:
            strength = min(float(self.data.signal_strength[0]), 1.5)
            target_pct = -float(self.p.position_size) * strength / 1.5
            self.sell_count += 1
            self.entry_bar = self.bar_num
            self.stop_price = close * (1.0 + float(self.p.stop_loss))
            self.take_profit_price = close * (1.0 - float(self.p.take_profit))
            target_size = self._get_position_size(target_notional_pct=target_pct)
            self.pending_order = self.order_target_size(target=target_size)

    def notify_order(self, order):
        """Clear the pending order and reset risk prices once flat.

        Args:
            order: The order whose status changed; when no longer live the
                pending order is cleared and, if flat, the stop/take-profit
                prices are reset.
        """
        if order.status in (order.Submitted, order.Accepted):
            return
        self.pending_order = None
        if not self.position:
            self.stop_price = None
            self.take_profit_price = None


