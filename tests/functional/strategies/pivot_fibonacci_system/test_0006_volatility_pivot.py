def load_mt5_csv(filepath, fromdate=None, todate=None, bar_shift_minutes=0):
    """Load a MetaTrader-5 style CSV export into an OHLCV DataFrame.

    Args:
        filepath: Path to the tab-separated MT5 CSV export to read.
        fromdate: Optional inclusive lower bound used to trim the index.
        todate: Optional inclusive upper bound used to trim the index.
        bar_shift_minutes: Minutes to shift the datetime index forward.

    Returns:
        A datetime-indexed DataFrame with open, high, low, close, volume and
        openinterest columns.
    """
    with open(filepath, 'r', encoding='utf-8') as f:
        lines = f.read().strip().split('\n')
    cleaned = '\n'.join(line.strip().strip('"') for line in lines)
    df = pd.read_csv(io.StringIO(cleaned), sep='\t')
    df['datetime'] = pd.to_datetime(df['<DATE>'] + ' ' + df['<TIME>'], format='%Y.%m.%d %H:%M:%S')
    df = df.rename(columns={
        '<OPEN>': 'open',
        '<HIGH>': 'high',
        '<LOW>': 'low',
        '<CLOSE>': 'close',
        '<TICKVOL>': 'volume',
        '<VOL>': 'openinterest',
    })
    df = df[['datetime', 'open', 'high', 'low', 'close', 'volume', 'openinterest']]
    df = df.set_index('datetime')
    if bar_shift_minutes:
        df.index = df.index + pd.Timedelta(minutes=bar_shift_minutes)
    if fromdate is not None:
        df = df[df.index >= fromdate]
    if todate is not None:
        df = df[df.index <= todate]
    return df


def resample_frame(df, rule):
    """Resample an OHLCV frame to a coarser timeframe.

    Args:
        df: OHLCV DataFrame indexed by datetime.
        rule: Pandas resample rule string (e.g. ``'240min'``).

    Returns:
        The resampled OHLCV DataFrame with rows lacking price data dropped.
    """
    out = df.resample(rule, label='right', closed='right').agg({
        'open': 'first',
        'high': 'max',
        'low': 'min',
        'close': 'last',
        'volume': 'sum',
        'openinterest': 'last',
    })
    out = out.dropna(subset=['open', 'high', 'low', 'close'])
    out['openinterest'] = out['openinterest'].fillna(0)
    return out


def compute_atr(high, low, close, period):
    """Compute the Wilder-smoothed Average True Range series.

    Args:
        high: Series of high prices.
        low: Series of low prices.
        close: Series of close prices.
        period: ATR smoothing period.

    Returns:
        A pandas Series of ATR values aligned to the input index.
    """
    prev_close = close.shift(1)
    tr = pd.concat([
        high - low,
        (high - prev_close).abs(),
        (low - prev_close).abs(),
    ], axis=1).max(axis=1)
    return tr.ewm(alpha=1.0 / max(int(period), 1), adjust=False).mean()


def compute_volatility_pivot(frame, atr_range=100, ima_range=10, atr_factor=3.0, mode='Mode_ATR', delta_price=200, point=0.01):
    """Compute the Volatility Pivot trend and buy/sell signal columns.

    Args:
        frame: OHLCV DataFrame to annotate.
        atr_range: ATR lookback period used in ATR mode.
        ima_range: Smoothing span applied to the ATR delta.
        atr_factor: Multiplier applied to the smoothed ATR.
        mode: ``'Mode_ATR'`` for ATR-scaled distance, else a fixed delta.
        delta_price: Fixed pivot distance in points when not in ATR mode.
        point: Price-per-point used to scale the fixed delta.

    Returns:
        The input frame extended with ``upper_trend``, ``lower_trend``,
        ``buy_signal`` and ``sell_signal`` columns.
    """
    close = frame['close'].astype(float)
    high = frame['high'].astype(float)
    low = frame['low'].astype(float)
    upper = np.full(len(frame), np.nan, dtype=float)
    lower = np.full(len(frame), np.nan, dtype=float)
    buy_signal = np.full(len(frame), np.nan, dtype=float)
    sell_signal = np.full(len(frame), np.nan, dtype=float)

    if str(mode) == 'Mode_ATR':
        atr = compute_atr(high, low, close, atr_range)
        delta = atr_factor * atr.ewm(span=max(int(ima_range), 1), adjust=False).mean()
        min_rates_total = int(atr_range + ima_range) + 1
    else:
        delta = pd.Series(float(delta_price) * float(point), index=frame.index)
        min_rates_total = 3

    if len(frame) <= min_rates_total:
        out = frame.copy()
        out['upper_trend'] = upper
        out['lower_trend'] = lower
        out['buy_signal'] = buy_signal
        out['sell_signal'] = sell_signal
        return out.dropna(subset=['upper_trend', 'lower_trend', 'buy_signal', 'sell_signal'], how='all')

    prev_stop = float(close.iloc[min_rates_total - 1])
    prev_upper = np.nan
    prev_lower = np.nan

    for i in range(min_rates_total, len(frame)):
        c = float(close.iloc[i])
        prev_c = float(close.iloc[i - 1])
        d = float(delta.iloc[i]) if not pd.isna(delta.iloc[i]) else 0.0

        if c == prev_stop:
            stop = prev_stop
        elif prev_c < prev_stop and c < prev_stop:
            stop = min(prev_stop, c + d)
        elif prev_c > prev_stop and c > prev_stop:
            stop = max(prev_stop, c - d)
        else:
            stop = c - d if c > prev_stop else c + d

        cur_upper = stop if c > stop else np.nan
        cur_lower = stop if c < stop else np.nan
        upper[i] = cur_upper
        lower[i] = cur_lower

        if np.isnan(prev_upper) and not np.isnan(cur_upper):
            buy_signal[i] = cur_upper
        if np.isnan(prev_lower) and not np.isnan(cur_lower):
            sell_signal[i] = cur_lower

        prev_upper = cur_upper
        prev_lower = cur_lower
        prev_stop = stop

    out = frame.copy()
    out['upper_trend'] = upper
    out['lower_trend'] = lower
    out['buy_signal'] = buy_signal
    out['sell_signal'] = sell_signal
    return out.dropna(subset=['upper_trend', 'lower_trend'], how='all')


