def resample_daily(df):
    """Resample an intraday OHLCV frame to daily bars.

    Args:
        df: Intraday OHLCV DataFrame indexed by datetime with a spread column.

    Returns:
        A daily-resampled DataFrame with aggregated OHLCV, openinterest and
        spread columns, with empty rows dropped.
    """
    daily = df.resample('1D', label='right', closed='right').agg({
        'open': 'first',
        'high': 'max',
        'low': 'min',
        'close': 'last',
        'volume': 'sum',
        'openinterest': 'last',
        'spread': 'last',
    }).dropna()
    return daily


