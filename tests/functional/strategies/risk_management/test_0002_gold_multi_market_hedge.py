def finite_or_none(x):
    """Return ``x`` when it is truthy and finite, otherwise ``None``."""
    return x if x and math.isfinite(x) else None


def calculate_ulcer_index(values):
    """Compute the Ulcer Index from an equity-value series.

    Args:
        values: Sequence of account equity values in chronological order.

    Returns:
        float: The Ulcer Index (root-mean-square percentage drawdown).
    """
    if len(values) < 2:
        return 0.0
    max_value = values[0]
    sum_squared = 0.0
    for v in values:
        if v > max_value:
            max_value = v
        drawdown = (max_value - v) / max_value * 100.0 if max_value > 0 else 0.0
        sum_squared += drawdown ** 2
    return math.sqrt(sum_squared / len(values))


