class MovingAverageCrossoverStrategy(bt.Strategy):
    """Trend strategy using SMA crossovers with optional martingale sizing.

    On confirmed crossover, it places bracketed long/short orders and tracks
    win/loss state to optionally scale lot size and risk parameters.
    """
    params = dict(
        use_martingale=True,
        ma_period=50,
        lot_size=0.01,
        tp_points=150,
        sl_points=150,
        starting_lot=0.01,
        max_lot=0.5,
        lot_multiplier=2.0,
        tp_multiplier=2.0,
        point_size=0.01,
        lot_min=0.01,
        lot_max=100.0,
        lot_step=0.01,
    )

    def __init__(self):
        """Initialize indicators, order handles, and trade/martingale state."""
        self.sma = bt.ind.SMA(self.data.close, period=self.p.ma_period)
        self.entry_order = None
        self.stop_order = None
        self.limit_order = None
        self.bar_num = 0
        self.buy_count = 0
        self.sell_count = 0
        self.trade_count = 0
        self.win_count = 0
        self.loss_count = 0
        self.last_trade_profit = None
        self.current_volume = self.p.starting_lot if self.p.use_martingale else self.p.lot_size
        self.current_tp_points = self.p.tp_points
        self.current_sl_points = self.p.sl_points

    def log(self, text):
        """Print timestamped strategy debug message."""
        dt = bt.num2date(self.data.datetime[0])
        print(f'{dt.isoformat()}, {text}')

    def _normalize_lot(self, lot):
        lot = min(max(lot, self.p.lot_min), self.p.lot_max)
        lot = int(lot / self.p.lot_step) * self.p.lot_step
        return round(max(lot, self.p.lot_min), 4)

    def _prepare_trade_parameters(self):
        if not self.p.use_martingale:
            self.current_volume = self.p.lot_size
            self.current_tp_points = self.p.tp_points
            self.current_sl_points = self.p.sl_points
            return
        if self.last_trade_profit is None:
            self.current_volume = self.p.starting_lot
            self.current_tp_points = self.p.tp_points
            self.current_sl_points = self.p.sl_points
            return
        if self.last_trade_profit < 0 and self.current_volume * self.p.lot_multiplier < self.p.max_lot:
            self.current_volume *= self.p.lot_multiplier
            self.current_tp_points *= self.p.tp_multiplier
            self.current_sl_points *= self.p.tp_multiplier
        elif self.last_trade_profit > 0:
            self.current_volume = self.p.starting_lot
            self.current_tp_points = self.p.tp_points
            self.current_sl_points = self.p.sl_points

    def _crosses_to_sell(self):
        ma_prev = float(self.sma[-1])
        ma_older = float(self.sma[-2])
        prev_close = float(self.data.close[-2])
        current_close = float(self.data.close[-1])
        return ma_prev < prev_close and ma_older > current_close

    def _crosses_to_buy(self):
        ma_prev = float(self.sma[-1])
        ma_older = float(self.sma[-2])
        prev_close = float(self.data.close[-2])
        current_close = float(self.data.close[-1])
        return ma_prev > prev_close and ma_older < current_close

    def next(self):
        """Advance one bar, evaluate crossover entries, and submit bracket orders."""
        self.bar_num += 1
        if len(self.data) <= self.p.ma_period + 2:
            return
        if self.position or self.entry_order is not None:
            return
        self._prepare_trade_parameters()
        size = self._normalize_lot(self.current_volume)
        tp_distance = self.current_tp_points * self.p.point_size
        sl_distance = self.current_sl_points * self.p.point_size
        if self._crosses_to_sell():
            entry_price = float(self.data.close[0])
            stop_price = round(entry_price + sl_distance, 2)
            limit_price = round(entry_price - tp_distance, 2)
            orders = self.sell_bracket(size=size, stopprice=stop_price, limitprice=limit_price)
            self.entry_order, self.stop_order, self.limit_order = orders
            self.entry_order.addinfo(kind='entry_short')
            return
        if self._crosses_to_buy():
            entry_price = float(self.data.close[0])
            stop_price = round(entry_price - sl_distance, 2)
            limit_price = round(entry_price + tp_distance, 2)
            orders = self.buy_bracket(size=size, stopprice=stop_price, limitprice=limit_price)
            self.entry_order, self.stop_order, self.limit_order = orders
            self.entry_order.addinfo(kind='entry_long')

    def notify_order(self, order):
        """Track completed or cleared bracket legs and count entry orders."""
        if order.status in (order.Submitted, order.Accepted):
            return
        kind = getattr(order.info, 'kind', None)
        if order.status == order.Completed:
            if kind == 'entry_long':
                self.buy_count += 1
                self.log(f'long entry price={order.executed.price:.2f} volume={order.executed.size:.4f}')
            elif kind == 'entry_short':
                self.sell_count += 1
                self.log(f'short entry price={order.executed.price:.2f} volume={abs(order.executed.size):.4f}')
        if order is self.entry_order and not order.alive():
            self.entry_order = None
        if order is self.stop_order and not order.alive():
            self.stop_order = None
        if order is self.limit_order and not order.alive():
            self.limit_order = None

    def notify_trade(self, trade):
        """Update aggregate trade counters and martingale state from closed trades."""
        if not trade.isclosed:
            return
        self.trade_count += 1
        self.last_trade_profit = trade.pnlcomm
        if trade.pnlcomm >= 0:
            self.win_count += 1
        else:
            self.loss_count += 1
        self.log(f'trade closed pnl={trade.pnlcomm:.2f}')


BASE_DIR = Path(__file__).resolve().parent

MINUTES_PER_TRADING_YEAR = 24 * 60 * 252


def resolve_data_path(filename):
    """Resolve module-local data filenames to absolute file system paths.

    Args:
        filename: Relative path to a data asset under this test module directory.

    Returns:
        Absolute `Path` to the data file.
    """
    path = (BASE_DIR / filename).resolve()
    if not path.exists():
        raise FileNotFoundError(f'Data file not found: {path}')
    return path


def load_backtest_frame(config):
    """Load and time-filter backtest data according to strategy config.

    Args:
        config: Loaded configuration with data source window settings.

    Returns:
        Dict containing dataframe and date window metadata.
    """
    data_cfg = config['data']
    fromdate = datetime.datetime.fromisoformat(data_cfg['fromdate'])
    todate = datetime.datetime.fromisoformat(data_cfg['todate'])
    df = load_mt5_csv(
        resolve_data_path(data_cfg['file']),
        fromdate=fromdate,
        todate=todate,
        bar_shift_minutes=data_cfg.get('bar_shift_minutes', 0),
    )
    if df.empty:
        raise ValueError('Loaded data frame is empty')
    print(f"Loaded {len(df)} bars: {df.index[0]} -> {df.index[-1]}")
    return {'data': df, 'fromdate': fromdate, 'todate': todate}


def add_default_analyzers(cerebro):
    """Attach the standard analyzer stack used by migrated regression tests."""
    cerebro.addanalyzer(bt.analyzers.SharpeRatio, _name='sharpe', timeframe=bt.TimeFrame.Minutes, factor=MINUTES_PER_TRADING_YEAR, annualize=True, riskfreerate=0)
    cerebro.addanalyzer(bt.analyzers.Returns, _name='returns', timeframe=bt.TimeFrame.Minutes, compression=15, tann=MINUTES_PER_TRADING_YEAR)
    cerebro.addanalyzer(bt.analyzers.DrawDown, _name='drawdown')
    cerebro.addanalyzer(bt.analyzers.TradeAnalyzer, _name='trades')
    cerebro.addanalyzer(bt.analyzers.SQN, _name='sqn')


def build_cerebro(config, frame):
    """Build Cerebro instance and wire data feed, strategy, and analyzers."""
    bt_cfg = config['backtest']
    data_cfg = config['data']
    cerebro = bt.Cerebro(stdstats=True)
    cerebro.broker.setcash(bt_cfg['initial_cash'])
    comm_type = bt.CommInfoBase.COMM_FIXED if bt_cfg.get('commission_type', 'fixed') == 'fixed' else bt.CommInfoBase.COMM_PERC
    cerebro.broker.setcommission(
        commission=bt_cfg['commission'],
        margin=bt_cfg['margin'],
        mult=bt_cfg['multiplier'],
        commtype=comm_type,
        stocklike=bt_cfg.get('stocklike', False),
    )
    feed = Mt5PandasFeed(dataname=frame['data'], timeframe=bt.TimeFrame.Minutes, compression=15)
    cerebro.adddata(feed, name=f"{data_cfg['symbol']}_{data_cfg['timeframe']}")
    cerebro.addstrategy(MovingAverageCrossoverStrategy, **config.get('params', {}))
    add_default_analyzers(cerebro)
    return cerebro


def finite_or_none(value):
    """Convert NaN/inf values to ``None`` for stable numeric assertions."""
    if value is None:
        return None
    if isinstance(value, (int, float)) and not math.isfinite(value):
        return None
    return value


def format_metric(value, fmt='.2f'):
    """Format float/integer metrics for human-readable reporting."""
    if value is None:
        return 'N/A'
    return format(value, fmt)


def extract_metrics(strat, cerebro, frame, config):
    """Collect normalized strategy and analyzer outputs for regression assertions."""
    sharpe = strat.analyzers.sharpe.get_analysis()
    returns = strat.analyzers.returns.get_analysis()
    drawdown = strat.analyzers.drawdown.get_analysis()
    trades = strat.analyzers.trades.get_analysis()
    sqn = strat.analyzers.sqn.get_analysis()
    initial_cash = config['backtest']['initial_cash']
    final_value = cerebro.broker.getvalue()
    total_trades = trades.get('total', {}).get('total', 0)
    won = trades.get('won', {}).get('total', 0)
    lost = trades.get('lost', {}).get('total', 0)
    gross_won = trades.get('won', {}).get('pnl', {}).get('total', 0) or 0
    gross_lost = abs(trades.get('lost', {}).get('pnl', {}).get('total', 0) or 0)
    annual_return = returns.get('rnorm')
    return {
        'fromdate': frame['fromdate'],
        'todate': frame['todate'],
        'bars': len(frame['data']),
        'bar_num': strat.bar_num,
        'buy_count': strat.buy_count,
        'sell_count': strat.sell_count,
        'trade_count': strat.trade_count,
        'win_count': strat.win_count,
        'loss_count': strat.loss_count,
        'initial_cash': initial_cash,
        'final_value': final_value,
        'net_pnl': final_value - initial_cash,
        'total_return_pct': (final_value / initial_cash - 1.0) * 100.0,
        'total_trades': total_trades,
        'won': won,
        'lost': lost,
        'win_rate': (won / total_trades * 100.0) if total_trades else 0.0,
        'profit_factor': finite_or_none((gross_won / gross_lost) if gross_lost else None),
        'max_drawdown': drawdown.get('max', {}).get('drawdown', 0),
        'sharpe_ratio': finite_or_none(sharpe.get('sharperatio')),
        'annual_return_pct': finite_or_none(annual_return * 100.0 if annual_return is not None else None),
        'sqn': finite_or_none(sqn.get('sqn')),
        'use_martingale': config['params']['use_martingale'],
    }


def run(plot=False):
    """Run the inlined strategy backtest and return execution artifacts."""
    config = _bt_load_config(_CONFIG, repo=_REPO)
    frame = load_backtest_frame(config)
    cerebro = build_cerebro(config, frame)
    print('\nStarting backtest...')
    results = cerebro.run()
    strat = results[0]
    metrics = extract_metrics(strat, cerebro, frame, config)

    if plot:
        cerebro.plot()
    return results, metrics, cerebro


def _close(actual, expected, *, tol, key):
    """Assert ``actual`` is finite and within ``tol`` of ``expected``."""
    assert actual is not None, f"{key}: expected={expected}, got=None"
    a = float(actual)
    assert math.isfinite(a), f"{key}: expected={expected}, got non-finite {actual}"
    assert abs(a - float(expected)) <= tol, (
        f"{key}: expected={expected}, got={a} (tol={tol})"
    )


def _invoke_strategy_main():
    """Call main() or run() depending on what the original script defined."""
    import sys as _sys
    _mod = _sys.modules[__name__]
    if hasattr(_mod, "main") and callable(_mod.main):
        return _mod.main()
    if hasattr(_mod, "run") and callable(_mod.run):
        return _mod.run()
    raise RuntimeError("Neither main() nor run() found in inlined module")


