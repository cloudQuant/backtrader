def parse_dt(value):
    """Parse ISO datetime string or return ``None`` for empty input.

    Args:
        value (str | None): Raw datetime string.

    Returns:
        datetime.datetime | None: Parsed datetime object.
    """
    if not value:
        return None
    return datetime.datetime.fromisoformat(value)


