def resolve_ma_class(name):
    """Return the backtrader moving-average class for a method name.

    Args:
        name: MA method name (sma, ema, smma, or a weighted fallback).

    Returns:
        The corresponding backtrader moving-average indicator class.
    """
    mode = str(name).lower()
    if mode in {'sma', 'mode_sma'}:
        return bt.indicators.SimpleMovingAverage
    if mode in {'ema', 'mode_ema'}:
        return bt.indicators.ExponentialMovingAverage
    if mode in {'smma', 'mode_smma'}:
        return bt.indicators.SmoothedMovingAverage
    return bt.indicators.WeightedMovingAverage


def resolve_price_line(data, mode):
    """Return the price line selected by an MT5 applied-price mode.

    Args:
        data: The data feed providing OHLC lines.
        mode: Applied-price mode name (e.g. ``price_close``, ``price_median``).

    Returns:
        The line or line expression for the requested applied price; defaults
        to the close line for unrecognized modes.
    """
    price_mode = str(mode).lower()
    if price_mode in {'price_open', 'open'}:
        return data.open
    if price_mode in {'price_high', 'high'}:
        return data.high
    if price_mode in {'price_low', 'low'}:
        return data.low
    if price_mode in {'price_median', 'median'}:
        return (data.high + data.low) / 2.0
    if price_mode in {'price_typical', 'typical'}:
        return (data.high + data.low + data.close) / 3.0
    if price_mode in {'price_weighted', 'weighted'}:
        return (data.high + data.low + data.close + data.close) / 4.0
    return data.close


class CorrectedAverageStrategy(bt.Strategy):
    """Corrected-average band reversion strategy ported from the EA.

    Opens, closes, or reverses positions on price crossings of a level-offset
    band around the adaptive corrected average.
    """

    params = dict(
        ma_method='sma',
        length=12,
        applied_price='price_close',
        level=300,
        signal_bar=1,
        lot=0.1,
    )

    def __init__(self):
        """Instantiate the corrected-average indicator and reset counters."""
        self.indicator = bt.indicators.CorrectedAverageIndicator(
            self.data,
            ma_method=self.p.ma_method,
            length=self.p.length,
            applied_price=self.p.applied_price,
        )
        self.bar_num = 0
        self.buy_count = 0
        self.sell_count = 0
        self.trade_count = 0
        self.win_count = 0
        self.loss_count = 0
        self._position_was_open = False
        self._level = float(self.p.level) * 0.01

    def log(self, text):
        """Print a log message prefixed with the current bar timestamp.

        Args:
            text: Message to log.
        """
        dt = bt.num2date(self.data.datetime[0])
        print(f'{dt.isoformat()}, {text}')

    def _signals(self):
        """Detect band-crossing buy/sell open/close signals.

        Returns:
            Tuple of (buy_open, sell_open, buy_close, sell_close) booleans.
        """
        shift = max(1, int(self.p.signal_bar))
        close_prev = float(self.data.close[-shift])
        close_cur = float(self.data.close[-shift + 1]) if shift > 1 else float(self.data.close[0])
        value_prev = float(self.indicator.corrected[-shift])
        value_cur = float(self.indicator.corrected[-shift + 1]) if shift > 1 else float(self.indicator.corrected[0])
        buy_open = False
        sell_open = False
        buy_close = False
        sell_close = False
        if close_prev > value_prev + self._level:
            if close_cur <= value_cur + self._level:
                buy_open = True
            sell_close = True
        if close_prev < value_prev - self._level:
            if close_cur >= value_cur - self._level:
                sell_open = True
            buy_close = True
        return buy_open, sell_open, buy_close, sell_close

    def next(self):
        """Open, close, or reverse positions on corrected-average band crossings."""
        self.bar_num += 1
        warmup = int(self.p.length) + int(self.p.signal_bar) + 10
        if len(self.data) < warmup:
            return
        buy_open, sell_open, buy_close, sell_close = self._signals()
        value = float(self.indicator.corrected[0])
        if self.position:
            if self.position.size > 0:
                if buy_close and not sell_open:
                    self.log(f'close long value={value:.4f}')
                    self.close()
                    return
                if sell_open:
                    self.log(f'close long & sell value={value:.4f}')
                    self.close()
                    self.sell(size=self.p.lot)
                    return
            if self.position.size < 0:
                if sell_close and not buy_open:
                    self.log(f'close short value={value:.4f}')
                    self.close()
                    return
                if buy_open:
                    self.log(f'close short & buy value={value:.4f}')
                    self.close()
                    self.buy(size=self.p.lot)
                    return
        else:
            if buy_open:
                self.log(f'buy value={value:.4f}')
                self.buy(size=self.p.lot)
                return
            if sell_open:
                self.log(f'sell value={value:.4f}')
                self.sell(size=self.p.lot)
                return

    def notify_trade(self, trade):
        """Tally opened and closed trades into win/loss counts.

        Args:
            trade: The trade whose status changed.
        """
        if trade.isopen and not self._position_was_open:
            if trade.size > 0:
                self.buy_count += 1
            elif trade.size < 0:
                self.sell_count += 1
            self._position_was_open = True
            return
        if not trade.isclosed:
            return
        self.trade_count += 1
        if trade.pnlcomm >= 0:
            self.win_count += 1
        else:
            self.loss_count += 1
        self._position_was_open = False
        self.log(f'trade closed pnl={trade.pnlcomm:.2f}')


