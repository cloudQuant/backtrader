class OpenTimeStrategy(bt.Strategy):
    """Time-of-day session strategy: enter at a trade time, flat at a close time."""

    params = dict(
        time_close=True,
        close_hour=20,
        close_minute=50,
        trailing=False,
        trailing_stop=30,
        trailing_step=3,
        trade_hour=18,
        trade_minute=50,
        duration=300,
        use_sell=True,
        use_buy=False,
        lots=0.1,
        stop_loss=0,
        take_profit=0,
        point=0.01,
        price_digits=2,
    )

    def __init__(self):
        """Initialize bar, signal, order, risk-price, and session-window state."""
        self.bar_num = 0
        self.signal_count = 0
        self.buy_count = 0
        self.sell_count = 0
        self.trade_count = 0
        self.win_count = 0
        self.loss_count = 0
        self.completed_order_count = 0
        self.rejected_order_count = 0
        self.order = None
        self.stop_price = None
        self.take_profit_price = None
        self.last_open_key = None

    def _point(self):
        return float(self.p.point)

    def _round(self, value):
        return round(float(value), int(self.p.price_digits))

    def _window_key(self, dt):
        return f"{dt.date().isoformat()}-{dt.hour:02d}:{dt.minute:02d}"

    def _arm(self, direction, price):
        sl = float(self.p.stop_loss) * self._point()
        tp = float(self.p.take_profit) * self._point()
        if direction == 'buy':
            self.stop_price = self._round(price - sl) if sl > 0 else None
            self.take_profit_price = self._round(price + tp) if tp > 0 else None
            self.signal_count += 1
            self.order = self.buy(size=float(self.p.lots))
        else:
            self.stop_price = self._round(price + sl) if sl > 0 else None
            self.take_profit_price = self._round(price - tp) if tp > 0 else None
            self.signal_count += 1
            self.order = self.sell(size=float(self.p.lots))

    def _manage_position(self):
        if not self.position or self.order is not None:
            return
        high = float(self.data.high[0])
        low = float(self.data.low[0])
        ts = float(self.p.trailing_stop) * self._point()
        step = float(self.p.trailing_step) * self._point()
        if bool(self.p.trailing) and ts > 0:
            if self.position.size > 0 and float(self.data.close[0]) - float(self.position.price) > ts + step:
                new_sl = self._round(float(self.data.close[0]) - ts)
                if self.stop_price is None or new_sl > float(self.stop_price):
                    self.stop_price = new_sl
            if self.position.size < 0 and float(self.position.price) - float(self.data.close[0]) > ts + step:
                new_sl = self._round(float(self.data.close[0]) + ts)
                if self.stop_price is None or new_sl < float(self.stop_price):
                    self.stop_price = new_sl
        if self.position.size > 0:
            if self.take_profit_price is not None and high >= float(self.take_profit_price):
                self.order = self.close(); return
            if self.stop_price is not None and low <= float(self.stop_price):
                self.order = self.close(); return
        else:
            if self.take_profit_price is not None and low <= float(self.take_profit_price):
                self.order = self.close(); return
            if self.stop_price is not None and high >= float(self.stop_price):
                self.order = self.close(); return

    def next(self):
        """Apply time-based close, manage exits, and enter at the trade time."""
        self.bar_num += 1
        dt = self.data.datetime.datetime(0)
        if self.order is not None:
            return
        if bool(self.p.time_close) and dt.hour == int(self.p.close_hour) and dt.minute == int(self.p.close_minute) and self.position:
            self.order = self.close()
            return
        self._manage_position()
        if self.order is not None or self.position:
            return
        if dt.hour == int(self.p.trade_hour) and dt.minute == int(self.p.trade_minute):
            key = self._window_key(dt)
            if self.last_open_key == key:
                return
            self.last_open_key = key
            if bool(self.p.use_buy):
                self._arm('buy', float(self.data.close[0]))
                return
            if bool(self.p.use_sell):
                self._arm('sell', float(self.data.close[0]))

    def notify_order(self, order):
        """Track completed, failed, and rejected orders and reset risk prices when flat.

        Args:
            order: The order object emitted by Backtrader for lifecycle updates.

        Side effects:
            - Increments `completed_order_count` for submitted-completed fills.
            - Increments `buy_count`/`sell_count` when entries are recorded.
            - Increments `rejected_order_count` for cancel/margin/rejected/expired.
            - Clears pending order state after terminal statuses.
            - Resets take-profit and stop-loss caches once the strategy is flat.
        """
        if order.status in [bt.Order.Submitted, bt.Order.Accepted]:
            return
        if order.status == bt.Order.Completed:
            self.completed_order_count += 1
            if self.position:
                if order.executed.size > 0:
                    self.buy_count += 1
                elif order.executed.size < 0:
                    self.sell_count += 1
            else:
                self.stop_price = None
                self.take_profit_price = None
        elif order.status in [bt.Order.Canceled, bt.Order.Margin, bt.Order.Rejected, bt.Order.Expired]:
            self.rejected_order_count += 1
        if self.order is not None and order.ref == self.order.ref and order.status not in [bt.Order.Submitted, bt.Order.Accepted]:
            self.order = None

    def notify_trade(self, trade):
        """Update trade counters after a closed trade event.

        Args:
            trade: The trade object from Backtrader, expected to expose `isclosed`
                and P&L (`pnlcomm`).

        Side effects:
            - Increments `trade_count` when the trade is closed.
            - Increments `win_count` when net P&L is non-negative.
            - Increments `loss_count` when net P&L is negative.
        """
        if not trade.isclosed:
            return
        self.trade_count += 1
        if trade.pnlcomm >= 0:
            self.win_count += 1
        else:
            self.loss_count += 1


