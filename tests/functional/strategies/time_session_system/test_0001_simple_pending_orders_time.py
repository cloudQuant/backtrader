class SimplePendingOrdersTimeStrategy(bt.Strategy):
    """Strategy class implementing session breakout setups using pending stop orders.

    Attributes:
        params (dict): Configured strategy parameters.
    """
    params = dict(
        trading_start=15,
        end_of_trade=16,
        indent=100,
        stop_loss=200,
        lots=0.1,
        magic_number=9462,
        point=0.01,
        price_digits=2,
    )

    def __init__(self):
        """Initialize indicators, backtest tracking metrics, and state variables."""
        self.buy_stop = None
        self.sell_stop = None
        self.last_setup_day = None
        self.bar_num = 0
        self.buy_count = 0
        self.sell_count = 0
        self.trade_count = 0
        self.win_count = 0
        self.loss_count = 0
        self._position_was_open = False

    def log(self, text):
        """Log message with current bar's timestamp.

        Args:
            text (str): Content string to log.
        """
        dt = bt.num2date(self.data.datetime[0])
        print(f'{dt.isoformat()}, {text}')

    def _round_price(self, value):
        """Round price value to configured price_digits parameter.

        Args:
            value (float): Price value.

        Returns:
            float: Rounded price value.
        """
        return round(value, self.p.price_digits)

    def _can_trade(self, dt):
        """Check if the provided datetime falls within the active trading session window.

        Args:
            dt (datetime.datetime): Current candle timestamp.

        Returns:
            bool: True if inside the window, otherwise False.
        """
        if self.p.trading_start > self.p.end_of_trade:
            return dt.hour >= self.p.trading_start or dt.hour < self.p.end_of_trade
        return self.p.trading_start <= dt.hour < self.p.end_of_trade

    def _cancel_pending_orders(self):
        """Cancel both active buy_stop and sell_stop pending breakout orders."""
        for order in (self.buy_stop, self.sell_stop):
            if order is not None and order.alive():
                self.cancel(order)
        self.buy_stop = None
        self.sell_stop = None

    def _close_all_positions(self):
        """Perform market closing on any active open position."""
        if self.position:
            self.close()
            self.log('forced close outside trade window')

    def _set_pending_orders(self):
        """Place buy stop and sell stop breakout pending orders at indent distance from current price."""
        ask = float(self.data.close[0])
        bid = float(self.data.close[0])
        buy_price = self._round_price(ask + self.p.indent * self.p.point)
        sell_price = self._round_price(bid - self.p.indent * self.p.point)
        buy_sl = self._round_price(buy_price - self.p.stop_loss * self.p.point)
        sell_sl = self._round_price(sell_price + self.p.stop_loss * self.p.point)
        self.buy_stop = self.buy(size=self.p.lots, exectype=bt.Order.Stop, price=buy_price, valid=None)
        self.sell_stop = self.sell(size=self.p.lots, exectype=bt.Order.Stop, price=sell_price, valid=None)
        self.log(f'set pending buy_stop={buy_price:.2f} sl={buy_sl:.2f} sell_stop={sell_price:.2f} sl={sell_sl:.2f}')

    def next(self):
        """Execute the strategy decision logic on each new bar."""
        self.bar_num += 1
        dt = bt.num2date(self.data.datetime[0])
        in_window = self._can_trade(dt)
        if not in_window:
            self._close_all_positions()
            self._cancel_pending_orders()
            return
        if dt.hour == self.p.trading_start and dt.minute == 0 and self.last_setup_day != dt.date():
            if not self.position and self.buy_stop is None and self.sell_stop is None:
                self._set_pending_orders()
                self.last_setup_day = dt.date()

    def notify_order(self, order):
        """Handle order life cycle updates and manage protective stop loss linkages.

        Args:
            order (bt.Order): Updated order instance.
        """
        if order.status in (order.Submitted, order.Accepted):
            return
        if order is self.buy_stop:
            if order.status == order.Completed:
                self.buy_count += 1
                buy_sl = self._round_price(order.executed.price - self.p.stop_loss * self.p.point)
                self.sell_stop = self.sell_stop if self.sell_stop and self.sell_stop.alive() else None
                self.log(f'buy stop filled price={order.executed.price:.2f} sl={buy_sl:.2f}')
                if self.position.size > 0:
                    self.sell(size=abs(self.position.size), exectype=bt.Order.Stop, price=buy_sl)
                self.buy_stop = None
            elif order.status in (order.Canceled, order.Margin, order.Rejected, order.Expired):
                self.buy_stop = None
            return
        if order is self.sell_stop:
            if order.status == order.Completed:
                self.sell_count += 1
                sell_sl = self._round_price(order.executed.price + self.p.stop_loss * self.p.point)
                self.buy_stop = self.buy_stop if self.buy_stop and self.buy_stop.alive() else None
                self.log(f'sell stop filled price={order.executed.price:.2f} sl={sell_sl:.2f}')
                if self.position.size < 0:
                    self.buy(size=abs(self.position.size), exectype=bt.Order.Stop, price=sell_sl)
                self.sell_stop = None
            elif order.status in (order.Canceled, order.Margin, order.Rejected, order.Expired):
                self.sell_stop = None
            return

    def notify_trade(self, trade):
        """Callback to handle closed trades and manage win/loss counts.

        Args:
            trade (bt.Trade): The closed trade instance.
        """
        if trade.isopen and not self._position_was_open:
            if trade.size > 0:
                self.buy_count += 1
                self.log(f'buy stop filled price={trade.price:.2f}')
            elif trade.size < 0:
                self.sell_count += 1
                self.log(f'sell stop filled price={trade.price:.2f}')
            self._position_was_open = True
            return
        if not trade.isclosed:
            return
        self.trade_count += 1
        if trade.pnlcomm >= 0:
            self.win_count += 1
        else:
            self.loss_count += 1
        self._position_was_open = False
        self.log(f'trade closed pnl={trade.pnlcomm:.2f}')


BASE_DIR = Path(__file__).resolve().parent

MINUTES_PER_TRADING_YEAR = 24 * 60 * 252


def resolve_data_path(filename):
    """Resolve data file path relative to BASE_DIR and ensure it exists.

    Args:
        filename (str): Name or path of data file.

    Raises:
        FileNotFoundError: If the data file does not exist.

    Returns:
        Path: Resolved absolute path.
    """
    path = (BASE_DIR / filename).resolve()
    if not path.exists():
        raise FileNotFoundError(f'Data file not found: {path}')
    return path


def load_backtest_frame(config):
    """Load high-frequency M15 gold data.

    Args:
        config (dict): Configuration dictionary.

    Raises:
        ValueError: If the loaded data frame is empty.

    Returns:
        dict: Loaded data frame dictionary containing base bars.
    """
    data_cfg = config['data']
    fromdate = datetime.datetime.fromisoformat(data_cfg['fromdate'])
    todate = datetime.datetime.fromisoformat(data_cfg['todate'])
    df = load_mt5_csv(
        resolve_data_path(data_cfg['file']),
        fromdate=fromdate,
        todate=todate,
        bar_shift_minutes=data_cfg.get('bar_shift_minutes', 0),
    )
    if df.empty:
        raise ValueError('Loaded data frame is empty')
    print(f"Loaded {len(df)} bars: {df.index[0]} -> {df.index[-1]}")
    return {'data': df, 'fromdate': fromdate, 'todate': todate}


def add_default_analyzers(cerebro):
    """Add standard performance analyzers to the backtest cerebro engine.

    Args:
        cerebro (bt.Cerebro): Backtest Cerebro engine.
    """
    cerebro.addanalyzer(bt.analyzers.SharpeRatio, _name='sharpe', timeframe=bt.TimeFrame.Minutes, factor=MINUTES_PER_TRADING_YEAR, annualize=True, riskfreerate=0)
    cerebro.addanalyzer(bt.analyzers.Returns, _name='returns', timeframe=bt.TimeFrame.Minutes, compression=1, tann=MINUTES_PER_TRADING_YEAR)
    cerebro.addanalyzer(bt.analyzers.DrawDown, _name='drawdown')
    cerebro.addanalyzer(bt.analyzers.TradeAnalyzer, _name='trades')
    cerebro.addanalyzer(bt.analyzers.SQN, _name='sqn')


def build_cerebro(config, frame):
    """Construct and configure Cerebro instance with feed, analyzers and strategies.

    Args:
        config (dict): Backtest configuration.
        frame (dict): Loaded and processed data frame dictionary.

    Returns:
        bt.Cerebro: Configured Cerebro backtest engine.
    """
    bt_cfg = config['backtest']
    cerebro = bt.Cerebro(stdstats=True)
    cerebro.broker.setcash(bt_cfg['initial_cash'])
    comm_type = bt.CommInfoBase.COMM_FIXED if bt_cfg.get('commission_type', 'fixed') == 'fixed' else bt.CommInfoBase.COMM_PERC
    cerebro.broker.setcommission(
        commission=bt_cfg['commission'],
        margin=bt_cfg['margin'],
        mult=bt_cfg['multiplier'],
        commtype=comm_type,
        stocklike=bt_cfg.get('stocklike', False),
    )
    feed = Mt5PandasFeed(dataname=frame['data'], timeframe=bt.TimeFrame.Minutes, compression=1)
    cerebro.adddata(feed, name=f"{config['data']['symbol']}_{config['data']['timeframe']}")
    cerebro.addstrategy(SimplePendingOrdersTimeStrategy, **config.get('params', {}))
    add_default_analyzers(cerebro)
    return cerebro


def extract_metrics(strat, cerebro, frame, config):
    """Extract backtest results, returns, Sharpe ratio, and drawdowns.

    Args:
        strat (bt.Strategy): Run strategy instance containing observers/analyzers.
        cerebro (bt.Cerebro): Backtest Cerebro engine.
        frame (dict): Loaded data frame dictionary.
        config (dict): Strategy and backtest configuration dictionary.

    Returns:
        dict: Performance and trade metrics dict.
    """
    sharpe = strat.analyzers.sharpe.get_analysis()
    returns = strat.analyzers.returns.get_analysis()
    drawdown = strat.analyzers.drawdown.get_analysis()
    trades = strat.analyzers.trades.get_analysis()
    sqn = strat.analyzers.sqn.get_analysis()
    initial_cash = config['backtest']['initial_cash']
    final_value = cerebro.broker.getvalue()
    total_trades = trades.get('total', {}).get('total', 0)
    won = trades.get('won', {}).get('total', 0)
    lost = trades.get('lost', {}).get('total', 0)
    gross_won = trades.get('won', {}).get('pnl', {}).get('total', 0) or 0
    gross_lost = abs(trades.get('lost', {}).get('pnl', {}).get('total', 0) or 0)
    return {
        'fromdate': frame['fromdate'],
        'todate': frame['todate'],
        'bars': len(frame['data']),
        'bar_num': strat.bar_num,
        'buy_count': strat.buy_count,
        'sell_count': strat.sell_count,
        'trade_count': strat.trade_count,
        'win_count': strat.win_count,
        'loss_count': strat.loss_count,
        'initial_cash': initial_cash,
        'final_value': final_value,
        'net_pnl': final_value - initial_cash,
        'total_return_pct': (final_value / initial_cash - 1.0) * 100.0,
        'total_trades': total_trades,
        'won': won,
        'lost': lost,
        'win_rate': (won / total_trades * 100.0) if total_trades else 0.0,
        'profit_factor': (gross_won / gross_lost) if gross_lost else None,
        'max_drawdown': drawdown.get('max', {}).get('drawdown', 0),
        'sharpe_ratio': sharpe.get('sharperatio'),
        'annual_return_pct': (returns.get('rnorm') or 0.0) * 100.0,
        'sqn': sqn.get('sqn'),
    }


def print_report(metrics):
    """Print the backtest metrics report.

    Args:
        metrics (dict): Performance metrics.
    """
    for k, v in metrics.items():
        print(f'{k}: {v}')


def run(plot=False):
    """Execute the full backtest workflow.

    Args:
        plot (bool, optional): Whether to plot results. Defaults to False.

    Returns:
        tuple: (results, metrics, cerebro) instances.
    """
    config = _bt_load_config(_CONFIG, repo=_REPO)
    frame = load_backtest_frame(config)
    cerebro = build_cerebro(config, frame)
    print('\nStarting backtest...')
    results = cerebro.run()
    strat = results[0]
    metrics = extract_metrics(strat, cerebro, frame, config)
    print_report(metrics)

    if plot:
        cerebro.plot()
    return results, metrics, cerebro


def _close(actual, expected, *, tol, key):
    """Assert ``actual`` is finite and within ``tol`` of ``expected``.

    Args:
        actual (float): Calculated actual value.
        expected (float): Baseline target value.
        tol (float): Precision tolerance.
        key (str): Label for target value.
    """
    assert actual is not None, f"{key}: expected={expected}, got=None"
    a = float(actual)
    assert math.isfinite(a), f"{key}: expected={expected}, got non-finite {actual}"
    assert abs(a - float(expected)) <= tol, (
        f"{key}: expected={expected}, got={a} (tol={tol})"
    )


