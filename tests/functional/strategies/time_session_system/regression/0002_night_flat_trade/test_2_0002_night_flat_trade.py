def load_mt5_csv(filepath: str, fromdate=None, todate=None, bar_shift_minutes: int = 0):
    with open(filepath, 'r', encoding='utf-8') as f:
        lines = f.read().strip().split('\n')
    cleaned = '\n'.join(line.strip().strip('"') for line in lines if line.strip())
    df = pd.read_csv(io.StringIO(cleaned), sep='\t')
    df['datetime'] = pd.to_datetime(df['<DATE>'] + ' ' + df['<TIME>'], format='%Y.%m.%d %H:%M:%S')
    df = df.rename(columns={
        '<OPEN>': 'open',
        '<HIGH>': 'high',
        '<LOW>': 'low',
        '<CLOSE>': 'close',
        '<TICKVOL>': 'volume',
        '<VOL>': 'openinterest',
        '<SPREAD>': 'spread',
    })
    df = df[['datetime', 'open', 'high', 'low', 'close', 'volume', 'openinterest', 'spread']]
    df = df.set_index('datetime').sort_index()
    if bar_shift_minutes:
        df.index = df.index + pd.Timedelta(minutes=bar_shift_minutes)
    if fromdate is not None:
        df = df[df.index >= fromdate]
    if todate is not None:
        df = df[df.index <= todate]
    return df



def finite_or_none(value):
    if value is None:
        return None
    if isinstance(value, (int, float)) and not math.isfinite(value):
        return None
    return value


def extract_metrics(results, cerebro, frame, initial_cash):
    strat = results[0]
    end_value = cerebro.broker.getvalue()
    drawdown = strat.analyzers.drawdown.get_analysis()
    returns = strat.analyzers.returns.get_analysis()
    sharpe = strat.analyzers.sharpe.get_analysis()
    sqn = strat.analyzers.sqn.get_analysis()
    trades = strat.analyzers.trades.get_analysis()
    total_closed = trades.get('total', {}).get('closed', 0)
    won = trades.get('won', {}).get('total', 0)
    lost = trades.get('lost', {}).get('total', 0)
    gross_won = trades.get('won', {}).get('pnl', {}).get('total', 0) or 0
    gross_lost = abs(trades.get('lost', {}).get('pnl', {}).get('total', 0) or 0)
    return {
        'bars': len(frame),
        'buy_count': strat.buy_count,
        'sell_count': strat.sell_count,
        'trade_count': strat.trade_count,
        'win_count': strat.win_count,
        'loss_count': strat.loss_count,
        'initial_cash': initial_cash,
        'final_value': end_value,
        'net_pnl': end_value - initial_cash,
        'total_return_pct': (end_value / initial_cash - 1.0) * 100.0,
        'total_trades': total_closed,
        'won': won,
        'lost': lost,
        'win_rate': (won / total_closed * 100.0) if total_closed else 0.0,
        'profit_factor': (gross_won / gross_lost) if gross_lost else None,
        'max_drawdown': drawdown.get('max', {}).get('drawdown', 0.0),
        'sharpe_ratio': finite_or_none(sharpe.get('sharperatio')),
        'annual_return_pct': (returns.get('rnorm') or 0.0) * 100.0,
        'sqn': finite_or_none(sqn.get('sqn')),
    }


def run(cfg_path: str | None = None):
    if cfg_path is None:
        cfg_path = os.path.join(SCRIPT_DIR, 'config.yaml')
    cfg = load_config(cfg_path)

    data_cfg = cfg['data']
    params_cfg = cfg.get('params', {})
    bt_cfg = cfg.get('backtest', {})

    data_file = data_cfg['file']
    if not os.path.isabs(data_file):
        data_file = os.path.normpath(os.path.join(SCRIPT_DIR, data_file))
    if not os.path.exists(data_file):
        raise FileNotFoundError(f'Data file not found: {data_file}')

    fromdate = dt.datetime.strptime(data_cfg['fromdate'], '%Y-%m-%d %H:%M:%S')
    todate = dt.datetime.strptime(data_cfg['todate'], '%Y-%m-%d %H:%M:%S')
    frame = load_mt5_csv(data_file, fromdate=fromdate, todate=todate, bar_shift_minutes=data_cfg.get('bar_shift_minutes', 0))
    if frame.empty:
        raise ValueError('Loaded data frame is empty')

    cerebro = bt.Cerebro()
    data0 = Mt5PandasFeed(dataname=frame.copy(), timeframe=bt.TimeFrame.Minutes, compression=1)
    h1_source = Mt5PandasFeed(dataname=frame.copy(), timeframe=bt.TimeFrame.Minutes, compression=1)
    cerebro.adddata(data0, name='XAUUSD_M1')
    cerebro.resampledata(h1_source, timeframe=bt.TimeFrame.Minutes, compression=60, name='XAUUSD_H1')

    strat_kwargs = {}
    for key in (
        'take_profit_pips', 'trailing_stop_pips', 'trailing_step_pips',
        'diff_min_pips', 'diff_max_pips', 'open_hour', 'lots', 'risk',
        'margin_per_lot', 'point',
    ):
        if key in params_cfg:
            strat_kwargs[key] = params_cfg[key]

    cerebro.addstrategy(NightFlatTradeStrategy, **strat_kwargs)

    trade_log_root = os.environ.get('BT_TRADE_LOG_DIR', '').strip()
    if trade_log_root:
        log_dir = os.path.join(trade_log_root, 'python')
        os.makedirs(log_dir, exist_ok=True)
        cerebro.addobserver(
            bt.observers.TradeLogger,
            log_dir=log_dir,
            log_orders=True,
            log_trades=True,
            log_positions=True,
            log_indicators=True,
            log_signals=True,
            log_position_snapshot=False,
            log_format='json',
        )

    cerebro.broker.setcash(bt_cfg.get('initial_cash', 100000.0))
    cerebro.broker.setcommission(
        commission=bt_cfg.get('commission', 0.0),
        margin=bt_cfg.get('margin', 1000.0),
        mult=bt_cfg.get('multiplier', 100.0),
        stocklike=bt_cfg.get('stocklike', False),
    )
    cerebro.addanalyzer(bt.analyzers.SharpeRatio, _name='sharpe', timeframe=bt.TimeFrame.Minutes, compression=1, factor=MINUTES_PER_TRADING_YEAR, annualize=True, riskfreerate=0)
    cerebro.addanalyzer(bt.analyzers.Returns, _name='returns', timeframe=bt.TimeFrame.Minutes, compression=1, tann=MINUTES_PER_TRADING_YEAR)
    cerebro.addanalyzer(bt.analyzers.DrawDown, _name='drawdown')
    cerebro.addanalyzer(bt.analyzers.TradeAnalyzer, _name='trades')
    cerebro.addanalyzer(bt.analyzers.SQN, _name='sqn')

    print(f'数据文件:  {data_file}')
    print(f'信号周期:  {data_cfg.get("signal_timeframe", "H1")}')
    print(f'回测区间:  {fromdate} ~ {todate}')
    print(f'初始资金:  {cerebro.broker.getvalue():.2f}')
    print('-' * 50)

    initial_cash = cerebro.broker.getvalue()
    results = cerebro.run()
    final_value = cerebro.broker.getvalue()
    metrics = extract_metrics(results, cerebro, frame, initial_cash)
    print(f'\n最终权益: {final_value:.2f}')
    return results, metrics, cerebro


def _close(actual, expected, *, tol, key):
    """Assert ``actual`` is finite and within ``tol`` of ``expected``."""
