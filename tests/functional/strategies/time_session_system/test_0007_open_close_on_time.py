class OpenCloseOnTimeStrategy(bt.Strategy):
    """Time-schedule strategy that opens and closes a position at fixed clocks.

    Each day it enters a fixed-size long (or short) on the first bar at or after
    a configured open time and flattens on the first bar at or after a
    configured close time, with no market signal involved.
    """

    params = dict(
        open_time='13:00',
        close_time='13:01',
        lots=1.0,
        buy=True,
        point=0.01,
        price_digits=2,
    )

    def __init__(self):
        """Reset counters/order state and parse the open/close schedule clocks."""
        self.bar_num = 0
        self.buy_count = 0
        self.sell_count = 0
        self.trade_count = 0
        self.win_count = 0
        self.loss_count = 0
        self.entry_order = None
        self._position_was_open = False
        self.open_clock = self._parse_clock(self.p.open_time)
        self.close_clock = self._parse_clock(self.p.close_time)
        self.last_open_day = None
        self.last_close_day = None

    def log(self, text):
        """Print a timestamped log line using the data feed's current datetime."""
        dt = bt.num2date(self.data.datetime[0])
        print(f'{dt.isoformat()}, {text}')

    def _parse_clock(self, text):
        """Parse an ``'HH:MM'`` string into a :class:`datetime.time`.

        Args:
            text: Clock string such as ``'13:00'``.

        Returns:
            The corresponding :class:`datetime.time` value.
        """
        hour, minute = [int(part) for part in text.strip().split(':')]
        return datetime.time(hour=hour, minute=minute)

    def _is_first_bar_after(self, current_dt, target_clock, last_day):
        """Return True on the first bar of the day that reaches ``target_clock``.

        Args:
            current_dt: Datetime of the current bar.
            target_clock: Scheduled time to cross.
            last_day: Date on which this schedule last fired (debounce guard).

        Returns:
            True if the current bar is the first to reach the target time today.
        """
        if current_dt.date() == last_day:
            return False
        current_clock = current_dt.time().replace(second=0, microsecond=0)
        if current_clock < target_clock:
            return False
        if len(self.data) < 2:
            return True
        previous_dt = bt.num2date(self.data.datetime[-1])
        previous_clock = previous_dt.time().replace(second=0, microsecond=0)
        if previous_dt.date() != current_dt.date():
            return True
        return previous_clock < target_clock <= current_clock

    def next(self):
        """Open at the scheduled open time and close at the scheduled close time."""
        self.bar_num += 1
        dt = bt.num2date(self.data.datetime[0])

        if self.entry_order is not None:
            return

        if self.position:
            should_close = self._is_first_bar_after(dt, self.close_clock, self.last_close_day)
            if should_close:
                self.last_close_day = dt.date()
                self.log('close position by schedule')
                self.entry_order = self.close()
            return

        should_open = self._is_first_bar_after(dt, self.open_clock, self.last_open_day)
        if not should_open:
            return

        self.last_open_day = dt.date()
        if self.p.buy:
            self.log('open buy by schedule')
            self.entry_order = self.buy(size=self.p.lots)
        else:
            self.log('open sell by schedule')
            self.entry_order = self.sell(size=self.p.lots)

    def notify_order(self, order):
        """Count filled entries/exits and clear the entry-order slot on settlement."""
        if order.status in [bt.Order.Submitted, bt.Order.Accepted]:
            return
        if order.status == bt.Order.Completed:
            if self.position:
                if order.executed.size > 0:
                    self.buy_count += 1
                elif order.executed.size < 0:
                    self.sell_count += 1
                self.log(f'entry filled price={order.executed.price:.2f} size={order.executed.size:.2f}')
            else:
                self.log(f'position closed price={order.executed.price:.2f} size={order.executed.size:.2f}')
        elif order.status in [bt.Order.Canceled, bt.Order.Margin, bt.Order.Rejected]:
            self.log(f'order failed status={order.getstatusname()}')
        if self.entry_order is not None and order.ref == self.entry_order.ref and order.status not in [bt.Order.Submitted, bt.Order.Accepted]:
            self.entry_order = None

    def notify_trade(self, trade):
        """Track position opening and tally win/loss outcomes on close."""
        if trade.isopen and not self._position_was_open:
            self._position_was_open = True
            return
        if not trade.isclosed:
            return
        self.trade_count += 1
        if trade.pnlcomm >= 0:
            self.win_count += 1
        else:
            self.loss_count += 1
        self._position_was_open = False
        self.log(f'trade closed pnl={trade.pnlcomm:.2f}')


